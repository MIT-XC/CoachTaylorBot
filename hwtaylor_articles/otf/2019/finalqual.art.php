                  <h3>Reduker Adds Long Jump to NCAA Plate           </h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>May 16, 2019              </p>
              <p>No less than five personal bests were set by MIT Track &amp; Field athletes at their own <a href="finalqual.res.pdf">MIT Final Qualifier</a>.&nbsp; The NCAA has to approve any meet that is conducted in the final week prior to the NCAA Division III Championship.&nbsp; MIT has been the regional host for the third year in succession.&nbsp; To enter the meet a competitor needs&nbsp;to be approximately ranked among the top 100 in the country for Division III.&nbsp; This guarantees a high quality, but relatively small meet that lasts three hours&nbsp;or so.</p>
              <p>Alec Reduker '20, already tied for first in the High Jump with his 7-0 1/4 jump, added the Long Jump to his NCAA repertoire by jumping a huge personal best 23-8 3/4 to move up to 16th on the NCAA list and fourth on the all-time MIT list.&nbsp; The top 20 marks in each event qualify for the championship for the men and the top 22 for the women.</p>
              <p>Liam Ackerman&nbsp;'21 cleared 16-3 1/2 to win the Pole Vault, moving up to #6 on the NCAA list.&nbsp; Ackerman made&nbsp;a great attempt at 16-7, narrowly missing it.</p>
              <p>Another amazing personal best was had by Will Woltmann '22.&nbsp; The freshman thrower put the shot 52-5 1/4 to win the event&nbsp;and move up to 27th in the country, as well as fifth on the MIT all-time list.&nbsp; A couple of fouls were much further, letting everyone know big things will happen in the years to come.</p>
              <p>Benton Wilson '21&nbsp;wanted to qualify in the 400 or 200 and ended up racing both events.&nbsp; In the 400 he easily ran his best 400 ever, pulling away from the field with a very fast 48.33, which ranks him 29th among all Division III runners and now fifth on the all-time MIT list.&nbsp; A relatively short time later he won the 200, but could only muster a 21.92, short of what he needs to make the NCAA field.</p>
              <p>The women's 800 was the premier event of the evening.&nbsp; Bailey Tregoning '19, already with&nbsp;a solid time of 2:11.01 agreed to help teammate Margaret Trautner '20 secure a faster time by taking out the pace.&nbsp;The reason Tregoning does not lead is that she tends to run too fast.&nbsp; So going out in 61.8 was not a total surprise.&nbsp; Trautner was hanging back in third, still fast at 63.0.&nbsp; Tregoning continued her pace, coming through the 600 in 1:34.&nbsp; Trautner moved into second and was trying to make up ground, but was still 20 meters back.&nbsp; In the final 100 the wheels came off of Tregoning's proverbial bus, but she still finished second in 2:11.57.&nbsp; Trautner finished third in 2:12.03, an outdoor personal best that moves her to 14th on the NCAA list and number two on the all-time MIT list.</p>
              <p>Also of note, Liana Reilly '21 matched her season best of 5-4 1/4 in the High Jump.&nbsp; She made a great attempt at 5-7, but just displaced the bar.</p>
              <p>The Engineers are below their normal numbers they send to the NCAA Division III Outdoor Track &amp; Field championship, mostly due to injury and fatigue.&nbsp; However, they do have a representative group.&nbsp; The following are making the trip.</p>
              <p>Liam Ackerman '21&nbsp; Pole Vault - 6th - 16-3 1/2</p>
              <p>Aidan Gilson '19&nbsp; 3,000 Steeplechase - 14th - 9:08.03</p>
              <p>Gabe Madonna '20&nbsp; Javelin - 13th - 197-6</p>
              <p>Alec Reduker '20&nbsp; High Jump - 2nd - 78-0 1/4, Long Jump - 16th - 23-8 3/4</p>
              <p>Yorai Shaoul '21&nbsp; Triple Jump - 1st - 49-9, Long Jump - 5th - 24-1 3/4</p>
              <p>Katie Collins '21&nbsp; 5K - 11th - 17:01.24</p>
              <p>Jenna Melanson '20&nbsp; 3,000 Steeplechase - 6th - 10:47.87</p>
              <p>Kari Stromhaug&nbsp;'19&nbsp; Pole Vault - 14th - 12-4 3/4</p>
              <p>Margaret Trautner '20&nbsp; 800 - 14th - 2:12.03</p>
              <p>Bailey Tregoning '19&nbsp;800 - 9th - 2:11.01              </p>
