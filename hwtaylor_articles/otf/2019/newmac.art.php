                  <h3>NEWMAC Win Streak Continues For MIT</h3>
                  <h5>Andree Sets Rookie Triple Jump Record, Shaoul Nearly Gets Long Jump Record</h5>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>April 27, 2019              </p>
              <p>The MIT Men's and Women's Track and Field teams endured 50 degree temperatures and six hours of gusting 25 mph winds to secure their 19th and 11th consecutive <a href="newmac.res.php">NEWMAC Championship</a> titles respectively.&nbsp; The men rode a strong field event performance to easily outscore their opponents 275 points to 183 for WPI, 107 for Springfield College, 105 for Coast Guard, 50 for Wheaton College and 46 for Babson College.&nbsp; The depleted women's team won a relatively close battle with Springfield College 182.5 to 152.16.&nbsp; Following them were Coast Guard at 114, 83.33 for WPI, 81.5 for Wheaton College, 47.5 for Smith College, 47 for Mount Holyoke, 44 for Wellesley, and 28 for Babson College.</p>
              <p>MIT's men field event athletes recorded seven out of eight wins and scored 152 of their total 275 points, using the 10-8-6-5-4-3-2-1 scoring format, and the Engineers were cautious, not competing Will Woltmann '22 in the throws, Alec Reduker '20 in the Long Jump and Steven Speck '20 in the High Jump or Long Jump.&nbsp; In addition, the NEWMAC Conference rule only allows for up to four entries per team in each event, further hampering the Engineers.&nbsp; Although he did not record a victory, Jon Fakkema '19 scored the most individual points for Tech with 22, placing second in the Shot Put,&nbsp;second in the Hammer and third in the Discus.&nbsp; Yorai Shaoul '21 was the only double winner for MIT, taking the Long Jump with a 24-1 3/4 effort (only using three attempts), just 1/2&quot; from tying the MIT record, and then the Triple Jump with a jump of 47-7.&nbsp; His Long Jump mark ranks third in the country while his Triple Jump mark of 49-9 still ranks first.</p>
              <p>Even without Woltmann, the talented throws group had the most dominant performance in the Shot Put, scoring 26 of a possible 29 points.&nbsp; Freshman Nate Basinger took advantage of an off day for Fakkema and Woltmann's absence, winning the event with a throw of 46-1 1/2.&nbsp; After Fakkema's second place, Albert Menio '20 placed fourth and Ryan Nall '22 took sixth place honors.&nbsp; While they only placed three individuals, Reduker led a 1-2-3 sweep in the High Jump by winning with a 6-6 3/4 effort.&nbsp; Chris Washington '20 and Caleb Harris '22 earned second and third respectively, both clearing 6-4 3/4.</p>
              <p>The only perfect event for MIT was the 10K.&nbsp; Not having run a 10K yet this season, Josh Derrick '20, Josh Rosenkranz '19, Finn Jacobson-Schulte '21 and Steven Goldy '20 entered the race with no time.&nbsp; Facing the strongest winds of the day, no one really wanted to take the lead.&nbsp; However, the MIT runners wanted to extend their season and to do so they had to qualify for next week's 10K by running at least 33:20.&nbsp; While this is obviously not a very challenging performance, in such strong winds&nbsp;it could be difficult without an organized plan.&nbsp; Jacobson-Schulte had no interest in waiting, taking the lead once it was clear no one else would,&nbsp;running a 77 second first lap.&nbsp; While the goal was to qualify and possibly take the top four places it was also desirable to do both while running as slow as possible and 77 pace was too fast.&nbsp; The quartet quickly settled on 79 pace, leading two additional runners through four consecutive 5:17 1600 splits.&nbsp; After one more lap, the group could sense some fatigue from the opposing runners and attacked with a couple of 77&nbsp;second laps to gain separation.&nbsp; They quickly attained a 100 meter lead and cruised in with a 1-2-3-4 finish, all four running 32:52, led by Josh Derrick's 32:52.55.</p>
              <p>The only other victory for MIT in the running events&nbsp;was earned by Aidan Gilson '19.&nbsp; After two laps of running with his opponents, despite&nbsp;the wind, Gilson took over in a very determined fashion, dropping a 72 lap.&nbsp; He then settled into 75 pace to easily win in 9:26.31.</p>
              <p>Back to the field events, Liam Ackerman '21 continued his dominant vaulting by winning the Pole Vault at 15-5.&nbsp; Scott Cameron '19 came in second, based on misses, also clearing 15-5, an excellent season best.&nbsp; Everett Brandyberry '21 placed fourth at 14-5 1/4 and Raja Rajcic '20 claimed eighth place.</p>
              <p>Albert Menio '20, who also placed fourth in the Hammer as well as the Shot Put, earned his first victory this season, throwing 143-2 to win the Discus.&nbsp; The event, severely hampered by the wind, also saw Tech earn third (Fakkema), sixth (Basinger) and seventh (Nall).</p>
              <p>The final win was earned by Gabe Madonna '20 in the Javelin.&nbsp; The junior took a limited number of throws, but got the job&nbsp;done with a 183-2 performance.&nbsp; Cooper Driscoll '22 added a fifth place with a&nbsp;165-2 throw.</p>
              <p>Benton Wilson '21 did not win any events, although he&nbsp;should have claimed the 200, was responsible for three seconds.&nbsp; First he anchored the second place 4x100 Relay&nbsp;with a very strong finish.  A 10.82 100 final after running 10.81 in the trials gave the sophomore another second place.&nbsp; In the 200, Wilson pulled away, had a commanding lead and relaxed, getting caught about 15 meters from the finish, running 22.11.</p>
              <p>With seniors accounting for only 48 of the points, knowing who they held out of the meet, and a very strong incoming class of 2023, the men should not be threatened any time soon.</p>
              <p>The women, while only winning by 30 points, were seeded to win by 100.&nbsp; In part, Springfield College stepped up, scoring 50 points more than seeded, but MIT also scored 26.5 less than predicted, 14 of which were in middle-distance events and 13 in the Long Jump.&nbsp; The only events in which the Engineers scored more than their seeds were the 100, Pole Vault, Hammer and Shot Put.</p>
              <p>On the positive side, Elena Andree '22 turned around a foul out in the Long Jump with a 38-2 1/4 freshman record to win the Triple Jump, currently ranking 17th in the country for Division III.&nbsp; Lucy Lee '21 jumped 37-5 for second place, Elizabeth Weeks '21 jumped 36-9 1/2 for fourth place and Yilinn Yang '22, placed seventh at 34-4 3/4, suffering through a painful back injury.</p>
              <p>In the Pole Vault, Kari Stromhaug '19 won the Pole Vault at 12-1 1/2 as she dealt with the gusting cross-wind.&nbsp; Anais Marenco '22 claimed third, clearing 11-1 3/4.&nbsp; Emily Caragay '22&nbsp;also cleared 11-1 3/4, a season best for fourth place, and Jacqueline Ahrens '21 took fifth by clearing 10-8.</p>
              <p>In the Hammer Throw and Shot Put, Folusho Jebutu '20, Elise McCormack-Kuhman '21, Tema Nwana '21 and Lily Papalia '22 combined to earn six more points than their seeds.&nbsp; Jebutu placed second in the Hammer (145-10) while also placing seventh in the Shot Put (33-11 1/4).&nbsp; McCormack-Kuhman placed fourth in the Hammer (143-10) despite dealing with a back injury and Nwana threw a personal best 135-6 for sixth place.&nbsp;&nbsp;Papalia placed sixth in the Shot Put at 35-9 1/4 with an MIT best.</p>
              <p>The remaining victories came in the running events.&nbsp; Claire Melvin '22 showed her competitiveness when despite stuttering on nine of the ten hurdles in the 400IH, she fought back to steal the victory just before the finish line, running 1:07.39.&nbsp; Jenna Melanson '20 took the Steeplechase out at the gun, ignoring the wind, led from start to finish with a 10:49.08&nbsp;victory.&nbsp; In the 1500, Margaret Trautner '20 also took the lead at the gun, hoping to be able to run fast enough to get into the fast section at next week's New England Division III Championship.&nbsp; However, the strong wind was too much for her.&nbsp; Once she realized she was not going to run a solid time, the junior eased off the gas, still winning by 11 seconds in 4:43.47.&nbsp; Later in the meet, Trautner joined Bailey Tregoning '19 for a 1-2 finish in the 800.&nbsp;&nbsp;After a wind-driven 71 second first 400, Tregoning took off with 300 to go, accelerating into the teeth of the wind to easily win in 2:16.03.&nbsp; Trautner made a later, but equally effective move to run 2:19.02 for second.&nbsp; The duo joined with Melvin and Kristen Frombach '19 to win the 4x400 in 4:01.73.&nbsp; Melvin led off with a solid 60.1.&nbsp; Trautner added a 60.8, Frombach a 61.5 and Tregoning a 59.4 for the victory.</p>
            <p>The injured and depleted women's team will have a very difficult time winning their seventh straight New England Division III Championship next week, particularly against Williams College on their home track.&nbsp; The men, going for their sixth consecutive New England Division III Championship will be challenged for sure, but hope to be able to pull it off.</p>
