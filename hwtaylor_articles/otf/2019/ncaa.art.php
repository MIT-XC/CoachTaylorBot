                  <h3>Men Take National Runner-up Honors</h3>
                  <h3>&nbsp;</h3>
                  <h3>Shaoul and Reduker Obliterate Long Jump Record     </h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>May 23-25, 2019              </p>
              <p>The highlight on day one at the <a href="ncaa.res.pdf">NCAA Division III Outdoor Track & Field championship</a> was the Men's Long Jump, at least as far as MIT was concerned.&nbsp; The result saw the MIT record shattered--four times, and the Engineers leading the meet with 14 points.&nbsp;&nbsp;The true sense of competition was embodied by Yorai Shaoul '21 and Alec Reduker '20.&nbsp; They fed off each other, supported each other, were motivated by each other and thoroughly enjoyed every minute of the event.&nbsp;&nbsp;Watching athletes compete when they are only thinking about what they can accomplish instead of fearing what they think they cannot do is such a pleasure for the spectators and their coaches.&nbsp; Seeded fifth and 16th going into the competition, no one could have predicted what happened in this event.&nbsp; Reduker was in the first section&nbsp;and opened up with a 23-6 3/4 jump to take the lead.&nbsp; Not satisfied, he snatched the MIT record with a 24-4 1/4 effort on his second attempt and he only started long jumping two months ago.&nbsp; His third attempt was only a 24-2 1/4 effort, giving him the best trial series in MIT history--for&nbsp;a few minutes--and first place in the competition.&nbsp; Yorai is an incredible athlete, but an even better competitor.&nbsp; His first attempt a 24-4 1/2 MIT record.&nbsp; Entering the finals they were sitting in second and third.&nbsp; However, Reduker fouled his first two while two competitors passed him.&nbsp; Shaoul's first attempt was yet another MIT record at 24-8 1/4.&nbsp; On his final attempt Reduker put out a bomb of a jump at 24-8 1/4 to tie for second place.&nbsp; Shaoul high fived his teammate and then raised him a 24-11&nbsp;for sole possession of second place&nbsp;and the top two jumpers in MIT history.</p>
              <p>In the Women's Pole Vault competition,&nbsp;14th seed Kari Stromhaug '19 was perfect after her initial miss at 11-5 3/4, clearing 11-11 3/4, 12-3 1/2 and 12-5 1/2 on consecutive attempts.&nbsp; However, at 12-7 1/2, although she had great attempts, she displaced the bar each time, possibly aided by the strong winds, particularly on the first attempt when she was over and the wind possibly pushed the pole back into the bar.&nbsp; She continued her streak of earning All America&nbsp;honors at every national championship, garnering eighth place, giving MIT their first point.</p>
              <p>Jenna Melanson '20, seeded sixth in the Steeplechase and a two-time All American in the event, had to withdraw from the competition due to two stress fractures in her foot, justed diagnosed two days earlier.&nbsp; Aidan Gilson '19, also an All American last year in the Steeplechase, seeded 14th, was unable to compete to the best&nbsp;of his ability as he&nbsp;fought through the pain of his achilles injury.&nbsp; He finished 19th.</p>
              <p>The final competitor for MIT on day one was Gabe Madonna '20 in the Javelin Throw.&nbsp; Madonna, seeded 13th entering the competition, did not have his best day, throwing 173-1 to finish 18th.</p>
              <p>Day two of the three day competition was&nbsp;a light one for the Engineers.&nbsp; First up was Reduker in the High Jump.&nbsp; The opening height was 6-4 and none of the 20 qualified jumpers went out.&nbsp; However, at 6-6, three went out and at 6-8 eight more failed to make it.&nbsp; Three more failed to make 6-9 and three more 6-10 1/4.&nbsp; With three jumpers remaining, Reduker was the only one who had not missed.&nbsp; Jumping at 6-11 1/2, if no one made it Reduker would win.&nbsp; Reduker's first attempt was excellent just barely displacing the bar.&nbsp; His next two not so great.&nbsp; Kyle Rollins from St. John Fisher made it on his second attempt to win the event, leaving Reduker with second place and a total of 14 points scored between his two events.</p>
              <p>The only other event for Tech on this day was the women's 800.&nbsp; Bailey Tregoning '19, seeded ninth and Margaret Trautner '20, seeded 14th represented MIT.&nbsp; Trautner was in the first of three heats.&nbsp;The top two in each heat and the next three fastest times would advance to the final on Saturday.&nbsp; Unfortunately Trautner's heat went out slow, splitting 67 at the 400.&nbsp; As the leaders took off, Trautner did not have the sprint speed to go with them.&nbsp; She was in third until the final 50&nbsp;when she lost the final qualifying spot, finishing in 2:13.88, finishing 12th overall.&nbsp; Tregoning was in the second heat, which went out very fast.&nbsp; The leader came through the 400 in 62.4 while Tregoning was in fourth in 64.5.&nbsp; She stayed in fourth, finishing in 2:11.25 to qualify to the final.</p>
              <p>Saturday's final day of competition started with the Triple Jump where Shaoul hoped to defend his indoor title.&nbsp; After his first jump he was sitting in fourth place with his modest 49-1 effort.&nbsp;&nbsp;Not accustomed to being in fourth in any competition Shaoul took umbridge with this and laid out a 50-7 1/2 performance.&nbsp; This sealed the deal, although three other jumpers exceeded 50'. Shaoul earned first place, giving MIT 32 points accumulated by just two athletes; Shaoul and Reduker.</p>
              <p>Next up was Liam Ackerman '21, seeded sixth in the Pole Vault.  Ackerman came in at the opening height of 14-7, partly because of the fierce cross wind and partly because up until yesterday he was unable to see very well as one eye was completely swollen shut due to a severe allergic reaction, which also had him sick and suffering from an enormous headache.&nbsp; The sophomore put it behind him and vaulted clean through 14-7, 15-1, 15-7&nbsp;and 15-11.&nbsp; Of the seven vaulters who cleared 15-11, only two were clean and Ackerman was one of them.&nbsp; Only two cleared the next height and Ackerman beat the other four on misses, giving him an unexpected third place, and MIT 38 points.&nbsp; This put them in second place at that time.</p>
              <p>On the track, Tregoning was up in the final of the 800.&nbsp; The pace was fast so Tregoning was in the back, and still came through in 64.4 in eighth position.&nbsp; On the backstretch she moved up to seventh and then poured it on in the final hundred to move up to fifth in a season best 2:10.98.&nbsp; This gave the women five points.</p>
              <p>The final event was the 5K featuring Katie Collins&nbsp;'21, seeded 11th.&nbsp; The heat was pretty intense in the stadium and the wind pretty strong, so the pace was going to be slow.&nbsp; The plan was to run about 10 seconds per mile slower than normal, or 5:30 pace.&nbsp; The first mile went according to plan in 5:31, but Collins had missed going with the top five when they split and settled with the next three.&nbsp; This pace was too slow at 85 quarter pace and this caused Collins to run a bit awkwardly and wasting energy.&nbsp;With 1000 to go she had run out of gas and fell from seventh to 10th in 17:42.15.</p>
              <p>The men had to wait through the final two events to see if they would remain in second place.&nbsp;&nbsp;The University of Wisconsin Eau Claire was a runaway winner with 58 points.&nbsp;&nbsp;Mount Union placed third in the 4x400 to move up to third with 36 points and that sealed the runner-up position for MIT.</p>
              <p>Prior to the competition, at the awards banquet, Jenna Melanson was awarded the Elite 90 award, the programs 12th such honor, by far the most in the country.&nbsp; The award is given to the individual female and male at that championship  with the highest GPA.&nbsp; If there is a tie it goes to the one with the  most academic units.&nbsp;&nbsp;Aidan Gilson, a two-time Elite 90 winner was in the tie-breaker, but lost out on the number of units.&nbsp; Both Melanson and Gilson have perfect 5.0 GPA's.</p>
              <p>Also winning awards were Yorai Shaoul, honored as the New England Region Male Field Event Athlete of the Year, and Coach Derek Rousseau, winning the New England Region Assistant Coach of the Year award.  Both will be among those considered for the same honors nationally, which will be decided next week.</p>
