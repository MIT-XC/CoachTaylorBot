                  <h3>Throwers Dominate For Engineers</h3>
                  <h5>MIT Men, Williams Women Win Dual Meet</h5>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>April 13, 2019              </p>
              <p>In this age of chasing performances around the region and country to qualify for the various championships, <a href="williams.res.pdf">MIT and Williams</a>, historically the two best Division III track and field teams in New England over the past 40 years competed in an old-fashioned dual meet.&nbsp;&nbsp;Williams women, 2019 indoor national champions, were too much for the injured and ill Engineers, winning by a score of 108 to 76.&nbsp; MIT's men, fresh off a New England championship and finishing seventh at the NCAA Division III Indoor Track &amp; Field championship, won 113 to 86.</p>
              <p>For both genders, MIT's throwers were the dominant story for the home team.&nbsp; They certainly took advantage of the 80 degree, sunny day.&nbsp; For the men, Jon Fakkema '19,&nbsp;Albert Menio '20, Will Woltmann '22, Ryan Nall '22, Nathan Basinger '22, Gabe&nbsp;Madonna '20 and Cooper Driscoll '22 scored every point available to them in the competition, outscoring Williams 41-3, giving up the three&nbsp;because MIT only had two javelin throwers.&nbsp; The first throwing event was the Discus, and Fakkema continued his very impressive senior year, winning with a throw of 149-1.&nbsp;Menio earned second with a&nbsp;personal best of 144-3, Basinger was third with 135-11 and Nall was fourth with&nbsp;a throw of 131-10.&nbsp; Next was the Hammer Throw and Fakkema again dominated, winning with a 167-4 throw.&nbsp; Menio threw another PR, this one 160-0 for second place. &nbsp;Nall earned third and Woltmann fourth with throws of 154-5 and 138-7 respectively.&nbsp; The five throwers made their way over to the Shot Put venue, but little else changed.&nbsp; Woltmann won with a season and outdoor best of 49-1.&nbsp; Fakkema was a close second at 47-1, Basinger third at 44-8 and Menio fourth at 42-7.&nbsp; While these events were being contested, Madonna and Driscoll were taking care of business in the Javelin Throw.&nbsp; Madonna, a junior, was competing in his first meet ever at MIT simply because it was his first year on the team.&nbsp; A 150' thrower in high school, the two years off seemed to serve him well.&nbsp; He proceeded to throw 197-6, the number five throw all-time for MIT.&nbsp; The mark ranks him fifth in New England and ninth in the country for Division III.&nbsp; This earned him first place, which Driscoll supported with second place at 158-11&nbsp;despite an ailing shoulder.</p>
              <p>The women dominated in similar fashion.&nbsp; In the Hammer&nbsp;the women swept with Elise McCormack-Kuhman '20 winning with a 155-0 PR.&nbsp; Folusho Jebutu '20 took second with a 139-11 throw and Savannah Inglin '20 and Tema Nwana '21 added third and fourth with marks of 122-0 and 114-7 respectively.&nbsp; In the Shot Put, Lily Papalia '22 and Jebutu took first and second with marks of 35-0 1/2 and 34-1 1/4.&nbsp; In the Discus, Jebutu won with a mark of 120-10.&nbsp; Papalia and Inglin added a third and fourth at 94-7 and 75-3.&nbsp; McCormack-Kuhman took second place honors in the Javelin with a mark of 89-0.&nbsp; All in all, the women throwers scored 30 of MIT's 76 points in just four events.</p>
              <p>There were many other highlights accomplished by MIT athletes.&nbsp; Liam Ackerman '21 returned to his All America form by clearing 15-11 in the Pole Vault to win that event. Supporting that, Raja Rajcic '20 cleared a season best of 14-5 1/4 for third place.&nbsp; Chris Washington '20 returned to competition for the first time this year, clearing 6-4 1/2 to win the High Jump.&nbsp; Alec Reduker '20 took the weekend off after clearing 7-0 last week.&nbsp; Steven Speck '20, who took second place honors in the High Jump, popped a big PR in the Long Jump, placing second with a jump of 21-4 3/4.</p>
              <p>On the track, Benton Wilson '21 continued his winning ways.&nbsp; He first won the 100 in a season best 10.99 and then won the 200 in another season best of 21.71.&nbsp; On either end of those events Wilson anchored both the winning 4x100 (Alex Liu '22, Nick Duchatellier '22, Sope Eweje '19) in 43.78 and the winning 4x400 (Reed Foster '22, Simon Alford '20, Kent Slaney '21).</p>
              <p>The other victories for the men were earned by Aidan Gilson '19, who took first in the 3,000 Steeplechase and Andrew Shao '22 in the 400IH.&nbsp; The senior trailed for a little over 800 meters in the steeplechase before deciding the pace was too slow.&nbsp; He finished strong with a 9:33.73.&nbsp; He returned in the 400IH, placing second in a personal best 57.16 to support the victory by Shao  in 56.99.</p>
              <p>The women claimed three victories outside of the throws.&nbsp; Bailey Tregoning '19 ran a 2:12.65 after coming through the 400 in 68.&nbsp; Given the gusting wind, most races became strategic and this was no exception.&nbsp; Margaret Trautner '20 and Marissa McPhillips '20 added a second and fourth place with a 2:16.15 and 2:16.69.&nbsp; Claire Melvin '22 took the victory in the 400IH, running 66.25, and Katie Collins '21 won the 5K from start to finish in 17:45.41.</p>
              <p>Although there was not a victory for MIT, Lucy Lee '21, Elizabeth Weeks '21 and Elena Andree '22 had a solid triple jump outing, jumping 37-4 1/2, 36-9 3/4 and 36-6 3/4 to place second, third and fourth.</p>
              <p>Next week, some of MIT elite middle-distance, distance runners will travel to Princeton for the Larry Ellis Invitational while others will compete at home in the Sean Collier Invitational.&nbsp; </p>
