                  <h3>Field Event Athletes Lead the Way for MIT</h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>April 20, 2019              </p>
              <p>On a wind swept day, MIT&rsquo;s men&rsquo;s and women&rsquo;s teams split time between Princeton New Jersey and their final home meet of the regular season at the <a href="seancollier.res.pdf">Sean Collier Invitational</a>.&nbsp; With a few of the team&rsquo;s top distance runners preparing for Princeton&rsquo;s Larry Ellis Distance Carnival later this evening, the day began a few minutes before 11 AM back in Cambridge, when the team gathered to recognize our 12 graduating seniors for their hard work and dedication to the program. </p>
              <p>With rain early, and windy conditions the entire day, weather would play a significant factor throughout the meet. However, despite adverse conditions that forced the pole vault and high jump indoors, the jumping events took center stage for the Engineers, netting several event wins, seasonal or career bests and even a likely national qualifier in the process.&nbsp; In the horizontal jumps, Junior Steven Speck started off the day, taking first place in the long jump. His performance of 6.41 meters (21&rsquo;0&rdquo;) was not as good as the previous week where he jumped 21&rsquo;4&rdquo; against Williams, but it was more than enough to claim his first collegiate victory. Once the boards were switched over to the Triple Jump, freshman George Stultz kept the momentum going for the Engineers, leading from wire to wire to claim his first collegiate win with a career best of 13.22 meters (43&rsquo;4 &frac12;&rdquo;). While Stultz set that mark on his fifth attempt, he captured the lead on his first jump and steadily increased pressure on the rest of the field, improving nearly every round and eventually winning by 14&rdquo; over his nearest competitor. </p>
              <p>Meanwhile indoors, junior Chris Washington was on a mission to punch his ticket to next month&rsquo;s national championship. Coming in at 1.90, Chris&rsquo; only competition came from volunteer coach Max Maritato, who battled with him up to 1.95.&nbsp; Washington went on to clear 2.00 (6&rsquo;6 &frac34;&rdquo;) for the first time since last spring, and set the bar at 2.07. Ultimately, he would fall just short, subtly dislodging the bar on his final attempt.</p>
              <p>The women&rsquo;s jumping events were bolstered by three career best performances and a national qualifying mark.&nbsp; Sophomore Liana Reilly, who had been battling injury throughout the year, kicked things off in the high jump, finishing in a two-way tie for second place, and clearing a collegiate best mark of 1.63 meters (5&rsquo;4 &frac14;&rdquo;). Less than 10 minutes later, it was outside to the elements, where Reilly would claim her second collegiate best of the day (17&rsquo;0 &frac12;&rdquo;) in the long jump to finish eighth overall. After wiping out in the 100 Hurdles, sophomore Elizabeth Weeks was on a mission for redemption in the triple jump.&nbsp; Weeks set the standard on her first jump, posting a collegiate best 11.53 (37&rsquo;10&rdquo;) for a lead that would be challenged, but never bested the rest of the event. In the Pole Vault Kari Stromhaug led all collegiate competitors, placing second with a jump of 3.75 meters (12&rsquo;3 &frac12;&rdquo;). The mark represented her third best jump of the year and moved her into a six-way tie for 11th nationally with a mark that has traditionally been a national qualifier. Finishing right behind Stromhaug was freshman Anais Marenco, taking third with a jump of 3.55m (11' 7 &frac34;&rdquo;). </p>
              <p>In the throwing events, junior Folusho Jebutu had a strong outing, winning the discus with a toss of 36.32m (119' 2&quot;), placing third in the hammer, and seventh in the shot put.&nbsp; On the men&rsquo;s side, freshmen Gabe Madonna and Cooper Driscoll took second and third in the javelin respectively with throws of 52.11m (170&rsquo;11&rdquo;) and 50.82m (186&rsquo;9&rdquo;).&nbsp; In the weight events, the men&rsquo;s group placed at least three in each of the throwing event finals. Of note, Freshman Nate Basinger had a strong day, throwing a collegiate best in both the shot put (14.24m, 46&rsquo;8 &frac34;&rdquo;) and discus (43.15m, 141&rsquo;7&rdquo;), placing sixth and eighth overall. Albert Menio &rsquo;20 also threw a two-meter lifetime best in the discus of 46.01m (150' 11&quot;) finishing 4th overall. </p>
              <p>Trackside, sophomore Gabrielle Enns kicked off the meet in the running events in quick fashion.&nbsp; Racing largely unopposed in the 3,000 Steeplechase Enns raced to a three second collegiate best in the wind and rain to finish in 12:06.37, nearly two minutes ahead of the rest of the field.</p>
              <p>Freshman Claire Melvin proved consistency is critical to success. For the fourth week in a row, the 400 hurdler&nbsp; ran a 1:06, rattling off her third straight victory in the event, a trend she will look to extend into next week&rsquo;s NEWMAC Championship. In the 1,500 Katie Bacher &lsquo;20 made her return to the track after being sidelined early in the winter with injury, finishing second in a time of 5:01.99.</p>
              <p>In the men&rsquo;s 5,000 Dennis Maloney &lsquo;20, David Walter &lsquo;G, and Finn Jacobson-Schulte &lsquo;21 worked together to battle off some horrific winds to sweep the top three spots with Josh Derrick &lsquo;20 finishing &nbsp;just a few seconds back in fifth. The 800 also saw a near sweep, as Simon Alford &lsquo;20, Aiden Foucault-Etheridge &lsquo;22, and Ellery Rajagopal &lsquo;21 finished second, third, and fourth. </p>
              <p>With the 2019 regular season officially behind us, it is now time to begin a tradition nearly as old as New England itself.&nbsp; Starting next Saturday, with NEWMAC&rsquo;s, the teams will participate in three straight championships, before taking a week off to either prepare or chase down final qualifying marks for the National Championship. Forget March Madness, welcome to May-hem.</p>
