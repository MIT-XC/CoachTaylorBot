                  <h3>Reduker First at MIT to Clear 7' in High Jump</h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>April 6, 2019              </p>
              <p>MIT hosted the <a href="quad.res.pdf">Bates, MIT, RPI, Tufts quadrangular meet</a> on a spectacular April day.&nbsp; The temperature was in the mid-60's with sun shining all day.&nbsp; The wind was nearly non-existent at the start of the day, but picked up to an average of 2.5+ meters per second&nbsp;by the middle of the meet.</p>
              <p>The NCAA manipulated the scoring system for four or fewer teams&nbsp;quite a few years ago to prevent teams from getting blown out by outrageous scores.&nbsp; The default scoring system is for each team to score no more than two places regardless of the places actually earned.&nbsp; For the meet at hand, a quadrangular meet, the point distribution would be 9-7-6-5-4-3-2-1. The alternative scoring system would be for only 5-3-2-1 points, but with no limit as to how many could score per team.&nbsp; The coaches by majority chose the default method of scoring.&nbsp; At the end of it all, MIT's men won their meet 190.5 to 185 for Tufts, 150.5 for RPI and 125 for Bates.&nbsp; Tufts women pulled out the victory with 198 points to 194 for MIT, 138 for RPI and 134 for Bates.&nbsp; If the alternative method of scoring had been chosen, MIT's quality depth for the women would have netted a victory with 75 points to 63 for Tufts, 41 for Bates and 28 for RPI.&nbsp; The men's order of finish would not have changed.</p>
              <p>While the score went back and forth throughout the meet between MIT and Tufts for both genders, it was nearly impossible for the coaches to keep up with as it was not obvious who was scoring due to the fact that only two could score per team in any given event.&nbsp; This unpredictability in the score and because the Engineers were down in strength&nbsp;due to injuries and illnesses (only one competitor in the Javelin, High Jump, and 5K for the women), which cost them a minimum of three points, the focus was more on performance.</p>
              <p>The overall top performance in the meet was turned in by Alec Reduker '20, who cleared 7-0 1/4 to easily win the High Jump and tie for the top mark in all of Division III.&nbsp; This is certainly a national qualifying mark, but also an MIT record.&nbsp; Not only did Reduker dominate the High Jump, but in his first collegiate competition in the Long Jump the junior landed a 22-9 3/4 performance for second place behind teammate&nbsp;Yorai Shaoul's PR of 23-10 1/4, possibly enough to get him into the NCAA Division III Championship, currently ranked third.&nbsp; Shaoul also won the Triple Jump with a 48-8 1/4 performance.  His  49-9 from last week still leads the country.&nbsp; In the High Jump, Reduker was supported by a 6-6 3/4 effort by Caleb Harris '22, who just made it on the MIT top 10 all-time list.</p>
              <p>Sticking with the men's side of the meet, the throwers continued their dominance, although the other teams certainly showed Tech they were not intimidated.&nbsp; Jon Fakkema '19 continued to lead the MIT throws group by winning the Discus (157-4), placing second in the Hammer (166-9) and earning fourth in the Shot Put with an off-day 45-5 put.&nbsp; </p>
              <p>On the track, Aidan Gilson '19 showed his array of skills as he won the Steeplechase in an impressive 9:14.65, which currently ranks number six in the country for Division III, came back to place fourth in the 400IH in 59.88 and finished with a 51.3 4x400 leg to help lead his team to victory&nbsp;in 3:24.43.</p>
              <p>Benton Wilson '21 made his presence felt, competing in four events, winning all but one of them.&nbsp; His day began while anchoring the 4x100, proceeded to&nbsp;the 400, then the 200, culminating with anchoring the 4x400.&nbsp; In the 4x100, Alex Liu '22 got MIT out to a lead, which was maintained by Nick Duchatellier '22.&nbsp; However, Sope Eweje '19 left early, causing him to slow&nbsp;dramatically to receive the baton, losing the lead.&nbsp; Wilson did his best to make up the difference but was unable to do so.&nbsp; In the open 400&nbsp;Wilson easily pulled away to win with a 49.25.&nbsp; The 200 was even more impressive as the sophomore ran 21.72, currently the 14th time in the country.&nbsp; In the 4x400, Gilson and then Kent Slaney '21 (51.8) maintained a close second to Tufts after Duchatellier opened with a 51.6 and that is all Wilson needed.&nbsp; He anchored with a 49.7 to open up a large lead on the homestretch.</p>
              <p>Two introductory performances deserve mention.&nbsp; Cooper Driscoll '22 competed in his first MIT track and field competition, placing sixth in the Javelin with a 170-2 performance.&nbsp; Dylan Doblar '21, injured since the summer, competed for the first time, running 4:03.27 in the 1500 to place eighth.</p>
              <p>While the time was not spectacular, Andrew Mah '22 ran an excellent 5K.&nbsp; At the peak of the wind there were two sections of the men's 5K with 40 total runners.&nbsp;Mah, running in the faster section ran a very smart race, tucking in, letting others do the work even&nbsp;though it was not particularly fast with 1600 splits of 4:48, 4:54 and 4:47.&nbsp; The one mistake was taking the lead&nbsp;with 300 to go, meaning the first 100 in the lead was dead into the wind.&nbsp; Taking the lead with either 600 to go or 200 to go would have been much smarter.&nbsp; Regardless the final 600 was 1:39 with the final 200 being 30 seconds.&nbsp;&nbsp;Mah lost by one hundredth of a second to John&nbsp;Reed '21 of RPI 14:59.54 to 14:59.55.</p>
              <p>Jenna Melanson '20 had the top performance of the meet for the women, although there were quite a few on this great spring day.&nbsp; Melanson perhaps went out a little too hard in the 3000 Steeplechase with a first lap in 82.&nbsp; Her lead was never in doubt so she was going for a fast time.&nbsp; After 1200 she slowed to an 87 then an 88, a 90 and a 91 before closing well in 44 to finish in 10:52.22, the third time in all of Division III at this time.&nbsp; Similar to Gilson, the junior&nbsp;came back in the 400IH, placing fourth in 69.29.</p>
              <p>Bailey Tregoning '19 was mindful not to take the lead too early in the 800, but at the end of a slowing 67 first 400 she could not take the pace any longer.&nbsp; Taking the lead she seemed to have no trouble running into the wind on the backstretch.&nbsp; Coming off the final turn she held on well to run a surprising 2:12.86, currently number four in the country.</p>
              <p>Michelle Menkiti '20, still rounding into shape after her hamstring injury, claimed victories in the 100, 200 and anchored the winning 4x100 Relay.&nbsp;&nbsp;Her winning 100 time was 12.43 and her 200 performance a 25.81.&nbsp;&nbsp;The 4x100 quartet of Elena Andree '22, Sarah Ishamuddin '21, Joanna Kennedy '22 and Menkiti pulled away more as the race progressed, winning in 49.85.</p>
              <p>Lucy Lee '21 had a great day, winning the Triple Jump with a very strong 37-10, good for #13 on the national list and number three on the MIT all-time list.&nbsp; She backed that up with a 100HH PR of 15.77, placing third in that event.</p>
              <p>In the Pole Vault, Kari Stromhaug&nbsp;'19 continued her great vaulting, winning by clearing 11-9 3/4.&nbsp; Makenzie Patarino '19, out the entire indoor season due to shoulder issues, competed for the first time this year, clearing 10-6&nbsp;and placing third overall.</p>
              <p>Losing Margaret Redfield '21 to appendicitis two days before, Lianna Reilly '21 was on her own representing MIT in the High Jump.&nbsp; The sophomore, fighting lower back problems, placed second with a jump of 5-3.</p>
              <p>Claire Melvin '22 played a role in the remaining victory by the Engineers.&nbsp; She won the 400IH with a dramatic come-from-behind victory 66.17 to 66.20.&nbsp; The freshman then led off the 4x400 Relay&nbsp;with a 60.3 leg.&nbsp; Marissa McPhillips '20, who had placed third in the 1500 earlier, ran a 61.7 leg.&nbsp; Kristen Frombach '19, coming back from the 400, 200 double, ran a 62.0 leg.&nbsp;Tregoning, coming back from the 800 had a lot of ground to try and make up, and even though she ran a 58.0 anchor, it was not enough to do better than second place in 4:02.36.</p>
            <p>Next week the Engineers are at home in a dual meet versus Williams College.</p>
