                  <h3>Fakkema, Shaoul Have Dominant Performances on UCSD Collegiate Open</h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>March 30, 2019              </p>
              <p>The MIT Track and Field teams traveled to Claremont, California for a week of warm weather training during MIT's Spring Break week. This was an opportunity to train the technical groups (throwers, jumpers, vaulters, sprinters and hurdlers) in warm weather and to bond as a team. </p>
              <p>The practices were challenging and successful thanks to the hospitality of the coaches and staff at Claremont-Mudd-Scripps.&nbsp; The bonding opportunities consisted of doing pretty much everything together during the week, a beach trip, a game of assassins, hiking and an excellent dinner hosted by parents. The week of training ended with the <a href="http://finishedresults.trackscoreboard.com/#/results/meet/8232">UC San Diego California Collegiate Open</a> to open the season. Competing with just technical &amp; sprint event athletes, the men placed fourth among 13 teams with 80 points and the women finished eighth among 13 teams with 36 points. </p>
              <p>The first event of the day on the track was the 4x100 relay, the women's quartet of Elena Andree '22, Sarah Ishamuddin '21, Yilinn Yang '22 and Michelle Menkiti '20 did pretty well for only having two weeks of handoff practices, but there is certainly room for improvement. Their 49.29 was competitive for this time of year, placing fourth and out pacing other New England schools such as Williams and Middlebury. </p>
              <p>Nick Duchatellier '22 was up next in the 400m.&nbsp;His first 200m was way too fast running 22.7 so even though he was in second place going into the final 200m, he faded badly to fifth place with a time of 50.31.</p>
              <p>In the Pole Vault competition, Kari Stromhaug '19 started where she left off Indoors as the third place finisher at Nationals, opening the outdoor season with a height of 12-1 1/2 to place second. Anais Marenco '22 cleared 11-1 3/4 to tie for seventh place. On the men&rsquo;s side Scott Cameron &rsquo;19 placed fourth, clearing 14-7 &frac14;, nearly getting 15-1. Liam Ackerman took sixth with 14-1 &frac14; while Everett Brandyberry &rsquo;21 placed eighth clearing the same height. This was Brandyberry&rsquo;s first time breaking 14 all year at a meet. </p>
              <p>Yorai Shaoul '21 had quite the opening day in the jumping events. He finished second in the Long Jump at 23 and then took second place again in the Triple Jump in 49-9, which he jumped on his sixth and final attempt in finals. </p>
              <p>In the High Jump Alec Reduker &rsquo;20 placed second with a jump of 6-5, although scoring eight points for the Engineers, Alec looks to get on track during his next few meets after a disappointing NCAA indoor Championship.&nbsp; Margaret Redfield &rsquo;21 finished tied for seventh in the High Jump with a mark of 5-2 &frac14;. </p>
              <p>Yilinn Yang started her outdoor season well with a PR of 37-5 in Triple Jump, with her first jump in finals. She ultimately finished fourth in the competition. The triple jumpers dominated as they have all season. Lucy Lee '21, placed sixth with a PR at 37-0 &frac14;, while Elena Andree placed seventh in the Triple Jump at 36-8 &frac14;. </p>
              <p>Joa Kennedy '21, took a break from decathlon training to compete in both the 110HH and 400IH. Kennedy got out well in the high hurdles to place fourth with a PR of 15.55. While in the 400IH, he raced from the slow section, took control of the race from the beginning, leading the entire way finishing seventh overall with a time of 57.04. Claire Melvin &rsquo;22 finished eighth in the 400IH in her first collegiate intermediate hurdle race and first race at 400IH in three years. </p>
              <p>Benton Wilson &rsquo;21 started the season well, placing fifth in the 100m in 11.10 and fourth in the 200m with a time of 22.14 even though he did not accelerate well the first 50m of his 200m.</p>
              <p align="left">Leading the way for the throws, Jon Fakkema &rsquo;19 competed well in all three throwing events Shot Put, Discus, and Hammer. Fakkema finished second place in Shot Put with a PR of 50-0, in the Discus he finished third with a throw of 154-8, and in the Hammer he threw 169-7 to finish fifth in the event. While Will Woltmann &rsquo;22 and Ryan Nall &rsquo;22 finished fourth and eighth respectively in Shot Put with marks of 48-11 and 45-8 &frac12;. &nbsp;</p>
              <p align="left">Folusho Jebutu &rsquo;21 finished sixth in both the Discus and Hammer. Her performance in the Discus was 122-7 and she had a PR in Hammer with a mark of 151-9. </p>
              <p>In summary, this year&rsquo;s Spring Trip resulted in numerous personal and season&rsquo;s bests.&nbsp; <br>
                Next week the Engineers are back home as a full team hosting a quad meet against Bates, RPI and Tufts. They will look to improve on their performances in order to be successful against the teams in New England during the Outdoor season.</p>
