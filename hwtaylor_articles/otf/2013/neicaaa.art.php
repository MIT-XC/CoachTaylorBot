              <p><span class="headline">Two Varsity Records Fall as Engineers set NCAA Qualifying Marks</span></p>
              <p align="left">May 10-11, 2013</p>
              <p align="left">With two meets left to achieve qualifying performances, MIT traveled to Stonehill College for the <a href="neicaaa.res.php">NEICAAA Championship</a>, a meet that brings together most of the Division I, II and III teams in New England. MIT did not compete any of their distance runners even though the competition in those events tends to be pretty weak. Instead the distance runners took the week off from racing after such a monster effort last weekend. For Tech, this meet is about getting qualifying performances for Nationals by competing against the best competition in New England.</p>
              <p align="left">Two MIT records fell during the competition and Michael Kaba was part of both of them. The freshman star qualified for the finals of the 400IH with a 53.50 performance on Friday. This performance was a personal best and very easy as he relaxed and 15 stepped the entire way. On Saturday Kaba was going to try and hit 14 steps for the first four hurdles, but did not bring enough speed off the first hurdle to enable that. He stuttered to hit 15 and continued that for the next three hurdles. Once he started running into the wind, that all changed as he was able to hit 15 without stuttering and really brought it home, passing all but one of the elite runners, finishing second in 52.20. This should certainly be fast enough to make the NCAA Division III Championship.</p>
              <p align="left">The 4x400 record of 3:12.70 is talked about with great reverence around MIT. For three freshmen and a junior to entertain taking that record down borders on track and field blasphemy. The foursome had a very busy weekend before going into the final race of the competition. States Lee '16 had run trials in the 400 (48.90), Derek Barnes '16 ran trials and finals in the 400 (48.79, 50.40), Michael Kaba '16, ran the 4x100 Relay, and the 400IH trials and finals, and Tyler Singer-Clark '14 ran the 4x100 Relay and trials and finals in the 400 (48.56, 48.87). Going into the race, knowing they were tired, it was decided the quartet would no longer worry about time but just go for the win. The adrenaline sparked by the competitive spirit of this group was all they needed. Lee led off, and set the wheels in motion as he ran an excellent 47.9 leg. Unfortunately, Kaba did not get out well, making the handoff nearly a still start. There was no way to tell he had recently run the 400IH final as after a moderate first 100, Kaba lit it up, actually taking the lead on the field with a 47.6 leg. As poorly as Kaba got out for the handoff, Barnes was worse, literally making it a stand still start. Barnes did race well despite having to get his momentum going while the Southern Connecticut and UMass Lowell runners were already in high gear. Barnes finished his leg in a respectable 48.9. Singer-Clark moved on the opposition and on the homestretch moved into second, but could not quite catch Southern. Singer-Clark's leg was 47.8, giving Tech a 3:12.42 performance despite the nearly two seconds lost in awful handoffs (the third handoff was not spectacular either). At the time they ran it, this time put them in fourth on the NCAA Division III performance list.</p>
              <p align="left">Martina de Geus '14, despite a pretty stressful week, raced the 3000 Steeplechase more aggressively than ever before. Going out at 79 for the first 400 and never hitting a 400 slower than 88 despite the four barriers plus waterjump every lap, de Geus ran a 14 second PR of 10:38.58, 30 seconds faster than her best previous to this year. She is currently ranked fifth on the NCAA list. Her 40 second final 200 with a waterjump and one barrier says she still has quite a bit left.</p>
              <p align="left">Brooke Johnson '13 was in the 1500 and hoped to make it three wins in as many weekends in the event. The seeded section went out in 72 for the first quarter and Johnson was in a great position towards the front of the pack. The pace slowed to 2:28 and the pack grouped up tighter. The pace opened up a bit to 71 for the third quarter and Johnson moved up to third place. In the final quarter Johnson moved to second, but despite her 50 seconds for the final 300 she was passed in the final meters to finish third in 4:30.75. Johnson is now ranked ninth on the NCAA list.</p>
              <p align="left">The 4x100 Relay was expecting to get into the low to mid 41's, but substituting Josh Duncavage '13 for the injured Nick Diamantoni '15 meant it would be the first time the four had run together. Michael DuPlessis '13 ran a great leg, but his sore calves did impact his speed. The handoff to Kaba was excellent and helped mitigate DuPlessis's speed. Kaba ran a strong leg and had a decent handoff to Duncavage. Duncavage is coming off a hamstring injury that still has him running cautiously. His leg was okay, but not his best, allowing the teams to catch up a bit. The handoff to Singer-Clark was solid and the anchor did his job, moving past everyone and nearly catching the leader, just missing by .02 as MIT ran 41.93.</p>
              <p align="left">The dependable Pole Vault went well for MIT although no one set personal bests or moved up on the NCAA list. Ben Schreck '15 continued his winning battle on the mental side, tying for third at 15-3. Yida Gao '13 jumped 14-9 but did not place. Lauren Kuntz '13, favored to win, was not quite at the top of her game, but still placed third at 12-5 1/2. Cimmy Virdi '16 finished in a tie for sixth at 11-5 3/4.</p>
              <p align="left">The only other scoring place for MIT was earned by Carrtington Motley '16 in the Triple Jump. Motley placed eighth in 45-10.</p>
              <p align="left">Isabella Stuopis '16 improved her PR and freshman record to 42-10 3/4, just missing placing by one position. Sam Parker '15 ran a season best in the 800 of 1:53.43.</p>
              <p align="left">Next week, MIT will take a few folks to the ECAC Division III Championship hosted by Springfield College on May 16-17. Those going will be trying to improve on their NCAA marks with hopes of making the big dance at the University of Wisconsin La Cross May 23-25.</p>
