              <p><span class="headline">ENGINEERS SET THREE  VARSITY AND THREE FRESHMAN RECORDS EN ROUTE TO QUALIFYING 15 FOR NCAA DIVISION III CHAMPIONSHIP</span></p>
              <p align="left">May 16-17, 2013</p>
              <p align="left">The MIT Track &amp; Field team did not let the fact their final projects and theses were due, and that final exams being Monday, bother them. Quite the contrary, with three varsity and three freshman records and improved qualifying marks by most of the men's and women's team at the <a href="ecac.res.php">ECAC Division III Championship</a> hosted by Springfield College, it seemed the Engineers were once again showing how they can do it all and excel at everything.</p>
              <p align="left">The meet began for Tech, with Jackie Konopka '14 in the Heptathlon. The NCAA takes 22 women and 22 men per event and 16 relay teams per relay event as there are no longer any qualifying standards. Going into the week, Konopka was 19th on the list and thinking about sitting on her mark. By Wednesday she was 24th and realized she had to compete. Her two day event, began with the 100HH and her 15.25 was the second best in the competition and the second best in her career. In the High Jump, she cleared 5-3, matching her second best of the year. In the third event, the Shot Put, she threw a season best 34-4 3/4. Konopka ended day one with a huge PR of 26.31 in the 200. Going into day two, the event that was the most inconsistent for Konopka was the Long Jump, but she did nothing but put down three great jumps, ending with a career best of 17-4. She hurt her shoulder in the Javelin but came back with a PR of 2:27.71 in the 800 to smash the MIT record of 4625 points, win the event, and move up to number nine on the NCAA list.</p>
              <p align="left">Michelle Johnson '15 did not move up on the Triple Jump list, but she did add a centimeter to her existing varsity record, jumping 37-9 1/2 to place eighth. Carrington Motley '16 placed seventh with his second best jump ever at 47-0 3/4.</p>
              <p align="left">Both the men's and women's Pole Vault was the first day. In the women's event, Lauren Kuntz '13 won the event at 12-7 1/4 and Cimmy Virdi '16 placed fifth and earned a trip to Nationals with a freshman record vault of 12-1 1/4. Sherry Wan '16 cleared 11-1 1/2 but did not place. In the men's vault things did not go quite as well. None of the men improved their mark, but Cyrus Vafadari '13 placed sixth at 15-3 1/2. Clearing 15-9 would have likely earned him a trip to the NCAA's, but he was unable to make the height.</p>
              <p align="left">Tilly Taylor '15, not having the most spectacular season compared to her freshman year last year needed to find some motivation. She knew she needed to produce instead of go through the motions. In her most competitive meet of the season, she threw 136-4 into a massive head wind to place fourth.</p>
              <p align="left">In the final event of the night for MIT, Brooke Johnson '13, already with a solid qualifying time in the 1500 of 4:30.75, was going for a qualifying time in the 5K. The plan was to let others do the work if possible, but knowing she needed to run between 81-82 second pace, or about 17:00 to have a solid qualifying time. She was fortunate enough to have someone else set the pace through 2400 meters. She had been on 81 second pace at that point but then slowed to 83's as she took the lead. After hitting 3200 in 10:54 she hit another 83 and even though she started to lap runners Johnson slipped to 84 and then 87 pace before rebounding to 83 and then closing to 38 for the final 200 to win in 17:09.69. She is 25th on the NCAA list and may get in if some of those ahead of her choose other events at Nationals.</p>
              <p align="left">Day two, after the Heptathlon, began with the men's 4x100. For the first time this year, MIT moved the marks back a step or two in order to move the handoffs deeper into the zone so the outgoing runners would be at full speed at the handoff. Unfortunately this only slightly worked. On the first exchange Michael DuPlessis '14 was unable to catch Michael Kaba '16 without him slowing slightly to make the handoff. Josh Duncavage '13 left a little late and Kaba ran up on him. Duncavage did not have the speed at the end and barely caught Tyler Singer-Clark '14, who slowed slightly to make a good exchange. Even with that, Singer-Clark burned the final 100 to get second in a season best 41.66, just off the MIT record, but good enough for 14th on the NCAA list, guaranteeing the quartet a trip to Nationals.</p>
              <p align="left">States Lee '16 fought the wind in the 400, but was too focused on time instead of racing. Without the adrenaline provided by actually racing he fatigued early, placing fourth in 48.42.</p>
              <p align="left">Michael Kaba, came back in the 400IH, but without the ability to master his non-dominant leg and being too fast for 15 steps and not fast enough for 13 steps, he could not help but stutter for eight of the ten hurdles. Somehow, he still managed to run 53.10 and place fifth. If he can hold 13 for two or three hurdles he might break 51 and earn All America status next week. Adrian Samsel '16 had his best race of the season, running 55.10.</p>
              <p align="left">Pat Marx '13 was late getting to the meet the day before as he had to get his thesis turned in. This did not affect his performance as he ran the best race of the year, pacing well and winning his section in a season best of 1:52.17. However, someone from the slower section ran faster to take first overall, leaving Marx in second place. At the end of the qualifying period Marx was sitting in 21st place, one place out of the National field unless someone ahead does not enter.</p>
              <p align="left">In the women's Shot Put, Isabella Stuopis '16 reset the freshman record while placing fourth with a PR of 42-11.</p>
              <p align="left">Sarah Quinn '16, down to about 15-20 miles per week due to her continued problems with her calves, did not let it affect her competitiveness. She was entered in the 1500, sitting with a 4:36, but realizing she would need a sub 4:33 to have a solid shot at Nationals. Trying to relax more early in the race, Quinn managed to let others do the work and to chill in third or fourth place. The pace was relatively quick to start with, hitting the 400 in 71. The pace slowed to 2:25 but was still fast enough for national qualifying. The third quarter stayed slow at 3:39, but Quinn took the lead with 500 to go and pushed the pace, perhaps a little too early. She ran the final 300 in 53, but was passed by two to finish in third with a 4:32.49. This put her in 18th on the NCAA list, earning a spot at Nationals.</p>
              <p align="left">Tyler Singer-Clark finished the meet for MIT, racing the 200. Singer-Clark had a best of 21.62 and was in a fast field with a two meter per second tail wind. He was as pumped as he could be. Undefeated in the 200 for the season, he was not about to lose on this day. Out well and running a great turn, Singer-Clark pulled away on the home stretch, winning in a new MIT record of 21.37, ranked sixth on the NCAA list.</p>
              <p align="left">MIT will be taking 15-16 athletes to the NCAA Division III Championship in LaCrosse, WI from May 23-25. Representing the Engineers for the women will be Lauren Kuntz and Cimmy Virdi in the Pole Vault, Brooke Johnson and Sarah Quinn in the 1500, Tilly Taylor in the in the Javelin, Martina de Geus in the 3000 Steeplechase, Elaine McVay in the 10K, Jackie Konopka in the Heptathlon and possibly Johnson in the 5K. The men will be represented by the 4x100 Relay (Michael DuPlessis, Michael Kaba, Josh Duncavage, Tyler Singer-Clark), the 4x400 Relay (States Lee, Michael Kaba, Derek Barnes, Tyler Singer-Clark), Singer-Clark in the 200, Kaba in the 400IH and possibly Lee in the 400, Pat Marx in the 800 and Arinze Okeke and Carrington Motley in the Triple Jump. Those on the bubble will know for sure on Sunday and will then fly out to Wisconsin on Tuesday.</p>
