              <p><span class="headline">MIT Sweeps Engineer's Cup, McVay Crushes 10K</span></p>
              <p align="left">April 6, 2013</p>
              <p align="left">MIT hosted the annual <a href="eng.res.php">Engineer's Cup</a>, a meet between Division III engineering schools; MIT, WPI and RPI. Despite holding back some key athletes on the men's side, MIT won all but two running events to dominate the competition with a score of 180 to 128.5 for RPI and 97.5 for WPI. The women had a very balanced showing, winning 12 of the 19 events to easily outscore the competition, winning with 201 points to 110 for WPI and 86 for RPI.</p>
              <p align="left">The most spectacular performance was one that did not even count in the scoring. Because of the lack of opportunities to race a 10K in non-championship meets, MIT added a non-scoring opportunity to start off this meet. The goal was to have Elaine McVay '15 go for a national qualifying performance (sub 36 minutes) and for a quartet of the men to go for New England Division III qualifying times (sub 33 minutes). However, while the temperatures of upper 40's were conducive for fast 10K times, the 15-20 mph gusts screaming down the backstretch and first turn were not. McVay, as usual, did not allow anything to phase her, particularly the strong winds. She needed to run between 86-87 seconds per lap, but opened up with two laps of 83, which could have been disastrous, and should have been, given absolutely no competition and the fierce wind gusts. Finally getting in the groove, McVay hit the 1600 in 5:37 and started to roll, hitting 1600 splits of 5:47, 5:45, 5:46, 5:48 and 5:46 before closing in 85 to run a remarkable 35:54.68, second best in the country for Division III at this time. The men; Matt Jordan '15, Allen Leung '15, Roy Wedge '14 and Matt Deyo '16, worked together remarkably well through 6400 meters, trading the lead, hitting 1600 splits of 5:14, 5:13, 5:11 and 5:11. Deyo, running his first race ever for MIT after coming into the cross country season with an extensive stress fracture, started to fall off the pace, but still raced well to finish in 32:37.66. The others continued to work together through a 5:09 fifth 1600 and then Jordan began to pull away, dropping a 5:01 before closing in 66 to win in 32:05.39. Leung finished strong in 32:25.75 with Wedge close behind in 32:27.59.</p>
              <p align="left">The women had many highlights in the meet as they brought a strong team approach to the meet and competed very well. Veronica Szklarzewski '15 put together a big day, winning the Long Jump in a personal best 17-0 and taking second in the 100 in another PR of 12.50. Szklarzewski also joined Jackie Brew '14, Michelle Johnson '15 and Jackie Konopka '14 to win the 4x100 Relay in 50.77.</p>
              <p align="left">Brooke Johnson '13, only ran one event, but it was pretty spectacular, putting together a solo effort for the win in the 5K with a 17:11.67, good for number four on the NCAA performance list. Johnson led a 1-5 sweep MIT. Martina de Geus '14 ran a personal best of 17:45.36 for second. Kali Benavides '15 finished third in 18:41.49. Dacie Manion '15, in her first race back since incurring a stress fracture during the cross country season, ran 18:41.64 for fourth and Shivani Kaushal '15 placed fifth in 18:51.25.</p>
              <p align="left">Kayla Harris '15 had a breakout meet, winning the 400 in 61.13 despite the wind and coming back with a 26.98 for third place in the 200.</p>
              <p align="left">Lauren Kuntz '13, did the expected, winning the Pole Vault in 12-5 1/2, leading her teammates to a 1-2-4-5 finish. Cimmy Virdi '16 placed second in 10-11 3/4 while Joanna Chen and Marissa Engle finished fourth and fifth at 10-0.</p>
              <p align="left">Christine Labaza '14 had a breakout throw in the Discus, unleashing a 120-5 1/4 PR for the win. Labaza also earned a fourth in the Shot Put.</p>
              <p align="left">Michelle Johnson '15 won the Triple Jump with a leap of 34-11 1/4. She also took fourth in the Long Jump with 16-4 1/4.</p>
              <p align="left">Tilly Taylor '15 claimed first place honors in the Javelin with a wind obliterated 128-1.</p>
              <p align="left">Back on the track, Sarah Quinn '16 debuted in the 3000 Steeplechase. The effort was far from graceful or efficient, but she stayed upright to win in 11:23.56. There will be big improvement in this event as she gets accustomed to the barriers.</p>
              <p align="left">In the 1500, Alexandra Taylor '14 took on the wind head-on, running splits of 73, 2:31 and 3:49 before bowing to Louise van den Heuvel '14 on the home stretch 4:43.47 to 4:43.15. The two came back in the 800 to do battle along with Cindy Huang '15. Huang took the gold in 2:21.03, leading start to finish. Taylor added a third and van den Heuvel a fourth. The trio joined Isabel Crystal '16, who placed second in the 400 to win the 4x400 in 4:10.30.</p>
              <p align="left">Although she did not win an event other than being a part of the 4x100 Relay, Jackie Brew had a big day, placing second in the 100HH in 15.32, second in the 400IH in 65.91 and third in the Long Jump in 16-8.</p>
              <p align="left">The men were behind early in the meet as MIT did not score in the Hammer Throw or Shot Put, but came back strong in the Long Jump, Triple Jump and Pole Vault, scoring 52 of 66 possible points. Yida Gao '13 led the way for the Engineer's in the Pole Vault, winning in 14-1 1/4. Cyrus Vafadari '13 cleared the same height but lost on misses. Ben Schreck '15 competed well, earning third with 13-1 1/2 and Marshall Wentworth '16 placed fourth with 12-7 1/2. </p>
              <p align="left">Carrington Motley '16 was a double winner for Tech, taking the Long Jump and Triple Jump with respective jumps of 21-0 1/2 and 44-6. Arinze Okeke '16 took second in the LJ and fifth in the TJ. Angel Echevarria '16 claimed third in both events and Jackson Wirekoh '13 placed sixth in the LJ and fourth in the TJ.</p>
              <p align="left">Tyler Singer-Clark '14 was also a double winner, as well as anchoring both winning relays. Singer-Clark first brought the baton home in the 4x100 with a 42.70 performance despite substitutes States Lee '16 and Adrian Samsel '16 filling in for injured Michael DuPlessis '14 and Josh Duncavage '13. Singer-Clark then ran a big PR of 10.96 with a tailwind of only 2.1 mps. In the 200 he won in 22.01 and then ran a 49.0 anchor to earn the victory for Michael Kaba '16 (49.5), Richard Lu '16 (52.3) and Pat Marx '13 (50.4).</p>
              <p align="left">Kaba, on the 4x100 as well as the 4x400, ran an outstanding 54.61 debut in the 400IH for the win and placed third in the 110HH in 15.39.</p>
              <p align="left">Derek Barnes '16 led a 1-2 sweep in the 400 with a wind battered 49.36, just slightly ahead of States Lee in 49.38. Barnes was sick afterwards and unable to come back in the 200, but Lee did come back to get second in 22.24.</p>
              <p align="left">MIT dominated the 800-5K, winning all events and outscoring their opponents 45 to 15 for RPI to 6 for WPI. Pat Marx took the win in the 800, negative splitting with a 57 last 400 for a 1:57.96 performance. Harrison Hunter '15 almost held on for the win but still ran a very nice 1:58.35.</p>
              <p align="left">The 1500 was nothing but a sit and kick race and Kyle Hannon '13 buried the field with a 46 last 300, much of it into the wind to win in 4:09.27. Chadd Kiggins '15 supported with second place in 4:11.67, also running 46 in the final 300.</p>
              <p align="left">Logan Trimble '13 went out hard in the 5K, matching the pace set by early leader Justin Bullock '14 who ran 2:22 for the first 800. He then teamed up with Edgar Gridello '16 as they slowly moved up to catch Ian Tolan '16. By the 4000 mark Trimble and Gridello moved up to Tolan at 12:21. The trio worked together at that point. Trimble, who had fallen behind, gathered his pride and charged to the front to win in 15:21.51. Tolan could not quite hold the kick, but still finished well in 15:22.51. Gridello was right behind in 15:23.63. Bullock fell off his initial fast pace, finishing sixth in 15:46.20.</p>
              <p align="left">Next week, MIT hosts a trio of Maine teams; Bates College, Colby College and the University of Southern Maine. It will be a challenge for the Engineers to remain unbeaten, particularly the men as Bates has a very strong and well balanced team.</p>
