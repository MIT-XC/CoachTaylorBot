              <p><span class="headline">WOMEN RIDE RECORDS TO VICTORY, MEN PERFORM WELL BUT TOO SHORT-HANDED TO WIN</span></p>
              <p align="left">April 7, 2012</p>
              <p align="left">It seemed fate was trying to tell the MIT Track &amp; Field team they should not compete this weekend in the <a href="eng.res.php">Engineers Cup</a> hosted by RPI in Troy, NY. For starters, a very unusual number of injuries were plaguing the team, few of which are the normal overuse injuries seen in track and field. Second, 40 minutes out of Boston, one of the two motor coaches broke down. After transferring those who had to compete earlier in the meet on the bus that was working the others waited for a replacement bus. Before the replacement bus could make it to them it had to stop for a flat tire. A third bus was sent and that was the charm as the Engineers finally made the meet about fifty minutes after the start, which was in time for their upcoming events.</p>
              <p align="left">The women defied the message fate was trying to send and had a very strong meet. Winning every single running event with the exception of the 4x400 Relay, in which they ran their B 4x400 team, and winning four of the eight field events, Tech easily won the meet, scoring 201 points to 110 for WPI and 81 for RPI. The men were simply too undermanned, missing 13 very key athletes for a variety of reasons, not all of which were injuries. The men's competitiveness was there, but with only six field event athletes and no one in the Discus Throw, Long Jump or Triple Jump, it just was not going to happen. Nevertheless, MIT placed second with 127 points to 177 for RPI and 102 for WPI.</p>
              <p align="left">Tilly Taylor '15 had the highlight performance of the meet for MIT, launching a 140-3 javelin throw to smash the MIT record, both varsity and freshman, by 9', laying down a very strong NCAA Division III qualifying mark. Taylor's victory was not the only record producing performance for the Engineers. Jackie Brew '15, taking off nearly two feet behind the foul board, landed 18-1 3/4 beyond the foul board for a new MIT record and  a 6&quot; PR. Cindy Huang '15 set a freshman record in the 800, running 2:21.87 with no competition as she won easily from wire to wire. Elaine McVay '15 also set a freshman record, hers in the 5K, running 17:41.24 for the victory on the windy day.</p>
              <p align="left">In addition to the records there were a plethora of victories. Marissa Engle '15 took first place honors in the Pole Vault, clearing 10-11 3/4. Christine Labaza '14 threw 108-2 to win the Discus Throw. Michelle Johnson '15 continued to set PR after PR. After jumping a personal best of 17' for second place in the Long Jump, she won the Triple Jump with a leap of 37-5 3/4, narrowly missing the MIT record.</p>
              <p align="left">In the running events, Portia Jones '12 did her usual damage to the opposition, winning the 100 and 200 and anchoring the 4x100 (Brew, Veronica Szklarzewski '15, Jamie Simmons '12) to victory. All of her events were into a strong wind, making fast times impossible, but she nevertheless ran 13.17, 25.89 and anchored the relay in 48.44. Brew also won the 100HH, just inching out Jones in 15.37 to 15.39. Jamie Simmons added a couple of victories to her sprint relay effort. In the 400, she battled a terrible head wind on the homestretch to win in 58.36 and then came back in the 400IH to win in 63.49 even though she virtually stopped coming into hurdle six. Brooke Johnson '13 shook off a nagging IT band problem to win the 1500 in 4:46.39. Claire O'Connell '14 won the 3000 Steeplechase in 11:59.13, showing much improved hurdling technique over last week.</p>
              <p align="left">For the men, Tyler Singer-Clark '14 won the 200 in 22.62, overcame a poor start to nearly win the 100, losing by .02 into the headwind and ran a superb leg on the makeshift 4x100 Relay that ran 43.61 with two replacement legs (DJ Ronde '13, Mac Gager '14). Sam Parker '15 was not seriously challenged as he easily pulled away from the field in the 800 to win in 1:57.52, leading MIT to a 1-2-3 finish. Kyle Hannon '13 also led a 1-2-3 sweep, as he and his Tech teammates severely negatively split  the 1500, running 2:02 in the second half of the race to win in 4:07.46. Benji Xie '15 ran a superb race in the 5K, fighting off a suicide pace by an RPI runner trying to steal the race, to win in 15:22.07. The only field event victory was earned by CK Umachi '12 in the Hammer Throw as he threw 164-5 despite an aggravating groin injury.</p>
              <p align="left">Next week, MIT travels to Colby College to face the host as well as Bates College. Hopefully Tech will return many of those not competing this past weekend.</p>
