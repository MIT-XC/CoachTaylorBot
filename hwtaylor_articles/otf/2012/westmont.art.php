              <p><span class="headline">DESPITE DEPLETED SQUAD, MEN AND WOMEN WIN WESTMONT INVITE</span></p>
              <p align="left">March 31, 2012</p>
              <p align="left">MIT Track &amp; Field enjoyed their Spring Trip to California where the warm weather permitted excellent training for the Engineers. The final day of the trip, Tech drove from Claremont to Santa Barbara to compete at Westmont College in the <a href="westmont.res.pdf">Westmont Invitational</a>. The Engineers brought 40 athletes on the trip, 20 men and 20 women, approximately 40% of the total squad. Despite the fact that only 16 men and 19 women competed, and many of those athletes were pulled from additional events due to injuries or to prevent injuries, MIT still managed to win the meet. The men scored 69 points to 68.5 for host Westmont in the eight team field. The women scored 84.66 points to outlast the 78.5 points for Fresno Pacific.</p>
              <p align="left">There were not a lot of outstanding performances in the meet. Perhaps the 60 degree temperatures and eventual rain had something to do with it, or maybe the Engineers were just not ready to compete. From a team perspective it was a dismal day despite the victory.</p>
              <p align="left">One of the few bright spots for the men was CK Umachi '12. Umachi won the Hammer with a throw of 166-07, won the Discus in the rain at 137-03 and took fourth place honors in the Shot Put at 44-00 1/2. Altogether he scored 17 points in the 7-5-4-3-2-1 scoring format. Running well for MIT were Dan Harper '12 in the 5K and Stephen Serene '12 in the 3000 Steeplechase. Harper was all alone from the gun, controlling the race from the front to run 15:10.08, and this was after running the 1500 in 4:08.14. Serene was the lone competitor in the Steeple, but nevertheless ran a competitive time of 9:33.25.</p>
              <p align="left">For the women, Jackie Brew '14 was the high point scorer, although a few more of the women performed well. Brew earned third in the Long Jump at 17-03 1/2, third in the 100HH in 15.1 (hand time due to camera failure), fourth in the Triple Jump at 34-09 3/4, sixth in the 100 with a 12.75 clocking and led off the victorious 48.32 4x100 Relay. Brew accounted for 13 3/4 points for Tech. Also performing well was Portia Jones '12, who appeared to have won the 100HH, but without a picture to back it up, was awarded second place in 14.8. Jones also placed third in the 400IH in 63.48 and had a superb anchor leg in the 4x100 to walk-down the leader and win by .01 seconds. Michelle Johnson '15 won the Triple Jump with a 35-09 1/2 effort and Lauren Kuntz '13 won the Pole Vault at 12-01 1/2. Jamie Simmons '12 ran an excellent third leg in the 4x100 after having to start over from a near dead stop due to the fact she had to wait on her teammate Maddie Bairey '15, who was having severe breathing difficulties due to allergies. Simmons had a busy and fatiguing day as she came back to win the 400 in 57.56 and then secure a fifth place in the 400IH with a 65.05. The final victory for the women, which was actually in the first event, was earned by Maggie Lloyd '12 in the 3000 Steeplechase. Lloyd ran a controlled 11:29.10 to easily lead a 1-2-3 sweep by MIT. Lloyd came back in the 800 to place fifth in 2:22.32, showing a very competitive spirit.</p>
              <p align="left">Although they did not win an event, nor run spectacular times, both Tyler Singer-Clark '14 and Justin Bullock '14 competed very well. Singer-Clark placed third in the 200 in 22.29, fourth in the 400 in 49.13 and ran an excellent second leg on the 4x100 Relay that earned second in 43.03. Bullock placed second in the 1500 after seemingly giving up early in the race, running 4:01.28, and again came from way behind in the 800 to place fifth in 1:58.93.</p>
              <p align="left">Although it is very early in the season, Tech Track &amp; Field needs to regain the spirit that has made them a National level team. Having the entire team together for the first  meet on April 7 against RPI and WPI will be a good measuring stick to test the competitiveness of both the men's and women's teams.</p>
