              <p><span class="headline">MATTOCKS, SERENE LEAD WAY WITH EXTRAORDINARY PERFORMANCES AT ECAC DIVISION III CHAMPIONSHIP</span></p>
              <p align="left">May 17-18, 2012</p>
              <p align="left">The last opportunity for teams to qualify for the NCAA Division III Championship ended on Friday. MIT traveled to the <a href="ecac.res.php">ECAC Division III Championship</a>, hosted by RPI in Troy, NY. The weather was excellent with 70 degree plus temperatures both Thursday and Friday and cooler temperatures on Thursday evening for the distance races.</p>
              <p align="left">Leading the way for MIT on day one was Stephen Serene '12 in the 3000 Meter Steeplechase. Entering the event with a PR of 9:17, but knowing he had not yet raced a full race without giving up at some point, Serene was determined to not let this be his last race. With the meet falling 30 minutes behind schedule, the steeplers were anxious to get going. The initial pace was crisp, hitting the first 400 in 67. From that point on Serene stayed with the leaders through lap after lap of 74 second quarters. Serene fell back briefly in the fifth lap, looking as though he might fall back to his old habits, but he instead caught back up and tried to win. In a very high quality field, his final 200 in 34 was only enough for third place, but his 9:04.32 was a huge MIT record and put him 12th on the phenomenal NCAA list where it will take a sub 9:09 to make it into the NCAA Championship field.</p>
              <p align="left">Also running an NCAA Division III qualifying time was the women's 4x400 Relay team of Jamie Simmons '12, Martha Gross '12, Jackie Brew '14 and Portia Jones '12. Going into the meet the team had not run together due to the injury to Brew, so their 3:58 only qualified them to race in the second of three sections. In order to race 3:50, which was probably what it would take to go to the NCAA Division III Championship, the quartet would have to do it without competition. Simmons led off with her typical outstanding 55.9 split to put the Engineers far into the lead. Gross took the baton, went out hard, but strained to much, trying to run fast rather than letting it happen. On the final 100 she locked up, barely making it in with a 59.1 leg. Brew, not having run a 400 since indoor Nationals, just did not run fast due to inexperience and possible doubt. Her 60.0 meant Jones would have to do something amazing. Jones did not play, running an excellent leg to finish in 55.9, giving Tech a 3:51.05. Three teams from the fast section beat their time, putting them 13th on the NCAA list.</p>
              <p align="left">Nathan Peterson '12 improved his NCAA qualifying performance by winning the Pole Vault with an outstanding jump of 16-5 1/4. Peterson and three others were jumping at the height, but he was the only one to clear, and he did so on his final attempt. Also in the vault, Lauren Kuntz '13 placed third in the women's vault, clearing her usual 12-1 1/2, again having solid attempts at 12-7.</p>
              <p align="left">CK Umachi '12, placed second in the Hammer Throw with a 182-5 performance. After barely making the finals it was a nice comeback for Umachi. Also in the field events, Tilly Taylor '15 placed third in the Javelin Throw at 134'.</p>
              <p align="left">The final performance of the first day of competition for MIT was the 10K. Tania Morimoto '12 fighting an injury all spring, remained hopeful for making Nationals. The initial pace for the leaders was very fast, catching Morimoto by surprise. The two leaders opened up a thirty meter lead on the pack when Morimoto decided she needed to close the gap or risk not making a fast enough time. By the 3200 mark Morimoto had caught up to the leaders. After she caught them the pace slowed a bit, so Morimoto took the lead, knowing if they did not stay on pace they would not make the National field which would likely take a sub 36:20. Continuing to hammer out 86 second quarters, Morimoto did the work for the leaders, hitting the 5K in 17:59. This is about where things started to unravel for Morimoto. Never one to fear dying in a race, Morimoto continued to press despite the fact she was in significant pain. Even when she realized she would not run a qualifying time, she did not give up. Finishing in a fine time of 37:22.47, in fourth place, Morimoto finished her collegiate career in style.</p>
              <p align="left">Day two began with the women's 4x100 Relay. In order to improve the time of 47.48 it was necessary to expand the exchange zones, which is always a little risky. The first handoff between Brew and Gross looked to go a little too deep into the zone, possibly causing Gross to back off a little in order to make sure the handoff was safe. The second handoff to Simmons seemed right on, putting MIT in good position. The final handoff did not go well as Jones slowed down to make the handoff work. The final leg by Jones was half-spirited as she went through the motions rather than compete all out. The end result was a poor time of 48.20 for third place.</p>
              <p align="left">The next event was the 1500. Both Ben Mattocks '12 and Justin Bullock '14 were trying to gain qualifying times and both were in the fast section. The first quarter was a screaming 58 and Mattocks was right in the front. Bullock was being conservative but still came through in 60. At the half Mattocks was still up front at 2:00 while Bullock fell way off the back in 2:04 and dropped out not long afterwards. Mattocks continued to ride the train, splitting 3:04. As he made his way through the final turn, he briefly took the lead as he tried to kick it in for the victory. He battled all the way to the finish, ending up in second in 3:50.51, just .05 out of first. That is certainly a time that will make it to Nationals.</p>
              <p align="left">Jones was up next in the 100HH final. As soon as she realized she was behind at the start she started to give up, and just like the 4x100, she just cruised in to finish seventh in a dismal 16.01.</p>
              <p align="left">In the 400, Gross ran, but was not her competitive self, finishing in 60.77. Running the 4x400 on day one may have taken its toll on many of the MIT relay members.</p>
              <p align="left">Alexandra Taylor '14 was giving it one last shot in the 800 this outdoor season. She was in the fast section, so the pace promised to be pretty quick as many were trying to improve their time for Nationals. The first 400 was an evenly paced 65 as Taylor went to the lead to start the second lap. However, on the back stretch she tightened up and was unable to hold onto the lead. She finished in 2:16.97.</p>
              <p align="left">Jamie Simmons '12 did not have a great day in the 400IH. She got out very well, looking great over the first hurdle. She stutter stepped the next few hurdles and seemed to lose steam on the sixth hurdle. Uncharacteristically, due to feeling ill, she was unable to come back, fading to seventh in 64.14.</p>
              <p align="left">Jones scratched out of the 200, but Gross ran a season best 25.50, looking very good the entire race despite the soft track. Also in the 200, Tyler Singer-Clark just did not have it, finishing in 22.09 for eighth place.</p>
              <p align="left">The Engineers now must wait to see who else around the country makes into the top 20 (men), 22 (women) or 16 (relays) and hope they are not bumped out. At the moment the men have Pat Marx '13 in the 800, Kyle Hannon '13 and Mattocks in the 1500, Serene in the 3000 Steeplechase, Dan Harper '12 in the 10K, Umachi in the Hammer and Peterson in the Pole Vault. The women have Jones in the 100HH, 200 and 400IH, Simmons in the 400IH, Elaine McVay '15 in the 10K, Taylor in the Javelin, Kuntz in the Pole Vault and both the 4x100 Relay and 4x400 Relay with Jones, Simmons, Gross and Brew.</p>
