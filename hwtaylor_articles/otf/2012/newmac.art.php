              <p><span class="headline">MIT Engineers NEWMAC Conference Championship Victories--Men win 12th in Row, Women 4th</span></p>
              <p align="left">April 28, 2012</p>
              <p align="left">Although both the men's and women's teams still have some key injuries, many of their hurt athletes are back and doing just fine. Both teams performed brilliantly at the <a href="newmac.res.php">NEWMAC Conference Championship</a>, hosted by Smith College, with the men gathering nine victories and the women eight. Despite the cool temperatures and windy conditions, the men scored 267 points, easily outdistancing runner-up WPI with 179 points. The women, who pulled out the top three seeds in the 5K once the victory was secure, scored 208 points to runner-up Wheaton's 144 points.</p>
              <p align="left">MIT seems to be rounding into championship form at the right time. Sheldon Trotman '15 showed just how much the Engineers missed him as he put together a very impressive meet. Trotman jumped a 6-5 PR in the High Jump to win that event, just missed 22' in the Long Jump to finish second and won the Triple Jump with a 45' effort. Michael DuPlessis '14 competed for the first time this season, placing second in the 100 in 11.11 and leading off the 4x100 Relay with a tremendous leg to set MIT up for a second place in 42.59.</p>
              <p align="left">MIT scored more than 60 over their seeds. Not seeded to score in the 10K as none of the MIT entries had run the race this season, MIT began scoring some of those unseeded points in the very first event. Matthew Jordan '15 ran an outstanding race, placing third in 32:27.63 and teammates Ricardo Paez '15 and Matt Weaver '12 placed 5th and 6th in 33:15.86 and 33:25.94.</p>
              <p align="left">After an excellent third place finish in the 3000 Steeplechase by Eric Safai '14, running a PR of 9:33.62 to join teammates Stephen Serene '12 in second and Logan Trimble '13 in sixth, DuPlessis, Tyler Singer-Clark '14, Nick Diamantoni '15 and Trotman '15 dropped a half second off their season best to place second.</p>
              <p align="left">MIT kept the pressure on with a 1-2-5-6 finish in the 1500. Kyle Hannon '13 led the way with a 3:55.74, followed immediately by Ben Mattocks '12 running a four second PR in 3:56.31. Jay McKenna '13 dropped his own PR of 3:59.33 on his opponents for fifth place and Justin Bullock '14 had a tough day, finishing sixth in 4:01.07.</p>
              <p align="left">Brian Djaja '13 and Brendan Ensor '12 added some much needed early points in the 110HH. Djaja was third in 15.83 and Ensor ran a PR of 16.39 for fifth.</p>
              <p align="left">The next big event for Tech was the 800. Seeded 1-2-3, MIT did exactly that. Sam Parker '15 took the pace out, leading the way through the 400 in a slow 58. Parker was closely followed by teammate Gil O'Neil '13 with Pat Marx '13 hanging out in fifth. With 150 meters to go, Marx made his move, passing his teammates on the home stretch to give MIT the sweep in 1:56.52, 1:56.83 and 1:57.24.</p>
              <p align="left">Supporting Trotman in the jumps, Jackson Wirekoh '13 jumped a monster PR of 21' in the Long Jump for third place and added a triple jump of nearly 45' for second place. John Thomas '15 and Matt Falk '12 added a sixth and seventh in the High Jump and Nathan Peterson '12 and Colin Hong '15 earned first and third in the Pole Vault.</p>
              <p align="left">CK Umachi '12 took care of business in the throws, winning both the Hammer and Discus Throws. In the Hammer, Umachi threw 164' and in the Discus he threw 153'. Although not his specialty, Umachi added a fifth place in the Shot Put. Freshmen Cameron Wagar and Harry Rein went 1-2 in the Javelin throw at 176' and 172' respectively. Rein's 172' was a personal best.</p>
              <p align="left">With the meet locked up, the 5K runners were given a choice whether they wanted to race or not. Dan Harper '12, Roy Wedge '14 and Benji Xie '15 elected to race, but were instructed if they race they would have to not take the lead and to not push too hard, saving their best for next week in the New England Division III Championship. Harper went with the leader, who after the 800 in 2:20 decided he wanted Harper to share pacing duties. Harper, following directions, was staying behind as the pace slowed dramatically to 74 seconds per quarter and eventually 78 seconds before Harper was given the green light after 3600 meters. The next 1200 was a comfortable 3:29 as Harper pulled away easily for the victory in 15:05.01. Wedge and Xie claimed third and fourth in 15:23.03 and 15:24.22.</p>
              <p align="left">The women were a little low on depth due to keeping Jackie Brew '14  to just the 4x100 Relay and the Long Jump, the absence of Veronica Szklarzewski '15 in the jumps, Maggie Lloyd '12 in the Steeplechase, Kristine Bunker '14 in the High Jump, Jackie Konopka '14 in many events and Maddie Bairey '15 in the long sprints. Those competing were asked to get the job done and not worry about who was not competing.</p>
              <p align="left">The ladies did just that. As usual, Portia Jones led the way. First she anchored a 4x100 team of Brew, Martha Gross '12 and Jamie Simmons '12 to a season best 47.84 victory, good for 10th in the nation. She added a first in the 100 in 12.44 and first in the 200 in 25.30 and a second in the 100HH in 14.53. </p>
              <p align="left">Simmons '12 was not her usual self, but still managed a come from behind victory in the 400IH in 63.09 and a second place in the 400 in 57.33. She also led off the 4x400 with a strong 57.5 to set up Gross, Cindy Huang '15 and Jones to run a 3:58.49. In the 400 Gross added support with a fifth place 59.09 and Martina de Geus '14 came back from winning the 3000 Steeplechase in 11:26.55 to add a sixth place in the 400IH in 70.04. Helping de Geus out in the Steeple were Claire O'Connell in second in 11:39.13 and Alexandra Taylor in fourth in 11:54.23 despite still recovering from a cold.</p>
              <p align="left">Prior to the 4x400, Huang took first place honors in the 800 in a strategic sit and kick, 2:19.08. She came through a slow 70 second first quarter and then unleashed a vicious kick in the final 200 to easily pull away. Brooke Johnson '13 and Louise van den Heuvel '14 had to run in the slower section due to not having a time this season, but managed to place fourth and fifth overall in 2:21.74 and 2:22.01. Taylor came back from the Steeple to place eighth.</p>
              <p align="left">Earlier in the meet, Johnson was involved in a very strategic 1500 where the pace was 79, 78, 73 and a final 300 in 44 for a 4:44.77 second place. Elaine McVay '15 dropped down from the distance events to place third in a surprising 4:46.78. Distance mate, Dacie Manion '15 added a fifth in 4:49.45 and van den Heuvel was sixth in 4:50.93.</p>
              <p align="left">Although not a victory, Kaitlyn Allen '14, running her first race ever for MIT, showed her talent, as she place third in the 10K in 39:50.77</p>
              <p align="left">In the field events, MIT had two victories. Tilly Taylor '15 continued her undefeated season with a 135' throw for first place in the Javelin. Preethi Vaidyanathan '15 surprised everyone with a first place in the High Jump, clearing 5' 3&quot;.</p>
              <p align="left">Michelle Johnson '15 added a 36' second place in the Triple Jump. Christine Labaza '14 threw 115' again to earn filth place in the Discus Throw. Ashley Wheeler '15 threw an impressive 136' in the Hammer Throw to earn a surprising fifth place. The Pole Vaulters came through as usual, with Lauren Kuntz '13 leading the way with a 11-6 second place, followed by Marissa Engle '15 in third, Joanna Chen '15 in fourth and Kate Wymbs '14 in eighth.</p>
              <p align="left">Next week, MIT turns its attention to the New England Division III Championship, which will be hosted by MIT. Last year the women won the meet in the final event while the men finished second in a very close meet.</p>
