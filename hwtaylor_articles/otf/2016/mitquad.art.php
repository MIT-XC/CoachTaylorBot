                  <h3>Event Victories Add Up to Win Quad Meet</h3>
                </center>
              
              
              <p>April 9, 2016              </p>
              <p>The MIT Men's and Women's Track &amp; Field teams were favored to win their home <a href="MITQuad.pdf">Quad meet against Bates, RPI and Tufts</a>, and despite not competing many athletes for precautionary measures with regard to injuries, and a more competitive schedule coming up in the remaining weeks, both the men and women came away with 10 event victories each, leading each team to the overall win.&nbsp; The men overcame no entries in the Steeplechase and a lack of healthy runners in the middle-distance events to win with 232 points to 158 for RPI, 146 for Bates and 125 for Tufts.&nbsp; The women overcame a severe lack of available depth in the running events shorter than the 5K by scoring 142 of their 206.5 points in the eight field events to win over RPI with 149 points, Tufts with 147.5 and Bates with 147.</p>
              <p>Jackie Vahey '18 led the women with three victories to lead them with 27 points in the 9-7-6-5-4-3-2-1 scoring format.&nbsp; She started by winning the Discus with a 137-10 1/4 effort&nbsp;in a 1-2-7 finish, then took the Shot Put by a centimeter over teammate Isabella Stuopis '16 at 39-10 1/2 for a 1-2-3 effort and finished with&nbsp;a Hammer victory at 159-4 3/4 to help her team to a 1-4-5-8 dominance.</p>
              <p>The Engineers did not compete anyone in the 3000 Steeplechase due to injury and did not need to run the 4x400 Relay at the end of the meet having already clenched the victory.&nbsp; </p>
              <p>Hannah Chen '18 scored 21 3/4 points although she was only victorious in the 400IH at 63.49.&nbsp; In the Long Jump her coaches limited her to three attempts, which netted her a fourth place at 16-9 1/4 in support of her teammates Nneoma Okonkwo '18, who won the event at 17-11 1/2 and Haley Strouf '18, who placed third with a jump of 17-2.&nbsp;She then anchored her team of Okonkwo, Strouf, and Kendra Knittel '17 to place second in the 4x100 Relay in 49.25.&nbsp;Prior to the 400IH she had to run two and a&nbsp;third efforts in the 100HH to finish third in 15.73 into the wind.&nbsp; In the first 100HH, the starter's pistol had a faulty shell that was not loud enough to start the automatic timing system.&nbsp; After the men's 110HH race, the women ran again.&nbsp;Another misfire was caught and the women stopped after three hurdles.&nbsp; Finally, the fatigued group of hurdlers completed the race, giving Chen the third place.</p>
              <p>Knittel added a victory in the 400 in 59.64 and a second place into the wind in 26.46&nbsp;to add 17 3/4 points to her team's effort.&nbsp; Okonkwo placed second in the 100, also into the wind in 12.95 to  give her 17 3/4 points as well.</p>
              <p>Natalie Alper '17 only competed in the High Jump as she comes back from a minor hip flexor injury.&nbsp; She made the most of that effort, winning with a 5-4 1/4 performance.&nbsp; Taylor V'Dovec '19 and Katherine Prutz '18 added a third and fourth at 5-0 1/4.&nbsp; The most dominant event by far was the Pole Vault.&nbsp; Sherry Wan '16 led the 1-2-3-4-6 domination with an outdoor PR of 11-7 3/4.&nbsp; Jen Tylock '17 added a second at 11-1 3/4, then Makenzie Patarino '19 claimed third, also at 11-1 3/4, followed by Madeleine Waller '17 in fourth at 10-8.&nbsp; Leah Schmitz '16 closed out the scoring with a 9-2 1/4 for sixth.</p>
              <p>Dana Balek '18 had a come from behind victory in the Javelin, throwing a personal best of 120-1 1/2 on her fifth throw.&nbsp;The final victory by Tech was earned by Mary Eccles '18 in the 5K.&nbsp; Eccles stole the event by turning a 76 opening 400 to open up an 80 meter lead on the rest of the pack that began the race with an 88 first 400.&nbsp; The wind and lack of competition had her running 85-86 pace for most of the rest of the way, gradually opening up a 100 meter lead on the chase pack as she lapped the majority of the field.&nbsp; In the final two laps, Jessica Wilson of Bates made a hard charge to nearly catch Eccles, but could not quite complete the task as Eccles won in 17:47.85.</p>
              <p>The men's victory was more spread out in that the Engineers scored 102 in the field events and 130 in the running events despite losing nine points by dropping the baton in the 4x100 Relay for the second time in three meets.</p>
              <p>MIT was led by two double winners.&nbsp; Tre Albritten '18, running both of his races into the wind, took the 100&nbsp;in 11.06 and then returned later to claim&nbsp;first place honors in the 200 in 22.34.&nbsp;&nbsp;The other double winner was Michael Kaba '16, who easily won the 110HH into the&nbsp;wind in 15.37 and then ran a season best of 53.71 in winning the 400IH.&nbsp; Both Albritten and Kaba ran the failed 4x100 Relay, comprising the first two legs.</p>
              <p>The Engineers had many dominant events.&nbsp; The highest scoring event was the Triple Jump with 25 points.&nbsp; Billy Ruschel '18 won the event with a leap of 45-10 and was supported by Angel Echevarria '16 taking second place, Carrington Motley '16 claiming fourth and Ryan Prinster '18 earning fifth.&nbsp; The next highest scoring event was the 400 with 24 points where Tech went 1-2-3-7.&nbsp; States Lee '16 took first in 48.60, Danny Newman '17 second in 49.26, Derek Barnes '16 third in 49.57 and Tom Frejowski '19 seventh n 51.03.&nbsp; At 23 points, the MIT 200 runners, led by Albritten, took 1-2-3-8 finishes.&nbsp; Lee was second, Newman third and Barnes eighth.&nbsp; The final dominant event was the Long Jump, scoring 22 points for MIT.&nbsp; Arinze Okeke '17 won  at 21-11, followed by Motley in third at 21-2, Ruschel in fourth at 21-0 and Prinster in eighth.</p>
              <p>There were two victories earned by freshman. Scott Cameron '19 earned the victory in the Pole Vault with a jump of 14-11.&nbsp; Jarod Wilson '19, after failing to win the 1500 when he took the lead a little prematurely with 400 to go and started doubting himself, came back in the 800 to run a flawless come from behind victory in 1:54.98.&nbsp; The final victory of the day was earned by the 4x400 Relay.&nbsp; Frejowski led off with a 52.5.&nbsp; Henry Tareque '17 put his team right on the tail of the leader with a 51.4.&nbsp; Talla Babou '19 ran a very competitive 52.6&nbsp;to keep his team close, handing off about five meters out of first.&nbsp; Newman, making up for the dropped baton in the 4x100, ran a 49.5 to pull out the victory in 3:25.90.</p>
              <p>A notable performance were turned in by Alex Noakes '16, throwing a PR of 150-1 3/4 for fourth place in the Hammer.&nbsp; The 5K also produced spectacular competition in the 45 degree, cloudy and windy conditions.&nbsp; Benji Xie 'G returned to competitive form following an injury to take third in 15:04.55, taking the lead in the final 400, but lacking the final kick to hold it.&nbsp; Daniel Weiss '19 followed in fifth in 15:11.09.&nbsp; David Walter '18 ran a superb race to finish sixth in 15:12.74 and Cooper Sloan '18 claimed seventh&nbsp;place in 15:19.98.</p>
              <p>Next week MIT travels to Williams College for a good old-fashioned dual meet.&nbsp; The meet should be a classic matchup between the top two Division III teams in New England. </p>
