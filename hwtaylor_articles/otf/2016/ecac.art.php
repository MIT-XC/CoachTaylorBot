                  <h3>Two MIT Records, Three More National Qualifiers Lead Engineers at ECAC DIII Championship</h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>May 18-19, 2016              </p>
              <p>As MIT prepares for the NCAA Division III Championship next week, the final opportunity to make the NCAA field was the ECAC Division III Championship&nbsp;hosted by Westfield State University.&nbsp; The Engineers did not let the chance go to waste as they qualified for Nationals in three more events.&nbsp; </p>
              <p>Bailey Tregoning '19 continued to surprise her opponents as their seems to be no limit to what the freshman can achieve.&nbsp;The 800 specialist, having dropped three more seconds last week when she earned second place at the NEICAAA Championship with her 2:11.51 was sitting in 19th position on the NCAA list where only the top 22 women would be selected in each event.&nbsp; She knew her time would probably not hold&nbsp;and would likely have to run sub 2:11 to make the final field.&nbsp; The pace for the first 400 was a casual 65.8, meaning Tregoning was going to have to take control and negative split her race.&nbsp; She did just that as she eased to the front on the backstretch, accelerating into the final 200 where she gapped the field to win in 2:10.84.&nbsp; In the process she broke her own MIT varsity and freshman records.</p>
              <p>The men's 4x400 Relay also needed a faster time in order to qualify.&nbsp; Currently only having a 3:16.73, they knew it would take at least a 3:14 to have a good chance of making the field.&nbsp; MIT decided to leave one of the faster legs (Michael Kaba '16) out of the relay to save his foot from too much racing&nbsp;and, more importantly, so&nbsp;that if the team were to qualify it would give them an automatic substitute since Kaba was already in both the 110HH and 400IH fields.&nbsp;As the second seeded team they were not sure the competition would be enough to pull them along to a fast enough time.&nbsp; States Lee '16 returning to competition after missing the past two meets with a ligament injury, led off with a solid 48.7.&nbsp;Chris Sweeney '18 blistered the first 200, taking a commanding lead, but paid dearly in the final straight as he handed off in 49.0.&nbsp; It seemed unlikely the Tech runners would be able to run the necessary splits to reach 3:14.&nbsp; Fortunately no one told Derek Barnes '16 and Danny Newman '17.&nbsp; Barnes ran the third leg, starting with the lead and holding it throughout the entire lap, splitting an amazing 47.8.&nbsp; The handoff to Newman was excellent, giving him a nice running start.&nbsp; Newman fought off the other anchor legs, crossing the&nbsp;line in 3:13.32 for the victory, splitting 47.5&nbsp;and earning his team a trip to&nbsp;Wartburg College in Waverly, Iowa, the site of next week's National Championship.</p>
              <p>Ariela Slutsky '18 was hoping to qualify for Nationals in the Discus, but knew she needed to add significantly to her 141-3 PR from last week.&nbsp; Her first five throws were either flat releases or nice angles with no finish.&nbsp; However, on her final throw she put it together, throwing 148-7 to move up to second place and put herself in the NCAA field of qualified discus throwers. Cody Bratten '18 was hoping to accomplish the same thing, but fell just short, throwing a fine 152-10 for sixth place.</p>
              <p>While the 4x100 Relay (Sweeney, Kaba, Newman, Tre Albritten '18) was also hoping to drop their time to make the national field, it was not to be.&nbsp; A couple of poor starts and not having the best handoffs resulted in a 41.72 season best, but not fast enough to prolong their season.</p>
              <p>Ben Schreck 'G put together a 15-7 1/4 vault, but kept blowing through poles as he tried to clear 16', eventually running out of attempts as he tried to find the right pole.&nbsp; He earned third place while teammate Scott Cameron '19 placed eighth with a 15-1 1/2 vault.&nbsp; In the women's vault, Jen Tylock '17 was still not cleared from her concussion, but Sherry Wan '16 was hoping to improve enough to join her teammate at Nationals.&nbsp; Wan performed well, clearing a PR of 11-10, but was unable to go any higher.</p>
              <p>MIT left most of their jumpers at home, but they did bring Carrington Motley '16 and Angel Echevarria '16.&nbsp; Motley, already going to Nationals, nevertheless wanted to work on improving his jump.&nbsp;&nbsp; Echevarria had not qualified and thought that he had the jump in him to get the job done.&nbsp; Motley earned third place with a 47-5 jump, just fouling a couple of longer jumps.&nbsp; Echevarria never got it going, jumping 44-2.</p>
              <p>Albritten  placed second in the 200, running an excellent 21.57 in zero wind.&nbsp; Taking the previous week off from competing and only racing the 200 and 4x100 this week should have the sophomore ready for the NCAA Division III Championship.              </p>
              <p>Kaba&nbsp;ran 14.68 in the 110HH trials and then 14.62 in the finals to place fourth.&nbsp; He is very close to making a big drop in time, which will be needed to score at Nationals.&nbsp; He did not race in the 400IH&nbsp;in order to save his foot&nbsp;for next week.&nbsp; Sweeney won his section in the 400IH, but fell just short of making the NCAA field, placing fourth overall in 53.73.</p>
              <p>Benji Xie 'G, having won the 10K each of the past two weeks, winning the New England Division III and NEICAAA titles, wanted to try one more time to make Nationals, this time in the 5K.&nbsp; The pace was never fast enough by the leaders, running just barely sub 70 pace for splits of 4:39 and 9:18. Xie's legs started feeling the effects of the weeks of racing, preventing him from speeding up.&nbsp; However, his determination was outstanding as he&nbsp;only slowed to 71 pace, finishing in second place with a 14:38.07.</p>
              <p>Natalie Alper '17 was another MIT athlete looking for a qualifying mark.&nbsp;Unable to jump well due to a strained hamstring the past two weeks, the junior was finally able to jump without worry.&nbsp; After clearing 5-4 1/2 she had a fine attempt at 5-6 1/2 but was unable to clear.&nbsp; She did place sixth.</p>
              <p>Hannah Chen '18 once again had a great start in the 400IH, and even though she was on 17 steps, her backstretch was just too slow, allowing the entire field to get the lead on her.&nbsp; She again&nbsp;finished strong until the final hurdle, but only managed 62.17 in placing third.</p>
              <p>Although leaving most jumpers and distance runners at home, as well as others, the Engineers still placed second in the men's meet with 58 points and the women placed sixth with 31 points, using only six athletes.</p>
              <p>MIT will be represented by 18 student-athletes at the NCAA Division III Championships next Thursday through Saturday; nine men and nine women.&nbsp; The seeds for each competitor are:</p>
