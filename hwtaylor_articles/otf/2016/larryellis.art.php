                  <h3>Gong Runs National Qualifying Performance in 5K</h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>April 22, 2016              </p>
              <p>Only five athletes made the trip down to Princeton University from MIT for the <a href="larryellis.res.php">Larry Ellis Invitational</a>.&nbsp; Freshman Bailey Tregoning raced the 800 with a goal to get closer to a national qualifying time.&nbsp; As she makes a comeback from low serum ferritin she is also building back her confidence.&nbsp; After a 65.4 opening 400 and being in a great position to go for the win in her section, she panicked a bit, missing the opportunity for a great time.&nbsp; Nevertheless she ran a 2:15.81 and is clearly on the verge of a big breakthrough.</p>
              <p>Three women represented MIT in the 5K, all seeking NCAA Division III qualifying performances.&nbsp; The NCAA no longer has qualifying standards, instead taking the top 22 women per event and the top 20 men.&nbsp; Maryann Gong '17 and Nicole Zeinstra '16 were in the top section.&nbsp; Gong, also in the process of regaining her confidence as she comes back from a lower leg injury, was reluctant to go with the leaders once the pace picked up from 80 second pace to 77 after two laps.&nbsp; Instead she  settled in with 82's, running 5:17, 5:23, 5:26 for the first three 1600's, closing with a 37 for a 16:43.96, a time that will easily get her into the NCAA Championship field.</p>
              <p>Zeinstra went out too fast for her current condition as she returns from a concussion that had her out of workouts for two weeks.&nbsp; Her initial 5:20 1600 put her in a hole physically and mentally.&nbsp; From that point she no longer raced, running 6:00 1600 pace the final 2K&nbsp;for a final time of 17:54.52.</p>
              <p>Mary Eccles '18 looked to be having a great race out of section two as she settled in&nbsp;with 74-75 400's.&nbsp; Her first 1600 was 5:37.&nbsp; However, as the group started to spread out she was unable to respond due to tight calves.&nbsp; Her second 1600 slowed to a 5:43, but then she slowed drastically, running the final 1600 in 5:53, and finishing in 17:58.06</p>
              <p>The lone male runner representing MIT was Benji Xie 'G.&nbsp; Xie is still making a comeback from an injury he had during the winter.&nbsp; He is in very solid racing shape, but perhaps not quite where he needs to be to run mid-14's for 5K.&nbsp; However, he went for it to see where he is with regard&nbsp;to overall fitness.&nbsp;He established himself near the back&nbsp;of the pack, moving up as the pace settled.&nbsp; His initial 1600 was a crisp 4:38, one which he would have a difficult time holding and he was having some breathing problems as well, but when fatigue hit he fought well, only slowing to a 4:49.&nbsp; However, the wheels definitely came off  as he paid for the initial pace and whatever was causing his breathing issues.&nbsp; His final 1600 was a 5:14 and overall time a 15:19.92.</p>
              <p>Both the men and women start their championship season next weekend with the NEWMAC Conference Championship at the Coast Guard Academy in New London, CT.&nbsp; The men have not lost the championship since 2000 and the women have won every championship since 2009.</p>
