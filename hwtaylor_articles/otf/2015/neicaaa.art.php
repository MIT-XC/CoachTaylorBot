              <p><span style="font-size: 18px; font-weight: bold">Women Take Second - Defeat All DI Teams</span></p>
              <p><span style="font-size: 18px">Szklarzewski Sets LJ Record, Freed Sets Rookie Steeple Mark</span></p>
              <p align="left">May 7-9, 2015</p>
              <p align="left">Even though there was no intent to go for the team title, with the total focus on rest and preparing for the upcoming NCAA Division III Championship in two weeks, MIT's women's team came in second in the NEICAAA Championship with 68 points, four points out of first. This championship is one in which NCAA Division I, II, and III colleges throughout New England compete against each other. Only Division II Southern Connecticut State University was able to take down the Engineers, doing so in the final event. In the process, the women defeated at least 12 Division I teams and quite a few Division II Colleges. The men tied for eighth with 35 points resting even more individuals than the women.</p>
              <p align="left">For the women, once again the field events led the way, scoring 46 of their 68 points. Veronica Szklarzewski '15 got things started by going from the first flight of the Long Jump (not seeded to score), to placing second with an 18-10 bomb to move to eighth on the NCAA list. </p>
              <p align="left">Cimmy Virdi '16 did what she does best in this sport, win the Pole Vault, doing so with a 13-1 1/2 vault. Abby Klein 'G supported the team with five points by placing fourth at 12-1 1/2.</p>
              <p align="left">Ashley Wheeler '15 and Tilly Taylor '15 earned third place finishes on the first day of competition. Wheeler threw 173-10 in the Hammer and Taylor threw 135-6 in the Javelin. Ellen Liverpool '15 added an eighth place in the Hammer with a 163-0 performance.</p>
              <p align="left">On day two the points kept coming. Isabella Stuopis '16 threw 43-8 in the Shot Put to place third. Natalie Alper '17 tied the MIT record in the High Jump at 5-5, earning a three way tie for fifth place. Jackie Vahey '17 had a less than stellar meet, but threw far enough in the Discus to place eighth with a throw of 127-6. Michelle Johnson '15 took one jump in the Triple Jump, felt her leg tighten up a bit and decided to call it quits for the day. That one jump was 37-6 1/2, one place out of the scoring.</p>
              <p align="left">There were not a lot of entries in the running events for MIT, but what there was brought a strong return. Hannah Chen '18 broke her own freshman record in the trials of the 400IH, running 61.43. The next day she ran 61.49 to place second overall. Chen is on the verge of a breakthrough performance and has two weeks to make it happen.</p>
              <p align="left">Maryann Gong '17 decided to go with the top runner in the 1500, a runner who has a best that is 10 seconds faster than Gong's. To her credit, Gong did not know what she was capable of and wanted to take advantage of the perfect weather. With the team not concerned about scoring and already qualified for the NCAA Division III Championship there was nothing to lose. The pace was quick at 68 for the first quarter, but Gong looked comfortable. At 2:19 she looked fine, but seemed to doubt herself a bit. After 900 meters she backed off, perhaps prematurely, but continued to run another 73 for 3:32 at 1200 meters. At this point she died, but held on for a 4:28.56 second place finish. Realizing that dying in a race does not hurt as much as giving up may serve her well in the future. Later she came back in the 800 to run 2:15.45 but did not place.</p>
              <p align="left">Nicole Zeinstra '16 delivered the biggest improvement for the women. Running the 10K on the evening of the first day, Zeinstra was wanting to run over a minute than she did the previous week when she did not feel well. She knew she needed at least a low 36 minute performance to have a shot at Nationals and was concerned the pace would not be fast enough. Fortunately there was a pack of eight that went out in 87 pace, locking that in for 1600 splits of 5:48, 5:48, 5:50 and 5:50 through 6400 meters. At this point the pace picked up with only Zeinstra and Serrano of the University of New Hampshire making the break. The fifth metric mile was a 4:44, but then Zeinstra fell off the pace, again running 87's. A runner from Middlebury College caught up to her, and although Zeinstra ran an 81 final 400 she was not able to move into second. Her third place 36:06.76 is 16th on the NCAA list.</p>
              <p align="left">The men's point leader all year, and in this meet, was Ken Cooper '15. The senior began the competition with a personal best in the Hammer Throw, a mark of 194-10 for fifth place. This puts Cooper sixth on the NCAA ranking. On day two, Cooper did not perform his best, but threw well enough to again place fifth in the Discus at 149-10 and sixth in the Shot Put at 49-11.  All together Cooper scored 11 points for the Engineers.</p>
              <p align="left">States Lee '16 ran a season best 48.25 to place second in the 400 after running 48.76 in the trials the day before. He later led off the 4x400 relay with a 48.3 that ended up placing fourth. With Michael Kaba '16 out with an illness, Sam Parker '15 would be substituting in after his 800 race. Danny Newman '17 was already a rookie among the quartet that had two members different from their All America fourth place team in 2014. The other veteran on the team was Derek Barnes '16. After Lee's leadoff, Newman ran a very inspired 49.0 split to barely hold onto the lead. Barnes was next, and although he was passed immediately after the handoff, Barnes retook the lead on the backstretch and held onto it with a 48.9 split. Parker, who had run 1:53.44 earlier in the day, ran as well as he could, splitting 49.0 to finish fourth in a season best 3:15.37.</p>
              <p align="left">Luca Cacopardo '18 had run a season best 53.78 in the 400IH trials on day one of the competition. On day two, he was a little slower to the first hurdle and the backstretch. The slower pace served him well on the homestretch as he passed people. He did not switch legs when he should have, forcing him to reach on the final three hurdles, just as he had done in the trials. His 54.37 earned him a hard fought third place.</p>
              <p align="left">Ben Freed '18 had wanted to prove he could be more competitive than he had been the previous two weeks, so he was given the opportunity since this was a home meet and he had made the qualifying standard. His 9:31 Steeplchase performance was enough to get into the fast section. The pace was solid at 4:56 for the 1600 and there were a lot of runners in contention. The place slowed for one lap and then Freed went with the leaders as they resumed 74 pace. He finished strong, covering the final 200, which includes a barrier and the water jump, in 34 seconds. His final time of 9:16.04 earned him a sixth place and has him at 19th on the NCAA Division III list and the new owner of the MIT freshman record.</p>
              <p align="left">Arinze Okeke '17, who had not scored, or jumped well in the Long Jump (21-9), came back well in the Triple Jump to place seventh at 47-3 3/4.</p>
              <p align="left">Next week MIT will travel for the first time as a team since April 11. The ECAC Division III Meet will be hosted By Springfield College on Thursday and Friday, the final days to qualify for the NCAA Division III Championship.</p>
