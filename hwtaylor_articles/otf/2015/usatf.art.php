              <p><span style="font-weight: bold; font-size: 18px">Chen and Gong Compete at USATF Junior Nationals</span></p>
              <p align="left">June 25-28, 2015</p>
              <p align="left">Hannah Chen '18 and Maryann Gong '17 both qualified for the <a href="http://www.flashresults.com/2015_Meets/Outdoor/06-25_USATF/index.htm">USATF Junior Championship</a>, which was held this weekend at the Mecca of Track and Field, Eugene, Oregon. Gong had qualified in the 1500, 3000 and 5000 meter races, while Chen qualified in the 400IH. Gong elected to only race the 3K due to the schedule and the fact she was primarily doing mileage in preparation the for the Fall.</p>
              <p align="left">Both had a long year of training and racing, but both wanted to have the opportunity to race one more time. Those placing among the top two in each event would represent the United States in the Pan Am Junior Championship later this summer.</p>
              <p align="left">The weather was pretty awful, especially for Eugene. Both days of competition for MIT were 99 degrees, sunny and humid. Whether it was the long season, the practices alone without a coach to help with the mental side of training and racing, the weather, or some combination of the above, the women did not compete well.</p>
              <p align="left">Chen was technically sound throughout most of the race in that she did not stutter, but was flat throughout the race. She overstrided on the first hurdle, decelerated as she landed off balance and floated her trail leg on the first four hurdles, but overall she just did not seem that competitive. However, she did place 12th out of 17 with a 62.24.</p>
              <p align="left">Gong, suffering a bit from allergies and not having particularly strong practices since March, seemed ready to compete mentally, but in reality she was never in the race. Figuring she could handle 76 second quarters throughout the race, she was fortunate in that the field ran the first quarter in 76. Then the pace slowed to 79 and then 80. Gong was at the back of the main pace, but seemed disengaged, and indeed fell off by a meter or two as the group slowed. At her worst, she could handle a 5:00 first mile, as long as she was racing. However, she had checked out mentally and fell off badly at the 1600, hitting 5:16. From there it was survival as she finished sixth in 10:05.48. Even though the performances were not up to par, the experience should serve both well in the future.</p>
              <p align="left">Both will take some time away from racing. Gong has to now focus on mileage as she prepares for the cross country season in the fall.</p>
