              <p><span style="font-weight: bold; font-size: 18px">MIT Finishes Regular Season Undefeated</span></p>
              <p><span style="font-size: 18px">Horizontal Jumpers Have Career Day</span></p>
              <p align="left">April 11, 2015</p>
              <p align="left">Both Men's and Women's Outdoor Track &amp; Field Teams braved the 40 degree, windy and cloudy day to dominate their competition in the <a href="colby.res.php">Colby College Invitational</a>, finishing the regular season undefeated. The women scored 287 points, more than doubling the score of their opponents; Bates College, Colby College, and Husson University. The men scored 218 points in defeating Bates, Colby, Husson and the University of Southern Maine.</p>
              <p align="left">The conditions were far from ideal, particularly the vicious wind gusts in the already cold temperatures, but the MIT athletes competed as if the conditions did not exist. For the jumpers, the conditions truly did not exist, as the conditions, which also included ice and snow in the horizontal jumping pits forced the jumpers inside. Arinze Okeke '17, further motivated at the opportunity to compete head to head against NCAA Champion, Jamie Ruginski, finished second to him in the Long Jump at 22-11 3/4, but then reversed the decision in the Triple Jump, taking the victory on his final jump, a 48-6 1/4 effort for the second best jump in MIT history. His long jump was the sixth best in MIT history.</p>
              <p align="left">Okeke was not the only record setter on this day. In the women's Long Jump, Veronica Szklarzewski '15 matched her indoor Long Jump record by taking first place honors while setting the MIT record at 18-2 3/4. Michelle Johnson '15, who easily had her best long jump ever with a second place 17-9 performance, took the Triple Jump victory, breaking her own MIT record with a leap of 37-10 1/2. Szklarzewski and Jocelyn Lorrey '18 added superb support with third and fourth place efforts of 36-2 3/4 and 35-8 3/4.</p>
              <p align="left">The horizontal jumpers were not the only ones to find success for the Engineers. The women in fact won every field event. The pole vaulters, led by Cimmy Virdi '16 claimed the top four places. Maeve Devlin '18 placed second with 11-11 3/4, a mark that may make the NCAA field at the end of the season. Abby Klein '15 also cleared that height for third place. Jen Tylock '17 took fourth place at 11-5 3/4.</p>
              <p align="left">Preethi Vaidyanathan '15 led the group of vertical jumpers with her first place 5-5 performance, tying her outdoor varsity record. Natalie Alper '17 tied for third at 5-2 1/4 and Katherine Prutz '18 had a PR for sixth place, clearing 5-1 1/4.</p>
              <p align="left">In the throwing events there was equal domination as the ladies spread the wealth. Tilly Taylor '15 was not challenged in the Javelin, winning with a throw of 127-6. Isabella Stuopis '16 took her favorite event, the Shot Put with a put of 41-4 1/4. Ashley Wheeler '15 set a personal best in the Hammer Throw with a mark of 165-5, leading her teammates to a 1-2-3-6 finish. Jackie Vahey '17 claimed the Discus at 125-1 while Ariela Slutsky and Stuopis took second and third.</p>
              <p align="left">The men did not dominate the field events as the women did, but they held their own and competed well. The only  victory other than Okeke's, was earned by Harry Rein '15, taking first place in the Javelin at 174-0. Ken Cooper '15 had exceptional competition but still took two seconds and one third, giving MIT 22 points in the 10-8-6-4-2-1 scoring format.</p>
              <p align="left">On the track the men took five victories. The most impressive was Allen Leung's 5K win. David Chelimo of Colby, a national level competitor, took the lead, trying numerous times to shake the competition, but Leung '15 and Cooper Sloan '18 would not let him go. Given the 25+mph winds the pace was impressive, hitting 1600, 3200 and 4800 marks of 4:56, 9:54 and 14:44. After a penultimate lap of 69, Chelimo still could not shake Leung, but he did break Sloan. With 200 to go, Leung dropped a 30 on his opponent, who could not respond to the challenge. Leung won in 15:14.34. Sloan did not fall too far behind, finishing in third in 15:19.64.</p>
              <p align="left">Tre Albritten '18 won the 200 in 21.98, leading teammates States Lee '16, Danny Newman '17 and Derek Barnes '16 to a 1-2-4-5 finish. Ben Freed '18 continued his winning ways in the 3000 Meter Steeplechase, winning a tactical, windy race in 9:58.53. Rick Paez '15 ran a come from behind victory over teammate Sam Parker '15 who had gone out in 54 seconds for the first quarter despite the high winds. Paez closed over 20 meters in the final 200 to win in 1:59.05 to Parker's 1:59.25.</p>
              <p align="left">The women won six of the 10 events they entered. Claiming four of those victories were Hannah Chen '18 and Maryann Gong '17. Chen took her specialties, the 100HH and 400IH, winning the 100HH in a personal collegiate best of 15.03 and the 400IH in 63.97. Gong led her teammates to a 1-2-3-4 sweep in the 1500 with her 4:57.55 victory. She returned later to even split a 2:20.56 800 win.</p>
              <p align="left">Szklarzewski had a hand in the other two first place efforts. She won the 100 in a personal best of 12.44 and led off the 4x100 Relay, made up of Annie Dai '18, Kendra Knittel '17 and Chen to a 51.52 victory. Knittel also ran a 26.11 second place personal best in the 200.</p>
              <p align="left">The day was a very successful one for Tech. With two weeks before the NEWMAC Conference Championship, the Engineers will split their squad into three meets in the upcoming week. Luke Gray '18 will attempt the Decathlon at the Holy Cross multi-meet on Wednesday and Thursday, a few distance runners will compete at Princeton Friday evening while many others will compete at MIT on Saturday in the Sean Collier Invitational.</p>
