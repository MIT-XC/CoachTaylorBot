              <p><span class="headline">FIVE NCAA QUALIFIERS LEAD THE WAY FOR WOMEN'S TRACK AND FIELD </span></p>
              <p align="left">April 17, 2010</p>
              <p align="left">Despite a relatively warm April, the weather for MIT's annual <a href="springinvite.res.php">Spring Invitational</a> was far from pleasant. Nevertheless, MIT showed just how ready they are for the championships as they produced five NCAA qualifying marks and a school record in the 45 degree temperatures and light rain. </p>
              <p align="left">Other than the performances, the highlight of the meet was a visit from World Record Holder in the mile, Hicham El Guerrouj of Morocco. In town for a road race on Sunday, El Guerrouj requested to meet and speak with the MIT track team. </p>
              <p align="left">Holding nearly a third of their athletes out to rest up for upcoming championships, the ones who competed performed very well. Alina Gatowski '11, set her second MIT record of the season, taking the 5000 in an NCAA provisional qualifying time of 17:22.81. Gatowski took advantage of the low temperatures and lack of wind, taking the lead at the gun, running 1600 splits of 5:29, 5:38, 5:37 and finishing with a 38 final 200, showing there was more there.</p>
              <p align="left">Earlier in the meet, Portia Jones '12, ran her first 400 of the season. Even though there was little competition, Jones toured the oval in 57.19 to provisionally qualify for the NCAA Championship in May. Jones also claimed the 200 in 25.75.</p>
              <p align="left">Jamie Simmons '12, returned to the 400IH, this time running a personal best of 63.33 to post the fourth fastest time in the country for Division III.</p>
              <p align="left">Jacqui Wentz '10, is wasting no time getting back to her All America shape of last year despite her two month lay off from her stress reaction. She was the only entrant in the 3000 Steeplechase but ran a solid NCAA qualifying performance of 10:50.06, the third fastest time in the country thus far.</p>
              <p align="left">Karin Fisher '11, matched her personal outdoor best in the pole vault with a 11-8 1/2 victory. Fisher had outstanding attempts at 12-2, but was not able to clear the height on this particular day.</p>
              <p align="left">While not qualifying for nationals, Brooke Johnson returned to competition in her first outdoor race, running 4:48.76 in the 1500. Johnson should improve quickly over the next few weeks if she can run 4:48 after only one week of running. Maggie Lloyd '12, made her 1500 debut, running 4:57.22.</p>
              <p align="left">On the men's side, the highlight event was the 800. Pat Marx '13, took advantage of a relatively quick pace of 55 for the first 400 and won easily in 1:53.94. Marx had more but did not push the third 200 sufficiently. Yermie Cohen '10, ran a fine time of 1:55.08 for his personal best. Other MIT runners doing well in the 800 were Dawit Zewdie '13, running 1:57.44, Bill Phipps '10, running 1:58.09 and Logan Daum '11, breaking 2:00 for the first time with a 1:58.34.</p>
              <p align="left">James Oleinik 'G, threw his season best of 50-3 1/4 in the shot put to win by nearly five feet. Also in the throws, Mitch Kelley '11, threw a personal best of 151-2 to win the discus.</p>
              <p align="left">In the 3000 Steeplechase, Stephen Serene '12, made his debut with an excellent negative split 10:01.97 for the victory.</p>
              <p align="left">Next week, the men and women attempt to defend their NEWMAC conference titles at Springfield College on Saturday, starting at 11:00.</p>
