              <p><span class="headline">MIT MEN AND WOMEN REPEAT AS NEWMAC CONFERENCE CHAMPIONS </span></p>
              <p align="left">April 24, 2010</p>
              <p align="left">Both the men's and women's track and field teams were expected to repeat as <a href="newmac.res.php">NEWMAC Champions</a> and neither team disappointed. The men won their 10th in succession and 11th of the 12 championships in conference history. MIT scored 249 points to 182.5 for runner-up Springfield College. The women won their second in a row and third in four years, racking up 197.5 points to 167 for host Springfield College.</p>
              <p align="left">Neither MIT team had superb meets, which can sometimes happen when the team is heavily favored to win and not &quot;pumped up&quot; for the meet. Both teams were also probably thinking more about the New England Division III Championship, which is next weekend. Nevertheless, the men set 14 personal bests, had two new NCAA Division III provisional qualifying marks and claimed eight individual victories. The women set two MIT records, 15 season or personal bests and two NCAA Division III provisional qualifying marks. </p>
              <p align="left">MIT men's team took a couple hits before the meet ever started. Rene Peters '13, and Pat Marx '13, both expected to score quite a few points, took the weekend off from competing in hopes their nagging injuries will be completely healed by next weekend. After a slow start which had the coaches wondering why the victories were not happening with the frequency and in the events MIT is accustomed to dominating, they did eventually start rolling in.</p>
              <p align="left">The Tech men lost a key battle to WPI in the hammer throw, only securing fourth and fifth place while WPI claimed 1-2-3. This seemed to be the way things were going to go when the 10K also went to WPI, leaving MIT with only third, fifth and sixth--unheard of in the 10K for MIT. If this was not bad enough, despite Richard Prevost '11, running a personal best and NCAA qualifying time of 9:22.05, he was out kicked in 9:21.82. Favored winner, Paul Welle '11, finished third, new steepler, Stephen Serene '12, showed this is his event as he claimed fifth in 9:45.11 and Dawit Zewdie '13, took seventh in a personal best 9:59.60 to take some of the sting out of losing another first place.</p>
              <p align="left">Finally things began to click, when Yermie Cohen '10, led a quartet of MIT runners to a 1-3-4-7 finish in the 1500. Cohen waited until there was 400 meter to go before he sprinted away, finishing his final 300 in 42 seconds. Hemu Arumugam '10, and Ben Mattocks '12, ran personal bests in 4:02.53 and 4:02.85 respectively to take third and fourth. The men's javelin throw was going on at the same time and Vlad Sobes '11, dominated the event, throwing a five meter best and earning a national qualifying position with his 200-11 throw.</p>
              <p align="left">The 110HH did not go so well as Brian Djaja '13, seeded third, hit the sixth hurdle, stumbled into the seventh hurdle, pushing it down and getting disqualified. Nick Leonard '11, got a poor start as did Josh Duncavage '13. Only Andrew Musacchio '11, delivered for MIT, claiming third in the final. Fortunately, the shot putters fought off a poor start and claimed a 1-2-3-6 finish led by James Oleinik's victory with a throw of 47-00.25.</p>
              <p align="left">Stephen Morton '10, won both the long jump and triple jump, taking a minimum of jumps, but did not bring much speed to the track. Even though he set a season best in the 100 trials with his 10.95, he was only able to claim fifth in the 100 and fourth in the 200.</p>
              <p align="left">Cohen returned in the 800, running an evenly split race, hitting 58 on both the first and last laps to win in 1:56.29. Logan Daum '11, missed is personal best by .01 as he raced brilliantly to place third in 1:58.35.</p>
              <p align="left">Mitch Kelley, took care of first place with a new personal best in the discus of 151-7. His teammates did not bring it in this event, but MIT still added a fifth and sixth. The final field event, the pole vault, an expected win, did not quite finish that way, but MIT did the next best thing, claiming 2-3-4-5 with Cyrus Vafadari '12, setting a personal best and Davey Hunt '12 setting a season best of 14-9 and 14-2.75 respectively.</p>
              <p align="left">In the remaining track events, Duncavage '13, ran a fine personal best in the 400IH to claim third in 56.43 as Leonard continued to struggle, running 57.61 for fourth. Dan Harper '12, the lone MIT runner in the 5000 after the team pulled Arumugam and Mattocks due to the teams insurmountable lead, won going away in a personal best of 15:20.65, running the final mile in 4:51 before closing the final 200 in 33.</p>
              <p align="left">The women only claimed six individual victories but their balance kept the other teams at bay, allowing them to pull three of their top seeded 5K runners and two of their 4x400 runners to save a little for the remaining championships.</p>
              <p align="left">Alina Gatowski '11, and Sarah Sprague '13, got things started in the 10K. Both ran as slow as necessary to get the job done on the warm and windy day. Gatowski finally tired of the pedestrian pace, opening up a lead after four miles to win easily. Sprague claimed third to give MIT an easy 16 points.</p>
              <p align="left">The 4x100, consisting of Margaret Leibovic '10, Martha Gross '12, Jamie Simmons '12, and Portia Jones '12, claimed second with a new MIT record and an NCAA Division III provisional qualifying time of 48.65.</p>
              <p align="left">The track events continued to go well as Maggie Lloyd '12, knocked nine seconds off her best to claim second in the 3000 steeplechase with a time of 11:40.40. Jacqui Wentz '10, Brooke Johnson '13, Alicia Kaestli '12 and Tania Morimoto '12, took care of the 1500, running 4:37.77, 4:47.34, 4:54.97 and 4:57.53 respectively to take 1-3-4-5. Wentz's time provisionally qualified her for the NCAA Division III Championship. </p>
              <p align="left">However, in the field events things were not clicking as well. The javelin throw provided no points and the long jump only gave MIT a sixth place. MIT's signature event, the pole vault only provided a third, fourth and fifth. The high jump only produced a 2-5-8 finish as Jones claimed second on a 5-1 height clearance. The remaining throwing events only gave MIT four points. </p>
              <p align="left">Fortunately, the track events continued to go well. Jones won the 100HH pretty easily in 14.73 and set a new MIT record when she sprinted to a 11.50 in the 100 meter dash trials. Her 11.52 gave her a third place in the final. Hazel Briner '11, ran a season best of 15.67 in the 100HH to support Jones's effort  with a fourth place and Leibovic earned a sixth in the 100.</p>
              <p align="left">Simmons and Gross took care of business in the 400, taking first and second with personal bests of 57.49 and 58.55. Wentz returned in the 800 and led it from start to finish in an even split 2:17.81. Wentz had to run out of the slow section since she had not run an 800 as yet in the outdoor season. Simmons returned in the 400IH and won easily in 63.85. Jones ran her sixth race of the day to place second to Simmons with a 65.46. Kirsten Aarsvold '11, added a fifth place for the cause. Gross returned in the 200 to place fourth in the 200 and Leibovic and Briner added a seventh and eighth.</p>
              <p align="left">After pulling the three top 5K runners, Lauren Allen '13, did her job, placing second in a personal best of 18:56.55, missing the New England Division III qualifying mark by .07 seconds.</p>
              <p align="left">In the final event, the 4x400, Emily Kuo (64.4), Gross (59.0), Aarsvold (61.8) and Simmons (57.1) placed second in 4:02.52.</p>
              <p align="left">Next week, the Engineers will need a lot more enthusiasm and much better performances if they are to stand a chance of taking the New England Division III Championship from favored Williams College. MIT's women won last years title despite being 50 point underdogs and the men won this years indoor New England title.</p>
