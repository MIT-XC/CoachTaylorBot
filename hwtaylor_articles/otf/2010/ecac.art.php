              <p style="font-size: 16px; font-weight: bold;"><span style="">TECH TRACK CONTINUES TO STEP UP WITH NCAA QUALIFYING PERFORMANCES </span></p>
              <p align="left">May 13-14, 2010</p>
              <p align="left">While MIT used the <a href="ecac.res.php">ECAC Division III Championship</a> as a national qualifier, resting some, attempting to get better NCAA marks from others and attempting to get some who had not qualified to the magical marks.</p>
              <p align="left">The women managed to win four events despite the reserved team effort and had four improved NCAA performances. Hazel Briner '11, started things off with the heptathlon. While Briner did not have any personal bests until her amazing 2:19.49 800 finale, she still managed a new PR and MIT record in winning the event with a score of 4378 points, which currently has her ranked 16th on the NCAA Division III list.</p>
              <p align="left">Also on the first day, Jacqui Wentz '10, ran the 3000 steeplechase, figuring she would need to improve on her 10:50 performance from earlier in the season. Improve she did, running the second fastest time in the country with her new MIT record of 10:22.32 for a twenty seven second victory.</p>
              <p align="left">Tania Morimoto '12, took to the track in the 10K, an event where her previous qualifying performance of 37:01 would not get her to nationals. The early pace was quick and Morimoto wanted to ease into it so she began with a 90 second first lap, eased into 89 second quarters to come through the 1600 in 5:56. Moving the pace up to 87s, Morimoto slowly reeled in the lead pack, catching them before the half way point. With four laps to go she took the lead, trying to win. Her 85 second lap opened up a five meter gap, but she was not able to hold the pace, eventually holding on for third in a 27 second PR of 36:34.11. A time that moves her to number 24 on the NCAA list.</p>
              <p align="left">On day two, the 4x100 got the Engineers underway. Unfortunately, MIT was unable to improve on their best performance due to two poor handoffs. Despite the mishaps the quartet of Margaret Leibovic '10, Martha Gross '12, Jamie Simmons '12 and Portia Jones '12, put together a time of 48.66 for third place, showing they have the ability to run under 48 seconds if they put three handoffs together.</p>
              <p align="left">Portia Jones entered the 100HH as the second seed. However, after a great start and a sprint intensity she has never shown before, Jones never saw another competitor after leaving the blocks, running 14.44 for the victory despite absolutely no wind.</p>
              <p align="left">MIT had no one else competing until the 400IH. Jones, having only run a 65 thus far in the season, and not a chance of qualifying for nationals with that time, knew she had to run a great time. However, due to her slower qualifying time, she was in the second of three sections. This did not matter as Jones screamed through a high 28 first 200, running flawlessly. She stretched to reach hurdles six and seven, slowing down on the hurdles. This caught up with her on the final three, but she nevertheless finished with a 61.34 performance, a new MIT record and second best on the NCAA list. Simmons, already number four on the list, was hoping to get out and run the sub 61 she is capable of. She looked great though three hurdles then seemed to totally lose her rhythm, having difficulty on every hurdle and finishing with a 65.04.</p>
              <p align="left">MIT's final event was the 4x400. Already with the third fastest time in the country, MIT did not need to try to run faster. What was more important was to try to see who the best fourth leg would be to join Jones, Simmons and Gross. After Simmons led off with a new personal best of 56.4 leg, Wentz came back from the steeplechase and showed she is multi-talented as she cruised to a 58.1 leg and seemed to have plenty left at the end. Kirsten Aarsvold '11, who ran a 59 split last weekend, was only able to provide a 61.4 leg this time around. Gross got the baton, trying a little too hard to catch the leader and faded a bit on the homestretch to give her a 58.0 anchor for a 3:54.07 total time, good for second place. </p>
              <p align="left">The men left many of their top performers at home such as Yermie Cohen '10, and Paul Welle '11, as well as sending Stephen Morton '10, down to the IC4A Championship at Princeton University.</p>
              <p align="left">Vlad Sobes '11, and James Oleinik '10, led the way for the Engineers, both claiming second place. Sobes threw 193-0 for runner-up honors in the javelin while Oleinik tossed the shot 50-0 on his final throw to claim second.</p>
              <p align="left">Mitch Kelley '11, had the only personal best for the men, throwing 157-7 in the discus for fourth place and 33rd on the NCAA qualifying list. This new mark moved Kelley to third on the all-time MIT list. </p>
              <p align="left">Greg Tao '10, had a sub par performance in the pole vault and was thus unable to move up on his NCAA 16th ranked spot with his early season 15-11 vault. Nevertheless he earned a fifth place with his 15-5 clearance.</p>
              <p align="left">Richard Prevost '11, dropped back suddenly after the 1600 mark of the 3000 steeplechase, going from running 74 second quarters to 79, 82 and 84 second final three 400s for a sixth place 9:37.84 finish.</p>
              <p align="left">MIT earned two seventh places, one by Kelley in the shot put with his 47-6 1/4 put and the other by Dan Harper '12, running 15:19.55 in the hot and humid conditions.</p>
              <p align="left">Next week, MIT hosts a true NCAA approved Last Chance Qualifier on Friday, May 21 at noon. This will be the final day anyone is permitted to qualify for the NCAA Division III Championship. </p>
