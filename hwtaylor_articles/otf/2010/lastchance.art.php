              <p><span class="headline">MIT TAKES ADVANTAGE OF HOME LAST CHANCE QUALIFIER</span></p>
              <p style="font-size: 14px"><span style="font-weight: bold">Six Improved NCAA Qualifying Marks, Two MIT Records, Eight Personal Bests Showcase MIT's Efforts </span></p>
              <p align="left">May 21 , 2010</p>
              <p align="left">MIT was fortunate to host the NCAA approved Last Chance Qualifier for the New England region, and the MIT track and field performers took advantage of the situation. Given that MIT was in their last day of final exams it would have been nearly impossible for MIT to have competed at any other facility. Only those already qualified for the NCAA Division III Championship next week, or those who were really close to doing so were representing MIT. Over 20 teams from throughout the region and New York were represented in the meet, which only lasted two and a half hours. Relatively small fields, a rolling time schedule and a facility that aids in efficient meet management enabled the very quick meet. </p>
              <p align="left">MIT entered 13 events in the men's and women's meet and won every one of them. MIT performers competed at an amazingly high level, producing six improved NCAA qualifying marks, two women's records and eight personal bests en route to one of the best competitive efforts in MIT history.</p>
              <p align="left">Jacqui Wentz '10, displayed her talents once again by demolishing the field in the 1500. Despite the windy conditions Wentz took the lead at the start, running splits of 70, 2:21 and 3:33 as she closed with a 52 final 300 to finish in an MIT record and NCAA leading time of 4:25.43.</p>
              <p align="left">Portia Jones '12, employed a similar strategy in the 400, making up the stagger halfway through the first turn and never looking back as she too set an MIT record of 55.83, the third fastest time in the country so far. Also in the race, Martha Gross '12, ran a very competitive 58.38 and Kirsten Aarsvold '11, set a personal best with her 61.45.</p>
              <p align="left">Karin Fisher '11, moved up to number  14 on the NCAA list, good enough to make it into the national field that will be vaulting on Thursday at 1:00 at Baldwin Wallace College in Berea, OH. Her vault of 12-2 1/2 won the event while Hazel Briner '11, cleared 11-0 3/4 and Lauren Kuntz '13, made 10-6 3/4.</p>
              <p align="left">Three other victories were earned by the Lady Engineers. Jamie Simmons '12, won the 400IH, as she ran a very respectable 63.04 in the strong wind. Jones came back in the 200 only about 30 minutes after her 400 record and ran 24.81. Gross also came back in the 200, running 25.99, while Briner ran 26.78. The 4x100 team of Margaret Leibovic '10, Gross, Simmons and Jones made an effort to improve their NCAA qualifying time by enough to make it into the selected field but it was not to be as they only ran a 48.70, which was good enough for the victory.</p>
              <p align="left">For the men, their were three personal bests to go along with the seven victories. Mitch Kelley '11, had the most impressive performance, throwing nearly a six foot personal best in the discus to win in 163-4, moving him up to number 22 on the NCAA list and number two on the MIT all-time list.</p>
              <p align="left">Yermie Cohen '10, ran a 1:53.45 PR to win the 800. Cohen went out in 55, took over the lead and sprinted home against the challenging wind. Already the number one seed on the NCAA list in the 1500, Cohen was just getting a tune-up prior to the national championship. </p>
              <p align="left">Greg Tao '10, finally put together the vault day he is capable of, setting a new personal best of 16-1 1/4 to move up to number 12 on the NCAA list.</p>
              <p align="left">Making an effort to improve in the 3000 Steeplechase, Paul Welle '11, was not able to hold a fast early pace as the wind got the best of him. His 9:16.75 was good for the win but not an improvement on his 9:12.22, which at number 20 on the NCAA list does not look promising for gaining selection.</p>
              <p align="left">Stephen Morton '10, needed to improve his mark in the triple jump and wanted to have a few good long jumps as well. All three of his long jumps were well over 23', with his best fair jump being 23-7 1/4 for the win. He did improve his triple jump even though he was only able to get two of his six jumps in. His winning jump was 48-0 3/4.</p>
              <p align="left">James Oleinik 'G, did not improve his 52-5 1/2 shot put, which has him at 25th on the NCAA list, but he did have his second best put ever with his 51-5 victory. Perhaps not the best finish to his competitive career, but pretty darn close.</p>
              <p align="left">The athletes find out on Sunday who is in the NCAA Championship and who is not. The competition begins on Thursday and ends on Saturday.</p>
