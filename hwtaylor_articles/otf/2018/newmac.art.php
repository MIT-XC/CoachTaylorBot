                  <h3>MIT Track &amp; Field Defends NEWMAC Titles</h3>
                  <h5>Seniors, Three Freshman Records Lead Way</h5>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>April 28, 2018              </p>
              <p>MIT successfully defended their <a href="newmac.res.php">NEWMAC Outdoor Track &amp; Field Championship</a> titles today at Springfield College, the women winning their 10th consecutive championship and the men earning their 18th in succession, having only lost the title in 2000.&nbsp; Although the women scratched two of their top-seeded 5K runners, and the men three of their 5K runners as well as their 4x400 squad, both teams won handedly.&nbsp; The women scored 213 points, 72.5 more than second place WPI, and the men 274.5, 53 up on runner-up WPI.</p>
              <p>Although there was a little wind, the weather could not have been much better.&nbsp; For the first day this season&nbsp;the temperature was conducive for outstanding track and field performances.&nbsp; MIT took full advantage, highlighted by three freshman records&nbsp;and&nbsp;some solid future championship qualifying performances.&nbsp; Jacqueline Ahrens '21 established a new MIT Freshman record in the Pole Vault, clearing 12-5 1/2 to place second and move her into a tie for sixth on the NCAA Division III ranking this season.&nbsp; Benton Wilson '21, who already owns the freshman records in both the 100 and 200 meter dashes, lowered his record in the 200 to a 21.69, which is still a couple tenths away from qualifying for nationals.&nbsp; Yorai Shaoul '21 added the freshman long jump record to his triple jump record, jumping 23-0 to win the event on his final jump.&nbsp; This mark ranks him 31st nationally, meaning he will need another eight inches or so to qualify for nationals.&nbsp; Shaoul's 48-4 record, which he tied to win this competition has him fifth ranked nationally.&nbsp;&nbsp;Shaoul was presented with the NEWMAC Field Event Athlete of the meet for his performances.</p>
              <p>The seniors led the way for MIT and will be difficult for the Engineers to replace next year.&nbsp; The seniors combined for 195.5 points; 99.5 for the men and 96 for the women in a&nbsp;scoring format of 10-8-6-5-4-3-2-1.&nbsp; For the men, Cody Bratten '18 scored 28 points, earning victories in the Shot Put and the Discus while also throwing a personal best of 151-4 in the Hammer to place second.&nbsp; Bratten's Shot Put victory of 50-0 3/4 was a personal best and his Discus win was a season best of 160-8.&nbsp; Bratten is ranked 14th on the national list.</p>
              <p>Chris Sweeney&nbsp;'18 accounted for 20 of those points, winning both the 110HH and the 400IH. &nbsp;His victory in the 110HH was a personal best of 14.88 and his 54.02 in the 400IH was a season best.&nbsp; Sweeney is ranked 17th in NCAA DIII for the 400IH.</p>
              <p>Billy Ruschel '18 and his fellow horizontal jumpers added 23 points.&nbsp; Ruschel placed second in the Triple Jump (45-7 3/4) and third in the Long Jump with a personal best of 22-0 3/4.&nbsp; Jason Villanueva '18 and Ryan Prinster '18 added a fourth and fifth respectively in the Triple Jump as MIT placed 1-2-4-5.</p>
              <p>The distance squad was responsible for the remainder of the senior's points contribution.&nbsp; David Walter '18 used an excellent negative split strategy (5:04, 5:01, 4:54) to win the 5K, finishing with a 32 final 200 to win in 15:31.13.&nbsp; In&nbsp;the 10K, Nicholas Waltman '18 and Alex Knoedler '18 led MIT to a 1-2-3-5&nbsp;scoring blast against their opponents.&nbsp;&nbsp;After a controlled 8K in 26:28, MIT picked up the pace.&nbsp; Waltman pushed ahead for the victory in 32:36.22 and Knoedler chose not to challenge, finishing second in 32:42.73.</p>
              <p>The women seniors were also led by a thrower.&nbsp; Ariela Slutsky '18 also scored 28 points, taking victories in the Hammer and Discus while placing second in the Shot Put.&nbsp; Limited in the Shot Put all year due to a thumb injury from that event, Slutsky threw 38-5 from a power position, a&nbsp;season best.&nbsp; In the Discus Slutsky won easily with a throw of 143-0 and likewise, won the Hammer with a throw of 172-6.&nbsp; Slutsky is nationally ranked in both the Hammer and Discus; 17th in the Discus and 4th in the Hammer.</p>
              <p>Hannah Chen '18 scored 22 points for the Engineers&nbsp;as she won the 400IH, placed second in the 110HH, ran&nbsp;a leg on the 4x100 Relay that placed third, as well as a leg on the victorious 4x400 team.&nbsp; Chen ran&nbsp;a season best 14.65 in the 100HH.&nbsp; In the 400IH she ran 62.47 to win&nbsp;the event.&nbsp;&nbsp;Running the second leg on the 4x400 team, Chen split 56.7 as she put her team in the lead for good.&nbsp; The quartet of Kristen Frombach '19, Chen, Katie Williams '21 and Bailey Tregoning '19 ran a season best 3:56.80.&nbsp; Chen is ranked 14th nationally in the 100HH and 7th in the 400IH.</p>
              <p>Nneoma Okonkwo '18 contributed 17.5 points to the cause.&nbsp; The senior won the Long Jump, matching her season best of 18-6, as well as placing third in the 100 with&nbsp;a season best of 12.47.&nbsp; She also ran a leg on the 4x100 Relay.&nbsp; Okonkwo is ranked 16th nationally in the Long Jump.</p>
              <p>Mary Eccles '18, scored 14 points towards her team's victory.&nbsp; She first ran her non-specialty 1500 meters, placing fifth in 4:46.92 and then came back in the 5K, winning in a sit and kick 18:19.99.</p>
              <p>Haley Strouf '18 added 9.5 points to the seniors' total, placing second in the Long Jump (17-10 1/4) and running a leg on the 4x100 Relay.&nbsp; Dana Balek '18 added five points with her fourth place finish in the Javelin, throwing 112-2.</p>
              <p>The men won three additional victories. Chris Washington&nbsp;'20, cleared 6-6 in the High Jump to win, just missing 6-9 1/2.&nbsp; Jon Fakkema '19 took first place honors in the Hammer Throw with a toss of 161-1.&nbsp; Aidan Gilson '19 was victorious in the 3,000 Meter Steeplechase, taking the lead early in the race, running a season best 9:26.65.</p>
              <p>The women's only other victory was a big one, as Jenna Melanson '20 won the Steeplechase in a season best 10:48.36.&nbsp; The sophomore currently holds the eighth fastest time in Division III.</p>
              <p>Next weekend MIT hosts the New England Division III Track and Field Championship.&nbsp; While both the women and men should do well, neither will be seeded to win and defend their titles.&nbsp; The men have won the past four years in a row while the women have won five consecutive New England Division III titles.              </p>
