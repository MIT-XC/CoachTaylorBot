                  <h3>Women Win Quad Meet With Balance, Men Place Close Third</h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>April 7, 2018              </p>
              <p>In the first full-team outdoor meet for MIT, the women showed incredible balance between the running and field events to easily win a <a href="quad.res.php">quadrangular meet versus Bates, RPI and Tufts at Tufts University</a>.&nbsp; The NCAA recommended scoring for four teams&nbsp;- 9,7,6,5,4,3,2,1 for individual&nbsp;events with no more than two scoring for each team, and 9,7,6,5 with only one relay scoring for each team was what was used. The women won 12 of the 19 events with both Ariela Slutsky '18 and Michelle Menkiti '20 winning two events for the Engineers.&nbsp; The final score was MIT 212, Tufts 186, Bates 132.5 and RPI 122.5.</p>
              <p>The men did not fair so well.&nbsp; Missing the top three 800 runners due to injury, two distance runners due to an ROTC commitment and a general lack of sprinters, Tech put together what they could, but failed to capitalize when their top runners were in position to dominate their events.&nbsp; Despite not competing Alec Reduker '20 (tight back) and Billy Ruschel '18 (precautionary), MIT's depth in the field events proved adequate to make-up for the missing stars.&nbsp; When the dust settled, RPI pulled out the victory with 175 points to 172 for Tufts, 170 for MIT and 142 for Bates.</p>
              <p>The weather certainly hampered performances, but with sunny skies,  mid-40 degree temperature and 10-15 mph winds, it was much better than had been expected.&nbsp; The women took first place in six of the eight field events.&nbsp; Slutsky won both the Hammer and Discus events, throwing 164-9 and 133-9 respectively.&nbsp; The senior also added a third place in the Shot Put.&nbsp;&nbsp;Kari Stromhaug '19 added a win for the cause in the Pole Vault, clearing 12-1 1/2 to lead a 1-2-3 effort as Jacqueline Ahrens '21 cleared 11-7 3/4 and Makenzie Patarino cleared 10-8 for second and third.&nbsp; Nneoma Okonkwo '18 took first place in the Long Jump&nbsp;with her 18-6 performance.&nbsp; Elizabeth Weeks '21 earned the victory in the Triple Jump, jumping 36-11 1/2 also leading her group to a 1-2-3 finish.&nbsp; Jasmine Jin '20 (35-11 1/4) and Lucy Lee '21 (34-1 1/2) finished second and third.&nbsp; Dana Balek '18 earned the other field event victory, taking first place in the Javelin with a throw of 123-2.</p>
              <p>Menkiti claimed two of the six running event victories and played a key role in another.&nbsp;&nbsp;The sophomore took&nbsp;the 100 and 200 with weather affected times of 12.81 and 25.80.&nbsp;&nbsp;She also led off the victorious 4x100 Relay in a 49.45 effort.&nbsp; The other members of the victorious relay were Haley Strouf '18, Hannah Chen '18 and Okonkwo.&nbsp; Chen added a victory along with a second place, both in the hurdle events.&nbsp;&nbsp;She won her specialty, the 400IH in 1:04.10 and was runner-up in the 100HH in 15.37 (15.18 Trials).&nbsp; Margaret Trautner '20 won her first ever 1500 in 4:39.19.&nbsp; Using Jamie Lord of RPI and teammate Marissa McPhillips '20 to block the wind, the sophomore was able to edge out McPhillips for the victory.&nbsp; The final victory was earned by Trautner, McPhillips, Katie Williams '21 and Bailey Tregoning '19 in the 4x400 Relay.&nbsp; The quartet never trailed and pulled away from the field with a 4:04.26 performance.</p>
              <p>Although not a winning effort, Mary Eccles '18 ran an impressive outdoor PR of 17:09.62 in the 5K.&nbsp; Katherine Cook&nbsp;of Bates took the pace out in a 76 for the first quarter despite the wind being pretty strong.&nbsp; Eccles, fresh off her indoor All America effort, went with her.&nbsp; Katie Bacher '20 running in her debut 5,000 also went with her, but was not expecting that pace and it immediately took her out of the race&nbsp;mentally.&nbsp; The sophomore nevertheless ran a respectable 18:02.40.&nbsp; Jenna Melanson '20, who ran her own race, disregarding the initial pace&nbsp;and settling in with 5:40 pace, went on&nbsp;to finish in a PR of 17:48.82.</p>
              <p>The men took first place in all the jumping events.&nbsp; Despite Reduker not competing, his teammates claimed the top three places in the High Jump with Chris Washington '20 earning first place at 6-4.&nbsp; Yorai Shaoul '21 was the only double winner for the Engineers and he set an MIT Freshman Record in the Triple Jump with his 48-4 victory.&nbsp; In the Long Jump the freshman jumped 22-1 1/2.&nbsp; Scott Cameron '19 completed the jumps sweep with his 15-5 victory in the Pole Vault.</p>
              <p>While not winning any of the throwing events, both Cody Bratten '18 and Jon Fakkema '19 scored in each of their three events.&nbsp; Bratten took second in his specialty the Discus (158-0)&nbsp;and fourth and eighth in the Shot Put and Hammer respectively.&nbsp; Fakkema was second in the Hammer (160-11), fifth in the Discus and sixth in the Shot Put.</p>
              <p>In&nbsp;the running events, Chris Sweeney '18 and Benton Wilson '21 were the most dominating for Tech.&nbsp; Sweeney won the 400IH in 54.74 and also placed second in the 110HH in 15.03.&nbsp;&nbsp;Wilson, in addition to leading off the 4x100 Relay, won the 200 in 22.34 out of lane one, and earned second place in his first collegiate outdoor 400 in 50.36.</p>
              <p>In both the 3000SC and the 5K, events MIT typically dominates, runners failed to deliver.&nbsp; In the Steeplechase, Aidan Gilson '19 (coming off an illness) and Ben Freed '18 (coming off an injury) put themselves in&nbsp;front of the pack, pushed the pace early, but only succeeded in putting a target on their back and blocking the wind for their opponents.&nbsp; Gilson faded to third and Freed to sixth at the finish.&nbsp;&nbsp;While Kent Slaney '21 and David Walter '18 did not lead and did not run an unreasonable pace, neither had it at the end as they faded to seventh and eighth in 15:25.95 and 15:28.02 respectively.</p>
              <p>Next Saturday, MIT will travel to Williams College for a good old-fashioned dual meet between the Ephs and Engineers.&nbsp;&nbsp;&nbsp;The Williams College team has looked impressive early in the season and appears to be more than a formidable opponent for Tech.</p>
