                  <h3>McPhillips and Trautner Add Their Names to the NCAA Championship List                  </h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>May 17, 2018              </p>
              <p>The NCAA permits one Final Qualifying meet per region and MIT again hosted the <a href="FinalQualifier.res.pdf">New England region Final Qualifier</a>.&nbsp; For the oval runners the weather was perfect with mid-70 degree temperatures, low humidity and no wind.&nbsp; Of course, the sprinters and jumpers were not too happy with no tail wind.&nbsp; The qualifying standards were steep so there was no more than one section or flight in any event.</p>
              <p>The performance of the meet for the Engineers was turned in by Marissa McPhillips '20 in the 1500.&nbsp;&nbsp;With a loaded field, Katie Bacher '20, who had already qualified for Nationals in the 1500, led the field through a 2:24 first 800.&nbsp; McPhillips stayed towards the front of the pack&nbsp;through a 3:36 1200.&nbsp; In the homestretch she held onto that pace, finishing second in a huge PR of 4:31.09, easily making the NCAA field.</p>
              <p>Almost equal to that was the 800 race.&nbsp; With even a more stacked field, Bailey Tregoning '19 and Margaret Trautner '20 were trying to improve their mark&nbsp;and chances of getting into the NCAA Championship.&nbsp; Tregoning already had a solid 2:11.18, but Trautner was a few spots out with a 2:13.39.&nbsp; Sydney Packard '20 from WPI set a quick pace of 62 for the 400 and 1:35 for the 600.&nbsp; Tregoning took the lead at that point but faded a bit in the homestretch, but still winning in 2:11.80.&nbsp; Trautner was running great, but was passed by two right at the finish, taking sixth place in 2:12.65.&nbsp; A mark that earned her a trip to Nationals.</p>
              <p>Nneoma Okonkwo '18 gave the Long Jump her best shot, improving her season best to 18-8 to win the event, but she was one person away from making the NCAA field.</p>
              <p>Ariela Slutsky '18 improved her season best in the Discus, winning with a throw of 149-3.&nbsp; Slutsky also won the Hammer with a 170-3 performance.&nbsp; She will represent MIT in both events at the National Championship.</p>
              <p>In the Pole Vault, Jacqueline Ahrens '21 and Kari Stromhaug '19 finished first and second with vaults of 12-2 and 11-8.&nbsp; Although neither jumped season bests, both will compete in the Pole Vault at Nationals.</p>
              <p>Jason Villanueva '18 jumped a personal best 46-0 1/2 in the Triple Jump, but it was not enough to make the NCAA field.</p>
              <p>Jon Fakkema '19 threw a season best in the Hammer, throwing 166-11, but this also was not enough to make the National field.</p>
              <p>In an impressive effort, Chris Washington '20 made three excellent attempts at 6-9 3/4, just hitting the bar with his legs on the final attempt,  failing to clear.</p>
              <p>MIT will take six men and 10 women to the <a href="http://www.uwlathletics.com/sports/2017/12/7/TRACK_0423131617.aspx">NCAA Division III Outdoor Track &amp; Field Championship</a>.</p>
