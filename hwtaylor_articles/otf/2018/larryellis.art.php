                  <h3>Eccles Leads Engineers With 10K Victory<br>
                  </h3>
                </center>
                  <p>April 20, 2018 </p>
                  <p>Four years ago when the nonrecruited Mary Eccles '18 asked about trying out for the Cross Country and Track &amp; Field teams, no one could have predicted the runner she would become.  Her freshman year it was astounding that she placed in the New England Division III Championship and ran PR's of 17:59.99 and 37:32.10.  She continued to improve each year, winning the 10K at the NED3 Championship and following that up with the 5K victory indoors this year.  Earning her first All America honor this past indoor season in the 5K Eccles is continuing to write her legacy every weekend in this her senior year. </p>
                  <p>At the <a href="larryellis.res.pdf">Princeton University Larry Ellis Memorial Track and Field Invitational</a> Eccles toed the line with seven other runners for the 10K just before 10 p.m. with the temperature a frigid 41 degrees.  She burst off the line, separated herself from the pack immediately and settled in for 85 second 400's.  After a 5:42 first 1600, Eccles dropped her pace slightly to a 5:41 and then a 5:39 on her third 1600.  After the fourth 1600 in 5:40 she was in sight of lapping the field and used that for further motivation.  She continued the 5:40 pace, lapping the field and finishing with a 39 second personal best of 35:27.11, number two on the MIT all-time list and number seven on the NCAA Division III performance list this season. </p>
                  <p>Six other MIT athletes competed at the Princeton meet.  Prior to the 10K not only was the temperature in the 40's, but the wind was pushing 15-20 mph, slowly dying down late in the evening.  Before the wind died down, Jenna Melanson '20 competed in the 3,000 Meter Steeplechase.  She got out a little too fast in 81 seconds for the first lap, and after another lap, she started paying for the early pace.  Her pace started creeping up to 86, 87, 88 and finally a 92 second lap before finishing in 10:54.33, a very respectable time, currently number 12 on the NCAA list.</p>
                  <p>In the 1500, Katie Bacher '20 and Marissa McPhillips '20 also braved the cold and wind.  Bacher was pulled out in 72, working with a group who were running the exact pace the sophomore was looking for.  She ran out of gas a little at the end, but produced a season best 4:38.23.  McPhillips was pulled out a little too fast in her section, splitting 70 for the initial 400.  She too fatigued a lot in the final lap running 4:38.85.</p>
                  <p>Prior to the 1500, McPhillips and Katie Williams '21 raced in the 800 Meter event.  Williams was pulled out to a very quick 64.4, and even though she lost a little in the end, she ran a solid 2:15.00.  McPhillips went out a little slower in her section and finished in 2:16.50.</p>
                  <p>Only two men made the trip for MIT, Aidan Gilson '19 in the Steeplechase and Josh Rosenkranz '19 in the 5K.  Gilson, struggling to find his racing groove after a fabulous indoor season and then dealing with a sickness as he began his outdoor season, was hoping to right the ship in this race.  Based on his 9:05 performance last year, he was seeded in the fast section.  The initial 400 in 68 was enough to shake his already vulnerable confidence.  The junior dropped towards the back as the race progressed, running 9:35.62.</p>
                  <p>Rosenkranz ran an excellent race, moving when he needed to in order to close gaps, coming through 1600 splits of 4:39 and 4:45.  When he needed to make his final move to keep up with the breakaway group he was unable to respond.  Low mileage and still building his fitness from an early season setback, showed as he is still building his strength.  Nevertheless he finished in a solid 14:51.99.</p>
              <p>Next Saturday the teams will try to defend their NEWMAC Conference titles at Springfield College.  The men have not lost the title since 2000 and the women have every championship since 2009.</p>
