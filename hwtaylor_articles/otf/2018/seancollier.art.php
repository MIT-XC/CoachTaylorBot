                  <h3>Ariela Slutsky Takes Down MIT Hammer Record</h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>April 21, 2018              </p>
              <p>In the final non-championship meet of the season, MIT rested most of their track &amp; field athletes in preparation to&nbsp;defend their NEWMAC and New England Division III Championship titles.&nbsp; However, in addition to the throwers and vaulters, a few competed to either improve their seed or to take advantage of the competitive opportunity.</p>
              <p>As a rule, the <a href="seancollier.res.pdf">MIT Sean Collier Invitationa</a>l is not a highly competitive track and field meet, but there were a few exceptions, particularly the efforts of Wadeline Jonathas '20 from UMass Boston.&nbsp; The defending NCAA Division III Champion in the 200 and 400 from the 2017 outdoor championship and the four time individual champion from this year's NCAA Championship (60, 200, 400, Long Jump), won the 100 in 11.66, the Long Jump in 19-2 and ran a scorching 4x400 leg.</p>
              <p>Among the performances by MIT athletes&nbsp;there were none better than what Ariela Slutsky '18 did in the Hammer Throw.&nbsp; Shortly after exclaiming her frustration at not being able to throw very far and not being able to focus on her technical problems, she commenced to throw four personal bests en route to a new MIT record of 182-4 1/2, which is fourth on the current NCAA Division III ranking.&nbsp; Also throwing&nbsp;a personal best in the Hammer Throw was Folusho Jebutu '20 (136-1 3/4).&nbsp;&nbsp;</p>
              <p>For the male throwers, Cody Bratten&nbsp;'18 had a personal best in two events and a season best in the other.&nbsp; Starting with the Discus Throw, Bratten threw 158-0 3/4 to win the event with his season best.&nbsp; In the Hammer&nbsp;he threw a PR of 148-1 3/4 to finish behind teammate Jon Fakkema '19 (160-7 3/4).&nbsp;&nbsp;Then in the Shot Put, the senior threw his final PR of 49-1 3/4&nbsp;to win&nbsp;the event.&nbsp; Fakkema threw a PR of 141-2 1/4 in the Discus.</p>
              <p>For the women Kari Stromhaug '19 won the collegiate portion of the Pole Vault, clearing 12-1 1 /2, losing only to Cimmy Virdi '16, who cleared 13-1 1/2.&nbsp;&nbsp;Elizabeth Weeks '21 jumped a season best 37-0 1/2 to win the Triple Jump.&nbsp; Lindsey McAllister '21 took first place honors in the Steeplechase, running 11:51.54.&nbsp; In the one loaded event for MIT, the 800, Katie Williams '21 took the race out in 64 to set up the MIT duo of Bailey Tregoning '19 and Margaret Trautner '20 as they went 1-2 in 2:14.17 and 2:15.30 respectively.</p>
              <p>On the men's side, Ellery Rajagopal '21 had the biggest breakthrough, finishing second in the 800 in a personal best 1:57.42.&nbsp; Nathan Munet '19, racing for the first time this season, went to the lead and took the brunt of the strong wind.&nbsp; He faded in the final 150 to finish fourth in 1:58.46.</p>
              <p>Scott Cameron '19 won the Pole Vault by clearing 15-5.&nbsp; Chris Washington '20 claimed the victory in the High Jump at 6-6 and Ryan Prinster '18 was victorious in the Triple Jump with a 46-2&nbsp; 1/2 effort.</p>
              <p>Most of the team goals were accomplished although a few will have to step up pretty big in next week's conference championship if they want their season to continue.&nbsp; The teams will try to defend their NEWMAC Conference titles at Springfield College.  The men have not lost the title since 2000 and the women have won every championship since 2009.              </p>
