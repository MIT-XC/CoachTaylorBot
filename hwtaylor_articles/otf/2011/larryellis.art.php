              <p><span class="headline">Four Athletes Travel to Princeton, Three Return With NCAA Qualifying Marks</span></p>
              <p align="left">April 22, 2011</p>
              <p align="left">Four MIT Track and Field athletes made the trip to Princeton University to race in the <a href="larryellis.res.php">Larry Ellis Distance Night </a>and three retured with NCAA Division III qualifying performances. The events raced were the 800-5000 meters. In order to enter the meet, an athlete had to make pretty challenging qualifying marks. MIT brought two male 3000 Meter Steeplechasers and two female 5K runners, all hoping to qualify for the NCAA Division III Championships.</p>
              <p align="left">With temperatures in the upper 40's and the threat of rain, it was close to ideal for the distance runners. Paul Welle '11, was up first for MIT, running in the second of three sections of the Steeplechase. Welle went out with the leaders, running 73 second quarters to come through the mile in 4:51. From the beginning, Welle's water jump was not the best, but as the race progressed he stopped driving his knee off of the barrier altogether, landing with both feet at the same time, virtually stopping after each jump. Nevertheless, Welle raced well and fought hard, finishing with a 35 second final 200 after two 87 second laps. His time of 9:12.93 met the NCAA Division III provisional qualifying time, missing the auto standard by less than six seconds.</p>
              <p align="left">Richard Prevost '11, was up next in the slower section of the event. Similar to his teammate, Prevost went out with the leaders. He came through the mile in 4:56 and took the lead briefly before fatiguing despite superb hurdle and water jump form. His final 600 meters in 1:57 hurt his time significantly, but the 9:23.10 still met the NCAA Division III provisional qualifying time.</p>
              <p align="left">Just before 9:00 p.m., Anna Holt-Gosselin '11, toed the line in the 5,000 Meter Race. Holt-Gosselin, the owner of a 36:02.79 10K NCAA qualifying time and a 17:19.86 MIT record, wanted to qualify with a solid sub-17:00 performance. The initial pace was a little fast, but Holt-Gosselin seemed to handle it well, splitting the first mile in 5:20. The pace of the second mile slowed as she fell off the group she had been with. Nevertheless, her 5:30 second mile and 5:31 third mile kept her in the race and even allowed her to move back up as many faded in the final mile. Her final 200 was 36 seconds, indicating there was still quite a bit more in the tank. The final time of 16:57.95, while just missing the NCAA Division III auto standard, is close to a sure thing for making the NCAA field. The 22 second demolition of the MIT record was an outstanding performance.</p>
              <p align="left">Alina Gatowski '11, in the second of three sections in the 5K, was also hoping to reach the NCAA provisional standard of 17:35. Her season best of 17:54 was certainly in danger. The 35 runners in the race, bunched up early in the race, running a pedestrian first mile of 5:39. Running much of that mile in the outside portion of lane two caused Gatowski to lose her focus and when the pace picked up she did not respond. While her second mile was a 5:34, she did not go with the leaders. Gatowski slowed to a 5:42 third mile and could only manage a 41 in the final 200. However, her 17:36.74 was a huge improvement and showed she just needs to build her confidence so she races for the win instead of just trying to hold on.</p>
              <p align="left">All four will be racing in their conference championship next Saturday, the NEWMAC Conference Championship will be hosted by the Coast Guard Academy in New London, CT.</p>
