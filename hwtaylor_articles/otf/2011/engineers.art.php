              <p><span class="headline">TECH TRACK BUILDS TWO VICTORIES IN ENGINEERS CUP</span></p>
              <p align="left">April 2, 2011</p>
              <p align="left">You never know what the weather will be like during early spring in New England. For the annual <a href="engineers.res.php">Engineer's Cup</a>, this years weather was warm enough at 50 degrees, but the 30 mph winds were very difficult to compete in and the temperatures felt more like low to mid 40's. Nevertheless, not a single MIT athlete seemed too concerned about the weather, preferring to follow the MIT tradition of looking at adversity as a competitive advantage and focusing on their performances. The women ran away with a score of 217 points to 124.5 for RPI and 61.5 for WPI. The men's meet was much closer with MIT not pulling ahead for good until the 800 meters. Tech won with 180 points to 158.5 for RPI and 66.5 for WPI.</p>
              <p align="left">The women's meet was never in doubt as MIT won every running event with the exception of the 4x400, an event they could have won if they chose to. Portia Jones '12, won the 100 and 200 meter dashes, setting a new MIT record of 12.41 in the 100. Jackie Brew '14, lowered her freshman record to 12.58 while claiming second place in the 100. Other than that double win, MIT spread the wealth. </p>
              <p align="left">In the 100HH, Amy Magnuson 'G, showed some of her old form as she won going away in 15.00. Magnuson led a one through six sweep of the event. Following behind her were Jones (15.38), Brew (15.50), Jackie Konopka '14, (16.05), Hazel Briner '11, (16.10), and Megan Bumgarner '14, (16.33). In the 7-5-4-3-2-1 scoring format, the 22 points put MIT out front to stay.</p>
              <p align="left">Jamie Simmons '12, took first place honors in the 400 in 58.19, a very fine time in the strong winds. Hazel Briner '11, jumped into uncharted territory to win the 400IH in 67.82. Alex Taylor '14, surprised many people by coming from behind to win the 800 in 2:24.98. In the 1500, Anna Holt-Gosselin '11, dropped down to the shorter distances and led start to finish in 4:43.27, leading MIT to the top four places.</p>
              <p align="left">In the distance events, Maggie Lloyd '12, took the 3K Steeplechase crown with a well paced 11:43.56. Alina Gatowski '11, led the 5K from start to finish, running 18:18.27 in the difficult winds.</p>
              <p align="left">The 4x100 team of Brew, Martha Gross '12, Simmons and Jones won easily in 48.98. In the 4x400, MIT sat out its All America unit and still nearly pulled off the victory. Emily Kuo '13, led off with a 64.8 to put MIT slightly behind at the exchange. Chandler Burfield '13, ran a fine 63.4 to put MIT in the lead. Taylor lost the lead during the leg, but reclaimed it by the exchange. Stephanie Ku '14, fought off the RPI runner through most of the leg, but was edged out in the final 20 meters, 4:18.18 to 4:18.43.</p>
              <p align="left">In the field events, Karin Fisher '11, led a 1-2-3 sweep in the Pole Vault with her 12-2 victory. Claiming second and third were All America vaulters Briner and Lauren Kuntz '13. The only other field event victory for MIT came in the Long Jump where Brew jumped 16-3 to defeat Briner by two centimeters. While not claiming any victories, it was nice to see Christine Labaza '14, compete for the first time. Out with knee and ankle injuries in the winter, Labaza threw well, taking third place in the Discus with her 113-05.</p>
              <p align="left">The men's meet was much different. Although expecting to win, Tech had to rethink things when Kyle Hannon '13, was unable to race due to foot pain and Mitch Kelley '11, had to sit out due to illness. The loss of Babajide Akinronbi '14, and Jake Jurewicz '14, due to slight hamstring pulls the week before, Mattias Flander '11, being away for interviews, Dan Harper '12, out with illness and Logan Trimble '13, and Gihan Amarasiriwardena '11, not being ready to race, put MIT at a competitive level where they would have to take advantage of every opportunity to score points.</p>
              <p align="left">MIT won seven of the 11 running events, dominating the middle-distance, and distance events to provide the margin of victory. In the sprint events, MIT won the 100 with Josh Duncavage '13, running a personal best of 11.02. Nick Leonard '11, and Duncavage battled the entire lap of the 400IH to finish 1-2 respectively in excellent times of 56.36 and 56.42. MIT also easily won the 4x100 Relay with Duncavage, Leonard, Tyler Singer-Clark '14, and Nathan Peterson '12, substituting very well for Akinronbi.</p>
              <p align="left">The middle-distance events were where MIT did the damage. In the 800, Eric Safai '14, returned from a 9:58.69 fourth place in his 3000 Meter Steeplechase debut, to win out of the slow section in 2:00.93. The fast section just raced to win the section, allowing the first 400 to go out in a pedestrian 62. Nevertheless, MIT claimed the top four places in that section and 1-5 overall. In the 1500, MIT came from behind to take the top four places. Justin Bullock '14, led the way with a 4:06.03 victory.</p>
              <p align="left">The distance events provided more victories but not the same depth. Paul Welle '11, led start to finish in the 3000 Steeplechase to win in 9:41.58. Richard Prevost '11, took second in 9:44.76. The 5000 was also a 1-2 finish for MIT with Stephen Serene '12, and Ben Mattocks '12, taking the honors in 15:42.84 and 15:45.06.</p>
              <p align="left">In the 4x400 Relay, DJ Ronde '13, led things off, running a little behind RPI in 52.2. Logan Daum '11, ran well, keeping MIT close with his 52.1. Mac Gager '14, ran a strong 51.0 leg to give MIT the lead. Pat Marx '13, a little fatigued from the 800 and 200 Meter races did not take it out hard. When the RPI runner decided to make a move, Marx was able to fight him off until the final 20 meters, where similar to the women's race, RPI was able to inch out the victory 3:26.08 to 3:26.21.</p>
              <p align="left">In the field events, MIT only claimed two first places, but still did quite well, scoring 55 points. Jackson Wirekoh '13, took first place in the Triple Jump with a leap of 42-4 and Nathan Peterson continued his winning ways in the Pole Vault with a 15-1 effort. While not winning, Ken Cooper '13, earned three second places and three personal bests. In the Shot Put, Cooper threw 47-7, in the Discus 144-1 to miss first place by 4&quot; and a provisional national qualifying throw of 178-2 in the Hammer. In the Javelin, Vlad Sobes '11, improved his national mark to 191-1, although he placed second.</p>
              <p align="left">Next Saturday, MIT travels to Lewiston, Maine to face host Bates College, Colby College and on the men's side, also Southern Maine.</p>
