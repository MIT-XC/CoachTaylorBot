              <p><span class="headline">DESPITE POOR WEATHER, ENGINEERS RESPOND WITH OUTSTANDING PERFORMANCES</span></p>
              <p align="left">April 16, 2011</p>
              <p align="left">MIT hosted their annual <a href="springinvite.res.php">Spring Invitational</a>, but the Spring part never really happened. Destined to work with 45 degree temperatures with  extremely strong winds coming off the ocean, the MIT athletes competed at their best, ignoring the conditions the way they always do. The non-scoring meet allowed the Engineers to rest many of their top athletes or put them in other events to qualify them for the upcoming championships.</p>
              <p align="left">Given the strong winds, the biggest PR's came in the sprints as expected. Martha Gross '12, ran the 100 Meters in 12.64 to finish second and Theresa Santiano-McHatton '14, ran 13.43. Josh Duncavage '13, ran 10.94 to break the 11 second barrier for the first time and win the event. Duncavage also ran a PR in the 200 with his 22.22 to claim second.</p>
              <p align="left">In the high hurdles, Megan Bumgarner '14, had her best race of the year, running 15.82 to claim second place to teammate Amy Magnuson 'G, who ran 15:14 despite her poor start. Nick Leonard '11, ran a hand time of 15.2, but the FinishLynx system timed him in 16:00. Nevertheless he won the race and is looking good for the upcoming championships.</p>
              <p align="left">The 400IH, as with all of the lap races, presented a formidable homestretch with a constant strong headwind, sometimes exceeding 30 mph. Jamie Simmons '12, is coming closer to rediscovering her rhythm that got her second place at the NCAA Division III Championship last spring, only having two poor hurdles technically. She ran a season best of 64.20, just barely qualifying provisionally for nationals with her 64.20. Paul Welle '11, the team's premier steeplechaser, debuted in the 400IH and ran quite well, placing fourth in 59.31.</p>
              <p align="left">The 800 was a bright spot for MIT despite the relatively slow times due to the strong winds. The men placed 1-3 as Pat Marx '13, ran a strong race to win in 1:58.48. Justin Bullock '14, normally running the 1500 first, picked the wrong time to try and get a big 800 performance, but raced well to get third in 1:58.87. Louise van den Heuvel '14, ran a great time for the conditions, but was unable to hold on for the win, claiming second instead in 2:22.40. Chandler Burfield '13, continued her comeback as she placed third in 2:25.85.</p>
              <p align="left">Dawit Zewdie '13, ran an excellent 4:02.85 to qualify for the New England Division III Championship. With the high winds, no one was willing to take the pace out hard. Zewdie knew this could be his final opportunity to qualify so he did it by himself. One week after qualifying in the 5K, Zewdie did all the work up front, ultimately setting himself up to be out kicked and settling for third place. On the women's side, Brooke Johnson '13, coming back from her injury, also took matters into her own hands, running 4:55.03 for second as she was out kicked in the end. Maggie Lloyd '12, ran a great race, PR'ing in 4:56.19.</p>
              <p align="left">In the 3000 Meter Steeplechase there was very little incentive to push the pace. Richard Prevost '11, took the race with two laps to go, running 9:46.21 to win easily. Christian Reed '14, made his debut with a 10:23.99 effort for seventh.</p>
              <p align="left">Alina Gatowski '11, wanted to run a fast 5K time, and did, but not as fast as she wanted, running 17:54.32 to claim second place. Tania Morimoto '12, raced well, although there was really no one to race at the pace she needed. Nevertheless, she ran 18:18.93 to show she is back and running well. In the men's race, Stephen Serene '12, Ben Mattocks '12, and Gihan Amarasiriwardena '11, had it as their goal to qualify for the New England Division III Championship, which require a 15:30 or better. The wind was not relenting so the runners took the pace out, but had difficulty maintaingin the pace. The first mile was 4:53 but the second mile 5:04. With 800 to go, they were in need of a sub 2:20 final two laps in order to qualify. Serene was the only one to pull it off, running 64 for the final 400 to claim first in 15:27.14.</p>
              <p align="left">The mixed gender 10K only had two MIT entries, Dan Harper '12, and Roy Wedge '14. In the New England Division III Championship, the women can qualify for the 10K with a 5K qualifying time, whereas the men have to meet the 10K qualifying standard with a 10K time, which required 33:40 or better. The two distance aces were not concerned about hitting the time, even in the windy conditions. Knocking out 79 second quarters, the pair hit the first 5K in 16:30 and closed slightly faster. Harper ran 32:41.30 to win and Wedge took it easy, running 32:47.85 for second.</p>
              <p align="left">Both teams fielded 4x800 squads, but again only for the sake of securing a time to guarantee getting into the fast section of the New England Division III Championship. The women, running van den Heuvel (2:24), Martina de Geus '14, (2:30), Lloyd (2:26) and Johnson (2:27), won by 40 seconds in 9:48.67, running alone the entire race. The men were also alone the entire race, winning in 8:12.61 with splits of 2:01 for Prevost, 2:02 from Bullock, 2:05 from Zewdie and a 2:03 from Kyle Hannon '13.</p>
              <p align="left">In the field events, MIT could not take full advantage of the wind, and the athletes had to endure the cold much longer due to multiple events and the breaks between efforts. In the Pole Vault, the Tufts University vaulters put it to the MIT vaulters as the Jumbos went 1-2 with Nakanishi clearing 15-7 3/4. Cyrus Vafadari '12, jumped well for MIT, claiming third at 14-7 1/4, but teammate Nathan Peterson '12 was not able to clear a height. Karin Fisher '11, won easily, nearly clearing 12-7 1/2, but settling for 12-3 1/2. Lauren Kuntz '13, was second at 11-5 3/4 and Hazel Briner fourth at 10-11 3/4. Kate Wymbs '14, Becky Stavely '11, and Lynn Jepsen '11, all cleared 10-6 for Tech.</p>
              <p align="left">In the throws, Brooke Hamilton '14, had a good day, throwing a PR 37-9 1/2 in the Shot Put, and a personal best of 86-3 in the Javelin. For the men, Jeremy Sharpe '14, saw the only improvements on the day, throwing nice personal bests in the Shot at 41-8 and the Hammer with a throw of 137-0.</p>
              <p align="left">Next week, the Heptathlon and Decathlon athletes will compete in their respective events at the Holy Cross meet on Wednesday and Thursday. A couple of the distance runners will go to Princeton to compete in the Sam Ellis Distance Festival on Friday night. Most of the team will travel to Springfield College for an invitational on Saturday while some MIT athletes will rest for the NEWMAC Conference Championship the following weekend, a meet the men have not lost since 2000, while the women will be going for their third consecutive championship.</p>
