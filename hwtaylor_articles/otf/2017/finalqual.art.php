                  <h3>MIT Athletes Perform Well in Final Qualifier</h3>
                  <h4>Prepare for NCAA Division III Championship</h4>
                  <p>&nbsp;</p>
              </center>
              
              
              <p>May 18, 2017</p>
              <p>MIT hosted the <a href="finalqual.res.pdf">NCAA Final Qualifier</a> for the New England Region on the hottest day in May one can imagine&nbsp;for this area of the country.&nbsp; The 95 degree heat was mitigated by a 15 mph breeze throughout the meet, aiding the sprinters and jumpers.</p>
              <p>This was the last day where an athlete could achieve a qualifying mark for the NCAA Division III Championship.&nbsp; Many MIT athletes performed well, but only a few took advantage and performormed personal bests.&nbsp; Starting things off, the quartet of Hannah Chen '18, Michelle Menkiti '20, Jane Reed '20 and Nneoma Okonkwo '18 ran a season best of 47.72 in the 4x100 Relay.&nbsp; Eventhough it was their best performance of the season, it still was not good enough to get them into the top 16 teams accepted into the NCAA field.&nbsp; It took a 47.39 to make it into the championship.</p>
              <p>In the Discus, both Ariela Slutsky '18 and Jackie Vahey '17 set season bests.&nbsp; Slutsky won with a 151-8 1/4 all time personal best.&nbsp; Vahey threw 141-11.</p>
              <p>In the men's competition, Jon Fakkema '19 threw a personal best in the Hammer, a 171-11 1/4 performance.&nbsp; </p>
              <p>Tre Albritten '18 competed in an open event for the first time this season, running the 200 in 21.61.&nbsp; His performance fell two tenths short of qualifying for nationals.</p>
              <p>In the 1500, a loaded field, aided by a pacer taking the race out in 2:03, screamed through the 1200 at 3:04 and Colin Godwin '17 was at the front.&nbsp;&nbsp;He finished strong, running a personal best 3:51.60.&nbsp; However, this time left him two spots shy of making Nationals&nbsp;as he ended up 22nd in the country and only the top 20 for the men, and top 22 for the women per event were selected.</p>
              <p>On Tuesday, Tech Track &amp; Field heads for Geneva, Ohio and the Spire Institute for the NCAA Division III Outdoor Track and Field Championship.&nbsp; The following 15 student-athletes will represent MIT Thursday through Saturday.</p>
