                  <h3>Deyo Leads Engineers in Early Season 10K</h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>March 25, 2017</p>
              <p>As in most years, when the weather permits, MIT sent a handful of distance runners down to <a href="bridgewater.res.php">Bridgewater State University</a> for a shot at qualifying in the 10K for the New England Division III Championship and possibly higher level championships.&nbsp; Most meets during the season do not run the 10K, and the temperature tends to become less conducive to faster times as the season progresses.&nbsp; The weather was near perfect in the women's race, and slightly windy in the men's race, but with the temperature in the low 40's one could not hope for a better situation.&nbsp; </p>
              <p>The qualifying times for the New England Division III Championship were fairly pedestrian at 33:20 for the men and 41:20 for the women, so&nbsp;for the MIT runners it was more a matter of competing well and running a reasonably fast time.</p>
              <p>MIT's male entries were hoping to lock onto a pace between 5:00 and 5:05 per 1600 meters.&nbsp; Unfortunately for some of them, they all went out in 4:59-5:03, and some were not quite ready, either mentally or physically.&nbsp; Matt Deyo 'G led the way after about a mile of Brian Gilligan '17 taking the lead from the gun.&nbsp; Both came through in 4:59.&nbsp; Deyo continued to run 74 pace, coming through 3200 in 9:55.&nbsp; Gilligan fell off a bit, but was solid at 9:57.&nbsp; Daniel Weiss '19 was next at 10:01, along with David Walter '18.&nbsp; Josh Derrick '20 was one who had gone out strong, but started fading after the mile mark as he was simply using too much energy--not running relaxed.&nbsp; Rory Beyer '18 was at 10:07, and Nicholas Waltman&nbsp;'18 and Alex Knoedler '18 were running better paced races, coming through at 10:10 and 10:11 respectively.&nbsp; Over the next two miles&nbsp;Deyo continued his pace, hitting 6400 at 19:50.&nbsp; Weiss was the next fastest at 20:17.&nbsp; Gilligan was trying to hold on at 20:24, but was falling back.&nbsp; Beyer was at 20:25, Waltman 20:27, Derrick 20:36, Knoedler 20:38 and Walter 20:41.&nbsp; Deyo had been all alone since 2K into the race and nothing was changing.&nbsp; As the wind picked up on the backstretch he slowed to a 4:58 on the fifth 1600, but then rallied with a 4:55 and 68 final quarter to win in 30:51.63.&nbsp; This is not nearly a fast enough performance to qualify for Nationals, but shows that with competition he can certainly get the job done.&nbsp; Weiss finished in&nbsp;a very solid 31:52.85 to place third.&nbsp; Waltman placed sixth in 32:14.22, Gilligan eighth in 32:16.91, Beyer ninth in 32:26.57, Knoedler 10th in 32:28.20, Derrick 13th in 32:38.40 and Walter was 15th in 33:12.17, out of a total of 33 runners.&nbsp; All MIT runners qualified for the New England Division III Championship</p>
              <p>The women had raced first&nbsp;when there was no wind, but it was also pretty early in the day at 9:00 a.m.&nbsp; The eventual winner, Kerri Ruffo '18 from Northeastern took the pace out hard, pulling everyone out a bit faster than they wished to go.&nbsp; Camila Thanos '19 went out at a suicidal pace of 87, slowed a bit, but did not hit a reasonable pace until 1200 meters, still hitting the 1600 in 6:07.&nbsp; Bailee Margolis '19, Darby LaPlant '19 and Charlotte Folinus '20 all went out in 90 for the 400, when they should have been back at 94.&nbsp; This seemingly small difference would have a huge effect over 6.2 miles.&nbsp; Thanos continued to widen the lead on her teammates through&nbsp;2400 meters, when she went to 95 second pace.&nbsp; Margolis and LaPlant were running as if they were on a mission, starting to negative split as they went from 6:20 to 6:14 in the second 1600.&nbsp;Folinus started falling off just before the 3200 mark.&nbsp; Thanos pulled out with a foot injury after 2800 meters and Folinus after 5K due to digestive issues, which was in about 20:00.&nbsp; Margolis and LaPlant continued to race well, putting in a 6:18 and then a 6:13.&nbsp; When Margolis continued to pick up the pace, dropping a 6:06, LaPlant fell off, but still ran 6:16.&nbsp; The final 1600 for Margolis was a 6:03, finishing her final quarter in 91&nbsp;for a 38:45.10, a new personal best, to finish fourth overall and the top Division III runner.&nbsp; LaPlant finished in sixth in&nbsp;39:11.50.</p>
              <p>The technical event athletes on the team will be in Texas for the Spring Trip this coming week, competing at Trinity University on March 31.&nbsp;</p>
