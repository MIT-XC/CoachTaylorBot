              <p><span style="font-weight: bold; font-size: 18px">OUTSTANDING PERFORMANCES DAMPENED BY INJURIES DURING SPRING TRIP</span></p>
              <p align="left">March 29, 2014</p>
              <p align="left">The MIT Track and Field teams traveled to Claremont, California for a week of warm weather training during MIT's Spring Break week. After five days of practices at Claremont McKenna, the teams traveled to Westmont College in Santa Barbara for the <a href="Westmont.res.pdf">Westmont Invitational</a>, a non-scoring meet with host Westmont, George Fox, Fresno Pacific, Santa Barbara Track Club, the France CE Team (primarily made up of multi-event athletes) and a few unattached performers.</p>
              <p align="left">MIT dominated many events at the meet despite only bringing 16 for each gender, primarily those in technical events. The women won five events as well as setting a varsity record and a freshman record. The men won nine events, led by Ken Cooper '15 who won three of them and Michael Kaba '16 and States Lee '16 who won two others after combining their skills to play a part in the 4x100 Relay victory.</p>
              <p align="left">Preethi Vaidyanathan '15 placed third in the High Jump, clearing 5-05 to set a new MIT record, leading the MIT performances for the women. Jackie Vahey '17 put herself on three MIT top ten lists; fifth on the Shot Put list at 38-3 1/2, ninth on the Hammer list at 131-6 and fifth on the Discus list, setting a new freshman record at 126-4.</p>
              <p align="left">Jackie Brew '14 played a role in three MIT victories. The first was running the leadoff leg in the 4x100 Relay, along with Veronica Szklarzewski '15, Kendra Knittel, and Stephanie Birkhimer '14 to win in 48.59. She also won the 100HH in a PR of 14.87 and the 200 Meter Dash in 25.94.</p>
              <p align="left">Christina Wicker '17 turned her debut in the 3000 Meter Steeplechase into an 11:53.22 victory. Cimmy Virdi led an MIT domination in the Pole Vault, winning with a vault of 12-0.</p>
              <p align="left">For the men, Ken Cooper '15 won the Hammer Throw, Discus Throw and Shot Put as he showcased his considerable talents. The most impressive of his victories was in the Discus where he threw 162-11, moving him to number three on the all-time MIT list. In the Shot, Cooper threw 52-6 1/2 to lead all throwers, and in the Hammer he threw 161-5 to easily win the event.</p>
              <p align="left">Michael Kaba '16 started his day running the second leg on the winning 4x100 Relay team, joining States Lee '16, Taylor Njaka '17 and Tyler Singer-Clark '14 in running 41.98, an excellent time for the first meet of the year. Kaba then claimed victory in the 110HH in 15.06, an event he rarely gets to practice. He ended his day  winning the 400IH in a come-from-behind 54.10 victory.</p>
              <p align="left">Njaka added the 100 Meter Dash to his victory resume, sprinting an 11.19 into a slight headwind. Singer-Clark, who was closing ground fast on Njaka, pulled up with an injured hamstring in the final 20 meters, still earning fourth place in 11.71.</p>
              <p align="left">States Lee '16 won the 400 in a personal best of 48.09, a time that is very likely fast enough to get him to the NCAA Championship in two months. He later returned to the track to win the 200 into a headwind, running 22.50.</p>
              <p align="left">The injury to Singer-Clark was added to Nick Diamantoni '15 pulling his hamstring at Wednesday's practice and Stephanie Guo's '17 IT band /patella-femoral injury. The hope is that all will be ready to return to competition soon.</p>
              <p align="left">Next weekend, MIT will take full teams to Troy, NY, home of RPI, the host of this year's Engineer's Cup, an annual triangular meet between MIT, RPI and WPI.</p>
