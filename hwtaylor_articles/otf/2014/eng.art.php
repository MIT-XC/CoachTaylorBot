              <p><span style="font-weight: bold; font-size: 18px">Men and Women Begin Season Undefeated Despite Depleted Roster - Virdi Sets MIT Pole Vault Record</span></p>
              <p align="left">April 5, 2014</p>
              <p align="left">The weather was a lot different from sunny California the week before, but the result very similar as MIT's Men's and Women's Track &amp; Field teams dominated RPI and WPI in the annual <a href="eng.res.php">Engineer's Cup</a> despite resting many of their distance runners and taking precaution with quite a few injured athletes. The women were never contested, winning 12 of the 19 events and taking four of the six scoring places in three events en route to winning 184 to 110 for host RPI and 98 for WPI. The men also won 12 of the 19 events in taking the team victory with 165.5 to 146 for RPI and 87.5 for WPI.</p>
              <p align="left">The story of the meet was made by Cimmy Virdi '16, who won the Pole Vault in a new PR and MIT record of 13-3 1/2, currently the leading vault in the country for the DIII Indoor National Champion. Even though the temperatures varied between 44 degrees at the beginning of the meet to 39 at the end, with winds gusting in excess of 20mph, the vaulters performed well, claiming five of the six scoring places in the women's meet and four of six scoring places in the men's meet. Ben Schreck '15 won the men's vault at 15-03.</p>
              <p align="left">Ken Cooper '15 won his requisite three events, the Hammer Throw, Discus Throw and Shot Put, although the temperature challenged all the throwers efforts after the first event. That first event was the Hammer and Cooper threw a season best of 182-3. In the Discus he came from behind on his last throw to win with a mark of 155-4. In the Shot Put, Cooper had to take a standing throw to get a fair mark in the trials and then finally got a full throw in on the first attempt of the finals to win at 50-0 1/2.</p>
              <p align="left">Throwing event victories for the women were earned by Tilly Taylor '15 in the Javelin and Isabella Stuopis '16 in the Shot Put. Throwing 123-3 despite the muddy approach Taylor managed to get decent but not great blocks without sliding too much. Stuopis threw 42-7 1/2 to win her event by over four feet.</p>
              <p align="left">Alexandra Taylor '14 was a double winner, taking both the 1500 and 800 meter races. In the 1500, she took over a slow paced race after the 800 mark and cruised to an easy victory in 4:54.19. For the 800, she negative split the race, coming through the first 400 in 69 and finishing in 2:17.83 despite the nasty headwind down the homestretch.</p>
              <p align="left">Maryann Gong '17 took the lead in the 5K and never relinquished it, negative splitting the race with 1600 splits of 5:45, 5:51, 5:28 and closing in 39 for a 17:43.43 victory.</p>
              <p align="left">Tech took both horizontal jumps for the men and women. Carrington Motley '16 won the Long Jump in a PR of 21-10 3/4 and teammate Angel Echevarria '16 won the Triple Jump with a jump of 46-0 1/2. Veronica Szklarzewski '15 won the Long Jump at 16-10 3/4 into a headwind and Michelle Johnson '15 took first place honors in the Triple Jump at 36-4 1/4.</p>
              <p align="left">Jackie Brew '14 fell over the first hurdle in the 100HH, an event she should have easily won, but she successfully navigated the 200 to victory, winning in 26.48 into the wind. </p>
              <p align="left">States Lee '16 won his specialty, the 400, in a meager 49.87 as he led his teammates to a 1-2-3-6 finish and also took first place honors in the 200 with a 22.60.</p>
              <p align="left">Stephanie Birkhimer '14 moved up in events to the 400 and came away with a nice victory in 62.16.</p>
              <p align="left">Both the men and women claimed victory in the 4x100 Relay, even though both units went through numerous alterations due to injuries that popped up since the previous meet, including another one during practice on Thursday. The women went first and won in 49.48 with a quartet of Brew, Szklarzewski, Stephanie Birkhimer '14 and Jackie Konopka, despite the fact that Konopka had to nearly stop to wait on the baton. The men, with only one member remaining from the team that ran 41.98 last week, were caught off guard when the starter fired the gun without checking to see if the zones were ready. While Michael DuPlessis '14 was fine as the leadoff runner, Jordan Mizerak '14, Lee and Adrian Samsel '16 were not ready. Samsel had to sprint back to his position and did not even have time to put down a take-off marker. The first handoff worked well, but the next two were not so great. However, the team managed the victory in 43.30.</p>
              <p align="left">Christina Wicker '17 remained undefeated in the 3000 Steeplechase. Her winning time of 11:51.13 moved her into MIT's top 10 list at number 10. She has a ways to go to match the record of 10:04.76 set by Jacqui Wentz back in 2010, but she may get the freshman record of 11:08.37 once she has some competition.</p>
              <p align="left">Harrison Hunter '15 won the 800 by even splitting his race, a difficult thing to do in the wind he was subjected to. His first lap was 58.5 and he finished in 1:57.09.</p>
              <p align="left">Samsel ended his busy day with a victory in the 400IH. Despite slowing a bit in the homestretch as he headed into the wind, he won in 56.65.</p>
              <p align="left">The final victory of the meet for MIT came in the final event, the 4x400 Relay. While the women were so far ahead they pulled the relay, the men ran it. Holding out all of the top four 400 runners, MIT still won in a come from behind victory in 3:24.86. Austin Fathman '15 led off in 51.5. Henry Tareque '17 ran 52.9, but fell behind WPI. Hunter ran 50.2 to bring MIT within 10 meters as he handed off to Sam Parker '14. Parker ran 50.2 to pull out the victory right before the finish.</p>
              <p align="left">Next week, MIT travels to Bates College in Lewiston, Maine to test their skills against Bates, Colby and Southern Maine.</p>
