              <p><span style="font-weight: bold; font-size: 18px">MIT Remains Undefeated - Vahey Sets Varsity and Freshman Discus Records</span></p>
              <p align="left">April 12, 2014</p>
              <p align="left">MIT's Track &amp; Field team traveled to Lewiston, ME, home of the Bates College Bobcats for a <a href="batesquad.res.php">quadrangular meet</a> on the men's side (Bates, Colby, MIT, Southern Maine) and a tri-meet on the women's side (Bates, Colby, MIT). The weather was by far the best thus far in the early spring of New England with near 70 degree temperatures and mostly sunny skies, although the wind was gusting to 30 mph much of the day, hurting all running events with the exception of the 100 Meter Dash.</p>
              <p align="left">Since the number of teams was different for both genders, the scoring system for the two meets differed as well. The men scored eight places with a format of 9-7-6-5-4-3-2-1 with relays scoring 9-7-6-5. The women only scored six places using 7-5-4-3-2-1 for individual events and 7-5-4 for the relays. Despite the large number of injuries on the men's side, as well as one critical one at the beginning of this meet, the men won with a score of 257.5 to 220 for Bates, 122.5 for Colby and 107 for Southern Maine. The women continued their unbeaten streak with 176.5 points to 129.5 for Bates and 110 for Colby.</p>
              <p align="left">As the men were warming up, testing their marks for the 4x100 Relay, Jordan Mizerak '14, an excellent pole vaulter and fill-in on the relay, sustained a hamstring pull while finishing a handoff. To add insult to injury, or actually injury to injury, Stephanie Birkhimer '14 rolled her ankle while preparing for the high jump and her leg on the 4x100 Relay.</p>
              <p align="left">Going into the meet with these additional injuries meant quick additions to the 4x100 and rethinking what would be needed to win the meet. MIT was favored to win, but it would be in jeopardy if the team did not perform to their abilities.</p>
              <p align="left">Fortunately, the teams raised their game. The men won four of the eight field events (gaining second place in three of the others as well as two of those where they also took first place). They also won six of the running events. The women were first in six of the eight field events as well as five of the running events. They also scored three of more of the six scoring places in eight events to dominate the meet.</p>
              <p align="left">Jackie Brew '14 was the dominant force for the women, winning three events (100, 100HH, 200) and setting personal bests in two of them; 12.48 in the 100 and 25.54 in the 200. Brew also ran a leg on the third place 4x100 Relay. </p>
              <p align="left">There were many other event champions for MIT, but two standout above the others. Martina de Geus '14 made her return to competition after tearing her ACL at the NCAA Division III Championship last May in the 3000 Steeplechase. It took a lot of fortitude to race, and even more to go after the win. Each hurdle and water jump got better as the race progressed. Her winning time of 11:24.24 was far from her best, but a huge step forward.</p>
              <p align="left">The other was the final event of the meet, the women's Discus. Freshman Jackie Vahey launched her final throw 132-3 from the circle to win the event and set both the freshman and varsity record at MIT.</p>
              <p align="left">Overall, the women took 20 of the possible 48 places in the field events, most of them of the first and second place variety. Isabella Stuopis '17 continued to dominate the Shot Put, winning at 41-5 3/4. She also threw a personal best of 115-7 to place second in the Discus. Preethi Vaidyanathan '15 returned to her winning ways in the High Jump, clearing 5-2 1/4 for the victory. Cimmy Virdi '16 battled tremendous cross winds in the Pole Vault, winning with a vault of 12-0 3/4. Michelle Johnson '15 took first place honors in the Triple Jump at 36-4 1/4. Tilly Taylor '15 threw her season best in the Javelin Throw, a 133-10 performance, good for first place.</p>
              <p align="left">On the track, Elaine McVay '15 added to the winning performances by Brew and de Geus. Her 36:03.33 in the 10K was phenomenal given that she was by herself and having to deal with the very high winds. The mark currently has her number nine on the NCAA Division III rankings.</p>
              <p align="left">The men had no double winners this week. Even Ken Cooper '15 was unable to pull off his normal &quot;hat trick&quot;. Cooper did win the Hammer with a throw of 178-3. He did place second in both the Shot Put and Discus with performances of 51-9 3/4 and 160-11 respectively.</p>
              <p align="left">Also in the field events, Carrington Motley '16 took first place in the Triple Jump with a leap of 46-6 1/4, his best jump of the season. Motley also earned second place in the Long Jump with 21-8 3/4. John Thomas '15 came right off his 4x100 relay substitution to clear 6-0 3/4 to win the High Jump. Harry Rein '15 threw a personal best of 175-9 to win the Javelin in the strong cross-winds.</p>
              <p align="left">States Lee '16 had a great day on the track, taking second the 100 in a personal best of 11.07, winning the 200 in a personal best of 21.84, running a strong third leg of the 4x100 Relay to assist in the 43.10 victory, and gave MIT the lead for good in the 4x400 Relay, leading off in 49.0.</p>
              <p align="left">Adrian Samsel '16 had a busy day. In addition to winning the 400IH in 56.18, he also ran a PR in the 110HH, getting second place in 15.34. He also took third in the Long Jump at 21-0 1/2, second in the High Jump at 5-10 and anchored the winning 4x100 Relay.</p>
              <p align="left">Harrison Hunter '15 edged out teammate Sam Parker '15 in the 800, proving his hard work is paying off. Despite the high winds Hunter ran 1:55.36 to Parker's 1:55.55. Both also ran on the victorious 4x400 Relay, running 51.2 and 50.2 respectively.</p>
              <p align="left">Joining the inspirational anecdotes for the day, Justin Bullock '14 returned to the track after his stress fracture on one leg and getting run over by a car on the other leg sidelined him for the championship portion of the indoor season and the first two weeks of the outdoor season. His start to finish victory in the 1500, braving the strong winds by himself and winning in 4:06.64 was very motivating for his teammates.</p>
              <p align="left">Benji Xie '15 teamed with Matt Jordan '15 to go 1-2 in the 10K. Both separated themselves from the pack after 800 meters and then Xie separated himself from Jordan in the final mile. Both were just running to qualify for the New England Division III Championship in three weeks. Xie won in 32:20.19 and Jordan ran 32:26.92.</p>
              <p align="left">Next week, MIT will take a handful of runners to Princeton for the Friday distance portion of the Larry Ellis Invitational. Adrian Samsel will compete in the Holy Cross Decathlon mid-week and the rest of the team will be at MIT's Sean Collier Invitational on the 19th.</p>
