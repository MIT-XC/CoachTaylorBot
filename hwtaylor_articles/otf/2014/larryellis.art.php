              <p><span style="font-weight: bold; font-size: 18px">Gong Sets Varsity / Freshman Record in 5K</span></p>
              <p align="left">April 18, 2014</p>
              <p align="left">The MIT Track &amp; Field team took a handful of athletes to the Friday evening distance festival portion of the <a href="larryellis.res.php">Princeton University Larry Ellis Invitational</a>. The cool temperatures and no wind were perfect conditions for the runners.</p>
              <p align="left">The highlight of the meet for the Engineers was Maryann's Gong performance in the fast section of the 5000 Meters. Gong '17 had only run one 5K previously, a sit and kick effort two weeks earlier that netted her a 17:43.43 win against RPI and WPI. This time she was with some very heavy hitters in East coast Division I runners as well as some high profile club runners. Gong started out in the middle of the 30+ runner field. The pace was perfect as she started clicking off 79-80 second quarters, hitting the 1600 in 5:17 and the 3200 in 10:37. At this point she started losing contact with the front runners in her group as they sped up slightly and she slid back to 81 and eventually 83 seconds before bringing it back in 80 for a final 1600 of 5:28. She finished in 16:45.86, which is a new MIT record, as well as a freshman record and currently the second fastest time in the country for Division III.</p>
              <p align="left">Earlier in the night, first up for MIT was Cindy Huang '15 in the 800 Meter race. Huang went out in the 400 at 66 seconds and was in the back of the pack, running comfortably. She then took off, running the next 400 hard, moving up the the front of the pack. With 200 to go she was about to take the lead when the wheels started to fall off the bus. It turned out she pushed too hard, too early. Her final 200 was a painful 39 seconds to finish in 2:19.23, a time far from her best.</p>
              <p align="left">Both Sam Parker '15 and Harrison Hunter '15 were entered in the 800. Hunter went first, racing in the fifth of eight sections. The pace was quick, and even though he was towards the back of the pack, he came through the 200 in 25 seconds. This was not the best of strategies, but he had to go with the pack. The pace slowed to 54 for the 400. It stayed this way through the 600 at 1:23 before Hunter took the lead and sprinted home to win the section in a new PR of 1:53.64. Parker was in the sixth section, and while the pace was not quite as fast as the previous section, Parker was not far behind at 26 seconds for the first 200. Per his usual tactics, Parker put himself on the rail and did not seem to want to come off, despite the frustration of being boxed in for more than 400 meters. With 200 to go Parker started moving to the front and in the final straight nearly pulled it off. He finished second in a new personal best of 1:52.46.</p>
              <p align="left">In the 1500, Alexandra Taylor '14 was testing her injured foot, which has prevented her from putting in very much mileage, but not from racing in her final season of eligibility. Again, the early pace was too fast as they hit the 400 in 70. She was relaxed and running well as they hit the 800 in 2:25 and the 1200 in 3:42. The fast first 400 and lack of mileage hit her about that point as she struggled in at 4:42.14.</p>
              <p align="left">Justin Bullock '14, continuing his comeback, and building on last week's 4:06 victory, was ready for a fast race. Also not back to high mileage, Bullock was not prepared for the 61 first 400, but did not let it affect his competitiveness. He continued to race through the 800 at 2:07 and the 1200 at 3:10. Despite his obvious fatigue, Bullock kept pushing and got a 3:58.02 for his efforts.</p>
              <p align="left">Next week MIT defends their NEWMAC Conference Championship titles at Mt. Holyoke College. The men have only lost once since the conference's inception back in 1997. The women are going for their sixth consecutive title.</p>
