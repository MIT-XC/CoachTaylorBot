              <p><span style="font-weight: bold; font-size: 18px">Vahey Sets Freshman Hammer Record at Sean Collier Invite</span></p>
              <p align="left">April 19, 2014</p>
              <p align="left">One year ago, when this meet, formerly called the MIT Spring Invite, was cancelled due to the shooting of MIT Officer Sean Collier and the safety concerns that followed, it was decided to rename the meet to honor Officer Collier. A total of 569 athletes from 37 teams and clubs entered the meet. The weather cooperated and the performances were excellent, putting the final touches on the renaming.</p>
              <p align="left">MIT's team had sent a small handful of athletes to the Larry Ellis Invitational at Princeton and gave a few more time off to heal or recover for next week's NEWMAC conference championship. Of those who did compete many performed very well, setting personal bests as the regular season came to a close.</p>
              <p align="left">Leading the way for Tech was Jackie Vahey '17 who set the Freshman Hammer record of 141-6, a week after setting the Discus record. For the men, Carrington Motley '16 destroyed the field in the Triple Jump, adding a foot to his personal best in jumping 48-3 1/2, tying him for number three on the NCAA list.</p>
              <p align="left">MIT had numerous victors at this meet, but only one individual had two wins and that was States Lee '16. The strong winds kept most oval performances down and this was also true for the 400 where Lee won in 49.04. He was able to come back in the 200 to win in 22.21, a victory that was not decided until Lee out-leaned Adam Julien from Emmanuel right at the line.</p>
              <p align="left">Rory Beyer '17 had a nice breakthrough in the 3000 Meter Steeplechase. With a previous best of 10:00, the freshman made a late charge to pull in the leader and then pulled away with a final 200 of 35, which includes a water jump and one other barrier to finish in 9:39.61. Teammate Kris Frey '15 also set a PR, running 10:01.21</p>
              <p align="left">Rick Paez '15, running out of the second fastest section of the 800, won the race with a huge PR of 1:57.77, beating everyone in the fast section as well. Dominique Hoskin '15 ran a personal best of 2:00.89 and Henry Tareque '17 made his 800 debut, running a very strong race in 2:01.31.</p>
              <p align="left">While he did not win, senior Michael DuPlessis, who has been coming back from injury after injury, did what he does each year, getting it done at championship time. DuPlessis finished third in the 100 in 11.09.</p>
              <p align="left">Although he did not win an event, Ken Cooper '15 claimed three second place finishes, taking runner-up in the Hammer, Discus and Shot Put. He just missed his personal best in the Hammer, throwing 187-6, which moves him to number eight on the NCAA list. Alex Noakes '16 did have a personal best, throwing 150-9.</p>
              <p align="left">Harry Rein '15 threw a personal best in the Javelin for the second week in a row, this one good for second place at 188-0. His new mark puts him number 10 on MIT's top 10 all-time list.</p>
              <p align="left">Ben Schreck '15 lost a jump-off in the Pole Vault, but did clear 15-9 prior to that to put himself at number 13 on the NCAA qualifying list.</p>
              <p align="left">Matt Deyo '16 ran a personal best in the 5K with a 15:07.12 effort. The plan was to just run fast enough to run sub 15:15, a time that should guarantee a spot in the fast section of the New England Division III Championship 5K in two weeks. However, BJ Pierce from Franklin Pierce College ran much faster than his seed, hammering out a fast pace and then out-kicking Deyo for the win in 15:03. Deyo and teammate Matt Jordan '15 were not expecting a race. Deyo dealt with it pretty well while Jordan did not, fading to 15:32.56.</p>
              <p align="left">Cimmy Virdi '16 won the Pole Vault as expected, clearing 11-11 3/4, but was unable to make a higher height. Abby Klein '14 set a personal best, clearing 11-5 3/4.</p>
              <p align="left">Ashley Wheeler '15 did not set a personal best, but she threw well, winning the Hammer Throw with a distance of 150-6. Also in the throws, Tilly Taylor '15 threw a season best of 135-7 to move up to number 12 on the NCAA list.</p>
              <p align="left">In the running events, Martina de Geus '14 continued her comeback, winning the 3000 Meter Steeplechase in 11:18.14. Her final 200 of 37 seconds shows she had a lot left as her pace had been between 90-95 seconds for all but the first 400.</p>
              <p align="left">Christina Wicker '17 had the only other victory for the Engineers. Her 4:43.70 in the 1500 was a personal best and her negative split finish shows a lot of promise.</p>
              <p align="left">Veronica Szklarzewski '15 had an excellent meet. First she took second in the Long Jump with a 17-4 3/4 jump, moving her up to number four on the all-time MIT list. She then took second in the 100, running a season best 12.62.</p>
              <p align="left">The women appear to be in pretty good position to defend their NEWMAC title next weekend and then make a run at defending their New England Division III title the following week. The men hope to continue the NEWMAC Conference win streak alive, having not lost since 2000. Although they lost the New England Division III title by one point last year, the Engineers hope to make it back to back championships, having won indoors a few weeks ago.</p>
