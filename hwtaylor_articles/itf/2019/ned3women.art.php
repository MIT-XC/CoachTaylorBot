            <p style="font-size: 12px"><span style="font-size: 16px"><strong>Williams Women Too Strong and Balanced for Engineers</strong></span>            </p>
            <h6 style="font-size: 12px">MIT Finishes Second in New England Division III Championship</h6>
            <p align="left">February 22-23, 2019</p>
            <p align="left"><a href="ned3women.res.php">The Women’s New England Division III championship</a> was hosted by Bowdoin College this past weekend. Going into the meet, Williams College was seeded to win by 25 with MIT seeded to place second.
</p>
            <p align="left">With the new format, in both the Men’s and Women’s D3 New England Meet, day one of competition saw for much more excitement with the Pole Vault, DMR, 5K, and 4x200 being added to the Pentathlon. Without any female athletes entered in the Pentathlon, the first event of the day for the Engineers was the Pole Vault. The Pole Vault has been a mainstay for MIT for quite a while, which currently has two All-Americans. However, heading into the meet, the Engineers were seeded to lose in the Pole Vault to Williams by a score of 13.5 to 12.5.  Jacqueline Ahrens '21, the returning D3 Indoor New England Champion, finished 3rd in the Pole Vault with a vault of 12-02. She appeared to finally figure out what is missing this season. Kari Stromhaug '19 took fifth at 11-8 while having some very close misses at 12-02. Finally, Anais Marenco ’22 tied for 6th with 11-02.25 and Lila Wine ’21 placed 8th at the same height. 
              </p>
            <p align="left">Next event up for MIT was the DMR. MIT put together a strong team made up of Margaret Trautner '20, Kristen Frombach '19, Bailey Tregoning '19, and Marissa McPhillips '20. Trautner led off with a very competitive 3:37 1200, followed by a 61.2 by Frombach in the 400. Tregoning helped extend the lead with a 2:13.9. Going into the mile leg McPhillips knew the anchor leg from Williams had the mile time to potentially catch her if it came down to the last 400m. Knowing that, McPhillips went out fast running a 70.9s first 400m, which is way too fast, however the hope was to scare off any other competitors. By doing so the Engineers lead in the DMR grew to about 60m with about 400m to go in the race. This strategy turned out to be a bit detrimental as McPhillips last 400m was 79.8s running 4:58.6 and was caught by the Williams mile leg with about 80m to go. The women’s DMR team finished 2nd to Williams in a time of 11:50.89 to 11:50.12, the number two and three times in the country, behind only MIT’s number one time from two weeks ago.
              </p>
            <p align="left">In the 5K, Kaitlyn Mooney '21 from Coast Guard took out the pace fast at 39 seconds for the first 200.  Katie Collins '21 went right with her with Jenna Melanson '20 just a few meters behind. Mooney continued to push the pace through the first mile, however Collins covered her every move for the first mile with a time of 5:25 and looked great. Melanson had started out at a pace of 82-83 and was feeling very relaxed through 1600 for a pace of 5:27. By the 2K mark, the lead pack started to string out as the pace continued. Collins looked strong in second place and Melanson was looking confident in fourth place. The 3200 was 10:48 for Collins and 11:00 for Melanson. At this point Melanson, was ready to go and was looking great so was given the green light to go,  leaving the other runner she was with. Collins continued to hammer away finishing with a PR of 16:53.81, which currently ranks her 6th nationally. While Melanson slowed slightly to a 5:35 third 1600 to finish in a personal best 17:15.9, moving into the top fifteen performances in the country for Division III. From a team standpoint both teams gained points from their projected totals.
              </p>
            <p align="left">The last MIT event running was the 4x200m. The legs were composed of Sarah Ishamuddin ’21, Elena Andree ’22, Yilinn Yang ’22 and Michelle Menkiti ’20. The group was not seeded to score, however, out of the slow section ended up running 1:48.28 to place 8th and score one point.
              
              After Day 1 of the meet, MIT had 36.5 points, trailing Williams by 11 points.
              </p>
            <p align="left">In a battle or championship, the winner is often the person or team that can strike first and build momentum. This is especially true for a team that is considered the underdog. MIT’s women’s team had an even harder hill to climb with not having entries in 4 of the events on day two. These events included 60mHH, 400m, 600m and Shot Put. However, everything was still in reach as the Ephs were only seeded to score 8 points in those 4 events combined.
              </p>
            <p align="left">The first 3 events on Day 2 of the championship for MIT were the Weight Throw, 60m Dash and  the Long Jump. 
              At the start of the 60, the excitement in the field house for MIT was extremely high. This 60m race was the first race for Menkiti since injuring her hamstring during the first week of January. She seemed tentative in the start of the race, never really seeming to get it going in the race, and ended up running 8.10. The Ephs officially struck first by having four athletes making the finals, which led to them scoring 16 points to MIT’s zero. 
              </p>
            <p align="left">Folusho Jebutu '20, not seeded to score in the Weight Throw, managed to get into finals and scored seventh with a PR of 48-11. This helped the Engineers make up 2 points that were lost in the 60m dash.
              </p>
            <p align="left">It seemed like Williams momentum rolled into the long jump as well. Williams was seeded to score 12.25 points in the LJ to MIT’s .25 points. The Engineers long jumpers seemed flat as, once again, MIT got shut out of the long jump while the Ephs continued to roll by scoring 13 points. After these 3 events, Williams lead grew to 69 to MIT’s 38.5.
              </p>
            <p align="left">After this it seemed as if the Engineers could never really recover, and the race was now for second place between MIT and Tufts.
              The Mile was the next big race for MIT on the track. Marissa McPhillips represented MIT as the number one seed.  However, after the DMR the previous night she knew it was going to be a difficult task with Anna Passannante ‘20 from Williams in the race. The pace was conservative for the first 800m with splits of 77 and 2:35 as the entire field was still bunched. Passannante, at about 1000m began to make her move and McPhillips followed, however, she began to fade with about 80m left in the race to finish sixth with a 5:03.23.
              </p>
            <p align="left">Tregoning, seeded second in the 800 wanted to improve her finish from the previous year, as well as help the team get back in contention. She positioned herself well throughout with a 65.9 first 400.  However, she seemed to not have the foot speed to cover the two moves by both Middlebury and Bates runners and faded during the last 150m, placing fourth in 2:15.44.
              </p>
            <p align="left">MIT was seeded for 19 points in the 1000 with Trautner seeded first, McPhillips fourth and Kira Buttrey ’22 fifth.  Trautner raced well at 2:55.85 to place second, while McPhillips ran 2:56.63 to finish fourth, obviously feeling the effects of the past two races.  Buttrey, who had been sick the past two weeks, had difficulty competing at the same level. The Engineers ended up scoring 13 points, six less than expected.
              </p>
            <p align="left">With a battle for 2nd place arising MIT was coming back with their 5K runners and a fresh Tessa Weiss ’20 and Gabrielle Enns ’21 in the 3K. Collins and Melanson, knowing their legs would be heavy for the first mile, eased into the race, sitting in a pack of 5 runners for a large portion of the race. Collins gradually moved up to fourth place in a personal best 9:49.66 which is currently eighth nationally.  Melanson also ran well to take fifth in 9:54.52.  This helped MIT pull away to go up 13 points over Tufts for second place.   However the 4x400 and 4x800 and Triple Jump still remained.
              </p>
            <p align="left">After the 4x400 with 2 events left the Engineers were in second 67.5 points to Tufts 59 points and with finals of Triple jump not yet decided, MIT decided to run the 4x800 to secure second place. Trautner, McPhillips, Katie Williams ’21 and Tregoning ran a great race from the start,  dominating the race,  running a meet record of 9:15.59.
              </p>
            <p align="left">The Triple Jump for Tech had the most potential to make a big swing in the competition. MIT was seeded to score nine points in the event and Williams was seeded to score zero. MIT had five athletes entered in this event, with all having a chance to score given the inconsistency in the event this year. Elizabeth Weeks '21 jumped a season best 36-09 1/2 to place fourth. Lucy Lee ‘21 jumped 36-05 3/4 in the finals to move up to sixth place while Elena Andree 35-11 ½ finished right behind her teammate in seventh place. MIT ended up scoring 10 points in this event.  However, Williams  Kene Odenigbo ’19 stepped up huge for the Ephs to continue with their dominance of the meet with a second place finish, scoring eight points.
              
              The final score was Williams 140, MIT 87.5 and Tufts with 67 points, finishing one, two, three among the 26 scoring teams.
            </p>
            <p align="left">As MIT prepares for the NCAA Division III Championship in two weeks, some of their athletes will travel to Tufts and the Final Qualifier to fine tune, and in some cases, qualify for Nationals.</p>
