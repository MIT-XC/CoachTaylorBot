            <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">MIT Track &amp; Field Teams Remain Undefeated</span>            </p>
            <p align="left">January 19, 2019</p>
            <p align="left">Some athletic teams look at the situation they are in when faced with injuries and illnesses and choose to focus on individual&nbsp;performances, particularly in a sport such as track &amp; field, which is often considered an individual sport anyway.&nbsp; MIT's decades of success defy that attitude and approach to the sport of track and field.&nbsp; If the &quot;next person up&quot; philosophy ever fit a team it is so for the Men's and Women's Track &amp; Field teams at MIT.</p>
            <p align="left">Faced with a most unusual amount and assortment of illnesses and injuries the Engineers traveled to Bowdoin College amidst the threat of winter storm Harper.&nbsp; With a relatively small roster competing, and a scoring system of 10-8-6-4-2-1 for each event in the <a href="bowdoin.res.php">six-team invitational</a>, it would be critical to get the majority of the top three places whenever possible.&nbsp; However, the competition was strong with teams from Tufts, Bowdoin, Brandeis,&nbsp;Southern Maine and Merrimack.</p>
            <p align="left">The Engineers did just that.&nbsp; Among the 18 events, the men garnered 10 first place finishes, four second places and seven third places, which totaled 174 points, comprising most of the 206 points earned by Tech.&nbsp; Tufts and Southern Maine who were both very close after eight events, failed to finish well, scoring 101 and 81 respectively.&nbsp; The women earned five first places finishes, six second places and three third places&nbsp;for 116 points, but added 10 fourth places for 156 of their 170 points.&nbsp; Bowdoin was second with 131 and Tufts close behind with 129.5.</p>
            <p align="left">While the men showed excellent balance, scoring in every event, there was major domination by the Engineers in the throws and distance events.&nbsp; Jon Fakkema '19 brought home a double victory, taking the Weight Throw with a toss of 53-7 and out-dueling teammate Will Woltmann '22 in their weekly Shot Put battle with a PR of 48-10 1/4.&nbsp; The throwers (Fakkema, Woltmann, Albert Menio '20, Ryan Nall '22 and Nathan Basinger '22) earned 43 of a possible 62 points.</p>
            <p align="left">The distance runners were led by Josh Rosenkranz '19.&nbsp; The senior&nbsp;set out in the 5K with a one man plan to run sub 14:45 on the flat 200 meter track.&nbsp; At the gun Rosenkranz totally dismissed his competition, led off with a 34 first lap and then strung together 18 laps of 35 seconds as he passed&nbsp;3200 meters in 9:20.&nbsp; He did not fade until 4200 meters where he ran three 36 second laps before closing in another 35 to lap the field in 14:38.70, a time that should get him into the NCAA Division III Championship in March.&nbsp; Backing up Rosenkranz's performance, teammates Steven Goldy '20 and Billy Woltz '20 earned second and third in 15:26.01 and 15:30.35 respectively.&nbsp; In the 3K, Matthew Kearney '22 patiently sat on the leaders through 2000 meters and then blasted a 2:50 final 1000 to easily win in 8:50.63.&nbsp; Nicky Medearis '22 placed third in 8:55.76 and Zach Johnson '22 returned to racing with a 9:00.39 as the all freshman group brought home 17 points.</p>
            <p align="left">Despite not having their top two high jumpers, Caleb Harris '22 led a trio of MIT jumpers to a 1-3-4 finish.&nbsp; Harris jumped 6-6 3/4 for the victory while Steven Speck '20 placed third at 6-2 3/4 and Sope Eweje '20 earned fourth, also at 6-2 3/4.</p>
            <p align="left">Yorai Shaoul '21 only took three of his six triple jump efforts and four of his six long jump attempts, but that is more than he needed.&nbsp; The two-time national runner-up won the triple in a leap of 48-4 3/4 and the long in a personal best 23-2 3/4&nbsp;for 20 total points.</p>
            <p align="left">Simon Alford '20 returned to racing after a  layoff from injury, but looked as though he never missed anything.&nbsp; The junior bided his time in a strategically paced 800 then closed well to win in 1:58.78.&nbsp;&nbsp;At the end of the meet, anchoring a 4x800 of Kent Slaney '21, Matt Schofield '22, and Ellery Rajagopal '21, Alford buried a 1:57.3 to win going away in 8:06.18.</p>
            <p align="left">Benton Wilson '21&nbsp;added a victory and a second place in the sprints.&nbsp; The sophomore, recently returned to good health, ran a personal best in the 60 Meter Dash to place second in 7.11 and then returned in the 200 to easily win in a season best 22.51.</p>
            <p align="left">The women showed strong balance throughout the events, but showed particular strength in the horizontal jumps and pole vault.&nbsp; In the Long Jump, Yilinn Yang '22 finished second with a best jump of 16-11 1/4.&nbsp; Tema Nwana '21 also jumped 16-11 1/4, but took third based on the second best jump.&nbsp; Elena Andree '22 added a fourth place at 16-8 3/4 and Sarah Ishamuddin '21 placed sixth with a jump of 16-5 1/4.&nbsp; Earlier in the meet, Ishamuddin, Andree&nbsp;and Yang comprised three legs of the 4x200 relay anchored by Bailey Tregoning '19, winning in 1:49.38.&nbsp; Ishamuddin also added a fifth place in the 200 in 27.39,&nbsp;winning her section uncontested. In the Triple Jump, Elizabeth Weeks '21 won a tie-breaker with teammate Lucy Lee '21 to place second and third respectively at 36-0 1/4.&nbsp; Andree added a fourth place with a jump of 35-11 1/4.&nbsp; The horizontal jumpers earned 37 points in the two events.</p>
            <p align="left">Kari Stromhaug&nbsp;'19 continued to show her dominance in the Pole Vault, winning with&nbsp;a jump of 11-11 3/4.&nbsp;&nbsp;Jacqueline Ahrens '21 earned second place at 10-11 3/4 and Anais Marenco '22 jumped a season best 10-11 3/4 for fourth.&nbsp; Lila Wine '21 added a sixth place to give the Engineers 23 points in the event.</p>
            <p align="left">In addition to successfully anchoring the winning 4x200 Relay, Tregoning added a victory in the 800, pulling away in the final 300 meters to win in 2:18.77, just in front of teammate Marissa McPhillips '20, who placed second in 2:19.18.&nbsp; McPhillips returned to anchor a winning 4x800 Relay comprised of Kira Buttrey '22, Katie Collins '21 and Skylar Brooks '20 in&nbsp;9:46.19.&nbsp; Tregoning anchored a fourth place 4x400 Relay (Kristen Frombach '19, Claire Melvin '22, Margaret Trautner '20) with a 60.6 anchor for a 4:09.90 overall performance.</p>
            <p align="left">The remaining individual victory was earned by Jenna Melanson '20 who led the 5K from start to finish.&nbsp; The junior went out a little too fast, starting off with a 38 initial 200, followed by a few 41-42 second 200's to hit the 1600 in 5:30, which was the planned overall pace, but not the planned path to get there.&nbsp; The initial&nbsp;pace and lack of competition made it difficult to hold onto the pace.&nbsp; Melanson slowed, coming through 3200 in 11:11 and the 4800 in 17:14.&nbsp; However running 17:59.51 was still a solid race to build off of as the two-time Steeplechase All American attempts to qualify in the 5K later in the season.</p>
            <p align="left">Next weekend MIT will split their team, sending some of the oval track athletes to Boston University for the John Thomas Terrier Classic, and the field event athletes along with the sprinters and hurdlers to Tufts University for the Branwen Smith-King Invitational.</p>
