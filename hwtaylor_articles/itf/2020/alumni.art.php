            <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">Alums Claim Seven Individual Victories in Annual Alumni/ae Meet</span>            </p>
            <p align="left">December 14, 2019</p>
            <p align="left">This weekend, as the MIT Track &amp; Field team members end a trying week of final projects and prepare for final exams, and the alums coming from around the country to watch and compete, take a break from their work schedules to honor the annual <a href="2019alumnires.pdf">Alumni/ae vs. Varsity meet</a><a href="../18-19/alumni.res.pdf"></a> that is now in its 46th year, there is one constant and that is the feeling of family.&nbsp; This meet continues to highlight the &quot;team first&quot; traditions that define the MIT Track &amp; Field program.</p>
            <p align="left">As is customary, MIT does not compete any of their cross country athletes who were competing during the championship season, which took out a good chunk of the 800-5K field.&nbsp; In addition, quite a few student-athletes were feeling the effects of the challenging academic week, enough so that more than a few were held out of competition to prevent further injury.</p>
            <p align="left">When it was all said and done, the Varsity men outlasted the &quot;Has-Beens&quot; 90-27, while the Varsity women were more challenged, winning 69-36.&nbsp; Leading the way for the Alums were&nbsp;Hannah Chen '18 and Jackie Vahey '17, both scoring eight points despite not practicing at all leading up to the competition.&nbsp; Chen won the Long Jump with a leap of 17-8, probably an indoor personal best, and placed second in the 60 Meter Dash, running 8.21.&nbsp; Vahey won the Weight Throw with a throw of 50-4 and then claimed second in the Shot Put with a 36-5 effort.</p>
            <p align="left">There were three additional winners for the Alumnae; Cimmy Virdi '16 in the Pole Vault, clearing 12-7 1/2, Isabella Stuopis '16, winning the Shot Put in 37-7 3/4, and Louise van&nbsp;den Heuvel '14 laying claim to the 800 title in 2:20.84.&nbsp; The Alumni had two victors; Scott Cameron '19 taking the Pole Vault in 13-11&nbsp;1/4 and Cody Bratten '19 winning the Shot Put with&nbsp;a throw of 44-3 1/2 on his final throw.</p>
            <p align="left">It is well known that the alumni are extremely loyal and supportive of the varsity program, donating hundreds of thousands of dollars to allow the program to grow and compete at the highest level of the NCAA.&nbsp; One of the main background stories going into the meet&nbsp;was a challenge presented by Arinze Okeke '16, who declared that if he were to get beat in the High Jump by Steven Speck '20, Alec Reduker '20 in the Triple Jump and Steven Marquez '23 in the Long Jump, he would double his donation this year.&nbsp; In addition, Okeke was trying to score in the 35 lb. Weight Throw and the Shot Put--a tall task for someone who graduated three and half years ago and was only a horizontal jumper when he attended MIT, although he always wanted to do more.&nbsp; The High Jump was first on the agenda.&nbsp; Okeke cleared 5-7 1/4, 5-9 1/4 and 5-11 1/4 with no problem.&nbsp; Speck was ahead on misses and was hurting too much to continue after 5-11 1/4.&nbsp; Okeke cleared a personal best 6-1 1/4 on his third attempt to nullify the challenge in the first event.&nbsp; For good measure, he came back to beat Marquez (6-5 1/4 in the High Jump) in the Long Jump when he leaped 21-7 1/2 and took down Reduker in the Triple Jump by going 46-0 1/2.&nbsp; For good measure, he threw the Weight 43-8 and the Shot Put 36-11.&nbsp; Although he only scored four points for his team in the 5-3-1 scoring format, his spirit was indefatigable.</p>
            <p align="left">Other point scorers for the alums were Tyler Singer-Clark '14, third in both the 60 (7.55) and 200 (24.28), Danny Newman '17, second in the 200 (23.66), Aidan Gilson '19, second in the Mile (4:29.84), David Walter '18 and Josh Rosenkranz '19 laying claim to second and third respectively in the 5K (15:45.49 and 15:48.56), and Jon Fakkema '19, third in the Weight Throw (51-4 1/4).&nbsp;</p>
            <p align="left">For the Alumnae, Preethi Vaidyanathan '15, was third in the High Jump (5-1 1/4), and Kari Stromhaug '19,&nbsp; was second in the Pole Vault (12-1 3/4).</p>
            <p align="left">The only double winner for the Varsity was two-time national champion Yorai Shaoul '21.&nbsp; Shaoul, still not on a full approach, jumped 22-5 3/4 in the Long Jump and then jumped an impressive 48-4 3/4 to take the Triple Jump.&nbsp; An impressive victory was turned in by Zach Johnson '22.&nbsp; Johnson led all but 600 meters of the 5K, clicking off 1600 splits of 4:50, 4:57, 5:03 en route to a 15:26.84 victory.&nbsp; Kenneth Wei '23 set the freshman record of 8.55 in an uncontested 60HH victory.</p>
            <p align="left">Margaret Redfield '21 had the most impressive Varsity victory for the women, claiming the High Jump with a 5-5 1/4 performance.&nbsp; This moves Redfield into a tie for second place on the MIT all-time list.&nbsp; Laena Tieng '23 won the Triple Jump with a mark of 35-9 1/4 to move her to ninth on the MIT all-time list.&nbsp; Also moving to ninth on the all-time list was Kimmy McPherson '23 who won the 60HH in 9.72.&nbsp; Sarah Ishamuddin '21 moved up to seventh on the 60 Meter Dash list with her 8.15 victory.</p>
            <p align="left">Other participating Alums were Sope Eweje '19 (200), Alex Knapp '19 (Mile, 5K), Nicholas Waltman '18 (Mile), Colin Godwin '17 (Mile, 5K), Jordan Mizerak '14 (High Jump, Mile), Rory Beyer '17 (5K), Matt Falk '12 (High Jump, Shot Put), James Kirtley '94 (High Jump), Nate Sharpe '09 (Pole Vault), Marshall Wentworth '16 (Pole Vault), Javit Drake '94 (Open Mile), Kristen Frombach '19 (400), Alexandra Taylor '14 (400), Jackie Brew '14 (4x200), Jen Tylock '17 (Pole Vault), Emily Cole '09 (Pole Vault), Stephanie Guo '17 (High Jump, Shot Put), Natalie Alper '17 (High Jump), and Julia Sharpe '09 (High Jump).</p>
            <p align="left">Those in attendance cheering on their fellow &quot;Has-Beens&quot; were Don Shobrys '75, Eric Khatchadourian '06, Patrick Barragan '08, Nick Leonard '11, Josh Duncavage '13, Roy Wedge '14, Angel Echevarria '16, Elaine McVay '15, Veronica Szklarzewski '15, Ariela Slutsky '18, Dana Balek '18, Bailee Margolis '19 and Camilla Thanos '19.</p>
            <p align="left">MIT will be off the next three weeks as they have a week of exams and then two weeks at home for the Holidays.&nbsp; The next meet will be a tri-meet vs. Bates College and Colby College&nbsp;on January 11&nbsp;at Bates.</p>
