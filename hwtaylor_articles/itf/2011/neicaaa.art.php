              <p style="font-size: 12px"><strong><span style="">FISHER AND DMR GAIN VICTORIES</span></strong></p>
              <p style="font-size: 12px; font-weight: bold">Team Records, NCAA Qualifying Marks, Personal Bests Lead the Way for MIT</p>
              <p align="left">February 25-26, 2011 </p>
              <p align="left">Not being concerned about points in the <a href="neicaaa.res.php">New England Intercollegiate Amateur Athletic Association Championship (NEICAAA)</a>, but instead focusing on only competing those capable of reaching or improving on the NCAA qualifying rounds, MIT had a marvelous weekend of competition at the Boston University Track and Tennis Center.</p>
              <p align="left">Hazel Briner '11, started things off for the Engineers, competing in the Pentathlon. Needing a score near 3300 to ensure her trip to nationals, Briner only needed solid performances, not personal bests. In the 60 meter hurdles, she ran 9.45, a couple tenths slower than her best, but still a solid start. In the high jump, her approach was deep and her arch non-existent, but she managed to clear 4' 9&quot;. The shot put went reasonably well as she tossed the implement 33-2. In the long jump, she was over a foot short of her best at 16-2 1/4, which certainly put the pressure on her. She was not feeling well, but knew she had to deliver a very good 800 to get a respectable score. Pushing too hard to get to the front and running the first 100 in 15 seconds, took a lot out of her overall potential time. Nevertheless, she competed well to win the 800 in 2:20.70. Her overall score of 3275 should be enough to get her to Nationals.</p>
              <p align="left">In the pole vault, Karin Fisher '11, was out to defend her title, and teammate Lauren Kuntz '13, whose personal best was 11-11 3/4, was hoping to add a few centimeters to her PR in order to improve her chance of making Nationals. In 2010, Fisher won the event in 12-4, while this year, she cleared 13' to win the meet and then proceeded to also clear 13-3 3/4 to reclaim the top spot in the country for Division III. Kuntz placed fourth, but was only able to clear 11-6. In the men's event, Nathan Peterson '12, continued to improve. Peterson cleared 15-7, and nearly clearing 16-1, as he placed third in the talented field.</p>
              <p align="left">Back on the track, the women's Distance Medley Relay was about to put their best foot forward in attempting to make the NCAA Championship. Knowing it would take at least a sub 12:00 effort, the women seemed ready for the task. Leading off and running the 1200 leg was Louise van den Heuvel '14. She got out well, towards the back of the pack, running 34 for the first 200. While a little fast, the pace settled and van den Heuvel moved up in the pack, coming through the 400 in 72 and the 800 in 2:24. With the pace slowing a bit, van den Heuvel made a move to the front, passing the 1000 mark in 3:01 and then bringing it home in an excellent 3:37.8. Martha Gross '12, ran the 400 leg, and uncharacteristically got out fast (26.8) and finished in 58.0. Jamie Simmons '12, took the trip up to the 800 and delivered marvelously. She started out with a fast 32.0 first 200 then raced very well, running 34.0 in each of the next three 200's to finish in 2:14.0, putting MIT in a solid second place. Anna Holt-Gosselin '11, anchored, running the 1600 leg. Holt-Gosselin raced to win, taking the lead in the first lap and never letting it go, running 4:59.9 for the metric mile. The overall time of 11:49.80 is the fastest time in the country this year for Division III and a new MIT record.</p>
              <p align="left">In the Weight Throw, both Ken Cooper '13, and CK Umachi '12, were hoping to improve their NCAA marks. Cooper has picked up a couple of bad habits that have prevented him from finishing his throws as he should. Continuing in this mode, he placed eighth with a throw of 55-8. Umachi however, just needed to get into a position to finish well and he did, throwing 61-0 on his fifth throw to place second overall and move into ninth place on the NCAA list, two inches behind teammate Cooper.</p>
              <p align="left">Saturday had Portia Jones '12, competing in the semi-finals of the 60HH and the finals of the 200. Her qualifying time in the 60HH trials was 8.95. In the semis, she had a delayed trail leg on hurdle one and a resulting deceleration from getting pulled back by her lead arm, but fixed things to make finals in a personal best of 8.85. In the finals, Jones continued her first hurdle errors and also messed up on hurdle two. She ran well after that, but was only able to claim seventh place in 8.99. In the 200 trials she set a new MIT record of 24.96, but cruised in the final five meters and that was enough to keep her out of the fast section in the final. Running out of the middle of three sections, Jones ran an excellent race, pushing the entire 200 to run 24.40 to win her section. In the fast section, two of the four runners ran faster than Jones, giving her third place overall and yet another MIT record.</p>
              <p align="left">In the mile run for the men, both Kyle Hannon '13, and Logan Trimble '13, were hoping to qualify for nationals. After his 4:05 1500 two weeks ago, Hannon was indeed ready to run fast. However, his foot had some shooting pains in the sole, causing him to drop out at the halfway point. Trimble, just starting to recover from being ill all week, was still too weak to race, starting to dry-heave at the 1100 mark and also dropping out.</p>
              <p align="left">In the shot put, Mitch Kelley '11, realized he was facing his final indoor track meet and rallied to throw a personal best of 49-00 1/4 to make finals, although he was unable to score.</p>
              <p align="left">Back on the track, Justin Bullock was running the 1000 finals. Bullock ran a flawless race, running splits of 29, 58, 1:28 and 1:58 en route to a final time of 2:28.29. Moving up through the pack, picking his way at  perfect times, Bullocks fourth place finish was also a new MIT freshman record.</p>
              <p align="left">Next week, MIT will travel to New York City to race in the ECAC Division III Championships at the Armory. Again, MIT will only be trying to improve their position at Nationals, not score points at the ECAC Championship, so only a portion of those who have actually qualified will be making the trip. The rest of the team is preparing for the outdoor season.</p>
