              <p style="font-size: 12px"><strong><span style="">MIT IMPROVES NCAA QUALIFYING MARKS AT ECAC DIVISION III CHAMPIONSHIP</span></strong><span style="font-weight: bold"></span></p>
              <p align="left">March 4-5, 2011 </p>
              <p align="left">With a number of NCAA provisionally qualified athletes on the bubble for making Nationals, MIT joined most of the other colleges in the North East to see if those marks could be improved at the ECAC Division III Indoor Track and Field Championship, hosted by New York University at the Armory in New York City.</p>
              <p align="left">MIT opened up with the women's pole vault where Karin Fisher '11, Hazel Briner '11 and Lauren Kuntz '13, were all trying to improve their personal best. Kuntz and Briner were able to place seventh and eighth respectively even though neither set PR's, both jumping 11-6 1/2. Fisher, vaulted exceptionally well, setting yet another personal best and school record while winning the event at 13-6 1/4, tied with the second place finisher, Abigail Schaffer of Moravian for the highest vault in the country for Division III.</p>
              <p align="left">On the track, Anna Holt-Gosselin '11, was racing in the slow section of the 5000. Not ready to race fast early in the season, Holt-Gosselin entered the meet with a meager 18:15.22 although her best ever was from the last time she was totally healthy, a 17:34 from two years ago. Racing in the slow section, and needing a sub 17:20 to have any chance at all of making the NCAA field next week, it was clear that Holt-Gosselin would have to do it by herself and hope to start lapping runners before the 3200 mark. Knowing she would need 83 second quarters to meet her goal, she was a little excited and ran 80 for the first 400 and then settled into a series of 82+ quarters to hit 1600 splits of 5:28 and 11:02. At the halfway point she was able to start lapping runners, giving her further motivation to continue on pace. With 800 to go she had already lapped the field and proceeded to go after lapping runners again. Her third 1600 was 5:31 and she closed the final 200 in 36 seconds to run 17:09.93. Although that mark put her fourth on the NCAA performance list, four in the fast section ran faster, which moved Holt-Gosselin to eighth on the NCAA list and fifth in placing for the ECAC 5K.</p>
              <p align="left">Nate Peterson '12, was entering the pole vault needing at least a 15-11 vault to have a chance at making Nationals next weekend. After steadily improving the past three weeks it seemed Peterson was ready. During the event, he was clean through 15-11. Knowing he was probably safe in making Nationals did not hold him back as he also cleared 16-5 1/4 and had a good attempt at the next height. Peterson's performance netted him third place in the event.</p>
              <p align="left">On day two, MIT started with the finals of the women's 55HH. The Engineers were represented by Portia Jones '12 and Amy Magnuson 'G, both of whom had qualified from the trials on Friday with times of 8.42 and 8.46 respectively. In the finals, both got out well out of the blocks. Jones was efficient on the hurdles and in between as she earned the victory in a personal best of 8.21. Magnuson, hampered by possible micro tears in her hamstring, placed fourth in 8.47.</p>
              <p align="left">The next event was the Mile Run. Louise van den Heuvel '14, was going to try to build on her 3:37 1200 from the weekend before, but a severe cold got the best of her, preventing her from competing. In the men's race, Kyle Hannon '13, continued to be bothered by an injury to his foot, which tends to flare up on race days. After a nice start he failed to respond to the perfect pace, taking himself out of the race mentally and finishing in a disappointing 4:35. Both will be in the DMR next week at Nationals and will hope for much better results.</p>
              <p align="left">In the 400, Jamie Simmons '12, and Martha Gross '12 took to the oval for Tech. Gross ran first, seeming to decide to let others set the pace, giving her a slow split of 28.0 at the 200, and in last place in her section. Managing to pass a couple of runners in the final 100 enabled her to run 59.71, a season best. Simmons got out a little better, but still allowed others to get the break, forcing her to run from behind. Her final time of 58.48 earned her sixth place.  Jake Jurewicz '14, represented the men's team. The tall, long legged Jurewicz had a little difficulty running full speed even with  the banked track. Unable to get even close to the break with his first lap of 23.6, Jurewicz had to work from behind and was only able to produce a 51.34.</p>
              <p align="left">In the women's Long Jump, Jackie Brew was hoping to get into the 18' range, but continued to have problems raising her hips on the board and over rotating. She managed to make the finals and place eighth at 17-2 3/4.</p>
              <p align="left">The men's Weight Throw was a disaster. Ken Cooper '13, and CK Umachi '12, ranked 11th and 12th in Division III with throws of 61-2 and 61-0, were hoping to compete well for the victory and in the process make it more of a guarantee that they make the National field. Both had control problems and were incapable of hitting their position. Neither made the finals. Cooper threw 54-6 and Umachi 53-9.</p>
              <p align="left">The women's 4x400 Relay was the final event for MIT and one they were not only hoping to win, but to run at least 3:51. Jamie Simmons ran an excellent first leg, but as she was coming in for the handoff, the officials moved MIT into lane one even though Wheaton College had pulled ahead. It was obvious Wheaton would hand off first so Simmons backed off in order to get a clean handoff. Hazel Briner got nervous and never turned to sprint, instead using an open handoff, moving with her back to the direction she was going. Just as she was about to receive the baton she tripped on the railing and missed the baton. She had to stop and go back and get it, losing significant time, perhaps two to three seconds. Briner proceeded to make up some ground, getting the team back into second place with her 58.4 split. Martha Gross took the baton and again got out too casually, running 28.1 for the first 200. She lost more ground to leading Wheaton as she split 59.1. Portia Jones received the baton about 25-30 meters in arrears. She proceeded to run 25.8 to bring herself within five meters of Wheaton's anchor and continued to move past her with 100 meters to go. Jones settled just a little as she went past, giving the Wheaton runner hope. The final 30 meters were dead even until Jones out leaned her opponent at the line, good for a 56.1 split and an overall time of 3:53.25.</p>
              <p align="left">Next week, the Engineers leave on Wednesday for Capital University, just outside of Columbus, Ohio. The women are hoping to make it three NCAA Championships in a row reaching the podium, an honor given the top four teams. Both last year's outdoor team and this year's cross country team finished third at Nationals. For the men, it will be the first visit for all but Kyle Hannon, so bettering their entering seed will be a worthy goal.</p>
