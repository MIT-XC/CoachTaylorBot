              <p style="font-size: 12px"><span style="font-weight: bold">MEN AND WOMEN OPEN SEASON WITH WIN OVER ALUMNI/AE TEAMS</span></p>
              <p align="left">January 8, 2011</p>
              <p align="left">The 2011 version of the MIT Indoor Track and Field team opened competition with the annual <a href="alumni.res.php">Alumni/ae Meet</a>. Typically, the most competitive regular season meet of the year for the men, this year's version did not disappoint as the meet came down to the final three events before the varsity pulled away to win 67-55. The history of the women's program is still pretty recent by comparison and the improvement of the varsity so dramatic over the past five years that the alumnae are still not showing up in high numbers. The varsity pulled away early, winning 104.5 to 16.5.</p>
              <p align="left">The &quot;Has-Beens&quot; on the men's side knew they had a chance of winning all six field events and the sprints and hurdles. The concern was whether or not there would be enough scoring bodies in the 400-5000 events to give the old guys the points to pull off the victory. The pair of fairly recent national champions, Uzoma Orji '06, and Stephen Morton '10, did their jobs, winning the weight throw, shot put, long jump and triple jump. This gave the Alumni a nice lead as the scoring awarded the first three places with 5-3-1 points respectively. In addition to the first places, the alums also earned second place in the long jump by Thad Wozniak '06, and third place in the triple jump by Obinna Anyanwu '09. The only surprise in the field events came when Matt Falk '12, took the victory in the high jump. This made it the first time Craig Mielcarz '03, lost the event as a varsity athlete or an alumnus. Mielcarz settled for second place, but promised to be back to regain the title in '12. In the pole vault, the &quot;Has Beens&quot; made up for the high jump by claiming the top five places. Nate Ball '05, and Greg Tao '10, gave up on a jump off, tying at 14-07 1/4. Omari Stephens '08, cleared the same height, but placed third.</p>
              <p align="left">In the running events, things went as expected in the short events. Zach Traina '05, returned to his double victory days, claiming first place in both the 55 and 200 meter dashes. Traina received some help from Morton when he placed third in the 55 meter dash. Morton discovered what it is like to compete without practicing for a few months as he failed to score in the 200. In the 55HH, Stephens showed he can still do more than vault as he stole the show with his victory. With eight victories under their belt, the alumni had 52 points and only needed 62 to guarantee victory. Unfortunately there just was not enough strength in the lap events as only Dave Reshef '09, placed for the Alumni in the remaining events. Reshef earned a second place in the 400.</p>
              <p align="left">The varsity claimed the remainder of the points. DJ Ronde '13, won the 400 in an excellent come from behind victory. Pat Mark '13, took the 800 out, leading the event wire to wire. In the mile, Stephen Serene '12, took the first 200 in a ridiculous 29, gave up the lead in the middle of the race before storming back to claim the victory. In the 5000, the final event before the 4x200 relay, the Alumni needed a second place in order for the meet to come down to the relay. Try as he might, Eric Khatchadourian '06, was unable to hold onto the leaders, giving the Varsity another sweep with Dan Harper '12, taking the victory.</p>
              <p align="left">The Alums were going down in a blaze of glory as hamstrings, backs and various body parts began to give out. Once it was clear they would not be able to win the meet, the substitutions were entered in 4x200. The varsity made a couple of substitutions as well, but the end results was still a victory as Josh Duncavage '13, Mac Gager '14, Babajide Akinronbi '14 and Tyler Singer-Clarke '14 easily took the victory.</p>
              <p align="left">For the women, the Alumnae were relatively few in number. As with the men, the majority of the Alumnae entries were in the pole vault. However, most of the points came from the weight throw where Julia Zimmerman '09, and Juls Andren '10, took advantage of illness and injury on the part of the Varsity, claiming first and second respectively. Meri Silberstein '95, always ready to compete earned a third pace in the long jump. Zimmerman also scored a third place in the triple jump. Also in the field events, Emily Hwang '09, claimed third in the her specialty, the pole vault and picked up a nice second in the shot put. Andren also scored in the shot, taking third place. Cat Tweedie '04, tied for third in the high jump to earn .5 points for the Alumnae.</p>
              <p align="left">The only point scored by the Alumnae in the running events was a third place in the 800 by Silberstein. The alums had no entries in the 55HH, 400 and 5000. With strong graduating classes in the next two years, and the possibility of recent grads who are competing for clubs being allowed by their coaches to compete in this meet, the Alumnae could be in position to take a victory.</p>
              <p align="left">Claiming victories for the Varsity were; Amy Magnuson '10, in both the long jump and triple jump, Brooke Hamilton '14, in the shot put, Karin Fisher '11, in the pole vault, Kristine Bunker '14, in the high jump, Louise van den Heuvel '14, in the mile, Portia Jones '12, in the 55, 55HH and 200, Jamie Simmons '12, in the 400, Olivia Papa '14, in the 800, Tania Morimoto '12, in the 5000 and Simmons, Megan Bumgarner '14, Bunker and Stephanie Ku '14, in the 4x200.</p>
              <p align="left">Competing, but not scoring for the Alumni were; Akin Aina '95 (55, 200), Patrick Barragan '08 (PV), Chris Bateman '07 (4x200), David Blau '06 (55), Josh Chartier '06 (PV), Eric Khatchadourian '06 (5000), Terry McNatt '87 (5000), Peter Mulligan '08 (800), Ulzie Rea '07 (400, 4x200), Joe Roy Mayhew '08, (800), Kevin Scannell '91 (LJ), Justin Schmidt '10 (PV), Nate Sharpe '09 (PV), Sam Sidiqi '99 (200), Andy Ugarov '95 (HJ) and Chris Wong '08 (5000). In attendance were; Dave Afshartous '89, James Flink '64, Ben Schmeckpeper '05 and Don Shobrys '75.</p>
              <p align="left">Competing, but not scoring for the Alumnae were; Adrienne Bolger '09 (mile), Adeline Kuo '02 (55, 200), Margaret Leibovic '10 (55, 200) and Alisha Schor '07 (mile). In attendance were; Chi-An Emhoff '01, Elizabeth Labuz '09 and Jacqui Wentz '10.</p>
              <p align="left">MIT travels to Lewiston, Maine on the 15th to compete in a tri-meet against host Bates College and visiting Colby College.</p>
