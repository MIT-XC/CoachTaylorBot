              <p style="font-size: 12px"><span style="font-weight: bold">Lee and 4x400 Improve NCAA Marks at IC4A Championship</span></p>
              <p align="left">March 8, 2014</p>
              <p align="left">Four MIT athletes made their way to Boston University's banked track to participate in the <a href="ic4a.res.php">IC4A Championship</a>, the oldest men's track and field conference in the country, mostly made up of Division I colleges. States Lee '16, Michael Kaba '16, Derek Barnes '16 and Tyler Singer-Clark were there to improve their 4x400 Relay time in hopes of making it into one of the two top sections of the 4x400 at Nationals next Saturday. Going into the meet, they were ranked sixth. The NCAA takes 12 relay teams, having a three section final with four teams per section. Only eight teams make All American and running out of the third section may make attaining that goal a bit difficult. While there, Lee would try to improve his chances in the open 400 and Singer-Clark would give the 200 a go.</p>
              <p align="left">In the 400, Lee got out well in 22.9, but did not get the break. However, the pace was fast enough for him to keep his momentum. He finished well, posting a PR and new MIT record of 48.36. This moved him up to 12th in the country. His previous time of 48.45 would not have made it in.</p>
              <p align="left">Singer-Clark was not so fortunate. Once again he drew the worst lane, lane three since they were only using 3, 4, 5 and 6. He was unable to improve on his 22.10, this time running 22.21.</p>
              <p align="left">The 4x400 team had the misfortune to be in a heat where two of the five teams scratched. It would be necessary to stay competitive with the lead team or MIT could find themselves in limbo. Lee led off as usual. He did not get the break but was in a great position. He waited too long to pass on the second backstretch, cut his speed slightly and almost took the lead at the handoff, running 48.4. Kaba got out well but slowed slightly as he turned to get the handoff, causing a bigger gap to develop. Kaba did a nice job of staying with the Cornell runner, closing the gap to five meters as they approached the handoff. Once again, the handoff caused the gap to grow as Barnes also slowed when he turned, losing significant ground to Cornell. Barnes brought the baton around in 49.4 and Singer-Clark made a clean handoff. However, he was in no-man's land, 20 meters behind the leader and over 20 meters ahead of the third place team. Nevertheless, Singer-Clark ran a 48.5 to give MIT a cumulative time of 3:14.98. While this time moved MIT up in the rankings two spots, another team passed them to put them at fifth on the National list.</p>
              <p align="left">The Engineers will be traveling to Nebraska for the NCAA Division III Championship, competing on Friday and Saturday.</p>
