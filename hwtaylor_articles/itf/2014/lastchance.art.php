              <p style="font-size: 12px"><span style="font-weight: bold">Brew and Johnson Lead Way for Engineers at Last Chance Qualifier</span></p>
              <p align="left">March 6, 2014</p>
              <p align="left">On the final weekend of qualifying for the NCAA Division III Championship, MIT was doing what everyone else was doing, chasing around for qualifying performances. Fortunately, the best track and field venues are in Boston, so travel was not part of the logistical problems for MIT. The first stop on the weekend began at Tufts University, host of the <a href="tuftslastchance.res.php">Last Chance Qualifier</a>.</p>
              <p align="left">Jackie Brew '14 continued her amazing improvement, knocking off .05 from her season hurdle best. She won the 60HH in 6.88, equaling her all-time PR from two years ago, prior to her foot problems. This time puts her number 10 on the NCAA list.</p>
              <p align="left">In the women's Triple Jump, Michelle Johnson '15 also returned to her best days from two years ago, breaking her own MIT record with a 37-0 1/2 jump to claim the victory. Stephanie Birkhimer '14 also jumped a personal best, hitting the sand at 36-04. Johnson is ranked 21st on the NCAA list with one meet remaining to try and move up on the list where only the top 17 entries for the women will get in.</p>
              <p align="left">In the Long Jump, Veronica Szklarzewski '15 jumped 17-3 1/4. Preethi Vaidyanathan '15 cleared 5-01 in the High Jump. In the Weight Throw, Ellen Liverpool '15 threw 48-03.</p>
              <p align="left">For the men, Benji Xie '15 was attempting to better his 5K time in hopes of improving his chances for making the national field. The initial pace was perfect and Xie looked very comfortable near the front. The 1600 time was 4:41 and at the 3200 mark it was 9:19 for a 4:38 second 1600. At this point Xie dropped the field as they were no longer interested in maintaining the rigorous pace. Xie continued to pound out the quarters, running 70 and 71 through 4000 meters. In the final 1000 the wheels came off the bus. At this point Xie had about a 30 meter lead. His next 400 was a 73 and that allowed the gap to close significantly as those who could no longer hold the pace earlier began to pick it up now that they were close enough to the end to not fear dying. His next 400 was a 76, which caused him to lose the lead. His last 200 was a painful 38 to finish in 14:47.96. Xie ran an excellent, gutsy race, laying it on the line.</p>
              <p align="left">The final event for MIT was the men's DMR. MIT had run 9:57 earlier in the year, but on a banked track. On a flat track the times would get a nine second bump due to the indexing of banked tracks. If MIT could break 10:00 they should have a great shot of making Nationals as only 12 relay teams are accepted into the field. Sam Parker '15, already in the 800 field with his 1:51.56 corrected performance, was leading off in the 1200 leg. The pace went out okay with 29.8 at the 200 mark, but Parker did his trademark move of getting on the rail. The pace slowed, he could not get out, and suffered through two laps, hitting the 600 in 1:32.1. The slow pace was too slow for Parker, making his legs feel heavy, which messed him up even further. By the time he handed off he had run a dismal 3:07.2. Mac Gager '14 ran an excellent 400, but even his 50.3 barely made a dent in the deficit. Harrison Hunter '15 ran the 800 leg and definitely brought his A game. However, he was not patient, trying to make up too much, too soon. his 26.0 and 54.3 was just too fast on the flat track. He faded a bit at the end, but was certainly competitive with his 1:55.8. Chadd Kiggins '15 had to do his best to make up ground. It was going to take a 4:07 at this point to give MIT a chance and Kiggins knew it. Again, MIT was so far behind it was difficult to judge pace, which Kiggins failed at. His 59.9 opening 400 was fun to watch, but it was not going to end well. He stayed competitive with a 2:03 and 3:07, but his final 400 was a 70, ending the dream of making the Nationals in the DMR for a fourth consecutive year.</p>
              <p align="left">Tomorrow many of MIT's athletes will be at the Reggie Lewis Center in Boston for the ECAC Division III Championship.</p>
