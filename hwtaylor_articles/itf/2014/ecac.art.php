              <p style="font-size: 12px"><span style="font-weight: bold">Johnson Leads Team for Second Day in Succession</span></p>
              <p align="left">March 7, 2014</p>
              <p align="left">Day one of the <a href="ecac.res.php">ECAC Division III Championship</a> brought mixed results for the MIT Engineers. On the bright side, Michelle Johnson '15 showed what bringing the right attitude can do as she again set the MIT record, for the second time in two days, jumping 37-6 to move up to 14th on the NCAA list, making it a wait and see if she stays in the top 17 that get selected to compete at Nationals next week. </p>
              <p align="left">The other highlight has Jackie Brew '14 advancing to the finals of the 60HH, running 9.06 in the trials. The finals did not go so well as she finished eighth in 9.38</p>
              <p align="left">Few others seemed to bring that same attitude. In the Men's Shot Put, Ken Cooper '15 fouled out, as did Angel Echevarria '16 in the Men's Triple Jump. Ben Schreck '15 no-heighted in the Pole Vault, but at least he had the scary experience of his pole breaking on him on his first attempt to partially blame it on. </p>
              <p align="left">While no one else threw down a personal best, they did demonstrate a readiness to compete at a high level. Isabella Stuopis '16 earned second place in the Shot Put, throwing 43-9 1/4. Stephanie Birkhimer '14 continued her string of strong performances, jumping 35-10 1/4, just missing finals. Carrington Motley '16 was unable to bring the right stuff in his final opportunity to improve his 15th ranked position. His jump of 45-7 did not even make finals.</p>
              <p align="left">The Pole Vault went reasonably well for the other vaulters. Cimmy Virdi '16 lost the vault in a jump-off after clearing 12-4 3/4 and then missing 12-9 three times despite being over the bar every time. Joanna Chen '15 cleared 11-1 as did Abby Klein '14. Jennifer Tylock '17 and Sherry Wan '16 both cleared 10-7 1/4. On the men's side, Jordan Mizerak '14 cleared 14-8 and looked very good over 15-2 although he did not clear that height.</p>
              <p align="left">Stephanie Guo '17 and Adrian Samsel '16 were giving their respective multi-event specialty one more try. Both competed relatively well, but neither well enough to improve their previous score. Guo scored 3075 points to place sixth while Samsel scored 4651 points to earn third place.</p>
              <p align="left">Day two had mixed results for MIT. On the positive side, Louise van den Heuvel '14 tuned up for Nationals with another win, leading from start to finish in 4:58.71. Ellen Liverpool '15 did not place but threw very well in the Weight Throw, tossing the 20 lb. weight 50-0 1/2. Preethi Vaidyanathan '15 jumped very well, clearing 5-4 1/2, tying the MIT varsity record. She also had strong attempts at 5-6 1/2. Harrison Hunter '15 placed sixth in the 800, winning his section in 1:55.11. Rick Paez '15 placed eighth in the 1000 with a 2:32.27.</p>
              <p align="left">The negative side of the ledger was unfortunately larger than it should be in the championship season. Christina Wicker '17 faded badly after the 800 in 2:28, finishing in 5:15.65. Cindy Huang did not fair much better, although it was not her fault, falling after the 600 mark, she struggled in at 2:21.62.</p>
              <p align="left">For the men, Ken Cooper fouled out in his second event, the Weight Throw. Mac Gager '14 and Nick Diamantoni '15 did not get the job done in the 400, running 51.17 and 51.40 respectively.</p>
              <p align="left">For most of those competing it is the end of the season. With results around the country still coming in on this final day to qualify, it looks as though MIT has the following nationals qualifiers: </p>
              <p align="left">Women</p>
              <p align="left">Cimmy Virdi - Pole Vault, Isabella Stuopis - Shot Put, Jackie Konopka - Pentathlon, Jackie Brew - 60HH, Louise van den Heuvel - 800, Mile, 3K, Maryann Gong - Mile, 3K, DMR (Christina Wicker, Stephanie Guo, Cindy Huang, Maryann Gong), Michelle Johnson - Triple Jump</p>
              <p align="left">Men</p>
              <p align="left">Sam Parker - 800, Ken Cooper - Weight Throw, Shot Put, States Lee - 400, Ben Schreck - Pole Vault, 4x400 Relay (Lee, Michael Kaba, Derek Barnes, Tyler Singer-Clark)</p>
