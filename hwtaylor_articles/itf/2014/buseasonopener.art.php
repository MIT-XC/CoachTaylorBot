              <p style="font-size: 12px"><span style="font-weight: bold">Gong Sets Varsity and Freshman Record in 3K to Lead Outstanding Distance Performances</span></p>
              <p align="left">December 7, 2013</p>
              <p align="left">MIT sent three women and one male to the Boston University Season Opener. All four were cross country runners who were ready to get a nice 3K time in, hoping to possibly qualify for the NCAA Division III Championship, but certainly qualifying for all other championships.</p>
              <p align="left">The women were first on the schedule. The trio of Maryann Gong '17, Louise van den Heuvel '14 and Brooke Johnson 'G were planning on running 78-79 second quarters to finish somewhere between 9:45 and and 9:52.</p>
              <p align="left">The section was pretty much all Boston College and MIT runners and all eight went with the pace set by van den Heuvel. At the 1600 mark the pack hit 5:15 with six runners still in the main pack, three from Boston College and the trio from MIT. One of the BC runners held onto the pace while everyone else faded to 80 second 400's for the next 800 meters. Gong made a move to go after the leader, briefly moving into second place, but fell back to third despite a final 200 in 37 for a new varsity and freshman MIT record of 9:52.74. van den Heuvel was close behind in fifth with a time of 9:56.28. Johnson, suffering from plantar fasciitis was unable to hold on in the last 800, ending up with a very respectable 10:10.14. All three easily qualified for all championships with a set standard. The NCAA Championship has no standard, instead taking the top 17 performances in each field for the women (15 for the men) and the 3K is a new event so one can only guess that 9:52 will be close, although the six second indexing penalty for banked track times may put the time out of the eventual top 17.</p>
              <p align="left">Justin Bullock '14 was the lone MIT representative for the men. Last year in this meet he had set the MIT record of 8:19.12. After a difficult finish to his cross country season Bullock was hoping the return to the track would bring back similar success as he experienced last season. </p>
              <p align="left">The pace was fast as the leader went out in 64 and maintained that pace. The pack strung out with Bullock running 66-67 pace back in sixth place. Nothing changed for the first 1200, then Bullock fell to 68 pace as he tried to maintain contact with the fifth place runner. Even though he was tightening up, Bullock managed to continue 68 pace and was even able to finish with a 32 final 200, giving him a 8:22.20 final time, the third fastest time in MIT history and a performance that may be fast enough to qualify for Nationals.</p>
              <p align="left">The Engineers, beginning exams a week from Monday, will not compete again until their annual Alumni Meet on January 4.</p>
