              <p style="font-size: 12px"><span style="font-weight: bold; font-size: 16px">VARSITY OVERWHELMS ALUMNI WITH DEPTH AND BALANCE</span></p>
              <p align="left">January 4, 2014</p>
              <p align="left">The Thursday-Friday snow storm cancelled many flights for those returning varsity athletes as well as some of the alums coming back to Cambridge for the annual <a href="alumni.res.php">Alumni/ae Meet</a>. Many took over 24 hours of travel to get back to MIT, some not arriving until just before the competition, and many not making it back in time for the meet. Friday's practice was cancelled due to the state of emergency in the area, meaning the team had not practiced or competed since December 14. To make matters worse, the heat was not working in the building until shortly before the start of the meet, making the temperature for competition between 50-60 degrees at its best.</p>
              <p align="left">Despite all that, the men's varsity squad won 98.5-18.5 and the women's varsity took first place 98-20 over their elders. Most of the performances were not outstanding, but the competitive effort was excellent. No doubt there was some rust that needs taking off, but overall the teams look ready to make their mark on the New England Indoor Track &amp; Field season.</p>
              <p align="left">On the men's side, there was solid representation from the &quot;Has-Beens&quot;, as 19 competed with many more in the house to support there comrades. The women had fewer competing with 14 performing, but also having quite a few in attendance for support.</p>
              <p align="left">The best performances by the men were turned in Ken Cooper '15, who won both the 35lb. Weight Throw and Shot Put, Carrington Motley '16 winning both the Long Jump and Triple Jump and Harrison Hunter '15 winning the 800. Cooper showed his talents by throwing the weight 59-5 and putting the shot 51-1 3/4, both very close to personal bests. Motley took no prisoners, winning the Long Jump by over a foot with his short approach 21-0 1/2 performance, and did even better in the Triple Jump with a 44-10 1/4 effort. Hunter out-dueled teammate Justin Bullock '14, running a very strong 1:56.94 to Bullock's 1:57.85, both impressive times on the tight turns of the MIT 200 meter flat track.</p>
              <p align="left">Also claiming a victory was John Thomas '15 clearing 6-0 in the High Jump. Richard Lu '16 led a nice sweep in the 60 meter dash of 7.15 to 7.16 for Taylor Njaka '17 and 7.19 for Tyler Singer-Clark '14. Singer-Clark reversed things in the 200, leading a sweep with his 22.99 to Njaka's 23.49, with Nick Diamantoni '15 claiming third with his 24.16. Mac Gager '14 took first place honors in a varsity sweep of the 400, clocking 52.02, with Sam Parker '15 moving down to the 400 to take second in 52.11 and Diamantoni again earning third place in 52.25. Matt McEachern led an all-freshman sweep in a very strategic, but pedestrian like mile as no one seemed to want to run fast. McEachern ran 4:32.32 while Ken Leidal ran 4:32.88 and Nick Matthews raced to a 4:38.99. The final sweep was in the 5K with Benji Xie '15 leading Matt Jordan '15 and Brian Gilligan '17 to the finish line in 15:48.81 to 15:54.26 and 16:05.02 respectively.</p>
              <p align="left">The key performances for the Alumni were had by former All American, Nate Ball '05 in the Pole Vault, winning for the second year in a row with a vault of 14-5 1/4, and Omari Stephens '08 who won the High Hurdles in 9.00 and claimed second in the vault at 13-5 1/4.</p>
              <p align="left">Other Alums who scored were many time All American, Craig Mielcarz '03, taking second in the High Jump at 6-0, Matt Falk '12 tying for third in the High Jump at 5-10, Patrick Barragan '08 earning third in the Pole Vault for an Alumni sweep, and Nick Leonard '11 taking third in the 60HH in 9.34.</p>
              <p align="left">The women's competition was led by Jackie Brew '14, as she won the 60 Meter High Hurdles in 9.43, won the 200 in 27.26, and took second in the 60 Meter Dash by .01 to teammate Veronica Szklarzewski '15, 8.21 to 8.22. She also anchored the winning 4x200 Relay.</p>
              <p align="left">There were many varsity victors and quite a few sweeps as well. Ellen Liverpool '15 moved up to number five on the all-time MIT list with her 46-6 3/4 victory over teammates Ashley Wheeler '15 (43-10 1/4) and Jackie Vahey '17 (40-4 1/4). In the other throwing event, Isabella Stuopis '16 came off injury to come close to her personal best to win the Shot Put in 42-4 1/4 over teammates Vahey at 37-7 3/4 and Christine Labaza at 33-11 1/4. Both of Brew's victories led sweeps as her teammates, Stephanie Guo '17, took second in the 60HH in 9.86 while Stephanie Birkhimer '14 claimed third in 10.01. In the 200, Kendra Knittel '17 ran 28.30 for second in the 200 and Birkhimer earned third with a 28.73 performance. In the 400 Guo earned the victory over Knittel 64.28 to 67.41. Cindy Huang '15 was the only runner in the 800, so she won with an uninspired 2:28.45. Louise van den Heuvel '14 dominated the Mile as she led her team to a sweep with a 5:08.11 victory. Maryann Gong '17 took second in 5:14.64 while Elaine McVay '15 ran 5:29.03 for third. Kaitlin Allen '14 took first place honors in the 5K with a 18:25.65 over teammates Shivani Kaushal '15 in 19:06.94 and Heather Sweeney '16 in 19:33.41. Other victories by varsity members were earned by Preethi Vaidyanathan '15 in the High Jump, clearing 5-1 3/4, and Michelle Johnson '15 taking first in the Triple Jump with a 34-3 1/2 performance.</p>
              <p align="left">The Alums, similar to their male counterparts, dominated the Pole Vault, taking four of the top five places with Karin Fisher '11 winning at 11-11 1/4 and Lauren Kuntz '13 taking second at 11-5 1/4. Diana Zhu '12 won the Long Jump with a leap of 16-0 1/2, and tied for second in the High Jump at 4-9 3/4. Other scorers for the &quot;Has Beens&quot; were a second place in the Triple Jump by Julia Zimmerman '12 in 33-8 3/4, Margaret Leibovic '10 in the 60 with a 9.14 third place effort and Martha Gross '12 taking third in the 400 in 1:16.09.</p>
              <p align="left">Next week, MIT travels to Lewiston, Maine to compete against host Bates College and Colby College.</p>
