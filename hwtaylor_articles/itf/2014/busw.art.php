              <p style="font-size: 12px"><span style="font-weight: bold; font-size: 16px">Lee, Stuopis, van den Heuvel and Wicker Lead Engineers With MIT Records in Final Tuneup Before Championships</span></p>
              <p align="left">February 15, 2014</p>
              <p align="left">With the MIT squad split between <a href="busw.res.php">BU's Scarlet and White Invitational</a> and the <a href="cupid.res.php">Tufts Cupid Invite</a>, most oval runners were at Boston University, and all field events, along with sprinters, hurdlers and some oval runners were at Tufts. </p>
              <p align="left">Louise van den Heuvel '14 started things off for the women, racing the 800 for the first time this year. The pace was fast from the outset with the leaders taking out the first 200 in 30. Cindy Huang '15 and van den Heuvel were both out in 31.5, running in good position. The second lap slowed slightly, but not by much as both MIT runners hit 64.0 at the 400. As the trail group fell away from the leaders, van den Heuvel started her move, blowing by Huang at the 550 mark and taking aim on the leaders. She crushed the final 200, took the victory and the MIT record with a monster PR of 2:11.98. Huang backed off when her teammate passed her, but still ran a respectable 2:16.82</p>
              <p align="left">Harrison Hunter '15, took to the track in the men's 800. Having taken a little over a week off from hard efforts after getting hit by a car while on his bike and injuring his back, Hunter was anxious to get back to racing. Uncharacteristically, he went towards the front after a 27.6 first 200. With the pace fast in 55.9 and 1:25.0 splits Hunter began to feel the pain. He initially tried to fight it, but then lost his competitiveness. He still finished in 1:55.57, but could have run 1:53.</p>
              <p align="left">The 400 was next for the Engineers. States Lee had two reasons to run fast. The first was to make up for his race last weekend when he got stuck in lane two the entire second lap, yet still ran 48.58. The second reason was to go after the MIT record of 48.46, held by the legendary Ayisi Makatiani '90. Lee was in the fast section, so getting the break was not a given. In lane six, Lee squeezed into second as he hit the 200 mark in 22.6. Lee almost made a move on the backstretch but then thought better of it, holding his kick until the end of the final turn. He was not able to get the win, but did get the record, by .01, running 48.45. Many of the other 400 runners were dealing with bronchial and sinus infections, either not racing or unable to run fast.</p>
              <p align="left">In the 200, Jackie Brew '14, who earlier in the day had run 8.97 in the 60HH, which ties her for seventh on the NCAA Division III list, came over to BU to race the one lapper. She ran a wonderful PR of 25.25, which puts her ninth on the NCAA Division III list. Kendra Knittel '17 ran a PR of her own, racing to a 25.94.</p>
              <p align="left">The 1000 was next, and MIT athletes put on a show. On the women's side, Christina Wicker '17, was focused on getting out and taking control of the race. A rabbit jumped into the race and helped no one as she ran much too fast for anyone's benefit. However, Wicker was not aware of the situation and followed the rabbit to a 30. first 200. As Wicker approached the end of the first lap she saw her split on the clock and realized it was much too fast. This error combined with the fact the meet had moved ahead of schedule, not permitting Wicker to complete her warm-up, would have mentally destroyed most competitors. Wicker took it in stride, adjusted her pace, and proceeded to hit splits of 66, 1:43 and 2:20. She was fading at the finish but still managed a 35 final 200 for a new MIT freshman and varsity record of 2:55.87. Rick Paez '15, who has floundered in the middle distance - distance events since his arrival at MIT, took a shot at the 800 for the first time last week and delivered a 1:59 on MIT's flat track. The confidence he shows in these shorter races is a complete 180 from his previous competitiveness. In the 1000, Paez came through the 800 in 1:59 and kept it going, completing the five laps in 2:29.82.</p>
              <p align="left">Meanwhile, over at Tufts, more amazing performances were taking place. Isabella Stuopis '16 added nearly a foot to her PR and MIT record, placing second at 44-8 3/4. Stuopis is now eighth on the national list and ranked first in New England Division III. Also throwing well in the Shot, Ken Cooper '15 threw 52-11.</p>
              <p align="left">In the Weight Throw, Cooper threw a personal best of 61-4 1/4. Kevin Kusch '17 had nearly a three foot PR of 45-7.  Ashley Wheeler '15 threw a personal best of 48-1 1/4. Stephanie Riocci '16 also threw a PR and moved into the MIT top 10 list with her 41-1 1/2 performance.</p>
              <p align="left">As usual at this time of year, MIT vaulters owned the event. Cimmy Virdi '16 won the event at 12-1 1/2. Abby Klein '14 vaulted a PR of 11-9 3/4 to move to a tie for 12th on the NCAA list. Also for the women, Joanna Chen '15 cleared a PR of 11-5 3/4 and Madeleine Waller '17, in the final opportunity to get it done, cleared 10-0 to qualify for the New England Division III Championship. For the men, Ben Schreck '15 won the event at 15-7. Schreck is ranked fourth on the NCAA list with a vault of 16-0 3/4.</p>
              <p align="left">In the men's Triple Jump, Carrington Motley '16 returned to his competitive self, having a wonderful series of jumps, winning with a new indoor PR of 47-2 1/2, which puts him in 11th on the NCAA Division III list. Angel Echevarria '16 tied his personal best of 46-3 1/4.</p>
              <p align="left">Adrian Samsel '16, primarily a multi-event athlete, jumped 21-2 1/2 in the Long Jump to qualify for the New England Division III Championship. </p>
              <p align="left">Alexandra Shade '17 ran a freshman record in the 600, despite going out in 30 seconds for the first 200. Her 1:40.77 was nearly a two second improvement.</p>
              <p align="left">Brian Gilligan '17 did not qualify for any championship, but his personal best performance of 15:46.63 in the 5K is certainly one of the most competitive efforts by anyone on the team.</p>
              <p align="left">Next week, the men are at home where they will attempt to defend their New England Division III title. The women will be at Springfield College to try to claim first place after finishing second the past three years.</p>
