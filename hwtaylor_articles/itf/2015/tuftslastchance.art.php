              <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">Triple Jumpers Lead MIT at Tufts Last Chance Qualifier</span></p>
              <p align="left">March 6, 2015</p>
              <p align="left">A handful of MIT's track &amp; field athletes traveled the four miles to <a href="tuftslastchance.res.php">Tufts University for the Tufts Last Chance Qualifier</a>--on this the final weekend to get in a mark for the NCAA Division III Indoor Track &amp; Field Championship.</p>
              <p align="left">MIT athletes earned three significant improvements while winning six events with only 17 athletes. The biggest improvement was by Arinze Okeke '17 who put in a 13&quot; PR in the Triple Jump to win in 47-11 3/4 and move up to eighth on the NCAA list. Teammate Carrington Motley '16 placed second in 45-0 1/4.</p>
              <p align="left">In the women's Triple Jump, Michelle Johnson '15 won the event with a new MIT record in 37-10. This mark moved her up to the magical 17th position on the NCAA list as only 17 make the Nationals field. Time will tell if her mark holds up through tomorrow's meets.</p>
              <p align="left">Cimmy Virdi '16 won the Pole Vault with her opening height and proceeded to set a new indoor PR with a 13-01 3/4 performance. This moves her back into the number one positiion on the NCAA list. Also in the Pole Vault Maeve Devlin '18 was second at 11-2 1/4, Jen Tylock '17 was third, also at 11-2 1/4, and Joanna Chen '15 was fifth at 10-8 1/4.</p>
              <p align="left">Ellen Liverpool '15 won the Weight Throw with a throw of 52-10 3/4. Isabella Stuopis '16 won the Shot Put with a toss of 42-9 1/2. Preethi Vaidyanathan '15 won the High Jump at 5-4 1/2 while Natalie Alper placed fourth at 5-0 1/2. Veronica Szklarzewski '15 placed second in the Long Jump at 16-8 3/4.</p>
              <p align="left">The only runner representing MIT was Hannah Chen '18 in the 400. She pressed a bit too hard in the first 200, was forced twice to break stride as was about to pass going into turns three and four, finishing in 58.71.</p>
              <p align="left">In the men's 800, Dan Kilcoyne '16 placed sixth in 1:56.12, a season best. Rick Paez '15 ran 1:57.78.</p>
              <p align="left">Tomorrow a few men will be competing at the IC4A Championship to try their hands, or rather feet at improving their NCAA rank.</p>
