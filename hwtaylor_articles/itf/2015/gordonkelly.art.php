              <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">Vaidyanathan Sets Another High Jump Record</span></p>
              <p align="left">February 14, 2015</p>
              <p align="left">Leading the way for the Engineers at the <a href="GordonKelly.results.pdf">MIT Gordon Kelly Invitational</a> was Preethi Vaidyanathan '15 in the High Jump where she set another MIT record, this one at 5' 6&quot;. While a large majority of the runners were over at Boston University, all but three of the field event athletes were at their home meet. In all 13 women's teams and 12 men's teams participated in the meet.</p>
              <p align="left">Of the 12 field events competed, MIT took eight victories, four for the men and four for the women. In addition to Vaidyanathan's victory, Jennifer Tylock '17 won the Pole Vault at 10-11 3/4. Teammate Maeve Devlin '18 cleared the same height but lost the event on misses. Ellen Liverpool '15 remained undefeated in Division III, taking the Weight Throw at 53-11 3/4. Michelle Johnson '15 claimed first place honors in the Triple Jump at 36-4.</p>
              <p align="left">As is customary, Ken Cooper '15 won his two specialties, the Weight Throw and the Shot Put. In the Weight Cooper threw 54-10 and in the Shot 49-8 1/4. Ben Schreck '15 cleared his first height this year, and won the Pole Vault in doing so, clearing 15-1 as he returns from a hamstring injury. Arinze Okeke '17 won the Triple Jump at 45-8 1/2.</p>
              <p align="left">While there were very few MIT runners, a few victories were claimed by the Engineers. Nick O'Connell won the 1000 in 2:36.94. Richard Lu '16 won the 200 in 23.05 for a new PR. Veronica Szklarzewski '15 won the 60 Meter Dash in 7.98. Manolya Altan '18 won the 800 in 2:27.77. Hannah Chen '18 won the 60HH in a personal best of 9.33, and she led off the winning 4x200 team. The quartet of Chen, Kayla Harris '15, Kendra Knittel '17, and Szklarzewski ran 1:46.13, the fastest time in New England Division III this year.</p>
              <p align="left">The New England Division III Championships are next weekend. The men hope to defend their title and claim their fifth indoor title in six years. The women, having not won since 2009, placing second the fast five years, are ready to take back the crown.</p>
