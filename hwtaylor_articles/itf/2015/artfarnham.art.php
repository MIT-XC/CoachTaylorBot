              <p style="font-size: 12px"><span style="font-weight: bold">Vaidyanathan Leads Engineers With High Jump Record</span></p>
              <p align="left">January 24, 2015</p>
              <p align="left">Preethi Vaidyanathan '15 led MIT performances at the <a href="artfarnham.res.pdf">Art Farnham Invitational</a> held at MIT's Shobrys and Aronson indoor track. Her 5'5&quot; PR set a new MIT record, tying her for 10th on the NCAA Division III list. </p>
              <p align="left">This meet was the  first of four weekends in which MIT will be competing in non-scoring invitationals as they focus on preparing themselves for the four weeks of championships that follow. Giving many of the runners the weekend off from competing, the focus was on individuals performing at a level to qualify for the New England Division III Championships, and for those who were already qualified, to work on getting an NCAA Division III Championship mark.</p>
              <p align="left">In the field events there were many improvements by Tech athletes. Ken Cooper '15 had a season best in the Weight Throw, winning with a toss of 58-4 3/4. Abe Gertler '18 vaulted a season best of 13-1 1/2 and Sam Ravnaas '18 vaulted a PR, also clearing 13-1 1/2. Not to be left out, Luke Gray '18 cleared 12-1 1/2 for a PR as he prepared for his Heptathlon debut next week. Arinze Okeke '17 won the Triple Jump with a 46-10 3/4 indoor personal best, which moved him up to number five on the MIT all-time list. Cody Bratten '18 put the shot 40-7 for a personal best.</p>
              <p align="left">The Weight Throw was the premier event for MIT on the women's side as all five competitors set either personal or season bests. Ellen Liverpool '15 led the way with the top throw in New England Division III, winning with a toss of 53-9 1/4, putting her seventh on the NCAA List. Ashley Wheeler '15 was second with her own PR of 49-7 1/4. Jackie Vahey '17 also had a personal best, a three foot improvement to 49-6 1/2. Ariela Slutsky '18 moved to number nine on the MIT all-time list with a throw of 43-7 and Isabella Stuopis '16 had a season best of 43-3 1/4. All five MIT throwers are among the top nine in MIT history. The Pole Vault was one of the top field events for the women as season bests were produced by Abby Klein '15 clearing 11-6 1/2, and Joanna Chen '15 clearing 11-0 1/4. Haley Strouf '18 had season bests in the Long Jump at 16-5 3/4 in winning that event, and 30-4 1/4 in the Shot Put. </p>
              <p align="left">On the track, starting with the shorter events, Richard Lu '16 had a season best in the 60 with a 7.28. Michael Kaba '16 had a season best by .01, running 8.53 in the 60HH. In the 200, both Lu and Chris Sweeney '18 ran to season bests of 23.46 and 23.47 respectively. Brandon Corts '17 and Danny Newman '17 ran season bests in the 400, sprinting to times of 53.11 and 53.19. One of the best performances was produced by Henry Tareque in the 600. Tareque ran a perfect race, allowing everyone to go out too fast as he took ownership in the back, running the correct pace to maximize his performance. Going into the final 200 it was clear he would claim the victory. He passed his final opponent about 40 meters from the finish, crossing the line in a personal best of 1:25.39. Michael Picchini '18 and Caspar Stinn '18 placed first and second with season bests in the 800 of 2:02.39 and 2:02.88. The highest quality race for MIT with regard to New England Division III qualifying performances was had in the 1000 where Sam Parker '15 won in 2:30.37, leading from start to finish. Backing that up, Rick Paez '15 and Dan Kilcoyne '16 placed third and fourth in 2:32.62 and 2:32.78. Thos performances have them currently ranked second, seventh and eighth in New England. In the 3K, Allen Leung '15 qualified with a fine 8:41.85 time. In the 5K, Nicholas Waltman '18 ran a spectacular 15:29.41 to qualify and Alex Knoedler '18 ran an impressive personal best of 15:46.10.</p>
              <p align="left">The women held out many more runners, so not as many personal or season bests were set. Veronica Szklarzewski '15, Kayla Harris '15 and Kendra Knittel '17 all ran season bests in the 60 Meter Dash, running 8.01, 8.11 and 8.24 respectively. Hannah Chen '18 won the 60HH with a personal best of 9.34. Harris came back in the 200 to win with a season best of 26.88. Liz Cox '18 won the 800 with a season best of 2:24.31. Manolya Altan '18 had a nice breakthrough, winning the 1000 in 3:12.64.</p>
              <p align="left">Next week, MIT will split their squad, sending most of their athletes to Tufts for the Tufts Stampede on one of the fastest flat tracks in New England, and the remaining runners to BU for the John Thomas Invitational, which undoubtedly has the fastest banked track in the region if not the country.</p>
