              <p style="font-size: 12px"><span style="font-weight: bold">Quinn, Gong and Godwin Post Excellent 3K Performances</span></p>
              <p align="left">December 6, 2014</p>
              <p align="left">Fresh off a successful Cross Country season where the women placed second at the NCAA Division III Championship and the men were eighth, three individuals, Sarah Quinn '16, Maryann Gong '17 and Colin Godwin '17, all earning All America honors at the championship two weeks ago, raced today at the <a href="buseasonopener.res.php">Boston University Indoor Track &amp; Field Season Opener</a>. The goal was to get a National qualifying performance first thing in the 3K and then take a few days off prior to gearing up for the bulk of the indoor season in February and March.</p>
              <p align="left">There was solid competition in both the men's and women's event, so there was little worry that the pace would be fast enough. The women's race was first, and as predicted the pace was quick, too quick. Quinn and Gong were planning to run 78's for the first mile and try to pick it up from there. Both were at the back of the pack, a good 30 meters from the leader and ran 75 for the first 400. They stayed at the back as they slowed a bit and then got on pace, hitting the 1600 right at the planned 5:12. Quinn sped up a bit too much at that point, hitting the next 400 in 75 to take the lead while Gong did not have the confidence and passion to go with her. Quinn threw in a 77, 78 and a final 200 in 40 as her calves had been tight before she even started the race and just got tighter as the race progressed. Her winning time of 9:42.74 was just three tenths off of Gong's Varsity and Freshman record set last winter. Gong closed well to finish in second place in 9:49.36. Quinn's time will certainly qualify for the NCAA Championship in March, but Gong's time may not hold. Based on their final pace, Quinn's and Gong's converted 3200 times would have been 10:23 and 10:27 respectively.</p>
              <p align="left">In the men's race, Godwin was running his first collegiate track race, and his first track race in over two years as he had been injured his entire senior year of high school track and all of his freshman year last year at MIT. The goal was to run 67-68 400's the entire race. The initial pace was perfect and Godwin settled to the rear of the lead pack. His initial 400 was 68 and he continued that pace through the 1600 in 4:33. He was hoping for 4:30, but was becoming cautious as he is still getting accustomed with the discomfort of racing at that level. Godwin slowed to 71 and then 72 before picking it up again with a 69 and 34 final 200 to finish sixth in 8:39.00. The converted sub 9:15 3200 would have been a nice personal best for the freshman eligible Godwin.</p>
              <p align="left">MIT finishes the pre-exam and holiday period with their annual Intrasquad Meet next Saturday, then take three weeks away from organized practice before starting up again in early January.</p>
