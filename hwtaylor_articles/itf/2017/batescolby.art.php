            <p style="font-size: 12px"><span style="font-size: 16px"><strong>Quality Overcomes Opponents Depth in Victory Over Bates and Colby</strong></span></p>
            <p align="left">January 14, 2017</p>
            <p align="left">A number of tweaks and illnesses, combined with more than the normal number of athletes out with injuries, put MIT in a situation where it looked doubtful for them to win their annual tri-meet with Bates and Colby colleges in the first team intercollegiate meet of the season, hosted at the Johnson Indoor Track by MIT.&nbsp; When the dust had settled MIT escaped with two victories due to excellent top end performances despite their lack of depth&nbsp;on this particular day.&nbsp; The men won with 178 points to 125 for Bates and 59 for Colby.&nbsp; The women took first in a hard fought battle with 160.5 points to 135 for Bates and 74.5 for Colby.</p>
            <p align="left">MIT's men's team won the&nbsp;meet by outscoring their opponents in the jumps with 60 points to 17 for Bates and 11 for Colby.&nbsp; In the High Jump, Alec Reduker '20 took first place with a clear of 6-7 3/4, and he had some decent attempts at 7-0 1/4.&nbsp; Scott Cameron '19 led a 1-2-3 finish&nbsp;in the Pole Vault as he&nbsp;won the event at 14-5 1/4 from a short approach.&nbsp; Bobby Upton '20 also cleared 14-5 1/4 for second place and Raja Rajcic '20 took third at 13-11 1/4.&nbsp; Arinze Okeke '17 won both the Long Jump and Triple Jump with performances of 22-5 3/4 and a personal best of 48-11, which is surely good enough to get him to the NCAA Division III Championship in March.&nbsp; For the third time this season Billy Ruschel '18 jumped 47-5 3/4, this time earning second place in the Triple Jump.</p>
            <p align="left">The men also came out ahead in the running events, but by a much closer margin.&nbsp;Tre Albritten '18 took his two primary events, winning the 60 in a personal best of 7.07 and taking first in his first 200 of the year in 22.40.</p>
            <p align="left">Tyndale Hannan '20 led a 1-2-4 finish in the 600 with an excellent 1:23.84 performance.&nbsp; Nathan Munet '19 took second in 1:24.08 and Henry Tareque '17 placed fourth in 1:25.33.</p>
            <p align="left">MIT also did well in the longer distances, winning the Mile and the 3K. Aidan Gilson '19 won the Mile in a strategic race, finishing in 2:07 for an overall time of 4:23.68. &nbsp;Dennis Maloney '19 took charge of the 3000 Meter race after he and teammate, Matt Deyo 'G came through the 1600 in 4:37.&nbsp; He then ran 67 quarters and finished with a last 200 in 32 to run 8:32.17.&nbsp; Deyo ran 8:40.39, running 31 for the final 200 to place third.</p>
            <p align="left">The other victory for MIT was in the DMR.&nbsp; Billy Woltz '20 led off with a 3:20 1200 after having run the 800 earlier in the meet.&nbsp; Daniel Sun '20, who placed third in the 60HH earlier, ran a 55.7 400 leg.&nbsp; Munet doubled back with a 2:00.5 800 and Josh Rosenkranz '19 came back from a&nbsp;4:26.27 third place in the Mile Run to run a 4:29 anchor for the victory.</p>
            <p align="left">Of particular note, Brandon Corts '17 showed tremendous heart in running an indoor PR of 52.21 to place second in the 400, beating many higher seeded teammates.&nbsp; Corts came back in the 4x400 made up primarily of middle-distance runners (Tareque, Hannan and&nbsp;Gilson) to anchor the team to second place in a very aggressive 51.9 performance.</p>
            <p align="left">In the women's competition, the team lead went back and forth the entire meet between Bates and MIT.&nbsp; Despite the win by Maryann Gong '17 in the Mile Run in 5:03.84, leading from start to finish and closing in 2:25, Colby was in the lead with 13 points with MIT at 11 and Bates with 10 points.&nbsp; The first&nbsp;event was the 4x200 Relay, in which MIT did not participate&nbsp;and Colby won.</p>
            <p align="left">The field events helped Tech a lot.&nbsp; After the High Jump, in which Colby took first and third, MIT was down by eight, but still ahead of Bates as Natalie Alper '17 cleared 5-3 3/4 to take second place on misses.&nbsp; About the same time, Ariela Slutsky '18 won the Shot Put with a throw of 37-3 1/2 to help the Engineers to close within three points of Colby and move ahead of Bates by seven points.&nbsp;Despite the loss of star jumper/sprinter, Nneoma Okonkwo '18&nbsp;due to a slight injury, Haley Strouf '18 took up the slack, winning the Long Jump with an indoor PR of 17-5 1/4 to help her team take the lead with 46.5 points to 39.5 for Colby and 36 for Bates.</p>
            <p align="left">Back on the track, the 60HH did not go the way it should have, which gave Colby the lead again.&nbsp; Allison Hill of Bates won in an excellent 8.97 and MIT got beat badly by two Colby College runners, scoring only five points with fourth and fifth places by Gabriella Zak '20 (9.54) and Hannah Chen '18 (9.86).&nbsp; In the 400, Tech only had one runner, Jane Reed '20.&nbsp; Reed competed very well, running a PR 62.65, just missing out on first place.&nbsp; However, Bates scored the remaining five places to earn 17 points, surging ahead with 53 points to 51 for MIT and 39.5 for Colby.&nbsp; After the 600, where Bailey Tregoning '19 ran an inspiring 1:37.45 for the victory, and Skylar Brooks '20 made a remarkable come from behind move to claim second, the Engineers were&nbsp;back in front.</p>
            <p align="left">Despite the loss of Okonkwo, Chen (8.32), Michelle Menkiti '20 (8.34) and Kathryn Mohr '20 (8.42)&nbsp;did the job in the 60 Meter Dash by sprinting to a 2-3-4 finish respectively even though the times were not the trio's best.</p>
            <p align="left">In the 800, Gong returned, and even though she did not run to her ability, she led start to finish as she had in the Mile Run, winning the 800 in 2:21.64.&nbsp; Bates still outscored MIT, taking the lead&nbsp;once again, leading 80 to MIT's 79.5.</p>
            <p align="left">After the Weight Throw and the 1000 it looked as though MIT had pulled ahead for good.&nbsp; In the Weight Throw Slutsky took second with a new PR of 52-1 and Jackie Vahey '17 returned from her high ankle sprain injury to take third with a 47-10 1/4 throw.&nbsp; In the 1000, freshmen Marissa McPhillips and Katie Bacher went 1-2, running an outstanding race in  3:00.78 and 3:01.01 respectively.&nbsp; Tech was up 101.5 to 91. However, then came the Triple Jump.  Tech's lone entry, Jasmine Jin '20 performed well in getting second with a jump of 35-10, but Bates claimed 1-3-4-5-6 to take the lead 107 to 106.5.</p>
            <p align="left">Fortunately, MIT saved their best for last.&nbsp; The only events in which they had depth were the 3K and the Pole Vault&nbsp;and those two were the final events other than the 4x400 Relay and the Distance Medley Relay.&nbsp; Leandra Zimmermann '19 (10:26.45) led Mary Eccles '18 (10:29.08) and Bailee Margolis '19 (10:30.23) to a 1-2-3 finish and Charlotte Folinus '20 added a point for sixth&nbsp;to give MIT the lead for good.&nbsp; In the Pole Vault, there were nine entries and Tech had seven of them.&nbsp; Bates had the other two, but they were finished before the MIT vaulters started.&nbsp; Kari Stromhaug '19 won at 10-11 3/4.&nbsp; Jenn Tylock '17 and Kathryn Mohr '20 tied for second, also clearing 10-11 3/4.&nbsp; Makenzie Patarino '19 took fourth at 10-6, Kathleen Brandes '19 was fifth at 10-6 and Kaitlyn Sparks '20 was sixth at 10-0.&nbsp; The sweep of the 22 points meant game over.</p>
            <p align="left">MIT took second in both relays.&nbsp; The quartet of Chen, McPhillips, Reed and Tregoning ran 4:05.46 with Chen leading off in 60.1 and Tregoning bringing it home in 59.3.&nbsp; In the DMR Bacher, Alper, Brooks and Jenna Melanson '20 ran 12:48.42.</p>
            <p align="left">Next week MIT travels to Brunswick, Maine to face host Bowdoin&nbsp;College, Tufts University and Division II&nbsp;Merrimack College.&nbsp; Getting a few of their athletes back will certainly help the cause&nbsp;for MIT.</p>
