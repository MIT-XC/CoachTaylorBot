            <p style="font-size: 12px"><span style="font-size: 16px"><strong>MIT Pole Vaulters Dominate Tufts Stampede</strong></span></p>
            <p align="left">January 27-28, 2017</p>
            <p align="left">There were not a ton of outstanding performances by MIT's Track and Field team at the <a href="stampede.res.php">Tufts University Stampede Invitational</a>, but the pole vaulters certainly took advantage of the opportunity.&nbsp; Most of the top runners, sans distance runners, were over at BU's Terrier Classic, but MIT had nearly all of the field event competitors, who were not given the weekend off, over at Tufts.</p>
            <p align="left">Bobby Upton '20 cleared another personal best, this one 15-5, narrowly missing the MIT Freshman Record in the Pole Vault, set by teammate Scott Cameron '19 last year.&nbsp; Cameron claimed second with his season best of 14-11.&nbsp; The women ruled as well.&nbsp; Kaitlyn Sparks '20 cleared 11-5 3/4 for a season best and indoor PR.&nbsp; Jen Tylock '17, Kathleen Brandes '19, Kari Stromhaug '19 and Makenzie Patarino '19 all cleared 10-11 3/4.</p>
            <p align="left">The other big event was the 3K.&nbsp; Matt Deyo 'G led the men with a second place 8:23.36, good for third on the NCAA list.&nbsp; Dennis Maloney '19 was fourth in 8:29.20.&nbsp; Both performances were personal bests.&nbsp; Also running well was Josh Derrick '20 at 8:42.97.&nbsp; Megan McCandless '20 continues her comeback as she led the Tech distance squad with a 10:10.93.&nbsp; Leandra Zimmermann '19 also ran well with a 10:19.41.</p>
            <p align="left">On Friday evening Natalie Alper '17 set a PR of 3137 points in the Pentathlon.&nbsp; Alper tied her best in the High Jump, clearing 5-5 1/4 along the way.&nbsp; </p>
            <p align="left">Jon Fakkema '19 threw a personal best of 51-10 in the Weight Throw&nbsp;as he continues his improvement.</p>
            <p align="left">Next weekend, MIT will be back at Tufts for the Cupid Challenge Invitational.</p>
