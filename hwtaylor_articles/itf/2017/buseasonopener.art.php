            <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">Deyo Sets MIT 5K record, Gong Runs NCAA Qualifying Time</span></p>
            <p align="left">December 3, 2016</p>
            <p align="left">MIT took four of the men's distance squad, and three of the women over to the <a href="../16-17/buseasonopener.res.php">Boston University Season Opener</a> with the purpose of achieving qualifying performances in either the 3K or 5K for the NCAA Division III Championship.&nbsp; If that failed they knew they would still get solid New England Division III marks. &nbsp;Continuing their training from the recently completed cross country season and knowing they would need to qualify for both the 3K and 5K this season so that their team would have more options come championship time, the seven were hoping to get one of these out of the way while they were race ready.</p>
            <p align="left">Marissa McPhillips '20, entered in the 3K, was first to compete for the Engineers.&nbsp; Seeded at 10:30, she somehow was moved to 10:04 and put into the fast section.&nbsp; She intelligently settled in the back of the field.&nbsp; After a first quarter that was much too fast even in the back of the pack at 72, she settled in, running a 5:21 first mile.&nbsp; At 2000 meters she started to show the signs of the fast early pace, but still held on for an excellent 10:21.66.</p>
            <p align="left">Aidan Gilson '19 was the sole 3K entry for the men.&nbsp; The goal was the get significantly under 8:20 by running 66-67 400's.&nbsp; This worked well through the 1600 as he looked strong at 4:28.&nbsp; However, instead of starting his race at that point, Gilson slowed to 76 pace for the next 1400 meters, finishing in 8:53.06</p>
            <p align="left">In the women's 5K, Maryann Gong '17 was in the fast section based on her 16:32.44 from last year,&nbsp;mid-season.&nbsp; She was not ready for that pace, and with the NCAA Division I Cross Country Champion&nbsp;leading the way, the pace was set at 15:30, or sub 5:00 per mile pace.&nbsp;&nbsp;Fortunately one other runner was setting a more reasonable pace in the back at 81 pace for the first 2000 meters.&nbsp; As the pace slowed to 82 through 3600 meters, it appeared Gong was ready to go ahead and get back to 81 pace, but she lost the confidence to do so after a partial attempt and fell back.&nbsp; Nevertheless she ran a solid 17:03.51, which is&nbsp;a time that has always qualified for the NCAA Championship, even with the nine second banked track penalty.&nbsp; </p>
            <p align="left">Mary Eccles '18 was in the second, and slower section.&nbsp; Hoping for an ambitious 83 second pace, she was out of luck as the leaders went out at a pretty quick pace.&nbsp;&nbsp;Eccles and one other runner settled into 84 pace in third and fourth, coming through the 1600 at 5:29.&nbsp; The pace slowed to 85 at the half-way point, so Eccles went in front&nbsp;to pick up the pace.&nbsp; The second 1600 was 5:37, but she was in a distant third, having to rely on lapping the slower runners to keep focused.&nbsp; Although she fell off to a 4:43 final 1600, she finished in a very strong 17:32.41.</p>
            <p align="left">All three men, Matt Deyo 'G, Dennis Maloney '19 and Rory Beyer '17, were in the second section of the 5K, which was too slow, but the fast section was too fast.&nbsp; They made the best of it by taking the lead.&nbsp; Maloney took the first turn through a 4:37 first mile.&nbsp; Deyo took over, but let the pace slow to 71, so Maloney took over again. However, he was unable to pick up the pace for more than 600 meters.&nbsp; After coming through the 3200 at 9:17, Deyo started to pull away by running 70's.&nbsp; He was able pick it up to 69 pace in the final 1000 meters.&nbsp; Once he got rolling he realized he had much more left than he thought, flying through&nbsp;the final 200 in 31 seconds to finish in a new MIT record of 14:26.55.&nbsp; Maloney also picked up the pace at the end to finish in a new PR of 14:40.38.&nbsp; Beyer, paying for the early ambitious pace, ran a 5:00 third 1600 to finish in a New England Division III qualifying 15:02.88.&nbsp; Deyo's time may be fast enough to make the NCAA Division III Championship field, but he will give it another go later in the season to make sure.</p>
            <p align="left">The Engineers have an intrasquad meet next Saturday before entering a two week period of classes and exams followed by a one week holiday break. &nbsp;They will pick up again on January 2 in preparation for their annual Alumni/ae Meet on January 7. &nbsp;Their first intercollegiate meet as a team will be on January 14 versus Bates and Colby at home.</p>
