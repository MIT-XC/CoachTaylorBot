            <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">Maloney Shatters 3K Record</span>            </p>
            <p align="left">February 26, 2017</p>
            <p align="left">The original plan was to rest the distance runners until Nationals and not worry about trying to qualify them in more events.&nbsp; Dennis Maloney '19 with the third fastest time in NCAA Division III in the 5K, was not among those who would go to Nationals in the 3K.&nbsp; So, when Maloney looked totally recovered in Tuesday's workout after his crushing 5K win in 14:33 last Saturday, the plan was developed to take advantage of a stellar field at the <a href="http://scripts.mit.edu/~hwtaylor/itf/16-17/bulastchance.res.php">Boston University Last Chance Qualifier</a> and enter him in the 3K.</p>
            <p align="left">Maloney, who had run 8:29.20 earlier in the year, needed an 8:15 or so to qualify because of the nearly six second penalty that he would incur&nbsp;from the banked track.&nbsp; The plan was to go for 4:22 mile pace at the slowest.&nbsp; While the Mile and 5K fields were Olympic caliber, the 3K was just extremely qualified.&nbsp; Fortunately, the pace turned out to be exactly what Maloney wanted.</p>
            <p align="left">Every 400 was between&nbsp;65-66 seconds, hitting the 1600 in 4:22 and continuing with that pace.&nbsp; With 600 to go it appeared Maloney was ready to kick it in&nbsp;and possibly win the race, but he held back until the final 200 where he dropped a high 31 to move into third, finishing in 8:11.60, a new MIT record and the third fastest time in Division III despite the correction to 8:17.31.</p>
