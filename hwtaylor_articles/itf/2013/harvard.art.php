              <p style="font-size: 12px"><strong><span style="">MIXED RESULTS FOR TECH TRACK AGAINST DIVISION I COMPETITION</span></strong></p>
              <p align="left">January 19, 2013</p>
              <p align="left">MIT Track &amp; Field made the trip up the river to compete in the <a href="harvard.res.php">Harvard Challenge</a> at Harvard University this Saturday. The unique concept pitted traditional Division III powerhouses MIT and Williams College against Patriot League members, Army and Northeastern and Ivy League teams, Harvard and Brown. With only six scoring places and a 7-5-4-3-2-1 scoring format, points were going to be difficult to come by, particularly for the Division III teams. In both meets, the Division III pair finished third. Individually, MIT claimed fifth in the men's meet, just three point behind Brown and very competitive throughout the meet. On the women's side, the injuries and lack of competitiveness were just too much to overcome as Tech finished last, although very competitive with all other teams except Harvard, who had way too much firepower and depth.</p>
              <p align="left">MIT entered the meet after the second week of practice with far too many injuries on the women's side left over from cross country and pre-season workouts, and a less than competitive attitude from many performers. While the majority of the male and female Engineers gave their all, far too many were held back by their mental approach to the competition as well as doubts about their own ability and readiness, particularly the runners.</p>
              <p align="left">In the men's meet, there were a few highlights. Carrington Motley '16, set a freshman record in the Triple Jump with a third place 46-1 1/4 performance. Supporting Motley, his teammates took fourth, fifth and sixth places, gaining 10 of MIT's 43 points. Arinze Okeke '16 placed second at 43-3, Jackson Wirekoh '13 earned fifth with 43-1 3/4 and Angel Echevarria '16 took the final point also jumping 43-1 3/4.</p>
              <p align="left">The other big event for the Engineers was the Mile Run. In a strategic, pedestrian paced race, Kyle Hannon '13 dropped a 58 final quarter to take second place at 4:13.42. Justin Bullock '14, after taking the lead with 600 to go, was not able to close as hard, finishing with a 62 final 440, good for third place in 4:15.09.</p>
              <p align="left">In the Pole Vault, Yida Gao '13 and Cyrus Vafadari '13 jumped season personal bests of 14-3 1/4 and 14-1 1/4 from a short approach to place third and fourth respectively.</p>
              <p align="left">In the 3K, Benji Xie '15 ran a superb race, fighting with the leaders the entire way, but unable to hang in the final 400. Nevertheless, Xie placed fifth with a 8:38.21 personal best. Teammate Matt Jordan '15 surprised everyone, not only with the fact that he stayed near the front, but also that he closed the final 220 in 31 seconds to place sixth in 8:38.61.</p>
              <p align="left">MIT was competitive in both relay events, the 4x440 and the Distance Medley Relay, placing third in both. Tech ran two 4x440 relay teams as they try to figure out who the best four legs will be come championship time. Freshman, States Lee led off for the A team, opening with a strong 50.2 to give MIT the lead at the handoff. Tyler Singer-Clark '14, not yet at championship form, kept MIT in it with a 50.3 effort. Mac Gager '14, lacking the all-out sprint speed, ran an excellent second lap to put MIT in contention for the lead, but anchor leg, Pat Marx '13 took off too soon, not able to maintain the speed of the baton through the zone. Marx ran a strong 50.0 leg but not the 47-48 that will be expected from the star 800 runner in a few weeks. The 3:20.92 performance was pretty good for the All American 4x400 unit so early in the season, but far from late season expectations. In the B relay entry Nick Diamantoni '15 ran a 50.7 leg and Derek Barnes '16 anchored with a 50.4.</p>
              <p align="left">The DMR was led off by Chadd Kiggins '15. Kiggins did not put MIT in contention as he was only able to produce a 3:11.7 1320 leg. Sam Parker '15 in his first meet after spending much of the fall injured, ran an excellent 49.6 440 leg. Jared Forman '13, a last minute replacement for the 880 leg, responded favorably with a 2:00.0 effort. Hannon doubled back, but never really engaged in the race after the first 440 in 63, instead slowing to finish in 4:25.6 for an overall time of 10:26.37.</p>
              <p align="left">Other point scorers for Tech were John Thomas '15 in the High Jump, clearing 6-0 1/2, Marx placing fifth in the 500 in 1:05.09, Lee in the 200, taking sixth place in a personal indoor best of 22.73 and a sixth place by junior Michael DuPlessis in the 60 meter dash, running it in 7.14 seconds in the trials and 7.15 in the finals.</p>
              <p align="left">In the women's meet, the only dominant event for the Engineers was strangely enough, one of the areas where they have the most injuries. Elaine McVay '15 and Allison Hallock '16 went with the leaders right from the gun in the 3K. The duo came through the mile in 5:21 and looked very strong. They both continued 81-82 second quarter pace to the end, just not having the kick of the eventual winner. McVay placed second in 10:06.32 and Hallock nearly earned a freshman record with her 10:07.70 for third place.</p>
              <p align="left">The Distance Medley Relay team earned the other second place for MIT. Alexandra Taylor '14, who earlier ran her debut Mile run in 5:04.24 to take third place honors, ran a very strong 3:42.3 three quarter mile leg. Isabel Crystal '16 turned an excellent and competitive 62.1 440 before executing a near perfect handoff to Cindy Huang '15. Huang did not perform near her best, running a slow 2:27.5, putting MIT in limbo, no where close to first or third. Louise van den Heuvel '15, who had earlier earned a sixth place in the 1000 with a 3:01.50, was also far off her best, running 5:22.6 to give MIT a cumulative 12:34.51.</p>
              <p align="left">Isabel Stuopis '16 set a freshman record in the Shot Put for the second week in succession, but this time added the varsity record to her list of accomplishments, throwing 42-9 3/4 to earn fourth place in the stellar field.</p>
              <p align="left">Lauren Kuntz '13 tied for fifth place in the Pole Vault, clearing 11-2, leading all MIT vaulters, all of whom were over 10-2 or better.</p>
              <p align="left">The final point for MIT was earned by Preethi Vaidyanathan '15 in the High Jump. She cleared a season best 5-2 1/4.</p>
              <p align="left">Next week, MIT will take their runners who race the 200 and up to Boston University's banked track in the Terrier Classic where the women will compete on Friday and the men on Saturday. The field event athletes along with some sprinters and hurdlers will compete at MIT's invitational on Saturday.</p>
              <p align="left">Based on today's results, the Engineers have some work to do, mentally and physically, to get ready for the Championships, which begin in four weeks.</p>
