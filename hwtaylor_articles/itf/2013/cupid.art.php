              <p style="font-size: 12px"><span style="font-weight: bold">Johnson, Quinn Set Varsity Records--Numerous Other Qualifying Marks Achieved at Tufts Meet</span></p>
              <p align="left">February 11, 2013</p>
              <p align="left">After three days of cancelled meets due to the historic snow storm on the East coast, MIT finally was able to get some of their athletes into a competition. Those few athletes competing took advantage of the opportunity, setting numerous personal and season bests, as well as two MIT records in the process, the women's 1000 and the women's Mile Run.</p>
              <p align="left">Friday and Saturday's meet at Boston University, the Valentine's Invitational, a meet with generally over 3,000 entries, was cancelled due to the massive snow storm (24-30&quot; in the Boston area) that hit the city on Friday and lasted for over 24 hours. Likewise, MIT's Invitational, as well as <a href="cupid.res.php">Tuft's Cupid Challenge</a> were postponed on both Saturday and Sunday as the two colleges continued to clear the snow away. With a lack of available officials and a severe reduction of interested teams, MIT cancelled their meet and sent their athletes to the Tufts meet. Only those MIT athletes who either needed to still qualify for the New England Division III Championship (NED3) or those who needed to improve their seed were going to compete.</p>
              <p align="left">While MIT had qualified four women and four men in the Triple Jump for the NED3 Championship, four women and four men, only one long jumper had qualified. Only competing in the Long Jump, both Jackie Konopka '14 and Carrington Motley '16 both met the qualifying mark. In the process, Motley won the event at 21-1 1/4.</p>
              <p align="left">Konopka's goal was not to qualify for the Long Jump, but rather to get five good marks in the Pentathlon events with hopes to qualify in the Pentathlon, an event in which she placed sixth in last year's meet. Konopka went on to have a phenomenal evening, totaling a big personal record while accumulating 3213 points. Her Long Jump was a personal best of 16-5, High Jump a season best 4-11 3/4, the Shot Put a season best 33-1, the 60HH a fine 9.59 and the 800 a best of 2:30.70.</p>
              <p align="left">Continuing in the field events, Marissa Engle '15, just off the injured list, took advantage of her one chance to qualify, clearing 10' to get the job done. Sherry Wan '16 cleared a personal best 10-6 and just missed 11-0.</p>
              <p align="left">On the track, the biggest highlights were supplied by senior star Brooke Johnson '13 and freshman sensation Sarah Quinn '16. Johnson ran the 1000 for the first time this year with the goal to run a time around 3:00 to qualify for the fast section of next week's 1000. The plan was to run 36 second 200's the entire way, but the leader took the pace out in 34 so Johnson just tucked onto her shoulder and let her do the work. However, the pace soon started to slow, hitting the 400 at 70. Johnson took the lead and did not relent, running the next 400 in 70 as well and then closing in 36 for a 2:56.27.</p>
              <p align="left">Quinn, fresh off of last week's MIT and freshman record of 9:56.01 in the 3K, was racing the mile for the first time this season. With a plan to go out in 74 and hold that pace, it worked well when the top seed went out in 73. The pace slowed to 2:28 for the 800, making Quinn anxious but ready to spring. With 600 to go, Quinn took off, running a 72 third quarter and closing with a 74 to win in 4:54.32, a new MIT and freshman record. Nicole Zeinstra '16, also racing the Mile for the first time this year, placed fourth in an excellent 5:07.42.</p>
              <p align="left">Jackie Brew '14, qualified in the 200 with a 26.88, as did Kayla Harris '15 in the 400 with a 62.73. Improving her qualifying time in the 800, Cindy Huang '15 ran 2:18.90</p>
              <p align="left">For the men, Josh Duncavage '13 finally started showing his wheels. Needing a qualifying time in the 200, Duncavage unfortunately was given lane 2. This did not affect the confident sprinter who blasted a 22.95 to easily run a season best and qualify for next weekend's championship.</p>
              <p align="left">Kyle Hannon '13 needed a qualifying time in the 1000 as an earlier illness prevented him from running it in previous meets. He ran well, although he did not race to win as he should have, qualifying easily in 2:31.45.</p>
              <p align="left">Logan Trimble '13 ran the Mile to give MIT some additional options in next weekend's meet, either the open mile or a leg in the Distance Medley Relay. Trimble tightened up in the latter stages of the race but ran a pretty strong 4:22.73.</p>
              <p align="left">MIT is not favored to win either the men's or women's championship next week, but if they perform well and want it more than their opponents they could be in position to pull out the team victory.</p>
