              <p style="font-size: 12px"><strong><span style="">TECH TRACK KICKS THE SEASON OFF WITH WIN OVER ALUMNI</span></strong></p>
              <p align="left">January 12, 2013</p>
              <p align="left">Forty six alums turned out to see what they could do against their younger counterparts on Saturday for the annual <a href="alumni.res.php">Alumni/ae Indoor Track &amp; Field meet</a>. Ranging in graduating class from as recent as last year to as far back as 1989 for those competing and back to 1974 for those cheering their fellow &quot;Has-Beens&quot; along.</p>
              <p align="left">In the end the numbers for the varsity teams proved too much for the out-manned alums. The women won their competition 87.5-34.5, while the men came out on top 87-35. On the women's side, Karin Fisher '11 and Portia Jones '12 were both double winners for the alumnae and those turned out to be the team's only victories. Fisher took the Pole Vault in 11-11 3/4 and the Triple Jump in a personal best of 34-03, while Jones claimed victories in the 60HH in 9.32 and in the 200 with a time of 27.37. Both ladies also scored in the High Jump with Jones earning second at 5-1 and Fisher tying for third at 4-11. In addition, Fisher placed second in the 60 Meter Dash in 8.33. The remaining 8 points were scored by Jamie Priest (Simmons) '12, earning second in the 200 and 400, Hazel Briner '11 placing third in the Pole Vault, and Diana Zhu '12 earning third in the 60HH.</p>
              <p align="left">The Varsity women had a pretty solid day on the track and in the field. Top point scorer was Veronica Szklarzewski '15, who won the Long Jump in 15-08 3/4 and the 60 Meter Dash in 8.26. She also placed third in the Triple Jump and was a member of the winning 4x200 Relay team. The other double winner for the Varsity was freshman Isabella Stuopis '16, who won both the 20 lb. Weight Throw (41-08 1/2) and the Shot Put (39-03 1/4), setting a new freshman record in the latter. The most impressive effort by the Varsity was the start to finish victory by Alexandra Taylor '14 in the 800 Meter Run. Taylor went out in 68 second for the first quarter and finished in an indoor PR of 2:17.92, a very impressive time on the tight turns of MIT's Shobrys and Aronson track. Other victories were earned by Preethi Vaidyanathan '15 in the High Jump at 5-1, Brooke Johnson '13 in the Mile Run in 5:10.19, Maddie Bairey '15 in the 400 in 63.75, Elaine McVay '15 with an 18:09.43 in the 5K, and the quartet of Szklarzewski, Bairey, Taylor and Isabel Crystal '16 in the 4x200 Relay.</p>
              <p align="left">The dominant force for the Alumni in the men's meet was CK Umachi '12. Umachi won the Weight Throw at 56-10, the Shot Put at 42-01 1/4 and placed third in the High Jump at 6-0 3/4. Matt Falk '12, a grad student with eligibility for the outdoor season, but none remaining for indoor season, claimed the victory in the High Jump at 6-2 3/4, an event the Alums swept with Craig Mielcarz '03 placing second. Nate Ball '05 took first place honors in the Pole Vault at 13-11 1/4. Similar to the women, those four victories were it for the old guys as they failed to claim any points in the running events. The additional 11 points were scored by Thad Wozniak '06, who earned a second place in the Long Jump with a 20-10 3/4 leap, a second place by Mitch Kelley '11 in the Shot Put at 38-11, a second and third in the Pole Vault by Omari Stephens '08, and Nate Peterson '12 to give the Alums the sweep in that event, both clearing 13-11 1/4, and a third by Scott Deering '89 in the Weight Throw at 36-4 3/4. The Alumni outscored the Varsity in the field events by a score of 35 to 19.</p>
              <p align="left">The story for the Men's Varsity was the freshmen, who accounted for 36 of the 87 points and winning four events, the horizontal jumps and the long sprints, outshining the very capable upper classmen in those events. States Lee '16 led the way with victories in the 200 and 400 events as well as anchoring the winning 4x200 Relay. Lee's times were 23.06 in the 200 and 51.68 in the 400. Arinze Okeke '16 took honors in the Long Jump at 21' and Carrington Motley '16 set a personal best in the Triple Jump to claim first with a jump of 44-5 1/4. Michael Kaba '16 was also solid for the freshmen, placing second in both the 60 and 60HH with times of 7.19 and 8.63 respectively. The other victories by the Varsity were well spread out among the team. Brian Djaja '13 ran a personal best in the 60HH, winning in 8.54. Michael DuPlessis '14 won the 60 in 7.17. Justin Bullock '14 led the 800 sweep with a 1:58.35 effort. Kyle Hannon '13 took the Mile in 4:22.23 and Benji Xie '15 earned the 5K victory in 15:17.77. The additional members of the winning 4x200 Relay were Tyler Singer-Clark '14, Josh Duncavage '13 and Duplessis.</p>
              <p align="left">Others participating were Gihan Amarasiriwardena '11 (5K), Obinna Anyanwu '09 (Triple Jump), Hemu Arumugam '10 (Mile), Patrick Barragan '08 (Pole Vault), David Blau '06 (60, 200), Jack Bourbonnais '09 (5K), Hazel Briner '11 (Pole Vault, Long Jump, Shot Put, Weight Throw), Logan Daum '11 (400), Caroline Figgatt '11 (Pole Vault), Martha Gross '12 (200, 400), Emily Hwang '09 (Pole Vault), Adeline Kuo '02 (60), Dan Kwon '02 (Pole Vault), Maggie Lloyd '12 (5K), Ben Mattocks '12 (Mile), Stephen Morton '10 (Long Jump), Richard Prevost '11 (Mile, 800), Crystal Ray (Shot Put), Ulzie Rea '07 (200, 400), Joe Roy-Mayhew '08 (5K), Stephen Serene '12 (Mile), Becky Stavely (Pole Vault), Anthony Teixeira '08 (Long Jump, Triple Jump), Zach Traina '05 (60, 200), Cat Tweedie '04 (Long Jump, High Jump), Chris Wong '08 (5K), Diana Zhu '12 (60HH, High Jump, Long Jump, Triple Jump) and Julia Zimmerman '09 (Weight Throw, Triple Jump, High Jump). Also in attendance, but not competing were Kirsten Aarsvold '11, Javit Drake '94, Tom Hansen '74, Kimathi Hardy '94, Dave Reshef '08, and Don Shobrys '75.</p>
              <p align="left">This was an excellent competitive start for the Engineers. Next week, MIT travels to Harvard for a Challenge Meet where MIT and Williams will be scored together vs. Army and Northeastern vs. Harvard and Brown. There will be no 400, 800 or 5K, but there will be a 500, 1000 and 3K. Only four entries per event per school are allowed so it will be an opportunity for some of the Tech athletes to have a weekend off from competing.</p>
