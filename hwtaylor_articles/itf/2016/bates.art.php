            <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">MIT Begins Intercollegiate Season Undefeated</span></p>
            <p align="left">January 16, 2016</p>
            <p align="left">Despite the snow storm that hit southern Maine, forcing MIT take an hour longer to arrive, which included one of the bus's inability to climb a very slippery hill near the campus, the Engineers competed well enough to leave Bates College with a solid victory by both the men and women.&nbsp;MIT's men competed against <a href="bates.res.php">Bates, Colby and The University of Southern Maine</a> (USM), outscoring the trio with a score of 266 points to 137 for USM, 131 for host Bates and 68 for Colby.&nbsp; The women scored 162 points despite a number of injuries, to 126 for Bates and 76 for Colby.&nbsp; The men's scoring format was 9-7-6-5-4-3-2-1 for the four teams while the women's scoring format was 7-5-4-3-2-1 for the three teams in the meet.</p>
            <p align="left">One of constraints of the meet was that no more than four individuals from a team could score in any particular event.&nbsp; With a depleted roster, MIT's women were concerned about their ability to win this meet.&nbsp; The premier event for the women is the Pole Vault.&nbsp; Being limited to only four scorers was bound to see MIT leaving a lot of points on the board, and that is exactly what happened.&nbsp; Led by Cimmy Virdi '16, who did not enter the competition until everyone was finished vaulting, and who won the event with a new facility record of 12-5 1/2, MIT earned the top seven places while still going from a short approach.&nbsp; Tech dominated most of the field events, scoring 85 points out of a possible 114, and not scoring more was largely due to only having one triple jumper.</p>
            <p align="left">Also sweeping the top four places for the ladies in the 20 lb. Weight Throw, led by Jackie Vahey '17, winning with a throw of 50-2 3/4.&nbsp;Vahey returned afterwards to win the Shot Put with a put of 38-6 1/4, leading her teammates to&nbsp;a 1-2-4 finish.&nbsp; Nneoma Okonkwo '18 led the long jumpers to a 1-3-4-5 finish with her 17-6 3/4 effort.&nbsp;&nbsp;The other winner for MIT in the field events was Natalie Alper '17 in the High Jump, clearing 5-2 1/2 for the victory.</p>
            <p align="left">On the track, where the Engineers have much fewer entries there were two particularly dominant runners for MIT.&nbsp; Hannah Chen '18 won the 200 in 26.27, the 400 in 60.43 and earned second place in the 60HH in 9.43.&nbsp; Dominating from start to finish, Maryann Gong '17 took the Mile run in 5:04.53 and then returned in the 800 to do the same thing in 2:25.02.</p>
            <p align="left">Other winners on the track were Bailey Tregoning '19 in the 1000 with a time of 3:05.96 and Mary Eccles '18 in the 3K in 10:35.30.</p>
            <p align="left">For the men, their depth and quality left no doubt as to whether or not they would win the meet, yet the excitement and desire to perform at a high level were still present.&nbsp; There were two double winners for MIT and they were Michael Kaba '16 and Colin Godwin '17.&nbsp; Kaba not only won the 60HH in 8.50 and 200 in an impressive 22.49 on the flat track, but also took second place in the 60 in 7.20.&nbsp; Godwin was a very dominant runner on this day, first taking the Mile Run in 4:23.92 after a pedestrian start of 68 seconds for the first 400 and then the 1000 in 2:35.75.&nbsp; In three of the four cases the winners had tremendous support.&nbsp; Kaba's teammates placed 4, 5, 6,&nbsp; and 8 in the 200 while Godwin's came through with a 2-3 finish in support of Godwin's win in the Mile and 4-5 in the 1000.</p>
            <p align="left">Arinze Okeke '17 won the Long Jump with a leap of 22-0 1/2 and was held out of the Triple Jump just for the sake of recovery in what will be a long season.&nbsp; Billy Ruschel '18 took advantage of Okeke's absence and won the Triple Jump at 45-0 1/4.</p>
            <p align="left">On the track, in&nbsp;addition to the four previously mentioned victories, States Lee '16 ran an impressive 400 with his 50.35&nbsp;win coming out of lane two&nbsp;with very talented runners in lanes five and six.&nbsp; Aidan Gilson '19 led his fellow freshmen to a 1-3-6 finish with his 8:41.24 3K win.&nbsp; Gilson and Josh Rosenkranz '19 (8:45.20) were trying to set a new MIT freshman record, but fell short of the 8:38.39 record held by Allen Leung '15.</p>
            <p align="left">The final victory was one of the most exciting as an all freshman team took first place honors in the Distance Medley Relay.&nbsp; Nathan Munet led things off with a 3:19.6 1200 leg, putting his team about 20 meters behind USM.&nbsp; Talla Babou ran a fine 53.2 400 leg to keep Tech within 20-25 meters.&nbsp; Chris Mutty took the baton, and despite not gaining any ground in the first two laps on the 200 meter track, he was able to then close the lead down to less than 10 meters with his 2:02.4 effort.&nbsp; Daniel Weiss anchored with the 1600 leg, owning the race from behind.&nbsp; As the pace slowed in the middle of the race, Weiss prepared to take control, doing so with 400 remaining.&nbsp; His final 400 in 65 gave him a 4:32 split and the team a 10:47.97 victory.</p>
            <p align="left">Next Saturday, MIT heads back to Maine to compete at Bowdoin&nbsp;College against Bowdoin, Coast Guard Academy, Connecticut College, Keene State, Merrimack, Plymouth State, Tufts and USM.&nbsp; This will be a challenge for both the men and women on a fast 200 meter flat track.</p>
