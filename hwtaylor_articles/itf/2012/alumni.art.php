              <p style="font-size: 12px"><strong><span style="">VARSITY DOMINATE ALUMNI IN ANNUAL INDOOR CONTEST</span></strong></p>
              <p style="font-size: 12px; font-weight: bold">2010-11 Women's Program Presented Vercauteren Award</p>
              <p align="left">January 7, 2012 </p>
              <p align="left">The Has-Beens came into town ready to make some noise in the annual Indoor Track and Field <a href="alumni.res.php">Alumni/ae Meet</a>, but the talent and depth of the Varsity squads was just too much as the men defeated the old guys 87 to 30 and the women took first place honors by a score of 92-30.</p>
              <p align="left">Before the start of the meet, Sam Seemes, the CEO of the United States Track &amp; Field, Cross Country Coaches Association (USTFCCCA), presented the Vercauteren Award to the Women's program for earning the distinction of Best Program in the Country for Division III by virtue of their combined NCAA Division III finishes of third in Cross Country, third in Indoor Track &amp; Field and fourth in Outdoor Track &amp; Field.</p>
              <p align="left">Josh Duncavage '13 won three events, running 6.61 in the 55 meter dash, 8.05 in the 55HH (both PR's), and 22.92 in the 200, to lead the current day Engineers to victory. Supporting Duncavage in his victories were Michael DuPlessis '14 in the dash, running 6.65 for second place and a nice personal best; Brian Djaja '13 in the 55HH with a third place 8.12; and Tyler Singer-Clark '14 running 23.00 to edge out Zach Traina '05 for second place in the 200.</p>
              <p align="left">The Varsity's other big point man was freshman, Sheldon Trotman. Trotman started by taking the High Jump with a personal best of 6-3 1/4. He then went over to the Long Jump and won that on his first jump, a 21-6 effort to edge out Stephen Morton '10. In the Triple Jump, Morton turned to tables with a 45-4 1/4 leap, forcing Trotman to settle for second in 42-11.</p>
              <p align="left">While he did not score a lot of points, CK Umachi '12 took down Uzoma Orji '06 in the 35lb. Weight Throw with a 62-7 1/4 throw to move up to second place on the MIT top 10 list. Umachi had five throws over 60', with all of them right down the middle. Umachi also earned a third place in the Shot Put, losing to Orji and former teammate, Mitch Kelley '11, with a 43-10 3/4 put.</p>
              <p align="left">The Varsity won 11 of 13 events, showing quality as well as depth. Nathan Peterson '12 took the Pole Vault at 15-5 despite a very short approach. Mac Gager '14 led teammates Singer-Clark and Nick Diamantoni '15 to a 1-2-3 sweep in the 400 from the slow section with an even split 52.63. Pat Marx '13 also led a 1-2-3 sweep, winning the 800 in 2:01.02 over Chadd Kiggins '15 and Jared Forman '13. Justin Bullock '14 continued the trend by leading Ben Mattocks '12 and Rick Paez '15 to a sweep in the Mile Run in a pedestrian 4:29.33. The final victory was earned by Dan Harper '12, who yes, also led a sweep of Roy Wedge '14 and Benji Xie '15 in the 5K in 15:27.14.</p>
              <p align="left">In addition to Orji's and Morton's 16 points, Omari Stephens '08 earned second places in both the 55HH in 8.09 and the Pole Vault in 14-5 1/4. Kelley chipped in three points for second place in the Shot Put, throwing 44-1 1/4 behind Orji's 55-3 1/2. Craig Mielcarz '03 added two points for a tie for second place in the High Jump when he cleared 6-1 1/4. Traina also earned third place in the 55 dash, contributing two points. Chris Bateman '07 added a point for his third place finish in the Long Jump.</p>
              <p align="left">In the women's meet, Portia Jones '12 led the way for the Varsity as she duplicated Duncavage's victories with first place efforts in the 55HH, 55 Meter Dash and 200 Meter Dash. In the 55HH she led Jacqui Brew '14 (8.76) and Jackie Konopka '14 (9.14) to a 1-2-3 sweep with her 8.48 effort. In the 55 Dash, she again led Brew (7.62) as well as Madeleine Bairey '15 (7.64) to a sweep with her 7.51 performance. The 200 was more of the same as Jones's 26.20 blew everyone away, including  teammates, Jamie Simmons in second place in 27.20 as well as Bairey in third in 27.48.</p>
              <p align="left">Brew added points by winning the Long Jump in 16-5 3/4 and earning a second place in the Triple Jump in 33-8 3/4 behind freshman teammate Michelle Johnson's  34-1 3/4. Kristine Bunker '14, not only won the High Jump in an MIT record of 5-3 1/4, but she also added a third place in the Shot Put in 29-4. Bunker led the sweep in that High Jump competition over teammates Preethi Vaidyanathan and Konopka. Simmons also earned five points for the victory in the 400 in 60.56. Claiming victory in the 20 lb. Weight Throw, freshman Ashley Wheeler threw a respectable 33-5 3/4.</p>
              <p align="left">MIT added two more sweeps in the running events. In the 800, Cindy Huang '15 took first place honors in 2:26.65 over teammates Margaret Coad '15 and Stephanie Marzen '15 for a freshman sweep. Elaine McVay '15, held on to win the 5K and lead the sweep despite a foolish 37 second first 200. McVay's overall time was 18:15.88 while teammate Dacie Manion '15 was close behind in 18:22.20 and Maggie Lloyd '12 closed out the sweep in 18:52.55.</p>
              <p align="left">The biggest highlight for the Alumnae came in the Pole Vault where they earned a sweep of their own as Karin Fisher '11 led Hazel Briner '11 and Emily Hwang '09 with her 11-11 3/4 performance. Briner added a victory of her own, taking the Shot Put in 31-11 1/4. She also scored three points for a second place in the 20 lb. Weight Throw, one point for her third place in the Long Jump and ran the key second leg of the 4x200 that gave the Has-Beens the lead for good. Fisher added a third place finish in the Triple Jump at 32-11.</p>
              <p align="left">Andrea Bradshaw '09 earned 7 1/4 points for the Alumnae with a very unconventional triple. AB started with a convincing Mile Run victory in 5:13.53, came back two events later to earn a very close third in the 400 in 62.26 and then anchored the winning 4x200 team to victory in the final event of the meet.</p>
              <p align="left">Other competing alums who did not score were: Akin Aina '95, Gihan Amarasiriwardena '11, Obinna Anyanwu '09, Hemagiri Arumugam '10, Patrick Barragan '08, David Blau '06, Jack Bourbonnais '09, Logan Daum '11, Eric Khatchadourian '06, Nick Leonard '11, Terry McNatt '87, Peter Mulligan '08, Jake Ruzevick '09, Bill Singhose '90, Ben Schmeckpeper '05, and Victor Williamson '04 for the men, and Kirstin Aarsvold '11, Caroline Figgatt '11, Lynn Jepsen '11, Adeline Kuo '02, Crystal Ray '04, Alisha Schor '07 and Becky Stavely '11. Those in attendance in a supporting role were Hana Adaniya '07, Javit Drake '94, Alina Gatowski '11, John &quot;Bo&quot; Light '96, Dave Reshef '08, Joe Roy Mayhew '08, Don Shobrys '75 and Bob Walmsley '84</p>
              <p align="left">Next Saturday, the Engineers play host to Bates College and Colby College starting at noon.</p>
