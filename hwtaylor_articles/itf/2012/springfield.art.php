              <p style="font-size: 12px"><strong><span style="">MIT CLAIMS VICTORIES AT SPRINGFIELD INVITATIONAL</span></strong></p>
              <p align="left">January 21, 2012 </p>
              <p align="left">The men and women from MIT dominated the competition at the <a href="springfield.res.php">Springfield Invitational</a>. The men claimed eight of 12 running events to easily outscore the other eight teams, scoring 176 to second place Springfield's 100. UMass Dartmouth was third with 93 points, followed by 67 for Tufts, 42 for Ramapo, 30 for Westfield State, 18 for Wheaton, 16 for Coast Guard and 13 for Keene State.</p>
              <p align="left">The women took first place honors in seven of the running events to score 163 points to 89 for Tufts, 75 for Wheaton, 68 for Springfield, 47 for Keene State, 45 for Ramapo, 41 for UMass Dartmouth, 17 for Westfield State, 6 for Coast Guard and 4 for Mt. Holyoke.</p>
              <p align="left">Josh Duncavage '13 was the dominant scorer for MIT, taking the 60 meter hurdles in an ugly 8.74, tying for the victory in the 60 meter dash in 7.08 and earned third in the 200 even though he won the final section in 23.07. </p>
              <p align="left">Other victories for the men were claimed by Tyler Singer-Clark '14 in the 200 in 23.05, Pat Marx '13 in the 600 in 1:22.98, Gil O'Neil in the 800 in 2:00.05, Justin Bullock '14 in the 1000 in 2:36.57, the quartet of Mac Gager '13, Nick Diamantoni '15, Jacob Jurewicz '14, and Marx winning the 4x400 in 3:28.35, and O'Neil, David Way '13, Jared Forman '13, and Stephen Serene '12 taking the 4x800 honors in 8:16.67. In the field events, CK Umachi '12 claimed the sole MIT victory, taking the Weight Throw with a mark of 60-2 1/2.</p>
              <p align="left">The men's meet was close, with MIT up by two over UMass Dartmouth, when Marx led a 1-2-3-4 sweep in the 500 for 28 points in the 10-8-6-4-2-1 scoring format. In the next three events, Tech took 1-2 in the 800, 1-4-6 in the 1000 and 1-3-4 in the 200 to put the meet away.</p>
              <p align="left">The women were led by Portia Jones '12 with her victories in the 60HH and the 200 as well as her second place in the 400. In the hurdles, Jones went up against national rival, Didi Jusme '12 of Wheaton College. At last year's NCAA Division III Indoor Track &amp; Field Championship, Jones had the second fastest time in the trials of the hurdles, earning a seed in the fast section of the two section final. Due to the host track only having six sprint lanes (something that should never happen in a national championship), both the 55HH and 55 Dash were run as two section finals. Jones won the fast section, running 8.11, the fastest time in the country for Division III that year. In the second, and supposedly, slower section, the eventual winner ran 8.10 and Jusme ran 8.11, getting the nod for second place over Jones when the two photo finishes were taken to the 1000th of a second. In the outdoor NCAA Division III Championship, Jones finished second while Jusme did not qualify for the finals. Nevertheless, the rivalry was alive and well. In the trials, Jusme ran the fastest time in the country thus far this year, an 8.86. In the finals, Jones sensed Jusme next to her going into the second hurdle and exploded forward, pulling away to win in 8.82 to 8.93 for Jusme.</p>
              <p align="left">MIT had two varsity records in the meet, Jackie Brew '14 reset her own varsity record in the Long Jump when she won with a mark of 17-7. In the 600, Jamie Simmons '12, was determined to get the varsity record, and she did, despite going out in 30.4 for the first 200. Simmons clocked 1:36.57 for the victory. Other victories were earned by; Cindy Huang '15 in the 800, running 2:24.36, Tania Morimoto '12 in the 5,000, running 18:22.96, the foursome of Jones, Brew, Simmons and Maddie Bairey '15 ran 1:47.13 in the 4x200 for a solid victory and the quartet of Alexandra Taylor '14, Margaret Coad '15, Stephanie Marzen '15 and Huang won the 4x800 in 9:47.21.</p>
              <p align="left">The women's meet was never in doubt as the Engineers put points up on the board in every event with the exception of the Shot Put and the 4x400, which they did not enter.</p>
              <p align="left">Next week, MIT will split up their team between two non-scoring meets. The runners will travel across the river on Friday for the women and Saturday to Boston University to run on one of the fastest banked tracks in the country, while the field event performers and all pure hurdlers and short sprinters will be at the MIT Invitational on Saturday.</p>
