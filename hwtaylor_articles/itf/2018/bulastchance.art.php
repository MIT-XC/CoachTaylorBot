            <h3 style="font-size: 12px">Williams, Washington Lead New NCAA Qualifiers For MIT<strong></strong>            </h3>
            <p align="left">February 25, 2018</p>
            <p align="left">The MIT Track &amp; Field team took an extra day to recover from last week's New England Division III Championships, competing today at the <a href="bulastchance.res.php">Boston University Last Chance Qualifier</a>, primarily a professional and Division I competition.&nbsp; The extra day of rest seemed to pay off as numerous competitors had personal bests, and more importantly, competed very well.</p>
            <p align="left">Leading the way were the 800 runners.&nbsp; Katie Williams '21, already owning the freshman records in the 600 and 1000 meter races for MIT, toed the line with teammates&nbsp;Margaret Trautner '20 and Bailey Tregoning '19, all looking for a&nbsp;time fast enough to make the list of the top 17 performers in NCAA Division III.&nbsp;&nbsp;Williams was on the inside, so she knew she had to&nbsp;get out or get boxed in.&nbsp; She succeeded&nbsp;and led the chase pack through the 200 in 32.&nbsp; Trautner and Tregoning were within striking distance with the same splits.&nbsp; The pace slowed a bit on the third 200 to 1:38 and Tregoning set up to pass.&nbsp; Entering the final 200 Tregoning went past, but Williams did not give up.&nbsp; On the back stretch Williams charged past and made a kick to the finish, running a freshman record and the fourth fastest time in Division III at 2:12.23.&nbsp; Trautner screamed home in a PR of 2:14.08, the number 15 time in Division III.&nbsp; Tregoning faded due to the effects of a lingering cold, but still ran a season best 2:14.86.</p>
            <p align="left">The men were up next and Simon Alford '20 and Nathan Munet '19 were both hoping for NCAA qualifying times in the 800.&nbsp; Munet went out strong, splitting 27 for the first 200.&nbsp; Munet had to cut his momentum going into the second lap as he mis-timed a pass on the inside.&nbsp; He recovered quickly, but settled in through the second lap before moving into second at the end of the quarter.&nbsp; He took another 100 meters deciding whether or not to take the lead, but then tried to bring it home.&nbsp; Alford moved into second with 200 to go and took the lead in the final 100, winning in a personal best 1:53.70.&nbsp; Munet also ran a personal indoor best, finishing third in 1:54.08.</p>
            <p align="left">In the Pole Vault both Makenzie Patarino '19 and Liam Ackerman '21 had personal bests.&nbsp; Patarino cleared 11-10 to come within four inches of what it will probably take to make it to Nationals.&nbsp; Jacqueline Ahrens '21 cleared 12-10 but did not have good attempts at 13-0.&nbsp; Kari Stromhaug '19 cleared 12-2.&nbsp; While neither PR'd, both showed they are ready to compete at Nationals as they are ranked fourth and fifth in the country for Division III.&nbsp;&nbsp;Ackerman was the top male vaulter for Tech, clearing 15-3.&nbsp;&nbsp;The freshman is approaching teammate Scott Cameron's freshman record of 15-5 1/2.&nbsp; Raja Rajcic '20 cleared 14-3 1/4.</p>
            <p align="left">Continuing with the field events, Chris Washington '20 rebounded from his failure to score in last week's championship and jumped 6-9, a new personal best, and currently 14th on the NCAA list.&nbsp; </p>
            <p align="left">In the Long Jump, Nneoma Okonkwo '18, already high on the NCAA list, moved up a few spots to sixth by jumping 18-10.&nbsp; Haley Strouf '18 did not improve on her 12th seed on the NCAA performance chart.&nbsp; Mac Gregg '21 jumped 22-5 1/2, but was no where near the 23-6 or so it will take to make it to Nationals.</p>
            <p align="left">The Triple Jumpers also saw some improvement.&nbsp; Jasmine Jin '20 jumped 37-8 3/4 for a new personal best.&nbsp; The sophomore needs another four inches or so to make it to Nationals.&nbsp; Billy Ruschel '18, currently ranked eighth in Division III after his 48-4 jump, joins teammate Yorai Shaoul '21 (third) on the list.&nbsp; Ryan Prinster '18 had his third strong outing in a row, jumping 46-5.</p>
            <p align="left">In the Weight Throw, neither Ariela Slutsky '18, nor Jon Fakkema '19 had personal bests, but both threw well.&nbsp; Slutsky, currently 14th on the list, threw 55-2 1/2 while Fakkema threw 55-5 1/2.</p>
            <p align="left">The 200 Meter race is one where MIT was hoping to do some damage.&nbsp; While both Michelle Menkiti '20 and Benton Wilson '21 ran indoor PR's, neither moved up on the list significantly.&nbsp; Menkiti ran 25.31, which puts her about .1 second away from going to Nationals.&nbsp; Wilson ran 22.08, which leaves him about .3 away from making the NCAA top 15.&nbsp; Tre Albritten '18 also raced, but drew lane three, giving him very little chance to improve on his 21.93 from earlier this season.&nbsp; He ran 22.21.</p>
            <p align="left">Chris Sweeney '18 tried to improve on his performance in the 60HH.&nbsp;He got out of the blocks and to the first hurdle very well.&nbsp;&nbsp;He just did not seem to have the sprint speed between the hurdles as he&nbsp;ran 8.51, not quite his best.</p>
            <p align="left">The final event for the Engineers was the 3K.&nbsp; Marissa McPhillips '20 and Katie Bacher '20 were trying to hit a national qualifying performance, giving them another option at the NCAA Division III Championship in two weeks.&nbsp; Looking for 5:10 pace at the 1600, both runners nailed it right on.&nbsp; Running pretty much alone up to that point, the pair started catching some fading runners in the fast section.&nbsp; McPhillips ran a 77 and then a 79 and with 300 to go collapsed to the side of the track, unable to finish.&nbsp; Bacher faded after 2K, more than likely not having&nbsp;a solid racing goal to keep her engaged when the going got tough.&nbsp; She finished in 9:57.69, far from what she is capable of running.&nbsp; McPhillips is fine and ready to race at Nationals.</p>
            <p align="left">Next week MIT will visit Tufts University for their Final Qualifier and their truly last opportunity to make the NCAA Division III Championship being held in Birmingham, AL, March 9-10.            </p>
