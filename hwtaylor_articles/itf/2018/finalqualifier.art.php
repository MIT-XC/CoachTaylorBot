            <h3 style="font-size: 12px">Engineers Led by Field Events in Tufts Final Qualifier<strong></strong>            </h3>
            <p align="left">March 3, 2018</p>
            <p align="left">For a final opportunity to qualify for and participate in the NCAA Division III Indoor Track &amp; Field Championship, those student-athletes with a chance to reach those standards traveled to Tufts University for the <a href="finalqualifier.res.php">NCAA Final Qualifier</a>.&nbsp; </p>
            <p align="left">MIT wasted no time in qualifying additional athletes to those who had already secured the NCAA Division III standard, which was top 17 in each event for women, top 15 in each event for men and top 12 in the relays for both genders.&nbsp;&nbsp;In the Men's High Jump Alec Reduker '20 returned to competition and proceeded to make up for lost time by clearing 6-9 1/2 on his ninth attempt in the competition, moving him to #15 on the NCAA list.&nbsp;&nbsp;Teammate Chris Washington '20, who had been ranked 14th entering the competition&nbsp;with a 6-9 performance the week before, was unable to clear 6-9 1/2.&nbsp; He was also passed by two other jumpers at other Final Qualifiers elsewhere in the country, moving him two places out of qualification.&nbsp; His only hope of making the field would be if two jumpers do not enter for some reason or another.</p>
            <p align="left">In the Women's Long Jump, Haley Strouf '18&nbsp;moved up one place on the list to 12th by improving her mark by one centimeter to 18-7.&nbsp; Teammate Nneoma Okonkwo '18&nbsp;did not improve her 18-10 mark and is steady in seventh place&nbsp;on the list.</p>
            <p align="left">Ariela Slutsky '18 got her head right&nbsp;and competed to her potential, throwing an MIT record 57-9 1/2 in the Weight Throw, moving all the way up to ninth on the NCAA list.&nbsp; Jon Fakkema '19 was unable to throw a&nbsp;better throw than before, managing a 54-1 3/4.</p>
            <p align="left">Staying with the throws, Cody Bratten '18 threw a huge indoor personal best, moving to number nine on the MIT all-time list with his 48-0 throw.</p>
            <p align="left">In the Women's Triple Jump, Elizabeth Weeks '21 set a freshman record for the Engineers, jumping 37-5.&nbsp; Although this was a season best it is far from enough to qualify for next week's championship.&nbsp; Jasmine Jin '20 just missed a personal best when she jumped 37-8, also short of a qualifying mark.&nbsp;&nbsp;Ryan Prinster '18 jumped 45-8 1/2, failing to improve on his 46-11 1/2 best.&nbsp; Neither Yorai Shaoul '21 nor Billy Ruschel '18, ranked third and ninth respectively, jumped in this competition.</p>
            <p align="left">Neither Makenzie Patarino '19, nor Liam Ackerman 21 were able to improve their marks and make the NCAA qualifying list.&nbsp;Patarino cleared 11-4 1/4, but was unable to join teammates Jacqueline Ahrens '21 and Kari Stromhaug '19 who are ranked fourth and fifth&nbsp;going into the championship.</p>
            <p align="left">The running events did not go quite as well for Tech.&nbsp; To start things off, the Men's Distance Medley Relay (DMR), entered in the meet, but not planning on racing since they were ranked third in the country on Friday morning, woke up to discover they were eighth on the list with yesterday's meets, and with the talented field ahead at Tufts, could easily fall out of the top 12.&nbsp; Although they were not as mentally prepared as they should have been knowing there was a chance they would race, they needed to try to improve on their banked track 9:54 performance.&nbsp;&nbsp;The leadoff 1200 leg was Simon Alford '20, having an amazing season.&nbsp;&nbsp;Alford positioned himself right off the lead runner and got pulled out a little fast.&nbsp; The first 200 was 29.0 and the 400 59.4.&nbsp; Alford runs better coming from behind, so being up front got the best of him mentally as he fell to a 3:08.8 when all was said and done, putting his team well out of contention.&nbsp; Benton Wilson '21 ran a well-paced 400, dropping a 49.3 to move Tech up one spot.&nbsp; Nathan Munet '19 ran an excellent 800, going out hard and holding on for a 1:53.8 to bring the Engineers a bit closer.&nbsp; Aidan Gilson '19 closed ground quickly, moving up to the lead pack with a 2:03.8 first 800.&nbsp; However, he made the mistake of settling in once he caught them instead of keeping up his momentum and moving past.&nbsp; The slower pace took it out of him physically and mentally as he struggled home in 4:11.6.&nbsp; Tech was passed by five teams, pushing them back to 13th on the list and out of luck unless a team does not enter.&nbsp; The women's DMR team, ranked eighth and  seemingly a solid lock on qualifying, elected not to race.</p>
            <p align="left">Okonkwo, still trying to get her sprinting back, entered the 60 Meter Dash and improved her season best significantly.&nbsp; Having run 8.02 entering the competition, the senior ran 7.98 in the trials and then 7.90 in the final.</p>
            <p align="left">Both Wilson and Michele Menkiti '20 did well in the 200, just not well enough to make it to Nationals.&nbsp; Both drew lane four, better than lane three, but far worse than lane six.&nbsp; Wilson ran 22.40 and Menkiti 25.85 on the flat track.</p>
            <p align="left">The 800 was supposed to be a big event for MIT, but with Alford and Munet both running the DMR first, neither had their best game for the open 800.&nbsp; Alford was in the fast section based on his 1:53.70 previous performance, but faded badly after a 56 first 400.&nbsp; Munet raced much better, but his 1:55.89 was not fast enough to make the top 17 list.&nbsp; Katie Williams '21, ranked fourth on the NCAA list was not racing, but Margaret Trautner '20, ranked 15th entering the competition was, as was Bailey Tregoning '19, ranked 23rd going into the race.&nbsp; Trautner wasted energy during the race, reacting to various moves and failed to mount her usual kick off the 1:41 600.&nbsp; Her final 2:16.82&nbsp;was&nbsp;a bit slower than her 2:14.08 banked track performance.&nbsp; Tregoning ran a perfect race through 650 meters, then just gave up, fading to 2:20.97.&nbsp; At the end of the day, Trautner was sitting in 18th position, but with Marissa McPhillips '20 ranked ninth, but not racing the 800, Trautner is likely to get in.</p>
            <p align="left">Katie Bacher '20 wanted to add the Mile to her DMR repertoire and the pacer who was supposed to go out in 2:28 would help her get that done.&nbsp; What the sophomore did not plan on was the pacer going out too fast (72.0), therefore leaving her to lead the entire pack.&nbsp;&nbsp;Leading got to her mentally and she panicked after a 2:30 800, causing her to fade out of contention.</p>
            <p align="left">Others not racing who have qualified were McPhillips in the Mile (6th) and possibly the 3K (20th), Gilson in both the Mile (5th) and 3K (10th) and Mary Eccles '18 in the 5K (6th).</p>
