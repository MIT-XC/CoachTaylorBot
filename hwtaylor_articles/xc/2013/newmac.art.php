              <p><span class="headline">MIT Men and Women Cross Country Dominate NEWMAC Competition</span></p>
              <p align="left">November 3, 2013</p>
              <p align="left">Going into this weekend, it was assumed the MIT Cross Country teams would continue to dominate the <a href="newmac.res.php">NEWMAC Championship</a>, hosted by Babson College at Franklin Park. After all, the men had never lost the championship, winning 15 in succession since the start of the conference in 1998. The women, late arrivals to that level of success, were going for their seventh in a row, having not lost since 2007. The NEWMAC Conference has some very good runners and at least a couple of teams that should be nationally ranked. Therefore, the Engineers were not taking anything for granted.</p>
              <p align="left">The harriers from MIT were determined to run aggressively on the damp course in order to test their ability to have a tight delta while pushing the limits. Of course, this strategy also makes it easy for opposing teams to follow along letting the leaders do the work, basically becoming targets. Both the men and women pulled the strategy off without error. The women won with a low score of 23 points, led by Elaine McVay's 18:17 wire to wire victory and a delta one through five of :33. Second place went to Wellesley College with 70 points. The men followed suit with an even better score of 18 points, led by Roy Wedge's 25:08 first place finish and a one through five delta of 44 seconds. Second place went to a very competitive Coast Guard team, scoring 44 points.</p>
              <p align="left">The conditions were wet from the drizzle that had fallen over the past 12 hours, but the temperature ideal at 45 degrees. At the gun Elaine McVay '15 went to the lead, hitting the mile mark is a cautious but steady 5:41. Her teammates (Maryann Gong '17, Brooke Johnson 'G and Alexandra Taylor '14) were right at the front of the chase group at 5:45. Christina Wicker '17, Louise van den Heuvel '14, Nicole Zeinstra '16 and Cindy Huang '14 were right behind at 5:47. There were one or two from a few of the teams up at the front, but for the most part it was all MIT. By mile two, McVay (11:37) had opened up a seven second lead on Wicker, who was having the race of her life. The rest of the MIT harriers were quite a bit back between 11:50-12:09. In the final full mile, McVay was relentless, putting down another 5:56 to stretch the lead to 19 seconds and no one closed that gap by the 5K+ (a little longer than 5K) finish in 18:17. Gong earned Rookie of the Year conference honors with her second place 18:37. Wicker held onto fifth place in 18:44. Johnson closed well for a sixth place 18:45. Taylor ran 18:50 for ninth and van den Heuvel ran her best race to date, finishing 10th in 18:59. Top 14 finishers earn All Conference honors.</p>
              <p align="left">The men also took an aggressive approach and it was a good thing, because Coast Guard was not going to give this away. Roy Wedge '14 and Benji Xie '15 took the pace out, but not as fast as was planned. The field seemed content with letting them lead so they were perhaps a little too cautious in the first mile, splitting 4:59 on the flat first mile. Matt Deyo '16, Rory Beyer '17, Allen Leung '15, Matt Jordan '15, Ken Leidal '17 and Justin Bullock '14 were close at 5:03 and another bunch (Ian Tolan '16, Matt McEachern '17, Rick Paez '15, Brian Gilligan '17, Eric Safai '14, Nick Matthews and Jay McKenna '14) were just behind them at 5:07. At the two mile mark Wedge and Xie held an eight second lead on  Beyer, Deyo and Leung. Two Coast Guard runners were trailing and then McEachern, Bullock and Jordan followed. As well as Tech raced, they took the foot off the gas in the third mile, which let Coast Guard back in the race. Wedge had begun to pull away from Xie. Deyo, Beyer and Leung were now 11 seconds behind, but Gary Ezzo of Coast Guard and Rob Ellinger of WPI  had joined them. By the four mile mark, Wedge put another eight seconds on the field. Xie faded back 20 seconds as his teammates caught him. Ezzo was now some 30 meters back and seemingly had given up. Hollinger was way back. Wedge added another four seconds to his lead as he won in 25:08. Deyo took second in 25:31. Ezzo must have taken advantage of Xie falling off the back of the group and found the energy to reengage in the race, preventing the sweep by MIT. Beyer took fourth in 25:39 to earn Rookie of the Year honors. Leung claimed fifth place and Xie sixth to close out MIT's scoring. Ian Tolan, in his first race of the year showed his competitiveness as he took 10th place, finishing sixth for the Engineers in 26:08. McEachern and Jordan, finishing 11th and 12th respectively, also earned All Conference honors.</p>
              <p align="left">MIT's next competition is in two weeks at the NCAA Division III New England Region Championship. The top two teams receive automatic bids to the NCAA Division III Championship while other teams may be selected as at-large selections.</p>
