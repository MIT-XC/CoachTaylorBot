              <p><span class="headline">Men and Women Both Start Poorly - With Very Different Results at NEICAAA Championship</span></p>
              <p align="left">October 12, 2013</p>
              <p align="left">The first championship of the season, albeit one that meant nothing with regard to advancement or the end of the season results, was the <a href="neicaaa.res.php">NEICAAA Championship</a> held at historic Franklin Park in Boston. The championship, one of the oldest in the country, dating back to 1912, matches Division I, II and III teams throughout New England. The weather was near perfect for cross country with temperatures in the low 60's with an overcast sky throughout the meet.</p>
              <p align="left">Both the men's and women's teams were ranked fourth in the country for Division III going into the meet. The women raced first. Unfortunately they were in box 39 of 43, nearly as far to the right of the course as possible with a right hand turn slightly to their left only 300 meters into the race. Not only would they have to get out well, they would have to move to the left significantly to avoid getting shut down at the turn. They moved to their left successfully but got caught too far back with at least 150 runners ahead of them as they hit the turn.</p>
              <p align="left">The women from MIT must have moved pretty hard because when they hit the mile mark they had moved up at least 50 individuals as Elaine McVay '15 was up to 32nd place and a pack of Tech runners, including Kaitlin Allen '14, Alexandra Taylor '14, Maryann Gong '17, Kali Benavides '15 and Stephanie Marzen '15 were in the top 100, but barely so. The Engineers continued to work throughout the second mile as McVay moved up to 28th while Allen and Gong moved into the top 50 with Taylor close behind. Unfortunately, while Benavides and Marzen continued to move up they were now 26 and 30 seconds behind McVay respectively. Louise van den Heuvel '14 had an excellent second mile, moving up to 125th place, impressive after being near last at the first turn of the course.</p>
              <p align="left">The third mile of the 5K course continued in the same fashion. McVay, who undoubtedly paid for her 2:43 first 800 and 5:34 first mile, was continuing to pass people as she finished in 23rd place in 18:14, the first Division III runner. Allen, running perhaps the smartest race of all MIT runners, moved all the way up to 33rd place in 18:25. Taylor also had an outstanding race, moving past Gong in the final 150 meters, finishing in 45th place in 18:37 to Gong's 18:39 in 50th place. The normally dependable Benavides and Marzen let various thoughts get to them, taking them out of the race mentally. Benavides ran 18:52 for 72nd place to give MIT a :38 second delta. Marzen slipped to 113th in 19:13. van den Heuvel fell back to 140th, running 19:29. The Engineers finished in seventh place, the second Division III team behind Middlebury College by 31 points.</p>
              <p align="left">In the women's sub-varsity race, Cindy Huang '15 led MIT to a 5th place finish, running a PR 19:19 in 18th place. Brooke Johnson 'G in her first race of the season after being out since last May, showed she should be back to top form in a few more weeks as she finished in 21st in 19:26. Also scoring for MIT were Christina Wicker '17 in 34th in 19:41, Heather Sweeney '16 in 52nd in 19:53 and Shivani Kaushal '15 in 69th place in 20:05.</p>
              <p align="left">The men were hoping to test their skills against the best in New England after already having defeated all the Division III teams in the past two meets. Similar to the women, the men were far to the right in box 28 of 41 boxes. While they did not get out really well, they were in pretty good position in the middle of the pack as they hit the first turn. However, between that point and the mile mark, the team ran more for position than for feel of pace. Unfortunately, for the first time in recent memory the leaders ran a conservative pace, hitting 4:44 at the mile mark, the exact pace Benji Xie '15 and Roy Wedge '14 were hoping for. Coach Halston Taylor had instructed them that a 4:45 first mile would usually have them around 50th or so at the mile mark. They must have been confused about their position and pace because they hit the mile between 4:57 and 4:59 between 90-100th position in the field.</p>
              <p align="left">At the two mile mark, Rory Beyer '17, Wedge and Xie were in 52nd-54th places in 10:07. Matt Deyo '16 was back at 10:14 around 85th place with Allen Leung at 10:17 in about 100th place. The third mile saw Beyer start to lose places, while Wedge and Xie moved all the way to 32nd place. Leung caught up to Beyer while Deyo stayed in 74th place. Justin Bullock '14 and Ken Leidal '17 were moving up, but had started to so far back that they were still in the 130's at 15:52 and 15:56 respectively. Wedge dropped Xie in the fourth mile, pouring on a 5:04 effort to move up to 26th place while Xie fell back to 36th. Leung moved to 49th but Deyo fell back to 89th and Beyer was back in the 90's. In the final, but somewhat short mile, Wedge dropped a 4:36 to move up to 15th place in 24:54. Leung finished second for the Engineers, placing 43rd in 25:27. Xie held on for 45th in 25:28. Deyo came back a bit, indicating a mental lapse in the middle of the race, finishing 74th in 25:48. Bullock ran a 4:48 to move up to 96th, finishing as MIT's fifth man in 25:59. Tech finished in ninth place, the fourth Division III team, 59 points out of fifth place and the first Division III team. A normal race by Beyer and Xie would have been a delta of 12 and four seconds from Wedge respectively, which would have been a 94 point reduction in score, which would have actually placed them fourth overall.</p>
              <p align="left">In the men's sub-varsity race, Matt Jordan '15 made his debut as he comes back from a patella tendon injury suffered when he jumped off a box at the beginning of the season. Jordan showed remarkable focus as he has only been running on solid ground for one week. His 26:03 time placed him seventh in his race and was sixth overall for MIT. Tech placed sixth out of 20 teams with 172 points. Eric Safai '14 placed 36th in 26:36, Matt McEachern '17 placed 48th in 26:46, Brian Gilligan '17 placed 50th in 26:50 and Jay McKenna '14 placed 64th in 27:03.</p>
              <p align="left">MIT does not race again until the NEWMAC Conference Championship, also at Franklin Park, in three weeks (November 3). The women need to work at having five runners having a good day on the same day as it seems they can only put four runners together on a given race day. The men need to be able to adjust to different circumstances and stay focused on the job at hand. Both are favored to win their conference championship and will undoubtedly be thinking ahead to the NCAA Division III Regional Championship two weeks later, but will be best served working out the kinks in their armor before Regionals.</p>
