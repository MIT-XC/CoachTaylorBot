                  <h3>Varsity Looks Strong in Season Debut Against Alumni/ae</h3>
                  <p align="left">&nbsp;</p>
                  <p align="left">September 4, 2015</p>
                  <p align="left">The runners could not have asked for a better September day for the annual <a href="alumni.res.pdf">Varsity vs. Alumni/ae</a> competition with temperatures in the low 70's, the humidity low and the sun shining. The athletes took advantage of the opportunity, running some very fast times as the varsity defeated the alums in both competitions. The men won 23 to 38 as Benji Xie '15 and Justin Bullock '14 broke up the MIT sweep with a fine second and third place finish respectively. The varsity women swept the first six places to win 15 to 49.</p>
                  <p align="left">The NCAA Division III seventh ranked men raced first, running the 5K distance in this friendly competition. Matt Deyo '16 was supposed to take the pace out hard in 4:45 and that is exactly what he hit at the mile mark. Bullock was not too far behind, but Xie was back at 4:55, just making his way to the front of a mass of MIT runners. By the two mile mark, Deyo had slowed, but maintained a healthy lead. Xie and Bullock were working well together and had opened up a nice lead on the young MIT horde. Deyo was able to pull away enough to remove all doubt in the third mile, finishing the 5K in 15:35. Xie claimed second in 15:51 and Bullock earned third in 15:54. Ben Freed '18 closed ground significantly but it was too little, too late as he finished in 15:56. Josh Rosenkranz '19 was right behind Freed in 15:58. Nicholas Waltman '18 finished sixth in 16:02 and Alex Knapp '19 closed out the scoring for MIT in 16:04. The 29 second delta through five runners showed nice depth this early in the season but might take a hit at the 8K distance. Even better, the next four MIT runners, Aidan Gilson '19, Dennis Maloney '19, Daniel Weiss '19 and Rory Beyer '17 were all within 16 seconds of Knapp. Also placing for the Alumni were Roy Wedge '14 in 16:28, Allen Leung '15 in 18:03 as he comes back from a broken ankle, and Logan Trimble '13 in 18:18. Others competing for the Alumni were Eric Khatchadourian '06, Eric Safai '15 and Gihan Amarasiriwardena '11.</p>
                  <p align="left">The second ranked women took to the course for their 3K after the men. While the women had a similar plan, they did not follow it so well. Maryann Gong '17 along with Christina Wicker '17 were planning on taking the pace out in 5:35, but split up early as Gong went out in 5:29. Wicker was back at 5:32 along with Nicole Zeinstra who was supposed to be back at 5:40 to 5:45 with her group, but they got pulled by Zeinstra and were strung out between 5:32 and 5:37--far from displaying group running.</p>
                  <p align="left">Fortunately they were only running 3,000 meters so the fast pace did not totally destroy the race. Gong won in 10:48 in what was actually more like 3,100 meters. Zeinstra was able to close well to finish second in 11:07, just one second ahead of Wicker. Anna Frederich '18 had a nice second half to finish fourth in 11:13 and Mary Eccles '18 was fifth in 11:16 to close out the sweep. Megan Montgomery '19, who went out exceptionally hard at the start, held on for sixth in 11:21. Louise van den Heuvel '14 was the top alum, finishing seventh in 11:34. Also placing for the Alumnae were Alexandra Taylor '14 in 12:20, Maggie Lloyd '12 in 13:03, Kaitlin Allen '14 in 13:45 and Stephanie Marzen '15 in 14:41.</p>
                  <p align="left">MIT does not compete for three weeks when they will have their first intercollegiate competition in the Williams College Purple Valley Classic on September 26.</p>
