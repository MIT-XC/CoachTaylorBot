                  <p><span class="headline">Women Earn Third Place at Nationals, Men 19th</span></p>
                  <p>&nbsp;</p>
                  <p align="left">November 21, 2015</p>
                  <p align="left">MIT's Men's and Women's Cross Country teams earned the right to be one of the 32 teams selected per gender throughout the country to race at the NCAA Division III Championship, hosted by the University of Wisconsin Oshkosh at Lake Breeze Golf Course in Winneconne, WI. by virtue of their successful seasons. After a relatively poor showing at last week's Regional Championship, both teams wanted redemption and the opportunity to show the country what they were capable of running.</p>
                  <p align="left">While snow was predicted, the storm passed just south of the area, but the cold temperatures were certainly present at below freezing for most of the championship. The course was relatively dry and flat, making for reasonably fast times and the women would be testing the course first. MIT, ranked eighth going into the meet after finishing fourth at the Regional Championship, was barely holding on from a health standpoint. Four of the seven racing were facing injuries or illness that were severely affecting their ability to perform. </p>
                  <p align="left">With a pretty solid start, MIT runners were in the top third of the 280 runner field. Maryann Gong '17, one of the pre-race favorites to win the competition, was in a good position at 5:42 for the first mile, just two seconds out of the lead. Leandra Zimmermann '19, running on a foot she could not jog or walk on without limping all week, was back at 5:46. Christina Wicker '17 and Sarah Quinn '16 were both at 5:47, Mary Eccles '18 at 5:48, Nicole Zeinstra at 5:50 and Megan Montgomery '19 having a difficult time mentally back at 5:56.</p>
                  <p align="left">During the second mile it was expected that Gong would move up to the front of the lead pack, but she stayed in the back, coming through in 11:39. The overall pace was slow as it seemed no one wanted to take the lead, normally a perfect setup for Gong. Zimmermann was racing tough and Wicker and Eccles were right with her at 11:42. However, Quinn's compartment syndrome affected calves were slowing her down and Zeinstra, who has been dealing with digestive issues all season, was not fairing well, putting them both back at  the 90's position wise and moving in the wrong direction at 11:49. Montgomery was already out of the race mentally.</p>
                  <p align="left">In the final 1.73 miles, Gong slid further back when the leaders made a break, but woke up a bit in the final 400 meters to pass about 14 runners, finishing 16th overall in 21:38. Zimmermann, Wicker and Eccles got the job done, placing 45th, 48th and 60th respectively in 21:52, 21:55 and 22:02. Zeinstra slid back another 13 spots, but held onto 107th place in 22:24. MIT's total points at 276 was pretty high, but the team score would be the actual place minus those individuals not on teams.</p>
                  <p align="left">Williams College ran a superb race, taking the team title with 81 points. No other team was close. Geneseo State was second with 179 points, MIT third at 182 points, pre-race favorite Johns Hopkins fourth at 188 and Washington University fifth at 193 points. While the women from MIT knew they could have easily finished second as a team, they could also have been fifth if any one of their top five runners had given up.</p>
                  <p align="left">The men were next, and while Matt Deyo '16 was running with a broken bone in his foot, everyone else was finally healthy and expecting big things. That all changed about 300 meters into the race when a runner battling another runner went down in the packed field. Approximately 50 runners proceeded to go down on top of one another. Three of those runners were from MIT and two of them were spiked pretty badly. By the time Alex Knoedler '18, Josh Rosenkranz '19 and Daniel Weiss '19 got up, they were among the final 10 runners since they had been at the bottom of the pile.</p>
                  <p align="left">In the first two miles, Rory Beyer '17 and Colin Godwin '17 were running around 30th place, looking strong, splitting 9:55. Deyo was dealing with the pain in his foot, but it was obviously slowing him down. After getting up to 68th place at one point, the senior began to slide back, splitting 10:05 at two miles. Cooper Sloan '18 was at 10:15 and Rosenkranz had moved up well to come through at 10:16. Knoedler was showing great patience as he was steadily moving up.</p>
                  <p align="left">Both Beyer and Godwin lost about 15 places in the final two miles, finishing in 48th and 54th place in 25:10 and 25:13 respectively. However, the remainder of MIT's runners were not so successful. Deyo finished in 159th in 25:50, Sloan in 172nd in 25:54, and Knoedler 183rd in 25:58 to close out the scoring. </p>
                  <p align="left">It was unfortunate the final race of a pretty successful season had to end with three runners falling and another choosing to race on a broken foot. Finishing 19th was a disappointment for the Engineers, but they may not be losing anyone for next year. Deyo has another year of eligibility, and if he goes to MIT for graduate school he will be able to race next season. </p>
                  <p align="left">On the women's side, only Zeinstra will not return, giving the Tech ladies a solid foundation for the 2016 season.</p>
