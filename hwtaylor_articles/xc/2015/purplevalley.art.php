                  <p><span class="headline">Engineers Cannot Overcome Injuries or Competition</span></p>
                  <h5>Women Third, Men Fourth at Purple Valley Classic</h5>
                  <p>&nbsp;</p>
                  <p align="left">September 26, 2015</p>
                  <p align="left">In their first intercollegiate race of the season, the Engineers traveled to Williamstown to take on 20+ teams from New England, New York, California and Texas in the annual <a href="purplevalley.res.php">Purple Valley Classic</a>. Both the men's and women's teams had top runners out due to injury and the 8K for men and 6K for women were the longest cross country races ever for the many freshmen on the team.</p>
                  <p align="left">The men raced first. Going into the meet there were seven top 21 nationally ranked teams representing Division III in the race. Amherst was ranked third, MIT tied for sixth, Williams eighth, Pomona Pitzer 13th, SUNY Geneseo 16th, Tufts 21st and Middlebury 26th. There was certainly no shortage of competition. MIT was missing their top runner, Matt Deyo '16, as well as number three (Rory Beyer '18) and five runners (Josh Rosenkranz '19), who were all sitting out, taking care of minor injuries. The remaining group of youngsters were there to step up and compete for the team title despite missing their upper class leaders.</p>
                  <p align="left">Typical of inexperienced harriers with high expectations, Tech runners went right to the front, certainly more aggressively than they should have done. By the mile mark, which they hit in 5:12, they had settled in pretty well, but the first 800 had been run pretty hard and was likely to take its toll when they hit the big hill for the second time, during the fourth mile.</p>
                  <p align="left">The second mile was strong for the Engineers although they hit the hill the first time and slowed to a 5:22 mile because of it. MIT was still towards the front and packed pretty well with four at 10:34, and all but one of their teammates within 10 seconds.</p>
                  <p align="left">The third mile was another exceptional mile for MIT. Nicholas Waltman '18 stayed amongst the leaders, dropping a 5:06 to do so. He pulled ahead of Dan Weiss '19, and Dennis Maloney '19 by five seconds. Ben Freed '18 fell back a little but was still only two seconds behind the freshmen. Alex Knoedler '18 and Alex Knapp '19 were only 10 seconds behind Waltman and hanging pretty tough.</p>
                  <p align="left">The fourth mile, the second time up the big hill, is what separates the men from the boys in this sport, on that course. MIT did not do well in this case. Weiss moved up to Waltman, or Waltman back to Weiss, but they were together at the end of mile four in 21:05. Maloney was only three seconds back, but Freed was 10 seconds behind that and Knoedler another four seconds. </p>
                  <p align="left">Weiss finished in 11th in 26:12; Waltman 15th in 26:19; Maloney 19th in 26:23, Freed 26th in 26:38 and Knoedler 30th in 26:41 for a 29 second delta. MIT will need to hope their upper classmen can join the outstanding freshmen and sophomore classes so they can compete with the front runners a little better.</p>
                  <p align="left">In the end, MIT finished fourth as a team with 98 points, losing to three nationally ranked teams, Amherst, Williams and SUNY Geneseo. Weiss and Maloney were the top two freshmen in the field. Tech races again in two weeks at the NEICAAA Championship, a race that pits Division I, II and III colleges in New England against each other.                  </p>
                  <p align="left">The women were ranked second in the country, but without Sarah Quinn '16 and Anna Frederich '18 it would be difficult to perform at that level against the competition, which included SUNY Geneseo at #3 in the country, Williams at #6, Middlebury at #7 and Amherst at #18.</p>
                  <p align="left">The start looked great, a little too great as the women hit the half mile in 2:49 for the leaders, which included Maryann Gong '17 and Christina Wicker '17. Unfortunately, the second group of MIT runners were up there as well in 2:52. While the first mile is flat and fast, runners who do not run with caution tend to pay for it in the third mile when the women race up the big hill. Gong and Wicker hit the mile in 5:51, four seconds up on Nicole Zeinstra '16, Mary Eccles '18 and Megan Montgomery '19. MIT's next group was perhaps being a little too cautious as they were :22 back from their teammates. </p>
                  <p align="left">Mile two was the same for Gong as she took the lead, splitting a 5:52. Unfortunately Wicker was not mentally prepared at all for this race as she had not been sleeping as much as she needed and it showed up at this point. Her 6:20 mile took her completely out of the race. Zeinstra had moved up well with a 5:55 mile as did Eccles with a 5:57. Montgomery was having hamstring issues in the second mile but was holding on, just :13 behind Gong</p>
                  <p align="left">The hill indeed took its toll on the Engineers. Gong, once in the lead did not continue to be aggressive, losing some of her competitiveness and slowing down to 6:09 pace. Zeinstra was :13 back, Eccles :27 back, Montgomery another :10 behind and Leandra Zimmerman '19 had moved up to save the day, but was still :52 behind Gong.</p>
                  <p align="left">The final .73 was not kind to Tech. MIT lost a lot of ground to Williams and Middlebury and put more spread between their top runners. When the dust settled, Gong finished third in 22:06. Zeinstra was ninth in 22:32 while Eccles was 16th in 22:47. Montgomery finished 20th in 23:05 and Zimmerman was 21st in 23:10. While Gong can certainly run a stronger race, MIT needs to close the gap between one and two as well as the overall delta between one and five.</p>
                  <p align="left">MIT will be off next week, but returns to action at the NEICAAA Championship on October 10 at Franklin Park, a race that is appropriately nicknamed the All New Englands as it pits all Division I, II and III teams against each other in  varsity and sub-varsity races.</p>
