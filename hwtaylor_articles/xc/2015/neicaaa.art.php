                  <p><span class="headline">Women First and Men Second Among DIII Teams</span></p>
                  <p>&nbsp;</p>
                  <p align="left">October 10, 2015</p>
                  <p align="left">MIT's Women's Cross Country team, ranked fourth in the country for Division III, finished as the top Division III team in the <a href="neicaaa.res.php">NEICAAA Championship</a>, a meet that pits all of the colleges in New England against each other, Division I, II and III. The men, ranked 13th in the country for Division III, were the second Division III team in the meet.</p>
                  <p align="left">On a very sunny, 65 degree day, the Engineers entered the meet determined to improve on their relatively poor race results from two weeks ago at the <a href="purplevalley.res.php">Purple Valley Classic</a>.  Working against this goal for the men was the fact that it was confirmed that All America runner Matt Deyo '16 has a stress fracture and will miss at least two more weeks, and Nicholas Waltman '18, currently the team's number three runner would have to miss the meet with an irritated calf. As it is, All America runner Colin Godwin '17 is only in his fifth week of running after a long term injury from last Spring and Rory Beyer '17 would be running his first race after injury, competing in the sub-varsity race. Fortunately, the injury plagued harriers have some very talented freshmen and sophomores determined to get the job done.</p>
                  <p align="left">Joining Godwin in the varsity race were Dennis Maloney '19, Daniel Weiss '19, Ben Freed '18, Alex Knoedler '18 and Alex Knapp '19. The entire race started off at a pedestrian pace for the top runners, as the leaders were only clocked at 4:40 at the mile mark. Maloney, Weiss, Godwin and Freed came through at 4:48, looking very strong and together. Knoedler was back at 4:55, but hoped  to make his way up to his teammates. </p>
                  <p align="left">The second mile runs a bit slow due to Bear Cage Hill, but it was obvious MIT was not nearly aggressive enough as they slowed to 5:18 pace for Maloney and Weiss with Godwin two seconds back and Freed two seconds behind  Godwin. Knoedler was nine more seconds back, so it did not appear he was going to catch up to his teammates.. </p>
                  <p align="left">Mile three brought more of the same as the runners worked their way through the winding Wilderness Loop. The inexperienced MIT runners were not pushing the pace. It seemed they were settled in a bit too much. In the fourth mile, the pace picked up and Maloney broke away from Weiss with a 5:08 mile. Weiss also picked up the pace with a 5:14 effort. Godwin and Freed increased their effort but still lost ground to the freshmen. Knoedler was moving up in the field, but also losing ground to his teammates.</p>
                  <p align="left">The final mile, which is a little short, saw Maloney finish with a 4:52, Weiss with a 4:51 and Godwin with a 4:52 to finish 29th (25:23), 38th (25:28) and 56th (25:38) respectively--excellent times for this early in the season. Maloney and Weiss were the third and fifth freshmen in the entire race and the top two DIII freshmen. Freed was 70th in 25:47 and Knoedler 98th in 26:03. The Engineers finished ninth overall out of 39 teams. </p>
                  <p align="left">In the sub-varsity race, David Walter '18 had a breakout performance, finishing third overall  in 25:52. Beyer ran an outstanding race given he has only had one workout, finishing 10th in 26:03.</p>
                  <p align="left">While it is not wise to speculate, if Deyo gets back and Godwin returns to last year's dominance, MIT would  finished second overall in a race against the same teams.</p>
                  <p align="left">The women have been going through some of their own difficulties as All America runner Sarah Quinn '16 has not been able to race due to chronic calf problems. Added to that, Nicole Zeinstra '16 has been having a stomach illness that has prevented her from racing her best. The women too would be depending on relatively inexperienced runners to step up.</p>
                  <p align="left">Maryann Gong '17, still trying to figure out how to reach her potential, ran a very intelligent, well paced first mile in 5:31, which was right on plan. Christina Wicker '17 and Zeinstra were right with her, which may not have been the best idea for Zeinstra. Megan Montgomery '19, Mary Eccles '18 and Leandra Zimmerman '19 were back a bit between 5:38 and 5:43.</p>
                  <p align="left">The women were even less aggressive on the second mile than the men. Gong should have moved up into the top ten in the race but instead, lost focus and slowed to a 6:03. Wicker followed suit, slowing to a 6:06. Zeinstra's stomach went south in a hurry, causing her to slow to a 6:15. Montgomery slowed to 6:18, Eccles to 6:16 as they came back to Zimmerman, bringing them all through in 11:55. </p>
                  <p align="left">It is apparent the women are not comfortable with discomfort as they continued to slow through the third mile and the 5K finish. Only Gong woke up, running a 5:53 to move her to 11th place at the finish in 18:06. Wicker placed 31st in 18:25, Montgomery 67th in 18:50, Eccles 72nd in 18:51 and Zimmerman 74th in 18:52 for a team total of 255 points, good for fifth overall.</p>
                  <p align="left">In the sub-varsity race, Bailee Margolis '19, suffering from lower leg tightness, went out way too fast, splitting 5:37 at mile one, but holding on for a 19:26, placing 21st in the race. Liz Cox '18 did not run to her potential, but still did well in 19:34, placing 28th.</p>
                  <p align="left">Like the men, the women have some work to do. If Zeinstra figures out her stomach illness and the women run with more focus and determination they will be very difficult to beat.</p>
                  <p align="left">Next week, MIT will be racing at the Connecticut College Invitational at the same course where the NCAA New England Regional Championship will be held.</p>
