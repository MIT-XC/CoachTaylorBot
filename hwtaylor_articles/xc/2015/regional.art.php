                  <p><span class="headline">Men and Women Race Poorly at Regionals, Hope to Get Opportunity at Redemption at Nationals</span></p>
                  <p>&nbsp;</p>
                  <p align="left">November 14, 2015</p>
                  <p align="left">The Men's and Women's Cross Country teams from MIT entered the <a href="regional.res.php">NCAA Division III New England Regional Championship</a> seeded eighth and third in the country for Division III respectively. Within the region they were seeded third on the men's side behind Williams and Amherst and second on the women's side behind Williams. Running in arguably the strongest of eight regions in the country, in addition to the top two teams earning automatic births to next week's NCAA Division III Championship, there was an assumption that four or even five additional teams may be selected along with the automatic selections per gender.</p>
                  <p align="left">The conditions were extremely windy on the course at Harkness Memorial Park in Waterford, Connecticut. The course was also a bit soft due to the rain over the prior two days. Both factors would slow the performances significantly. The wind kept anyone from attempting a breakaway so the women came through the mile mark in a conservative 5:44 for the leaders. Maryann Gong '17 was in that lead group. The other six MIT runners were just a bit back at 5:48. The second mile, which was mostly into the wind, slowed a bit as the lead group grew even larger while coming through two miles in 11:43 with Gong  still up with the leaders. The remaining Tech runners were starting to spread out, but Leandra Zimmermann '19 and Mary Eccles '18 were racing well still only four seconds back in 11:47. </p>
                  <p align="left">Here the race picked up a bit and it seemed most of the MIT pack simply did not want it as badly as their opponents. Gong came through the third mile in 17:32 looking as though she was holding back, just waiting for her kick once they turned away from the wind ~3.5 miles. Zimmermann was having an outstanding race, coming through in 17:38, as was Eccles at 17:42, although she had lost some focus in allowing Zimmermann to pull ahead. After these three runners MIT's good news was over.  Nicole Zeinstra '16, coming back from the flu and a digestive issue did not handle the discomfort well, hitting the three mile at 17:52. Megan Montgomery '19 was just behind at 17:55. All Americans Christina Wicker '17 and Sarah Quinn '16 were having bad days for different reasons; Wicker because she was not mentally prepared for the race and Quinn because she is suffering through her third compartment syndrome injury.</p>
                  <p align="left">In the final .73, Gong pulled away, unleashing a vicious kick in the final quarter mile to drop the leaders by :07 seconds to win in 21:36, the third female regional winner for MIT. Zimmermann held on for ninth place in 21:57. Eccles lost quite a few places, finishing in 27th place in 22:13. Zeinstra was 33rd in 22:24. Montgomery lost a lot of ground, finishing 44th in 22:38 to close out the scoring. </p>
                  <p align="left">Williams College ran with the confidence MIT did the year before when the Engineers won the meet with 40 points. Williams totaled 44 points to easily run away with the victory. Tufts University put together a nice race to claim second with 107 points, just one point ahead of third place Middlebury College and seven points in front of MIT at 114 points.</p>
                  <p align="left">The men from MIT hoped to compete favorably with Williams and Amherst, but hoping is not the same as believing. While the men got off the line well, they did not work together as they have all season. Matt Deyo '16, only running for two and a half weeks after suffering a stress fracture, went to an ill advised lead despite the strong head wind, and held the lead until nearly the mile mark, which he hit in 4:54. Colin Godwin '17, planning on working with Deyo, and Rory Beyer '17 were both back at a more reasonable 4:56 with the lead pack. Daniel Weiss '19, MIT's most consistent runner all year, was not being his aggressive self, two seconds back at 4:58, along with Dennis Maloney '19 and Cooper Sloan '18. Alex Knoedler '18, a replacement for the injured Nicholas Waltman '18, was in a great place at 5:01. </p>
                  <p align="left">Throughout the second mile MIT runners continued to work alone, losing the advantage they had all season. At the end of the second mile, Deyo fell back to Godwin and Beyer at 10:00. Weiss was back at 10:06, not running at all like himself, and Sloan was at 10:08. Knoedler was staying close at 10:11, but Maloney ran a 5:17 mile and was quickly falling further back.</p>
                  <p align="left">Throughout the next two miles the trend was continuing. Godwin was moving ahead of Beyer and Deyo was paying for the misadventure of his first mile.  Weiss fell back to Knoedler, who was continuing to run a solid race. Cooper fell apart and dropped behind Maloney.</p>
                  <p align="left">At the finish Godwin was 12th in 25:08 and Beyer 14th in 25:14. Neither of those were as high as they should have been, but were not disastrous for the team. Deyo finished in 24th in 25:28 and Weiss won his last mile battle with Knoedler to finish 36th in 25:40 compared to Knoedler's 42nd in 25:45. MIT had a respectable total of 128 points, but that was only good enough for fifth place. Williams earned the double victory with 67 points, just one point ahead of Amherst. Tufts was in third with 80 points and Wesleyan surprised a lot of folks with a fourth place finish with 114 points.</p>
                  <p align="left">Sunday at 3:00 the official announcement will come regarding which at-large teams will be selected.  If selected MIT will have a rare opportunity to show of what they are truly capable.</p>
