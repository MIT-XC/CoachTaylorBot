                  <p><span style="font-weight: bold; font-size: x-large">Johnson and Harper Lead the Way as Tech</span></p>
                  <p><span style="font-weight: bold; font-size: x-large"> Wins All Four Races at UMass Dartmouth</span></p>
                  <p align="center">September 17, 2011</p>
                  <p align="left">For the final weekend of summer, the conditions were quite ideal on the virtually flat, grassy course on the campus of <a href="umd.res.php">UMass Dartmouth</a>. The temperature was a dry 65 degrees with bright sunshine for the women's races and a little more cloudy for the men's.</p>
                  <p align="left">Tech runners took advantage of the conditions as this was the Varsity debut for the presumed top seven on the respective squads. Maggie Lloyd '12, started things off by leading a 1-11 MIT sweep in the Women's Sub-Varsity race, winning in 19:17. In the Women's Varsity Race, Brooke Johnson '13, brought home the gold with an excellent 17:55 victory on the 5K course. When it was the men's turn, Dan Harper '12, led the way for the Engineers with his second place 24:51 performance. In the final race of the day, the Men's Sub-Varsity, Ben Mattocks '12, finished second in 25:59.</p>
                  <p align="left">Although there were 37 women's varsity teams in the race, there were no nationally ranked teams in attendance. Despite not running two of the top seven runners, both freshmen, MIT dominated the results. In addition to Johnson's victory, freshman Elaine McVay ran her way to a third place finish in 18:01. Martina deGeus '14, finished very strong to place fifth in 18:34. Another freshman, Dacie Manion, had an excellent collegiate debut, placing seventh in 18:40 and Tania Morimoto '12, closed out the top five with her ninth place finish in 18:49. No other team had more than one runner in the top 10.</p>
                  <p align="left">The race was well paced with the MIT quintet going out in 5:48-5:50. In the second mile it became obvious that this was MIT's race. Johnson and McVay had begun to run down Kate Warwick of Brandeis, who had established a large lead and was still nine seconds ahead at the two mile mark despite the MIT runners putting in a 5:51 second mile. The remaining Tech runners slowed a bit to 6:00-6:04 pace, but were still in a dominant position. It took nearly all of the last mile for Johnson to take the lead, but she did so with authority, winning by four seconds. McVay almost claimed second, running out of space as she finished two seconds behind. The women easily won with a score of 25 points. Coast Guard was second with 109 points and Wellesley was third with 114.</p>
                  <p align="left">In the sub-varsity race, MIT's mob dealt with the pedestrian early pace, coming through the mile in 6:20, moved up to the front in the second mile with a 6:19 for the first five and then picked it up in the final mile to claim first place. Lloyd pulled away from freshman Kali Benavides in the final straight to claim the one second victory. Just two seconds back was Claire O'Connell '14. Alexandra Taylor '14, took fourth in 19:28 and Shivani Kaushal '15, claimed the fifth spot for MIT, and overall in the race in 19:29. MIT showed remarkable depth as 14 runners were within 2:00 of each other.</p>
                  <p align="left">The total delta for MIT in the Men's Varsity race was 62 seconds. However, it would have been within 25 seconds had it not been for a couple of freak situations. Roy Wedge '14, had his glasses knocked off in the third mile and lost about 30 seconds finding them and Benji Xie '15, running with the other MIT runners through 4.5 miles, suddenly started to lose control of his muscles, barely making it to the finish line. It is assumed he was dehydrated going into the race.</p>
                  <p align="left">Harper ran a superb race, following the race plan to not be in the lead in the early stages of the race. Rob Dextradeur of Bentley somehow pulled away early and Harper was never able to make up the difference as Dextradeur won in 24:35.</p>
                  <p align="left">Outside of Harper and Stephen Serene '12, who went out in 4:58 at the mile, the other MIT runners raced patiently, splitting the first mile in 5:06. In the second mile, Harper had separated himself from his teammates at 10:03. Serene slowed and his teammates sped up as they all came through in 10:11, forming an excellent pack. Other than Wedge losing his glasses, the third mile was excellent as MIT runners passed runners going up the only slight hill on the course putting in a 5:01, virtually putting the race away. The slightly downhill fourth mile was a crisp 4:57 for the Engineer's pack, coming through in a cumulative 20:10. For the most part, MIT harriers still had a bit left, knowing they can be more aggressive in their next race. Harper closed in 4:45. Serene ran a 4:49 final mile to claim fifth place in 25:00. The top freshman in the race, Spencer Wenck made his collegiate debut with a 25:04 seventh place finish. Fellow freshmen, Allen Leung and Karl Baranov placed ninth and eleventh respectively, running 25:05 and 25:16. Wedge and Xie would have been right with their teammates under normal circumstances, giving MIT phenomenal depth. Tech claimed first out of 34 teams with 31 points, followed by Brandeis with 79 and Division II Bentley with 134.</p>
                  <p align="left">The Men's Sub-Varsity race went pretty much the same as the previous three races. MIT ran with the pack mentality and although they did not manage to win individual honors, they did finish 2-7 to easily win the team title. Similar to the Women's Sub-Varsity race, the opening mile was painfully slow, 5:18 for the MIT runners. Following the directions to not take the lead until after the second mile, MIT's pack had to endure an even slower second mile of 5:29. With the reins loosened, Tech runners responded with a 5:14 uphill mile to take control of the race. The pack started to spread with the downhill mile opening things up with splits varying between 5:02 and 5:29 for MIT runners. The final mile was again a very competitive mile for MIT, permitting the excellent team finish. Behind Mattocks was Logan Trimble '13, in 26:07, Dawit Zewdie '13, 26:13, Chadd Kiggins '15, 26:15, and Matt Jordan '15, 26:17 to close out the top five for the Engineers.</p>
                  <p align="left">Although MIT will next race at the Bowdoin College Invitational in two weeks, the varsity will not race for three weeks, racing again on October 8 at the All Division New England Championship.</p>
