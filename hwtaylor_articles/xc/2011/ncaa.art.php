                  <p><strong><span class="headline">Women Conclude Season With 5th Place Finish at NCAA Division III Championship</span></strong></p>
                  <p align="left">November 19, 2011</p>
                  <p align="left">The MIT women's team traveled to the <a href="ncaa.res.pdf">NCAA Division III Cross Country Championship</a>, hosted by the University of Wisconsin Oshkosh at the Lake Breeze Golf Course, seeded seventh in the country. True to tradition, Tech moved up on their seed, placing fifth with 276 points.</p>
                  <p align="left">Over the past four years, MIT, now one of the countries top teams, has finished 10th (2008), 5th (2009), 3rd (2010) and now 5th again in 2011.</p>
                  <p align="left">Freshman Elaine McVay continued her excellence by leading the way for the Engineers, placing 38th overall, missing All America honors by three places. McVay ran the 6K course in 21:45, a 5:51 per mile average. Martina de Geus '14, was second for Tech for the first time this year, running an outstanding race, finishing in 52nd place in 21:55. Brooke Johnson '13, typically MIT's number one runner, was exhausted and struggled on this day as she began falling back after the two mile mark, losing 53 places in the final 1.73 miles to finish 72nd in 22:06. Finishing fourth for MIT was co-captain, Tania Morimoto '12. Although Morimoto lost 39 places in the final 1.73 miles, she ran a courageous race, never giving up. Claire O'Connell '14, closed out the scoring for the Engineers with her 22:30 for 132nd place. Also competing for MIT were freshmen, Stephanie Marzen and Dacie Manion.</p>
                  <p align="left">The team scoring is achieved by taking the individuals out of the scoring, which provided the following scores for MIT: McVay 25th, de Geus 35th, Johnson 48th, Morimoto 76th and O'Connell 92nd.</p>
                  <p align="left">Early in the race MIT looked very good, possibly in first place through two miles. Other than de Geus, MIT runners faded, some due to physical fatigue, some due to doubts about their ability and some simply losing focus for periods in the race.</p>
                  <p align="left">This team is accustomed to a lot of success. Although finishing fifth at the NCAA Championship is quite an accomplishment, the team was not satisfied with their cumulative result and knows they could have competed better collectively. They must now focus on their goals for next year and do what is necessary to return to the podium.</p>
                  <p align="left">Dan Harper '12, was the lone representative for MIT at the <a href="ncaamen.res.pdf">NCAA Division III Championship</a>. Harper was determined to finish as an All America runner after missing by 13 places in 2010. He did everything necessary this year to make it happen, proving to be one of the most dedicated athletes in the program's history. However, it was not to be as Harper never got closer than at the finish when he eventually moved up to 64th place in 25:01.</p>
                  <p align="left">Harper's plan was to be with the leaders at the mile mark, but once in the race, felt the pace was too fast as the leaders came through the first mile in 4:39 while Harper was back in 74th place at 4:50. He moved up slightly to 67th place by the two mile mark, but had fallen behind the leaders by 32 seconds. Seeming to run with caution for the next two miles, Harper fell back to 72nd place. In the final mile, he moved up eight places by the finish.</p>
                  <p align="left">MIT was represented well on this cold and cloudy day. It is a tribute to the program's success that the team and individuals have such high expectations, even when running against the best in all of Division III.</p>
