                  <p class="bigtitle">MIT Extends NEWMAC Championship Win Streak</p>
                  <p align="left">October 29, 2017</p>
                  <p align="left">The MIT Men's and Women's Cross Country teams were looking forward to attempting to extend their <a href="newmac.res.php">NEWMAC Conference Cross Country Championship</a> winning streak today, which stands at 19 for the men and 10 for the women.&nbsp; From an individual standpoint the team members were also looking forward to racing in temperatures below 85 degrees for the first time this year.&nbsp; </p>
                  <p align="left">The championship was hosted by Emerson College at historic Franklin Park.&nbsp; Although the temperatures were in the upper 60's, the humidity was very high and the ground was soft from rain.&nbsp; Not ideal conditions, but much better than the Engineers have experienced thus far&nbsp;this year.</p>
                  <p align="left">The nationally 10th ranked men raced first and the plan was to go out somewhat aggressively while respecting that the soft course would not be returning much energy to the runners.&nbsp; At the mile mark Dennis Maloney '19 was in the lead, comfortably pacing a 4:50 first mile.&nbsp; Following three second back were five MIT runners; Josh Rosenkranz '19, Aidan Gilson&nbsp;'19, Billy Woltz '20, Nate Foss '20 and Kent Slaney '21, two from Babson; Matthew Dynan '19 and Josh Decker '18, and two from WPI; Michael Brady '19 and Avery Ingegneri '20.&nbsp; Overall the MIT team was pretty close&nbsp;with a total spread of 15 seconds at that point for the 17 runners.</p>
                  <p align="left">Things spread out quite a bit with the hilly second mile&nbsp;and Maloney pushing the pace.&nbsp; Maloney hit the two mile in 9:58 and had a six second lead over Dynan of Babson, but the next seven runners were from MIT within 12 seconds of one another.&nbsp; Alex Knoedler '18, Nicholas Waltman '18, Brian Bates '20, Alex Knapp '19 and David Walter '18, all from MIT, were moving up from a more conservative start.</p>
                  <p align="left">At the end of three miles, Rosenkranz had passed Dynan and was only 11 second behind Maloney.&nbsp; Dynan looked to be hurting a&nbsp;bit and Waltman, Knoedler, Woltz and Gilson were closing the gap.&nbsp; At the end of four, both Knoedler and Waltman passed the Babson runner and Knapp, Gilson and Woltz were looking strong. Then behind Ingegneri, Decker and Jack Pinho '19 from Springfield, Bates from MIT was about to unleash his famous finish.&nbsp; At this point Bates was in 12th place.</p>
                  <p align="left">In the final mile&nbsp;Maloney pushed it to widen the gap significantly, winning in 25:03.&nbsp; Rosenkranz faded a bit but claimed second in 25:32 with Knoedler in third in 25:48.&nbsp; Dynan outkicked Waltman to take fourth and then was followed by Pinho from Springfield, three second ahead of Bates in sixth.&nbsp; MIT closed out the scoring with Gilson right with Bates (possibly ahead of) and Knapp and Woltz right on their heels.&nbsp;MIT's 18 points won by a substantial amount over the eight team field with WPI claiming second with 71 points.&nbsp; MIT's men's team has never lost the championship in the 20 year history of the conference.</p>
                  <p align="left">Maloney earned Runner of the Year honors and Jake Oeding of MIT took NEWMAC Rookie honors.</p>
                  <p align="left">The Women's race would be the final 5K of the season, although the course is actually slightly longer than 5K.&nbsp; The pre-race favorite was freshman Kaitlyn Mooney from Coast Guard.&nbsp; The rookie has only lost to superstar Taryn  Cordani '18 of Ithaca College, as she has torched all of New England.&nbsp; The MIT group of Mary Eccles '18, Katie Collins '21, Leandra Zimmermann '19, Katie Bacher '20 and Marissa McPhillips '20 were planning on doing their best to stay with Mooney.</p>
                  <p align="left">At the mile mark it looked as though&nbsp;the plan might work pretty well.&nbsp; The Coast Guard freshman was in the lead at 5:32, which was impressive for how soft the ground was on this day.&nbsp; MIT's pace was two to three seconds back, looking comfortable.&nbsp; Behind them it was another six seconds to Jenna Melanson '20 of MIT, who was just ahead of Sydney Packard '20 of WPI.&nbsp; They were followed by MIT's Megan McCandless '20 and Tessa Weiss '20&nbsp;in 5:44.</p>
                  <p align="left">The plan for the Engineers, ranked second in the country, was to catch up to Mooney at the end of the second mile, but that was not to be.&nbsp;&nbsp;Mooney had 10 seconds on Eccles at the two mile mark, coming through in 11:18.&nbsp; Eccles, Collins, Zimmermann, Bacher and McPhillips were all within four seconds of each other.&nbsp;&nbsp;Packard of WPI was well back of the quintet but had pulled ahead of McCandless by a bit. Melanson had fallen back a lot and Gabrielle Enns '21 of MIT joined the fray, passing Weiss.</p>
                  <p align="left">In the final 1.1 miles, Eccles finished well, running 18:09, 17 seconds behind Mooney.&nbsp; Collins closed well, finishing two seconds behind Eccles with Zimmermann being less than a second behind Collins.&nbsp; Bacher, suffering from a cold, lost a lot in the final mile, finishing in 18:31.&nbsp; McPhillips, with her teammates with a quarter mile to go, collapsed briefly before getting up and finishing, struggling to get to the line in 18:32.&nbsp; The 23 second spread is impressive but can be even better as each of the five runners have been the lead MIT finisher in a race this year.&nbsp; The parts are interchangeable, depending on the day.&nbsp; McCandless finished well, 20 seconds back with Weiss&nbsp;10 more seconds back to finish 9th.</p>
                  <p align="left">Tech claimed the victory with 20 points, 42 better than second place WPI.&nbsp;&nbsp;This earned the women their 11th consecutive conference title.&nbsp; Mooney of Coast Guard claimed the Runner of the Year as well as the Rookie of the Year.</p>
                  <p align="left">In two weeks, MIT will race in the NCAA Division III New England Region Championship, hosted by Southern Maine, to determine if they will get to run at the NCAA Division III Championship on November 18th&nbsp;in Elsah, Illinois at Principia&nbsp;College.&nbsp; The top two teams earn automatic selection with the possibility of others being selected on an at-large basis.</p>
