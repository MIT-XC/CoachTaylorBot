                  <p class="bigtitle">MIT Harriers Defend NCAA Division III New&nbsp;England Region Titles</p>
                  <p align="left">November 11, 2017</p>
                  <p align="left">MIT traveled to Gorham Country Club this weekend, home of the <a href="newenglandregion.res.php">NCAA Division III New England Region Cross Country Championship</a> hosted by the University of Southern Maine.&nbsp; The course was soft and even torn up a bit in some areas, but also frozen solid in other areas due to the below freezing temperatures the day before and day of the meet.&nbsp; The men from MIT were seeded second in the region and 10th in the country for Division III while the women were seeded first in the region and second in the country.</p>
                  <p align="left">The men raced first on the hilly course.&nbsp; The plan was simple, be somewhat cautious in the first mile, move up in the second mile and put themselves in position to race number one seed Amherst College man to man.&nbsp; </p>
                  <p align="left">At the mile mark, MIT runners looked strong.&nbsp; Dennis Maloney '19, the top returning runner, having finished fourth in 2016, was among the lead pack in a relatively slow 5:05, and quite surprisingly, Josh Rosenkranz '19 and Aidan Gilson '19 were right with him, all three in the top 15.&nbsp; Billy Woltz '20, Alex Knoedler '18 and Brian Bates '20 were also together at 5:11, probably around 60-70th place.&nbsp; Nicholas Waltman '18 took himself out of the race pretty early with a 5:16 first mile.</p>
                  <p align="left">During the second mile Maloney gained three seconds on Gilson and Rosenkranz as he tried to push the pace.&nbsp; Knoedler, Woltz and Bates were still together but Bates was looking as though he was not committed enough&nbsp;to stay with his teammates but instead starting to give-in to his doubts.</p>
                  <p align="left">In the&nbsp;third mile, Maloney gained a little more on his teammates, but the spread one through five was still only 13 seconds.&nbsp; Some&nbsp;of this was due to a relatively slow pace by the&nbsp;leaders, but most of it was due to a tremendous improvement by Gilson and Woltz.&nbsp; Maloney, Rosenkranz and Knoedler had been very consistent most of the season.&nbsp; Tech knew that both Gilson and Woltz should be among the top five,&nbsp;and if they would make that leap, the team would be one of the best in the country and could win the region.</p>
                  <p align="left">During mile four Maloney went for the win, pulling away from the main pack.&nbsp; He opened up an additional 15 seconds on his teammates.&nbsp;&nbsp;But in the final mile he did not continue to push, giving up the lead and ultimately losing by :09&nbsp;for third place in 25:31.&nbsp; Amherst's first runner was fifth with their second runner in eighth.&nbsp; Gilson was MIT's second man in 12th at 25:45, making up a lot of ground in the final mile.&nbsp; Rosenkranz was 16th in 25:51, two spots ahead of Amherst's third man.&nbsp; Amherst also put their fourth ahead of MIT's fourth, Woltz in 26:00 in 22nd place.&nbsp; MIT was at 53 points to 52 for Amherst through four runners.&nbsp; Knoedler finished in 28th in 26:07 to Amherst's fifth in 39th to take the team title with 81 points to 91 for Amherst in the 55 team field.</p>
                  <p align="left">The women's race plan was to also go out somewhat conservatively, but close on the chase pack during mile two, who would likely be chasing outstanding freshman, Kaitlyn Mooney of the Coast Guard Academy.&nbsp; With the top five being interchangeable thus far in the season, it was thought that Katie Bacher '20, Katie Collins '21, Mary Eccles '18, Marissa McPhillips '20 and Leandra Zimmermann '19 would be together and all within the top 10-15 runners.&nbsp; Megan McCandless '20 also has been returning to the skills of her freshman year bit by bit and would likely be with them.</p>
                  <p align="left">However, things did not quite go as planned.&nbsp; Collins did not get out well and McCandless, wearing transition glasses, had difficulty seeing in the shady areas, unsure of her footing.&nbsp; By the mile mark, the foursome of Bacher, Eccles, McPhillips and Zimmermann&nbsp;were in the top 15 at 5:44&nbsp;and looking pretty strong, although already about five seconds back.&nbsp; Collins, having been swallowed up by the field when she was unable to get out, was 10 seconds back and McCandless was another six seconds behind.&nbsp; Tessa Weiss '20&nbsp;took herself out of the race mentally by the mile mark, coming through in 6:16.</p>
                  <p align="left">In mile two, nothing much changed, but the leaders continued to put seconds between themselves and the lead MIT runners.&nbsp; Collins and McCandless stabilized, all six running between 5:42 and 5:48.&nbsp; </p>
                  <p align="left">Mile three was a different story.&nbsp; Bacher, tending to suffer from asthma, was struggling with the cold weather and Collins fell off a bit.&nbsp; At the three mile mark, McPhillips came through in 17:13 and Collins, the sixth MIT runner was clocked in 17:48 on the very hilly course, so&nbsp;a pretty solid delta on a hilly and challenging course.</p>
                  <p align="left">In the final .73, McPhillips held on well, finishing in seventh in 21:04&nbsp;and Zimmermann was eighth in 21:12.&nbsp; Eccles was two back in 10th in 21:14.&nbsp; Bacher lost a lot of time, but not a lot of places, earning 16th in 21:39.&nbsp; McCandless placed 17th n 21:40 to close out the scoring.&nbsp; Collins had an outstanding finish, placing 22nd in 21:47.&nbsp; MIT won easily with 58 points in the 56 team field.&nbsp; Tufts University upset Williams College for second with 92 to the Ephs 106.</p>
                  <p align="left">One of the advantages for both the men and women at MIT was the fans who drove up from Cambridge&nbsp;to support their teammates.&nbsp; About 30 or so of the men's and women's track &amp; field and cross country team&nbsp;members&nbsp;took the initiative to rent cars and drive up to the meet.&nbsp; Those who were racing, felt the constant cheers, seemingly everywhere on the course, made the difference whenever they felt fatigue or doubt.</p>
                  <p align="left">Both the men and women earned automatic bids to the NCAA Division III Championship at Principia College in Elsah, Illinois next Saturday.&nbsp; Both teams have some mental work to do if they hope to accomplish the&nbsp;goals they have been pointing to all season.                  </p>
