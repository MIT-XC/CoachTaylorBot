              <h4>Varsity Outruns Alumni in Annual Meet</h4>
          </div>
            <p>September 1, 2018</p>
            <p>On a Fall day with near perfect September weather (75 degrees, dry, partly sunny) at the Larz Anderson Park in Brookline, MA, the varsity squads for both the men and women were able to sweep the game, but less than fully fit alums in the annual <a href="2018xcalumni.pdf">Alumni/ae Meet</a> on the hilly, 2.76 mile course.</p>
            <p>The men were first to race.&nbsp; Nicholas Waltman '18 led the&nbsp;pack across the field initially, but the varsity soon took over.&nbsp; The figure eight course provided some intense hills on the second half of the loop.&nbsp; The&nbsp;varsity runners showed their skills at hill running as they opened up a large gap on the first downhill.</p>
            <p>At the mile mark, which the top runners crossed at 5:14, the varsity held the top four spots with Billy Woltz '20 in the lead followed closely by Andrew Mah '22, Josh Rosenkranz '19 and Aidan Gilson '19.&nbsp; The alums were still in the race as David Walter '18 and Matt Deyo '16 were within five seconds and Justin Bullock '14 and Alex Knoedler '18 were sitting just off the very talented freshman group of Nassim Oufattole, Zach Johnson, Matthew Kearney, Nicky Medearis and Ryan Wilson.</p>
            <p>The varsity runners took over in the second mile.&nbsp; Mah continued to rule the downhills, but Rosenkranz was catching back up on the flat and uphills, both coming through in 10:16.&nbsp; Woltz fell back by five seconds and Gilson, feeling the effects of a cold, fell off badly.&nbsp; The freshmen moved up well, not closing ground on the leaders, but passing the alumni to claim the top seven positions with a 15 second delta.&nbsp; Walter, Deyo, Knoedler and Bullock were still within 10 seconds of the seventh varsity runner.</p>
            <p>In the final short loop there was little change.&nbsp;&nbsp;Mah broke the course record of 14:36 by one second to claim a three second victory over Rosenkranz.&nbsp; Woltz took third, Johnson fourth and Kearney fifth for an 11 second delta through five runners.</p>
            <p>Walter continued to lead the way for the alumni, running 15:05.&nbsp; Deyo ran 15:06, Bullock 15:18 and Knoedler 15:19.&nbsp;&nbsp;Colin Godwin '17 was the fifth runner for the alums, finishing in 15:37.&nbsp; Other alumni who were racing and their performances were; Brian Gilligan '17 16:03, Jared Forman '13 16:36, Roy Wedge '14 17:25, Josh Feldman '97 17:36, Nicholas Waltman '18 18:32 and David Westenberg '80 18:49.&nbsp; Those in attendance, unable to race due to injury were Bob Collins '82 and Don Shobrys '75.</p>
            <p>The women also got off to a strong start, but perhaps a little more sane that the men.&nbsp; The alumnae were only able to round up four runners, so the scoring was adjusted to four finishers to comprise a complete team.</p>
            <p>At the mile mark Jenna Melanson '20 was in command of the lead at 6:16.&nbsp; Izzi Gengaro '22 was two seconds back and both Leandra Zimmermann '19 and Marissa McPhillips '20 were at 6:20.&nbsp; Mary Eccles '18, representing the alumnae was with Zimmermann and McPhillips.&nbsp; There was an eight second gap before Einat Gavish '22 came through the mile mark.&nbsp; She was closely followed by Emily Niu '21, Tessa Weiss '20, Abby Mauermann '22 and Emily Skilling '21.&nbsp; Louise van den Heuvel '14, Alexandra Taylor '14 and Elaine McVay '15 were at the back of that group.</p>
            <p>Zimmermann closed the gap to join Melanson and Gengaro, but McPhillips was only four seconds back at the two mile mark.&nbsp;&nbsp;Gavish and Mauermann remained the same distance behind McPhillips, but the others lost six to 14 seconds.&nbsp;&nbsp;Eccles fell off a bit, but was still in fifth place overall.&nbsp; van den Heuvel and Taylor moved up a bit.</p>
            <p>The varsity women swept the top four places with a 17 second delta and had a 40 second gap through five runners.&nbsp; Melanson won in a course record 17:10.&nbsp; Gengaro ran 17:13, Zimmermann 17:14 and McPhillips 17:27&nbsp;for a 10 point perfect score.&nbsp; Eccles was close at 17:35.&nbsp; Gavish ran 17:50 and Mauermann closed nicely at 17:52, giving MIT three&nbsp;freshmen in the top six.&nbsp; van den Heuvel ran 18:41, Taylor 18:56 and McVay 19:48.</p>
            <p>MIT's first intercollegiate competition will be the Purple Valley race, hosted by Williams College on September 24.</p></td>
