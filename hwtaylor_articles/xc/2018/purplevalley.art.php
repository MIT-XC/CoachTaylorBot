              <h4>Women Win Purple Valley, Men Finish Fifth</h4>
          </div>
            <p>September 22, 2018</p>
            <p>The MIT Engineers Cross Country teams traveled to Williamstown, MA to race in the <a href="purplevalley.pdf">Purple Valley Classic</a> hosted by Williams College, racing against intercollegiate competition for the first time this year.&nbsp; The stellar field included many nationally ranked teams.&nbsp; For the men, of the 16 teams racing the hilly 8K course, nationally ranked Amherst (3rd), RPI (6th), MIT (7th), Middlebury (8th), Williams (17th), Connecticut College (24th) and Tufts (28th) were heading the field.&nbsp; The women's field boasted a slightly weaker field, but only slightly, among the 13 teams; MIT (5th), Middlebury (6th), Williams (8th), Tufts (11th) and RPI (12th).</p>
            <p>It was&nbsp;a breezy day with ideal temperatures, for late September at least, in the mid-60's.&nbsp; The course was wet and even quite muddy back in the woods.&nbsp; Unfortunately, for the men, they did not apply their intelligence and realize they would not get the same energy out of the ground they would be putting in on such a soft and muddy course.</p>
            <p>When the men started out towards the front instead of running behind top ranked Amherst, they set themselves up to die hard&nbsp;and die they did.&nbsp; After being relatively competitive with the top teams through two miles, the fast pace (for the course) and the large hill in mile two, did them in.&nbsp;</p>
            <p>While the top five gap was fabulous (:38) given that&nbsp;both Aidan Gilson '19 and Kent Slaney '21 went out the back after three miles, MIT's top runner, Josh Rosenkranz '19 was :41 off the winner in 26:25, Sean O'Connor '19 of RPI.&nbsp; In fact RPI put four runners in front of MIT's #1, nearly sweeping them.</p>
            <p>The team ran tough, particularly the freshmen, with Zach Johnson '22 placing 25th overall in 26:39, and third on the team, right behind teammate Billy Woltz '20 in 26:38.&nbsp; Andrew Mah&nbsp;'22, also going out too hard, held on remarkably well for a freshman, finishing fourth on the team and 28th overall in 26:42.&nbsp; Closing out the scoring for Tech was Josh Derrick '20 in 27:03 and 41st place.&nbsp; Derrick has only&nbsp;just started back running after dealing with numerous injuries, displaying his toughness.</p>
            <p>The men placed fifth overall with 127 points.&nbsp; RPI won with 41 points.&nbsp; Williams was second with 72, Middlebury 103, and Amherst with 118.</p>
            <p>The&nbsp;women, down three All Americans; Jenna Melanson '20, Katie Bacher '20 and Megan McCandless '20, were hoping to work on their gap as well.&nbsp;Keeping a small delta when missing three of your top five would be&nbsp;a major feat.</p>
            <p>Fortunately the pace at the start of the race was cautious.&nbsp; Even when Kaitlyn Mooney '21 of Coast Guard took off before the mile mark and Natalie Bettez '19 of Tufts went after her, the rest of the field settled in for the team race.</p>
            <p>MIT positioned themselves well early on with Leandra Zimmermann '19 and Izzi Gengaro '22 up in the top five and Katie Collins '21, Marissa McPhillips '20,&nbsp; and Einat Gavish '22 formed up once Collins fell off the lead group and worked well together between 10th and 15th place for most of the race.</p>
            <p>In the final three quarters of a mile of the 6K, Zimmermann and Gengaro pulled away by over 20 seconds from fifth place, earning third and fourth in 22:39 and 22:41 respectively.&nbsp;&nbsp;McPhilips started falling back in the final 400, fell and labored in, placing 6th for MIT&nbsp;and 35th overall.&nbsp;&nbsp;Gavish, who placed 17th overall and 3rd on the team, ran an excellent race&nbsp;in 23:31.&nbsp; Collins placed 19th in 23:34.&nbsp; Emily Niu '21 came out of nowhere to finish fifth for the Engineers, closing out the scoring with her 24th place in 23:41.&nbsp; Overall MIT finished first with 67 points, 14 points&nbsp;ahead of Williams.</p>
            <p>Even though the women won, the 1:02 gap for their top five is a major concern, particularly with the :50 gap between the second and third finishers.</p>
            <p>The Engineers next race will be at the NEICAAA Championship at Franklin Park against primarily Division I competition on October 6.</p></td>
