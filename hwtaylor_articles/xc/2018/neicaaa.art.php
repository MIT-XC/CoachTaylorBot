              <h3>Freshmen&nbsp;Lead Engineers to NEICAAA Podium</h3>
          </div>
            <p>October 6, 2018</p>
            <p>The NEICAAA Championship is a stand alone championship where the Division I, II and III teams get a chance to compete against one another at historic Franklin Park in Boston, Massachusetts.&nbsp; While many teams prefer out of region meets the week before, bypassing this&nbsp;meet, many more take part to make it a very competitive competition.</p>
            <p>MIT's women, ranked fourth nationally in Division III, were racing without All Americans Marissa McPhillips '20 and Katie Bacher '20, but showed a very talented squad that took second place with 78 points, losing only to Division I Boston College.</p>
            <p>The men's race showcased 26 teams, including Division II&nbsp;nationally ranked #18 American International and #24 Stonehill College.&nbsp; MIT claimed third with 125 points, losing to UMass Amherst and Northeastern University, both Division I teams.</p>
            <p>This was a nice cross country day in that the temperatures, while humid, were in the mid-60's.&nbsp; The course was soft and muddy in spots, but in pretty good condition overall.</p>
            <p>The first race of the day was the women's sub-varsity 5K.&nbsp; Lindsey McAllister '21, still coming back from injury, led the way for MIT, placing 22nd in 19:30.&nbsp; Gabby Enns '21 had a breakout race, placing 32nd in 19:39 and running a very smart, well-paced 5K.&nbsp; Tessa Weiss '20, also returning from injury and running her first race this year, finished 45th in 19:56.&nbsp; Dhaman Kaur '21 place 67th and Megan McCandless '20, racing her first race of the year, placed 74th.</p>
            <p>The women's varsity 5K was next.&nbsp; With McPhillips out for personal reasons and Bacher still coming back from injury, the engineers needed their runners to step up.&nbsp; At the mile mark, Jenna Melanson '20 led Leandra Zimmermann '19, Izzi Gengaro '22 and Katie Collins '21 at 5:39 in around 25th-30th place. &nbsp;Einat Gavish '22 and Emily Niu '21 were back in 60th-65th position in 5:49.&nbsp; By the second mile, Gengaro had moved up to 10th with Melanson 11th and Zimmermann in 13th.&nbsp; Collins was in 20th and Gavish 26th.&nbsp; All looked pretty strong at this point and appeared to still be moving up in the field.&nbsp; While Gengaro was able to move up to 6th in 18:07, and Collins to 17th in 18:35, Zimmermann and Melanson fell back to 14th and 15th respectively in 18:24 and 18:26.&nbsp; Gavish held onto 26th in 18:54 to round out the scoring.&nbsp; The 47 second gap was as impressive as the individual performances, particular the efforts by freshmen Gengaro and Gavish.</p>
            <p>In the men's varsity 8K, all MIT runners were present, but&nbsp;Josh Rosenkranz '19 and Josh Derrick '20 were getting over  illnesses and a number of freshmen were being counted on in only their second 8K.&nbsp; At the mile mark, Aidan Gilson '19, Andrew Mah '22, Billy Woltz '20 and Rosenkranz  led the Engineers in 4:51 at around 50th place.&nbsp; Derrick  led Zach Johnson '22 and Nicky Medearis '22 as MIT's second group and were between 70th-80th place&nbsp;in 4:55.&nbsp; By mile two Tech runners had moved up significantly&nbsp;with Woltz in 28th with Mah, Rosenkranz and Gilson right with him.&nbsp; Johnson had moved up to 43rd and Derrick was up to 50th.&nbsp; Mah took over in the third mile, moving up to 17th.&nbsp; Woltz and Rosenkranz were in 19th and 21st, but Gilson was falling back quickly.&nbsp; Fortunately Johnson had moved up to 27th and Derrick 40th.&nbsp; In the final two miles Mah was able to move&nbsp;up to eighth, but then lost one place right at the finish in 25:15.&nbsp; Woltz moved up to 18th in 25:32, but Rosenkranz, obviously not at the top of his game, faded slightly to 27th in 25:41.&nbsp; Johnson held up well for a freshman but lost some ground, finishing 32nd in 25:46.&nbsp; Derrick closed well, placing 39th in 25:52.&nbsp;&nbsp;Medearis lost some in the middle miles but finished well, placing 59th in 26:12.&nbsp; Three freshman in the top six&nbsp;was quite an impressive effort for the class of '22.</p>
            <p>The final race of the day was the sub-varsity 8K.&nbsp; Ryan Wilson '22 ran a smart, conservative first half-mile, got to where he needed to be by the mile mark, around 40th place in 5:02.&nbsp; Kent Slaney '21 was more aggressive, particularly in the first half-mile at 2:27, but at mile two was leading the way for MIT in eighth place in 10:18.&nbsp; Wilson and Daniel Weiss '19 were eight seconds back in 11th and 12th.&nbsp; Back in 16th, having the race of his career, was Jake Jorgensen and in 18th and 20th were teammates Alex Knapp '19 and Nassim Oufattole '22.&nbsp; They were chasing Northeastern University who were in 1, 2, 3, 4, 7 at that point.&nbsp;By mile four Wilson had passed Slaney and Jorgensen had passed Weiss.&nbsp; Most surprising of all, Finn Jacobson-Schulte&nbsp;'21 came out of nowhere to move within four seconds of Weiss.&nbsp; At the finish, MIT was still second to NU 17 to 51 with only Wilson breaking up the sweep.&nbsp; Wilson was sixth overall in 26:13.&nbsp; Slaney finished 12th in 26:38, followed closely by Weiss in 14th in 26:42.&nbsp; Jacobson-Schulte was 17th in 26:44 for a breakthrough race and Jorgensen 18th in 26:45 to close out the scoring.</p>
            <p>Next week MIT will take a select group of runners to the Pre-National meet hosted by the University of Wisconsin - Oshkosh at the Lake Breeze course in Winneconne, WI.</p></td>
