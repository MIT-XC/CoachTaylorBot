              <h3>Engineers Construct Another NEWMAC Double Championship</h3>
              <p>&nbsp;</p>
              <h4>Men Earn 21st in succession, Women Win 12th</h4>
          </div>
            <p>October 27, 2018</p>
            <p>From a spectator's point of view the weather could not have been more miserable for the 2018 NEWMAC Cross Country Championship.&nbsp; The rain was constant&nbsp;since early in the morning.&nbsp; The temperature in the low 40's and the wind was blowing harder as the meet progressed.&nbsp; From the runner's point of view, it could not have been better.&nbsp; This weather defines cross country and the Engineers embraced the elements, the <a href="newmac.res.pdf">men</a> winning their 21st consecutive title with 17 points to runner-up WPI's 52, and the <a href="newmacwomen.res.pdf">women</a> claiming their 12th in a row with 20 points to WPI's 71.</p>
            <p>The Smith College course is quite hilly with a lot of turns, so when you add significant amounts of mud, the pace tends to slow down.&nbsp; MIT worked on group running rather than trying to set any performance records.&nbsp; The men did not take the lead until they had passed three miles, and that was more that everyone else slowed down&nbsp;as the course reached the downhill stage except for the final uphill with about two thirds of a mile to go.</p>
            <p>Billy Woltz '20 worked with Andrew Mah '22 and Josh Rosenkranz '19 through four miles before opening up a small lead with 800 to go and then managed to out-sprint Mah to the finish for a three second victory 26:14 to 26:17.&nbsp; The teammates won NEWMAC Runner of the Year and Rookie of the Year&nbsp;respectively.&nbsp; Rosenkranz was another eight seconds back and Josh Derrick '20 at 26:32 was only :18 back of Woltz in fourth.&nbsp; Brian Brooks '22 from WPI broke up the MIT sweep,&nbsp;but Alex Knapp '19 had a career race to finish fifth for MIT and sixth overall in 26:40, giving MIT a 27 second delta&nbsp;through five runners.</p>
            <p>Aidan Gilson '19 (26:48) and Kent Slaney '21 (27:01) added all-conference honors for MIT as they placed seventh and ninth respectively.&nbsp; Both Gilson and Slaney will have to step-up to their top five ability if the men are to three-peat as New England region champions&nbsp;in two weeks at Bowdoin College.</p>
            <p>The women were without&nbsp;number one runner, Izzi Gengaro '22, who was out for precautionary reasons, but that just gave others the opportunity to show what they could do in the messy conditions.&nbsp; The MIT women did not go out with the leader, sticking with the plan to work on their group running, which brought them through the mile mark in 6:34.&nbsp; Trailing the second place runner until the 1 1/2 mile mark.&nbsp; Jenna Melanson '20 and&nbsp;Leandra Zimmermann '19 took off, opening up an immediate gap on their teammates and other runners.&nbsp; Kaitlyn Mooney '21 from Coast Guard was well out in front by 20 or more seconds, but behind her&nbsp;Tech was putting together quite the team performance.</p>
            <p>Katie Bacher '20, making her varsity debut this year after coming back from an achilles tendon injury and a very recent sprained ankle, was in fourth all alone.&nbsp; Einat Gavish '22 and Marissa McPhillips '20 were holding down fifth and sixth a few seconds behind Bacher.&nbsp; Katie Collins '21 was in eighth and trying to make up ground to get up with her teammates.</p>
            <p>After another mile and a half&nbsp;and Mooney's victory in 18:39, Melanson finished 21 seconds back in 19:00.&nbsp; Zimmermann was just four seconds back in 19:04.&nbsp;Bacher finished fourth in 19:18, McPhillips in fifth in 19:28 and Gavish in sixth in 19:32.&nbsp; The :32 delta was impressive, especially without Gengaro competing.</p>
            <p>Adding to the all-conference list were Collins in eighth, Emily Niu '21 in 12th, Lindsey McAllister '21in 15th and Tessa Weiss '20 in 16th.&nbsp; Gavish earned Rookie of the Year honors as the top finishing freshman.</p>
            <p>In two weeks the women will attempt to three-peat at the NCAA New England Region Championship&nbsp;when they compete in that 6K championship at Bowdoin&nbsp;College.</p></td>
