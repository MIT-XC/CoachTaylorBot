              <p><span class="headline">CROSS COUNTRY DEFENDS UMASS DARTMOUTH TITLES</span></p>
              <p align="left">September 15, 2012</p>
              <p align="left">MIT was one of 39 colleges competing in the <a href="umd.res.php">UMass Dartmouth Invitational</a> on Saturday. The Engineers were there to defend their four titles from 2011; men's varsity and sub-varsity races and the women's varsity and sub-varsity races. The Engineers were not able to field a full team in the men's sub-varsity race as a few individuals were out with severe colds. However, the four individuals who did race for MIT claimed the top four spots. In the other three races, MIT prevailed to remain undefeated.</p>
              <p align="left">The course is as flat as any cross country course can be. The grass was thick and holding water from an early rain, but still relatively fast so it was expected that times would be pretty quick despite the 70 degree temperature. </p>
              <p align="left">The men's sub-varsity ran first. Justin Bullock '14, Rick Paez '15, Eric Safai '14 and Jay McKenna '14 represented MIT, coming through the mile mark in a conservative 5:17. By the two mile mark it was clear they were moving up on the competition despite a very slow 5:34 second mile. During the third mile it became apparent the quartet had been holding back for the first two miles as they jumped the pace to 5:09-5:12 on the uphill mile. Coming down the hill in the fourth mile, Bullock threw in a 4:56 mile to drop his teammates and most of the field with the exception of one unattached runner. In the final mile, MIT runners continued to move up, eventually taking the top four scoring spots. Bullock took the win in an excellent 25:51. Paez ran 26:17, Safai 26:26 and McKenna 26:37.</p>
              <p align="left">When the men's varsity took the line the rain had stopped. Number one runner, Roy Wedge '14, would not be racing, but for the most part the top seven was intact. This race proved to be quite competitive as MIT won with 63 points, with Springfield  close with 104. However, a healthy Roy Wedge would have cut at least 20 points off of MIT's score. Tech's entire pack was together at the mile and in great position at 5:06. In the second mile, Logan Trimble '13, had made a strong move with a 5:07 to move into the top four, while his teammates were back in 5:17. Allen Leung '15, rolled his ankle and lost 24 seconds getting it to the point where he could run again. Matt Jordan '15, also suffering from a cold, was not able to race his best, dropping to 5:25. In the uphill third mile, Trimble threw down a 4:57 as the lead group continued to separate themselves from the rest of the pack. The rest of MIT ran a very competitive 5:05-5:09 to move up well in the pack. Edgar Gridello '16, running very well as a freshman, got cramps in his quad and had to drop out. In the downhill fourth mile, Trimble continued to battle with the leaders, but the group stayed intact despite the 4:43 split. The rest of the MIT group split up as Benji Xie '15 ran 4:49, Ben Mattocks '13 ran 4:52, and Ian Tolan '16 ran 4:54 along with Leung. In the final mile Trimble took it down to the final 100 meters before he was outkicked, settling for third place in a very impressive 24:45. Xie finished in eighth in 25:11. Mattocks was not far behind in 14th in 25:24. Tolan placed 17th in 25:35 after suffering through a side stitch. Leung closed out the scoring for Tech, finishing in 25th in 25:55. Leung ran 15:08 for the final three miles after twisting his ankle, a very inspirational performance. The one disappointment for MIT was a delta of 1:10 for their top five, particularly on a flat course. They hope to lower that delta next week to under 45 seconds.</p>
              <p align="left">MIT, the fifth ranked team nationally, wanted to race together and show their depth, and was certainly on the way to doing that as they were in total control of the race, just off the lead at the mile mark in 5:41 while their second group was back at 5:50. However, in the second mile, which is a loop through a heavily wooded area that the lead vehicle did not go into, the lead runner went off course and all of MIT's runners went with her. After 70 meters or so, the mistake was realized and Tech's runners reversed their course to get back on the loop. However, at this point they were now in back of nearly 300 runners. Not knowing how far the leaders were ahead, Brooke Johnson '13, Sarah Quinn '16 and Elaine McVay '15 led the way for the Engineers to work their way through the immense pack of runners on a very tight loop in the woods. Somehow, by the two mile mark, MIT was back in front with Quinn leading the way and Martina de Geus '14 along with Johnson and McVay right behind. Four runners back of McVay was Dacie Manion '15 and Allison Hallock '16. In the final mile, Johnson caught Quinn, outkicking her to finish off a 5:38 final mile for a total time of 18:25 to defend her title from last year. Quinn was second in 18:30, McVay third in 18:34, de Geus fourth in 18:37 and Manion seventh in 18:44 to give MIT a score of 17 points. All MIT runners had to have lost at least 20 seconds on the error and quite a bit more on having to fight and weave through the entire field of runners with half the race already run. Nicole Zeinstra '16, one of the top five runners for MIT had to drop out with a hamstring injury.</p>
              <p align="left">The final race of the day, the women's sub-varsity had 13 runners entered for MIT. Tech's lead group got out well and led by Kaitlin Allen '14 came through the mile in 5:55. The second group was within reach at 6:03. By the two mile mark, MIT, as a team had pulled away from the opposing runners  with Allen well out in front in 11:58. Kali Benavides '15 was back in third at 12:18. Marie Burkland was in fifth at 12:21, Louise van den Heuvel '14 in seventh at 12:26 and Elena de la Paz '16 in eighth at 12:27. Allen did not let up in the third mile, running a 5:45 to finish the 5K in 18:16, winning by 27 seconds. Benavides was third in 18:57, Burkland fourth in 19:02, de la Paz seventh in 19:12, van den Heuvel eighth in 19:18, Marissa Stephens '16 ninth in 19:21, Shivani Kaushal '15 10th in 19:22, Frankie Perone '16 11th in 19:22, Heather Sweeney '16 12th in 19:29, Margaret Coad '15 14th in 19:39, Cindy Huang '15 in 19:50, Veronica Montgomery '16 17th in 19:59 and Natasha Gunther '16 18th in 20:01. Only five total runners broke up MIT's team among the 215 finishers.</p>
              <p align="left">Next week will not be so dominating as the Engineers go up against some of the best teams in the region and country. For the women, the #1, 2 and 6 teams will be going up against the 5th ranked Engineers. On the men's side there will be many top ranked teams as well. This will provide MIT with a sense of what they have to do to contend for the regional title in two months.</p>
