              <p><span class="headline">MEN'S AND WOMEN'S CROSS COUNTRY OPEN WITH  VICTORIES</span></p>
              <p align="left">September 1, 2012</p>
              <p align="left">Due to the closing of their home course, Franklin Park, for renovations, Tech had to move their Alumni/ae Meet or cancel it for 2012. The decision was made to move it to <a href="alumni.res.php">Keene State College</a>, who were also hosting an Alumni/ae Meet. Other colleges joining in the fray were UMass Lowell, Assumption, Fitchburg State, UMass Dartmouth, Clark, Norwich and Eastern Connecticut State University. The predicted temperature in the 70's turned out to be upper 80's and very humid, and of course, sunny.</p>
              <p align="left">The women ran first. The Engineer's Alumnae team was represented by Tania Morimoto '12 and Jacqui Wentz '10. The varsity raced 21 of the 28 members. Both Elaine McVay '15 and Claire O'Connell '14 were sick with colds.  Stephanie Marzen '15, Kelsey Jamieson '16, Katie Ellinger '16 and Oliva Papa '14 were returning from injuries and not quite ready to race and Sarah Quinn '16 was given the weekend off from racing.</p>
              <p align="left">Given that the race was only for two miles, Tech new they would have to get out quickly to make sure they kept everyone in sight on the twisting and turning course. At the mile mark, Alexandra Taylor '14, Brooke Johnson '13, Nicole Zeinstra '16 and Dacie Manion '15 were in a nice tight pack in fourth through seventh place, trailing Jeptui Cherutich from AIC as well as Morimoto and Wentz. 13 seconds back of their teammates in 10th-14th place, Allison Hallock '16 led her group of Martina de Geus '14, Heather Sweeney '16 and Kaitlin Allen '14.</p>
              <p align="left">During the second mile, Taylor made a great move, running the second mile seven seconds faster than the first mile to catch up the Cherutich and then proceed to outkick her to the line in 11:29. Johnson lost focus for a while, falling back a bit, but finally waking up and moving up to fourth behind Cherutich and Wentz. Zeinstra was only two seconds behind Johnson and Manion three more seconds back. Morimoto was next and Hallock closed out the scoring in 11:52, giving MIT the victory with 19 seconds and a delta of :23.</p>
              <p align="left">The men also earned the victory, outscoring the Keene State Alumni by a score of 27 to 38. The MIT Alumni, represented by Stephen Serene '12 (14th), Eric Khatchadourian '06 (26th), Matt Bieniosek '09 (45th), Ethan Crain '96 (56th), Fivos Constantinou '06 (58th), Paul Welle '11 (82nd), Richard Prevost '11 (83rd), Jack Bourbonnais '10 (97th), Bob Collins '82,  and Tom Hansen '74 (142nd), placed fifth out of the 11 teams with 145 points.</p>
              <p align="left">At the mile mark, Logan Trimble '13 led the way for MIT, running in fourth place in 5:04, while Roy Wedge '14, Benji Xie '15 and Edgar Gridello '16 were in seventh, ninth and 10th back at 5:06. MIT's second group, not exactly running together were Allen Leung '15, Matt Jordan '15, Ben Mattocks 'G, Justin Bullock '14, Ian Tolan '16, Rick Paez '15 and Patrick Juras '16, all between 5:10-5:14.</p>
              <p align="left">In the second mile, Wedge moved into second at 10:14 with Xie in third one second back. However, the Keene State Alumni, in addition to having a solid lead on first place, also had fifth, sixth and 11th. Trimble fell back to seventh, Gridello was feeling the effects of starting too quickly and Mattocks, Tolan and Jordan were moving on the leaders from 12th, 13th and 14th respectively at 10:29-10:30.</p>
              <p align="left">The final mile showed the MIT strength and determination. Both Wedge and Xie caught and passed the leader, earning the win and runner-up positions in 15:46 and 15:49 respectively. Four Keene State Alumni took the next four places. Tolan ran an excellent third mile, moving up the seventh in 16:16. Trimble was one second back in eighth and Gridello held on to ninth in 16:19 to close out the scoring for Tech. Jordan took 10th and Mattocks 12th as bumpers. MIT had a nice delta of :33 for runners 1-5. However, it will be interesting to see what both the men and women will do at the full distance of 5K and 8K in two weeks at the UMass Dartmouth Invitational.</p>
