              <p><span class="headline">WOMEN TOP DIVISION III TEAM, FIFTH OVERALL IN NEICAAA CHAMPIONSHIP</span></p>
              <p align="left">October 7, 2012</p>
              <p align="left">For as long as anyone can remember, the <a href="neicaaa.res.php">All New England Championship (NEICAAA)</a>, which pits the best college teams in all NCAA divisions (I, II and III) against each other, has always been run at Franklin Park, MIT's home course. This year, the park is undergoing renovation, so the race was moved to Stanley Park in Westfield, MA. Since there were already races scheduled for the Saturday, the NEICAAA Championship was moved to Sunday.</p>
              <p align="left">MIT's women's team was primarily focused on racing a conservative plan, and of course placing as high as possible amongst the Division I teams. A little dessert would be to defeat Division III, number one ranked Williams College, who had beat MIT two weeks earlier by five points.</p>
              <p align="left">Thirty-seven teams toed the line. While MIT wanted a well paced race, the first turn was 90 degrees and within a quarter mile of the start. After a half-mile the path narrowed to about 8-10 feet in width with very bad footing on large rocks and roots the entire course until the final 700 or so meters on the fields. So, getting out conservatively was a risk in that passing the runners could be a problem.</p>
              <p align="left">Exercising caution, Sarah Quinn '16, Elaine McVay '15 and Brooke Johnson '13, led their teammates through the start, hitting the correct pace, with one problem. The second group, which included Kaitlin Allen '14, Nicole Zeinstra '16, Martina de Geus '14 and Dacie Manion '15, had to stop as they approached the first turn, as many of the runners in front of them had fallen and backed everyone up. The group recovered, but it took some time. Quinn lost her discipline and attacked a little too early, coming through the mile in 5:34, having moved up to 28th place. McVay followed four places behind in the same time as did Johnson. The second group made up for their stall at the start, moving up, but still in 140th place at 5:59. Their patience paid off as a 6:01 second mile moved them up all the way to 65th-71st place. Quinn broke from her group, moving all the way to ninth with a 5:52 second mile. McVay moved up to 16th despite a 6:00 mile. Johnson, suffering from low serum ferritin, felt the effects of the fast pace, falling back to 6:10 pace. The second group continued to move up, passing about half the remaining field to move into the top 70. The final mile was difficult for Quinn as she started to pay for her fast first mile. Nevertheless she showed her mettle as she fought to a 5:50 third mile even though she fell back to 17th place at the end with a 17:50 5K. Quinn was the top Division III runner in the meet as well as the second freshman finisher, although three of the top DIII runners in New England were absent from the meet. McVay rallied with a strong finish to place 19th in 17:52. Allen continued her pace, actually running her fastest mile last, moving up to 51st place in 18:25. The other freshman varsity runner, Zeinstra was right behind in 18:28 in 55th place. Johnson held on barely for 56th place, also in 18:28 to close out MIT's scoring. MIT ended with 198 points for fifth place, 50 points ahead of Williams back in eighth.</p>
              <p align="left">In the women's sub-varsity race, Kali Benavides '15 ran an outstanding race, although she too went out a little too fast in the first mile. Her 18:21 was good enough for fourth place in the sub-varsity race, leading her team to second place, three points behind Williams, as Division III showed superior depth amongst the New England schools. Also scoring for MIT were Alexandra Taylor '14 in 12th place, running 18:38, Allison Hallock '16, placing 16th in 18:48, Marie Burkland '13, placing 21st in 18:57 and Cindy Huang '15, finishing in 22nd in 18:59. MIT had a pretty decent delta of :58 for the top 10 and :42 for the top seven. A total of 12 runners were under 19:00 on a fairly difficult course.</p>
              <p align="left">The men's team was hoping to show their true talent and race among the top teams in Division III. There is no question the talent is there and also no question the team was really ready to compete. However, the excitement was not contained and at the end of the first 400 meters, as they were about to enter the first turn, right in front of the pack were three of MIT runners. By the mile mark, they had settled back some, but most of the damage was done.  Even after settling down the first mile was still 4:49 for Benji Xie '15, Logan Trimble '13 and Ian Tolan '16, all in the top 30. Three to four seconds back were Ben Mattocks '13, Roy Wedge '14, Allen Leung '15 and Edgar Gridello '16, back around 90th place. In the second mile, which was still on the grass fields for the men's course, Xie and Trimble ran 5:19, falling back to ~65th place. The second group stayed up to Xie, passing many runners, with only Leung a little behind, but all ran around 5:23 for the second mile. Once they hit the woods  and uneven footing, only Xie and Mattocks moved up, with Xie finishing in 59th place in 25:30 and Mattocks one second back in 62nd place. After a large gap, Leung finished in 94th in 25:53. Gridello ran 25:58 in 99th position and Tolan was right behind in 100th in 25:59. Wedge was running a strong race, but fell and never fully recovered mentally from the spill. The varsity effort netted them a 13th place overall and fourth among Division III teams, although two strong Division III teams were not in attendance.</p>
              <p align="left">In the sub-varsity race, Matt Jordan, also going out way too fast, ran a relatively strong race, finishing in 14th in 26:01. The sub-varsity team finished in eighth with 202 points. Supporting Jordan was Justin Bullock '14, running 26:36 for 38th, despite a sore back, Rick Paez '15, running 26:38 for 41st place, Jay McKenna '14, running 26:54 for 57th place and Eric Safai '14, running 27:15 for 85th place.</p>
              <p align="left">MIT will be off from competition for three weeks, not competing until the NEWMAC Conference Championship on October 27th at Smith College. The women have won the past five years while the men have never lost the championship, racking up 14 straight victories.</p>
