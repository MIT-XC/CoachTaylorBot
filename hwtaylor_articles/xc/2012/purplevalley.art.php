              <p><span class="headline">#1 RANKED WOMEN FINISH SECOND IN PURPLE VALLEY CLASSIC</span></p>
              <p align="left">September 22, 2012</p>
              <p align="left">Five days after receiving the #1 ranking in Division III Cross Country by the college coaches in America, MIT's women lost to last week's #2 team, Williams, while defeating 13 others, including last week's #1 team, Middlebury. The end result on the hilly 6K course at the <a href="purpvalley.res.php">Purple Valley Classic</a>, had Williams with 78 points, MIT 83 and Middlebury 87. The next closest team in the 15 team field was Oberlin College with 121 points. </p>
              <p align="left">MIT's men never really got in the race, but ran a smart pace, allowing them to place relatively high. The highest place finisher for Tech in the 17 team field was Logan Trimble '13, who finished 16th in 26:56, 43 seconds behind the leader. The very challenging course took its toll, but the smart pacing by the Engineers allowed them to place fifth with 137 points and a 44 second delta through five runners.</p>
              <p align="left">The men raced first. The controlled pace by the Engineers put them in good position at the 1/2 mile mark, but by the mile mark they had already fallen off the pace, ranging in pace from 5:15-5:32. A 5:10 first mile would have been more appropriate for the top runners. The second mile, which has a very difficult hill, was run well by Tech. Benji Xie '15, MIT's top man at that point in the race, came through the two mile in 10:40, putting him in excellent position among the top runners. Trimble was only three seconds back, but then the next closest MIT runner was Ben Mattocks '13 at 10:50. The third mile, a flat mile, was a good one for Xie and Trimble, along with Allen Leung '15 and Edgar Gridello '16, all of whom ran 5:20 or better. The fourth mile was a repeat of the big hill and what everyone had saved up for. Xie dropped out at the three mile mark due to a glute problem, but all but three of MIT's runners put in a solid 5:15-5:32 mile. Other than the fact that the overall pace was too slow by MIT, the only other issue was the fact that the fifth mile, overall a downhill mile, was horribly slow (5:25-5:57) relative to their competitors. After Trimble, freshman Gridello was 26th in 27:07, Mattocks placed 29th in 27:16, Leung was 27:21 in 32nd place and Ian Tolan '16, running with a cold, placed 39th in 27:40. Roy Wedge '14, Tech's top runner had been out with an illness for the past seven days. This combined with Xie not finishing certainly made a difference in MIT's competitiveness.</p>
              <p align="left">The women were next, and despite the fact they were missing three of their top 10 runners, they were confident they could win the meet. MIT set themselves up for defeat in the first half mile as they went out at 2:52-3:02, which was about 5-10 seconds too fast per group given the difficulty of the course. By the time they were going up the slight hill towards the mile mark, they were already slowing down. So instead of entering mile two running 5:55-6:20 pace, they were actually now running 6:00-6:30 pace even though their mile time was faster than that. The second mile was also pretty flat so maintaining the pace would not have been a problem had they paced better. Freshman Sarah Quinn and Brooke Johnson '13 were about 10 seconds behind the leader at the two mile mark, but looking strong at 11:50 running in fourth and fifth place. Falling off the pace was Elaine McVay '15 in 12:06. After another 10 second gap, Tech had three more runners, Alexandra Taylor '14 in 12:14, Martina de Geus '14 in 12:15, and Kaitlin Allen '14 in 12:16. On the women's course, the hill was in the third mile. Quinn handled it pretty well, keeping her position, but slowing to a 6:11 mile. The rest of the Engineers suffered, paying for that fast first 1/2 mile. Johnson fell back three places, McVay and de Geus lost numerous places. Williams and Middlebury moved up on MIT considerably in the final mile. MIT reclaimed some places in the final 3/4 of a mile once they hit the field, but not enough to catch Williams. Quinn ended up fourth in 22:30, an excellent time and the best ever by an MIT runner on the 6K course. Johnson finished in eighth place in 22:49. McVay placed 14th in 22:59, Allen in 25th in 23:22 and de Geus in 32nd in 23:35 to close out MIT's scoring. The :55 second delta was far too much for a top ranked team and ultimately was the reason for the loss.</p>
              <p align="left">MIT will have next weekend off, but in two weeks, they will get another chance at both schools along with all the Division I teams in New England in the All New England Championship on Sunday, October 7. Hopefully all the ailing runners will return to top form for the Engineers.</p>
