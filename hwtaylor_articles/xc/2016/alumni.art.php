                  <p><span class="headline">Men and Women Dominate Alumni Meet</span></p>
                  <p>&nbsp;</p>
                  <p align="left">September 2, 2016</p>
                  <p align="left">Although a few individuals came into the pre-season with injuries sustained over the summer, the Engineers appear deep enough to weather the storm.&nbsp;&nbsp;The men's and women's <a href="alumni.res.pdf">Alumni -Varsity</a> races were 5K's (actually 3.2) held at historic Franklin Park on a sunny mid-70 degree day.&nbsp;The Engineers swept both races, winning by a score of 15 to 50&nbsp;over the alumni in each race.</p>
                  <p align="left">The nationally 7th ranked women raced first.&nbsp; Megan McCandless '20 and Jenna Melanson '20 wasted no time taking control of the pace.&nbsp; Although a little too fast, the freshman duo had a comfortable lead at the mile mark as they came through in 5:37.&nbsp; Leandra Zimmermann '19 and Mary Eccles '18 working together were :14 back in third and fourth.&nbsp; The alums, either out of race shape, or recovering from recent longer races, were more towards the back of the pack.</p>
                  <p align="left">McCandless opened up a :22 second lead on Melanson in the second mile as she attacked the major hill on the course to maintain her race effort.&nbsp; Zimmermann and Eccles were now :12 behind Melanson, and Katie Bacher '20, Jenny Xu '19, Marissa McPhillips '20 and Bailee Margolis '19 were another :15 back&nbsp;with no alums in front of them.</p>
                  <p align="left">In the final 1.2 miles, McCandless continued to widen the gap, eventually winning in 18:10 with a :50 lead.&nbsp; Melanson outkicked Zimmermann and Eccles as they ran 19:00, 19:00 and 19:06 respectively.&nbsp; Bacher closed out the scoring with a 19:12.&nbsp; Xu was also timed in 19:12&nbsp;and McPhillips was 19:18.</p>
                  <p align="left">Alina Gatowski '11 led the Alumnae with a 20:01 performance in 9th place.&nbsp; Louise van den Heuvel '14 finished in 11th place in&nbsp;20:05, just ahead of Nicole Zeinstra '16, also timed in 20:05.&nbsp; Brooke Johnson '13 placed 16th in 20:37 and Alexandra Taylor '14 placed 19th in 21:57 to close out the scoring.&nbsp; Maggie Lloyd '12 and Stephanie Marzen '15 were the final alumnae competitors in 22:16 and 22:38 respectively.</p>
                  <p align="left">The nationally 7th ranked men raced second.&nbsp; Similar to the women, the alums took a cautious approach to the race.&nbsp; Dennis Maloney '19 wasted no time getting out front.&nbsp; At the mile mark, Matt Deyo 'G joined him at 4:44.&nbsp; Josh Derrick '20 and Ben Freed '18 were very close at 4:46&nbsp;with Daniel Weiss '19 one second back.&nbsp; </p>
                  <p align="left">At the end of the second mile, Maloney had separated himself from the pack at 9:58, a four second lead over Deyo, Derrick and Freed.&nbsp; Weiss fell back to 10:10 as Steven Goldy '20 moved past him at 10:07.&nbsp; Tech had seven more runners between 10:12 and 10:22, showing their depth.</p>
                  <p align="left">Maloney allegedly made a wrong turn somewhere allowing the gap to close&nbsp;in the final mile.&nbsp; Nevertheless he maintained his lead to the end, winning in 15:45.&nbsp; Deyo did his best to close, finishing in second at 15:47.&nbsp; Derrick was third in 15:49 and Freed fourth at 15:54.&nbsp; Goldy was unable to close the gap over the last mile, but did not lose much either, finishing fifth in 16:01.&nbsp; A 12 second gap separated Goldy from the sixth man, Alex Knoedler '18.&nbsp;David Walter '18 was seventh at 16:16 and Colin Godwin '17 was eighth at 16:18.&nbsp; Freshman Nate Foss '20 was one second back.</p>
                  <p align="left">The alums top finisher was Benji Xie '15, who finished 17th overall in 16:52.&nbsp; Roy Wedge '14 was 19th at 17:41, while Matt Jordan '15 was 20th in 17:48.&nbsp;&nbsp;The remaining alums were Logan Trimble '13 in 18:11, Eric Khatchadourian '06 in 18:37, Joe Roy-Mayhew '08 in 19:26 and Bob Collins '82 in 21:03.</p>
                  <p align="left">MIT will not be back in action until September 24 when they race in the Purple Valley Classic at Williams College&nbsp;where many nationally ranked teams will do battle over the hilly 6K and 8K courses.</p>
