                  <p><span class="headline">Men and Women Both Finish 2nd in Border Battle</span> </p>
                  <p align="left">October 15, 2016</p>
                  <p align="left">MIT traveled to Rowan University in Glassboro, NJ for the weekend to compete in the annual Border Battle cross country race.&nbsp; Both the men's and women's races had numerous nationally ranked teams.&nbsp;In the women's race, #1 ranked SUNY Geneseo, #2 ranked MIT, #4 ranked Johns Hopkins, and nine other teams ranked between 11th and 30th among the 45 total teams would do battle.&nbsp;On the men's side, #1 ranked SUNY Geneseo, #5 MIT, #11 RPI and eight more teams ranked between 13th and&nbsp;34th&nbsp;among the 43 total teams would see who was the best.</p>
                  <p align="left">Both the men and women finished second for MIT, however, the races could not have been more different.&nbsp; The women were set to race first on the very flat 6K course with temperatures in the upper 60's with bright sunshine.&nbsp; The ground was hard so there was no question the pace would be fast, and Amy Regan was there to guarantee that.&nbsp; Winning four national titles last year (3K, 5K indoors and 5K, 10K outdoors), the 2014 cross country champion likes to take the pace out very hard to totally discourage her competition from going with her.&nbsp; With an if it isn't broken, don't fix it mentality that is exactly what she did.&nbsp; The downhill start got her going as she hammered the first mile in 5:08.&nbsp; MIT's Megan McCandless '20 was trying to get up to her, but by the mile was still back four seconds in 5:12 and in third place.&nbsp; While this was going on up front, Johns Hopkins entire team was near the front, taking everyone by surprise.&nbsp; The glut of MIT runners were back at a quick 5:26 mile, but already out of the race for first.&nbsp; Their original sights had been on SUNY Geneseo&nbsp;and that battle seemed to going well.</p>
                  <p align="left">By the two mile mark, McCandless was 10 seconds behind Regan and still in third place at 10:50.&nbsp; Maryann Gong '17 was leading the MIT pack at 11:17 with Katie Bacher '20 having a solid race in 11:21.&nbsp; Leandra Zimmermann '19 was close behind in 11:23, but not having her usual race.&nbsp; Mary Eccles '18 was at 11:24 trying to form the tandem with Zimmermann that worked so well in 2015.&nbsp; Jenna Melanson '20, normally MIT's #2 runner was not having a good day, letting a reduced week of mileage get to her head as she hit 11:29.&nbsp; Christina Wicker '17 and Jenny Xu '19 were back at 11:31 and 11:32 respectively.</p>
                  <p align="left">Although MIT held steady with Geneseo for the final 1.73 miles, a lot of switching of positions took place.&nbsp; McCandless finished in third in 20:44 for 5:34 pace per mile.&nbsp; Gong was 16th in 21:20 showing she is on her way back.&nbsp; Bacher ran 21:50, finishing in 36th and Zimmermann was in 38th in 21:54.&nbsp; Xu moved up nearly 10 seconds on Bacher and Zimmermann in the final .73 for 42nd place.&nbsp; Wicker also moved up well as she gets back in condition from her injury.&nbsp; Melanson and Eccles went the other direction with Eccles developing a side stitch before the 4K mark.&nbsp; </p>
                  <p align="left">In the end, Johns Hopkins won easily with 70 points.&nbsp; MIT squeaked by Geneseo 134 to 136 for second place.</p>
                  <p align="left">The men also went out fast, dictated as much by the start as the talented field, but not as insanely fast as the women.&nbsp; Matt Deyo 'G, Dennis Maloney '19, Daniel Weiss '19 and Josh Derrick '20 were up with the front pack at 4:36.&nbsp;Racing without #4 runner Colin Godwin '17 and #5 runner Steven Goldy '20 the&nbsp;pressure was on for someone to step up in this race. In the early going that honor was earned by Alex Knoedler '18 and Josh Rosenkranz '19 as they both came through in 4:41.</p>
                  <p align="left">Through three miles Deyo and Maloney were right up at the front at 14:11.&nbsp; Weiss&nbsp;had fallen back a bit, but was still in it at 14:16.&nbsp; Derrick was starting to fade (14:16) after thinking too much when he heard the two mile split of 9:32.&nbsp; Knoedler began to pull away from Rosenkranz at 14:30 to 14:33.</p>
                  <p align="left">The hope of defeating Geneseo was pretty much over after three miles, but MIT had a solid lead over the other teams.&nbsp; In the final two miles&nbsp;the lead group broke up as Isaac Garcia-Cassani of Geneseo pulled away&nbsp;to win in 23:19.&nbsp; Deyo finished in eighth despite a severe case of plantar fasciitis, running 23:42 on what was presumed to be a 7.8K distance.&nbsp; Maloney was in ninth in 23:43.&nbsp; Tech had a big gap of :38 seconds before Weiss came through in&nbsp;24:21 in 29th place.&nbsp; Derrick placed 34th in 24:24 and Knoedler came though just as he did last year&nbsp;at the end of the season, finishing in 47th in 24:36.&nbsp; Nicholas Waltman '18 ran a 4:47 final mile to move up to sixth for MIT, placing 59th overall in 24:42. MIT scored 127 points to Geneseo's 71 to earn runner-up.&nbsp; Third place went to RPI with 168 points.</p>
                  <p align="left">MIT is off next weekend as they prepare for their NEWMAC Championship at Smith College on October 29.</p>
