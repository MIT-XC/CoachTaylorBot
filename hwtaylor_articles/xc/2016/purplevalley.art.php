                  <p><span class="headline">Women 2nd, Men 3rd at Purple Valley</span></p>
                  <h4>Freshmen McCandless and Derrick Shine</h4>
                  <p>&nbsp; </p>
                  <p align="left">September 24, 2016</p>
                  <p align="left">Despite an auspicious start, the men's and women's cross country teams from MIT performed very well in their first race of the year, the women placing just six points out of first among seven of the top 11 teams in the country, and the men placing third among nine nationally ranked teams and 23 teams in all for both genders at the <a href="purplevalley.res.php">Purple Valley Classic</a>.&nbsp; En route to&nbsp;the meet, the bus MIT was taking was overheating.&nbsp; Fortunately, the bus company's headquarters was close by.&nbsp; Between the multiple stops for the bus to cool down and the delay to deviate from the route and transfer buses, the cost was about 30 minutes.&nbsp; The men were forced to alter their normal warm-up routine but did not seem to let it affect them.</p>
                  <p align="left">For late September, the mid-60 degree temperature&nbsp;and partly cloudy skies were as good as one could expect.&nbsp; The ground was a little soft, but overall the course was in great shape.&nbsp; With MIT's women only having a practice meet against their alumni under their belt, this was trial by fire.&nbsp; They were about to see if they deserved their number five ranking nationally, even though they were not running All America runners Maryann Gong '17 and Christina Wicker '17.&nbsp; The nationally ranked competition included host Williams (1), SUNY Geneseo (2), Tufts (6), Middlebury (8), UW Lacrosse (10), RPI (11), Ithaca (14), Amherst (21), and Dickinson (30) among the 23 colleges racing in the women's 6K race.</p>
                  <p align="left">From the start, the pace was reasonable for the conditions--quick, but not too much so.&nbsp; At the mile mark&nbsp;a group of seven or so, including MIT's Megan McCandless&nbsp;'20 as well as three from SUNY Geneseo, one from Middlebury and one from Williams were slightly in front, at 5:55, of a chase pack loaded with Williams, SUNY Geneseo and MIT runners, among others.&nbsp;During the latter part of the second mile MIT's chase group moved up in the pack as the lead group pulled away a&nbsp;bit.&nbsp; At the two mile mark, McCandless was pretty comfortable at 11:48 while MIT's chase group of Leandra Zimmermann '19, Jenna Melanson '20, Mary Eccles '18 and Katie Bacher '20 were all at 11:56, although it appeared Bacher was struggling to stay up.&nbsp; </p>
                  <p align="left">The third mile was where the race would be decided as it went into the woods and then up a major hill, followed by&nbsp;a steep downhill.&nbsp; About halfway up the hill McCandless pulled away, splitting the third mile in 5:57 despite the hill.&nbsp; Her teammates fell off with 6:20 to 6:40 third miles, but continued to race and be competitive.&nbsp; McCandless held her lead, winning in 21:58 on the very challenging course.&nbsp; Zimmermann finished in 11th in 22:35, Melanson 14th in 22:39, Eccles 18th in 22:47 and Bacher 27th in 23:04.&nbsp; Geneseo placed their runners 6, 7, 16, 17, 19 to just beat Tech by six points.&nbsp; Williams, who seemed to be without a couple of runners, was third with 100 points.</p>
                  <p align="left">MIT's men's team was also quite young, but  quite deep as well.&nbsp;This was also their first race, outside of their Alumni Meet, and a big jump in distance for all the freshmen, going from 5K in high school to 8K in college.&nbsp;&nbsp;MIT, ranked number six in NCAA Division III, were up against SUNY Geneseo (2), Williams (3), Amherst (10), Colby (16), RPI (19), NYU (25), Tufts (28) and Dickinson (30).</p>
                  <p align="left">Unlike the women's race, the men went out pretty fast at the start.&nbsp;After the half mile they settled into a more conservative pace, given the men have to ascend the big hill twice.&nbsp; MIT was packed pretty well among the lead group, but they did not seem relaxed, almost as if they were a bit too anxious.&nbsp;&nbsp;Josh Derrick '20, Matt Deyo 'G, Colin Godwin '17 and Dennis Maloney '19 came through the mile in 5:07 and Steven Goldy '20 was close behind at 5:12.&nbsp; </p>
                  <p align="left">The second mile included the first time the men ran the big hill.&nbsp; MIT's top seven ran the mile in 5:17-5:22 to stay in contention.&nbsp; In the third mile, which was flat, Tech runners collected themselves and ran well.&nbsp; Derrick and Maloney came through in 15:30 and Godwin and Deyo were right behind.&nbsp; Goldy was eight seconds back, but looking good.&nbsp;Daniel Weiss '19 and Alex Knapp '19 were closing ground, hitting the three mile at 15:46 and 15:47 respectively.</p>
                  <p align="left">The fourth mile hit the hill for the second time and ended anyone who had pushed the pace early in the race.&nbsp; Derrick was a monster on this mile, separating himself from his teammates with a 5:13 mile.&nbsp; Deyo, Godwin, Maloney, Goldy and Weiss were all in the 5:20's as they lost contact with the leaders.&nbsp; Derrick continued his fast pace to&nbsp;the finish, running a 5:00 last mile for an eighth place 25:43, the fourth fastest time ever run by an MIT runner on this course.&nbsp; Deyo ended up in 15th place in 25:58, Godwin 17th in 26:00, Maloney 22nd in 26:06 and Goldy 26th in 26:14.&nbsp; Weiss and Knapp held on well to finish in 32nd and 36th respectively in 26:25 and 26:27.&nbsp; Geneseo finished with an impressive 48 points with Williams in second with 64 points.&nbsp; MIT was solid, but back at 89 points&nbsp;to beat out Amherst with 107 points.</p>
                  <p align="left">The women will have to work on closing their gap between McCandless and the rest of the team, so perhaps Gong and Wicker can take care of that.&nbsp; The men will have to get more front runners at the finish line if they hope to be among the top teams at nationals.&nbsp; Both teams have next weekend off and will next compete at the NEICAAA All Division New England Championship on October 8 at Franklin Park.</p>
