                  <p><span class="headline">MIT Men and Women Continue Stronghold on NEWMAC Cross Country Championship</span> </p>
                  <p align="left">October 29, 2016</p>
                  <p align="left">MIT dominated the <a href="https://www.tfrrs.org/results/xc/10783.html">NEWMAC Cross Country Championship</a>, hosted by Smith College, the NCAA fifth ranked men winning their 19th consecutive title and the NCAA second ranked women taking their 10th straight championship.&nbsp; Due to a significant storm on Thursday, the meet had to be delayed three hours so the Smith College grounds crew could clear the fallen trees and branches from the course.&nbsp; They did a marvelous job, leaving the course in excellent condition.&nbsp; </p>
                  <p align="left">With temperatures in the mid-50's, the men raced first in the 8K race.&nbsp; The course had four significant hills so the Engineers played it safe through the first two miles of the race, running mile splits of 5:25 and 10:44.&nbsp; This took the team through two of the big hills and at that point they took the lead and picked up the pace&nbsp;significantly.&nbsp; There were three WPI runners, one Babson College runner and seven MIT harriers in that lead group.&nbsp;</p>
                  <p align="left">After a third mile of 4:49, Matt Deyo 'G, Dennis Maloney '19, and Josh Derrick '20 had a three second lead over Matthew Dynan '19 of Babson, Austin Scott '17 of WPI, Alex Knoedler '18 of MIT and Rory Beyer '17 of MIT.&nbsp;Ben Freed '18 and Daniel Weiss '19 were another four seconds back, giving chase.</p>
                  <p align="left">In the final two miles, Deyo, Maloney&nbsp;and Derrick fought it out as they hit the final two hills.&nbsp; Deyo finally opened up a lead in the final downhill as he came out of the woods going full speed.&nbsp; His final mile of 4:47 was too much for Maloney who finished three seconds back, 25:26 to 25:29.&nbsp; Deyo earned Runner of the Year honors for his victory.&nbsp; Derrick finished in third&nbsp;in 25:32, taking home the Rookie of the Year honors.&nbsp; Dynan and Scott broke up the MIT sweep, earning fourth and fifth places respectively.&nbsp;&nbsp;Beyer had gapped Knoedler along with Dynan and Scott on the fourth mile, but cramped and backed off, losing the lead.&nbsp; Knoedler took sixth in 26:05 with Beyer earning seventh in 26:07&nbsp;to give MIT 19 points.&nbsp; Freed closed well to finish in eighth in 26:11 with Weiss taking ninth in 26:15.&nbsp; Aidan Gilson '19 and Nicholas Waltman '18 claimed 12th and 13th respectively in 26:19 and 26:21 to earn All Conference awards.</p>
                  <p align="left">The women's 5K course  had three major hills, so a similar strategy was employed, letting others set the pace through the second of the three hills, which was about at the halfway point in the race.&nbsp; At the mile mark (6:09) there was a pack of 12 runners, Edie Sharon '17 of Wellesley, Casey Hecox '19 of Smith, eight MIT runners (Megan McCandless '20, Mary Eccles '18, Maryann Gong '17, Christina Wicker '17, Leandra Zimmermann '19, Jenny Xu '19, Katie Bacher '20 and Jenna Melanson '20) along with two runners from WPI.</p>
                  <p align="left">After the second hill, McCandless took things into her own hands, dropping the pace so that she opened up a four second lead by the two mile mark at 12:08.&nbsp;Eccles and Gong were right behind in 12:12 with Wicker at 12:16 and Zimmermann at 12:21.&nbsp; Melanson, Bacher and Xu had fallen off the pace, trailing Sharon of Wellesley.</p>
                  <p align="left">In the final 1.1 miles&nbsp;not much changed other than McCandless throwing down a 5:32 to open up a 24 second victory in 18:17 over Eccles (18:41) who had an amazing race, attacking the uphills and downhills.&nbsp; Gong was third in 18:42.&nbsp; Wicker stayed close, finishing fourth in 18:46 and Zimmermann finished the sweep with her 18:54.&nbsp; Xu finished strong while Bacher and Melanson faded in the final mile, giving Xu seventh place behind Sharon&nbsp;in 19:04.&nbsp; Bacher was two places back in 19:14 and Melanson two more places back in 11th in 19:17.&nbsp; Rounding out the All Conference team&nbsp;were MIT runners Marissa McPhillips '20 finishing 12th in 19:19, Clemmie Mitchell '18 in 13th at 19:30, Darby LaPlant in 14th at 19:34 and Tessa Weiss '20 in 16th at 19:39.&nbsp; McCandless took home both Runner of the Year and Rookie of the Year awards.</p>
                  <p align="left">MIT takes a week off from racing as they prepare for the NCAA Division III New England Region Championship / NCAA Division III Qualifier on November 12 at Westfield State College.</p>
