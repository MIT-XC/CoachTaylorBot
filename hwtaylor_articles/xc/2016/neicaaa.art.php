                  <p><span class="headline">Men Place 3rd Overall Among Division I Teams</span></p>
                  <h4>McCandless Places Fourth in Women's Race</h4>
                  <p>&nbsp; </p>
                  <p align="left">October 8, 2016</p>
                  <p align="left">In just the second race of the season, MIT entered the Division I waters as they competed in the <a href="NEICAAA.res.php">NEICAAA Championship</a>.&nbsp; The organization is comprised of the all the Division I, II and III teams in New England, hosting a championship in each of the three seasons (cross country, indoor track and field, outdoor track&nbsp;and field).&nbsp; Not all of the teams enter the meet as there are many meets around the country various teams point to, and this particular championship is not&nbsp;a prerequisite for any other meet or championship.&nbsp;&nbsp;This year, of the 35 teams racing 21 were Division I, eight were Division II and only six were Division III.&nbsp; In the men's race, the quintet of Matt Deyo 'G, Josh Derrick '20, Dennis Maloney '19, Daniel Weiss '19 and Steven Goldy '20 scored a low total of 133 points to finish a historic third, 20 points behind Brown and 31 points behind Northeastern.&nbsp; The women, led by freshman star Megan McCandless's fourth place individual finish, placed fifth for the second year in a row with 159 points, just two points behind Northeastern, seven behind Vermont, and 22 behind Harvard.&nbsp; The winning team, New Hampshire, won easily with 71 points.&nbsp; In both races MIT was easily the top Division III team.</p>
                  <p align="left">The women's race was first on a day that offered a very dry course, but a mostly cloudy and very humid 70 degrees to make the performances slower than usual.&nbsp;The Engineers had a pretty solid start, but Katie Bacher '20 got a little carried away, sprinting across the field, opening up a 15 meter gap on her teammates.</p>
                  <p align="left">At the mile mark, McCandless had put herself in a solid position of 16th place in 5:28.&nbsp; Jenna Melanson '20, Jenny Xu '19 and Bacher were together between 60th and 70th place in 5:36 while Leandra Zimmermann '19 and Mary Eccles '18 were in about 100th position at 5:44.  Maryann Gong '17, racing for the first time after her recovery from a stress fracture this past Summer, was in 120th in 5:48.</p>
                  <p align="left">By the two mile mark all had moved up nicely. McCandless was in a solid fourth place, by herself, about 60 meters or so behind the top three.&nbsp; Melanson had moved up to 30th place with Xu close behind in 35th.&nbsp; Zimmermann and Bacher were back at 45th and 46th.&nbsp;Eccles was in 88th with Gong in 92nd.&nbsp;</p>
                  <p align="left">Working alone must have hurt McCandless a bit as she was unable to move up, losing ground to the leaders and dropping back to fifth for a short period during the third mile.&nbsp;A solid finish brought her across the line in fourth place in 17:57, the best time ever by an MIT runner&nbsp;on a course slightly longer than 5K.&nbsp; Melanson maintained her place of 30th and Xu moved up with her, finishing in 31st, both earning times of 18:33. Zimmermann closed a lot, finishing in 40th in 18:39.&nbsp; Gong closed out the scoring with an 18:51 in 54th place.&nbsp; Eccles finished sixth for Tech in 71st overall at 19:00 while Bacher paid for her fast start with a finish in 80th place in 19:10.&nbsp; The women performed well, showing why they are ranked second in NCAA Division III.</p>
                  <p align="left">In the women's sub-varsity race, MIT also placed fifth with 133 points.&nbsp; Christina Wicker '17, racing for the first time after a long recovery from a hamstring injury, led the way in 19:14.&nbsp; Clemmie Mitchell '18 was close behind in 19:17.&nbsp; Tessa Weiss '20&nbsp;(19:28), Bailee Margolis '19 (19:30) and Darby LaPlant '19 (19:44) were the other scorers.</p>
                  <p align="left">The men, led by Deyo were very aggressive&nbsp;and were somewhat aided by an unusually well paced effort by&nbsp;the leaders.&nbsp; In what is usually a sub 4:35 first mile, was a comparatively slow 4:44.&nbsp; Deyo, Derrick and Maloney worked exceptionally well together, coming through the first mile in 4:47 around 25th place.&nbsp; The rest of the top seven, Weiss, Goldy, Alex Knoedler '18 and Alex Knapp '19 were clustered around 120th in 4:56.</p>
                  <p align="left">With both groups working so well together&nbsp;they easily moved up in the field.&nbsp; By two miles, Derrick was up to seventh place and Deyo and Maloney were right behind in 10th&nbsp;and 11th.&nbsp; Weiss and Goldy were around 45th place after a strong second mile.&nbsp; Knoedler and Knapp fell off the pace by 10 seconds or so.</p>
                  <p align="left">During the third and fourth mile, particularly at the end of the fourth mile, Deyo pulled away slightly from his teammates but lost ground overall to the leaders as he fell back to 12th place.&nbsp; Derrick and Maloney stayed together, but had also fallen back to 16th and 17th place.&nbsp; Weiss and Goldy were also working really well together&nbsp;through three miles when they had both moved up into the top 40, but separated through the wilderness loop as they fell back into the mid-40's.</p>
                  <p align="left">In the final mile, Deyo moved back to 10th place to finish in 25:08.&nbsp; Derrick and Maloney stayed together but lost more ground, dropping back to 21st and 22nd respectively, both in 25:21.&nbsp; However, Weiss caught back up to 39th and Goldy finished very well to nab 40th place, their times both 25:38.&nbsp; The :30 delta was quite impressive, showing the NCAA Division III ranking of 5th in the country is very deserving.</p>
                  <p align="left">Although Knoedler and Knapp did not have their best days, 26:22 and 26:50 respectively, a few of their teammates had a very solid showing in the sub-varsity race.&nbsp; The men nearly pulled off the sub-varsity team victory, losing a five point decision to Amherst, 87 to 92.&nbsp; Leading the way was Josh Rosenkranz '19 finishing in eighth in 26:10.&nbsp;Nicholas Waltman '18, running a very ambitious race, held on for 11th in 26:13.&nbsp; Brian Gilligan '17 ran a very even paced race to finish 22nd in 26:25.&nbsp; Aidan Gilson '19 ran his best collegiate cross country race, placing 31st in 26:37.&nbsp; Closing out the scoring was a come from behind effort by Cooper Sloan '18, placing 41st in 26:44.</p>
                  <p align="left">Overall, it was a great day for the Engineers.&nbsp; Next weekend, MIT travels to Rowan University in Glassboro, NJ for the &quot;Border Battle&quot;, a meet with&nbsp;a large number of nationally ranked Division III teams.                  </p>
