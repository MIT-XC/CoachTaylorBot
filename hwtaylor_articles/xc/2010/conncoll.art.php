                  <p style="font-size: large; font-weight: bold;">Women Place Well Despite Lack of </p>
                  <p style="font-size: large; font-weight: bold;">Varsity Runners </p>
                  <p align="center">October 16, 2010</p>
                  <p align="left">This Saturday, the MIT Engineers traveled south to the<a href="conncoll.res.php"> Connecticut College Cross Country Invitational</a> at scenic Harkness Memorial State Park. The nineteen team field showcased teams from many New England states, as well as New Jersey and New York. The course was flat and relatively firm given the amount of rain the day before, the temperature was excellent in the mid 50s, but the winds were extremely powerful, gusting in excess of 30mph. </p>
                  <p align="left">The men were up first and without any of the top six runners racing for MIT, no one from Tech challenged for the lead. MIT's top runners, Andrew Erickson '12, and Logan Trimble '13, came through the first mile at 5:10, along with teammates Jay McKenna '14, and Eric Safai '14, but the prescribed group running ceased to exist after that, as it appeared to be every man for himself. </p>
                  <p align="left">Erickson threw in a strong second mile to move up through the field, at one point being around the top 20, but then faded a bit in the third mile. Trimble ran steady but slowed a bit each mile. Joe Christopher '12, and Matt Weaver '12, caught up to the fading McKenna and Safai at the end of the third mile, moving up to third and fourth respectively for MIT.</p>
                  <p align="left">In the last mile Trimble moved past Erickson by a couple of seconds, but Erickson found his kick and beat him to the finish by one second in 26:45 for 37th place. Trimble was two places back and Christopher was 58th in 27:07. Weaver finished in 71st place in 27:16 and McKenna held on to 75th position in 27:20 to give the Engineers 253 points, good for ninth place in the 19 team / 225 person field. </p>
                  <p align="left">The women were represented by one of their varsity runners, Alina Gatowski '11, and those vying for the seventh position on the team. This was the first 6K, the championship distance for women at the NCAAs, for most of the women on the team. The ladies paced themselves quite well, running near even splits and moving up well throughout the race to end up with a fine third place finish out of the 18 team field, scoring 93 points.</p>
                  <p align="left">Gatowski was cautious in the beginning but by the mile mark was with the leaders at 5:46. The rest of the MIT pack, were not as together as they were supposed to be. For some reason the group running plan seems to evade this particular group of athletes. Maggie Lloyd '12, was leading this group, coming through the mile in 6:11, but Olivia Papa '14, who had missed a couple weeks of consistent running with a minor leg injury and a cold, seemed to not be into the race at all and was one second back. Sarah Sprague '13 and Sherry Fu '14, were two seconds behind Papa, but not really working together.</p>
                  <p align="left">While not running as well together as they should, it was obvious by the end of the second mile that the MIT runners were racing well. The second mile was very competitive for the Engineers with Gatowski sitting in the lead with two others and Lloyd actually speeding up by one second. Sprague and Fu only slowed down by four seconds in the second mile, so were still moving up through the field which was slowing down at a much more significant rate than they were. </p>
                  <p align="left">At the three mile mark, Gatowski had to settle for second position as she could not stay with the leader on this day. She tried to close the gap over the final three quarters of the mile but was unable to do so, finishing in a fine time of 22:26, a 5:58 average per mile. Lloyd's third mile was yet another second faster than the previous mile and she closed well to finish in seventh place with a time of 23:11, easily her best race ever. Sprague and Fu placed 25th and 27th respectively, running 23:46 and 23:48. Papa stopped the bleeding by picking up the pace a little in the final mile to finish in 31st place to round out the scoring for the Engineers. </p>
                  <p align="left">Plugging in MIT's varsity runners into the fields, using the typical deltas from the competing MIT runners, would have resulted in solid victories for the Engineers in both races. </p>
                  <p align="left">MIT has the next weekend off from racing as they prepare to defend their respective conference titles in the NEWMAC Championship, held in Paxton, MA at Moore State Park. </p>
