                  <p><strong><span class="headline">MEN WIN REGIONALS, WOMEN PLACE FOURTH</span></strong></p>
                  <p><strong>Both Teams Advance to NCAA Division III Championship</strong></p>
                  <p align="center">November 13, 2010</p>
                  <p align="left">MIT competed in the <a href="ned3.res.php">NCAA Division III New England Regional Championship</a> this weekend at the Williams College course at Mt. Greylock High School to try to advance to the NCAA Division III Championship on November 20. </p>
                  <p align="left">With temperatures in the 50's on a bright sunny day and about two dozen MIT alumni and current teammates cheering, the men toed the line first. Forty-nine complete teams were entered and finished in the race with nationally ranked teams among them from Middlebury (11th), Williams (13th), Brandeis (21st), Tufts (24th), Keene State (27th) and Bates (31st). MIT, seeded ninth in the region, and not ranked nationally since a mid-season race when two of the top five were recovering from colds, was not considered one of the favorites.</p>
                  <p align="left">It seemed the Engineers decided they wanted a little respect. Getting out well at the gun, Tech established themselves towards the front of the race. At the mile mark, the MIT men were all among the top 30 and looking very strong. The hilly second mile only reinforced the strength of MIT as they stayed towards the front as others dropped off the pace. The third mile, somewhat downhill had Paul Welle '11, with the leaders and Dan Harper '12 and Roy Wedge '14, in the top 12. Stephen Serene '12, and Richard Prevost '11, began to fade a bit but not much. Gihan Amarasiriwardena '11, was having an exceptional race, staying in the top 25. Joe Christopher '12, who had begun to struggle in the second mile, lost significant time on this mile. The hilly fourth mile took its toll on many runners, but MIT runners held strong for the most part. Welle ran a very hard mile to stay with the leaders, while Harper and Wedge lost some ground but held onto their places. Serene and Amarasiriwardena lost a little more ground but not enough to take them out of the top 30. The final mile, slightly downhill coming out of the woods and then around a large open field to the finish saw Welle fade from the leaders, but kicking hard at the end to finish in sixth in 25:57. Harper and Wedge worked well together, staying the course and finishing 11th and 12th. Serene rallied to hold onto 29th place and Amarasiriwardena saved the day with a great 35th place. All five earned All New England honors and their total of 93 points beat host Williams, at 101 points, for the championship.</p>
                  <p align="left">The women, although co-ranked seventh in the country, did not seem as confident as the men as they prepared for the start. There were 51 complete teams in the race and 336 finished. Other nationally ranked teams were Middlebury (1st), Williams (3rd), Amherst (16th), Colby (26th), Wellesley (28th) and Tufts (35th). While they did get out well, Katherine Eve '12, Claire O'Connell '14, and Anna Holt-Gosselin '11, may have expended a little too much effort. At the mile mark Eve had fallen back quite a bit, but Alina Gatowski '11 and Tania Morimoto '12, were right with the leaders. Holt-Gossselin, injured most of the year, was running well and staying among the top 20. Freshmen, Martina de Geus and O'Connell were five and nine seconds back but looking fresh. Maggie Lloyd '12, was only three seconds back of O'Connell and certainly among the top 40 runners. During the hilly second mile, Morimoto opened up a gap on Gatowski as she stayed with the leaders in 11:40. Eve moved back up to third on the team in 12:03 with Holt-Gosselin and de Geus right behind. The third mile, by far the hilliest on the 6K course, was also the slowest for all MIT runners. Morimoto was no longer with the leaders but she was holding on to her place. Gatowski fell 11 seconds back of Morimoto but was also holding onto her place as the field spread out. Eve and de Geus held their own but Holt-Gosselin, O'Connell and Lloyd all lost more ground and some places. The final .72  went pretty well for MIT. Morimoto and Gatowski fought hard and stayed in the top 10, finishing 8th and 9th respectively in 22:41 and 22:42. Eve finished with an excellent kick to move up to 21st in 23:02 and de Geus was only 16 seconds behind in 31st place. Holt-Gosselin missed All New England by one place, finishing 36th. MIT totaled 105 points to finish fourth, two points behind Amherst College. Williams finished in second place with 91 points and Middlebury showed why they are ranked first in the country, scoring 58 points.</p>
                  <p align="left">Next week the Engineers travel to Waverly, Iowa, where <a href="http://www.ncaa.com/sports/c-xc/champpage/c-xc-div3-index-2010.html">Wartburg College will host the NCAA Division III Championships</a>.</p>
