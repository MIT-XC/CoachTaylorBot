              <h3>Women Win Pre-Nationals Behind Collins, Men Fourth</h3>
          </div>
            <p>October 5, 2019</p>
          <p>MIT entered the <a href="http://www.rtspt.com/events/2019/lc/">Louisville Cross Country Classic / Pre-National championship</a> as favorites to win on the women's side and seeded fourth on the men's side, at least according to the Nationals polls.&nbsp; Despite a disaster in the women's championship 6K, where the lead vehicle took the runners on&nbsp;an incorrect path after the 3.9K mark, due to a lapped runner and the&nbsp;wrong gate on the course being open, the women won easily, although only calculated through the 3.9K mark.&nbsp; The men indeed finished fourth, but not&nbsp;quite the way they planned for.</p>
          <p>MIT brought 10 runners per gender, so most anyone with a chance to be on the team that hopes to make Nationals in seven weeks, would have a chance to see the course.&nbsp; Only seven were permitted in the Varsity race (Silver), so MIT had three runners in the Blue race, which was&nbsp;a 5K for women and the normal 8K for men.</p>
          <p>The men raced first and were represented by Ryan Wilson '22, Vedang Lad '23 and Zach Johnson '22.&nbsp; All three got out really well, and at the mile mark, all three were in the front pack at 4:50.&nbsp; As the race progressed, Wilson continued to race well, particularly in the final two miles where he ran 5:04 and 4:45 respectively to run 25:04 and place 15th.&nbsp; Lad lost some focus in the middle of the race but finished with a 4:45 for a 25:20&nbsp;and 31st overall.&nbsp; Johnson also seemed to lose focus, but did not really get it back as he finished 53rd in 25:40.</p>
          <p>The women's Blue race was next.&nbsp; Alana Chandler '22, Marissa McPhillips '20 and Anna Chau '23 represented MIT in the 5K race.&nbsp;Chandler ran her best cross country race yet, running 5:54 and 6:01 through two well paced miles.&nbsp; She fell off a bit in the final 1.1 of the course, but led MIT with a 47th place finish in 19:09.&nbsp; McPhillips, stayed close, finishing 64th in 19:19.&nbsp; Chau did not race to her potential, finishing 190th in 20:17.</p>
          <p>The men, ranked 11th nationally in Division III, were seeded behind #1 North Central College, #5 UW LaCrosse,&nbsp;and #9 Johns Hopkins.&nbsp; The goal was to get out and race with the top teams, knowing rankings do not always accurately portray a team's actual performance level.&nbsp; At the mile mark, Andrew Mah '22, Sanjay Raman '23 and Cameron Kleiman '23 were out with the front pack in 4:46 and 4:47. Sam Acquaviva '23 and Lowell Hensgen '23 were right behind in 4:49.&nbsp; Back a little were the old men on the team, Finn Jacobson-Schulte '21 and Steven Goldy '20.&nbsp; Up to this point in the season MIT's top five has been dominated by freshmen and sophomores.</p>
          <p>Through the second and third miles, Mah, Raman and Kleiman stayed within two seconds of each other and continued to race among the top 10 individuals.&nbsp; Acquaviva, suffering from tight calves, and Hensgen having a poor physical and mental day, fell back badly.&nbsp; However, Jacobson-Schulte and Goldy were racing well, about 20 seconds back at three miles.&nbsp; </p>
          <p>Mah pulled away from his teammates in the fourth mile and Jacobson-Schulte and Goldy lost a little more time.&nbsp; However, all finished well in the somewhat downhill final mile.&nbsp; Mah closed with a 4:47 to finish 10th in 24:44.&nbsp; Raman threw down a 4:43 to move up to 17th in 24:51 and Kleiman ran 4:44 to move up to 20th in 24:53.&nbsp;&nbsp;Jacobson-Schulte was :26 back in 47th place&nbsp;and Goldy three places further back in 25:21.&nbsp; The total score of 144 put MIT fourth.&nbsp; North Central won with 85 points and Johns Hopkins was second with 116 points.&nbsp; The surprise team was Otterbein in third (nationally ranked 14th), just ahead of MIT with 127 points, while UW LaCrosse was seventh&nbsp;with 201 points, just behind 16th ranked RPI with 194, and John Carroll, also with 194 points.&nbsp; Acquaviva finished 71st in 25:42, and Hensgen finished 115th in 26:15.</p>
          <p>The Women's Silver race was the final collegiate race of the day.&nbsp; The temperature, which started out at 60 degrees for the Gold race, was now about 73.&nbsp;MIT was seeded first, ranked seventh nationally.&nbsp; RPI was ranked 11th, UW LaCrosse 13th, Oberlin 14th, St. Thomas 15th, Hope 16th and a few other colleges ranked among the top 30 in the country.</p>
          <p>MIT got out well, but certainly not in front.&nbsp; However, by the mile mark, MIT was front and center with Katie Collins '21 and Megan McCandless '20 leading the way in 5:45, along with Katie Bacher '20 right behind.&nbsp; Kirsi Rajagopal '23, Einat Gavish '22 and Anna Haddad '23 came through in 5:47. &nbsp;Katie Williams '21, not feeling her best was back at 5:50.&nbsp; </p>
          <p>At the two mile mark, Collins had begun to pull away, but only by 10 meters or so as she ran a 5:37 to gap her opponents and teammates.&nbsp; Rajagopal and McCandless were 10 seconds back at 11:32 and Bacher and Gavish were together at 11:35.&nbsp; Haddad was falling back as her hip began to bother her.&nbsp;</p>
          <p>At 3.9K of the 6K race, the Gator went the wrong way, taking the runners with it.&nbsp; By the time the Gator stopped and the runners began to turn around&nbsp;to go back the right way, Collins had run over 200 meters in the wrong direction.&nbsp; When she turned around she was now behind all the runners she was ahead of, in 125th place.&nbsp; The race continued to the finish with some part of the course cut off.&nbsp; Although they completed the race with Collins moving all the way back up to 25th and MIT still winning, the Games Committee and officials agreed the only fair way to call the race was to end it at 3.9K.  MIT finished with 43 points, while Oberlin was second with 115 points.&nbsp; St. Thomas was third with 192 and Hope College was fourth with 202.</p>
          <p>Individually, Collins won in 13:50, five seconds over second place.&nbsp; Seventh and eighth were Rajagopal and McCandless in 14:06 and 14:08 respectively.&nbsp; Gavish was 11th in 14:13 and Bacher finished out the scoring in 16th in 14:15.&nbsp; Williams was 50th in 14:34 and Haddad 92nd in 14:58.</p>
          <p>MIT's next race is on October 19th at the Conn. College Invitational.</p>
