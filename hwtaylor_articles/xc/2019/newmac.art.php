              <h3>MIT Men and Women Defend NEWMAC Crowns</h3>
              <h4>Gengaro and Mah Claim Individual Titles</h4>
          </div>
            <p>November 3, 2019</p>
            <p>On a near perfect weather day at historic Franklin Park (site of the 1992 World Cross Country Championship and two USA Championships), MIT kept their&nbsp;<a href="newmac.res.php">NEWMAC Cross Country Championship</a> streak alive, with the men winning their 22nd consecutive championship (the only team to have ever won the championship) and the women bringing home the trophy for the 13th time in succession.</p>
            <p>The men's race was delayed 20 minutes due to the Coast Guard Academy bus breaking down half-way to Boston.&nbsp; However, everyone was made aware early enough that it did not affect the race in any manner.&nbsp; Once the race was underway, MIT's Andrew Mah '22 and Sanjay Raman '23 led the way, although a bit slower than planned, coming through the flat first mile in a pedestrian 5:03.&nbsp; With such a slow pace, the front was bunched up much more than usual, and MIT had 13 runners either at 5:03 or 5:04.</p>
            <p>Although they did not speed up, but rather maintained their effort through the hilly second mile, Mah and Raman pulled away from most of the pack, and those now remaining were Ryan Wilson '22, Matthew Kearney '22 and one opposing runner, David Reynolds '23 of WPI.&nbsp; MIT's next pack, Finn Jacobson-Schulte '21, Josh Derrick '20 and Steven Goldy '20 were only six seconds back.&nbsp; In-between was Vedang Lad '23 at 10:21.</p>
            <p>Throughout the third and fourth mile, Mah and Raman pulled away a little more, Jacobson-Schulte, Derrick&nbsp;and Goldy were closing the gap on all opposing runners, and Lad, Wilson and Kearney were falling back a little.</p>
            <p>In the final mile, Mah pulled away with a strong 4:44 final mile to gap Raman by eight seconds, 25:22 to 25:30.&nbsp;Reynolds took third place to break up the MIT sweep, but the next six runners wore MIT colors, led by Jacobson-Schulte's 25:48.&nbsp; Close behind were Derrick (25:51), Lad (25:53), Wilson (25:58), Kearney (25:59) and Goldy (26:04).&nbsp;</p>
            <p>MIT's 18 points easily won out over WPI's 45.&nbsp; Third was Coast Guard with 99.&nbsp; Springfield followed in fourth with 122, then Babson with 156, Wheaton at 158, Emerson with 212 and Clark with 228. </p>
            <p>Mah earned NEWMAC Runner of the Year and Raman claimed Rookie of the Year honors.</p>
            <p>The women's start was changed at the last minute, going from 12:10 to 12:15, which caused some runners to panic a little.&nbsp; When the race finally went off, there&nbsp;was not too much of a lead group as MIT's top group took it out in 5:43.&nbsp; Izzi Gengaro '22, Katie Bacher '20 and Katie Collins '21 were running well together, and Einat Gavish '22 was close behind at 5:45.&nbsp; Quite a few opposing runners were braving the early pace, but that disappeared in the second mile.</p>
            <p>Without her teammates; Megan McCandless '20 and Kirsi Rajagopal '23, both out with illnesses, Gavish was running alone, trying to stay close to her teammates, but she lost 14 seconds in the second mile.&nbsp; Of course so did most everyone else as Gengaro, Bacher and Collins came through two miles in 11:47.&nbsp; Staying between the group of Gengaro et al, and Gavish were the Rieders twins; Hannah and Madeline, from Mount Holyoke College.&nbsp; Closing in on the other top opponents were Katie Williams&nbsp;'21, Marissa McPhillips '20 and Alana Chandler '22.&nbsp; </p>
            <p>After three miles and entering the last .73 where they had to run Bear Cage Hill once more, Gengaro was pulling away and looking very comfortable.&nbsp;Bacher was running strong as was Collins.&nbsp; Gavish was trying to close in on the twins, but could not narrow the gap.&nbsp; Williams was working on passing two opposing runners remaining between her and Gavish, and McPhillips was trying to do the same.</p>
            <p>In the end, Gengaro pulled away to win in 21:58.&nbsp; Bacher was second in 22:19 and Collins was third in 22:27.&nbsp; After the twins, Gavish was sixth in 22:57 and Williams placed seventh in 23:01.&nbsp; While 1:03 is not the best spread for 1-5, the addition of McCandless and Rajagopal should remedy that a bit.&nbsp; McPhillips was in ninth in 23:12 for her best race of the season.</p>
            <p>MIT finished with 19 points to 80 for Mount Holyoke.&nbsp; Wellesley was third with 92, followed by WPI with 116.&nbsp; In fifth was Coast Guard with 141, Smith College was sixth with 142, Wheaton was seventh with 182, Springfield was eighth with 195, Babson was ninth with 217, Emerson was 10th at 328 and Clark was 11th with 333 points.</p>
            <p>Gengaro was honored as NEWMAC Runner of the Year.</p>
            <p>In two weeks, MIT heads to Bowdoin College for the NCAA Division III New England Region championship.&nbsp; The top two teams earn an automatic birth to the NCAA Division III National championship, with other top finishing teams possibly earning at-large births to the 32 team championship in Louisville, KY.          </p>
