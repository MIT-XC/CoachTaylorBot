                  <p style="font-weight: bold; font-size: 18px">McVay's 12th Place Leads Women to National Runner-up</p>
                  <p style="font-weight: bold; font-size: 18px">Wenck's Places 4th, Men Place 8th</p>
                  <p align="left">November 22, 2014</p>
                  <p align="left">MIT entered the <a href="ncaa.res.html">NCAA Division III Championship</a> ranked number two in the country for the women and eighth in the country for the men. Both teams thought they could better their seed and were determined to run aggressively enough to make that happen. In the end, they placed exactly as seeded, the women earning their highest place ever with second place and the men tying for their highest place ever at eighth place. </p>
                  <p align="left">The normally fast course at King's Island Golf Center in Mason, Ohio endured a snow storm early in the week and then freezing weather all week, leaving residual snow on the course until race day. With hundreds of runners practicing on the course Thursday, Friday and warming up on Saturday, combined with the warming temperatures on race day, the course quickly became a mud pit, slowing times significantly.</p>
                  <p align="left">Although relatively flat, the soft course would take a lot out of the runners legs and anyone who went out fast would pay dearly. The plan for MIT was to get out quickly off the line, evaluate the field and ease into the top 20-50 by the mile mark, moving up from there. The plan worked well for Spencer Wenck '15, Colin Godwin '17 and Matt Deyo '16, but not so well for Allen Leung '15, Benji Xie '15, Rory Beyer '17 and Matt Jordan '15. Wenck was out with the leaders hitting the mile in 4:45. Godwin and Deyo were in the top 30 at 4:50 and Beyer and Xie were in the top 70. Leung and Jordan were pretty far out of it as they never got out at the start and were very near the back of the entire field of 280 runners after the first 200 meters.</p>
                  <p align="left">Things continued to look very good up front as Wenck, Godwin, Deyo and Xie moved up in the field through the halfway point in the race with Wenck in fourth, Godwin and Deyo in 29th and 30th and Xie in 62nd, but Beyer fell back to the 80's. On the plus side Leung moved up to around 100th and seemed to be on the move. Just before the four mile mark Wenck was still in fourth and Godwin and Deyo moved up to 24th and 25th. However, the wheels started falling off the bus for Xie who had fallen all the way back to 92nd. Leung moved up to 96th, but could he move up enough. If they could hold on or move up a little the Engineers had a shot at the podium where the top four teams are awarded trophies.</p>
                  <p align="left">In the final mile Wenck moved into third but was caught at the line giving him fourth place in 23:58. Godwin finished in 22nd and Deyo 25th, both earning All America honors with times of 24:30 and 24:32 respectively. Leung lost 25 places in the final mile and Xie over 50 places, finishing 121st and 145th respectively at 25:29 and 25:35. Beyer, who had been shoved to the ground and banged his head, never really recovered, finishing 185th in 25:49 and Jordan was right behind in 190th in 25:50. With the top 35 receiving All America honors, Wenck, Godwin and Deyo all earned All America status, a record number for MIT. Amazingly, all three had colds going into the race, but did not let it bother them.</p>
                  <p align="left">The women had the unfortunate selection of going after the men, and running on a course that was totally wrecked. Even with half inch spikes they were getting little traction. The plan was for Sarah Quinn '16 to go out towards the front and work her way up to the leaders by the mile mark. Maryann Gong '17, Christina Wicker '17 and Elaine McVay '15 were going to get into the top 30 by the mile and work their way up. Nicole Zeinstra '16, suffering from a quad injury and Kali Benavides '15 were hoping to be close behind and trying to beat every team's fifth runner. Mary Eccles '18, a freshman, was trying to even pace the race and be there if anything happened to Tech's front runners.</p>
                  <p align="left">The first mile played out as planned with three exceptions. First, Quinn went out too hard, getting caught in front of a chase group focused on catching a breakaway runner and coming through the mile in 5:38. The second was Zeinstra and Benavides not getting out off the line and then getting squeezed to a walk in the first 200 meters. The third and worst error was Zeinstra panicking and moving up to the top 60 or so in the first mile, spending all of her energy in a five minute span.</p>
                  <p align="left">By the 2K, Quinn was still in fourth, Wicker and McVay in 14th and 15th, and Zeinstra and Gong in 36th and 40th respectively. MIT was narrowly winning the race over favorite Johns Hopkins. At 3K, Zeinstra began to fade and the score at the halfway point was tied at 79 between Hopkins and MIT.</p>
                  <p align="left">In the second half of the race Quinn began to tie up and Zeinstra continued to free fall. On the plus side, Benavides was moving up slowly but surely. At the end of the race, McVay moved up to 12th in 22:00 for 6K, earning her second cross country All America honor. Quinn held on to 16th place in 22:10 and Wicker pulled out a 17th place in 22:12. Gong finished strong to place 32nd in 22:24, giving MIT four All America winners. Hopkins had a two point lead through four runners so it came down to the fifth runner and they had a better finish. Fortunately Benavides moved up to 67th to save MIT's second place finish over a hard finishing St. Lawrence University.</p>
                  <p align="left">Given where both teams started, the season was a fantastic success for the Engineers. Losing four of the top seven for the men will challenge this year's freshman class to step up next year if they want to continue to improve on the national scene. The women will lose two of their top seven but have reasonable depth for next year.</p>
