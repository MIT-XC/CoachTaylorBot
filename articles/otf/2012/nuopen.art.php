              <p><span class="headline">DISTANCE RUNNERS SHINE</span></p>
              <p align="left">March 24, 2012</p>
              <p align="left">A small handful of distance runners kicked off the 2012 MIT Outdoor season at the <a href="nuopen.res.php">Northeastern University Spring Open</a> at their Solomon Track in Dedham. There are not many opportunities to run fast 10K's in favorable weather conditions for distance running, and this early season meet tends to have temperatures in the upper 40's to low 50's and is generally overcast--perfect conditions!</p>
              <p align="left">Dan Harper '12 and Roy Wedge '14 went into the race with the intention of making the pedestrian New England Division III standard of 33:40 and possibly the NEICAAA standard of 32:40.24. The first 400 was 71 and Harper was right with the leader while Wedge said no way and decided to stick with 76 for his first lap, hanging with the second pack already vying for third place. Harper continued to run with Brad Mish of the Boston Athletic Association, who was setting a crisp pace, coming through the mile in 4:53, the two mile in 9:50 and the three mile in 14:48. At this point Harper took the lead after Mish had run a couple of 75 second quarters, picking the pace back up to 74. They came through the four mile in 19:46 and Harper slowly began to pick up the pace, moving to 73 pace at the end of mile five for a 24:42. He tried to drop Mish but was unable to do so even after a mile of 72's, hitting the six mile in 29:31. A final lap of 65 finally allowed Harper to pull away for the win and personal best by a minute in 30:37.38, a time that may be good enough to make the top 20 times in Division III, which would qualify him for the NCAA Division III Championship.</p>
              <p align="left">Wedge, for his part, had a solid battle for fourth place, outlasting his opponent in 32:21.39, qualifying for the above mentioned championships as well as the ECAC Division III Championship. Wedge kept the pace at 5:11 per mile throughout the race.</p>
              <p align="left">Dacie Manion '15 was also in the race as the meet combined the men and women in this race, the only running event  the NCAA allows qualifying times when the genders are combined. Manion, running her first 10K, wanted to run 6:00 miles and did a remarkable job of doing so, hitting mile splits of 5:56, 5:59, 5:59, 6:03, 6:03 and 6:09 as she thought she was fatiguing at the end. However, she ran an 83 final 400 to show she actually had quite a lot left. Manion placed fourth in 37:33.47, missing the freshman record by 17 seconds.</p>
              <p align="left">A few of the steeplechasers and milers needed to get a NEDIII 5K qualifying time in since they get few if any opportunities to do so. Stephen Serene '12, Justin Bullock '14 and Maggie Lloyd '12 were MIT's representatives.</p>
              <p align="left">Serene and Bullock needed a 15:30 performance and were well on their way, running 4:55 and 4:56 for the first two miles. At this point, Serene kept up the pace, running another 4:54 while Bullock gave in to the pain for a couple of laps, slowing to a 5:08 third mile. Bullock, like Manion, realized he had too much left, finishing with a 34 final 200 to just miss the qualifying mark with 15:34.72. Serene was able to finish strong, placing fourth in 15:18.05.</p>
              <p align="left">Lloyd did not fair so well. She started out in front, running a solid pace of 88 second quarters to hit the first mile in 5:53. As the two leaders kept up that pace, Lloyd did not feel she had it on this day, slowing to 90 second pace for the second mile and then a 6:09 for the third mile. However, her 18:47.06 was safely under the 18:56.48 qualifying standard for NEDIII.</p>
              <p align="left">MIT travels with a little less than half the team to Westmont College in Santa Barbara, CA for their Spring Break Meet on March 31, finishing a good week of training in the warm California climate.</p>
