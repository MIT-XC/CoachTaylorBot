              <p><span class="headline">Women Manage Easy Victory While Men Succumb to Outstanding Bates Squad</span></p>
              <p align="left">April 14, 2012</p>
              <p align="left">MIT Track &amp; Field traveled three plus hours to Waterville, Maine on Saturday to compete against Host <a href="mittri.res.php">Colby College, Bates College, University of Southern Maine and Husson University</a> (women only). The site of the meet rotates annually between Bates, Colby and MIT and this was the year to go to the furthest non-championship travel destination for the MIT team.</p>
              <p align="left">It seems each year the team travels to Colby some key performer misses the early bus (8:00 a.m.) due to oversleeping. This year it was Sheldon Trotman '15, who was expected to score big points in the LJ, TJ, HJ and 100 Meter Dash. As it was, Tech was missing a very unusual amount of star athletes due to injury or illness. While there are always a handful of injuries on any team, most are of the nagging type that can be managed throughout a season, but many of these are enough to put the athletes out for a few days to a few weeks and no two are the same. Even the women were down a few key bodies, but not enough to derail the train.</p>
              <p align="left">The MIT approach entering the meet was to go after Bates College, winners of the Men's Indoor New England Division III Championship. Bates has a very strong and well balanced team. It would take a healthier and more complete MIT team than got off the bus, but since you never know what will happen once the gun goes off, the Engineers were going to try their best for the victory without putting anyone in jeopardy. For the women, as long as MIT competed well they should have no difficulty recording the win.</p>
              <p align="left">When all was said and done the women won handily with 198.5 points to 152 for Bates, 146.5 for Colby, 130 for Southern Maine and 44 for Husson University. The men managed a second place with 183 points to 254 for Bates, 113 for Colby and 98 for Southern Maine.</p>
              <p align="left">The women won eight events, with Portia Jones '12 pulling down three of the victories, preferring to save her energy at the start of the races to make for dramatic victories at the finish line in  the 100, 100HH and 200. The weather was to the benefit of the sprinters, with 65 degrees and gusts in excess of 30 mph. Jones ran times of 12.41, 14.46 and 25.25, all season bests. Jamie Simmons '12 ran her first 100 of the year to warmup for her 400IH and ran an impressive 12.52, good for number two on MIT's all-time list. She then proceeded to run a season best of 62.39, despite slightly stuttering the first seven hurdles, to win the event. Brooke Johnson '13 ran her first 5000 of the year, a 17:35.1 PR under windy conditions, good for 26th on the NCAA Division III list. The women's other victories were in the field events. Preethi Vaidyanathan '15 competed for the first time this season, winning the High Jump with a freshman record of 5-1 3/4. Joanna Chen '15 worked through her problems in the Pole Vault to win at 10-11 3/4. Tilly Taylor '15 made it two in a row with another victory in the Javelin. She again threw over 130', this time a 132-7.</p>
              <p align="left">Other performances of note for the women were another freshman record in the 800 by Cindy Huang. This week, Huang lowered the record to 2:19.53 as she claimed second place. Alexandra Taylor '14 made her debut in the 3000 Steeplechase, running a very fine 11:32.89 for fourth place and the sixth fastest time in MIT history. Kali Benavides '15 smashed her previous personal best in the 5K, running 18:34.4 for seventh place.</p>
              <p align="left">The men again had no one in the Triple Jump or Long Jump and only one thrower in the Shot Put, Discus and Hammer Throw. Nevertheless, Tech won seven events. Tyler Singer-Clark '14 was the only double winner, taking both the 200 and 400 Meter Dashes. In the 200 Singer-Clark used the little wind that remained at that time of the meet and broke 22 seconds for the first time, clocking 21.97. In the 400, he had to run into the full force of the strong winds earlier in the meet, crossing the line in 50.54. In both events Nick Diamantoni '15 finished second (22.40, 51.01). Dan Harper '12 remained undefeated in the 5K and 10K. Just planning on winning the event, Harper took over the lead once the initial fast pace had begun to slow, making it his job to pull teammate Roy Wedge '14 to a New England Division III qualifying time of sub 15:30. Hammering 73 second quarters, Harper ran 15:19.12 with ease and Wedge followed in second place with a 15:21.72. The other running event victories came in the two relays, the 4x100 and 4x400. In the 4x100, Tech ran their third different anchor leg in this the third meet of the season. This week, the honors went to Matt Falk '12 when Trotman missed the bus. Falk had never practiced the event and only got in two run-throughs in the warmup. DJ Ronde '13 did not get out well and then leaned forward too much to run his top speed. Singer-Clark took off a little late and Ronde ran up on him. Singer-Clark made up a lot of ground on the field, putting Diamantoni in good position. Diamantoni put MIT in the lead and Falk took off perfectly, but then suddenly put the brakes on and looked back. By the time he got moving again, Diamantoni had caught him and had to wait for Falk to get into the zone. Somehow they maintained the lead and Falk showed he has some speed, maintaining the lead to the finish line. In the 4x400, a makeshift group was thrown together and they all stepped up very nicely. David Way '13 led off with a 52.4, much better than his open 400. Justin Bullock returned from the 800 to run a fine 51.0. John Thomas '15, running his first 400 leg of the year, turned an impressive 50.5 to bring MIT even with Colby. Sam Parker '15 took the baton, running on the heels of the Colby anchor until the final turn. Using much better form than the end of the 800 Parker slowly moved past his opponent to win by .02 in 3:24.77.</p>
              <p align="left">In the field events, Thomas pulled off a win in the High Jump by clearing 6' 2&quot; and narrowly missed 6' 4&quot;. The other victory was earned by Cameron Wagar '15 in the Javelin. In his first outdoor meet, Wagar threw 191-2 for a new freshman record.</p>
              <p align="left">Other impressive performances were had in a few different events. Parker ran 1:55.91 in the 800 to just miss out on first place honors. Eric Safai '14 continued to improve in the Steeplechase, this week running more than a 10 second PR to place fourth in 9:46.12. In the Pole Vault, Yida Gao '13 and Colin Hong '15 both cleared season bests of 14-1 1/4 to place third and fourth. Although CK Umachi '12 did not win any events, he competed well, throwing season bests of 176-10 in the Hammer and 151-4 in the Discus. Also competing in the Javelin, Harry Rein '15 threw 165-0 for third place.</p>
              <p align="left">Next week, MIT will send a few athletes to Princeton's Larry Ellis Invitational to race in the 800, 1500, 3000SC, and 5K. The remainder of the team will either rest up for the start of the championship season in two weeks or race in MIT's Spring Invite.</p>
