              <p><span class="headline">TRACK &amp; FIELD SHINES AT SPRING INVITE, LARRY ELLIS MEETS</span></p>
              <p><span class="bigtitle">NUMEROUS PERSONAL BESTS ACHIEVED AS TEAM PREPARES FOR CHAMPIONSHIPS</span></p>
              <p align="left">April 22, 2012</p>
              <p align="left">MIT hosted their <a href="springinvite.res.php">Spring Invitational</a> on Saturday, playing host to 13 other teams under sunny 75 degree skies with fairly strong wind conditions. However, the day before, a handful of Tech runners made the trip to Princeton to run in the <a href="larryellis.res.php">Larry Ellis Memorial Distance Festival</a>. Kyle Hannon '13 led the way with an NCAA qualifying time, running a 1500 PR of 3:50.73, a mark sure to get him to the National Championship. Stephen Serene '12 also ran a personal best, his in the 3000 Steeplechase. Although he seemed to lose focus and fall off after the mile mark, he recovered to finish in 9:20.32. Maintaining his focus would get him a spot at the NCAA's in May. Sam Parker '15 continued his excellent freshman year, racing the 800 and finishing in 1:54.00. The lone representative for the women was freshman Elaine McVay. She went out in 80 for the first 400 and then settled into 82 second pace until the final lap of the first 3200 meters. At this point she slowed to 84 pace and one disastrous 89 lap before rallying to finish in a new personal best and lowering her own Freshman Record to 17:15.42, a mark that may get her to Nationals.</p>
              <p align="left">Back at the MIT track, Dan Harper '12 was going to try to improve his 10K time, hoping to move up on the NCAA list and solidify a place in the NCAA Championship. He had early morning 65 degree temperatures, but a challenging wind to deal with. With the help of Brad Mish of the BAA, the pace was constant and right on target, running 73 seconds per lap as they traded off the first three miles. The three mile split was 14:40, having slowed to 74's in the third mile. The fourth mile was back on pace but Harper was on his own as Mish could not hold the pace. His fifth mile was 4:53 and the sixth a 4:55 as fatigue was obviously setting in. His final 400 was uncharacteristically slow for Harper. However, running 71 seconds showed he still had something in the tank. His 30:30.57 moved him to number 14 on the NCAA Division III list with a month to go to qualify.</p>
              <p align="left">In the 1500, both Kali Benavides '15 and Jay McKenna '14 ran personal bests. Benavides ran splits of 78, 80 and 83 before closing well to finish in 4:58.56. McKenna took the lead at the start despite the tough wind conditions. This led to a faster pace, but he definitely was set up for the kill by those drafting off him. Even after being passed he did not give up, fighting for every place, finishing in 4:02.88.</p>
              <p align="left">In the High Hurdles, Brian Djaja '13 got tough, shook off his doubts about his hamstring and competed well. Although the rust was definitely there he still ran a season best of 15.87. Portia Jones '12, got off to a good start and was very aggressive, but she over-rotated her lead arm coming back, throwing her off balance enough to slow her time. Nevertheless she won in 14.76.</p>
              <p align="left">In the 400, Maddie Bairey '15 returned from her quad injury, joining recently returned Martha Gross '12. Gross won the event in a well run 59.22 and Bairey ran a very competitive 60.56. On the men's side DJ Ronde '13 ran an excellent PR of 50.63.</p>
              <p align="left">The 800 was one of the best events for MIT. For the women, Cindy Huang '15 and Alexandra Taylor '14 placed second and third. Huang set the freshman record for the third consecutive week, this time running a complete 800, but still learning how to relax when trying to kick while fatigued. Her 2:16.47 led Taylor to the line in 2:16.60, a huge personal best for the sophomore. Pat Marx '13 returned to competition for the first time since indoor nationals. Even though he is not in the best condition, the competitive spirit was certainly there. He went out in 56 for the 400, moved up well entering the second lap, and finished with a very strong final 100 to win in 1:54.24.</p>
              <p align="left">The next event, the 400IH, was an excellent one for the women. Jamie Simmons '12 and Portia Jones '12 placed 1-2 in the race, running 61.89 and 62.12 respectively. Both got out well with Jones leading the entire race until the final 80 meters. Both pushed with all they had but Simmons just won out. They are currently ranked 2-3 on the NCAA Division III list.</p>
              <p align="left">In the field events the good performances kept on coming. Nathan Peterson '12 was able to compete in the Pole Vault for the first time since the Indoor NCAA Division III Championship. His ankle still not 100% from the sprain that took place in late March, Peterson only used a four step approach, but still cleared 14-9. Also in the vault, Yida Gao '14 and Colin Hong '15 set season bests of 14-3 1/4. For the women, Lauren Kuntz '13 won the event in a jump-off at 12-1 1/2.</p>
              <p align="left">Sheldon Trotman '15 joined those competing for the first time outdoors this season. In the Long Jump, Trotman leaped 20-7 3/4. In the Triple Jump, Trotman went 43-7 1/4 to get second behind teammate Jackson Wirekoh '14, who also just returned from injury. Wirekoh jumped 45-1 3/4 to win the event. Trotman also jumped 6-1 1/4 in the High Jump, anchored the 4x100 relay and ran a 11.37 100.</p>
              <p align="left">Preethi Vaidyanathan '15 improved her own freshman record in the High Jump to 5' 3.</p>
              <p align="left">In the throws, Cameron Wagar '15 and Tilly Taylor '15 both won their respective Javelin competitions. Wagar threw 187-5 and Taylor 139-9. CK Umachi '12 won the Hammer with a 176-0 throw, and added a personal best in the Discus with a 158-1 performance. Christine Labaza '14 had a great meet, throwing personal bests of 115-1 in the Discus and 36-0 in the Shot Put. Ellen Liverpool '15 threw a personal best of 125-6 in the Hammer and Ashley Wheeler threw personal bests in the Discus of 93-3, and the Javelin in 93-3.</p>
              <p align="left">Next week begins a five week series of championships. First on the list is the NEWMAC Championship at Smith College. The MIT women are favored to win the title for the fourth time in succession while the men are seeded second to Springfield College although MIT has won the meet every year since 2001.</p>
