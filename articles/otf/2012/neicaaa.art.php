              <p><span class="headline">JONES, HARPER HAVE STELLAR WEEKEND PERFORMANCES TO LEAD MIT</span></p>
              <p align="left">May 11-12, 2012</p>
              <p align="left">2012 was one of the rare years where both the <a href="ic4a.res.php">IC4A Championship</a> (the oldest and most prestigious conference in the country) and the <a href="neicaaa.res.php">NEICAAA Championship</a> (All Division New England Championship) fell on the same weekend. Many schools are members of both organizations and had to choose or split their squads. MIT chose to send two males to the IC4A meet and the remainder to the NEICAAA meet, which was hosted by MIT.</p>
              <p align="left">Dan Harper '12, continued his quest to qualify for the NCAA Division III Championship 10K. Already having run 30:37, 30:30 and 30:20, Harper keeps getting passed on the NCAA list, where only the top 20 performers are chosen to compete. He entered the IC4A field hoping the competition would be adequate to finally allow him to get some help for the entire race. The pace began extremely slow, coming through the 1200 in 3:47, until someone picked up the pace just short of the first mile. From that point on the lead pack ran 71-72 pace until 800 meters to go. With two laps remaining, Geoghegan of Syracuse made a break away and only Harper responded. That next to last lap was a 69 for Harper with the final circuit being run in 64, but it was not enough to win as Harper settled for second place in a new PR of 30:07.44, a time that ranks 13th on the NCAA Division III list and one Harper is willing to sit on with one week to go to qualify.</p>
              <p align="left">Back at MIT, at the NEICAAA Championship, day one had a number of outstanding performances despite the strong winds. MIT was resting some athletes and simply trying to qualify others for the NCAA Championship, choosing to not run the 4x100 or 4x400 in the women's competition and certainly not trying to maximize points.</p>
              <p align="left">In the field events, CK Umachi '12 placed fourth in the Hammer Throw, throwing 184-2 after nearly fouling out in the trials. Lauren Kuntz '13 continued to lock on 12-1 1/2, placing second. Kuntz did have two very good attempts at 12-5 and should be ready for that height at Nationals. Joanna Chen '15 matched her personal best with an 11-1 3/4.</p>
              <p align="left">On the track, Portia Jones '12 had a busy day, running three events; the 100HH, 200 and 400 meter dashes. In the hurdle trials, despite a slow start, Jones corrected her form and raced well to a season best 14.32 to qualify for finals. In the 400, she ran casually, easily making the final in 57.19. Her 200 was the best this outdoor season, a 24.58 to again make the finals. Martha Gross '12 also ran a season best in the 200, a 25.54, very close to her 25.40 PR.</p>
              <p align="left">Jamie Simmons '12 had her fastest 400IH of the season, despite stuttering nearly every hurdle. Her 61.31 shows she can easily break the minute barrier if she can visually set up the next hurdle after going over the previous hurdle.</p>
              <p align="left">Saturday was a very sunny, and even hot day by New England standards, with temperatures in the 80's even though there was a strong wind throughout the day. The men ended the competition with 39 points, good for sixth place overall as three MIT athletes earned second place finishes. The women combined for 26 points.</p>
              <p align="left">Nathan Peterson '12 earned second place with a 16-4 3/4 vault, nearly making 16-9. Peterson's vault is good for seventh on the NCAA list. Yida Gao '13 cleared 13-11 1/4. </p>
              <p align="left">In the Triple Jump, Michelle Johnson '15 had an excellent competition, finishing seventh with a nice jump of 37-7 3/4, good for a new Freshman Record. Sheldon Trotman '15 also jumped but was unable to place with his 44-7 1/2 performance.</p>
              <p align="left">On the track Jones led the way for the women, placing in three events. In the 100HH final, she ran a season best of 14.28, good for third place. With the 400 close in time to the hurdles, and with the 200 up a little later, she did not extend herself in the one lapper, placing eighth. In the 200, a tired Jones managed a fifth place season best of 24.51, putting her seventh on the current NCAA list.</p>
              <p align="left">Singer-Clark lit the track up with yet another PR of 21.62 to garner second place. Also earning second place for the men was a remarkable 3:54.56 in extremely windy conditions in the 1500 Meter Race by Ben Mattocks '12, out of the second of three sections. Also placing in the 1500 was Justin Bullock '14, finishing in seventh in 3:55.55.</p>
              <p align="left">Jamie Simmons ran a season best of 60.84, good for fourth place in the 400IH. She was in position to win, even running into the wind on the backstretch as she was clean through seven hurdles, but then lost focus on the hurdles and stuttered the final three. Simmons is now second on the NCAA list.</p>
              <p align="left">Stephen Serene '12 lowered his Steeplechase best to 9:17.33 in his pursuit of making the NCAA Championship. Serene placed fifth, but will probably need a sub 9:10 to make it to the big dance.</p>
              <p align="left">The final scoring effort for MIT was the men's 4x400 relay. Running first was Singer-Clark who lit up the track with a 48.4 leadoff. Sam Parker '15, who had run 1:54.73 in the 800 earlier in the day, ran a 400 best of 49.4 to handoff in fifth place. DJ Ronde '13 took over baton duties, running a personal best 49.7, although he handed off in seventh to Pat Marx '13. Marx, always better when running from behind, split 48.4 to catch two teams, giving MIT a fifth place 3:16.08.</p>
              <p align="left">Next week, MIT travels to RPI in Troy, NY to race in the ECAC Division III Championship, a last chance qualifier of sorts.</p>
