                  <h3>Alec Reduker Leads Engineers in Spring Trip Meet</h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>April 1, 2018              </p>
              <p>While spring break brought a week of relaxation for most in the MIT community, the track and field teams were hard at work preparing for the upcoming season. As is the case every year, the technical athletes (throwers, jumpers, vaulters, hurdlers, sprinters) make their pilgrimage to warmer climes in the hopes that they would be able to capitalize on the warmer weather and come away with some New England DIII and NCAA DIII qualifying marks under what could potentially be the most favorable conditions of the season. </p>
              <p><br>
                Throughout the week, the teams practiced each morning and focused on strengthening their bond in the afternoon.&nbsp; The weather was great, and training appeared to go well for each event group, with the California sun and warm air energizing everybody. If the beginning of the week was any indication, the teams were primed and ready to compete.&nbsp; However, Saturday at UCSD told a slightly different story.&nbsp;&nbsp; <br>
                In the <a href="ucsdcollegiate.res.php">California Collegiate Open</a>, MIT had the opportunity to compete alongside Division I powerhouses, Wisconsin, Louisville, and the throws group from USC, while also lining up against New England rivals Williams and Middlebury among other colleges competing from the local area. Beyond the opponents however, Saturday brought many challenges that the teams needed to solve, in order to bring the trip to a successful conclusion.</p>
              <p><br>
                These challenges included a long layoff between competitions and maintaining team energy with a smaller group in the larger outdoor environment.&nbsp; Additionally, the weather, while still warm, overcast in the high 50&rsquo;s and low 60&rsquo;s for much of the day, presented a significant change from the 80 degree days the teams enjoyed in practice. In their effort to navigate these challenges some athletes rose to the occasion, turning in PR&rsquo;s and national qualifying marks, while others struggled to shake off their rust and return to form.&nbsp; Ultimately, while performances were effective enough to score points, many fell flat, when compared with the marks they were reaching at the end of the indoor season.</p>
              <p><br>
                On the men&rsquo;s side in the running events, freshman Benton Wilson started his season off strong, etching six points with a seventh and fifth place showing in the 100 and 200 meters respectively, running slightly behind his lifetime bests in each event, while Chris Sweeney '18 placed third in the 400 Hurdles, running approximately half a second off his career best, finishing in a time of 54.16.<br>
                <br>
                In the field events, the High Jump took center stage early on, creating an exciting competition that saw Alec Reduker '20 take the victory on his final attempt at 6&rsquo;10.75&rdquo;, setting a new career best on a jump that has historically qualified for the NCAA Championships. Finishing right behind him, was sophomore Chris Washington who on two misses at 6&rsquo;8.75&rdquo; passed on his final attempt to take one shot at a national qualifying mark.&nbsp; As the bar was raised, Washington took his shot at a national mark, but it was just out of reach on the day, dropping him to third overall. Also, in the event, sophomore Steven Speck would go on to take seventh, with his clearance of 6&rsquo;2.75&rdquo;, tying the Triple Jump group for highest scoring event group on the day with 18 points.</p>
              <p><br>
                Merely minutes later, senior Billy Ruschel had to overcome a deficit of his own, using the last jump of the competition to reclaim the lead and secure his win in the Triple Jump, taking first with a mark of 46&rsquo;11.75&rdquo;.&nbsp; Senior Jason Villanueva took third with a jump of 45&rsquo;3&rdquo;, while senior Ryan Prinster struggled on the day, but managed to add two more points to scoring, finishing in seventh.&nbsp;</p>
              <p>&nbsp; </p>
With the win in the Triple Jump, Ruschel closed out the day with a team high 15 points after opening the day with a career best in the long jump of 21&rsquo;11&rdquo;, taking fourth overall.&nbsp; However, this did not stop Cody Bratten '18 from trying to close that gap.&nbsp; The senior thrower finished with 13 points, scoring five in the Shot Put and eight in the Discus, which was called early due to darkness.&nbsp; Junior Jon Fakkema had a strong showing in the Hammer, placing third among Division 3 competitors with a throw of 162'0&quot;, and sixth in the Shot Put. While not a scoring mark in the Hammer, sophomore Albert Menio also rose to the occasion on the day, with a strong showing and a nearly three meter lifetime best of 134&rsquo;3&rdquo;.<br>
<br>
On the women&rsquo;s side, senior Hannah Chen added her name to the national discussion by claiming fourth place in the 400 hurdles, with a time of 1:02.31. That performance debuted Chen at number three on the national list. Prior to her run in the hurdles, Chen also teamed up with sophomore Michelle Menkiti, seniors Haley Strouf and Nneoma Okonkwo to race the 4x100 relay. A quartet with high aspirations, the team finished fifth with a time of 49.24, as they worked to improve their passing and timing.<br>
<br>
In the field events, senior Ariela Slutsky and the throws group became the bedrock of scoring for the day. Slutsky led the way in the Hammer Throw, scoring ten for finishing first among division three athletes with a throw of 49.76 meters (163&rsquo;3&rdquo;). She was joined by sophomore Elise McCormack-Kuhman, who despite being stung by a Stingray earlier in the week found a way to unleash a 25 foot lifetime best of 135&rsquo;7&rdquo; to earn a much needed four points for the team.&nbsp; Rounding out scoring was sophomore Folusho Jebutu, who added two points with a throw of 40.08 meters (131&rsquo;6&rdquo;).<br>
<br>
In the horizontal jumps, the women achieved mixed results on the day, scoring several points but ultimately failing to reach their goals.&nbsp; In the Long Jump, Strouf and Okonkwo finished fourth and fifth overall, accounting for nine points, but their marks of 18&rsquo;1.75&rdquo; and 17&rsquo;9.5&rdquo; are well short of what will be needed to punch their tickets to next month&rsquo;s NCAA Championship. Hours later, when the Triple Jump rolled around, conditions were at their coolest for sophomore Jasmine Jin and freshman Elizabeth Weeks. The outdoor season started off well for Jin, who used a strong opening round jump of 37&rsquo;8.5&rdquo; to secure fourth place, while Weeks struggled to an eighth place finish with a jump of 36&rsquo;0.75&rdquo;.&nbsp; In the Pole Vault freshman Jacqueline Ahrens was the top performer for MIT, tying for second place overall (tying for first among Div. III), with a clearance of 11&rsquo;1.75&rdquo;.<br>
<br>
Ultimately, the teams struggled to capitalize on their early successes and spread the energy throughout the stadium, as they placed sixth and eighth overall.&nbsp; However, the teams returned from California with 29 New England DIII qualifying marks and two likely NCAA qualifying performances. Next week, it&rsquo;s back to cooler temperatures, when distance and mid-distance runners return to the track to join the speed and power groups in their first real team test of the year Saturday in a quad meet with Bates, RPI, MIT and Tufts.
