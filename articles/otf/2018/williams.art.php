                  <h3>Women Finish Regular Season Undefeated / </h3>
                  <h4>Benton Wilson Sets Two Freshman Records in Men's Losing Effort</h4>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>April 14, 2018              </p>
              <p>The third annual <a href="williams.res.php">Dick Farley / Gordon Kelly, Williams College vs. MIT dual meet</a> was expected to be close for both the men and women.&nbsp; With both teams dealing with some injuries and the weather not expected to be favorable for outstanding performances it was expected to be a matter of which team wanted it more.&nbsp; Over the past four decades the two programs have dominated New England Division III Track &amp; Field.&nbsp; If there are common threads between the two programs it is a team mentality and a do not give up attitude.</p>
              <p>The weather was much better than expected in that it did not rain.&nbsp; Temperatures in the mid-40's, occasional sun and a consistent 15-20 mph wind hitting the runners on the turn entering the home stretch in the oval events&nbsp;certainly affected overall performances.</p>
              <p>Although the score of the men's MIT vs. Williams meet was 104 to 93 in Williams favor, what it really came down to was a matter of .11 seconds.&nbsp;&nbsp;With the relays scoring 5-0&nbsp;and the Engineers not expected to win any of the three relays, they knew if they could win one or two, it would be as much as a 20 point swing and likely enough to win the meet.&nbsp; In the 4x100 relay, the quartet of Benton Wilson '21, Chris Washington '20, Steven Speck '20 and Ryan Prinster '18 had near perfect handoffs to come within .04 of winning the event 43.47 to 43.51.&nbsp;&nbsp;In the next to last event, the fatigued and unlikely foursome of Chris Sweeney '18, Ken Acquah '20, Kent Slaney '21 and Benton Wilson '21 came within .07 of winning, running respective splits of 49.9, 52.3, 52.1 and 48.3 for a 3:22.70 for MIT to 3:22.63 for Williams.</p>
              <p>The overall theme of the men's meet was that MIT dominated the field events and sprints/hurdles, with Williams ruling the middle-distance and distance events.&nbsp; Neither team had their best lineups in the meet.&nbsp; MIT had numerous athletes out, who were not quite ready to compete after returning from injury and a couple others in different events than usual to try and get championship qualifying performances in other events.&nbsp; Other than the javelin, an event where the Engineers are void of competitors, MIT was able to claim victory in every field event.&nbsp; Cody Bratten '18 was the one double winner, claiming the Discus (152-10) and the Shot Put (46-11 1/2).&nbsp; Yorai Shaoul '21, unable to triple jump due to a bruised heel, won the Long Jump in a personal best 22-9 1/4 effort, moving him to eighth on the MIT all-time list and just 3/4&quot; off the freshman record.&nbsp; Scott Cameron '19 won the Pole Vault at 13-11 1/4, Chris Washington the High Jump at 6-07, Jon Fakkema '19 the Hammer with a 155-9 throw and Ryan Prinster the Triple Jump with a 44-9 1/2 jump.</p>
              <p>Tech had two double winners in the running events.&nbsp; Chris Sweeney  ruled the hurdle events, taking the 110HH in 15.11 as he battled the cross wind, and the 400IH in 55.47.&nbsp;&nbsp;Wilson, in addition to both the 4x100 and 4x400, ran two freshman records in recording victories in the 100 (10.83) and the 200 (21.82).</p>
              <p>The Engineers managed three second place efforts in the running events&nbsp;with strong efforts from Dylan Doblar '21 in the 1500 (4:02.68), Alex Knoedler '18 in the 5K (15:16.19) and a 9:36.53 performance in the 3,000 Meter Steeplechase from Aidan Gilson '19, who is still rounding into physical and mental shape.</p>
              <p>With three events to go (5K, 4x400, 4x800) Tech had taken a four point lead.&nbsp; Needing to score nine points in&nbsp;the 5K to clinch the meet, or four points to put themselves in a position of only needing to win one of the two final relays, Knoedler was the only one who could come through for MIT, earning three points for second.</p>
              <p>In the women's meet, MIT again dominated the field events, winning all but two of them, but also claimed the middle-distance events to secure the victory prior to the 5K.</p>
              <p>Ariela Slutsky '18 won three events to lead MIT to an undefeated season.&nbsp; Slutsky claimed first in the Discus (143-9), Hammer (169-2) and Shot Put (35-6 3/4).&nbsp; The other field events victories came from Kari Stromhaug '19 in the Pole Vault with a 12-4 1/2 clearance, which is currently number seven on the NCAA list; Haley Strouf '18 in the Long Jump at 17-10 13/4; and Jasmine Jin '20 in the Triple Jump with a jump of 36-8 1/2.&nbsp; The throwers took 30 out of a possible 44 points and the jumpers were not far behind, scoring 28 of a possible 44.</p>
              <p>As the running events began, MIT was hoping for some great races in their favor.&nbsp; However, the 4x100 team of Michelle Menkiti '20, Haley Strouf '18, Hannah Chen '18, and Nneoma Okonkwo '18 were unable to stay with their counterparts, and in the 3,000 Steeplechase, Jenna Melanson '20 was ill prepared for the competition when she was unable to pull away in the first couple of laps, although she did run 11:02, a fine first attempt for the season.</p>
              <p>Fortunately the 1500 was a surprise in that the Engineers claimed the top three places led by Katie Bacher '20 in 4:40.00.&nbsp;&nbsp;Margaret Trautner '20 was second in 4:45.30 and Mary Eccles '18 surprised everyone but herself with a third place 4:45.63.</p>
              <p>From that point MIT was off and running.&nbsp; The trio of Bailey Tregoning '19, Katie Williams and Trautner delivered a 1-3-4 finish in the 800 with respective times of 2:16.07, 2:18.17 and 2:19.47.&nbsp; Michelle Menkiti won both the 100 and 200 in 12.57 and 25.37.&nbsp; Hannah Chen won the 400IH in 61.93 when the apparent first place runner from Williams was DQ'd when there was no hurdle in her lane at the first flight.</p>
              <p>With the meet won, and some overuse going on, MIT pulled their 4x400, but won the 4x800 in 9:19.38 with Bacher, Williams, Trautner and Tregoning&nbsp;to reach the final score of MIT 107, Williams 92.</p>
              <p>Next week, MIT will take a few middle-distance runners to Princeton for the Larry Ellis Invitational on Friday and will host the Sean Collier Invitational on Saturday at the Morrison track.</p>
