                  <h3>Focus on National Qualifying Marks Brings Excellent Individual Results</h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>May 11-12, 2018              </p>
              <p>After two hard fought&nbsp;championship meets (NEWMAC, New England Division III) that produced&nbsp;victories for both the MIT Men's and Women's Track &amp; Field teams, it was a refreshing opportunity for the Engineers to compete and not focus on how many points the team was scoring.&nbsp; <a href="neicaaa.res.php">The New England Intercollegiate Amateur Athletic Association (NEIcAAA) Championship</a> is certainly a championship opportunity for some, particularly some of the Division I colleges, but for MIT, having won the championships they were focused on and with the NCAA Division III Track &amp; Field Championship in two weeks, it was time to focus on performances instead of points.</p>
              <p>MIT left four national qualifying athletes and a couple of others at home to recover from the past two weekends.&nbsp; Those they brought were hoping to move up on the NCAA performance list and solidify their chance of being among the top 20 men or 22 women in their respective events, earning the trip to Nationals, which will be held at the University of Wisconsin La&nbsp;Crosse.</p>
              <p>The only victory earned by MIT was by Cody Bratten '18.&nbsp; The senior continued his impressive season, throwing a personal best 169-2 on his final throw to claim the victory in the Discus Throw.&nbsp; Bratten is now number two on the all-time MIT list and number eight on the NCAA performance list.</p>
              <p>Aidan Gilson '19 led MIT in the 3,000 Steeplechase.&nbsp; The junior was in a great position early on, but when the time to move was there he chose to back off.&nbsp;After a 77 and then a 76 lap, Gilson decided he wanted to go for it.&nbsp; Running a 69 final 400 (33 final 200) Gilson ran a season best 9:09.62 for second place and, number nine on the NCAA list.&nbsp; Ben Freed '18 ran an excellent race and a personal best of 9:13.48 to place eighth.&nbsp; Freed is currently 13th on the&nbsp;NCAA list.&nbsp; Brian Bates '20, ran his best 2K ever, coming through in 6:11.&nbsp; The sophomore gave up at that point, but still managed a personal best of 9:31.57.&nbsp;&nbsp;Bates can undoubtedly run sub 9:15 if he can put it together mentally.</p>
              <p>Chris Sweeney '18 qualified for the 400IH final by running a personal best 53.20 on Friday, which moved him to number 15 on the NCAA performance list with five days remaining to qualify.&nbsp;&nbsp;In the final&nbsp;the senior worked too hard to run fast and fell off after 200 meters.&nbsp; Nevertheless his 54.06 placed fourth in the event.</p>
              <p>Benton Wilson '21 also qualified for the finals on Saturday.&nbsp; In the 200 trials, despite having lane one he ran 21.96.&nbsp; Unfortunately, his performance earned him lane one again in the finals as the seventh qualifier.&nbsp; The freshman placed sixth in the finals in 22.07.</p>
              <p>Also on Friday, Chris Washington '20 placed in a two-way tie for sixth place in the High Jump with a 6-5 performance.</p>
              <p>Jon Fakkema '19 did not earn points but did throw a season best in the Hammer with a 165-6 performance.&nbsp; The men placed 11th out of 36 scoring teams with 29.5 points.</p>
              <p>The women's top place winner was also in the Discus Throw.&nbsp; Ariela Slutsky '18 placed second with a throw of 148-3, missing first place by six inches.&nbsp; Slutsky is ranked sixth in the Hammer and 13th in the Discus on the NCAA list.</p>
              <p>Jenna Melanson '20 had a perfect opportunity presented to her in&nbsp;the 3,000 Steeplechase.&nbsp; The second place runner was setting a pace that would get her in the mid to low&nbsp;10:30's, but she did not seize the opportunity.&nbsp; The sophomore still ran a season best and placed third with a 10:43.76.&nbsp; Melanson is ranked seventh nationally.</p>
              <p>In the women's 1500, Katie Bacher '20 did not let a slow early pace of 2:29 affect her.&nbsp; She made a strong move with 700 to go, running a 72 third 400 and then a 50 second final 300 to win her section in a personal best 4:31.71 and place fifth overall and the top Division III runner.&nbsp; Marissa McPhillips '20 was right with Bacher until the final 300, but still held on to place 8th overall in a new personal best of 4:34.09.&nbsp; Bacher is now ninth on the NCAA performance list and McPhillips 19th.</p>
              <p>On Friday MIT scored some points in the field events. Nneoma Okonkwo '18 jumped 18-4 1/2 to place sixth in the Long Jump.&nbsp; Jacqueline Ahrens '21 led the MIT pole vaulters with a sixth place effort, clearing 11-7 3/4.&nbsp; Kari Stromhaug '20 and Makenzie Patarino '19 were not able to clear their opening height.&nbsp;&nbsp;Ahrens is 10th and Stromhaug 13th on the NCAA list at 12-5 1/2 and 12-4 1/2 respectively.&nbsp; Okonkwo is 24th on the NCAA list with one meet to go to move up.</p>
              <p>Hannah Chen '18 just missed qualifying for the finals in the 100HH, finishing ninth in the trials in 14.76.&nbsp; However, she did make the finals in the 400IH, running 61.32 in the trials.&nbsp;&nbsp;In the finals Chen was not as up, fading in the homestretch to place seventh in 62.55.&nbsp; The senior is ranked sixth on the NCAA list.</p>
              <p>Although she did not quite run a season best, Bailey Tregoning '19 placed eighth in the 800 with a 2:11.98.&nbsp; Running outdoor personal bests, Margaret Trautner '20 ran 2:13.39 and Katie Williams '21 ran 2:13.44.&nbsp; Tregoning is ranked sixth on the NCAA list while Trautner and Williams are ranked 22nd and 23rd.</p>
              <p>The women finished eighth of 39 scoring teams with a total of 28 points.</p>
              <p>Those nationally ranked who did not make the trip were Yorai Shaoul '21 and Billy Ruschel '18, ranked fourth and 10th in the Triple Jump, Alec Reduker '20 ranked eighth in the High Jump&nbsp;and Mary Eccles&nbsp;'18&nbsp;ranked ninth in the 10K.</p>
              <p>Thursday is the final day to earn an NCAA qualifying mark.&nbsp; MIT will host the New England Region's Final Qualifier&nbsp;that day, starting at 5:00 p.m.</p>
