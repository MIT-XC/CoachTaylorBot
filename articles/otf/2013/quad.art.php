              <p><span class="headline">TECH TRACK TEAMS FINISH REGULAR SEASON UNDEFEATED WITH WINS OVER BATES, COLBY, USM</span></p>
              <p align="left">April 13, 2013</p>
              <p align="left">With an invitational next week and the championships starting the week after, this Saturday's <a href="quad.res.pdf">quad meet</a> marked the final regular season scoring meet. Bates, Colby, Southern Maine, and Husson College on the women's side, made the trip down from Maine. For the men, Bates College, even just coming off their exams, would be a formidable force to deal with. The women, despite holding a few individuals out, just had too much depth and quality to lose the meet. MIT performed well in both competitions, leading to victories by both the men and women. With the scoring for non-relay events allotting 9-7-6-5-4-3-2-1 for the top eight places, and 9-7-6-5 for the relays, the men scored 246.5 to 229 for Bates, 115.5 for USM and 85 for Colby. The women scored 237 points to 160.5 for Colby, 158 for Bates, 89.5 for USM and 37 for Husson.</p>
              <p align="left">The Engineers won eight of the 19 events to squeak out the win. For the second week in a row, Tyler Singer-Clark '14 took the 100 and 200 as well as anchored both the 4x100 relay and the 4x400 relay to victories. Unlike last week, there was no wind on the track and temperatures in the 50's. Singer-Clark led start to finish in the 100, winning in 11.06. In the 4x100, MIT and USM anchor legs got the batons together, but Singer Clark changed that pretty quickly, pulling away to win by .13, 42.60 to 42.73.  In the 200 the only competition came from teammate States Lee '16. Singer-Clark won in a season best 21.93 while Lee completed the half-oval in 22.25.  Back from a pre-season injury, Nick Diamantoni '15, placed fifth in 22.78. Derek Barnes '16, not quite recovered from his 400 earlier, placed seventh in 22.87. In the 4x400, MIT decided to take advantage of the lack of wind and loaded up the relay despite it being the fourth event for three of the legs. Michael Kaba '16 led off with a 49.3 leg, giving MIT a pretty large lead. Lee took the baton and despite a relatively poor handoff, ran 49.6 to blow the race open. Pat Marx '13 ran an excellent 49.4 all alone to give Singer-Clark an immense lead. Singer-Clark looked in championship form as he polished off the relay with a 48.6 anchor, giving Tech a 3:17.03, good for number five on the NCAA performance list.</p>
              <p align="left">Also having an outstanding day and contributing significant points was Kaba. He won the 400IH in a personal best 54.58 for a new MIT freshman record, one poor hurdle away from running sub 54. Once he gets comfortable with leading his other leg he will drop into the 52's at least. Prior to that effort, Kaba ran a very successful second leg of the 4x100 relay. He also ran a very nice third place PR in the 110HH in 15.15, just one hundredth of a second off the freshman record.</p>
              <p align="left">Derek Barnes '16 and Lee did battle in the 400 for the second week in succession. Once again Barnes edged out Lee, this time the difference came down to the thousandths of a second, both getting credit for a 48.84. Barnes wiped out the freshman record with that winning effort. This was the most dominant event for MIT as they claimed six of the eight scoring places. Marx moved down from his usual race, the 800, showing speed, or the lack of, is not his problem. Marx placed third in 49.32. Diamantoni placed fifth in 50.73 after going out in 22.9 at the 200. Mac Gager '14 ran 51.06 for seventh place as he continued his comeback from a late indoor season injury. DJ Ronde '13 closed out the scoring in the event, finishing eighth in 52.17. The 29 points put MIT ahead for good.</p>
              <p align="left">MIT claimed two field event victories as well. Cyrus Vafadari '13 continued to vault well, clearing 15-7 in a jump-off on his fourth attempt. While expected point scorers Yida Gao '13 and Ben Schreck '15 no-heighted, Marshall Wentworth '16 came through with a sixth place at 13-1 1/2. Jordan Mizerak '14, a decathlete just learning the pole vault, earned seventh with a 12-7 1/2 and Cory Monroe '13, just off an injury, placed eighth, also at 12-7 1/2.</p>
              <p align="left">Carington Motley '16 had a pretty big day himself. He won the Triple Jump in a season best 45-1 and took second in the Long Jump at 20-10 3/4.</p>
              <p align="left">One other impressive performance was turned in by Harrison Hunter '15 in the 800. With Marx moved to the 400 and Sam Parker '15 unable to compete due to missed practices from an early week injury, Hunter knew Tech needed big points in the 800 and figured he should go out and get them. He was in great position at the 400 in 57 seconds. In the last lap he moved up perfectly, looking like he could win against the talented field. With 100 meters to go, as he fit the wall, he also let it get to him mentally and stopped competing. However, his 1:56.66 for seventh place was a huge PR and shows he has the potential to run much faster.</p>
              <p align="left">The women were missing a few distance runners, who were resting, and not risking injury to a couple others. This did not seem to affect the team's overall performance as others took up the slack. Although the ladies only won seven of the 19 events, they totally dominated the field events, winning five of them and scoring 81 out of a possible 148 in the four jumping events despite two significant no-heights in the Pole Vault. </p>
              <p align="left">Preethi Vaidyanathan '15 got back to her winning ways in the High Jump, taking the event with a jump of 5-0 1/2. She was supported by a fourth place by Veronica Szklarzewski '15. </p>
              <p align="left">Even with the no-heights, the ladies claimed four places in the Pole Vault. Lauren Kuntz '13, just off a red-eye flight from a graduate school interview, won in 11-11 3/4. Sherry Wan '16 took third place in 10-6, Marissa Engle '15 placed fifth at 10-0 and Abby Klein '14 earned sixth at 9-6 1/4.</p>
              <p align="left">The Long Jump produced the most MIT place winners with five, but the Triple Jump produced the most points with 25. Szklarzewski took the victory in the Long Jump with a jump of 16-10 3/4. Jackie Brew '14 placed fourth at 16-7, Michelle Johnson '15 earned sixth at 16-2 1/2, Katharina Ross took seventh with a season best 15-11 3/4 and Jackie Konopka '14, completing the seven events in the Heptathlon over two successive meets in order to qualify for the New England Division III Championship, placed eighth at 15-11. Johnson came back from the Long Jump to win the Triple with 35-6 3/4. Szklarzewski continued her point scoring spree by getting second in a best of 35-4 1/2. Ross continued to improve, placing fourth at 34-0 1/4, and Stephanie Birkhimer '14 placed fifth with a jump of 33-9 1/2.</p>
              <p align="left">Tilly Taylor '15 won her specialty, the Javelin Throw, with a 131-7 effort for the final win in the field events for the Engineers.</p>
              <p align="left">On the track, MIT only won the 800 and 1500 meter runs. Cindy Huang '15 ran an excellent race to win the 800. Sitting back in third place at the 400 in 68 seconds, Huang moved hard as soon as the pace slowed, running away with the event in 2:16.77. Louise van den Heuvel '14, coming back after racing the 1500, won the second section of the 800 with a 2:19.38, good for third place overall. Brooke Johnson '13, the winner of the 1500, took fourth place honors in 2:19.58.</p>
              <p align="left">Johnson won the 1500 in 4:37.27, taking the lead after the 400 mark and running away with the race. van den Heuvel got bumped pretty hard when she made a move to go with Johnson, let it get to her a bit, but still ran a quality 4:44.54 for third place.</p>
              <p align="left">Next week, some MIT runners will head to Princeton for the Larry Ellis meet. Others will compete in the MIT Spring Invite that will host 15 other teams, including four Division I teams (Boston University, Brown, Harvard and Northeastern) as well as a strong group of Division II and III teams.</p>
