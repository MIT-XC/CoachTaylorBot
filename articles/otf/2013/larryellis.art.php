              <p><span class="headline">De Geus Shines at Princeton, MIT Meet Canceled</span></p>
              <p align="left">April 17-20, 2013</p>
              <p align="left">The week did not start off well. On Monday, Patriots Day Holiday in Massachusetts, or Marathon Monday to most runners, two bombs went off near the finish line of the Boston Marathon, killing three and injuring many. Fortunately, none of our runners were injured, nor any MIT students, staff or faculty, but it was a tragic reminder that no place is entirely safe in this day and age. How all of this would affect the MIT student-athletes would be determined.</p>
              <p align="left">On Wednesday and Thursday, our male decathletes traveled to <a href="hcdecathlon.res.php">Holy Cross</a> in an attempt to qualify for the New England Division III Championship. Adrian Samsel '16, Jordan Mizerak '14 and Matt Falk 'G competed among 19 individuals. Samsel, very new to the event, only training for the past month, scored 5518 points and has plenty of room for improvement in two weeks. He ran 11.41 in the 100 and a season best 16:16 for the 110HH. Even though he jumped 20-1 and 5-10 in the Long Jump and High Jump, he can do much better. His 400 in 52.23 was far from his best. The experiment was good enough to have him go for it at the D3 Championship. Mizerak was having a pretty good day when he pulled his hamstring going for 13-3 in the Pole Vault. He still scored 4731 points without doing the 1500. Falk, injured most of the season, faired well in the throwing events and High Jump but that was it. He will try to focus on the High Jump for the final week to see if he can qualify for the D3 Championship and return to his scoring days of the past in that event.</p>
              <p align="left">Six individuals made the trip down to Princeton University's <a href="larryellis.res.php">Larry Ellis Memorial Invitational</a> held on Friday evening. The five hour drive was done on Thursday evening after practice so the athletes would be well rested for their Friday evening races. On the trip down, around 9:30 p.m. everyone's cell phones started going off with the messages from MIT that there was an emergency on campus and to stay away from the Stata Center. Our trip continued, as did the ensuing tragedy where the bombers shot and killed Officer Sean Collier of the MIT Police and one of the bombers was killed in the ensuing shootout. The reaction of the police and government forces was unprecedented, shutting down most of Boston and the town of Watertown where the remaining suspect was hiding. The tragedy ended on Friday night with the capture of the final suspect. While this awful tragedy was senseless, the outstanding job done by the various law enforcement agencies shows the dedication and preparedness for safety for all those concerned.</p>
              <p align="left">Another six individuals were scheduled to leave for Princeton on Friday, but given the situation were not permitted to leave MIT. In addition, the 32 team MIT Spring Invitational scheduled for Saturday was cancelled along with other home and away contests scheduled for Saturday. This was an early decision made to ensure everyone's safety.</p>
              <p align="left">The athletes at the meet in Princeton did their best to focus on the races at hand even though their thoughts and prayers were with those back at MIT. Martina de Geus '14 was the first competitor for Tech, running in one of the Elite sections of the 3000 Steeplechase. She stayed in the pack the entire race, not really racing to her full ability, but running a smart race. She started with an 85 second first lap, went with the pack as they picked up to an 83 and then an 86 before locking into 91's for the next four laps and then finishing strong with a 40 second last 200 to run a 10 second PR in 10:58.15.</p>
              <p align="left">Justin Bullock '14, scheduled to run in the open section on Saturday, was allowed to race in the Friday section given the circumstances back at MIT and the fact he would have to come back with the team Saturday morning, missing his scheduled race. He was unfortunately boxed in and involved in a lot of contact for the first half mile. When the opportunity to move was provided, Bullock was not able to move although he did run a respectable 3:58.51. He is ready to run an excellent race if he can stay out of trouble in the early parts of races.</p>
              <p align="left">The ladies in the 5K, Sarah Quinn '16, Elaine McVay '15 and Brooke Johnson '13, were expected to all break 17:00 despite the windy conditions. Things did not go as planned. Johnson was not able to finish the race due to gastrointestinal problems, dropping out after five laps of racing and coming through the 1600 in 5:20. McVay's hamstrings and quads started cramping, possibly due to electrolyte imbalances, and fell off the pace early, only able to run 17:46, just about her 5K pace in her recent 10K of 35:54.  Quinn continued to have calf problems as they seized up on her prior to the 3200 mark. She maintained a solid pace, hitting 10:46 at the 3200 mark, but then fell off dramatically, finishing in 17:33.</p>
              <p align="left">Benji Xie '15 was the final athlete to compete for the Engineers. A deluge of rain began the section before Xie's and continued throughout his race. Xie's goal was to run sub 14:30, but it did not appear he had mentally bought into the effort that would take. He went out with the leaders, actually in second position, hitting 70 for each of the first four laps, instead of the 69 second 400's necessary to achieve his goal. As he slid further back in the pack, Xie slowed to 74 second laps shortly after the 9:26 3200 to hit the 4800 in 14:19. Finishing at the same pace, Xie ran 14:56.63.</p>
              <p align="left">With the NEWMAC Conference Championship on hand for the upcoming weekend and the New England Division III Championship the week after, the MIT Engineers need to get their focus back for the tasks at hand or their string of championship victories could be at an end.</p>
