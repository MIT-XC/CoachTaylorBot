              <p><span style="font-weight: bold; font-size: 24px">MIT IMPROVES NATIONAL QUALIFIERS</span></p>
              <p><span style="font-weight: bold; font-size: 24px"> AT NEICAAA CHAMPIONSHIP</span></p>
              <p align="left">May 13-14, 2001</p>
              <p align="left">MIT accomplished what it wanted to do at the <a href="neicaaa.res.php">NEICAAA Championship</a>, getting some more NCAA Division III provisional qualifiers and improving some provisional marks to automatic marks. This championship, hosted by Southern Connecticut State University, that combines the colleges from all three NCAA divisions throughout New England, lost some of its teams and top individuals this year due to a conflict with the IC4A/ECAC Championship. In addition, with many conferences holding their championships the weekend before, quite a few individuals, particularly distance runners were resting up if they had already qualified for nationals or planned on making another attempt the following weekend.</p>
              <p align="left">The women started things off on the final day with the 4x100 Relay. Barely having qualified provisionally the past two weeks with times of 48.28, the quartet of Jackie Brew '14, Martha Gross '12, Jamie Simmons '12 and Portia Jones '12, knew they would have to run under 48 seconds to have a good shot of making the national field. They did better than expected, stretching out the acceleration zone to make the handoffs more full speed, they had excellent exchanges, keeping the baton moving full speed through the zones to finish fourth in 47.70, a new MIT record.</p>
              <p align="left">Next for MIT was the 100HH, where Amy Magnuson 'G, had hit the auto standard of 14.57 the day before in the trials. With a great first step out of the blocks but standing up too early afterwards, Magnuson was a little behind the leaders on the first hurdle. Technically, she ran a solid race, but did not improve her time, running 14.58 to finish seventh. Brew did not make the finals, but did run a new personal best of 15.18 the day before.</p>
              <p align="left">In the Pole Vault, MIT did not quite have their usual dominant showing and no one earned a personal best, but certainly performed well enough to score some significant points. Karin Fisher '11, placed third, clearing 12-5 3/4. Lauren Kuntz '13, was fifth with a vault of 11-11 3/4 and Hazel Briner '11, tied for eighth at 11-5 3/4.</p>
              <p align="left">Jones was up again, this time in the 100 Meter Dash. She had a good start and an excellent acceleration and transition phase, clocking 12.02 to finish third and set a new personal best and MIT record. She also moved up significantly on the NCAA list, putting her in solid position to make the national field.</p>
              <p align="left">Simmons made the final of the 400IH by recording the fastest time in the trials on day one, a 61.04, the fastest time in the country for Division III. In the final, although she had a very poor final hurdle to slow her time by about a second, she sped to victory with a season best 60.73.</p>
              <p align="left">The final event for the women was the 4x400 Relay. There were some unanswered questions regarding the individuals on the team being ready for nationals and this was the race to answer those questions. Simmons, who seemed to be lighting the track up recently, got off to an uncharacteristic slow start until the inside lane made up the stagger. She then took off, closing very well to handoff in 56.4. Gross had not run a great leg all year, but this is the time of year when she usually comes around and she did not disappoint, running an excellent 56.9 to put MIT in a close second. Briner took over baton duties next, running a very respectable 59.8 while keeping MIT close to second place. Jones got out well despite a slow baton exchange. She moved into second place by the 200 mark, pulling away in a 55.2 split to give MIT second place in 3:48.53, their fastest time this year.</p>
              <p align="left">On the men's side, the Pole Vault was held on the first day. Nathan Peterson '12, looked a little more like he did indoors when he finished fifth at the NCAA Championship. He cleared a season best of 15-9 to win the event and had a decent attempt at 16-3. Cyrus Vafadari '12, also cleared 15-9 to finish third on misses.</p>
              <p align="left">In the Shot Put, Ken Cooper '13, had a huge PR, putting the shot 52-8 3/4 to place seventh in the talented field. Teammate Mitch Kelley '11, threw well but was out of the scoring with his mark of 48-10 1/4. Kelley came back in the Discus to earn three points with his sixth place throw of 156-5.</p>
              <p align="left">The women finished in 7th place with 41 1/3 points. The men earned 14th place with 21 points. Not bad for partial teams against Division I and II competition.</p>
              <p align="left">Next week, the Engineers travel to Moravian College in Pennsylvania to compete on Thursday and Friday in the ECAC Division III Championship. MIT will be in exams next week and therefore only taking those who have qualified for the NCAA Championship.</p>
