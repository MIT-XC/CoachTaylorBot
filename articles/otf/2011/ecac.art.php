              <p><span class="headline">SIMMONS AND JONES DOUBLE UP ON VICTORIES AT ECAC DIVISION III CHAMPIONSHIP</span></p>
              <p align="left">May 19-20, 2011</p>
              <p align="left">The Engineers of MIT made the six hour bus ride to Moravian College for the <a href="ecac.res.php">ECAC Division III Championship</a>, competing against the best Division III athletes in the East, the week prior to the NCAA Division III Championship. With MIT in finals this week, some student-athletes had to take exams early or have them proctored on the trip. Although quite a few Tech athletes qualified for this meet only those who have qualified for the NCAA Division III Championship or those with a great chance of doing so were brought to compete.</p>
              <p align="left">The first event of the meet was the women's Pole Vault. MIT faired very well in the event with Hazel Briner '11, placing sixth by clearing 11-2, Lauren Kuntz '13, tying for fourth at 12-2 and Karin Fisher '11, earning second place by clearing 12-11 1/2.</p>
              <p align="left">In the 100HH trials, Portia Jones '12, moved to the finals with a 14.63 to win her heat. In the 100 Meter Dash trials Jones also advanced to the finals, running 12.49 to win her heat into the strong headwind.</p>
              <p align="left">For the men of MIT, the only events taking place on day one were the 3000 Steeplechase and the 10K. In the Steeplechase, Stephen Serene '12, went out pretty well, but gave up half-way into the race, not capitalizing on an excellent opportunity to improve his NCAA Division III qualifying time. His final time of 9:46.02 was not competitive with his PR.</p>
              <p align="left">In the 10K, Dan Harper '12, was unable to get out with the leaders and quickly settled into the second group. The pace of the lead group was exactly what Harper wanted but he never bridged the gap to move up to that group. The first three miles were solid with a 15:05 split. However, the pace faltered a bit and Harper had a difficult time staying on pace. The second three miles slowed 15:19. Harper finished with a 71 second final lap so he had something left, but waited too long to use it. His final time was 31:35.29, good for fourth place.</p>
              <p align="left">The men's Pole Vault and Hammer Throw, both scheduled for day 1, were pushed back to day 2 due to the lightning delays.</p>
              <p align="left">Day two started with a slow, but constant rain for the early part of the meet. The first event was the Hammer while the Pole Vault was delayed for three more hours until the rain looked as though it might abate. Ken Cooper '13, threw 186-0 to place second despite the wet circle. CK Umachi '12, placed fifth at 176-02. That was the end of the throwing day for MIT. Even though Cooper, Umachi and Mitch Kelley '11, competed in the Discus Throw and Shot Put, all threw poorly, not coming close to their best in either event.</p>
              <p align="left">When the Pole Vault finally commenced, it was still raining. Nathan Peterson '12, had the pole slip out of his hands and decided not to take any more warmups. This was quite possibly an ill advised decision as he no-heighted, ending the season for the indoor All America vaulter. Cyrus Vafadari '12, performed well, earning third place by clearing 15-02. This was not a personal best, so he will have to sit on his 15-11 PR and hope that will get him into the NCAA field.</p>
              <p align="left">For the women, the first event was the 4x100 Relay. The foursome of Jackie Brew '14, Martha Gross '12, Jamie Simmons '12, and Jones, were hoping to improve on last week's 47.70 time and guarantee a trip to Nationals in that event. Whether it was the poor warmup, which did not allow enough time to test all the handoffs, or the weather, the Tech team ran poorly, badly exchanging the baton on the second and third handoffs and not  running particularly fast when they did have the baton. Their final time of 48.95 saw the top seeded team place fifth.</p>
              <p align="left">Things continued to go not so well when Jones ran the 100HH final. Getting too far behind in the first third of the race, as she was being too cautious instead of sprinting, there was too much to make up and not enough time to do it. Her 14.85 earned her a third place.</p>
              <p align="left">Fortunately, things took a big turn for the better in the next event, the 400 meters. Simmons went out fast and never slowed as she surpassed the NCAA auto standard in winning the event in a new MIT and ECAC Division III record of 55.68. Gross, running out of the slower section, won her section and placed fifth overall with a personal best of 57.74.</p>
              <p align="left">Jones kept the momentum going in the 100 Meter Dash when she won in 12.41. Her time was pretty good considering the wind and no tail wind whatsoever.</p>
              <p align="left">Simmons showed she can handle the 400 / 400IH double with less than an hour between when she came back on the track and won the long hurdle race in 60.86 despite being a little off pace for the first three hurdles.</p>
              <p align="left">Not to be outdone, Jones came back and won the 200 with a time of 24.95. Gross also raced the 200, placing fifth in 25.94.</p>
              <p align="left">The final race for Tech was the women's 5,000 Meters. Alina Gatowski '11, was ready to capitalize on her final opportunity to qualify for Nationals. Needing a sub 17:15, Gatowski moved to the front group by the end of the first 400. Her opening 1600 of 5:31 was right where she wanted to be. At the end of six laps, Gatowski made a premature move to take the lead. After one lap in the lead, she gave up for no apparent reason, slowing from 83 second pace to 87 seconds. Nevertheless, her 3200 was an excellent 11:06 and she looked fresh. However, she could not right the ship, continuing to spiral towards 90 second pace, running the third 1600 in 5:55. Her final time of 17:44.24 earned her fifth place in the race.</p>
              <p align="left">Since MIT was not in the meet to score points, and the higher priority was having everyone ready for the NCAA's next week, the Engineers decided not to race the 4x400, settling for second place as a team.</p>
              <p align="left">MIT will place their entries on Saturday and wait until Sunday to see who makes the final event fields for the NCAA Division III Championship. So far, the only ones making the automatic standard are Vlad Sobes '11 in the Javelin for the men, and for the women, Karin Fisher in the Pole Vault, Portia Jones and Amy Magnuson 'G in the 100HH, Jones in the 200, Simmons in both the 400 and 400IH, Anna Holt-Gosselin '11, in the 10 and 5K and the 4x400 team of Simmons, Gross, Briner and Jones. Hoping to make the field from provisional status for the men are Cooper in the Hammer, Paul Welle '11 in the 3000 Steeplechase and Vafadari in the Pole Vault. For the women, Lauren Kuntz in the Pole Vault, Hazel Briner in the Heptathlon, Jones in the 100 and the 4x100 team of Jackie Brew, Gross, Simmons and Jones.</p>
              <p align="left">The NCAA Division III Championship is being hosted by Ohio Wesleyan University, Thursday, May 26 - Saturday, May 28.</p>
