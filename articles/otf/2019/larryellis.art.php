                  <h3>Steeplers Dominate for Tech</h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>April 20, 2019              </p>
              <p>When Princeton University decided to move the distance festival part of the <a href="larryellis.res.pdf">Larry Ellis Invitational</a> from Friday evening to Saturday evening due to pending thunderstorms, MIT was not certain they could stick with their plans to bring select middle-distance and distance runners to the meet given that their own meet (Sean Collier Invitational) was on the same day.</p>
              <p>Not only did everything work out, including the weather at the Princeton meet, but the Engineers took advantage of the competition.&nbsp; Leading the way were the steeplers.&nbsp; Jenna Melanson '20 ran a great race, taking the lead after coming through the 1600 mark&nbsp;in 5:33.&nbsp; Taking the lead may have been the wrong decision as this caused her to think, in addition to not having competition.&nbsp; The pace slowed to 86 and then 90, which she maintained to the finish in a season best, and national&nbsp;qualifying mark of 10:47.87.&nbsp;</p>
              <p>Aidan Gilson '19 was next,&nbsp;and he followed the same plan.&nbsp; He raced well early, coming through the 1600 in an impressive 4:45 as he was with the leaders.&nbsp; His next lap slowed to a 75 and as runners passed him he struggled to stay engaged and competitive.&nbsp; He did not fall off much more and even kicked in the final 80 meters, also finishing in a season best and national qualifying mark of 9:08.03.</p>
              <p>Earlier, Bailey Tregoning '19 and Margaret Trautner '20 raced the 800.&nbsp; They were in a perfect section&nbsp;where at the back of the pack they&nbsp;were able to come through in 65-66&nbsp;at the 400.&nbsp; Both began to move and passed nearly the entire field on the backstretch and final turn.&nbsp;&nbsp;Both started to fall apart a little over the final 120, but still finished with season bests.&nbsp; Tregoning ran 2:12.34 and Trautner 2:13.64.</p>
              <p>The final race of the day for MIT was the men's 5K with Andrew Mah '22 representing the Engineers.&nbsp; Unfortunately&nbsp;Mah was seeded in the slower section as they only took this year's performances, not last year's.&nbsp; The pace was slow, coming through the 1600 in 4:49.&nbsp; Mah was not experienced or comfortable enough to take the lead, so he was subject to&nbsp;the pace at hand.&nbsp; After a 4:48 second 1600 and another slow lap the pace picked up&nbsp;to 68 seconds, but only for one lap.&nbsp; The changing pace got to Mah, who finished well, but could not kick with&nbsp;the leaders.&nbsp; His 14:53.49 was a good race, but a wasted opportunity&nbsp;as the freshman tries to qualify for the NCAA Division III Championship.</p>
