                  <h3>Women Qualify Two More For Nationals                </h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>May 10-11, 2019              </p>
              <p>After pushing the teams through attempting to win two weekends of championship track and field, MIT took a step back to recover for those individuals who had already secured a spot among the top 22 women or top 20 men on the NCAA Division III Championship field.&nbsp; With only a couple of exceptions, the Engineers only competed those who were still hoping to secure one of those valued spots.&nbsp; With many championships being contested around the country this weekend and regional&nbsp;NCAA approved Final Qualifying meets next week, unless a&nbsp;student-athlete is ranked among the top eight or so, their place on the list is not likely secure.</p>
              <p>On the first day of competition at the <a href="neicaaa.res.php">NEICAAA Championship</a>, the pole vaulters were the first to compete.&nbsp; Due to the threat of rain and the fact that the host college, Southern Connecticut State University, only owned one high jump pit and one pole vault pit, the events were moved inside.&nbsp; For the men, Liam Ackerman '21 and Scott Cameron '19 were representing MIT.&nbsp; Ackerman, who was ranked 11th on the national list with a vault of 15-11, was inconsistent in his approach because of his lack of ability to practice full approaches due to shin splints, was able to clear 14-11 1/2, but missed all three attempts at 15-5 1/2, placing sixth in the competition.&nbsp; Cameron looked very good at 15-5 1/2, but missed at 15-11 1/2, having an inconsistent take-off.&nbsp; He finished in fifth place.&nbsp; He had been seeded 29th going into the competition and moved up to 27th with his half inch improvement.</p>
              <p>The women were next and Kari Stromhaug '19, seeded 11th nationally, Anais Marenco '22 and Lila Wine '21 were ready to represent MIT.&nbsp; Stromhaug placed fifth by clearing 12-2, but did not move up nationally.&nbsp; Wine cleared 11-2 1/4 and Marenco failed to clear a height.</p>
              <p>At the same time, Liana Reilly '21 was up in the High Jump and Gabe Madonna '20 was about to throw in the Javelin.&nbsp; Reilly, ranked 43rd nationally with a jump of 5-4 1/4, cleared 5-2 1/4, but just missed matching her previous best.&nbsp; Madonna, ranked 11th nationally with a throw of 197-6, threw 187-7 to make finals, but tweaked his elbow on his first throw in finals, withdrawing from the competition.</p>
              <p>On the track Benton Wilson '21 was hoping to move up from his rank of 22nd nationally with a 21.56 in the 200.&nbsp; In the&nbsp;trials, Wilson ran 21.71 to advance to the finals on Saturday.</p>
              <p>The final event for MIT on day one was the women's 5K.&nbsp; Katie Collins '21 was ranked 19th nationally in the 5K entering the competition.&nbsp; Getting over an early season illness and also regaining some of her confidence after last week's 17:18, Collins was ready to take advantage of the 60 degree temperature, good competition and no wind.&nbsp; The initial pace was perfect at 80 seconds, but it sped up from there.&nbsp; Collins was a little out of her comfort range coming through the 1600 at 5:18, but knew she had to stay with the leaders if possible.&nbsp; However, she slipped to 82's and the lead pack of four pulled away.&nbsp;&nbsp;Within a couple of laps the leaders were breaking up.&nbsp; Collins moved up to fourth as one dropped out and then third&nbsp;after coming through the 3200 in 10:47.&nbsp; Her final 1600 was&nbsp;a 5:34, but she maintained third place.&nbsp; She finished with a 40 final 200 to finish in 17:01.24, moving her up to, you guessed it, 11th nationally.</p>
              <p>Day two began with the Men's Discus Throw.&nbsp; Jon Fakkema '19, who had a very difficult championship last week, got it back together today.&nbsp; Throwing&nbsp;154-8 earned him a fifth place.</p>
              <p>Next up for the Engineers was the Men's High Jump.&nbsp; Chris Washington&nbsp;'20, who had dealt with an injury the entire indoor season and part of the outdoor season was ready to return to his days of flirting with national qualifying jumps.&nbsp;Washington looked great in warm-ups and at the start of competition, clearing 6-3 and 6-5 with no misses.&nbsp; At 6-7 he could not make it work, narrowly missing on his final attempt.&nbsp; However, jumping clean through 6-5 earned him third place.</p>
              <p>On the track, Bailey Tregoning '19 and Margaret Trautner '20 were giving the 800 another try.&nbsp; Tregoning was already a pretty sure lock with her 2:11.29, but Trautner's 2:13.69 was ranked 20th nationally.&nbsp; In separate sections, Trautner was up first.&nbsp; Running 66 high for the first 400, Trautner moved to the front with 250 to go.&nbsp; She waited too long as she was still going strong at the finish with a 2:13.10 season best section victory, moving her up to 14th nationally.&nbsp; Tregoning had an opportunity to go for a big time as she had nothing to lose.&nbsp; Instead she was concerned the first lap would be too fast.&nbsp; She came through the 400 in 65&nbsp;in the back of the pack.&nbsp; She waited to move until about 150 to go when she suddenly came on strong, moving all the way to third place with a season best 2:11.01.&nbsp; Trautner placed sixth overall.</p>
              <p>In the Men's Shot Put, Will Woltmann '22 had a 53' warm-up throw and that ended his chance at a big day.&nbsp; He forgot about competing, instead focusing on throwing far.&nbsp; Although he did throw a season best 50-6 1/4 and placed ninth, he was not competing well.</p>
              <p>Back on the track, Benton Wilson was up for the 200 final.&nbsp; He had a slow start and was unable to get the time he needed to get to Nationals.&nbsp; However, he did catch most of the field, placing fourth in 21.73.</p>
              <p>Tech's last event was the Women's Triple Jump.&nbsp; Lucy Lee '21 and Elizabeth Weeks '21 were both hoping to move up on the national list.&nbsp; Both had jumped 37-10 this season, but needed to get into the mid 38's.&nbsp; Lee jumped 37-8 in the trials, but was unable to move up in the finals, finishing in ninth.&nbsp; Weeks did not have a great day, spraining her ankle and not making it out of the trials.</p>
              <p>MIT will host an NCAA approved Final Qualifier on Thursday evening.&nbsp; This truly will be the final opportunity to qualify for the NCAA Division III Championship.              </p>
