              <p><span class="headline">MIT MEN AND WOMEN TAKE VICTORIES IN ENGINEER'S CUP </span></p>
              <p align="left">April 3, 2010 </p>
              <p align="left">In near 80 degree temperatures and high winds, MIT successfully defended the <a href="eng.res.php">Engineer's Cup</a> in Troy, NY, home of Rensselaer Polytechnic Institute (RPI) in the annual tri meet pitting MIT against RPI and WPI. The men overwhelmed their competition by a score of 176 to 133 for RPI and 96 for WPI with the scoring format of 7-5-4-3-2-1 for first through six places. The MIT women held on to a 149.5 to 139.5 victory over RPI with WPI posting 107 points for third. The MIT women succeeded despite only competing 25 athletes.</p>
              <p align="left">On the men's side, MIT won seven of the 11 running events and three of the eight field events. For the women, MIT also won seven of the 11 running events, but only one of the field events, that being the pole vault. </p>
              <p align="left">MIT moved some runners around to different events, particularly on the men's side where the larger roster allowed such flexibility. Continuing to dominate, Stephen Morton '10, won his three events as well as anchored the winning 4x100 relay team. Despite the strong tail wind, Morton showed he thrives more on competition than conditions. His winning efforts in the long and triple jumps were 23-1 3/4 and 47-1 1/2 respectively, far from his best. Keeping him out of the 100 to give him a small rest, Morton did run the 200, winning in 22.67.</p>
              <p align="left">Despite Morton not racing in the 100 meter dash, Rene Peters '13, made certain MIT would not lose the points, coming away with an excellent 11.07 victory. Peters also ran the leadoff leg in the successful 4x100.</p>
              <p align="left">Pat Marx '13, moved from the 800 to the 400, with the same results, a victory. Marx ran a come from behind 50.36 in the strong winds. Marx also dropped down to the 200, earning a fourth place with a time of 22.85.</p>
              <p align="left">Paul Welle '11, moved from his favorite event, the steeplechase, to the 1500 and 800, and came away with two victories. In the 1500, Welle took off after the first 400 went out in a pedestrian 68 seconds, leading his teammates; Richard Prevost '11, Ben Mattocks '12, and Hemu Arumugam '10, to a 1-2-3-4 finish with times of 4:05.72, 4:06.40, 4:08.11 and 4:09.87 respectively. In the 800, the results were similar, with Welle braving the strong winds on the backstretch to win in 1:58.89. Stephen Serene '12, and Bill Phipps '10, supported with second and third place finishes in 1:59.18 and 1:59.20.</p>
              <p align="left">MIT's other track victory was earned by Yermie Cohen '10, who moved up to the 5000. Although Cohen tried to run a reasonably fast time, the strong backstretch winds were just too much, forcing him to slow in the last mile, finishing in 15:36.49. Dan Harper '12, supported with a second place performance of 15:43.79.</p>
              <p align="left">In addition to Morton's two horizontal jump victories, James Oleinik 'G, led the MIT shot putters to a 1-2-3-4 finish with a throw of 48-7 1/4. Mitch Kelley '11, threw 46-6 3/4 for second place, while Tony McDonald '10, threw 45-0 1/4 for third place and Ken Cooper '13, put the shot 44-2 3/4 for fourth.</p>
              <p align="left">For the women, many carried the load, but Portia Jones '12, was the only MIT athlete with two victories, earning first place in the 100HH with a time of 14.68 and a 65.75 performance in the 400IH. She also anchored the winning 4x100 team, who combined to run a time of 49.88. Hazel Briner '11, did not win any events, but she led all MIT scorers with 21 points. Briner earned a second place in the pole vault with a leap of 11-5 3/4. On her final attempt at 11-11 3/4, in what looked to be a good vault, her pole broke, slapping her in the leg in the process. Briner also placed third in the long jump with a personal best of 17-2, third in the 100HH with a 15.81, fourth in the shot put with a 35-7 3/4 toss, fourth in the 200 in 26.87 and fifth in the high jump with a clearance of 4-11.</p>
              <p align="left">Jamie Simmons '12, moved from the 400IH to the 400 with similar results as she won in 59.03. Simmons also ran a strong leg on the 4x100 relay and led off the 4x400 with an excellent 58.5 leg.</p>
              <p align="left">Alicia Kaestli '12, returned to winning ways, taking the 800 by moving into the lead early and holding on for a 2:24.46 victory. Alina Gatowski '11, who won the 1500 in a personal best of 4:50.01, supported Kaestli with a second place in 2:24.53.</p>
              <p align="left">Maggie Lloyd '12, took the lead at the start of the 3000 Steeplechase and never relinquished it, winning in 12:02.34.</p>
              <p align="left">MIT's final victory was in the final event, the pole vault. Karin Fisher '11, who also jumped to a personal best of 34-2 1/4 in the triple jump to earn second place, won the vault with a 11-5 3/4 effort, beating Briner on misses.</p>
              <p align="left">Next week, MIT hosts their first home meet of the outdoor season. The men take on Bates, Colby and Southern Maine while the women do battle with Bates and Colby. Both meets begin at noon on Saturday, 4/10.</p>
