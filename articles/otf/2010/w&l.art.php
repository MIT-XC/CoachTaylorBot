              <p><span class="headline">MIT OPENS WITH EIGHT NCAA PROVISIONAL QUALIFYING MARKS </span></p>
              <p align="left">October 27, 2010 </p>
              <p align="left">MIT traveled to <a href="w&l.res.php">Washington &amp; Lee University in</a> Lexington, Virginia, to compete in a multi-team invitational this past weekend. The intent was to seek out some warmer weather during the spring break week. With the exception of some wind and rain to start the day on Friday the weather could not have been much better. At the end of the two day affair MIT had accumulated eight NCAA Division III provisional qualifying performances.</p>
              <p align="left">Day 1, the first day of the heptathlon, saw Hazel Briner '11, take a substantial lead with marks of 16.51 in the 100HH, 4-11 in the high jump, 35-8 in the shot put and 27.82 in the 200. Greg Tao '10, won the pole vault with an NCAA qualifying performance of 15-11. Later in the day (nine hours later) Briner and Karin Fisher '11, tied for second in the vault with a clearance of 10-11 3/4. In other first day field events Vlad Sobes '11, won the javelin throw despite the muddy approach on the grass runway, with a throw of 186-5. Stephen Morton '10, won the triple jump with a leap of 46-4, but only took three attempts in an effort to save his legs for later in the season. Tony McDonald '10, led the MIT throwers in the discus with a toss of 140-1 for third place. </p>
              <p align="left">On the track, the women's 4x400 team of Jamie Simmons '12, Martha Gross '12, Briner and Portia Jones '12, led the way to victory with a national qualifying performance of 3:54.28, thanks to a 55.8 anchor by Jones. The men ran a fine early season time of 3:24.94 as Pat Marx '13, anchored a team of Nick Leonard '11, DJ Ronde '13, and Josh Duncavage '13, with a 48.9 split. Stephen Serene '12, ran a personal best in the 1500 with a time of 4:03.38. In the 3000 steeplechase, Paul Welle '11 and Richard Prevost '11, had an excellent start to the season. Going out in an ambitious first mile of 4:57, Welle kept pace running a provisional qualifying time of 9:24.11. Prevost fell off a bit, but still ran a fine fourth place time of 9:34.71. In the 5K, Ben Mattocks '12, ran a very even paced race, running a personal best of 15:36.52 to win his section. The 10K was run as a combined men's and women's race, the only event the NCAA will permit to be conducted in such a manner. On the men's side, MIT ran very well. Hemu Arumugam '10, negatively split the race, which started with a pedestrian 81 second first 400. His mile splits were 5:14, 5:07, 5:11, 5:08, 5:02 and 5:05 before finishing the race in 32:02.11. Gihan Amarasiriwardena '11, stayed on pace with Arumugam until 4.5 miles, finishing in 32:10.90. Dan Harper '12, also raced well, placing eighth in 32:31.63. The women put the exclamation point on a very good first day of competition. Alina Gatowski '11, led start to finish in a national qualifying time and MIT record of 36:11.12. Tania Morimoto '12, who shared pace setting duties with Gatowski through 4.5 miles, also ran a provisional qualifying time, placing third in 37:01.55. Freshman, Sarah Sprague ran an excellent 39:12.60 10K debut. Gatowski ran 18:28 for the first 5K and 17:43 for the final 5K. Her final mile was 5:34 and looked as though she had just begun to run.</p>
              <p align="left">Day two was sunny with temperatures between the upper 40s to lower 50s and a bit of wind. Briner closed out the heptathlon with a score of 4079, just short of NCAA qualifying, earning the victory with marks of 15-6 in the long jump, 78-6 in the javelin (muddy approach) and 2:27.42 in the 800.</p>
              <p align="left">Also for the women, the 4x100 unit of Margaret Leibovic '10, Gross, Simmons and Jones ran an impressive early season mark of 49.68 despite two pretty bad handoffs. Jones claimed a victory with a national qualifying performance and personal best of 14.56 in the 100HH. Maggie Lloyd ran two personal bests, 11:49.98 in the steeplechase on Friday and then 2:25.17 to win her section and place sixth overall in the 800. Gross ran an excellent 26.33 in the 200 after running an outdoor personal best of 58.98 in the 400. Simmons won the 400IH in 63.62 to provisionally qualify for nationals. Marie Burkland '13, raced to a 4:56.27 1500 to win her section and move into MIT's top 10 list.</p>
              <p align="left">Stephen Morton led the way on day two for the men. In the long jump, he again only took three jumps, improving on each one as he finished in second place with a national qualifying mark of 23-4 3/4. In the 200, he won with an excellent time of 22.15. In the 100 he raced to a 10.98 to place second. He also anchored the 4x100 to a very fast time of 42.82 despite poor handoffs. Rene Peters '13, opened up, handing off to Josh Duncavage '13, who handed off to third leg, Nick Leonard '11. Peters also placed sixth in the 100 with an 11.16 clocking. In the 800, Pat Marx '13, ran an excellent race, winning it with a personal best of 1:55.38. Also running quality opening times were Dawit Zewdie '13, and Serene, finishing in 1:57.58 and 1:57.66 respectively.</p>
              <p align="left">Also in the field events, James Oleinik 'G, placed third in the shot put with a put of 48-3 1/2. In the hammer, MIT had three men in the finals. CK Umachi '12, led the way in fifth with a personal best of 162-10. Tony McDonald '10, threw 151-7 for seventh and Ken Cooper '13, threw 149-11 for eighth place. Matt Falk jumped to a personal best in winning the high jump with a leap of 6-2 3/4.</p>
              <p align="left">On a side note, Yermie Cohen '10, completing his final outdoor season, competed in the 1500 in the Stanford Invitational, running 3:52.82 to qualify for nationals. </p>
              <p align="left">Overall, MIT had an excellent first meet. Next Saturday, at 1:00, Tech travels to RPI in Troy, NY to compete in the Engineer's Cup against RPI and WPI. </p>
