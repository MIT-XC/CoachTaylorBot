                  <h3>Balanced Attack Helps Engineers Defend NEWMAC Titles</h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>April 29, 2017</p>
              <p>The Men's and Women's Track &amp; Field teams of MIT not only withstood the unusual&nbsp;80 degree heat&nbsp;and high humidity, they overpowered the competition to win the <a href="newmac.res.pdf">NEWMAC Championship</a>, the 17th in succession for the men and the ninth consecutive title for the women.&nbsp; The Engineers won the meet with a balanced attack that gave the women 270 points, 153.5 more than runner-up WPI.&nbsp; The men, seeded to win by about 50 points, prevailed with 291.5 points, 91 more than runner-up WPI.</p>
              <p>The balance was not only between the field and running events, but also throughout the events within those respective disciplines.&nbsp; For the women, the only event&nbsp;in which they did not score was the one they scratched from in order to not run up the score, the 4x400 Relay.&nbsp; The men scored in all but the 110HH and Javelin, event areas where they are really void with the exception of decathlete Luke Gray '18, who is primarily a pole vaulter.&nbsp; The men's field events scored 140.5 points while the runners collected 151 points.&nbsp; The women's field events totaled 118 points while the track athletes scored 152.5.&nbsp; The women earned nine victories, as did the men.</p>
              <p>Michelle Menkiti '20, broke both of her freshman records as she finished second in both the 100 and 200 Meter sprints.&nbsp; In the 100 the rookie ran an impressive 12.11, a .29 PR.&nbsp; In the 200 she broke 25 seconds for the first time, running 24.85.&nbsp; For her performances Menkiti earned co-Rookie of the Year for Track &amp; Field in the NEWMAC Conference.</p>
              <p>Two individuals earned double victories, Nneoma Okonkwo '18 and Hannah Chen '18.&nbsp; Okonkwo&nbsp;won her primary event, the Long Jump with an 18-10 1/2 performance.&nbsp; In the 100, Okonkwo ran a personal best 12.04 to edge Menkiti and set up a 1-2 finish.&nbsp; Chen ran to a personal best in the high hurdles, running 14.46 for the victory.&nbsp; In the 400IH, Chen won easily, running 62.61 despite the strong headwind on the backstretch. </p>
              <p>Jackie Vahey '17 totaled the most points for the women.&nbsp; Vahey won the Discus with a throw of 135-3 1/2, finished second in the Hammer with a toss of 170-7 1/2 and put the Shot 39-6 1/4 to finish fourth in that event, totaling 23 points.&nbsp; Both Okonkwo and Chen were on the victorious 4x100 relay, along with Menkiti and Jane Reed '20, which ran 47.80, giving both juniors 22.5 points.&nbsp; The other 20 point scorer was another thrower, Ariela Slutsky&nbsp;'18.&nbsp; The junior&nbsp;did not have&nbsp;her best day, but competed well, placing second in the Discus (133-9), third in the Hammer (168-4 1/4) and third in the Shot Put (40-0 1/4).</p>
              <p>The most dominant event for the women was the 1500.&nbsp; Tech earned 27 of a possible 29 points, which is limited by the NEWMAC rule of only allowing four entries per event.&nbsp; Maryann Gong '17, a national qualifier&nbsp;in both the 5K and 10K, had not raced a 1500 as yet this season, and because of another NEWMAC rule had to run in the slow section with a seed of no-time.&nbsp; This was unfortunate for the rest of the competition who missed an opportunity to compete against the 15 time All American.&nbsp; Gong ignored the wind, ran splits of 71, 74, 74 and 57 for a&nbsp;solo 4:36.32.&nbsp; In the seeded section, Marissa McPhillips '20 waited until less than 600 meters to go before she took the lead and pushed through for the section victory in a huge personal best of 4:38.51.&nbsp; Katie Bacher '20 also ran a personal best, placing fourth in 4:43.78.&nbsp; Clemmie Mitchell '18, still suffering the after&nbsp;effects of being sick, finished fifth in 4:56.46.</p>
              <p>Natalie Alper '17 was another victor for the women.&nbsp; Alper cleared 5-4 1/4 to win the High Jump on misses.&nbsp; Jenna Melanson '20 cruised to&nbsp;victory in the 3000 Steeplechase, winning in 11:19.26.</p>
              <p>The high point scorer for the victorious men's team was Jon Fakkema '19 with 26 points.&nbsp; The sophomore had a very impressive day.&nbsp; He started the day with a strong effort of 140-7 for second place in the Discus.&nbsp; He then won the Hammer with a personal best of 164-3 1/4 and finished the day with another second place and a personal best of 45-5 1/4 in the Shot Put.</p>
              <p>The only double winner for the Engineers was Cody Bratten '18, which earned him NEWMAC Field Event Athlete of the Year.&nbsp; The junior won the Discus with a throw of 156-10 1/2&nbsp;and the Shot Put at 48-2 1/4.&nbsp; Having not throw the Hammer in over a year, he added one point by placing eighth in that event.</p>
              <p>Arinze Okeke '17 showed significant improvement in his second week back.&nbsp; He won the Triple Jump with a performance of 46-1 1/2 and placed second in the Long Jump with a leap of 21-11 3/4.&nbsp;&nbsp;Scott Cameron also joined the winner's circle, earning the victory in the Pole Vault with a height of 15-5.&nbsp; Danny Newman '17 had quite the day in the sprints, taking the 200 victory with a personal best of 21.68 and placing second in the 100 at 10.89.&nbsp; Newman also led off the 4x100 Relay with teammates Michael Amoako '19, Ken Acquah '20 and Tre Albritten '18 to place third in a season best of 43.10.&nbsp; Aidan Gilson '19&nbsp;won the 3000 Steeplechase in a casual 9:39.81 and then came back to place fourth in the 400IH with a 58.86 performance.&nbsp; Daniel Weiss '19 ran a great race in the 5K to win in 15:30.71.&nbsp; The sophomore took the lead just past the 2K mark and never relinquished it, running 37, 35, 34 for 200 splits in the final 600 meters.&nbsp; Alex Knoedler '18 used similar tactics, taking the lead with a strong move just 2900 meters into the race.&nbsp; After a 5:10 first mile in the humid conditions, the pace began to slow, so after a lap in 82, the junior blasted to the front and never gave up the lead, winning in 32:37.96.</p>
              <p>The most dominant events for the Engineers were the Shot Put and Triple Jump, both events netting 26 points.&nbsp; After Bratten and Fakkema, Adam Estes '20 threw an amazing personal best of 40-7 3/4 for fourth place and Albert Menio '20 added a sixth at 40-2 1/2.&nbsp; After Okeke's victory&nbsp;in the Triple Jump, Jason Villanueva '19 added&nbsp;a second at 44-6 3/4, Ryan Prinster '18 placed third with a 44-6 performance and Shane Lyons '20 jumped 40-8 1/4 for eighth place.&nbsp; Just one point behind, the men's 5K earned 25 points with Billy Woltz '20 (15:46.37), David Walter '18 (15:55.69) and Steven Goldy '20 (15:56.67) placing third, fourth and fifth respectively behind Weiss's victory.</p>
              <p>Next week, both teams hope to successfully defend their New England Division III titles at Williams College.&nbsp; Neither MIT team will be favored, but that has not prevented the Engineers from being victorious in the past.</p>
