                  <h3>Gong and Maloney Race to NCAA Qualifying Performances</h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>April 7, 2017</p>
              <p>On a cold and windy night (40 degrees with 20 mph gusts), one had to wonder was this four hour drive south to Princeton University for the Sam Howell Invitational going to be worth the effort and time.&nbsp; Well, with this being the best opportunity available this season to get an NCAA qualifying performance in the 10K, the athletes did not have the luxury of waiting for a better situation.&nbsp; They had to make it happen and so they did.</p>
              <p>In the women's 10K, Maryann Gong '17 made an ill-advised decision in assuming she was the best in the field and took the lead before finding out if anyone else was willing to do the work.&nbsp; Not only that, but she opened up with an 83 first quarter when she was looking for 86-87 pace.&nbsp; She continued to lead through a 5:39 and 5:43 first two miles as she settled into 86 pace.&nbsp; The eventual winner, Cassandra Gill '17of Iona (Division I) took the lead, but sped up to 84 pace, which gapped Gong, who was not capable of holding that pace after bearing the brunt of the wind through the first two miles.&nbsp; Gong continued to battle the wind with machine-like efficiency, running splits of 5:45, 5:47, 5:48 and 5:46.&nbsp; During the entire fifth and sixth mile Emma Zehner '17 of Williams was closing the gap on Gong.&nbsp; In the final lap it looked as though she would overtake Gong, but Gong managed a 79 last lap to hold her off and finish in 35:47.93 for second place.&nbsp; While this was not a personal best, it was certainly the best race of Gong's career.</p>
              <p>The men's 10K not only had the 3K and 5K indoor national champions, Peter Hale '17 of Williams and Tim Nichols '17 of Tufts respectively, but also a rabbit setting a vicious pace at 71 seconds per 400.&nbsp;Within the first mile, it was clear that Nichols was the only one capable of going with the rabbit's pace.&nbsp; The chase pack settled in at 72-73 pace and hit splits of 4:53, 4:49, and 4:48 through three miles.&nbsp; At this point, Dennis Maloney '19 and Matt Deyo 'G were with two others, one of them&nbsp;being Hale, who was setting the pace.&nbsp; The 5K for Maloney was 15:04 and Deyo was 15:06 as he began to give in to GI issues he had been experiencing since his warm-up.&nbsp; Maloney ran 4:49 for mile four and Deyo 4:55.&nbsp; Deyo did not realize he had&nbsp;about a 15 second buffer on a national qualifying time and gave in to the pain, not finishing the race after a 75 quarter.&nbsp; Maloney stayed in the three man chase group until Hale made a move with four laps to go, running 4:50 for the fifth mile.&nbsp;&nbsp;Maloney was again reluctant to make a move before he was comfortable doing so, which meant he was not going to catch Hale.&nbsp; Nevertheless, he ran 4:49 for the sixth mile and closed with a 68 final 400 to ran 30:07.93&nbsp;to finish fourth and the highest placing non-senior.&nbsp; This is also the third fastest time in MIT history.</p>
              <p>Next week, the trio will join their teammates in a good old-fashioned dual meet with Williams College.</p>
