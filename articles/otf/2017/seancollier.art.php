                  <h3>Distance Runners and Throwers Raise Their Game</h3>
                  <h6>Return of Injured Athletes Give Tech Hope For Championship Season</h6>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>April 22, 2017</p>
              <p>After two scoring meets the MIT Engineers appreciated the opportunity to compete in the <a href="seancollier.res.pdf">Sean Collier&nbsp;Invitational</a>, a non-scoring meet.&nbsp; This would permit those who needed a break the opportunity to rest prior to the championship season that begins on April 29th at home with the NEWMAC Championship.&nbsp; While many would rest, many others took the opportunity to compete in order to improve performances that would allow them to compete deeper into the championship season, thus giving their team a better chance of winning those championships.</p>
              <p>While most of the women's distance crew rested, Leandra Zimmermann '19 competed in her first meet of the season.&nbsp; Zimmermann led the 5K from start to finish, running an excellent 18:05.28 to easily win the event and qualify for the New England Division III Championship.&nbsp; Many of the men still needed qualifying times in the 5K for the New England Division III Championship and the 50 degree temperature&nbsp;with little to no wind provided a great opportunity.&nbsp; The plan was to run 73-74 second pace and work with each other, and this they did very well.&nbsp; The group split 4:54 at the 1600 and 9:49 for 3200 meters, taking turns setting the pace.&nbsp;&nbsp;Josh Derrick '20, Daniel Weiss '19, Steven Goldy '20, Alex Knoedler '18, and Cooper Sloan '18 were working well together through this point in the race.&nbsp; Sloan began to struggle in the last third of the race, but was desperately trying to hold on.&nbsp; The remaining foursome continued to another 4:56 1600 for a 14:45 4800.&nbsp; With an accompanying runner going for the win, Derrick decided he did not want to lose the race after all the work put in, and accepted the challenge.&nbsp; Derrick won the race in 15:15.15, a 30 second final 200.&nbsp; Weiss (15:20.28), Goldy (15:21.24), and Knoedler (15:23.71) decided to stick to&nbsp;the plan and save their energy for next week.&nbsp;&nbsp;Sloan held 1:47 pace for the final 600, but missed the qualifying mark by 2.25 seconds, running 15:32.25.</p>
              <p>Earlier in the day Aidan Gilson '19 ran a personal best in the 3000 Steeplechase, posting a 9:21.28 while running the most casual race possible.&nbsp; Gilson jogged along behind the leader for 2000 meters before taking the lead as the pace had slowed to 78 second pace.&nbsp; Gilson picked the pace up and then finished with a 35 final 200 to easily secure the victory.&nbsp; Displaying further hurdling prowess, the sophomore ran his first ever 400IH later in the day, placing third with a time of 58.59.</p>
              <p>In the throws, Ariela Slutsky '18 had another one of career days.&nbsp; The junior began with a victory in the Discus, throwing a personal best of 149-6 1/4.&nbsp; This mark puts Slutsky in seventh on the NCAA Division III list.&nbsp; She then went to the Shot Put where she proceeded to place second with a fine throw of 40-08 3/4.&nbsp; If she can keep her speed across the circle she will add many feet to that mark.&nbsp; Her day not done, she took on the Hammer Throw, again winning with a personal best.&nbsp; Her throw of 171-6 1/2 moves her to 12th on the NCAA list, two spots below teammate Jackie Vahey '17.</p>
              <p>Cody Bratten '18 had a fine competitive day by winning the Discus with a throw of 151-3 1/4 and placing second in the Shot Put at 44-5 3/4.&nbsp; However, Jon Fakkema '19 threw the big personal best, placing second in the Hammer with a 161-2 1/4 effort.</p>
              <p>Other Tech winners on the men's side included Scott Cameron '19, clearing a PR of 15-7 3/4 to take first place in the Pole Vault.&nbsp; Also returning to competition after suffering a broken fibula back in January, Arinze Okeke '17 won the Triple Jump in 44-6 3/4 from a very short approach.&nbsp;&nbsp;Tyndale Hannan '20 returned to competition after missing some time with a&nbsp;hamstring injury, running 1:56.32 in the 800.</p>
              <p>Jen Tylock '17 joined the winner's circle as she showed she is ready for the championships.&nbsp; The senior won the Pole Vault with a clear of 11-4 1/2.&nbsp; Hannah Chen '18 was a double winner for MIT, taking first place honors in the 100HH (15.29) and the 400IH (1:03.30).&nbsp; Neither were spectacular times, but with the weather and lack of competition, the junior performed well.&nbsp;&nbsp;Katie Bacher '20 was the other victor, taking first place in the 800 in 2:19.62.&nbsp; The freshman allowed herself to be boxed in for nearly 300 meters of a slow first 400 before breaking out to negative split, finishing with a 68 last quarter.</p>
              <p>Next Saturday MIT will go for their 17th consecutive NEWMAC title for the men and ninth in a row for the women.&nbsp; Despite illness and injuries the Engineers are hopeful they can persevere at home.</p>
