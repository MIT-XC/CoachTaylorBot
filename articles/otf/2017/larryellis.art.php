                  <h3>Three Out of Four Race to National Qualifying Times</h3>
                  <h4>Deyo and Melanson Set Records</h4>
                  <p>&nbsp;</p>
              </center>
              
              
              <p>April 21, 2017</p>
              <p>MIT brought four athletes to Princeton to race in the <a href="larryellis.res.php">Larry Ellis</a> Friday night distance portion of the meet.&nbsp; Leading off for the Engineers was Jenna Melanson '20 in the 3000 Meter Steeplechase.&nbsp; Having set the MIT freshman record at 11:06 last week, the rookie was aiming to get a time fast enough to qualify for the NCAA Division III Championship in five weeks.&nbsp; With a crisp pace of 84 seconds to start off, Melanson averaged 85 as she came through the 1600 in 5:42.&nbsp; Instead of racing, she now was just running, slowing to 89 pace and then 91 on the final full lap.&nbsp; However, she was able to finish the final 200 in 44 to blow away her freshman record with a 10:55.17.&nbsp; This time may be fast enough for Nationals, but will be very close.</p>
              <p>Next up was Maryann Gong '17 in the 5K.&nbsp; The senior has been battling a bone growth in her foot that has reduced her mileage over the past few weeks, allowing her to work on speed instead.&nbsp; The goal was just to qualify for the NCAA Division III Championship, adding the 5K to her qualifying effort two weeks prior in the 10K.&nbsp; The leaders went out pretty hard in 77 for the first quarter.&nbsp; The chase group, which included Gong, was running 81 pace initially, but that gradually picked up to 79's after a 5:25 first 1600.&nbsp; At the 3200, Gong came through in 10:43 for a 5:18 second 1600, but had started to lose contact with the leaders of the chase pack.&nbsp; Struggling with staying mentally engaged in the race, the last few laps she slowed to 83 pace before rallying for an 81 and then 39 final 200 to finish fifth in 16:52.36, accomplishing her goal.              </p>
              <p>The final race for MIT was the men's 5K, featuring Matt Deyo 'G and Dennis Maloney '19.&nbsp; Both got out well in the pack of 26 runners although they were towards the back at 67 pace.&nbsp; The pace stayed steady, coming through the 1600 at 4:33.&nbsp; A gap had separated Deyo and Maloney from the chase pack of 10 so Deyo set out to bridge that gap.&nbsp; After two more laps an individual&nbsp;tripped&nbsp;right in front of the two Engineers, causing Deyo to hurdle him and Maloney to go around.&nbsp; This created a gap between the two and Maloney never got back in the race.&nbsp; Deyo ran a 4:38 second 1600, too slow to get him to Nationals.&nbsp; He slowed to 71 for the next lap, but then rallied a bit with a couple of 70's and then a 67 before closing in 32 to not only run what is surely a National qualifying time of 14:21.28, but broke the 37 year old MIT record set by Colin Kerwin '82.&nbsp; Maloney did not give up, but he did not race either, not connecting mentally.&nbsp; His final time was 14:42.46.</p>
              <p>All four competitors will next compete at the NEWMAC Conference Championship next Saturday at MIT.</p>
