              <p><span style="font-weight: bold; font-size: 18px">MIT Track &amp; Field Teams Begin Season With Victories at Point Loma Nazarene University Invitational</span></p>
              <p align="left">March 28, 2015</p>
              <p align="left">MIT Track &amp; Field made their annual trip West to enjoy the warmer climate afforded them by California, ending the week of intense training with the <a href="plnu.res.php">Point Loma Nazarene University Invitational</a> in San Diego to open their season. Despite only bringing 16 men and 16 women on the trip, all technical athletes (field events, hurdlers, steeplechasers and 4x100 runners), the Engineers still managed to win both meets. The men won handedly, scoring 320 points to defeat the 11 other teams. The women scored 272.5 points to win by 3.9 points over the second place Cerritos team in the 13 team field.</p>
              <p align="left">Leading the way for the women were the throwers in general with three victories, Cimmy Virdi '16 winning the Pole Vault and Hannah Chen '18 winning the 400IH. Isabella Stuopis '16 took first place honors in the Shot Put with a heave of 40-7 3/4. Ellen Liverpool '15 won the Hammer Throw as she started the season with a personal best of 160-2. Jackie Vahey '17 started where she left off last year, throwing 135-5 for the victory in the Discus.</p>
              <p align="left">Virdi easily cleared 12-5 1/2 as her opening height after everyone else was out of the competition, but then lost focus at 12-11 1/2, even cutting her chin on the bar. Maeve Devlin '18 cleared 11-5 3/4 for second place.</p>
              <p align="left">Chen posted the fastest time in the country for Division III in the 400IH as she was victorious in 61.71 in her third race of the day, setting a new MIT freshman record. Earlier she anchored the 4x100 Relay made up of Veronica Szklarzewski '15, Kayla Harris '15, and Kendra Knittel '17 to a third place time of 48.81. She then came back in the 100HH. She looked excellent through seven hurdles as she was with the leaders. She hit the eighth hurdle pretty hard, managed the ninth hurdle then four stepped and hopped the 10th hurdle, finishing seventh in 15.58.</p>
              <p align="left">Szklarzewski had a good opening day, placing fourth in the 100 in 12.46 for a new PR. She also placed sixth in the Long Jump at 17-2, another personal best and sixth in the Triple Jump with yet another PR of 35-10 1/2.</p>
              <p align="left">Nneoma Okonkwo '18, coming back from a stress reaction in her shins, wasted no time putting up some solid marks, jumping 17-8 3/4 in the Long Jump for second place and running 12.87 in the 100, placing 11th. </p>
              <p align="left">The most dominating event for the women was the Hammer Throw. Supporting Liverpool's victory were a third place finish by Ashley Wheeler '15 with a throw of 145-9, a fifth place showing by Vahey with a throw of 144-7, a ninth place effort by Stuopis, throwing 131-6 and Ariela Slutsky '18 throwing 107-5 for 13th place.</p>
              <p align="left">Ken Cooper '15 certainly dominated for the men, coming away with three victories, taking the Hammer Throw in 185-10, the Discus Throw in 152-10 and the Shot Put in 52-8. </p>
              <p align="left">Arinze Okeke '17 had quite the opening day as well. He won the Long Jump in a personal best 22-5 3/4 and then took first place in the Triple Jump in 47-9. The horizontal jumpers dominated as they have all season. Supporting Okeke was Carrington Motley '16, placing second in the Triple Jump at 45-9 1/4 and fourth in the Long Jump at 21-3 1/2. Billy Ruschel '18 took fourth in the Triple Jump at 44-1 1/2 and sixth in the Long Jump at 20-6 1/2. Jason Villanueva '18 was sixth in the Triple Jump at 43-8, and 12th in the Long Jump at 18-4 1/2.</p>
              <p align="left">On the Track, States Lee '16 had a strong first meet. He first anchored the 4x100 made up of Tre Albritten '18, Okeke and Ruschel to a fine 42.96 fourth place finish. He then won the 400 in 48.37. A couple hours later he placed second in the 200 in 22.06 and then anchored the victorious 4x400 made up of Michael Kaba '16 (49.6), Luca Cacopardo '18 (50.2), Chris Sweeney '18 (50.5), in 48.1 for a cumulative time of 3:18.53.</p>
              <p align="left">Ben Freed '18 ran the fastest debut steeplechase ever for an MIT runner, taking second in 9:33.98. </p>
              <p align="left">The most dominant event in the entire meet for MIT was the 400IH for the men. Fighting a pretty strong headwind on the backstretch, Kaba ran conservatively over the first six hurdles, allowing him to finish strong in 53.94 for the victory. Cacopardo '18 went out pretty hard in the first half of the race, struggling over the final two hurdles in a second place 54.15. Sweeney finished fourth in 55.36. Kaba also placed second in the 110HH in 15.07.</p>
              <p align="left">Next week Tech is at home versus RPI and WPI in the annual Engineer's Cup.</p>
