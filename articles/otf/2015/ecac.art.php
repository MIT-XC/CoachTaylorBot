              <p><span style="font-size: 18px; font-weight: bold">Women Win ECAC DIII Championship</span></p>
              <p><span style="font-size: 18px">Motley Soars to Triple Jump Best</span></p>
              <p align="left">May 14-15, 2015</p>
              <p align="left">Even though MIT left all middle-distance and distance runners home to rest for the NCAA Division III Championship next week, they still managed to win the <a href="ecac.res.php">ECAC Division III Championship</a> held at Springfield College on Thursday and Friday. On the strength of their field event athletes the Engineers scored 64 points, 4.5 points more than runner-up Ithaca College. The men, also leaving top distance runners behind, as well as Arinze Okeke out of the horizontal jumps, finished fourth with 41 points, well behind first place Brockport State's 72 points.</p>
              <p align="left">The top performance for Tech came when Carrington Motley '16 blasted a personal best 48-7 1/4 in the Triple Jump to place third and move up to 7th on the NCAA Division III ranking list.</p>
              <p align="left">The only winning performance for the men was earned by States Lee '16, the eighth seed in the 400. Lee ran a remarkable final 200 in lane one, walking down the entire field to cross the line in 48.21, a season best.</p>
              <p align="left">Ken Cooper '15, unable to throw the Hammer on Thursday due to an academic conflict, made his Discus count, placing second with a season best 163-1. That mark moves Cooper to 20th on the NCAA list, with the top 20 making the field at midnight tonight.</p>
              <p align="left">Ben Freed '18 went for a better NCAA mark in the 3000 Steeplechase, but fell short, placing third in 9:25.82. Freed could still make the NCAA field if eight individuals decide to not run due to injury or in favor of other events.</p>
              <p align="left">Both relays for the men, the 4x100 and 4x400 placed fifth. On Thursday, States Lee, Danny Newman '17, Derek Barnes '16 and Michael Kaba '16 tried to improve on their 3:15.37 run last week. Lee opened up with a 48.4 and a slight lead. Newman held the lead with a 49.4 leg, handing off to Barnes still with a slight lead. Barnes ran a great leg, handing the baton to Kaba slightly behind in 48.9. Kaba tried to move into the lead on the backstraight but was held off. He forced his way ahead, cutting off the Brockport State runner, causing both of them to stumble a step. By the time Kaba got to the homestretch he was out of gas, partly from his first 200 effort and part from being bedridden the week before. His 50.8 gave the team a 3:17.72, missing the NCAA Championship for the first time in six consecutive seasons. The 4x100 quartet of Tre Albritten '18, Luca Cacopardo '18, Lee and Kaba, with a best of 42.04, was hoping to get to the mid-41's by stretching the exchange zones. The handoffs were near perfect and fairly deep in the zones, but for some reason the unit only ran 42.26.</p>
              <p align="left">The final points earned by MIT were brought in by Sam Parker '15 in the 800. Parker was in the second fastest section so he knew he would need to run fast if he was going to make the NCAA field. He hit the first quarter in 54.9 and began to move to towards the front. At the 600 mark he came up on the shoulder of the leader, but stalled there. In the final 100 he tried to move for the win but was not able to. He finished in 1:53.31, a season best and sixth place overall.</p>
              <p align="left">The women had two victories supporting their team win. Cimmy Virdi '16, who was voted the outstanding female athlete of the meet, won the Pole Vault at 13-7 1/4. She entered at 13-2 3/4 after everyone else in the field was already out of the competition. Her attempts at 14-1 were really strong but not quite successful. Joanna Chen '15 added an eighth place and PR at 11-8 1/2. Three other MIT vaulters cleared 11-8 1/2 although they did not score; Abby Klein 'G, Maeve Devlin '18 and Jen Tylock '17.</p>
              <p align="left">The other victory was owned by Ashley Wheeler '15 in the Hammer Throw. Wheeler was consistent in the 170's, winning with a throw of 172-6. Ellen Liverpool '15 also scored in the Hammer, placing seventh with a throw of 159-5.</p>
              <p align="left">The one second place was supplied by Hannah Chen '18 who once again broke her own freshman record in the 400IH as she ran 61.03, remaining at number three on the NCAA list.</p>
              <p align="left">Third place finishes were earned in the throws by Tilly Taylor '15 in the Javelin at 146-1 and Jackie Vahey '17 in the Discus at 138-1, both efforts were the second best performances this season respectively. Taylor is ranked sixth on the NCAA list.</p>
              <p align="left">The Engineers placed fourth in three events. Kendra Knittel '17 ran an outstanding 200 in a personal best 25.17, placing fourth overall. Knittel was also a key leg in the 4x100 Relay that placed fourth. Veronica Szklarzewski '15 led off with an outstanding leg, handing off to Nneoma Okonkwo '18. Okonkwo seemed cautious as she did not gain any ground, but her handoff to Knittel was excellent. Knittel put 10 meters on the field, handing off to Chen in first. Chen added another five meters on the field to claim first in their section, but fourth overall in a season best 48.06. Szklarzewski also earned an individual fourth place, taking the honors in the Long Jump by going 18-0 1/2. She remains number 10 on the NCAA list.</p>
              <p align="left">Preethi Vaidyanathan '15 placed fifth in the High Jump at 5' 5&quot;, but was over 5' 7&quot; on her final attempt, just knocking it off with her heel.</p>
              <p align="left">Isabella Stuopis '16 brought in the final points by placing sixth in the Shot Put. She is sitting at 23rd on the National Shot Put list, hoping someone does not enter since the top 22 women per event make the NCAA championship field.</p>
              <p align="left">Those who qualify will leave on Tuesday for St. Lawrence University in Canton, NY. The NCAA Championships begin on Thursday and continue through Saturday. The Championship web page can be accessed at <a href="http://www.saintsathletics.com/sports/2015/4/21/WTRACK_0421154811.aspx?path=wtrack">http://www.saintsathletics.com/sports/2015/4/21/WTRACK_0421154811.aspx?path=wtrack</a></p>
