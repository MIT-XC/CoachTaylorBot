              <p><span style="font-weight: bold; font-size: 18px">Tech Remains Undefeated - Compete Well Despite Cold and 30-50 mph Winds</span></p>
              <p align="left">April 4, 2015</p>
              <p align="left">The winter continues in Cambridge as the Engineers hosted the annual <a href="EngineeersCup2015.res.pdf">Engineers' Cup</a>, minus WPI, braving temperatures in the upper 40's and a wind that was around 30 mph at the start of the meet and built throughout the meet to gusts of 50 mph. </p>
              <p align="left">Throughout the meet, MIT Track &amp; Field athletes focused on competing rather than worrying about times, distances or heights, so even though many of MIT's best athletes were held out or moved to the non-scoring 10K, Tech remained undefeated by taking down Rensselaer 107 to 86 and the women winning 141.5 to 53.5.</p>
              <p align="left">The women won every event except for the 5K and 3000 Steeplechase, an event in which they did not enter anyone. With only four scoring places per event, the top places were even more important. In nine of the events MIT took three of the top four places, with complete sweeps in both the Hammer Throw and the 800. </p>
              <p align="left">Veronica Szklarzewski '15 led the team in scoring, winning the Long Jump at 16-11 1/4 and the 100 in 13.33. She also took second in the 200 and ran the leadoff leg in the victorious 4x100 Relay. </p>
              <p align="left">Maryann Gong '17, Hannah Chen '18 and Kendra Knittel '17 also won two events. Gong took the 1500 in 4:49.69 (running the third 400 in 72 and the final 300 in 56 despite the extremely windy conditions) and the 800 in 2:27.68. She also added the winning anchor leg to the 4x400 Relay. Chen won the 100HH in 15.75 and the 400IH in 66.10, as well as anchoring the 4x100 to victory. Knittel took the 400 and 200 in 61.94 and 26.31 respectively. The 200 moves her to a tie for sixth all-time at MIT.</p>
              <p align="left">Other winners were Cimmy Virdi '16 in the Pole Vault (12-5 1/2), Ellen Liverpool '15 in the Hammer Throw (157-7 1/2), Michelle Johnson '15 in the Triple Jump (35-10), Isabella Stuopis '16 in the Shot Put (42-4), Ariela Slutsky '18 with a new PR in the Discus (128-1 1/4), Natalie Alper '17 with the second best jump in MIT history in the High Jump (5-4 1/2) and Tilly Taylor '15 in the Javelin (132-4 3/4).</p>
              <p align="left">Of particular note even though she did not win the event, Jackie Vahey '17 threw the hammer 157-03 3/4 for a big personal best.</p>
              <p align="left">Although the 10K was not a scoring event, Elaine McVay '15 cruised to a 37:48.96 victory despite the incredibly windy conditions.</p>
              <p align="left">The men held out Tre Albritten '18, Chris Sweeney '18, Colin Godwin '17, Rory Beyer '17, Karl Baranov '17 and Angel Echevarria '16, and ran all the top distance runners in the non-scoring 10K. Despite these losses and leaving the 5K void of entries, Tech won seven of the eight field events and every running event entered except the 1500 and 4x100 Relay.</p>
              <p align="left">The most dominant team performances were in the Long Jump and the 100 where MIT took the top three places. Ken Cooper '15 was the top scorer for the Engineer's from Cambridge, winning the Shot Put (46-5 1/2) and Hammer Throw (173-1 1/2) as well as earning second in the Discus (151-10 1/4). Also taking two victories was States Lee '16, who won the 100 (10.89 - tied for sixth all-time at MIT) and 200 (22.19).</p>
              <p align="left">Other winners for MIT were Arinze Okeke '17 in the Long Jump (22-3), Carrington Motley '16 in the Triple Jump (43-7 1/4), Sam Ravnaas '18 in the Pole Vault (13-7 1/2), John Thomas '15 in the High Jump (6-0), Harry Rein '15 in the Javelin (181-3 3/4), Derek Barnes '16 in the 400 (51.39), Michael Kaba '16 in the 110HH (15.99), Luca Cacopardo '18 in the 400IH (56.72), Rick Paez '15 in the 800 (2:00.93), Ben Freed '18 in the 3000 Steeplechase (9:54.17) and the 4x400 Relay (Cacopardo, Sam Parker '15, Henry Tareque '17 and Barnes).</p>
              <p align="left">The non-scoring 10K had MIT taking all five places; Matt Deyo '16 (32:22.73), Allen Leung '15 (32:33.19), Cooper Sloan '18 (33:00.94), Alex Knoedler '18 (33:04.99) and Nicholas Waltman '18 (33:58.05).</p>
              <p align="left">Next week MIT travels north to Waterville, Maine for a quadrangular meet with Bates, Colby and the University of Southern Maine.</p>
