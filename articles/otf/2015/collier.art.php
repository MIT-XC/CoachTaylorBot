              <p><span style="font-size: 18px; font-weight: bold">MIT Women Assault Top 10 List</span></p>
              <p><span style="font-size: 18px">Teams Prepare for Championships</span></p>
              <p align="left">April 18, 2015</p>
              <p align="left">The MIT Women's team assaulted their Top 10 List at the <a href="Collier.res.pdf">Sean Collier Invitational</a> with nine new or improved top 10 performances, one of them a new MIT record. The weather helped as the temperature was much improved from the two previous weekends, reaching a balmy 60 degrees. While the wind was also improved with a fairly constant  15 mph headwind blasting the runners on the homestretch in the oval events. The short sprints and jumps were able to change direction and go with the wind.</p>
              <p align="left">The Hammer Throw was the premier event and the final event of the day. With most of the MIT team there cheering, Ashley Wheeler '15 threw 169-1 3/4, to move within 16&quot; of the MIT record. The performance also moves Wheeler to number 10 on the NCAA Division III Performance List. Ellen Wheeler '15, improved to 165-3 3/4, third on the MIT list and 19th on the NCAA list. Jackie Vahey '17 improved her best to 158-2, but did not move up from fifth on the MIT list. The trio placed 1-2-3 in the event.</p>
              <p align="left">Veronica Szklarzewski '15 continued her remarkable season. She added a quarter inch to her MIT record in the Long Jump, improving to 18-2 1/2. Earlier she had placed second in the 100 in 12.53 and led off the victorious 4x100 relay (49.12). She finished her day by running a new personal best of 26.33 in the 200 to move to number eight on the MIT list. Kendra Knittel '17 improved her 200 to 25.96 to slide up to fifth on the MIT list. Earlier she had not only run on the 4x100 Relay, but had earned the sixth best 100 time on the MIT list with a 12.61 performance.</p>
              <p align="left">While the Hammer Throw was the best for improved performances, the Pole Vault was the most dominant, claiming the top five places. Cimmy Virdi '16 took first with a new season best of 13-1 1/2, number two in NCAA Division III, but Abby Klein 'G jumped a new PR of 12-1 1/2 to take second place, move up to number five on the MIT list and number 12 on the NCAA performance list. </p>
              <p align="left">The other Top 10 performance was turned in by Ariela Slutsky '18 in the Shot Put. From a power position the freshman threw 37-8 to move up to number 10 on the MIT list. Isabella Stuopis '16 threw a season best of 43-3 to win the event.</p>
              <p align="left">Tilly Taylor threw a season best 138-7 1/4 to win the Javelin and move to 13th on the NCAA list.</p>
              <p align="left">A big breakthrough on the track came in the 800 Meter Race as the Engineers took 1-2-3-5 with four freshmen. Niki Waghani '18 ran a season best in winning the 800 in 2:21.70. Liz Cox '18 took second in 2:23.33, Lauren Paul '18 was third in 2:23.89 and Clementine Mitchell '18 placed fifth in 2:25.17.</p>
              <p align="left">The surprise performance of the meet was turned in by Jason Villanueva '18 in the Triple Jump. He had a monstrous PR of 46-3 1/2 to lead the jumpers to a 2-3-4 finish. A close second with regard to improved performances was that of Sam Ravnaas '18 who placed third in the Pole Vault with a huge personal best of 14-7 1/4.</p>
              <p align="left">Michael Kaba '16 had a big day on the track, running a season best 14.94 to win the high hurdles. Kaba also anchored the winning 4x100 Relay team of Tre Albritten '18, Luca Cacopardo '18 and States Lee '16 to a 42.58 season best. At the end of the day he ran the 200 for the first time this season, winning the 200 from his three relay mates with a 22.14.</p>
              <p align="left">With a gaping hole in the 1500 provided when Nick O'Connell '18 came down with mono and Colin Godwin '17 had to end his season with a stress reaction, the 800 group and distance runners stepped up to provide some support for the upcoming championships. Rick Paez '15 ran 4:03.48 for a new personal best and Allen Leung '15 won his section in 4:06.10. Both Paez and Leung finished strong with their final 300 in 48 seconds. </p>
              <p align="left">The 800 runners also came through with an impressive 4x400 Relay victory. Henry Tareque '17 led off with a 51.6, Michael Picchini '18, finishing a 1500, 800, 4x400 triple, split 53.0, Paez doing the same triple ran 51.4 and Dan Kilcoyne '16 closed the deal with a 51.4 for a 3:27.49 cumulative time.</p>
              <p align="left">Despite the strong headwind at the finish of the 400, Derek Barnes '16 ran 49.89 to win that event. The other winner for Tech was Ben Freed '18 in the Steeplechase as the freshman waited until three laps to go to pull ahead and win easily in 9:51.89.</p>
              <p align="left">Next Saturday MIT plays host to the NEWMAC Conference Championship. With so many individuals on the men's side ending their season due to illness or injury the Engineers lack depth in many areas and will depend on quality to win their 15th consecutive conference title as a strong WPI team tries to use their depth across the board to wrestle the title away. The women, also lacking depth in the running events, will depend on their dominance in the field events to take their seventh straight NEWMAC title.</p>
