              <p><span style="font-size: 18px; font-weight: bold">Gong and Zeinstra Run National 5K Performances</span></p>
              <p align="left">April 17, 2015</p>
              <p align="left">MIT took four individuals down to Princeton University to race in the Friday night distance festival at the <a href="larryellis.res.php">Larry Ellis Invitational</a>.</p>
              <p align="left">The only male runner, Sam Parker '15 ran the 800. His section was very competitive and with a 55 second first 400 he was in the middle of the pack. He missed an opportunity to move with 300 to go, but still ran his best race of the season as he finished in 1:53.52.</p>
              <p align="left">The 5K women did not toe the line until 9:15, but by this time the sky had cleared nicely and the temperature was an excellent 60 degrees. With numerous sub 16 minute runners in the field, the pace started out fast. Maryann Gong '17 and Nicole Zeinstra '16 resisted the urge to go out too fast, but still hit 77 and 79 second quarters respectively to open the race. Gong, running her first 5K since last year's outdoor NCAA Division III Championship, was cautious, slowing after an initial first 1600 of 5:17. Her subsequent 1600 splits were 5:26 and 5:34 before finishing with a 38 final 200 for a 16:55.27 performance. Zeinstra, just getting her legs back after being sick for a couple of weeks, ran a solid race, splitting 1600's in 5:20, 5:31 and 5:38 and then also finishing with a 38 for a 17:07.98 in what appeared to be an easy effort.</p>
              <p align="left">The final competitor for MIT, Elaine McVay '15, was in the final race, the 10K. With only six runners and none of them very fast it was difficult for McVay to get into a rhythm. On top of that, her back continued to give her problems, seizing up after 2000 meters. She stuck it out, but was only able to manage a 36:52.94 performance.</p>
              <p align="left">Next Saturday MIT plays host to the NEWMAC Conference Championship. With so many individuals on the men's side ending their season due to illness or injury the Engineers lack depth in many areas and will depend on quality to win their 15th consecutive conference title as a strong WPI team tries to use their depth across the board to wrestle the title away. The women, also lacking depth in the running events, will depend on their dominance in the field events to take their seventh straight NEWMAC title.</p>
