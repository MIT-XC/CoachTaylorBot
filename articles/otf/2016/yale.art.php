                  <h3>MIT PERFORMANCES NOT UP TO ENGINEER'S STANDARD AT YALE INVITE</h3>
                </center>
              
              
              <p>April 2, 2016              </p>
              <p>Fresh off a weekend of outstanding performances, MIT's Track &amp; Field team followed it up with a rather lackluster meet&nbsp;at the <a href="yale.res.php">Yale Invitational</a>.&nbsp;The non-scoring meet  was put together in just the past few weeks, involving partial teams from MIT, host Yale, Southern Connecticut State, New Haven, Brown and University of Birmingham from the UK.&nbsp; The fact that most teams were not bringing&nbsp;all of their top athletes combined with the rainy, cold start to the day,probably took many of the Tech athletes out of their competitive mentality.&nbsp; Another factor that affected the team was that all the jumps were moved to the indoor facility, which meant some athletes had to juggle their performances between jumping indoors and running outdoors.&nbsp; Excuses can certainly be made, but the competition was good enough in most cases, there was virtually no wind, which in April in New England is a rarity, and it stopped raining shortly after the start of the meet.&nbsp; It seemed something was missing from the typical&nbsp;MIT Track &amp; Field attitude, and that seemed to affect their performances.</p>
              <p>However, quite a few, particularly the women's throwers responded well.&nbsp; Jackie Vahey '17 won&nbsp;the Discus and placed second in the Hammer, where she threw a personal best of 168-3, a solid national level mark.&nbsp; Also throwing personal bests in those events were Ariela Slutsky '18, throwing 128-4 for second in&nbsp;the Discus, and Haley Strouf '18, who threw a PR of 128-3 for eighth place in the Hammer. Isabella Stuopis '16 won the Shot Put in a steady rain with a throw of 38-6 1/4 and Audrey Pillsbury '19 threw a best of 36-1 1/4 for sixth place.&nbsp; Also in the throws, Dana Balek '18 threw a personal best of 114-4 for second place in the Javelin.</p>
              <p>In the jumps, Scott Cameron '19 jumped a season best of 14-11 in the Pole Vault in claiming second place, while Leah Schmitz '16 cleared a season best of 10-2 for sixth place.&nbsp; Trevor McMichael '19 jumped a season best of 20-11 3/4 to place fourth in the Men's Long Jump.&nbsp; Katherine Prutz '18 jumped 5-3 1/4 for a huge outdoor best, taking fourth place in the High Jump.&nbsp; In the Women's Long Jump, Nneoma Okonkwo '18 won with a personal best of 17-11 3/4, while Strouf again came through, jumping a PR of 17-2 1/4 to claim second. Other winners in the jumps included Cimmy Virdi '16 in the Pole Vault at 13-1 1/2 and Billy Ruschel '18 in the Triple Jump at 46-4 1/4.</p>
              <p>On the track there were very few highlights for the Engineers.&nbsp; Standing out was Jarod Wilson '19, who won the 800, running a casual 1:55.62 and then returning to lead off the 4x400 Relay with a 48.0 lead off leg.</p>
              <p>In the 1500, Clemmie Mitchell '18 and Leandra Zimmermann '19 went 1-2 in the slow section, recording times of 4:50.27 and 4:51.21 respectively.</p>
              <p>The men's 4x100 Relay, comprised of Richard Lu '16, Michael Kaba '16, Tre Albritten '18 and Luca Cacopardo '18 won in a fine time of 42.97.</p>
              <p>Chris Sweeney '18 had a strong day in finishing second in the 110HH in 15.37 and then winning the 400IH in 54.83.</p>
              <p>Next weekend, Tech will be at home against strong squads from Bates College, RPI and Tufts University.&nbsp; They will need to step up their game to come out ahead against those very competitive teams.</p>
