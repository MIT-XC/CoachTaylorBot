                  <h3>MIT Sets Two MIT Records in First Day at NCAA Division III Championship - Come Away With 10 All America Honors</h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>May 26-28, 2016              </p>
              <p>The night before Nationals there is always a banquet for the athletes and coaches where the season's awards are presented.&nbsp; MIT often comes away with a few of these awards and this year was no different.&nbsp; Jackie Vahey won the New England Region Female Field Event Athlete of the Year.&nbsp; Nick Davis earned the New England Region Men's Assistant Coach of the Year and Halston Taylor was awarded the New England Region Men's Head Coach of the Year.&nbsp; Although both Carrington Motley and Maryann Gong had perfect cumulative GPA's, neither had the highest number of academic units, thus losing the tie-breaker for the Elite 90 Award.</p>
              <p>Day one of the <a href="http://results.deltatiming.com/ncaa/tf/2016-ncaa-d3-outdoor-championships#/2016-05-28">NCAA Division III Outdoor Track &amp; Field Championship</a> at Wartburg College in Waverly, Iowa turned out to be a much better day for MIT than was expected.&nbsp; With thunder storms predicted for the entire three days of the meet, the NCAA committee decided to move the time schedule forward two hours to try and get day one in uninterrupted.&nbsp; Not only did that work, but the MIT team seemed to also do well with that change.</p>
              <p>The women's Long Jump was the first event for Tech.&nbsp; In the third round, Nneoma Okonkwo '18 put out a bomb of a jump at 19-6, breaking her own MIT record by 6.75&quot;, and taking the lead.&nbsp;&nbsp;Two jumpers were motivated by that jump, passing Okonkwo going into the finals.&nbsp; In the first round of finals she was passed by one more jumper, but the sophomore was able to hold onto fourth place&nbsp;after only being seeded 10th.&nbsp; Haley Strouf '18 jumped 17-8 1/4 and barely fouled a jump that would have put her in the finals.</p>
              <p>Michael Kaba '16&nbsp;ran 53.05 in the 400IH to qualify for finals on Saturday.&nbsp; The race was not pretty, but Kaba got the job done in the second half of the race.&nbsp; The women were next, and although her race also had its glitches, Hannah Chen '18 had the fourth fastest time in the trials, tying her PR of 61.03.</p>
              <p>Next up was the 200 with Tre Albritten '18 representing MIT.&nbsp; Albritten did not have the best first 100, but it is the second hundred that counts and he made certain that it did.&nbsp; He placed seventh overall&nbsp;in 21.67, running into a slight headwind, making the finals on Saturday.</p>
              <p>In the women's Discus Throw, Jackie Vahey '17 placed 11th at 141-0 and Ariela Slutsky threw 133-1. Neither made the finals, but both threw about their average throw for the season.</p>
              <p>Although Cimmy Virdi '16 was not able to defend her national title due to her torn ACL, Jen Tylock '17 was&nbsp;representing her team for the first time at this level of competition.&nbsp; Tylock cleared her first two heights 11-05 3/4 and then 11-11 3/4&nbsp;before failing at 12-3 1/2, which would have been a new PR.&nbsp;The women's Pole Vault is improving at an amazing rate.&nbsp; It took 12-7 1/2 to place eighth and earn All America status this year.</p>
              <p>Maryann Gong '17 and Mary Eccles '18 represented MIT in the 10K.&nbsp; The weather was in the low 80's at the start of the race and quite humid, certainly not conducive to fast times.&nbsp;&nbsp;Eccles competed well, but did not have her best day, running 37:49.71 to place 12th after being seeded 18th.&nbsp; Gong allowed her time off from injury, and the heat, get to her.&nbsp; She fell off the lead pack at the halfway point and lost all focus, only placing 14th.</p>
              <p>The final event for day one was the men's 4x400 Relay.&nbsp; The quartet of States Lee '16, Kaba, Derek Barnes '16 and Danny Newman '17 were seeded 10th going into the race and only eight would make the finals on Saturday.&nbsp; Starting out in lane eight, Lee, still coming back from his foot injury, led off with a hard fought 48.6, putting his team in sixth at the handoff.&nbsp; Kaba started out well on the turn&nbsp;using the three turn stagger, ran a great tangent after the break and went from sixth to first by the 200 mark.&nbsp; Kaba slowed, but held onto fourth at the handoff&nbsp;with his 47.9 leg.&nbsp; Barnes went out hard moved into third and closed on the leaders with a magnificent 47.2.&nbsp; Newman ran an excellent leg, but was tripped from behind&nbsp;halfway through the final turn, stumbling onto the rail, but got back on the track after falling off for a couple of steps, and still ran a 47.9.&nbsp; However, the trip caused him to lose two places, giving his team a fifth place finish in 3:11.74, a&nbsp;new MIT record.&nbsp; The second heat only had one team run a faster time, giving the Engineers the sixth fastest time&nbsp;and putting them in the finals.</p>
              <p>Kaba led off day two for MIT with the 110HH.&nbsp;&nbsp;Seeded 18th with a 14.54, he knew it would take a spectacular race to make finals.&nbsp; Kaba had a poor start, got behind immediately and was unable to make up the ground, finishing in 14.80&nbsp;into a headwind for 14th place overall.</p>
              <p>Lee was up next in the 400.&nbsp; It had been&nbsp;nearly two months since Lee had run his 47.68 and only two weeks since returning from an ankle injury that put him out for two and a half weeks.&nbsp; At his best he was seeded ninth, but he was not at his best.&nbsp; The senior gave a good go, although he may have been on the negative side mentally.&nbsp; He finished in 48.80, failing to make finals.</p>
              <p>The final competitor on day two was Bailey Tregoning '19.&nbsp; The freshman was seeded 17th at 2:10.84, but given her rate of improvement, making finals was&nbsp;a definite possibility.&nbsp; The past three weeks she had won New England Division III, earned second place at All New Englands out of the slow section and won ECAC's.&nbsp; There was no doubt the rookie was nervous, but she seemed in control.&nbsp; The first lap was 64, well within the wheelhouse of Tregoning, but she thought the pace was much faster.&nbsp; On the backstretch when she began to move up in the field and the leaders were responding in kind, she panicked, got scared and tightened up because of it, finishing in a dismal 2:17.05.</p>
              <p>On the final day of competition, the Triple Jump led off the competition for Tech.&nbsp; Arinze Okeke '17 entered as the third seed while Carrington Motley '16 and Billy Ruschel '18 were seeded 10th and 11th respectively.&nbsp; Ruschel was in the first flight and had the best series of his young life, topping out at 48-5 1/2, a new PR and #4 on the MIT all-time list,  winning his flight.&nbsp;Okeke launched a 48-7 1/2 in the second flight to move him to&nbsp;fourth overall and a new PR of his own, moving to #2 on the MIT list.&nbsp; Motley had a fine series but could not make finals with his 47-1 effort.&nbsp; Both Ruschel and Okeke continued to jump well, but were trying too hard to jump far instead of relaxing and competing, so neither improved.&nbsp; Okeke stayed in fourth to match his indoor finish, but Ruschel fell back to eighth to earn his first All America honor.</p>
              <p>While this was going on, Kaba was about to start the 400IH.&nbsp; With a horrible start and stuttering every hurdle, somehow he still managed to run 52.46 and place seventh&nbsp;for his first individual All America award.</p>
              <p>Hannah Chen was up next in her 400IH final.&nbsp; For some reason, she also got out slow, came up on the wrong leg on the first hurdle and continued to stutter most of the hurdles.&nbsp; She pushed the final 100 to move up to fifth in 61.89.</p>
              <p>Tre Albritten stood up at the start of the 200, was dead last in the first 10 meters and seemed to give up, running 23.11, but still placing seventh.</p>
              <p>The final event for MIT was the men's 4x400 Relay.&nbsp; States Lee was in lane two&nbsp;and ran a solid leg, putting MIT in about sixth place in 48.4.&nbsp; Kaba did not go out with the vigor he exercised on Thursday and it showed as he came in with&nbsp;a much slower 48.5 split with his team in fifth place.&nbsp; Derek Barnes&nbsp;was in limbo but closed well to put his team in fourth with his 48.3.&nbsp; Danny Newman took the baton and tried to make up ground but the top three teams were too far ahead.&nbsp;At the end of the final turn, two teams ran past Newman even though he ran a 47.7 split.&nbsp; The final time was 3:13.19, good for sixth place.</p>
              <p>The men ended up with 13 points, putting them in 22nd place overall.&nbsp; The women netted nine points for 31st place.&nbsp; Overall, the Engineers came away with eight All America honors for the men and two for the women.&nbsp; Given the number of injuries the Engineers sustained this year it was a very successful finale.</p>
