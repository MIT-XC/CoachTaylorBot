                  <h3>Okonkwo Sets Long Jump Record at Collier Invite</h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>April 23, 2016              </p>
              <p>What started out as a cool and rainy meet, turned into an extremely nice day as MIT hosted the <a href="seancollier.res.pdf">Sean Collier Invitational</a> in their final tune-up prior to the start of the championship season.&nbsp; In addition to looking for championship qualifying performances, the Engineers were also taking advantage of the non-scoring meet to rest some key individuals in order to have them at their competitive best as they enter the championships next weekend.</p>
              <p>The best performance of the meet for MIT was the record breaking effort in the Long Jump by Nneoma Okonkwo '18.&nbsp;Even though there was no wind during the event, Okonkwo had a great series&nbsp;of jumps, but her 18-11 1/4 jump set the MIT record.&nbsp; Also jumping a personal best for MIT was Haley Strouf '18, jumping 17-6 1/4.</p>
              <p>The throwers did their job, winning three events.&nbsp; Jackie Vahey '17 won her two specialties, the Hammer and&nbsp;Discus Throws.&nbsp; In the Discus she threw 137-0 1/4 and took the Hammer honors with a throw of 158-9 1/2.&nbsp; Isabella Stuopis '16 won the Shot Put with a put of 41-4 3/4.&nbsp; In all three events Ariela Slutsky '18 was the second best Tech thrower, finishing second in the Discus (132-0 1/4) and third in the Hammer (139-4 3/4) and Shot (38-7 1/4).</p>
              <p>MIT competitors also took both vertical jumps, the High Jump and Pole Vault.&nbsp; Natalie Alper&nbsp;'17 won the High Jump at 5-4 1/4.&nbsp; Jen Tylock '17 claimed first place in the Pole Vault for the second meet in a row, clearing 11-2 1/2.</p>
              <p>On the track MIT held most of their runners out.&nbsp; However, Kendra Knittel '17 ran two season bests, running 12.68 in the 100 and 25.34 in the 200.</p>
              <p>The top field performer for the men was Cody Bratten '18.&nbsp; Bratten threw two outdoor personal bests, finishing second in the Discus at 149-7 1/4 and then fourth in the Shot Put at 44-5 3/4.</p>
              <p>On the track there were many season and personal bests.&nbsp; MIT moved  a few athletes to different events in order to increase their options in the championships.&nbsp; In the short sprints States Lee '16 raced in the 100 Meters for the first time this season, running 11.25.&nbsp; In the 200, Derek Barnes '16 delivered an excellent 22.49.</p>
              <p>The 400 was probably the best event for Tech.&nbsp; Barnes, prior to running the 200, won in a season best 49.13.&nbsp; Danny Newman '17 delivered a second place in 49.45 and Tom Frejowski '19 ran a personal best of 49.98, breaking the 50 second barrier for the first time.&nbsp;Richard Lu '16 ran a season best of 56.22 in the 400IH.</p>
              <p>In the 800, Nathan Munet '19 ran a very smart race and set a season best of 1:56.92.&nbsp; Running for the first time since the indoor season, Chris Mutty '19 ran a 1:58.80.</p>
              <p>David Walter '18 ran a PR in the 1500, racing well to finish in 4:02.67.&nbsp; Walter had bookend&nbsp;quarters of 63 and what would have been a 62 as ran his final 300 in 46.&nbsp; The middle 400's were too slow as the pack settled for 66-67 pace.</p>
              <p>Aidan Gilson '19 continued to improve in the 3000 Meter Steeplechase.&nbsp; He took the lead at the beginning and pulled away more as the race progressed, running 9:43.13 for a new PR.</p>
              <p>MIT won the 4x100 Relay with the quartet of Newman, Lu, Tre Albritten '18 and Lee running 42.49 for a season best.&nbsp; In the 4x400 Relay, Tech worked on finding depth as they ran a B team.&nbsp; The foursome nearly pulled out the victory, losing by .01 second.&nbsp; Frejowski opened with a fine 50.3&nbsp;to give MIT the lead.&nbsp; Munet added a 51.2, keeping MIT in front.&nbsp; Gilson ran his first 400 of the year, splitting 51.0, but nearly losing the lead as the opposing runner ran 48.7.&nbsp;Henry Tareque '17 anchored after having raced to a season best of 51.34 in the open 400.&nbsp; Tareque held his opponent off for the entire lap, but was out-leaned despite his 50.7 split.&nbsp; The total time was 3:23.42.</p>
              <p>Next weekend, the Engineers will travel to the Coast Guard Academy in New London, CT to defend their NEWMAC conference titles.&nbsp; The men have won each year since 2000 and the women since 2009.</p>
