                <center><h2>MIT Begins Outdoor Season With National Level Performances</h2></center>
              <p>&nbsp;</p>
              
              
              <p>March 25-26, 2016              </p>
              <p>Two weekends after the conclusion of the NCAA Division III Indoor Track and Field Championship, MIT began their outdoor season with three different meets.&nbsp; On Friday, the pole vaulters and throwers competed at the <a href="sdsu.res.php">San Diego State University Invitational</a> at the conclusion of the Spring Break Trip to California.&nbsp; Cimmy Virdi '16 led the way, finishing second with the nation's leading vault for Division III at 13-8 1/4.&nbsp; Jen&nbsp;Tylock '17 also performed well, clearing 11-8 1/2.&nbsp; All three of the men performed well with Ben Schreck 'G clearing 15-1, Scott Cameron '19 successfully getting over 14-7 1/2 and Marshall Wentworth '16 making 14-5 1/2.</p>
              <p>The throwers had a pretty solid opening day.&nbsp; Jackie Vahey '17 dominated for the Engineers, throwing a PR of 42-10 3/4 in the Shot Put, 138-1 in the Discus and 151-3 in the Hammer.&nbsp; Isabella&nbsp;Stuopis '16 put the shot 41-3 and Ariela Slutsky '18&nbsp;threw 38-6 3/4.&nbsp; Audrey Pillsbury '19 nearly took down the freshman record in the Hammer with her throw of 142-10.&nbsp; For the men, Cody Bratten '18 was the only thrower for MIT and he had two strong performances, 148-9 in the Discus and 138-3 in the Hammer.</p>
              <p>On Saturday morning, back in Massachusetts, the distance runners took advantage of the excellent 50 degree temperature at the <a href="bearsinvite.res.php">Bridgewater State Invitational</a> and made the attempt to qualify for the New England Division III Championship 10K as they may not race that distance again until that championship.&nbsp; Maryann Gong '17, running her debut 10K was going after a solid NCAA qualifying performance.&nbsp; Although the men and women were racing together, Gong had no one to run&nbsp;with as the women could not hold her pace and the back of the men's pack was too erratic in pace.&nbsp; As she has a bad habit of doing, the junior started out too fast with an 80 second quarter.&nbsp; However, she soon settled in at 85-86 second pace, hitting the 1600's in 5:39, 5:42, 5:40, 5:45, 5:45, 5:39 and finishing with an 81.&nbsp; Her overall time of 35:31.22 will certainly earn her a spot at Nationals.&nbsp; Mary Eccles '18 ran a nice personal best of 37:10.30.&nbsp; She was followed by Nicole Zeinstra '16 in 37:20.33, Jenny Xu '19 in 37:54.59 and Camila Thanos '19 in 38:32.56.&nbsp; Matt Deyo '16 was attempting to qualify for Nationals, but there was just no one for him to race with.&nbsp; His first 1600 was solid in 4:53, but he soon slowed, running 5:04, 5:03, 5:05, 5:04 and 5:06, finishing in 31:27.58.&nbsp; Racing together through 23 of the 25 laps, freshmen Daniel Weiss, Dennis Maloney and Josh Rosenkranz ran 31:31.47, 31:34.02 and 31:38.41 respectively.</p>
              <p>Also on Saturday, the rest of the technical athletes joined the vaulters (sans Virdi) and throwers in San Diego to compete in the <a href="springbreak.art.php">Point Loma Nazarene Invitational</a>.&nbsp; This was a scored meet, but with only&nbsp;16 athletes per gender competing for Tech, the ability to win the meet was severely hindered.&nbsp; Nevertheless, the Engineers made a good run of it.&nbsp; The women finished second in the 15 team field with 192.5 points and the men were third among the 14 teams with 185 points.&nbsp; In beautiful 75-80 degrees and sunny skies, MIT competed to the best of their ability.&nbsp; However, at the start of the meet, the mental side was not keeping up with the physical side.&nbsp; </p>
              <p>In the Long Jump only Arinze Okeke '17 performed at a reasonably high level as he took fifth place at 22-6.&nbsp; MIT's next best jumper was Luca Cacopardo '18 at 21-4 1/4.&nbsp; In the High Jump&nbsp;nothing was happening for Tech.&nbsp; Natalie Alper had the best jump for the women at 5-1 3/4 and Milo Knowles '19 only managed 6-0 for the men.&nbsp; On the track things did not go any better.&nbsp; In their Steeplechase debut Aidan Gilson '19 and Leandra Zimmermann '19 ran reasonably fast performances on their abundance of talent, not their mental skills as they lost focus of the race very early on.&nbsp; Gilson ran 9:55.91, placing sixth and Zimmermann placed third in 11:51.02.&nbsp; The next event, the 4x100 Relay made it seem as though it was going to be a very long day at Point Loma.&nbsp;In the women's race, Annie Dai '18, coming back from months of inactivity due to&nbsp;a concussion, was unable to catch Alper, forcing Alper to nearly stop to receive the baton.&nbsp; Kendra Knittel '17 and Hannah Chen '18 made up a lot of ground but only managed fifth in 49.99.&nbsp; The men were next.&nbsp; Tre Albritten '18 had an excellent opening leg and the handoff to Michael Kaba '16 went well.&nbsp; Vying for first place, things then went wrong.&nbsp; First, Danny Newman got out late, and also slow.&nbsp; Kaba ran up on him and in the attempt to pass the baton it dropped, ending their race.</p>
              <p>Just when it looked the bleakest, things started to turn around.&nbsp; In the Hammer, Vahey led MIT to a 1-6-7 finish with a huge throw of 165-11.&nbsp; Following Vahey were Pillsbury at 140-1 and Slutsky at 134-2.&nbsp; The vaulters also took charge, placing 2-4-6 as Tylock took second place at 11-5 3/4.&nbsp; Sherry Wan '16 claimed fourth, also at 11-5 3/4.&nbsp; For the men, Kaba started things going in the right direction despite a sprained ankle he suffered on the botched handoff attempt in the 4x100.&nbsp; He ran a personal best of 14.54 in the 110HH to claim third place.&nbsp; Immediately after, States Lee '16 put down a 47.68 to place second in the 400.&nbsp; Both Kaba's and Lee's performances should get them to Nationals.&nbsp; MIT had support in both events with Chris&nbsp;Sweeney '18 at 15.34 in the 110HH and Newman ran 48.97 in the 400.</p>
              <p>Once the ball got rolling in the right direction the momentum continued.&nbsp; Chen jumped 18-1 to place fourth in the Long Jump, and at the same time ran 15.12 in the 100HH to place third.&nbsp; Stuopis and Vahey went 2-3 in the Shot Put with both throwing 41-10 1/2.&nbsp; Okeke led an assault on the Triple Jump, placing third with a huge leap of 48-4.&nbsp; Okeke's performance should earn him a return trip to the NCAA Division III Championship in May.&nbsp; Billy Ruschel '18 and Angel Echevarria '16 supported with&nbsp;fifth and sixth place jumps of 47-3 and 46-10 respectively.&nbsp; Albritten then placed fourth in the 100 at 10.82 and third in the 200 in 21.49, a performance that should make Nationals.</p>
              <p>Chen was not done as she took first place in the 400IH with a 62.80 performance.&nbsp; Kaba matched her by taking the men's race in 53.89.&nbsp; Sweeney and Cacopardo placed third and fourth, running 54.33 and 54.52.&nbsp; </p>
              <p>Vahey and company were not done.&nbsp; Vahey&nbsp;won her second event, this time claiming first place honors in the Discus with a new MIT record of 140-10, breaking her own record.&nbsp; Pillsbury took third at 124-0 and Slutsky was eighth at 120-0.&nbsp;Bratten placed second in his specialty, throwing the discus 147-11.</p>
              <p>In the final event&nbsp;of the evening, the men's 4x400 Relay, the Engineers were hoping for a National qualifying performance.&nbsp; However, the gas was running low after 10 hours of competition.&nbsp; Lee led off with a slowish 48.4, but gave Tech the lead.&nbsp; Kaba's ankle finally started to give out, and even though the senior held the lead, he only ran 50.2.&nbsp; Albritten's first effort in the 4x400&nbsp;was solid, but not what he is capable of as he held onto the lead with a 49.7.&nbsp; Newman ran a great anchor, holding off the other teams with his 48.5 to give MIT the victory in 3:17.08.</p>
              <p>Next week, MIT will travel to Yale with part of the team to compete in a small invitational.</p>
