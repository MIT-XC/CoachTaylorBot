                  <h3>Tech Performs Well - Setting Two Varsity and Two Freshman Records                  </h3>
                  <p>&nbsp;</p>
                </center>
              
              
              <p>May 13-14, 2016              </p>
              <p>After two weeks where the MIT Men's and Women's Track &amp; Field teams put together championship victories at the NEWMAC Conference and New England Division III Regional meets, they needed to back off for a couple of weeks as they prepared for the NCAA Division III&nbsp;Championship.&nbsp; The <a href="neicaaa.res.php">NEICAAA Championship</a> was the first of those preparation weeks.&nbsp; This meet is unique in that all three&nbsp;NCAA Divisions in New England compete against each other for the title.&nbsp; This particular year the meet was in conflict with the IC4A Championship, an even larger meet that includes most of the teams in the NEICAAA meet.  Most teams went to the NEICAAA Championship but there was no doubt the overall quality of this meet was less than in other years.</p>
              <p>Even though MIT left some of their top athletes at home to rest or heal, they brought enough weapons to take fourth in the men's meet with 56 points while the women took fifth place with 46 points.&nbsp; The amazing part, even with leaving some of their best athletes at home the men accomplished this feat with only 16 people while the women did it with only nine athletes.</p>
              <p>The men brought a balanced attack that was led by two victories.&nbsp; On Friday night, three of Tech's distance runners were going to attempt qualifying for Nationals in the 10K.&nbsp;Although the competition did not help with a fast enough pace, and the MIT runners were still feeling the effects of the previous two weeks of 5K's and or 10K's, they gave it their best shot.&nbsp; Benji Xie 'G, after winning the&nbsp;10K at the New England Division III Championship, topped that by pulling away from the competition with two miles to go.&nbsp; His fifth mile was a 4:56 and the sixth&nbsp;a 4:49 as he finished it off with a 71 for a victory in 30:48.41, a personal best.&nbsp; Dennis Maloney '19 placed third while setting the MIT Freshman record in 31:16.23.&nbsp; Daniel Weiss '19 added a point with an eighth place finish.</p>
              <p>Also on the first night, Arinze Okeke '17 jumped a personal best of 23-1 1/4 in the Long jump to place fifth.&nbsp; Unfortunately he had one jump significantly farther, but his fist was placed behind him in the pit, killing the jump.</p>
              <p>Most of MIT's entries on Friday qualified for the finals the next day.&nbsp; Michael Kaba '16 led the way, qualifying for both the 110HH and 400IH.&nbsp; Chris Sweeney '18 qualified for the 400IH with a PR of 53.65, and although he did not qualify in the 110HH, he did set a personal best of 14.94.&nbsp; Danny Newman '17 qualified in the 400, matching his personal best of 48.97.</p>
              <p>The women had a strong first day as well.&nbsp; Jackie Vahey '17 led the way with a third place in the Hammer Throw.&nbsp; Vahey moved up two spots on the NCAA Division III list to number 18 with her PR of 170-1.&nbsp; Ariela Slutsky '18&nbsp;did not place but threw a big personal best of 154-9.</p>
              <p>Nicole Zeinstra&nbsp;'16, feeling her 10K / 5K double from the week before was not able to mount a quality qualifying performance although she gave it her best effort.&nbsp; Nevertheless she placed fifth in 37:36.37.</p>
              <p>In the Long Jump, Nneoma Okonkwo '18 placed fourth at 18-3 3/4 and Haley Strouf '18 took seventh place at 18-1 1/2.</p>
              <p>On Saturday, the men started strong and just kept going.&nbsp;The 4x100 Relay team of Sweeney, Kaba, Newman and Tre Albritten '18 ran a season best of 41.74 to place fifth and move to #24 on the NCAA list.&nbsp; If they can drop another .3 they should make the qualifying field.</p>
              <p>The members of the 4x100 came back and added quite a few points for the cause.&nbsp; Kaba, having run 14.59 in the trials of the 110HH, made up for a poor start and placed sixth in 14.68. Newman was up next, earning sixth place in the 400 with a 49.09.&nbsp; Three events later, Kaba and Sweeney were back in the 400IH.&nbsp; Kaba, getting a good headwind on the homestretch the way he likes it, powered past his competitors to win in a new MIT record of 52.18.&nbsp; Sweeney added a 54.26 effort for seventh place.</p>
              <p>In the field events, Cody Bratten '18 threw a PR in the Discus to claim fourth place with a throw of 156-6.&nbsp; If the sophomore can add three more feet he should make Nationals. Okeke came back in the Triple Jump, adding an eighth place with his 46-11 3/4.</p>
              <p>The final event for the men was the 4x400 Relay.&nbsp; With Kaba's foot bothering him, Jarod Wilson's '19 stress reaction putting him on the shelf, and already missing States Lee '16 to injury it was time to try a new lineup.&nbsp; Albritten, typically a short sprinter, would lead off, followed by Sweeney, then Derek Barnes '16, and anchored by Newman.&nbsp; Albritten did not appear to be into the race mentally, running far from his best with a 50.3 opening leg.&nbsp; Sweeney, running his first 400 race at MIT without&nbsp;hurdles, absolutely hammered the first 200, making up at least 15 meters on the field.&nbsp; He held on well in&nbsp;the final 100 into the wind, running 48.8.&nbsp; Barnes ran an excellent, well paced leg as well, keeping MIT in the thick of things with a 48.7.&nbsp; A poor handoff dropped MIT to fourth but Newman made up for it and then held on in the final straight to finish in third in a season best 3:16.73.&nbsp; Tech will have to drop two more seconds at least in the final meet if they hope to qualify for Nationals.</p>
              <p>The women in day two performed very well.&nbsp; Although only represented in two track events they made the most of it.&nbsp; Hannah Chen '18, having run the fastest time in the trial of the 400IH at 61.39, finished third in the finals with a 62.36.&nbsp; Even though she started well and hit 17 steps on the backstretch she lost a lot of ground.&nbsp; In the final 100 she made up the places, having her best last 100 ever, but it was not enough to set a PR.</p>
              <p>In the 800, Bailey Tregoning '19 was in the second fastest of the sections, but was fortunate enough to have two opponents set a strong opening 400 of 64 seconds.&nbsp; With the place slowing in the third 200, Tregoning prepared to take the lead and did so in the final 200 as she powered home in a personal best of 2:11.51, a new MIT Varsity and Freshman record.&nbsp; Her performance was good for second place overall.&nbsp; She is currently ranked 19th on the NCAA list with one week to go and 22 individuals making the final field.</p>
              <p>Slutsky figured if one PR was a good thing, then more must be better.&nbsp; In the Shot Put, although she missed placing by one place, she threw 42-10 3/4 for a big personal best.&nbsp; In the Discus, she continued the trend, this time placing fourth with a big PR of 141-3.&nbsp; Vahey added a fifth place at 139-8.</p>
              <p>Sherry Wan&nbsp;'16 added the final points for Tech, placing third in the Pole Vault at 11-7 3/4.&nbsp; She will need to clear at least 12-2, possibly 12-2 1/4 in her final meet if she is to make the NCAA field.</p>
              <p>Next week, those needing the opportunity&nbsp;will travel to Westfield State University for the ECAC Division III Championship for a&nbsp;final effort at qualifying.&nbsp; This meet, held on Wednesday and Thursday as Thursday is the final day to qualify for the NCAA Championship, is also in the middle of MIT's final exam week.</p>
