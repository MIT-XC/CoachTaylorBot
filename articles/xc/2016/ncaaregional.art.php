                  <p><span class="headline">Tech Cross Country Claims Men's and Women's NCAA Division III New England Region Championship</span> </p>
                  <p align="left">November 12, 2016</p>
                  <p align="left">Entering the <a href="ncaaregional.res.php">NCAA Division III New England Region&nbsp;Championship</a>, the MIT women's team, ranked number two in the country, were favored to win by a small margin over Williams College and Tufts University, ranked number five and six respectively.&nbsp; The men were ranked fifth whereas Williams was ranked third, although both teams had experienced recent losses.&nbsp; Williams was racing without number one runner Peter Hale, while MIT was without number three man, Josh Derrick '20.&nbsp; In addition, two days ago, number one runner, Matt Deyo 'G was hit by a car while riding his bike to class, injuring his knee and ankle in addition to some road rash.&nbsp; The decision to race was made the morning of the event.&nbsp; There was a lot of doubt, but if anyone could put the injuries and pain aside, it would be Deyo, who competed last year with a broken foot.</p>
                  <p align="left">The women raced first.&nbsp; After a fast start by a couple of runners who were no threat to win the title, the pace slowed.&nbsp; MIT, running an aggressive race plan, ended up with six of their seven runners at the front, running abreast at 5:30 for the first mile, which may have been an inaccurate location as many of the mile marks seemed to be off&nbsp;for both genders.&nbsp; The Engineers really did nothing with the race at this point, allowing the other teams to gather and recover.&nbsp; By the time Megan McCandless decided to make a move after two miles, the field was ready for a 1.73 mile race&nbsp;with little to no fear of &quot;dying&quot;.</p>
                  <p align="left">McCandless opened up a lead on the field, pulling ahead of teammates Maryann Gong '17, Leandra Zimmermann '19 and Christina Wicker '17 by 14-16 seconds in the third mile.&nbsp; Mary Eccles '18 was another eight seconds back with Katie Bacher '20.&nbsp; Jenny Xu '19 sprained her ankle twice in the third mile  on the very rocky trails, putting her far behind.&nbsp;&nbsp;Once they hit the field for the final 700 meters, McCandless still had the lead, but it was narrow.&nbsp; In the final straight, Brittany Bowman '18 from Tufts pulled ahead, taking the victory by two seconds, 21:14 to 21:16.&nbsp;Zimmermann overtook Gong to place 11th and 12th in 21:38 and 21:40 respectively.&nbsp; Williams was seventh, ninth and 10th, giving MIT a 25 to 26 lead through three runners.&nbsp;Wicker placed 18th&nbsp;to Williams 21st for their fourth finishers and Eccles lost by one to the Williams fifth runner, 24th place to 25th place to give Tech a three point victory, 68 to 71.&nbsp; Tufts placed one, six, 14, 26 and 30 to total 77 points.</p>
                  <p align="left">The men's race had an individual favorite, Tim Nichols '17 from Tufts University, but it was not clear if he would go for the victory right from the gun. MIT, hoping for an aggressive race, was hoping he would take it out.&nbsp; Nichols did indeed go out hard, as did a few positive thinking opponents, who would later pay the price.&nbsp;&nbsp;Deyo and Dennis Maloney '19 were in fifth and sixth at the mile mark at a crisp 4:35 as they positioned themselves in position to pick off those who went out with Nichols.&nbsp; The rest of the MIT team were unable to find each other and work together.&nbsp; Rory Beyer '17 was racing well at 4:40.&nbsp; Daniel Weiss '19 was supposed to be with Beyer, but was three seconds behind.&nbsp; Ben Freed '18 was at a respectable 4:45, but Nicholas Waltman '18 and Alex Knoedler '18 were back at 4:47 and 4:49 respectively.&nbsp; Knoedler had fallen on the first straight, giving him&nbsp;a good excuse.</p>
                  <p align="left">Not a lot changed for the top three as they came back onto the field for the second mile and then went back into the woods for the third mile.&nbsp; Deyo and Maloney were still in fifth and sixth and Beyer was only six seconds back in about 12th.&nbsp; Freed was moving up in the field even though he was 16 seconds behind Beyer&nbsp;at the two mile mark and :20 back at the three mile mark.&nbsp; Knoedler had caught up to Waltman and Freed was closing in on Weiss&nbsp;who was falling back relative to his teammates.</p>
                  <p align="left">The hilly fourth mile was a killer for Tech.&nbsp; Deyo and Maloney had an excellent mile, separating themselves from the rest of their teammates by 12 to 27 seconds.&nbsp; The delta was the worst it had been all season.&nbsp; Granted, throughout the past five weeks the Engineers had lost their 3,&nbsp;4, and 5 runners due to injury, forcing MIT to prove its depth.&nbsp;</p>
                  <p align="left">Deyo closed hard, moving into second place in 24:28.&nbsp; Maloney also closed well, finishing in fourth in 24:33.&nbsp; Beyer had a great race, running 25:03 for 13th.&nbsp; At this point Williams had two runners across the line, finishing in sixth and 12th.&nbsp; It seemed like an eternity before MIT put another runner across the finish, although it would only be 33 seconds.&nbsp; During that wait, Williams put three more runners across at 28th, 29th&nbsp;and 35th to total 110 points.&nbsp; Freed crossed the line in 43rd at 25:36 and Weiss in 48th at 25:42.&nbsp; MIT had 19 points through three runners, but the 91 for four and five gave them 110.&nbsp; The NCAA tie-breaker pairs the teams' individual runners&nbsp;against each other and the team that wins the majority of the top five battles&nbsp;wins the tie-breaker.&nbsp; MIT's one, two and three runners beat the Williams top three runners in each head to head comparison even though Williams won the number four and five runner battles, giving MIT the victory.</p>
                  <p align="left">It was a great day for MIT Cross Country.&nbsp; It is the first time&nbsp;both genders won in the same year, which also marks the first time MIT has swept the New England Division III championships in Indoor Track &amp; Field, Outdoor Track &amp; Field and Cross Country for both genders in the same calendar year.</p>
                  <p align="left">In one week, MIT will join 31 other teams to decide the NCAA Division III Championship in Louisville, Kentucky.&nbsp; For MIT to make the podium both teams will have to do better, running closer to their potential.</p>
