                  <p><span class="headline">NCAA Championship Mixture of Outstanding and Poor Performances</span> </p>
                  <p align="left">November 19, 2016</p>
                  <p align="left">Coming off a pretty strong regional performance, and an outstanding week of practice, the Engineers appeared very ready for the <a href="http://results.deltatiming.com/xc/2016-ncaa-d3-xc-championships">NCAA Division III Cross Country Championship</a> hosted by Spalding University in Louisville, KY at the Tom Sawyer Park, at least Coach Halston Taylor so believed.&nbsp; It seems that was indeed the case for a few individuals, but could not have been further from the truth for others.&nbsp; Very poor preparation by the coach left many of the team members ill prepared, some physically and some mentally.&nbsp; The women, seeded second in the country for most of the season, including going into this meet, finished in fifth place, two points off of the podium.&nbsp; The men earned their best finish ever, placing seventh although they had been seeded third going into the meet.</p>
                  <p align="left">The women, in box 41, may have been at a disadvantage, possibly running slightly farther to the first turn.&nbsp; The teams with lower box numbers, for the women's and men's races seemed to be in front coming off the start.&nbsp;The higher box numbers were certainly at a disadvantage with regard to being on the far outside of the first turn, which was sharp enough to cause them to run quite&nbsp;a bit farther.</p>
                  <p align="left">Regardless, at the 3K mark Tech was in 8th place with 339 points, while Johns Hopkins, who had been in box 34, was in first with 74 points.&nbsp; Megan McCandless '20,  had moved up&nbsp;a lot from 28th at the mile mark to 12th after the two mile mark.&nbsp; Unfortunately, the next closest MIT runners, Maryann Gong '17 and Christina Wicker '17, were right around 100th place. Interestingly, Wash U and Williams were 18th and 19th at the 3K with 449 and 469 points respectively.</p>
                  <p align="left">Generally runners do not move up that much&nbsp;on a flat course when the pace is not that aggressive to start with by the leaders.&nbsp; By the 3K, MIT had cut their score by about 100 points, coming in at 228 in third place, just ahead of quite a few teams, including Wash U, now in 6th with 278, and Williams in 8th with 294 points.&nbsp; Meanwhile, Johns Hopkins had slipped slightly to 87 points.</p>
                  <p align="left">This is where Coach Taylor failed to prepare his team.&nbsp; Instead of realizing they were moving up and had a lot of room to go, it seemed that they had been frustrated they were not further ahead and appeared to lose hope and confidence.&nbsp; They had not prepared for not getting out well and staying relaxed in the process of moving up.&nbsp; The net result of this poor preparation was that the team basically stayed the same with 234 points.&nbsp; Many of the other teams lost a lot of places, but not Wash U and Williams.&nbsp; Wash U moved up to 202 points, good for second, while Williams moved up to 232 points, good for fourth.&nbsp; Johns Hopkins maintained first although they dropped back a bit to 128 points.&nbsp; SUNY Geneseo, had been in second the entire meet until the end&nbsp;when Wash U pushed them back to third with 214 points.&nbsp; </p>
                  <p align="left">McCandless finished in 17th, earning her first All America honor with her 21:04 performance and the highest finishing freshman.&nbsp; Gong, who had finished 16th in 2015 was 64th in 21:43.&nbsp;&nbsp;Wicker, a 2014 All American, who had finished 48th last year, was 79th in 21:46.&nbsp; Leandra Zimmermann '19, who was 45th in 2015 as a freshman, placed 97th in 21:58.&nbsp; Mary Eccles '18 closed out the scoring&nbsp;with her 22:05 in 122nd place after finishing  60th in 2015.&nbsp; The confidence and attitude this team showed all season in 2014, and somewhat at this meet in 2015, was lacking all season.</p>
                  <p align="left">The men were probably over-ranked at the end of the season.&nbsp; Having lost three very good runners&nbsp;who were their 3-4-5 runners; Colin Godwin '16, Josh Derrick '20 and Steven Goldy '20, put the usual 7-8-9 runners into the position of being 4-5-6 runners.&nbsp; In addition, with number one runner, Matt Deyo '16 being hit by a car a week ago, giving him a severe contusion at best and meniscus tear, or both at the worst, made the team very vulnerable.&nbsp; Somehow, they never worried about what could have been.&nbsp; Instead they stepped up and won the New England regional.</p>
                  <p align="left">Now at Nationals, after seeing the women not get out well, and being on the other end of the starting line, they made sure to get out and put themselves in a position to succeed.&nbsp; Deyo, who could only manage eight miles this week, and that was very painful, blocked out the pain and went out with the leaders, as did Dennis Maloney '19.&nbsp; Rory Beyer&nbsp;'16, the number three runner, who had only been running for five weeks, also went out fast.&nbsp; Daniel Weiss' '19 and Ben Freed '18 were also out pretty well&nbsp;at the mile mark, somewhere in the 40-60 range of front runners.&nbsp; At the 2K, the Engineers were in the lead with 88 points to 94 for North Central.</p>
                  <p align="left">Throughout the second and third mile, Deyo and Maloney were still among the top 15 runners and Beyer was in the top 25.&nbsp; However, Freed had lost over 20 seconds to his front-running teammates and Weiss over 30 seconds.&nbsp; By the 6.2K, MIT had slipped to third although they had actually reduced their score among the top three runners.&nbsp; The problem was that the four and five runners had lost 98 points in the past 4K.</p>
                  <p align="left">Bad got worse.&nbsp; Deyo moved up in the final 2K, finishing seventh overall in an impressive 24:28.&nbsp; Maloney slipped slightly to 12th in 24:39, the highest finishing freshman or sophomore.&nbsp;&nbsp;Beyer lost some ground, finishing 43rd in 25:02, pretty remarkable for five weeks of running.&nbsp; Overall, the trio lost eight points in the final 6K.&nbsp; Freed was the fourth runner for Tech, finishing in 25:48 for 165th place and Alex Knoedler '18 closed out the scoring, running a well paced race to finish&nbsp;192nd in 26:02.&nbsp; MIT lost 205 points in the final 6K from the four and five runners.&nbsp; To say the coaching was a failure is a severe understatement.&nbsp;The delta for one through five was the worst it had been all season&nbsp;at 1:34.</p>
                  <p align="left">On&nbsp;the positive side, MIT finished with three All America winners.&nbsp; The women finished among the top six teams in the country for the eighth consecutive year and the men earned their highest finish ever at the NCAA Division III Cross Country Championship.&nbsp; What could have been?</p>
                  <p align="left">Graduating Gong, Wicker, Deyo, Beyer and Godwin, the Engineers will have to hope the preparation for some of those who fell short today will be good enough to move up next year.                  </p>
