              <p><span class="headline">WOMEN WIN NEW ENGLAND REGION - ADVANCE TO NCAA CHAMPIONSHIP</span></p>
              <p align="left">November 10, 2012</p>
              <p align="left">The lady harriers from MIT entered the <a href="ned3.res.php">NCAA Division III New England Regional Cross Country Championship</a> as the number one team in the country and did not disappoint, claiming the victory among 51 complete teams at Stanley Park, hosted by Westfield State University.</p>
              <p align="left">On a sunny, 60 degree day, all eligible Division III teams in New England toed the line to vie for a shot at advancing to the NCAA Division III Cross Country Nationals next Saturday, November 17. The top two teams in each of the eight regions would automatically qualify. Another 16 teams would be selected at-large by the NCAA committee. </p>
              <p align="left">The start of the 6K race was a bit precarious with permanent soccer goals (no nets) presenting obstacles en route to a sharp right turn ~300 meters out, right in the middle of starting arc, meaning all the teams to the right had to sprint to the left side of the field and pray they did not get trapped on the inside of the turn.</p>
              <p align="left">MIT was a little to the right of the arc in slot 39 of 51. The Engineers got out to a great start, managing the field and the turn without error. Tech took their time, moving up through the field without much effort. By the mile mark the lead group for MIT, consisting of Elaine McVay '15, Brooke Johnson '13 and Sarah Quinn '16, hit the mile in 5:38, sitting in 3-4-5 and looking very strong. The B group of Nicole Zeinstra '16, Kaitlin Allen '14, Martina de Geus '14 and Alexandra Taylor '14 hit the mile at 5:47 and were already in the top 20.</p>
              <p align="left">As they entered the woods, MIT continued to push the pace through the hilly part of the course. Between the two and three mile mark the Engineers were in a dominating position, having five runners in the top 12 positions. McVay, Johnson and Quinn hit the two mile at 11:34 while the B group were at 11:50. In the hilly third mile, Quinn's hamstring began to bother her and she started to slow. However, Zeinstra, Allen and de Geus moved up well.</p>
              <p align="left">In the final .72, McVay moved to earned fifth place in 21:43, Johnson was eighth in 21:48, Zeinstra 10th in 22:00, Allen 12th in 22:04 and de Geus 23rd in 22:13. Quinn tried, but her ailing hamstring forced her to fall back to 31st in 22:21. Taylor completed the team in 45th at 22:42. MIT took the title without the scoring benefit of one of their top runners, totaling 58 points to second place Williams College with 75 points.</p>
              <p align="left">The men from MIT did not fair quite as well. At the start the team was almost to the end of the arc in 49th position. To make matters worse, it was not clear the gun had been fired as the sound was not very loud. Half of the MIT contingent got caught standing still at the start and could not get into a good position at the end of the straight.</p>
              <p align="left">Although both the A group for MIT (Roy Wedge '14, Justin Bullock '14, Ben Mattocks '13, Benji Xie '15) and the B group (Ian Tolan '16, Edgar Gridello &quot;16, Allen Leung '15) hit the mile mark in a very respectable 4:55 and 4:59 respectively, they were very far back in the field. In the following short loop in the woods there was not adequate room to pass, forcing them to come to a near halt, losing their momentum and rhythm.</p>
              <p align="left">By the two mile mark they were back in 70-90th positions between 10:21 and 10:29, about 20 to 30 seconds slower than the race plan asked for. Had they continued on pace they would have been right with the leaders of the race.</p>
              <p align="left">In the third and fourth mile Bullock and Wedge made strong moves getting up to the top 30. Bullock fell off after the fourth mile, but still managed to hold on for 45th place in 25:55. Wedge moved up to 27th place, good enough for All Region honors in 25:29. The tight delta MIT had maintained all year disappeared after Mattocks clocked a 26:03 for 52nd place. Tolan was 52 seconds off of Wedge, finishing in 66th place. Xie placed 73rd in 26:30 to close out MIT's scoring, good for ninth place among the 50 teams with 263 points. Gridello was close behind in 75th, also running 26:30 and Leung closed things out for MIT in 82nd in 26:42.</p>
              <p align="left">The women's team will fly out to Terre Haute, Indiana on Thursday and hope to improve on last year's fifth place finish. In the past four years the women's team has had four top 10 finishes at the NCAA Championship, finishing 10th in 2008, fifth in 2009, third in 2010 and the fifth in 2011.</p>
