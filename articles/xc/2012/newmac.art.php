              <p><span class="headline">CROSS COUNTRY DEFENDS NEWMAC TITLES, WOMEN GAIN PERFECT SCORE</span></p>
              <p align="left">October 27, 2012</p>
              <p align="left">MIT's Men's and Women's Cross Country teams traveled to Smith College, the host of the 15th annual <a href="newmac.res.php">NEWMAC Cross Country Championship</a>, hoping to defend their titles as champions. The male harriers from MIT had never lost the championship and were now going for their 15th consecutive NEWMAC victory. The women, ranked number one in the country for Division III, were attempting their sixth title in succession.</p>
              <p align="left">The women toed the line first. The goal was not just to win the competition, but also to continue to learn patience and run their own race. There was also a need to decide on who the top seven would be for the NCAA Division III Regional Championship in two weeks. With 10 of the 26 member team all racing well enough to claim one of the seven spots, the task of selecting those seven would not be easy.</p>
              <p align="left">The course was real cross country with three hills, one in the first 800 meters, another longer hill in the middle of the course and a very steep 50 meter hill ending with 800 meters to go in the 5K race. The weather cooperated, bringing overcast skies and 58 degrees.</p>
              <p align="left">As usual, at the start a number of runners took off, stringing out the 150 runners  right away. Managing the first hill with caution, the pace was pretty slow with the leaders coming through in 3:05. The MIT runners were working their way into the race so the closest runner to the leaders was a solid five seconds back.</p>
              <p align="left">By the mile mark, MIT runners Brooke Johnson '13, Elaine McVay '15 and Sarah Quinn '16 had taken the lead at 6:04, with only three Wellesley, two Smith and one Mt. Holyoke runner strung out behind them. From there the trail group was at least another five seconds back. There were 11 opposing runners in front of Tech's second group of runners (Alexandra Taylor '14, Kaitlin Allen '14, Nicole Zeinstra '16, Dacie Manion '15, Martina de Geus '14, Kali Benavides '15 and Allison Hallock '16) who came through the mile mark in 6:14. </p>
              <p align="left">MIT continued to negative split the race despite the long hill in the second mile. The front runners ran a 6:02 with Tech's B group running 6:13, putting them 21 seconds behind their front running teammates. However, not only had MIT supporting runners moved past those in their group but had begun to close on all the runners falling off the pace set by the leaders.</p>
              <p align="left">The final mile continued with more of the same. MIT opened up a commanding gap and looked comfortable doing so. At the three mile mark, McVay led Johnson by a second, but Johnson's kick was just enough to take the victory for her first NEWMAC Cross Country title with a time of 18:30 for the NEWMAC Runner of the Year. McVay was second, also in 18:30. Quinn, running with a mildly sprained ankle, was tighter than usual, but still managed third, with Rookie of the Year honors for her 18:37 effort. Sixteen seconds behind Quinn, Taylor emerged as the front runner for the B group, claiming fourth in the race in 18:53. Allen closed out the MIT scoring and sweep in 18:55. MIT was not finished yet as they claimed the top eight places. In sixth was Zeinstra in 18:57, seventh was Manion in 18:59 and de Geus finished in eighth with a time of 19:04. Also earning All NEWMAC honors was Benavides in 19:09, good for 12th place.</p>
              <p align="left">The 29 second delta for MIT's top seven was just what they were looking for. Twelve Engineers were within a minute of Johnson, saying a lot of their depth and youth, with only Johnson being a senior.</p>
              <p align="left">The men knew they would have a much more difficult race as Springfield College had been close to them in three previous races this year. The pressure was on the Engineers to continue the victory streak. The men knew they had not run a race this year that represents their ability, and that with this one race to go prior to Regionals, it was time to get their act together.</p>
              <p align="left">Springfield College took the race out, but not too fast. Most runners were respecting the course as the men's version was much more hilly than the women's, with two additional large hills, one at 1.5 and one at 2.5 miles. For once, Tech stayed in their groups and exercised patience. Springfield had the two leaders and five of the first nine runners at the mile mark. Three MIT runners were mixed in the back of that group, Roy Wedge '14, Logan Trimble '13 and Benji Xie '15 came through the mile in 5:17. MIT's depth was starting to show as the second group, led by Ben Mattocks 'G, was right behind in 5:20-5:22. In that second group were Allen Leung '15, Ian Tolan '16, Edgar Gridello '16 and Matthew Jordan '15.</p>
              <p align="left">At the second mile, MIT had taken over the team competition, having four of the top 10 runners as well as runners 13-16, with Justin Bullock '14 joining the group with an excellent 5:24 mile. The third mile continued to see MIT move up, although Springfield continued to have the top two runners. Wedge and Xie were now in third and fourth respectively, hitting the split in 15:40. Supporting them was Mattocks in eighth, Bullock in ninth, Leung in 11th, Trimble in 12th and Gridello in 13th.</p>
              <p align="left">Wedge hit the four mile split in 20:58, moving into second place. Wedge's fourth mile push was too much for Xie, who slipped badly to 21:10. Mattocks had moved up to Xie with Bullock and Leung right behind.</p>
              <p align="left">At the finish, Wedge faded to fourth behind Salvucci and O'Connell from Springfield and Ezzo of Coast Guard. Wedge finished in a very respectable 26:02 and was followed in fifth place by Mattocks in 26:08. Bullock ran the best cross country race of his life, finishing seventh in 26:21. Leung was eighth in 26:22, Tolan came back out of nowhere to claim Rookie of the Year honors for his ninth place finish in 26:28, less than a second ahead of fellow freshman, Gridello in 10th and Jordan was 11th in 26:29, giving MIT seven runners within 27 seconds and ahead of Springfield's third runner. Xie and Trimble faded a bit but were still All Conference at 13th and 14th in 26:30 and 26:31 respectively. Tech's 33 points gave them their victory to continue the streak.</p>
              <p align="left">In two weeks, MIT will vie for the New England Regional Championship and a trip to the NCAA Division III Championship. Westfield State College will host the meet at Stanley Park with the men racing at 11:00 and the women going at 12:00.</p>
