              <p><span class="headline">WOMEN CLAIM A DISAPPOINTING SIXTH PLACE AT NCAA CHAMPIONSHIP</span></p>
              <p align="left">November 17, 2012</p>
              <p align="left">The weekend could not have started better. The women from MIT were ranked number one in the country for the fifth week in succession. At the Coaches' and Athletes' Banquet the night before the <a href="ncaa.res.php">NCAA Division III Cross Country Championship</a> hosted by Rose Hulman Institute of Technology in Terre Haute, Indiana, Dacie Manion '15 was awarded the NCAA Elite 89 Award--an award presented at each NCAA Championship to the male and female student-athlete competing at the championship with the highest GPA. Manion, a sophomore in Mechanical Engineering has a perfect 5.0 at MIT.</p>
              <p align="left">Going into the race on Saturday the team appeared more ready and relaxed than any team in memory according to Coach Halston Taylor, the Director and Head Coach of the Men's and Women's Cross Country and Track &amp; Field teams. &quot;I would have bet anything that this team was ready to take care of business.&quot; Perhaps they were ready, but maybe the little nagging injuries that showed up at the end of the season were not so little.</p>
              <p align="left">Deciding not to run Manion due to a shin injury and holding out alternate Kali Benavides '15, the competing team was comprised of Kaitlin Allen '14, Martina de Geus '14, Brooke Johnson '13, Elaine McVay '15, Sarah Quinn '16, Alexandra Taylor '14 and Nicole Zeinstra '16. Johnson, Quinn and Zeinstra, all suffering through injuries that appeared to be minor, all claimed to be ready to go.</p>
              <p align="left">MIT, placed in box 37, had a great start, not out front, but in a good place to pace evenly and move up through the field. At the mile mark, Group A (Johnson, McVay, Quinn) was together at 5:44, perhaps a little fast given the hills and soft ground, but the position was solid, well back from the leaders, but still within easy striking distance of the main pack. Group B was spread out a bit. Zeinstra and Taylor were at 5:44, too fast for the soft conditions, while de Geus was back at a reasonable 5:47. The normally rock solid Allen, suffering from some situation that had her unable to see, was back at 5:54.</p>
              <p align="left">Group A did a great job of moving up as planned in the second mile, posting a 11:23, although maybe a little sooner than necessary, putting them in the top 30. Group B was spreading out even more, but Zeinstra and Taylor were hanging tough at 11:35, in great position within the top 60. de Geus faded back to 11:43 instead of moving up to assist and Allen continued to have problems at 11:58. At this point MIT was in a very close second place.</p>
              <p align="left">The third mile was the beginning of the end for Tech. Johnson, ignoring her foot pain, and McVay continued to work well together but stopped moving up, and in fact, started losing a few places with their 5:58 split. Quinn's hamstring started bothering her, forcing her to slow to a 6:16 mile. Zeinstra's ailing calf, became too painful, forcing her to drop out. Taylor hit the wall, lost focus and slowed to a 6:53 mile. de Geus was the most steady at 6:14, but also lost ground. Allen remained in the race but was only able to manage a 6:25. </p>
              <p align="left">In the final .73, Johnson fought hard to earn All America honors (top 35), but still missed it by one, placing 36th in 21:59. McVay finished in 38th at 22:04. Quinn, fading almost the exact amount as the week before when she finished sixth for the team and 31st overall, finished in 74th place at 22:34. de Geus was MIT's fourth finisher at 22:43 in 94th place. Allen closed out the scoring for MIT in 184th place at 23:20. Taylor was 234th in 23:51.</p>
              <p align="left">If MIT had maintained the same top five delta they had boasted all season at sub :30 seconds, even with the less than perfect races by Johnson, McVay and Quinn, they still would have won the meet.</p>
              <p align="left">It was a great season, the best in MIT cross country history, but the coach and team were unable to finish. The fifth consecutive NCAA top 10 finish and fourth in the top six, brings a unique, if perhaps unfair, level of expectation each year, one where anything short of the podium (top four teams) or of winning the team title in this particular case, is a little disappointing.</p>
              <p align="left">Fortunately, with only Johnson graduating from the very deep top 10 performers and what appears to be a very strong recruiting class, the hopes of a national title in the near future remain very high.</p>
