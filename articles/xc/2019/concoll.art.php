              <h3>Women Win Conn College Invite, Men Finish Second</h3>
          </div>
            <p>October 19, 2019</p>
          <p>MIT's Women's Cross Country team felt they had something to prove after a poorly run race at the Purple Valley Classic, where they finished third behind Tufts and Williams, and a race cut short  by the lead vehicle taking a wrong turn at the Pre-National&nbsp;race--even though they won that race.&nbsp; With a perfect 60 degree, sunny day they got their chance against a 29 team field at the <a href="https://www.tfrrs.org/results/xc/15785/Connecticut_College_XC_Invitational">Connecticut College Invitational</a>, that included&nbsp;nationally ranked #4 Tufts, #5 Williams, #12 Carleton, #14 RPI, #18 Middlebury, #20 Rochester, #29 Wesleyan, #31 Coast Guard and #34 Vassar.</p>
          <p>MIT, ranked seventh, got out well, positioning themselves towards the front of the pack.&nbsp; &nbsp;At the mile mark Tufts had&nbsp;runners at second and third place, Middlebury also had two among the top nine and Williams had three among the top 17.&nbsp;&nbsp;Although only Katie Collins '21 represented MIT among the top 10, Tech had five among the top 20.</p>
          <p>At the two mile mark, Tufts was still in second and third, Williams, Middlebury and Carleton all had two in the top 12. Izzi Gengaro '22 joined up with teammate Collins in the top 10 and Katie Bacher '20 joined Megan McCandless '20 and Einat&nbsp;Gavish '22 among the top 17.&nbsp; Freshman star, Kirsi Rajagopal was racing with a&nbsp;cold and her fast start was starting to bite her as she fell out of the top 20, but was racing tough.</p>
          <p>At mile three it was clear that Collins was getting in her own head and trying to hold on.&nbsp; It was equally clear that Gengaro was on fire and hunting people down at a furious pace.&nbsp; Tufts remained in second and third but were falling off a bit.&nbsp; The two Williams runners had moved past Collins.&nbsp;&nbsp;However, MIT's depth was rock solid and still among the top 20.</p>
          <p>At the finish, Gengaro had thrown down a 4:09 final .73 on top of her 5:36 third mile to move into second place in 21:07 for 6K.&nbsp; With Collins falling off and Rajagopal not able to respond at her normal level due to her sickness, Bacher came through in 12th place in 21:49.&nbsp; Collins held onto 15th in 21:58, McCandless was 18th in 22:04 and Gavish finished in 28th in 22:16&nbsp;to seal the victory with 75 points.&nbsp;The Engineers had put five runners across the finish line before any other team's fourth runner.</p>
          <p>The men, seeded second in a 28 team field, were ranked 12th&nbsp;nationally going into the meet.&nbsp; Among the 28 teams were nationally ranked Williams at #1, RPI at 14, Amherst at 18, Middlebury at 20, Ithaca at 27 and St. Lawrence at 35.&nbsp; </p>
          <p>Unlike the women, MIT's men got out a little too fast, putting themselves  too far up in the pack early in the 8K race.&nbsp; They were clearly dominating the race at the mile mark.&nbsp; Andrew Mah '22 was in the top five.&nbsp; Sanjay Raman '23 continued his outstanding racing as he was in 12th place.&nbsp; Vedang Lad '23, Matthew Kearney '22 and Ryan Wilson '22 were together between 15th and 20th.</p>
          <p>At the two mile mark Mah was still there in sixth, Raman had moved up to 10th.&nbsp; Kearney was still among the top 15, but Wilson and Lad had fallen back to 23rd and 27th respectively, while Williams' runners were starting to roll and move up to MIT.</p>
          <p>In the third and fourth mile it was clear that Williams had run&nbsp;a superior race, catching MIT and moving past with no challenge&nbsp;as the Engineers were tired and holding on.&nbsp; At four miles Williams was in first, fifth, 15, 16 and 19 while MIT was in eighth, 10th, 16th, 24th and 26th.</p>
          <p>At the finish, the momentum continued in favor of Williams College as they finished with an impressive 47 points with first, fifth, 11th, 12th and 18th places.&nbsp; MIT finished 10th (Mah), 15th (Raman), 21st (Kearney), 22nd (Lad) and 28th (Sam Acquaviva '23)&nbsp;for a convincing second place finish with 96 points, 53 points ahead of third place RPI.</p>
          <p>Both teams performed well for MIT, running hard and tough.&nbsp;There is work to be done, but the Engineers are headed in the right direction with five weeks to go before the NCAA Division III Championship in Louisville, KY.</p>
          <p>In two weeks MIT returns to competition to defend their NEWMAC Conference Championship.&nbsp; The men will be going for their 22nd consecutive title.&nbsp; The women have a nice streak of their own, having won the past 12 years.</p></td>
