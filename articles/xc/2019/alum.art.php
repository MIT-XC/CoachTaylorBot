              <h3>Varsity Teams Prevail Over Alumni in Annual Meet</h3>
          </div>
            <p>August 31, 2019</p>
            <p>Racing in 75 degree, sunny weather, with low humidity, it was about as comfortable as one can expect on the last day of August.&nbsp; Both the Alumni/ae and current MIT runners got caught up in the moment and ran a little too aggressively on the extremely hilly Larz Anderson Park course in Brookline, Massachusetts, leading to some pretty serious decay by some over the final half of the 2.76 mile course.&nbsp; Both Varsity teams prevailed in the annual Alumni/ae competition.&nbsp; The men won with a perfect score of 15 to 50, while the women won 7-16 in an adapted scoring of only three per team due to a shortage of alumnae present to race.</p>
            <p>The men raced first, with the varsity wasting no time in taking control of the pace.&nbsp;&nbsp;At&nbsp;the mile mark, Sanjay Raman '23 was leading Steven Goldy '20 and fellow freshmen Cameron Kleiman '23 and Lowell Hensgen '23 in a crisp 5:04 split.&nbsp; The alumni, represented by Jared Forman '13, Brian Gilligan '17, Harrison Hunter '15, Alex Knapp '19, Aidan Gilson '19, Nicholas Waltman '18, Josh Feldman '97, Terry McNatt '87, Kevin DiGenova '07, David Westenberg '79 and Bob Collins '82, were not too close to the leaders, but were more than challenging themselves on the difficult course.&nbsp; Forman, Gilligan and Hunter were the closest to the varsity runners.</p>
            <p>At the two mile mark, Kleiman and&nbsp;Hensgen were in the lead with Raman at 10:32, but Matthew Kearney '22 had&nbsp;moved slightly ahead of Goldy with Sam Acquaviva '23 close behind.&nbsp; Going into the finish, Kearney had pulled up to Kleiman, but then Kleiman opened up a pretty vicious kick to win despite having difficulty recognizing the finish line chute.&nbsp; Both were timed in 14:33.&nbsp; The next three were freshmen Hensgen 14:36, Raman 14:40 and Acquaviva 14:44&nbsp;to close out the perfect score for the varsity.&nbsp; A 12 second delta&nbsp;with four of the first five being freshmen is pretty impressive, even on a short course.</p>
            <p>Forman finished in 16th in 15:46, out-kicking Gilligan, who finished 17th in 15:50.&nbsp; Hunter was 18th in 16:42, Knapp 20th in 17:06, Gilson 21st in 17:34, Waltman 22nd in 18:10, Feldman 23rd in 18:26, McNatt 24th in 18:50, DiGenova 25th in 18:50, Westenberg 26th in 19:03 and Collins 27th in 20:25.</p>
            <p>The women went out even more aggressively than the men.&nbsp; Katie Collins '21 led the way from start to finish, although she was pushed along the way.&nbsp; At the mile mark, Collins led the way at 6:01.&nbsp; Kirsi Rajagopal '23 and Anna Haddad '23 were extremely aggressive for a pair of freshmen, coming through at 6:03 and 6:04 respectively.&nbsp; Megan McCandless '20 was close behind in 6:05.&nbsp; Representing the alumnae were Mary Eccles '18, Maryann Gong '17 and Alexandra Taylor '14.&nbsp; Gong was with Rajagopal at the mile mark and Eccles was just ahead of McCandless.&nbsp;Only scoring three runners, if the alums could go 1-2, they would win the meet.</p>
            <p>The hills took their toll on the women even more so than the men.&nbsp; At the two mile mark Collins was still in the lead, but had slowed to 12:34 and was only one second ahead of McCandless.&nbsp; Haddad had moved into third at 12:41,&nbsp;and Katie Williams '21 (12:56) and Einat Gavish '22 (12:58) had moved past Rajagopal (12:59).&nbsp; Overall, Eccles was just ahead of Gong in fourth and fifth place at 12:42 and 12:43 respectively.&nbsp; It was still a race.</p>
            <p>In the final .76, Collins put the race out of reach, winning by eight seconds in 17:17.&nbsp; McCandless finished second and Eccles passed Haddad for third&nbsp;in 17:32.&nbsp; Gong was just one second behind Haddad in 17:38.&nbsp; Williams and Gavish finished fourth and fifth for the varsity in 17:48 and 17:56.&nbsp; The spread was 39 seconds, which was a little much for a short course.</p>
            <p>In attendance, cheering on their alumni teammates were Eric Khatchadourian '06, Josh Rosenkranz '19, Don Shobrys '75&nbsp;and Roy Wedge '14.</p>
            <p>The Engineers will now get down to some serious training in preparation for the season.&nbsp;&nbsp;Pre-season rankings mean very little, but the women, who have finished among the top six in the country at the NCAA Division III Championship for the past 10 years, are ranked fifth, while the men&nbsp;are ranked eighth.&nbsp; Their next meet will by September 28th at Williams College for the Purple Valley Invitational.</p>
