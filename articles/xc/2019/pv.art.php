              <h3>Mah and Rajagopal Lead MIT at Purple Valley Classic</h3>
          </div>
            <p>September 28, 2019</p>
            <p>Entering their first intercollegiate race of the season, MIT traveled to Williamstown, Massachusetts to race at the <a href="purplevalley.res.php">Williams College Purple Valley Classic</a>.&nbsp; The challenging, hilly course was made even more difficult by&nbsp;an 80 degree day without a cloud in the sky.&nbsp; Of course, everyone had to deal with this, so the significance was just a matter of slower times.</p>
            <p>In the men's 8K race, a total of 19 teams competed, and of those, seven were among the top 35 teams in the country for Division III, led by number two Williams College.&nbsp; MIT ranked 10th, RPI  (16th), Amherst College (19th), Middlebury College&nbsp;(22nd), Conn. College (34th) and St. Lawrence (35th)&nbsp;represented the other nationally ranked teams.&nbsp; This is the first weekend where results might affect the decision on which teams are eventually selected for the NCAA Division III championship on an at-large basis, so the results actually matter beyond the race itself.</p>
            <p>MIT, depending primarily on freshmen, planned on being conservative and working together as a group.&nbsp;&nbsp;Well that lasted for less than one mile as the group splintered.&nbsp; The lead group for MIT, Andrew Mah '22, Sanjay Raman '23 and Cameron Kleiman '23 were among the top five at the mile mark, which was indeed a conservative 5:13.&nbsp; Back a little at 5:15 were Lowell Hensgen '23, Vedang Lad '23 and Zach Johnson '22.&nbsp; Steven Goldy '20 was right behind with a few more MIT runners.&nbsp; While not in a group per se, they were feeding off the presence of each other.</p>
            <p>The men's course has a very long and challenging hill that must be done twice, once at the end of mile two and the second at the end of mile four.&nbsp; After the first time up the hill the meet was pretty much decided.&nbsp; MIT was in pretty good position with Raman in third, Mah in fifth, Kleiman in sixth and Hensgen and Johnson in 15th&nbsp;and 16th respectively, but Williams was literally running away with it at one, two, eight, nine and eleven, as well as six more in the top thirty.</p>
            <p>By the end, Aidan Ryan '21 of Williams won easily in 26:04, and Williams took the team title with a low score of 31, also claiming third, seventh, ninth and eleventh.&nbsp; MIT claimed runner-up honors with a solid 56 points.&nbsp; Mah finished fourth, Raman fifth, Kleiman sixth, Lad 19th and Hensgen 22nd.&nbsp; MIT's total delta was only 39 seconds, but the 27 seconds between third and fourth runners is what hurt them.</p>
            <p>Of particular note, Mah, if taken out of the scoring, the five freshmen; Raman, Kleiman, Lad, Hensgen and Sam Acquaviva '23, who was 24th overall would have still earned second place as a team as third place RPI was back with 91 points.</p>
            <p>The women raced second and again there was a loaded field among the 14&nbsp;teams.&nbsp; Williams was ranked fourth nationally, MIT fifth, Tufts eighth, RPI 10th and Middlebury 19th.&nbsp; The women's 6K race only had one repetition of the long hill, but that was enough on this hot day, particularly with the location being at the end of the third mile.</p>
            <p>After a start that was perhaps a little too strong, MIT settled in with Katie Collins '21 in the front group of three runners and the rest of MIT's chase group pretty far back.&nbsp; Tufts was dominating the front of the pack&nbsp;and at the mile mark the score was Tufts 44, MIT 77 and Williams 107.&nbsp; Middlebury and RPI also had a significant number of runners up in the front, but were not quite threatening the top three teams.</p>
            <p>After the two mile mark as they headed towards the large hill, Tufts was still in the lead, but had lost some ground with 53 points.&nbsp; MIT had gained some of those points and seemed to be in position to take the victory after the hill as they were at 65 points.&nbsp; Williams had moved up significantly to 79 points and perhaps their cautious approach was paying off.</p>
            <p>At the end, Collins fell off the lead, suffering a tough mental third mile, falling back to 12th overall.&nbsp; Kirsi Rajagopal '23, continues to impress as the freshman consistently moved up in the race, finishing fourth overall in 23:26.&nbsp; Tufts held on&nbsp;to win with 60 points, led by a 1-2 finish&nbsp;with rookie Sabrina Gornisiewicz taking first in 22:44.&nbsp; Williams moved past MIT to finish in second with 68 points.&nbsp; MIT faded only a few spots, but enough to finish with 72 points, 12 out of first.&nbsp; </p>
            <p>MIT's delta of 43 seconds was far too much for a top nationally ranked team and 1:07 through seven runners will hurt them badly if they cannot fix it.&nbsp; Going out a little too quickly and lacking the mental toughness from a total team perspective can be corrected, and will have to be if the team hopes to continue their 10 year string of finishing among the top six teams at nationals.</p>
            <p>Next weekend the Engineers fly to Louisville to compete in the Pre-Natonal, Greater Louisville Classic at Tom Sawyer Park.&nbsp; Both the men and women are sure to be tested by many nationally ranked teams.</p>
