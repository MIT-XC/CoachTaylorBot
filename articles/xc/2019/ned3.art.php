              <h3>MIT Fails To Defend Regional Title</h3>
              <h4>Gengaro Wins Women's Individual Championship</h4>
          </div>
            <p>November 16, 2019</p>
            <p>In the first cold-weather meet of the season, the <a href="https://www.tfrrs.org/results/xc/16567/NCAA_Division_III_New_England_Region_Cross_Country_Championships">NCAA New England Region championship</a> at Bowdoin College in Brunswick, Maine, where the temperature during the men's warm-up was&nbsp;in the upper 20's, and warmed up to 30 at race time, MIT's&nbsp;men were seeded second among the 54 team field, ranked 12th in the country.&nbsp; Williams was the favored team to win, ranked&nbsp;second in the NCAA Division III rankings.&nbsp;&nbsp;Other nationally ranked teams&nbsp;in the field were Amherst (17th), Middlebury (21st), Colby (24th), and Bates (28th).</p>
            <p>In the previous two meetings Williams had run a conservative start relative to MIT's overly aggressive starts.&nbsp; They worked together to run down MIT runners and win both outings.&nbsp; Going into this race, MIT planned on running more intelligently and not putting themselves out there as a target.&nbsp; MIT had won this meet two of the past three years and wanted to get back there.&nbsp; Williams could not have cared less as they moved quickly to&nbsp;the front of the race, totally disregarding everyone else.&nbsp; Even though the pace was not very fast (4:49 for the leader at the mile mark on the flattest course possible), MIT was thrown off by Williams moving to the front and settled in a bit too far back.&nbsp; At the mile mark MIT had Sanjay Raman '23 and Andrew Mah '22 sandwiched between Williams fifth and sixth runners.&nbsp; The next group of MIT runners, Vedang Lad '23, Josh Derrick '20, Matthew Kearney '22 and Ryan Wilson '22, were back between 50th and 60th place, three to four seconds back.</p>
            <p>In the woods it was difficult to move up and MIT had a major challenge on their hands.&nbsp; Middlebury and Colby were also ahead of MIT and Amherst had four ahead of MIT's third runner.&nbsp; Two miles later, with the exception of Raman and Mah, MIT fell further behind the surging Ephs, now 15 seconds back.&nbsp;&nbsp;MIT had moved up significantly place wise to the other teams, passing Middlebury and Amherst, but still were three points behind Colby.&nbsp;At three miles.&nbsp; </p>
            <p>In the fourth mile MIT had continued to move up.&nbsp; Mah made a big move to slide into a solid third place and was closing in on second.&nbsp; Raman was in 13th, Lad in 23rd, Kearney in 24th and Derrick in 27th.&nbsp; Their total of 90 points was well behind Williams, but had passed Colby by nine points.&nbsp; However, in the final mile, Mah&nbsp;lost motivation and gave up enough to fall to 15th place in 25:18.&nbsp; Raman moved up to 10th in 25:07, but Colby runners moved up to third and fifth.&nbsp; Lad gained one place, moving to 22nd in 25:31, but Derrick lost two places, finishing in 29th in 25:36 and Kearney hit the wall, dropping to 31st in 25:40.&nbsp; Colby held on better, netting one place better among their three through five runners, compared to one mile earlier, to take second place by a score of 93 to 107.&nbsp; Williams literally and figuratively ran away with the title, totaling 24 points.</p>
            <p>By the time the women took to the course, the ground was still frozen, but the ambient temperature was about 34 degrees and the sun was out.&nbsp; The men were not able to chew up the course too badly due to it being frozen in most places, so overall the conditions were excellent.&nbsp; The NCAA Division III #3 ranked team, MIT had lost to both Williams and Tufts back in September, but had beaten both teams three weeks ago.&nbsp;In addition, MIT had won this race the previous three years and the seniors were looking for the four year sweep.&nbsp; Williams, apparently not concerned about MIT, uncharacteristically moved to the front and took the race to the rest of New England.&nbsp;</p>
            <p>At the mile mark, MIT had covered the strategy up front, and looked to be doing the same on the back-end.&nbsp; Williams occupied places 17, 18, 20, 22 and 23.&nbsp; MIT was now in 4 (Izzi Gengaro '22), 7 (Katie Collins '21), 21 (Katie Bacher '20), 25 (Einat Gavish '22) and 37 (Megan McCandless '20), giving the Engineers a 94 to 100 advantage.&nbsp; The pace was quick with Gengaro splitting 5:37 and McCandless 5:47.&nbsp; From this point on Williams was the more aggressive of the two teams.&nbsp; They had moved up and continued to pull ahead of Bacher, Gavish and McCandless.</p>
            <p>By the three mile mark, Gengaro had taken the lead, splitting 17:06.&nbsp; Collins was up to fifth in 17:11, but Bacher was back in 18th at 17:30.&nbsp; Williams had moved up to 7, 8, 16, 17 and 24.&nbsp; MIT had improved to 81 points, but Williams was now ahead with 73.&nbsp; In the final .73 the only change was Williams pulling further ahead.&nbsp; Gengaro held on for the victory in 21:19 and Collins had the best cross country race of her career, finishing fifth in 21:28.&nbsp; Williams then closed with eighth, ninth, tenth and 16th before Bacher finished in 20th at 22:05.&nbsp; The fifth Williams runner was immediately behind her to close out the scoring with 64 points.&nbsp; Closing out MIT's scoring was Gavish in 28th (22:20) and Katie Williams in 30th (22:23).&nbsp; The final score was Williams 64 and MIT 84.&nbsp; Third place went to Tufts with 156 points.</p>
          <p>As the teams prepare to travel to Louisville and Tom Sawyer Park, the Engineers have to do a &quot;gut-check&quot; to see if they want a shot at the podium badly enough to bring their A game and stay on focus the entire race.</p></td>
