              <h3>Women Seventh, Men Ninth at NCAA DIII Championship</h3>
              <h5>Gengaro, Collins and Raman Claim All American Honors</h5>
          </div>
            <p>November 23, 2019</p>
            <p>MIT entered the <a href="ncaa.res.php">NCAA Division III Championship</a> seeded fifth in the women's rankings and 15th in the men's rankings.&nbsp; Neither team believed they had run their best race and wanted to prove they could beat their seeds&nbsp;among the top 32 teams in the country.</p>
            <p>The weather in Louisville inspired true cross country.&nbsp; Rain and 40 degree temperatures made for exciting race conditions if not necessarily the best spectator environment.&nbsp; It had rained off an on much of the week, making the course at Tom Sawyer Park very muddy by the start of the men's race, and an absolute mud pit on parts of the course after the men's race--setting the women up for a&nbsp;memorable if not enjoyable experience.</p>
            <p>MIT changed their spikes to 1/2&quot; length to aid in navigating the muddy terrain, which seemed to help considerably.&nbsp; The men were lined up in box 43 of 46 boxes.&nbsp; Experience on this particular course dictated that they needed to get out hard or they would be on the far outside of the first left sweeping turn, losign significant time.&nbsp; They did this well, possibly too&nbsp;well.&nbsp; However, they had demonstrated that their best races came when they went out fast but comfortable and raced where they felt they belonged.</p>
            <p>At the mile mark, Sanjay Raman '23 was in 15th position in 4:48, which was certainly not too fast on this flat course, despite the mud.&nbsp; Andrew Mah '22 was close behind in 20th.&nbsp;Matthew Kearney '22 and Josh Derrick '20 were right there in the mid-20's.&nbsp; Steven Goldy '20 was in solid position in 4:54 as MIT's fifth man.&nbsp; At the 3K mark the chip reader mat had MIT in first, which they knew would not hold, but they were certainly going to fight to hold it.</p>
            <p>At two miles, Raman was still in 15th in 9:44, but Mah had faded back to 26th as he illogically thought he could settle-in and be okay, not realizing that disengagement was taking him out of the race.&nbsp; Kearney and Derrick were in 38th and 39th in 9:50 and Goldy was in 90th in 9:54.</p>
            <p>The third mile had a slight uphill&nbsp;and a predominance of mud.&nbsp; This temporarily slowed the field, but not significantly.&nbsp; At the half-way point MIT was in third place and looking strong.&nbsp; At the four mile mark, Raman was still in 17th.&nbsp; Mah was in 44th, and with the top 40 earning All American, it was hoped he could make a charge to the finish.&nbsp; Derrick was in 64th, Kearney in 73rd and Goldy in 110th.&nbsp; The team was running an excellent race.</p>
            <p>In the final mile the Engineers lost a lot of places&nbsp;although they kept fighting.&nbsp; Raman slipped to 30th in 24:52 to earn All-American&nbsp;as a freshman, the first male to ever do so at MIT&nbsp;in Cross Country.&nbsp; Mah fell back to 60th in 25:11.&nbsp; Kearney dropped to 83rd and Derrick to 87th.&nbsp; Goldy was the only one to hold onto his position.&nbsp; Even though&nbsp;the Engineers lost 62 points in the final mile, they finished ninth with 299 points, which deducts the individual places of those not on teams.&nbsp; </p>
            <p>The women from MIT were in box 19, which was a much more favorable position than the men.&nbsp; Still not wanting to be in the back of the pack on the first turn, the women went out hard&nbsp;to gain an advantageous position.</p>
            <p>Izzi Gengaro '22 was in fifth position at the mile mark, coming through in 5:28, well behind the leaders, who were out in 5:20.&nbsp; Katie Collins '21 and Katie Bacher '20 were in 33rd and 36th places respectively, both running 5:36.&nbsp; Einat Gavish '22 was in 53rd place in 5:40 and Megan McCandless '20 and Katie Williams were between 105th and 110th in 5:47.</p>
            <p>Gengaro and Collins moved up in the second mile, but the rest of the Engineers, while fighting hard were starting to lose ground.&nbsp; Gengaro hit two miles in 11:12, still in a five person group between fourth and eighth.&nbsp; Collins was now up to 16th in 11:17.&nbsp; Bacher was hanging tough in 38th position in 11:32.&nbsp; Gavish ran 11:41 in 66th place.&nbsp; McCandless was in 118th in 11:53.&nbsp; Williams lost her shoe during the second mile and was struggling to race with one shoe.&nbsp; Already with a :41 delta, the challenging third mile could doom Tech from their goal of being among the top four teams.</p>
            <p>During the third mile the delta 1-5 expanded to 1:16, taking the women out of the top four teams.&nbsp; In the final .73, Gengaro lost a few spots, finishing 10th in 21:30, earning All America honors, the highest individual finish ever for the MIT women's team.&nbsp; Collins was only :12 seconds back in 15th place, also earning NCAA All America honors.&nbsp; This was also the first time MIT had two women among the top 15 at the NCAA Championship.&nbsp; Bacher finished in 70th in 22:36, Gavish was 111th in 22:57, and McCandless 159th in 23:15.&nbsp; </p>
            <p>The women finished in seventh place&nbsp;with 252 points once the individuals not with teams were taken out of the scoring.&nbsp; Losing 119 places over the last 1.73 miles was primarily a coaches error.&nbsp; Coach Halston Taylor took the blame as he had encouraged the team to get out hard based on the teams inability to move up on this course in 2016 when the team was caught behind the majority of the field.&nbsp; His strategy worked for Gengaro and Collins.&nbsp; However, he admitted he should have instructed runners three through five to be slightly more conservative at the start and  move up through the field.</p>
          <p>The men have two seniors among their top 12 runners, Josh Derrick and Steven Goldy, both with eligibility remaining.&nbsp; Goldy will return to MIT  for graduate school, while Derrick is still deciding on his best path academically.&nbsp; Returning all members would be a big plus for the Engineers. The women will lose two among their top five, but otherwise have a relatively young team.&nbsp;MIT will look forward to the 2020 season.</p></td>
