              <h3>Women 2nd, Men 10th at Pre-Nats</h3>
              <p>&nbsp;</p>
              <h4>MIT Has Work To Do!</h4>
          </div>
            <p>October 13, 2018</p>
            <p>MIT traveled to the <a href="prenat.res.php">Division III Pre-National meet</a> and was hoping to discover how they fared against national level competition from outside their region while also getting an opportunity to see the national championship course at Lake Breeze golf course in Winneconne, WI, hosted by the University of Wisconsin-Oshkosh.</p>
            <p>The course was so inundated by rain over the past two weeks that it had to be altered a bit and no teams were allowed on the course the day before.&nbsp; Even with this the course was wet, soft and muddy before the races even started.</p>
            <p>Going into the week it was discovered that MIT lost their number four runner, freshman Zach Johnson, for the season due to injury.&nbsp; With a significant gap  after their fifth runner, the Engineers would now have to see if someone would step up to make up for the loss of Johnson, who averaged 18 seconds from Tech's top finisher.</p>
            <p>The field was impressive with 50 teams in both the men's and women's fields.&nbsp; For the women, other than the fourth ranked Engineers, competing were #2 Washington of St Louis, #5 UW Eau Claire, #6 Carleton, #9 Hope, #12 Wheaton (IL), #13 Pomona Pitzer, #15 Calvin, #21 Wartburg, #21 Otterbein, #23 North Central, #24 Bates, #26 Chicago, #28 Emory and #31 St. Thomas.&nbsp; Of course early season rankings are guess work at best so the muddy course would certainly see which teams knew how to race cross country under such conditions and give the rankings a good shake-up.</p>
            <p>MIT was concerned about getting out too fast on the 6K course and were also trying to be aware of Wash&nbsp;U and Eau Claire runners.&nbsp; At the mile mark, Wash U had runners in third, sixth and 20th, but their next runners were in 57th and 87th&nbsp;for a total of 153 points.&nbsp; MIT went out conservatively at 5:56-6:00 pace for their top six runners&nbsp;with Izzi Gengaro '22 in 18th, Jenna Melanson '20 19th, Leandra Zimmermann '19 23rd, Katie Collins '21 27th, Marissa McPhillips '20 29th and Einat Gavish '22 in 30th position for a total of 116 points.&nbsp; Even though MIT put a pretty good effort into the second mile, Wash U showed their dominance, moving up to within twenty points 97 to 117.&nbsp; In the third mile, Gengaro moved up to 10th, Zimmerman to 15th, Melanson to 16th, Gavish to 27th and Collins to 29th.&nbsp; However, Wash U pushed hard in the back and now had an 87 to 97 point edge with runners in 2, 5, 11, 34 and 35 positions.</p>
            <p>At the finish, MIT lost a few places from their fourth and fifth finishers, totaling 107 points.&nbsp; Wash U's fourth and fifth runners continued to move up, giving them 70 points&nbsp;and a clear victory.&nbsp; Third place overall went to Brandeis with 213 points.&nbsp; </p>
            <p>MIT finished where they were seeded, and freshmen Gengaro and Gavish ran superb races.&nbsp;&nbsp;However, if MIT wishes to move up to the top two teams in the country they need to either move up their top runners or close the gap between three and four/five, which stood at :41 and :48 in this race.</p>
            <p>By the time the men stepped up to the starting line the course was a mud bath and was getting significantly worse with each runner.&nbsp; Strategy was difficult to balance.&nbsp; Getting out hard among the leaders meant you were taking a huge risk in blowing up since the energy return from the ground was so discounted, but running conservatively early meant trying to move up in the field on a muddy course and having to pass so many runners.</p>
            <p>MIT was seeded seventh in the field of nationally ranked teams, which were #1 North Central, #3 Pomona Pitzer, #5 Haverford, #6 Wash U, #9 Calvin, #11 Wartburg, #14 MIT, #15 Chicago, #18 UW Stout, #20 UW Eau Claire, #20 Carleton, #24 Otterbein, #31 Emory, #32 UW Stevens Point and #33 Washington and Lee.</p>
            <p>MIT runners chose the very conservative route and at the mile mark were very far back with Billy Woltz '20 in 88th place, Andrew Mah '22 in 89th place, Josh Rosenkranz in 92nd place, Aidan Gilson in 110th place, Josh Derrick in 122nd, Nicky Medearis '22 in 150th and Ryan Wilson '22 in 162nd.&nbsp; All were between 5:10 and 5:17.&nbsp; They pushed from that point on and by mile three were in 47th, 48th and 51st with Rosenkranz, Woltz and Mah.&nbsp;&nbsp;Derrick was in 54th, however, Gilson had fallen to 128th and Wilson was only up to 135th.</p>
            <p>As the course got worse, MIT found it difficult to go anywhere but backwards.&nbsp; Woltz moved up slightly to 43rd place.&nbsp; Derrick moved up to 49th but Rosenkranz fell back to 56th.&nbsp; The biggest losses came when Mah lost in the final two miles what he had gained in the previous two, falling back to 89th place.&nbsp; Wilson passed Gilson, finishing as Tech's number five runner in 131st place&nbsp;to total 368 points.&nbsp;The Engineers finished in 10th place behind North&nbsp;Central (41 pts), Wash U (97 pts), Calvin (127 pts), Haverford (144 pts), Pomona Pitzer (277 pts), UW Stout (302 pts), Otterbein (305 pts), Wartburg (314&nbsp;pts) and Chicago (314 pts).</p>
            <p>Being so far back in the field at the mile mark made it a challenge physically to move up with the conditions and mentally in that it was difficult to be motivated by being so far back they could not see the front.&nbsp; Overall, the 45 second final delta by MIT's top five was excellent over the 8K. However, having the highest finisher in 43rd place would not get the job done.</p>
            <p>MIT's next race will be the&nbsp;NEWMAC Conference Championship at Smith College on October 27th.&nbsp; The men have never lost the championship in its 20 year history.&nbsp; The women have won every title since 2007.</p></td>
