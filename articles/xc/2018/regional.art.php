              <h3>Women Win Third Consecutive NCAA D3 New England Title</h3>
              <p>&nbsp;</p>
              <h4>Men Advance to Nationals on Fourth Place Finish</h4>
          </div>
            <p>November 10, 2018</p>
            <p>Accustomed to less than ideal racing conditions this season, the Women's Cross Country team from MIT felt right at home with the rain soaked, muddy course at Bowdoin College, host of the <a href="Women-s_NCAA_D3_Regional.pdf">NCAA Division III New England Region&nbsp;championship</a>.&nbsp; Despite a fast start and losing quite a bit of ground in the final mile, the women turned in a three-peat, winning the title for the third year in a row, scoring 87 points to outscore 56 other teams.</p>
            <p>The team's strategy was to go out aggressively despite not having done so all season.&nbsp; Leandra Zimmermann '19, the only senior on the team, went out with the leaders, blasting out in 5:34 for the first mile.&nbsp; She was sitting in fifth at that point.&nbsp; The remainder of the top six MIT harriers, led by Jenna Melanson '20, were all between eighth and 14th, running between 5:44&nbsp;and 5:46.&nbsp; </p>
            <p>The pace settled a bit after that first mile&nbsp;as the soft ground started getting to everyone's legs.&nbsp; After the two mile mark Zimmermann was in sixth with Melanson still in eighth.&nbsp; Katie Collins '21 was having the race of her year&nbsp;in 10th.&nbsp; Katie Bacher '20 and Einat Gavish '22 were in 14th and 15th, but looking as though they were losing focus and not racing with the same intensity as their teammates ahead of them.&nbsp; Marissa McPhillips '20 was in 18th, looking totally defeated.&nbsp; On the plus side, Middlebury and Williams, MIT's two main opponents, had not started aggressively for the most part and were pretty far back.&nbsp; Middlebury's first two runners were aggressive, starting out in the front pack, were now in fifth and seventh.&nbsp; Their next three runners were in 23rd, 35th and 44th.&nbsp; Williams was very cautious, running 19th, 27th, 30th, 41st and 42nd.</p>
            <p>The question to be answered, would the cautious approach work and the Middlebury and Williams runners catch the dying Engineers from MIT.&nbsp; At the finish not much changed at the front of the race.&nbsp; Zimmermann finished sixth in 21:38 on the 6K course.&nbsp; Melanson held ground for the most part, finishing in 10th in 22:18 and Collins finished an outstanding 12th in 21:20.&nbsp; Then it got interesting.&nbsp; Bacher slid to 27th in 22:40 and Gavish dropped to 32nd in 22:47.&nbsp; The 1:09 gap was the higherst all season for Tech&nbsp;and would need fixing at Nationals.&nbsp; Emily Niu '21 finished&nbsp; 77th for MIT in 23:33.&nbsp; McPhillips dropped out of sight to 86th place in 23:44.</p>
            <p>Middlebury and Williams both moved up significantly, but not enough to upset MIT.&nbsp; Middlebury finished with 108 points, placing 5, 8, 28, 31 and 36.&nbsp; Williams finished in third with 115 points, placing 13, 19, 20, 26 and 37.</p>
            <p>MIT was seeded fourth in the men's race, but the two-time defending champions had not been favored either of the two previous years which they won, so the plan was to go for it.&nbsp; However, since the course was much worse after the women raced on it, and the women had shown some dying off at the end, the men were&nbsp;guardedly aggressive.</p>
            <p>Early on in the race MIT was in a solid position, but everyone seemed to have the same plan, making it a free for all up front.&nbsp; Andrew Mah '22 showed his championship attitude as he was solidly with the front pack in about seventh place through the first two miles, splitting 9:57.&nbsp; Kent Slaney '21, a&nbsp;very talented runner, was trying to conquer the mental side of his racing woes, but was already starting to fall back as he went from one second back from Mah at the mile mark to seven seconds back to the two mile mark.&nbsp; Josh Rosenkranz '19 was steadily moving up, currently&nbsp;at 10:05 in 19th place.&nbsp; Billy Woltz '20, who had wanted to be aggressive and was planning on finishing among the top runners, was moving up, but still in about 26th place.&nbsp; Josh Derrick '20 was still back quite a bit in 50th place, but he&nbsp;has been very successful at moving up in races throughout the season.</p>
            <p>By the end of the fourth mile, Mah had fallen off the leaders but was still in eighth place in 20:11.&nbsp; Rosenkranz was in 19th, but 21 second back at 20:32.&nbsp; Woltz was now in third for Tech in 32nd place at 20:44 with Derrick only three spots back at 20:45.&nbsp; With Slaney going out the back it was now up to&nbsp;either Aidan Gilson '19 or Alex Knapp '19 to come&nbsp;through as the fifth runner.&nbsp; Last year, Gilson had stepped up to finish 12th overall and second for MIT, helping his team to victory.&nbsp; This year Gilson has been struggling, but today he was battling hard and hanging onto a place in the mid-40's.</p>
            <p>In the final mile, Mah lost a few spots to finish 13th for Tech in 25:24.&nbsp; Rosenkranz placed 19th in 25:43, Derrick moved past Woltz, who&nbsp;had been MIT's top runner the past two races, finishing in 31st and 32nd place in 25:55 and 25:56 respectively.&nbsp; Gilson closed out the scoring in 50th place in 26:12.&nbsp; </p>
            <p>MIT totaled a respectable 145 points&nbsp;to finish in their predicted fourth place.&nbsp; It was heartbreaking as they lost to third place Middlebury by three points.&nbsp; Amherst College won with an impressive 65 points and Williams was second in the 58 team field with 86 points.</p>
            <p>All four of those teams will advance to the NCAA Division III Championship at the Lake Breeze Golf Course in Winneconne, WI, hosted by the University of Wisconsin - Oshkosh on November 17.            </p>
