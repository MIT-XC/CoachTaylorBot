                  <p style="font-size: large; font-weight: bold;">Despite Individual Success, Teams Run</p>
                  <p style="font-size: large; font-weight: bold;"> Average at NEICAAA Championship </p>
                  <p align="center">October 9, 2010</p>
                  <p align="left">The weather was near perfect, as was the competition at the <a href="neicaaa.res.php">NEICAAA Championship</a> on Saturday, a race among all NCAA Divisions in New England. MIT failed to make the most of the opportunity, in part due to a barrage of illness and also due to a lack of competitiveness necessary at this level of competition. Although the ground was soft from the deluge of rain earlier in the week, the times were quite fast. </p>
                  <p align="left">In the women's race, last minute withdrawals of Anna Holt-Gosselin '11, (upper leg injury), Emma Broderick '14, (slight plantar fasciitis) and Olivia Papa '14, (possible reaction to flu shot), combined with a bad cold and sinus infection to Claire O'Connell '14, left the varsity decimated. To the credit of those who raced, they did not let their small numbers affect their performance, earning 12th place overall out of the 47 teams, and third among Division III teams.</p>
                  <p align="left">At the start of the race, the quintet racing [Katherine Eve '12, Alina Gatowski '11, Tania Morimoto '12, Martina de Geus '14, and O'Connell (feeling she felt well enough to run)] got squeezed by the teams on either side of them, relegating them to the back of the 350+ person field. However, the team had positioned themselves pretty well by the mile mark. All but Eve came through the flat first mile between 5:40-5:49, well behind the leaders who came through around 5:25.</p>
                  <p align="left">By mile two, Morimoto was the lead Engineer at 11:50 with Gatowski right behind her in 11:52. Eve had moved up to third on the team with a solid 6:14 challenging second mile. O'Connell and de Geus had fallen back to 12:17 and 12:13 respectively but had not given up. </p>
                  <p align="left">In the final mile, Eve continued to move up, eventually passing Gatowski in the final 200 meters to place first for MIT and 41st overall in 18:43. Gatowski finished in 44th in 18:45 with Morimoto fading in the final mile to 68th place in 18:56. de Geus held on to 95th place in 19:12 while O'Connell placed 112th in 19:17--quite amazing after suffering all week from her cold. </p>
                  <p align="left">The overall caution of the group in the second mile as well as the failure to engage the top competitors is what is keeping this very talented group from reaching their potential.</p>
                  <p align="left">For the men, the expectations were very high as they thought they had a strong possibility of placing in the top 10 and first among Division III teams. Paul Welle '11, the top runner on the team had a cold most of the week, but appeared ready to go. Roy Wedge '14, Stephen Serene '12, and Dan Harper '12, were ready to take care of business. Gihan Amarasiriwardena '11, was still feeling the after effects from an earlier infection in his lungs while Richard Prevost '11, also suffering from a cold believed he was ready to go, as was Logan Trimble '13, recently moved up to the varsity top seven.</p>
                  <p align="left">The men learned from the women's mistake at the start and got out very well. Coming to the end of the first mile, the team looked great, positioned between 60-120th positions with times between 4:53 and 5:04. Welle, leading the way for MIT, blasted a great second mile, moving into 45th position and looking strong. The rest of the varsity fell behind by another 8-10 seconds instead of pushing the second mile as prescribed. Wedge did push it relative to his 5:04 opening mile, but still fell further behind Welle. </p>
                  <p align="left">The third mile was a painful one for the Engineers. Welle fell back 10 places and was obviously not feeling well. The third mile should be faster than the second mile due to the lack of any real hills, but MIT just held even, losing ground to the competition. The fourth mile was very strong for Wedge, Serene and Harper, but Welle, Amarasiriwardena, Prevost and Trimble continued to slow.</p>
                  <p align="left">The final mile saw MIT's top three continue to move up, while the rest continued to lose ground. Wedge had an excellent race, particularly for a freshman, placing 64th in 25:50. Serene placed 69th in 25:56 while Harper finished 74th in 25:59. Welle made the most of a bad situation, finishing in 26:15 in 107th position while Amarasiriwardena placed 137th in 26:26. Nevertheless, MIT placed 18th among the 47 teams and 6th among Division III teams. If the Engineers had had their normal deltas from Welle and Prevost, they would have placed 9th overall and 2nd among Division III teams.</p>
                  <p align="left">In the sub-varsity races, Katy Gordon '11, ran an excellent race in leading the way for MIT in 19:21 with support from Maggie Lloyd '12, and Sarah Sprague '13 in 19:24 and 19:38 respectively. Jacob Young '13, and Joe Christopher '12, were the top two Tech runners in the sub-varsity race with respective times of 26:53 and 26:55.</p>
                  <p align="left">Next week, MIT heads to the Connecticut College Invitational at the Harkness Memorial State Park, although many of their top seven will not be racing. </p>
