                  <p><strong><span class="headline style1">Men and Women Both
                        Claim Runner-up Spots in UMass Dartmouth Invitational  </span></strong></p>
                  <p align="center">September 18, 2010</p>
                  <p align="left">In the first real test for MIT, the Engineers
                    ran nearly their full teams at the <a href="umd.res.php">UMass
                    Dartmouth Invitational</a>.&nbsp; The
                    weather was a little warm and humid for cross country, but
                    as nice as is likely at this point in September.&nbsp; Of
                    the four races, varsity and sub-varsity for both genders,
                    MIT placed second in the two varsity and men's sub varsity
                    races, while winning the women's sub-varsity contest. &nbsp;</p>
                  <p align="left">The men's varsity 8K race featured nationally
                    ranked teams from Brandeis (10th) and Keene State (18th)
                    among the 36 teams.&nbsp; Being
                    a very flat course with only a minor hill in the third mile,
                    the pace was very fast from the start.&nbsp; Paul Welle '11,&nbsp;was
                    a few seconds back of the leaders at the mile mark, along
                    with Dan Harper '12, Richard Prevost '11 and Gihan Amarasiriwardena
                    '11, at 4:53.&nbsp; The second mile slowed marginally as
                    Welle came through in 9:54 as he moved closer to the leaders.&nbsp; Prevost
                    and Amarasiriwardena could not handle the early pace and
                    began to fade to between six and fifteen seconds behind
                    their teammate.&nbsp; By mile three, Harper and Stephen Serene
                    '12 were moving up on the lead group and freshmen Roy Wedge
                    and Matt Metlitz were within striking distance.&nbsp; In
                    the slightly downhill fourth mile Welle made a nice move
                    to pull up even with the leaders and looked very strong.&nbsp;&nbsp; Wedge
                    ran a great 4:50 fourth mile to pull up to Serene and Harper,
                    but Metlitz, while running very well, was unable to move
                    up to that group.&nbsp; In the final mile Welle got outkicked,
                    finishing fourth in a remarkable time of 24:51.&nbsp; Both
                    Keene State and Brandeis faded during the race, while MIT
                    moved up, but not enough to catch Keene State, who finished
                    with 73 points.&nbsp; Tech finished strong with 76 points
                    as Harper (13th - 25:07), Serene (15th - 25:09), Wedge (17th
                    - 25:10) and Metlitz (36th - 25:42) nearly won the meet.&nbsp; Brandeis,
                    minus their top runner, finished with 79 points.</p>
                  <p align="left">In the sub-varsity race, which MIT lost to
                    Brandeis, Logan Trimble '13 and Jay McKenna '14, led the
                    Engineers with third and fifth place respectively, running
                    26:06 and 26:12.&nbsp; Other scorers for MIT were Joe Christopher
                    '12 (26:29) in 10th, Matt Weaver '12 (26:33) in 13th, and
                    Andrew Erickson '12 (26:41) in 15th.</p>
                  <p align="left">The women, ranked 7th in the country, figured
                    they would be tested by Wellesley College and possibly by
                    Keene State, but despite not running one of their top four
                    and another of their top seven, MIT was confident in their
                    abilities to win the 5K race.&nbsp; Wellesley went out fast,
                    clearly showing they were not intimidated by MIT.&nbsp;At
                    the 1K, Wellesley had the top five runners with the two lead
                    MIT runners at least seven seconds back.&nbsp;At the mile
                    mark, the leaders came through in a blazing 5:30.&nbsp; Alina
                    Gatowski '11 and Tania Morimoto '12, were back at 5:40, but
                    ahead of the pace MIT wanted.&nbsp; Back at 5:46 was Anna
                    Holt-Gosselin '11, and the freshmen Martina de Geus and Claire
                    O'Connell were fast at 5:51, but it was necessary to get
                    out given the fast pace of the leaders, and even with that
                    there were more than 100 runners in front of them.&nbsp; Olivia
                    Papa '14, got out slow and was having trouble catching up
                    with her group as she came through in 5:56.&nbsp; At mile
                    two, Morimoto had caught the leaders and Gatowski was just
                    four seconds back at 11:35.&nbsp; Holt-Gosselin was moving
                    up in the field but falling further behind Morimoto and Gatowski.&nbsp; de
                    Geus, O'Connell and Papa were now together between 12:03
                    and 12:06 and moving up well.&nbsp;The final mile was very
                    fast with the winner, Paige Mills from Keene State running
                    around a 5:30 to easily win.&nbsp; Gatowski ran a 5:50 to
                    finish fourth in 18:01.&nbsp; Morimoto placed seventh in
                    18:18, Holt-Gosselin ninth in 18:22, de Geus 17th in 18:38,
                    O'Connell 18th in 18:39 and Papa 19th in 18:42.&nbsp; MIT's
                    45 points were only four behind Wellesley, lost it on their
                    fourth and fifth placed runners.&nbsp; Keene State was third
                    with 93 points out of the 39 teams.</p>
                  <p align="left">In the women's sub-varsity race, Emma Broderick
                    '14, surprised everyone with a second place finish and time
                    of 18:32 in her first cross country race ever, at any level.&nbsp; MIT
                    also placed 3-4-5-9 out of 208 runners, by Maggie Lloyd '12,
                    Sarah Sprague '13, Sherry Fu '14, and Juliann Lajoie '13,
                    to easily win the race. </p>
                  <p align="left">Next week, MIT travels to Williams College
                    for the Purple Valley Invitational run at their course at
                    Mt. Greylock HS.&nbsp; There will be many of the best teams
                    in New England as well as a couple of nationally ranked teams
                    from outside the region participating in the race.</p>
