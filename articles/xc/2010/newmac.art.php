                  <p><strong><span class="headline style1">Eve and Wedge Lead MIT Women and Men to NEWMAC Titles</span></strong></p>
                  <p align="center">October 30, 2010</p>
                  <p align="left">MIT Men's and Women's Cross Country teams traveled to Moore State Park in Paxton, MA to defend their NEWMAC titles and were not disappointed with the result. The windy and cool day with temperatures in the low 50's and the very hilly course did nothing to slow down the Engineers who defended both titles successfully. The women finished first for the fourth consecutive year, scoring just 24 points, while the men recorded 29 points en route to their 13th consecutive victory--remaining the only team to ever win the title.</p>
                  <p align="left">The women's race was expected to be a virtual dual meet between MIT and Wellesley College. In the first meeting of the two teams, Wellesley surprised Tech by going out fast on a flat course and stealing the victory. In two later meetings involving more competitive meets, MIT was able to run away from Wellesley. With the two teams focused on each other the strategy that would be employed was anyone's guess.</p>
                  <p align="left">Wellesley sprinted to the front at the start with MIT close behind. The first mile had two very large hills with one equally long downhill. Even though Wellesley's runners were leading things, the pace was conservative at 6:08 for the mile split. The second mile finished at a slightly higher elevation than the first mile, having one long downhill and one very long, rocky uphill. Both teams pushed this mile as the lead group started to split up. Randelle Boots '13, of Wellesley was in a close battle with Katherine Eve '12, Tania Morimoto '12 and Alina Gatowski '11, of MIT. They powered through the second mile at 12:17, producing an even split race to that point. The third mile was predominantly downhill, finishing slightly higher than the lowest point of the course, which was the start. In the final downhill, Boots made a move that could not be matched by the MIT trio, leading her to the individual title at 18:25. Eve was somewhat close at 18:33 and was closely followed by Morimoto in 18:38 and Gatowski in 18:39. Wellesley had the next two finishers, but then MIT closed it out with two freshmen finishing seventh and eighth, Martina de Geus in 19:03 and Claire O'Connell in 19:08. MIT's depth was impressive as they had 10 of the first 19 runners.</p>
                  <p align="left">The men's race was on the exact same first three miles as the women, but they then repeated most of the first two miles, meaning three additional large hills. Similar to how the women's race started, the men allowed the others teams to set the pace and Springfield College obliged with a quick sprint from the uphill start. MIT fell in behind, content to let others set the pace for the first mile. The leaders came through the mile in 5:18 with MIT's top group of Paul Welle '11, Richard Prevost '11, and Stephen Serene '12, back at 5:20. The second mile, being very hilly and rocky was another conservative effort, but MIT was now right with the leaders at 10:42 and freshman Roy Wedge had worked his way up to join the other three Engineers. The downhill third mile saw the race open up a bit with four MIT runners along with one Springfield and one Coast Guard runner as they came through in 15:41. The fourth mile had the two biggest hills and MIT was ready to attack them. However, Serene had coasted the downhill portion of the third mile and suffered a bad side stitch he was not prepared to endure. Also, Prevost lost contact with the group, losing 23 seconds to the leaders, who were now three, Welle, Wedge and Zach Pietras '13, of Springfield. Welle had broken out to a six second lead by the end of the fourth mile, coming through in 21:05. However, he was no match for Wedge on the downhill fifth mile. Wedge ran 4:57 in the final mile to win in 26:08 to Welle's second place finish in 26:12. Prevost recovered to run a fine fifth mile and finish fifth in 26:37. Dan Harper '12, not having his best day, performed well enough to finish ninth in 26:59. Gihan Amarasiriwardena '11, had a solid finish in 12th place to give MIT the victory.</p>
                  <p align="left">In addition to the two team victories, MIT also earned both Freshman of the Year Awards with de Geus earning it for the women and Wedge winning it for the men. Wedge also earned Runner of the Year with his victory.</p>
                  <p align="left">The Engineers are off from racing until the NCAA Regional Qualifier at Williams College on November 13.</p>
