                  <p><strong><span class="headline style1">Eve Leads Women to Victory,
                        While Prevost's Win is Not Enough to Secure Men's Title </span></strong></p>
                  <p align="center">September 11, 2010</p>
                  <p align="left">While the women definitely went out much too
                    fast at the start of the <a href="eng.res.php">Engineer's
                    Cup</a> at Franklin Park,
                    they managed to run faster than their opponents, securing
                    the victory with a score of 18 points to 57 for RPI and 63
                    for WPI.&nbsp; Katherine Eve '12, took individual honors,
                    leading from start to finish in a superb time of 18:39 on
                    the warm and windy day.&nbsp; Richard Prevost '11, passed
                    teammate Gihan Amarasiriwardena '11, in the final two tenths
                    of a mile to claim the victory in 26:39.&nbsp; MIT's 1-2
                    finish was not enough to secure the victory as RPI packed
                    in six runners between fourth and ninth to outscore MIT 30
                    to 38, while WPI came in at 69 points.&nbsp; MIT held out
                    three of their top four women as well as much of their second
                    group of runners while the men held out two of their top
                    three and three of their next four.&nbsp; The purpose was
                    to give everyone an opportunity to race while not over racing
                    anyone for the season.&nbsp; This strategy obviously did
                    not play out well in the men's race. </p>
                  <p align="left">The MIT women uncharacteristically sprinted
                    across the field at the start, perhaps following Eve's lead
                    too closely, to easily take early command of the race.&nbsp; The
                    first mile split of 5:44 for Eve was much faster than planned,
                    but she felt fine so she went with it.&nbsp; The problem
                    was that Katy Gordon '11, came through in 5:48, and Casey
                    O'Brien '14, who was supposed to be with Gordon, was not
                    able to handle such a fast first mile.&nbsp; Fortunately,
                    Lauren Allen '13, Sarah Sprague '13, Chandler Burfield '13,
                    and Maggie Lloyd '12, were with the leaders from RPI and
                    WPI at 6:07, which was also a bit too fast, and certainly
                    they were running much slower than 6:07 pace at the time
                    they passed through the mile mark.&nbsp; Eve
                    continued to pull away, running the challenging second mile
                    in 6:12.&nbsp; Gordon
                    was fortunate to have the classic, &quot;out of sight, out of
                    mind&quot; scenario play out as she slowed to a 6:35 second mile
                    but still maintained a large lead.&nbsp; O'Brien faded, but
                    Allen, Sprague, Burfield and Lloyd moved up, distancing themselves
                    from their opponents with the exception of Vicky Hewey of
                    WPI, who broke up the MIT sweep with her third place finish.</p>
                  <p align="left">The men ran a much smarter  race, coming through
                    the mile mark in the planned 5:11 pace for Amarasiriwardena
                    and Matt Metlitz '14 and 5:14 for Prevost, Jacob Young '13
                    and Matt Weaver '12.&nbsp; The core group from RPI were running
                    well as a pack and ahead of MIT's second group of runners.&nbsp; The
                    second mile was too slow for all of the MIT runners, missing
                    the opportunity to close the meet early.&nbsp; However, by
                    the third mile mark, MIT was in the lead and in position
                    to take over the race.&nbsp; The problem was that RPI was
                    prepared for an 8K race, while it appeared MIT was only ready
                    for a 5K.&nbsp; Metlitz faded with a 5:36 fourth mile, while
                    Young was even worse with a 5:41.&nbsp; The RPI runners maintained
                    their group and took over the race in the fourth mile, 
                    closing it out in the final mile.&nbsp;With the exception
                    of Prevost, Amarasiriwardena and Logan Trimble '13, who ended
                    up as MIT's fifth man in 27:28, everyone else continued to
                    fade in the fifth mile.&nbsp; The loss was the first for
                    MIT in the Engineer's Cup since 1996. </p>
                  <p align="left">Next week, MIT travels to UMass Dartmouth in
                    North Dartmouth, Massachusetts for a large invitational pitting
                    many of New England's top Division III teams against one
                    another.&nbsp;This
                    will be the first time MIT will run the majority of their
                    top runners in the same race in 2010. </p>
