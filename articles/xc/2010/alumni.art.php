                  <p><strong><span class="headline style1">Freshmen Spoil Alumni
                        Turnout - Wentz and Serene Win </span></strong></p>
                  <p align="center">September 4, 2010</p>
                  <p align="left">Despite the best turnout of alumni in the history
                    of <a href="2010 Alumni XC.pdf">the meet</a>, the varsity, led by the core performance of
                    a trio of freshmen for both the men and women, outlasted
                    the alumni by a score of 24 to 31 in the women's 5K event,
                    and 20-31 in the men's 8K race.</p>
                  <p align="left">On a windy, but warm day of 80 degrees, 10
                    alums lined up against six varsity harriers in the women's
                    race at Franklin Park.&nbsp; The
                    pre-race favorite was Jacqui Wentz '10, All America winner
                    in cross country and national champion in the 3000 meter
                    steeplechase.&nbsp; The alumni were well represented with
                    a supporting cast of Adrienne Bolger '09, Andrea Bradshaw
                    '09, Martha Buckley '04, Karen Condon '07, Betsy Eames '06,
                    Elizabeth Finn '08, Liz Labuz '09, Rachel Niehuus '07 and
                    Alisha Schor '07.&nbsp; However, as we all discover, it is
                    more difficult to train consistently for races like this
                    when one is no longer with the team.&nbsp; The spirit and
                    ability were there, but the training not quite.</p>
                  <p align="left">As predicted, Wentz led at the start.&nbsp; Tania
                    Morimoto '12, started off conservatively, but moved up and
                    pulled even with Wentz by the mile mark, coming through with
                    a split of 6:08.&nbsp; The alumni were spread out a bit,
                    but Bradshaw was running just ahead of the three freshmen,
                    Olivia Papa, Claire O'Connell and Martina de Geus, all of
                    whom came through in 6:13.&nbsp; The second mile was where
                    Morimoto tried to make her move, but Wentz was just too good
                    on this day and she pulled away, with a very impressive 6:10
                    second mile on the uphill portion of the course.&nbsp; Morimoto
                    managed a very strong 6:15 and continued to look strong.&nbsp;&nbsp; The
                    freshmen had caught Bradshaw and began to push the pace as
                    they came through the second mile in 6:32.</p>
                  <p align="left">Wentz continued to pull away, winning with
                    a strong 19:09.&nbsp; Morimoto faded a bit in the final mile
                    with a 6:28, but managed an excellent second place with an
                    overall time of 19:37.&nbsp;O'Connell made a strong move
                    in the Wilderness but took a wrong turn, negating her break
                    and putting in a catch-up position.&nbsp; Papa took advantage
                    and made a move of her own, running a 6:14 third mile to
                    claim third in 19:44.&nbsp; O'Connell recovered and appeared
                    to have fourth place locked up, but Bradshaw's kick was too
                    much.&nbsp;Bradshaw earned fourth in 19:50 while O'Connell
                    ran 19:52.&nbsp; de Geus finished well, placing sixth in
                    19:54.&nbsp; Finn was the third Alumni finisher, placing
                    seventh in 20:32.&nbsp; Juliann Lajoie '13, was the fifth
                    finisher for the Varsity, placing eighth in 21:05.&nbsp; Bolger
                    and Niehuus were the final scorers for the Alumni with respective
                    times of 21:11 and 21:27. </p>
                  <p align="left">In the men's race, Dan Harper '12, with Stephen
                    Serene '12, trailing, took the pace out, coming through the
                    first mile in 5:13.&nbsp; Serene was close behind in 5:15
                    with Carlos Renjifo '04, following and looking intent on
                    reeling them in.&nbsp; The three freshmen, Roy Wedge, Eric
                    Safai and Jay McKenna were together at 5:33 along with Bill
                    Phipps '10.&nbsp; New dad, Ben Schmeckpeper '05, and Trevor
                    Rundell '09 followed closely.</p>
                  <p align="left">In the second mile, Renjifo took a solid lead
                    going into the third mile with Harper still ahead of Serene.&nbsp; Wedge
                    was starting to edge away from the group, although Phipps
                    was hanging tough.&nbsp; Schmeckpeper, Chris Wong '08, and
                    Eric Khatchadourian '06, were not the chase group for the
                    Alumni.&nbsp; During the third mile, Renjifo had built a
                    four second lead over Harper, who was three seconds ahead
                    of Serene.&nbsp; Wedge and Safai had pulled away from Phipps,
                    who had in turn pulled away from McKenna.&nbsp; Khatchadourian
                    was pulling away from Schmeckpeper and Wong, but Joe Christopher
                    '12, a Cambridge University student on a year abroad to MIT,
                    was closing in on Khatchadourian.</p>
                  <p align="left">Serene made the move of the race, throwing
                    down a 5:25 fourth mile to catch and pull away from both
                    Renjifo and Harper by a sizeable margin.&nbsp; Wedge and
                    Safai were still close together, but Khatchadourian had passed
                    Phipps and was right with McKenna, who was just ahead of
                    Christopher.&nbsp; Chris Wong, Terry McNatt '87, and Bob
                    Collins '82, were now the chase group, taking advantage of
                    a conservative start. </p>
                  <p align="left">The order of things really did not change much
                    in the final mile.&nbsp; Serene won in 26:39 to Renjifo's
                    26:53 and Harper's 27:14.&nbsp; Wedge had the fastest final
                    mile, running 4:57 to close within nine seconds of Harper.&nbsp; Safai
                    held onto fifth, but Khatchadourian moved into sixth, defeating
                    McKenna by three seconds.&nbsp; Phipps held onto eighth although
                    Christopher closed to within two seconds for ninth.&nbsp; Closing
                    out the scoring for the Alumni were Wong and McNatt, running
                    28:24 and 28:30 respectively.&nbsp; </p>
                  <p align="left">Also representing the &quot;Has Beens&quot; were Fivos
                    Constantinou '06, Ian Driver '05, Joe Roy-Mayhew '08, Tyler
                    Abrams '09, Bob Zak '86, Peter Mulligan '08, Ashley Clayborne
                    '99, Neal Karchem '99, Don Shobrys '75, and Jeff Lukas '82.&nbsp;In
                    addition, many alums came to watch and cheer on their teammates.
                   &nbsp; Afterwards, everyone enjoyed the great weather as they
                   shared old stories and pizza, promising to be in better shape
                   in 2011.</p>
                  <p align="left">Next Saturday, MIT is back at Franklin Park
                    to compete against RPI and WPI, starting @ 12:00 for the
                    women and 12:30 for the men.&nbsp; MIT will run most of those
                    who were held out of this weekend's race.</p>
