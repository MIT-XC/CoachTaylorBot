                  <p><strong><span class="headline">Women Third at NCAA Division III Championship, Men 12th in 32 Team Field</span></strong></p>
                  <p>&nbsp;</p>
                  <p align="center">November 20, 2010</p>
                  <p align="left">MIT made the most out of their trip to the <a href="ncaa.res.pdf">NCAA Division III Cross Country Championship</a>, hosted by Wartburg College in Waverly, Iowa. The men, making their first trip since 2001, finished 12th overall in the 32 team field with 347 points, once again the top team in New England. The women, improving each year, <a href="ncaa.wres.pdf">finished third with 214 points</a> after earning fifth place in 2009 and 10th in 2008.</p>
                  <p align="left">The men's race was first to go in the sub 30 degree temperatures. The strong winds certainly made it seem a lot colder, but this did not affect the Engineers. Tri captains Paul Welle '11, Richard Prevost '11 and Gihan Amarasiriwardena '11, led the rest of the top seven (Dan Harper '12, Stephen Serene '12, Logan Trimble '13, Roy Wedge '14) to an excellent start, establishing a great position to the first turn.</p>
                  <p align="left">By the mile mark, Welle moved into the top 30 with his 4:56 and tried to run with the leaders. Serene and Prevost also established themselves among the 80 runners in the 280 runner field with miles of 4:58. Harper was getting close to joining them and Wedge, who had been knocked down at the first turn was still trying to work his way back into the race. Everyone was looking solid at the two mile mark, although Wedge was still playing catchup. Welle came through in 10:00 while the others were between 10:09 and 10:11. Of course, Wedge was still back at 10:20. By the four mile mark, Serene and Harper had almost caught up to Welle, behind by three and six seconds respectively. Wedge was moving up fast, but Prevost had suffered a slow mile and was continuing to lose ground. In the final mile, the Engineers poured it on, moving past dozens of runners. Harper covering the mile in 4:39, finished first for MIT and 48th overall in 25:08. Right behind him in 25:09 for 51st place was Serene who ran a 4:43 final mile. Welle closed well to finish in 71st place in 25:18, although his 4:55 final mile seemed slow compared to Harper and Serene. Wedge finally made it back to the group, placing 134th in 25:37. Prevost closed out the scoring for MIT, placing 169th in 25:53. Subtracting the non-team finishers, MIT dropped to 347 points.</p>
                  <p align="left">The women, never really putting together their best race all season, finished fourth last week in the New England Regional and were tied for seventh in the country according to the rankings. However, just like the past two years, the Lady Engineers came to race and the rankings did not matter. </p>
                  <p align="left">The top seven, comprised of co-captains, Alina Gatowski '11, Tania Morimoto '12, and Anna Holt-Gosselin '11, Katherine Eve '12, Martina de Geus '14, Claire O'Connell '14 and Emma Broderick '14, after one called back start, got off the line very well and established themselves in the top third of the 280 person field. </p>
                  <p align="left">Gatowski put herself among the top 40 at the start and slowly worked her way up. Morimoto, Eve, Holt-Gosselin, de Geus and Broderick were also moving well in the first mile, which they covered in 5:47-5:50. By the two mile mark, Gatowski was in 27th place in 11:37 and looking strong. Morimoto looked fatigued but was still holding her own in 11:46. Eve was moving up very well but still back at 11:51. Holt-Gosselin and de Geus were just behind in 11:54 and 11:56 and looking strong. The 21 second delta through two miles was not great but pretty good.</p>
                  <p align="left">During the third mile, Gatowski and Eve both turned in 5:46 miles to hold their position and maybe move up a spot or two. Morimoto held steady with a 5:54 and de Geus caught Holt-Gosselin with a 5:59 effort. The final .72 of the 6K, was run in 4:17 by Gatowski and Eve as they placed 24th and 42nd respectively. Gatowski's place earned her All America status. Morimoto lost a lot of ground, losing 19 seconds to her teammates up front, finishing in 80th place. Holt-Gosselin turned up the speed, finishing in 85th place and de Geus finished 100th overall to close out MIT's scoring, which once the non-team runners were taken out gave MIT the score of 214. Gatowski's 21:40 was a fast 5:49 per mile.</p>
                  <p align="left">The third place finish put the ladies on the podium, earning MIT a nice NCAA team trophy and each of the runners an individual trophy.</p>
