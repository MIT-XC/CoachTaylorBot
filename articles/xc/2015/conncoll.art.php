                  <p><span class="headline">Engineers Construct Victory at Conn. College Invite</span></p>
                  <p>&nbsp;</p>
                  <p align="left">October 17, 2015</p>
                  <p align="left">MIT's nationally ranked Men's and Women's Cross Country teams at 12th and 4th respectively, competed at the <a href="conncoll.res.php">Connecticut College Invitational</a> with three goals in mind. The first was to work on the mental side of competing with hopes of raising their level of performance. If successful, this should allow the Engineers to achieve their second goal of winning the competition. The third goal was to get a good feel for the course that will be the site of the NCAA Division III New England Region Championship. By all accounts all three goals were achieved by both genders. </p>
                  <p align="left">The women raced first on the sunny, but cool day with temperatures in the mid to upper 50's. Going into the 6K race, MIT was without number three runner Nicole Zeinstra '16, who was going through some stomach issues. Just before the race, Megan Montgomery '19 had a hamstring tighten up, so she was pulled just to be on the safe side. Those racing knew they would have to step up. Nationally ranked Tufts (3rd), Carleton (11th) and Oneonta (12th) were among the opposing teams so MIT would need to be at the top of their game.</p>
                  <p align="left">After a successful start in the 25 team field, the women settled into the race plan. At the mile mark, Maryann Gong '17 was conservative at 5:36, but was in fourth place, about six seconds out of first. Her teammates were running conservatively, but were moving up in the field. At the two mile mark, Gong had lost some focus and slowed to 11:30. Christina Wicker '17 was four seconds back and Leandra Zimmermann '19 and Mary Eccles '18 were racing amazingly well, only three seconds behind Wicker. After going out too fast, Bailee Margolis '19 had fallen back with a 6:07 second mile. </p>
                  <p align="left">The third mile saw Gong lose more ground and slip from second to fourth place. Wicker was now only two seconds back at 17:25 and Zimmermann and Eccles were just four seconds behind Gong. Margolis had lost focus and lost 30 more seconds. Miraculously, Wicker coming up to Gong woke her up and Gong sprinted the final half mile to take the lead and win the race in 21:31. Wicker finished in seventh in 21:47. Zimmermann was 11th and Eccles 12th, both crossing the line at 21:54. Margolis ran a strong last 1000 to hold onto 32nd place in 22:43 and give MIT the victory with 62 points over Tufts at 67 points. Having an excellent race in terms of improvement and being well paced was Darby LaPlant, who finished 58th overall in 23:14. Keene State surprised many by getting third with 108 points, just ahead of fourth place of Carleton with 111 points and Oneonta in fifth with 119 points.</p>
                  <p align="left">MIT's men's team was starting to get their runners back to full health and they hoped to show off their depth in this meet. Hart Horner of Carleton College took the lead at the start of the race, quickly turning the race into one for second place. One of those vying for that position was Colin Godwin '17, who has been getting closer to his All America form from last year week by week. Godwin came through the mile mark in fourth place in 4:54. Just behind him was freshman Daniel Weiss, who had made a spurt to move up into the chase group. Back at 5:00 was Dennis Maloney '19, Rory Beyer '17, and Ben Freed '18. Most of the remainder of Tech's runners were back around 5:05-5:10, following the race plan.</p>
                  <p align="left">Throughout the next two miles Godwin, Weiss, Maloney and Beyer either held ground or moved up slightly. Freed was struggling a bit in the third mile, but Cooper Sloan '18, still coming back from injury, was making a statement as he moved through the field, running with Freed and Alex Knoedler '18 at 15:29 at the end of mile three. The fourth mile can wear on a runner as the footing is not the best and it includes a slight climb in elevation. Godwin continued to gap himself from his teammates, now at :13 over Weiss -- 20:08 to 20:21. Sloan continued to move up, now only two seconds behind Beyer as MIT's fifth runner. For the finish, Godwin ran a 4:58 final mile to move into third place in 25:06. Weiss finished will in 5:02 to claim seventh place. Maloney finished just 28 seconds behind Godwin in 25:34 and Sloan out-leaned Beyer at the finish in 25:44 for 14th and 15th place. Racing well after taking a week off to rehab a minor lower leg injury, Nicholas Waltman '18 finished in 28th place as the Engineers seventh runner in 26:00. Tech finished with 49 points in the 26 team field, 46 points ahead of second place Connecticut College.</p>
                  <p align="left">Next week MIT will not compete as they prepare for the NEWMAC Conference Championship on Sunday, November 1 at Franklin Park. The men have never lost the title and will be going for their 18th consecutive title. The women have won the past eight titles and hope to claim their ninth.</p>
