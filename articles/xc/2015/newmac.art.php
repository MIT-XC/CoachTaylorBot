                  <p><span class="headline">MIT Sweeps NEWMAC Victories</span></p>
                  <p><strong>Men Claim 18th in Succession, Women Ninth</strong></p>
                  <p>&nbsp;</p>
                  <p align="left">November 1, 2015</p>
                  <p align="left">MIT's nationally ranked Men's and Women's Cross Country teams at 10th and 3rd respectively, entered the <a href="newmac.res.php">NEWMAC Championship</a> favored to win their 18th in a row on the men's side and the ninth in succession for the women. Season long injuries combined with a strong WPI men's team and a nationally ranked women's team from Wellesley posed a serious threat to MIT. The Engineers were not taking anything for granted.</p>
                  <p align="left">Returning to the Tech lineup for the first time this season as he comes back from a stress fracture was All America Matt Deyo '16. To ease Deyo into the competition and to work on group running, MIT was looking for a somewhat slow first mile in 4:50-5:00. MIT was going for a one through five sweep, but with WPI's top runners, who had split MIT's top two runners in two previous races, Tech runners would have to be really tough in the middle of the race.</p>
                  <p align="left">Following directions, MIT posted up behind the lead runners. However, the pace was a bit too slow with MIT runners bunched up between 5:02-5:05. The second mile, which is inherently slower due to Bear Cage Hill, was a strong one for MIT as Rory Beyer '17 took a one second lead over Deyo and two seconds over the rest of his teammates. Both of WPI's runners were with the MIT pack and looked as though they were there to stay. At the three mile mark, Beyer came through in 15:32 along with Deyo and Colin Godwin '16, a trained EMT, who was racing well despite taking a large chunk of time from his warmup to give medical attention to a young lady who had passed out during the women's race. </p>
                  <p align="left">The third mile was the best opportunity to put their foot on the accelerator to drop their opponents, but they instead continued to run 5:15 pace as they did for the more difficult second mile. During the fourth mile MIT's top five runners were still within two seconds of each other, but so too were the runners from WPI. Coming down off their second time on Bear Cage Hill Godwin made a surge to take the lead from Ryan Moran of WPI, which he held to the finish line in 25:35. Beyer also went around Moran as they made the final turn to the finish line, finishing in second in 25:37. In the final fifty meters, Daniel Weiss '19 took over third place in 25:39. Dennis Maloney '19 nearly took fourth place but could not quite get around Moran as he finished in 25:40. Just behind was Deyo in sixth place as he faded in the final mile due to only have started running on Monday.  Claiming five of the top six places fell short of the team's goal, but it was a very impressive showing with only a 12 second delta. MIT's 17 points earned them a 32 point victory over second placed WPI.</p>
                  <p align="left">Godwin earned Runner of the Year honors and Weiss took Rookie of the Year honors, giving the men a clean sweep of available awards. In addition to the top five, other MIT runners in the top 14, thus earning All NEWMAC honors were Cooper Sloan '18 in 8th, Nicholas Waltman '18 in 9th and Alex Knoedler '18 in 10th.</p>
                  <p align="left">The women's race had the same flavor as the men's except Wellesley had a legitimate five runners who could get the job done. While MIT wanted to follow for the first mile, so did the Wellesley runners. The net effect was a very slow first mile with MIT's top runners bunching up between 5:50 and 5:52. Tech runners were more aggressive in the second mile as Maryann Gong '17 led the way in 11:35. Christina Wicker '17 was supposed to be with Gong, but had fallen off badly, coming through in 11:48. Close behind were Nicole Zeinstra '16 in 11:51, Mary Eccles '18 in 11:52 and Leandra Zimmerman in 11:54. In the third mile MIT pulled away from the Wellesley team with the exception of Priyanka Fouda '16, who was hanging with the MIT chase pack. </p>
                  <p align="left">Gong dropped a 5:48 final mile to create nearly a 30 second gap as she cruised to the finish in 18:04 for the slightly long 5K course. Zimmerman ran an excellent last mile to get into an all out sprint with Fouda, but lost at the finish in 18:30. Eccles finished fourth in 18:41. Zeinstra was fifth in 18:43 and Wicker sixth in 18:46. Megan Montgomery '19, still coming back from a hamstring injury, was cautious, but still finished in 10th in 19:01. MIT scored 19 points to 37 for Wellesley in the 11 team field.</p>
                  <p align="left">Gong earned Runner of the Year and Zimmerman Rookie of the Year honors to give the women the same sweep of awards as the men. </p>
                  <p align="left">MIT will not race next weekend, but will race in the NCAA New England Division III Regional Championship / National Qualifier on November 14 at Harkness Park in Waterford, CT. The top two teams earn automatic births to the NCAA Division III Championship in Oshkosh, WI on November 21. Other at-large teams can be earned, particularly in a region as stong as New England's.</p>
