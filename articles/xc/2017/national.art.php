                  <p class="bigtitle">Cross Country Ends Season With Disappointing Performance</p>
                  <p align="left">November 18, 2017</p>
                  <p align="left">Today, MIT's Men's and Women's Cross Country teams competed in the <a href="ncaa.res.php">NCAA Division III Championship</a>, hosted by Principia College in Elsah, Illinois.&nbsp; Both&nbsp;teams were ranked high and expecting to do well after both won, what is considered to be one of the two best regions in the country, last week.&nbsp; Of all the things that went well last week, those same things blew up this week&nbsp;in the biggest race of the season.&nbsp; The men, seeded seventh, and firmly believing they could finish among the top four teams if they repeated their effort from the week before, only had their top two perform well, finishing 16th out of 32 teams.&nbsp; The women, a solid second place seed had three&nbsp;solid performances, which was not enough, finishing fourth, 19 points out of second place.</p>
                  <p align="left">The temperature at the course dropped 25 degrees from the time warmups started for the men to the time the women started their warmup after the men's race, a drop from 64 degrees to 39 degrees. This was accompanied by thirty to forty mile per hour gusts and some light rain.&nbsp; The&nbsp;adversity was what MIT was hoping for, but they did not seem to handle it well.&nbsp; The men, who raced first, looked as though&nbsp;they got out on the uphill start into the wind pretty well.&nbsp; However, by the mile mark, only Dennis Maloney '19 was in an ideal position of 11th place in 4:57 on the uphill mile, which had a rise of 100'.&nbsp; Josh Rosenkranz '19 was out reasonably well along with Aidan Gilson '19,&nbsp;in 94th and 95th place in 5:07.&nbsp; Based on the team's usual spread, both should have been about :05 closer to Maloney, which would have put them in the top 50--a much better position.&nbsp; From here it got much worse.&nbsp; The rest of the team (Alex Knoedler '18, Brian Bates '20, Billy Woltz '20 and Nicholas Waltman '18) came through between 5:10 and 5:13, which had them between 147th and 200th place, much too far to come back from.&nbsp; Woltz had come through in a big way last week, but this start was more like how he had raced prior to that.</p>
                  <p align="left">Nevertheless, they were in eighth place at the two mile mark as Maloney moved to third, Rosenkranz to 71st and Gilson to 73rd.&nbsp;&nbsp;Of the other four, only Knoedler moved up in the second mile, going from 147th to 140th&nbsp;on the downhill mile.&nbsp; It seemed&nbsp;the determination the team showed last week, evaporated when they were caught in the sea of humanity and probably started to lose hope when they were unable to move up as expected.</p>
                  <p align="left">Maloney stayed in position during the third and fourth miles and Rosenkranz continued to move up, reaching 34th place at mile four.&nbsp; Knoedler was inching up, making it up to 117th by the start of the final mile.&nbsp; However, Gilson lost 44 places as he raced similar to how he did on the same course in the Pre&nbsp;National&nbsp;race in October.&nbsp; Bates, Woltz and Waltman also lost places, all sitting between 212th and 231st.&nbsp; During this time the team overall had fallen back to 13th place.</p>
                  <p align="left">In the final mile both Maloney, Rosenkranz and Knoedler lost their forward momentum.&nbsp; Maloney earned his second straight All America award by placing 19th in 24:43.&nbsp; Rosenkranz placed 47th, seven places out of All America in 25:04.&nbsp; Knoedler was 154th in 25:47, his largest delta of the season.&nbsp; Gilson ran about what he had most of the season with the exception of last week where he ran more to his potential.&nbsp;&nbsp;This week he was 157th in 25:47.&nbsp; Bates closed out the scoring, finishing 196th in 26:04.&nbsp; The total delta was 1:21 delta for one through five was the worst of the season.</p>
                  <p align="left">The women did not seem as relaxed as they should have and certainly did not seem excited to go after pre-race favorite Johns Hopkins.&nbsp; Instead they seemed tight and anxious.&nbsp; The attitude that had been there the entire season seemed to be missing, or at least second guessed.&nbsp; Nevertheless, having placed among the top six teams in the country every year&nbsp;since 2009, the women knew what they were doing.&nbsp; They knew they needed to get out better than in the past, getting closer to Johns Hopkins and then moving to catch them in the final two miles.&nbsp; </p>
                  <p align="left">Hopkins did what they do best, getting out pretty well.&nbsp; They had six runners&nbsp;in&nbsp;the top 39 with the first MIT runner being Marissa McPhillips '20 in 47th in 5:52 for the uphill mile.&nbsp; Worse, only Katie Bacher '20 was close to McPhillips in 49th place.&nbsp; Leandra Zimmermann '19 was in 82nd place.&nbsp; Megan McCandless '20 was 102nd and Mary Eccles '18 was 115th.&nbsp; Emily Niu '21 was back at 159th and Katie Collins '21, MIT's top runner here at pre-nationals, was having confidence problems back in 238th.</p>
                  <p align="left">McPhillips, Zimmermann, Eccles and McCandless ran an excellent second mile, moving up to 30th 33rd, 64th and 73rd respectively.&nbsp;&nbsp;Bacher, feeling she had nothing in her legs, fell back to 60th.&nbsp; Nevertheless the team was in a solid second place at this point with 161 points to 99 for Johns Hopkins.&nbsp;&nbsp;In the team scoring, subtracting individuals not with teams, MIT was in 17, 20, 37, 40 and 47 while Hopkins was 2, 14, 18, 30 and 35.&nbsp; MIT was headed in the right direction if they could keep moving up and Bacher could fix her&nbsp;backslide.</p>
                  <p align="left">McPhillips, Eccles and McCandless did indeed move up more, but Zimmermann lost six places and Bacher 50.&nbsp; Regardless, MIT was still in second.&nbsp; Hopkins now had 108 points and MIT 190.&nbsp; However, Wisconsin Eau Claire was closing hard at 212 points and the University of Washington, St. Louis was moving up with 238 points</p>
                  <p align="left">In the final .73 of the 6K course, McPhillips again had a problem staying on her feet, as has also happened in three other races this fall, losing 41 places as she fell at least once, barely making it to the finish before collapsing in 69th place.&nbsp; Zimmermann moved back up to 30th to claim her first All America honor.&nbsp; Eccles moved up to 53rd and McCandless to 58th, her best race of the season.&nbsp; Bacher maintained her position in 111th place.&nbsp; Unfortunately, both UW Eau Claire and Wash U passed MIT, but Tech still maintained a podium position in fourth place.</p>
                  <p align="left">All MIT athletes performed at their best on this day.&nbsp; Their mental weaknesses were exposed, which meant their best was no where near their potential.&nbsp; Coach Halston Taylor, thinking he had his teams prepared, obviously failed to get them ready to race their best.</p>
                  <p align="left">The men and women both graduate only one of their top five.&nbsp; There are quite a few runners waiting to step up next year.&nbsp; Perhaps next year they can run without fear.</p>
