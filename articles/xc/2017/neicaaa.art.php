                  <p class="bigtitle">MIT Outperforms Competition at NEICAAA Championship</p>
                  <p align="left">October 7, 2017</p>
                  <p align="left">In the summer that would never end, MIT competed in the <a href="neicaaa.res.php">NEICAAA Championship</a> at Franklin Park in Boston amid mid-80 degree temperatures under sunny and humid conditions.&nbsp; Fortunately this effects everyone and the MIT runners feel confident when facing adversity.&nbsp; Both the men's and women's teams did very well, the women placing third overall despite not running their top runner from their first race, Katie Bacher, defeating all Division II and III teams, losing only to the University of New Hampshire and Boston College.&nbsp; The men, running intact, placed second despite not having the&nbsp;best race from three of their top runners, losing by a mere five points.</p>
                  <p align="left">The NEICAAA Championship is made up of all Division I, II and III teams in New England.&nbsp;Some teams choose not to attend in favor of large invitationals the previous week or the week after.&nbsp; However, for the most part it is still a very competitive meet.&nbsp; The men's race had 27 teams while the women's had 28 teams compete.</p>
                  <p align="left">Since the varsity races only permit seven runners each, sub-varsity races allowed teams to put their remaining runners in&nbsp;a race, which this early in the season is necessary in determining who the ultimate top seven will be at the end of the season.&nbsp; The women's sub-varsity race was first.&nbsp; Tessa Weiss '20, led the way for MIT, placing 11th in the race in 19:31.&nbsp; Weiss went out a bit too fast for the weather conditions, particularly the first half mile.&nbsp;By the mile mark she was already fatigued.&nbsp; Given the start of the race, Weiss competed well, coming back in the third mile.&nbsp; Emily Niu '21, racing for the first time, raced well, finishing 19th in 19:39.&nbsp; Her lack of training due to a previous injury showed in the final mile.&nbsp; Margaret Trautner '20 had a breakout race, placing 25th in 19:49.&nbsp; Overall the sub-varsity women finished fifth with 138 points.</p>
                  <p align="left">The men's sub-varsity team had an outstanding performance, finishing second, just six points behind the winning team, UMass Lowell, a&nbsp;Division I team.&nbsp; Alex Knoedler&nbsp;'18, returning from injury, is still at relatively low mileage, but showed his competitiveness by going with the leaders right from the start.&nbsp; Knoedler and David Walter '18 stayed among the top runners for much of the race.&nbsp; Knoedler finished in sixth in 26:30 and Walter in seventh in 26:33.&nbsp; Both went out&nbsp;a little fast for the conditions.&nbsp; Even though they paid for it, they continued to race with focus and determination.  </p>
                  <p align="left">In the women's varsity race, the pace was quick given the temperature.&nbsp; The leaders came through the mile in 5:22.&nbsp; Mary Eccles '18 led the Engineers with a 5:39 first mile, which put her in 43rd place.&nbsp; Marissa McPhillips '20 and Megan McCandless '20 were right behind her and Leandra Zimmermann '19 led teammates Katie Collins '21, Jenna Melanson '20, and Lindsey McAllister '21 at 5:43, which put them between 60th and 70th place.&nbsp; By the two mile, Zimmermann had moved into 10th place in 11:39, which included  Bear Cage Hill at about the 1 1/2 point.&nbsp; Eccles moved up to 23rd and McPhillips up to 25th place.&nbsp; Collins was up to 33rd position, with McCandless at 45th, running in trainers to protect her foot, and Melanson in 49th.&nbsp; McAllister was back in 71st place after suffering through a 6:26 mile.&nbsp; The third mile saw Zimmermann lose a few places, finishing in 13th in a personal best 18:34.&nbsp; McPhillips moved up to 18th in 18:30 while Eccles held her 23rd place in 18:37.&nbsp; Collins ran an excellent race for a freshman, placing 24th in 18:39.&nbsp; McCandless, not really race ready, faded to 53rd in 19:08 and Melanson was 55th in 19:11.&nbsp; McAllister finished in 68th in 19:21.</p>
                  <p align="left">The men were determined to go after this race&nbsp;and got carried away a little with a start that was a bit faster than necessary.&nbsp; By the mile mark they were slowing down instead of speeding up or maintaining.&nbsp; Dennis Maloney '19 was in 29th place at 4:49.&nbsp; Josh Rosenkranz '19, Aidan Gilson '19, Billy Woltz '20 and Kent Slaney '21 were between 49th-53rd in 4:52.&nbsp; Nate Foss '20 was 58th in 4:53 and Nicholas Waltman '18 was 84th in 4:58.&nbsp; By the end of the second mile Maloney was in third, about twenty meters from the two leaders&nbsp;in 9:56.&nbsp; Gilson, Rosenkranz and Slaney were closing in&nbsp;towards the front of the chase pack in 28th 29th and 30th in 10:10.&nbsp; Woltz was back in 55th and Foss in 60th in 10:17, convincing themselves they could make it up later, which was not likely to happen in that heat.&nbsp; Maloney was still in third at mile three, but was now about thirty meters out of second and first was no where in sight.&nbsp; Gilson moved up to 12th and Rosenkranz was in 16th close behind.&nbsp; Woltz had made a move to 38th place, but Slaney had slipped to 45th as he had been heel striking for the past two miles.&nbsp;&nbsp;Foss was still in 60th, but looking pretty comfortable.&nbsp; The trends continued for the final two miles.&nbsp; The chase group caught up with Maloney in the final mile, but he held on for sixth place overall in 25:30.&nbsp; Rosenkranz moved up to 14th in 25:40 and Gilson slipped to 17th in 25:42, a huge PR for the junior.&nbsp; Woltz continued to move up, placing 34th in 26:05 and Foss woke up a little to finish in 26:26 in 53rd place.&nbsp; Losing the meet by just five points knowing they did not race their best had to be frustrating to the Engineers.&nbsp; Nevertheless they should be pleased with their best finish in this race in over 60 years.</p>
                  <p align="left">Next week, the top 10 men&nbsp;and women will represent MIT at the Cowbell Classic, the Pre-National race for Division III at Principia College in Alton, IL.&nbsp;&nbsp;Many of the top teams in the country will be there including the top three in the women's race and the top two in the men's race.</p>
