                  <p class="bigtitle">Bacher Leads Women to Victory, Men Fail Without Maloney</p>
                  <p align="left">September 16, 2017</p>
                  <p align="left">MIT&nbsp;Men's and Women's Cross Country traveled to the western part of the state to race in its first intercollegiate meet of the year, the <a href="purplevalley.res.php">Purple Valley Classic</a> hosted by Williams College.&nbsp; The women won the 6K handily against an impressive field despite not racing last year's #1 runner, Megan McCandless '20.&nbsp; The men, not running any of their top seven from last year, did plan on racing #1 returner Dennis Maloney '19.&nbsp; However, he came down with an ear infection and did not race.&nbsp; His presence alone would have had the team finishing second.&nbsp; If Nate Foss '20, running in a comfortable front position through four miles had not had difficulty with the 85 degree heat, the Engineers would have won.&nbsp; As is the 8th ranked Engineers finished a disappointing sixth&nbsp;in a stellar field that included Nationally ranked #2 SUNY Geneseo, #4 Williams,  #14 RPI, #15 Amherst #18 Middlebury, and #24 Dickenson among the 26 entered teams.</p>
                  <p align="left">The heat and cloudless sky had everyone looking for shade prior to race time.&nbsp; The men raced first&nbsp;and right off Josh Rosenkranz '19, Kent Slaney '21, Billy Woltz '20, Nate Foss '20 and Aidan Gilson '19 positioned themselves just behind the front pack, running a comfortable pace.&nbsp; Through the first three miles all five were close together, never more than seven seconds apart.&nbsp; Ben Freed '18 moved up in the second mile to almost join the group, but then fell back in the third mile.</p>
                  <p align="left">In the fourth mile, Gilson started thinking too much and lost the desire to win, falling back 22 seconds behind Rosenkranz.&nbsp; Still, at the four mile mark the front four were within seven seconds of each other.&nbsp;  Foss was overwhelmed by the heat in the fifth mile, actually passing out at one point in the final mile.&nbsp; He finished on his own, but ran a 6:35 final mile.&nbsp; </p>
                  <p align="left">Rosenkranz finished 10th on the hilly, challenging course in 26:41.&nbsp; Slaney fell off a bit in the final mile to finish 17th in 26:53 followed by Woltz at 19th in 27:00.&nbsp; Gilson was the fourth Tech runner, fading to 42nd in 27:25 with Freed closing out the scoring, finishing 56th in 27:41.&nbsp; Foss would have certainly been in the top 25 and Maloney in the top five.&nbsp; Still working on returning to competition are top seven runners from last year, Alex Knoedler&nbsp;'18, Daniel Weiss '19, Josh Derrick '20 and Steven Goldy '20.&nbsp; When Tech competes in the Pre-National Championship in three weeks they should have a different looking top seven.</p>
                  <p align="left">The women, ranked #3 in the country, were up against #4 Williams, #5 SUNY Geneseo, #7 Ithaca, #8 Middlebury, #12 RPI, #16 Dickinson, #17 St. Lawrence, #26 Tufts and #35 Rochester.&nbsp;&nbsp;The plan was to hold back a bit and run a group race behind the main pack, which meant the top three runners were able to run  far in front of the pack with none of our runners going after them.</p>
                  <p align="left">At the mile mark, the MIT pack was slightly trailing the chase pack with Katie Bacher '20, Marissa McPhillips '20 and Mary Eccles '18  at 5:56 running comfortably.&nbsp; Just off that lead chase group were Leandra Zimmermann '19, Katie Collins '21, Tessa Weiss '20, Lindsey McAllister '21 and Jenna Melanson '20, all between 6:00 and 6:01.&nbsp; All was looking pretty good for the Engineers.</p>
                  <p align="left">Coming through two miles a little separation occurred with Eccles falling off :03, 12:17 to 12:14 for Bacher and McPhillips.&nbsp; Collins stayed aggressive at 12:24, but the rest of her group did not stay with her.&nbsp; Zimmermann was 12:30 with McAllister at 12:33, Weiss at 12:37&nbsp;and Melanson at 12:38.&nbsp;Overall the group stayed together well given the large hill at the end of mile two.</p>
                  <p align="left">At mile three, Bacher and McPhillips pulled away, running 5:48 for the mostly downhill mile.&nbsp; Eccles lost :10 to her teammates at 5:58.&nbsp; Zimmermann had&nbsp;a good mile, almost catching back up to Collins.&nbsp; Weiss and McAllister ran well, but lost at least 17 seconds on the mile.&nbsp; </p>
                  <p align="left">In the final .72 Bacher pulled away, from the chase group, coming within :05 to catching third place, finishing in fourth in 22:17.&nbsp; McPhillips closed well until the final straight where she looked as though she might collapse.&nbsp; She lost :31 to Bacher in the final leg of the course, finishing in 10th in 22:48, one place behind Eccles in 22:47.&nbsp; Zimmermann closed well, finishing in 14th in 22:57.&nbsp; Collins closed out the scoring in 18th in 23:08.&nbsp; The five finished with a score of 55 points, 36 points ahead of McGill University from Canada and 76 points ahead of #5 ranked Geneseo and 91 points ahead of #4 ranked Williams.</p>
                  <p align="left">All in all it was a pretty good day for the Engineers.&nbsp; The next race for MIT is in two weeks at the NEICAAA Championship where all Division I, II and III teams in New England will vie for bragging rights as the top team, regardless of Division.</p>
