                  <p class="bigtitle">Varsity Outduels Alumni in Annual Meet                  </p>
                  <p align="left">September 2, 2017</p>
                  <p align="left">On a sunny, 70 degree day, both the women's and men's varsity cross country teams defeated the &quot;Has-Beens&quot; in the annual <a href="alumni.res.pdf">Alumni/ae Meet</a> held at Franklin Park, although both alumni teams showed significant improvement over recent years.&nbsp; The women alums had nine racing while the men had 14 competing runners.&nbsp; The varsity women placed 1-2-3-5-7, led by Marissa McPhillips '20, tying for the 11th fastest time ever at Franklin Park by an MIT runner with her 18:30&nbsp;performance.&nbsp; The final score was varsity 18, alumni 41.&nbsp; The men were led by Dennis Maloney '19 who was edged at the line by Matt Deyo '16, both timed in 15:31.&nbsp; The varsity placed 2-3-4-5-6 for 20 points to 43 for the alums.</p>
                  <p align="left">The women's race started out fast as Katie Bacher '20 attempted to dominate the race from the front.&nbsp; Her 5:45 first mile was quick for this time of year, but McPhillips, Mary Eccles '17 and Jenna Melanson '20 were right with her as was Christina Wicker '17 representing the alumnae.&nbsp;&nbsp;Supporting chase runners, Katie Collins '21 and Lindsey McAllister '21 came through in 5:50 and 5:51 respectively.</p>
                  <p align="left">By the second mile mark Bacher had a nice six second lead over McPhillips after a 6:00 clocking on the hilly mile as the group broke up.&nbsp; Wicker moved past Melanson as she slowed to a 6:17.&nbsp; Brooke Johnson '13 had moved past McAllister and Louise van den Heuvel '14 was on McAllister's heels.</p>
                  <p align="left">In the final 1.1 mile Bacher seemed to let her thoughts of being caught get the best of her as McPhillips moved past her in the final 300 meters.&nbsp; Bacher ran 18:33 for a big personal best at Franklin Park and second place overall.&nbsp;&nbsp;Eccles ran&nbsp;a course&nbsp;PR of 18:37 for third place.&nbsp; Wicker placed fourth in 18:57.&nbsp; Melanson was fifth overall in 19:05. Johnson was sixth in 19:08 and Collins closed out the scoring with her 19:19.&nbsp; </p>
                  <p align="left">The varsity women were missing a couple of their top runners who should both be competing in their next meet.&nbsp;&nbsp;This should close the :49 gap one through five.</p>
                  <p align="left">Closing out the scoring for the alumnae were van den Heuvel in ninth in 19:26, Alina Gatowski '11, placing 10th in 19:52, and Alexandra Taylor '14 placing 15th in 20:41. Other alums racing were Elaine McVay '15, Claire O'Connell '14, Maggie Lloyd '12 and Stephanie Marzen '15.</p>
                  <p align="left">The men's pace was also quite fast as Maloney pushed the pace, leading the first mile in 4:53.&nbsp; In addition to Deyo trailing right behind were Aidan Gilson '19, Billy Woltz '20 and Kent Slaney '21.&nbsp; The chase group was spread out over a four second gap, but were quite close (4:55-4:59).&nbsp; Included in the group were Ben Freed '18, Nate Foss '20, Josh Rosenkranz '19, Dylan Doblar '21, David Walter '18, Noah Zamzow-Schmidt '21, Nicholas Waltman '18 and Jake Jorgensen '21.</p>
                  <p align="left">Maloney pushed the second mile, delivering a 5:07 to open up a four second lead on Deyo.&nbsp; The rest of the group also broke up as Woltz ran an aggressive mile, but Gilson and Slaney slowed too much.&nbsp;Rosenkranz and Foss moved past Freed and looked ready to keep on moving through the field.</p>
                  <p align="left">Deyo took the lead coming out of the Wilderness Loop and with a tenth to go had a 10 meter lead.&nbsp; Maloney closed the gap, taking the lead in the final straight.&nbsp; However, in the final 20 meters Deyo took the lead for good.&nbsp; After Maloney, Gilson placed third, putting together a strong third mile to run 15:53.&nbsp; Woltz closed strong to finish right behind in 15:55.&nbsp; Foss and Rosenkranz did indeed keep moving, running 5:10 for the final mile to place fourth and fifth on the team in 15:56 and 15:57 respectively.</p>
                  <p align="left">The varsity men displayed a 36 second delta through seven runners, although the distance was only a 5K.&nbsp; Many top runners were held out for various reasons.&nbsp; Their return should provide the quality and depth to keep the seventh ranked team in the country for Division III towards the top of the best teams.</p>
                  <p align="left">Scoring for the alumni were Justin Bullock&nbsp;'14, placing 12th&nbsp;in 16:32, Colin Godwin '17, placing 13th in 16:35, Roy Wedge '14, placing 15th in 16:59 and Allen Leung '15, placing 19th in 17:32.&nbsp; Others who ran were Logan Trimble '13, Jared Forman '13, Eric Khatchadourian '06, Joe Roy-Mayhew '08, Terry McNatt '87, Bob Collins '82, Gihan Amarasiriwardena '11 and Peter Mulligan '08.&nbsp;Supporting alums were Ethan Crain '96, Tom Hansen '74 and Sarah Quinn '17.</p>
                  <p align="left">The varsity got the job done but need to work on their race focus, spread of runners one through five and eliminating the mental obstacles prior to their first intercollegiate race, the Purple Valley Classic on September 23&nbsp;hosted by Williams College.                  </p>
