                  <p><span class="headline">Women Place Third, Men Ninth at NEICAAA Championship</span></p>
                  <p align="left">October 11, 2014</p>
                  <p align="left">The weather helped, along with a very competitive field to produce outstanding cross country conditions at the <a href="neicaaa.res.php">NEICAAA Championship</a>. With temperatures in the low 50's all day, a constant rain soaking the course, producing more and more mud as the races progressed through the day was perfect for cross country. This is the one race in the New England Cross Country season when all the teams in New England (Division I, II and III) get to compete against each in the same race, head to head. The combination of the two may not have produced the fastest times, but if you love cross country this was a great day.</p>
                  <p align="left">MIT seized the opportunity, exhibiting excellent competitiveness in all four races (women's varsity, women's sub-varsity, men's varsity, men's sub-varsity). The women's varsity were hoping for nicer weather so they would have an opportunity to compare their performances with what Johns Hopkins (defending Division III NCAA Champion) ran on the course two weeks prior. However, in addition to the weather, both Maryann Gong '17 and Elaine McVay '15 came down with colds the past two days. Nicole Zeinstra '16 had also been ill and unable to eat well. However, the team showed their desire to be the best by not letting anything distract them from their goal. </p>
                  <p align="left">They received box 40, which put them on the far right. In fact they were so far right that they had to angle to the left to get into position for the first right hand turn less than 250 meters from the start. This course is not properly surveyed, so those on the right have a distinct disadvantage and must also keep from getting blocked out by the teams from the left positioning themselves for the right hand turn. The team got out very well across the rain soaked field, putting themselves in perfect position without wasting too much energy. At the 800 mark, Sarah Quinn '16 was a little fast at 2:46, but the rest of the team was right where they wanted to be between 2:50-2:55. </p>
                  <p align="left">At the mile mark, Quinn and Gong came through in 38th and 43rd respectively in 5:37 and 5:38, right on plan. Christina Wicker '17, McVay and Zeinstra were back in the mid 5:40's in about 100th place, while Cindy Huang '15 was around 140th at 5:52. At mile two they had maintained their effort, which had them passing many people who had gone out too fast for their ability. So many in fact that Quinn had moved up to 18th, Gong to 27th, Wicker to 34th, Zeinstra to 53rd, Huang to 55th and McVay to 65th. In the final mile and a tenth, Quinn moved a little too quickly, causing her calves to seize up and forcing her to slow quite a bit, fading back to 28th after moving up into the top 10. Gong had an excellent finish, closing to 12th in a new MIT record for Franklin Park at 18:02. Wicker moved up slightly from the two mile, finishing 31st in 18:17. Huang had the race of her life, finishing in 47th with a phenomenal 18:29, a full minute PR. McVay closed out the scoring, finishing 57th in 18:35 as she battled a strained anterior tibialis in addition to her cold. McVay is typically about 20 seconds faster on this course. Zeinstra was also close, placing 62nd in 18:38. This 36 second delta and incredible times highlighted the best performance in MIT history, placing them third among the 38 teams.</p>
                  <p align="left">In the sub-varsity race, Liz Cox '18 led the way for Tech, finishing 11th in 19:02. Kali Benavides '15 was 17th in 19:07 and Mary Eccles was 33rd in 19:19. Also running a huge personal best was Ciera Gordon '18, finishing in 19:44.</p>
                  <p align="left">By the time the men took to the course it was getting very muddy, particularly on the turns. It was going to be more difficult to make up ground, so a strong start was critical. Similar to the women, the men were on the far right, box 36. Unlike the women, the men did not heed the warning of getting out and pretty much got buried on the first turn, almost coming to a stand still. </p>
                  <p align="left">By the mile mark they were approximately 10 seconds too slow, which put them in a position that made it impossible to achieve their goal places or times. Spencer Wenck '15 led the way in 115th place. Matt Deyo '16, Benji Xie '15 and Colin Godwin '17 were within a few places. Back at around 160th were Rory Beyer '17, Allen Leung '15 and Cooper Sloan '18.</p>
                  <p align="left">At the two mile mark the team moved up significantly. Wenck was still leading the way for the Engineers at 63rd place. Xie was in 86th, with Godwin and Deyo at 97th and 98th respectively. Beyer moved up the most, all the way to 107th with Sloan right behind in 109th. Leung was close behind at 118th. The third mile started catching up with Tech runners. Wenck continued to move up well, closing to 39th. Xie slowed his move through the ranks of opposing runners, but was still gaining ground at 67th place. Deyo moved up to 83rd, Beyer to 84th and Godwin to 86th. Leung slid up to 103rd, but Sloan fell back to 114th.</p>
                  <p align="left">Xie had the most productive fourth mile, rebounding well to move up to 52nd. Wenck was not backing off either, continuing to blow past runners as he passed the four mile mark in 25th place. Deyo moved up four spots to 79th and Beyer stayed the same at 84th. Godwin lost two places, Leung moved up four and Sloan lost 15 as this is only his second 8K ever.</p>
                  <p align="left">At the finish, Wenck ended up in 17th place in 24:58, moving almost 100 places in four miles. Xie finished in 41st in 25:15. Deyo placed 70th in 25:29. Godwin and Beyer were 84th and 86th respectively in 25:39 and 25:40 to close out the scoring for MIT. Leung was not far behind in 25:50 and Sloan refocused to finish in 26:04. The 298 points placed Tech ninth out of 39 teams and second among Division III teams, just seven points back of Middlebury. A reasonable start across the field would have placed the team in sixth as sixth place had just 51 fewer points.</p>
                  <p align="left">In the sub-varsity race, Matt Jordan '15 made his return from injury a successful one as he led the team with a 17th place in 26:07. Also racing well were freshmen Ben Freed and Michael Picchini, running 26:14 and 26:15.</p>
                  <p align="left">Next week, MIT will travel to Oshkosh, Wisconsin to race some of the top teams in the country. The women will be out to prove they are in contention for one of the top teams in NCAA DIII, while the men need to put a complete race together, which should prove to them and the rest of the country that they are a solid top 10 team in NCAA Division III.</p>
