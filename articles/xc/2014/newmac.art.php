                  <p style="font-weight: bold; font-size: 18px">Wenck, Quinn Lead MIT Sweeps in NEWMAC Championship</p>
                  <p align="left">November 1, 2014</p>
                  <p align="left">MIT's Men's and Women's Cross Country teams traveled to Smith College with the intent on defending their New England Women's and Men's Athletic Conference <a href="2014 NEWMAC Cross Country Championship.html">(NEWMAC) Championship</a> title and to extend their streak of consecutive NEWMAC titles to 17 for the men and eight for the women. Not only did the Engineers accomplish their goal, but won in dominating fashion by overwhelming their competition with sweeps in both races for perfect 15 point totals.</p>
                  <p align="left">The men had not swept since the first year of the NEWMAC Championship back in 1998 and then it was only the top five places. This year's harrier version took the first seven places. The women had swept once, the previous time Smith hosted (2012) where Tech took the top eight places. This was certainly the first time both men and women had swept in the same year.</p>
                  <p align="left">The #11 nationally ranked men were first off the line on the cold and windy day. The plan was to go out with reasonable aggressiveness on the somewhat hilly and winding first mile, hitting the mile mark between 5:15-5:20 and negative splitting the course from that point on, which was certainly a tall task. Spencer Wenck '15 took the lead and control of the race, splitting 5:12 at the mile mark. By the two mile mark, which contains a major hill, Wenck was with teammate Colin Godwin '17 in 10:27 with Matt Deyo '16 close behind. A WPI runner trailed Deyo with co-captain Benji Xie '15 right on his heels in 10:30. Rory Beyer '17 and Allen Leung '15 were very close to Xie in virtually the same time. Another WPI runner was just behind Leung with co-captain Matt Jordan '15 about three seconds back in 10:33 with Cooper Sloan '18 a second back. Then there was a pretty big gap as a few runners were strung out behind.</p>
                  <p align="left">Watching this part of the race one could not help but think, could MIT really hold this pace. The third mile was flat and the Engineers were relentless, continuing to hammer the pace. Wenck ran 4:49 to come through three in 15:16, leading his teammates to what was now first through sixth places. Deyo, suffering from a cold and lack of sleep fell back to sixth and the first WPI runner fell back to seventh. Jordan had passed the second WPI runner to move into seventh overall. Cooper, feeling sick, fell back, ending the chance of a 1-8 sweep.</p>
                  <p align="left">The second large hill is in the fourth mile. Although the pace slowed, the effort increased with Tech runners determined to put the hill away. Wenck threw down a 5:07 mile and the rest of MIT's top seven were between 5:08-5:18. The order was the same with the exception of Jordan moving into seventh place and widening the gap between him and his opponents.</p>
                  <p align="left">The final mile was a reversal of the first mile, winding steep uphills through the woods followed by a downhill then flat final 600 meters. Wenck widened his gap with a 4:50 final mile as did his teammates who ran the final mile between 4:53 and 5:04 to secure the 1-7 sweep. The delta was a ridiculous low of 24 seconds through the top five and 40 seconds through seven. To put the quality of this effort into perspective, two years ago when MIT won, their top runner was Roy Wedge '14 in fourth place in 26:02 with better weather conditions. Jordan, Tech's seventh runner this year, finished in 25:53. MIT had four runners faster than the winning time from 2012.</p>
                  <p align="left">In addition to the top seven, Cooper Sloan and Nicholas Waltman earned All Conference honors, finishing 12th and 14th respectively.</p>
                  <p align="left">Spencer Wenck earned NEWMAC Runner of the Year, while Colin Godwin picked up the Rookie of the Year award.</p>
                  <p align="left">By the time the women's race got underway the rain had begun to fall. There was a bit of concern in the air as tri-captain Elaine McVay '15 had difficulty sleeping the night before, tri-captain Cindy Huang '15 was unable to race due to a foot injury acquired during the week and Anna Frederich '18, preparing for her season debut, missed the bus.</p>
                  <p align="left">Similar to the men, the #4 nationally ranked women were supposed to be a little aggressive at the mile mark and then increase their effort to run negative splits to the finish line. For some reason, the pace bogged down in the first half mile, with the leaders at 3:18. The Engineers finally broke away from the huge pack, getting to the mile mark in 6:15, about 15-20 seconds slower than was planned. Nicole Zeinstra, Kali Benavides '15, and Frederich (who got to the race about 40 minutes before the start of the race) were close behind at 6:20</p>
                  <p align="left">At the second mile things had opened up quite a bit with Christina Wicker '17, tri-captain Sarah Quinn '16, McVay and Maryann Gong '17 leading the way at 12:18. Nicole Zeinstra '16 ran a 6:00 mile to move within two seconds of the lead pack. Benavides and Frederich were unable to go with Zeinstra, falling back 16 seconds with the hill taking some of the speed away from the runners.</p>
                  <p align="left">In the final mile the women really opened up with Quinn pulling away with a 5:30 mile to come through three miles in 17:48, finishing with the victory in 18:22. Wicker finished three seconds back in 18:25. McVay put her sleep worries aside and stepped up the way she always does, finishing in third in 18:31. Gong, not having her best race, still finished fourth in 18:37. Zeinstra did a marvelous job of running by herself, but always within sight of her teammates, finishing fifth in 18:41 to complete the sweep. </p>
                  <p align="left">The 19 second delta was amazing by any standards, given the pace of the race, particularly the final two miles and the weather conditions. Quinn took NEWMAC Runner of the Year with her victory.</p>
                  <p align="left">Finishing sixth for MIT was Benavides in 15th place in 19:09. Mary Eccles '18 missed Rookie of the Year honors by :06 as she finished 19th in 19:24. Frederich was just behind in 20th place in 19:31.</p>
                  <p align="left">The Engineers are off next weekend, next competing on November 15 at the NCAA Division III New England Regional Qualifier hosted by Williams College. The top two teams in each of the eight regions will automatically qualify for the NCAA Division III Championship on November 22 in Mason, Ohio. 16 at-large teams will be selected from all eight regions to complete the 32 team field.</p>
