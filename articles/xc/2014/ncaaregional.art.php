                  <p style="font-weight: bold; font-size: 18px">Quinn Leads Team to NCAA Regional Title</p>
                  <p style="font-weight: bold; font-size: 18px">Wenck Earns Runner-up, Men's Team Claims Third</p>
                  <p align="left">November 15, 2014</p>
                  <p align="left">Going into the <a href="ncaaregional.res.php">NCAA Division III New England Regional Championship</a> hosted by Williams College at the Mt. Greylock High School course, the MIT men's team, ranked eighth nationally, was ranked 3rd behind Williams College, ranked fourth nationally, and Colby College, who was ranked sixth nationally. The incredibly strong New England region also boasted 12th ranked Middlebury, 16th ranked Amherst, 24th ranked Tufts, 30th ranked Bowdoin and 35th ranked Bates College among the 54 teams competing in the race to advance to the NCAA Championship next weekend. There are only two automatic bids per region (eight regions) and then 16 additional at-large teams are selected throughout the eight regions.</p>
                  <p align="left">MIT's women's team, ranked fourth nationally, entered the meet seeded second behind Middlebury College, ranked third nationally. Host Williams College was seeded sixth nationally, Amherst 21st, Colby 28th and Tufts 30th among the 57 competing teams.</p>
                  <p align="left">The weather was a little chilly, but otherwise perfect at 30 degrees and sunny with no wind. The men's race was first. With 54 teams spread across the line, each team only had room for two or three individuals at the front of each box. The sheer numbers normally would make the pace fast as the course narrows significantly prior to the mile mark, making it difficult to pass. MIT ran their own race, and that put them out front a little earlier than they were hoping for. Spencer Wenck '15, Matt Deyo '16, Colin Godwin '17 and Rory Beyer '17 all hit the mile mark together at 5:03 and they were the front pack. Benji Xie '15, Allen Leung '15 and Matt Jordan '15 were all among the top 30 runners at the mile mark, coming through in 5:07, 5:09 and 5:10 respectively.</p>
                  <p align="left">The relatively slow pace was due to the respect paid by the runners for the hills that would come in the second and fourth miles. Mile two was the slowest mile for the leaders as no one wanted to take it away from the Engineers and they were happy with how it was going. Wenck and company were 10:20-10:21, still in front, with their teammates about the same distance back as the first mile, but having moved up a few places. Mile three opened things up a little and more of the favorites joined the fray up front. Wenck put down a 5:02 mile to stay in the lead and Deyo and Godwin were right behind. Beyer fell off a little. Xie ran 5:01 to move to 15:29, just two seconds behind Beyer and seven behind Wenck for a very nice three mile, five man delta.</p>
                  <p align="left">The fourth mile was fast given the hill and this broke up the lead group. Wenck ran 5:03 but gave up the lead to Colin Cotton of Williams, who flew up the hill. Deyo ran 5:11 and Godwin 5:12 to lose some ground and places. Beyer ran 5:23, Xie 5:20, Leung 5:26 and Jordan 5:29, opening up the possibility for other teams to have a shot at the victory. The final mile was more of the same as Wenck ran a remarkable 4:50 to finish in second place in 25:15, just five seconds behind Cotton and 11 seconds in front of third. Deyo finished hard to claim eighth in 25:38. Godwin was close behind in 12th in 25:42. Beyer came back well to take 19th place honors in 25:57. Xie finished in 29th in 26:10 to close out the scoring for MIT at 70 points. When the smoke settled it turned out to not be quite enough as Colby College won with 63 points and Amherst surprising all to take second with 69 points. MIT had the best depth in the race as Leung finished 33rd in 26:17 and Jordan was 39th in 26:27. MIT laid claim to six All Region honors as the top 35 earned that distinction. There is very little doubt that MIT will not go to Nationals as an at-large team.</p>
                  <p align="left">The women started an hour and a half after the men. They hoped to be just as aggressive in their race. At the start, which was very fast, Middlebury runners seized the lead and then instantly slowed the pace. MIT's Elaine McVay '15, Sarah Quinn '16, Christina Wicker '17 and Maryann Gong '17 would have nothing of it and went right around, ignoring their competition. The foursome hit the mile mark in 5:48, a crisp mile indeed. Nicole Zeinstra '16, who was nursing an injured quadricep, was racing as if she had no pain, leading the chase group at the mile in 5:51. Kali Benavides '15 and Mary Eccles '18 came through 6:01, and very much in the race.</p>
                  <p align="left">Mile two was exactly the same pace for the lead foursome. The chase group, which had fallen back even more, began to realize that these ladies were not coming back. Zeinstra ran 5:52 to fall back a little, but Benavides sped up, running 5:58 to move up a few spots. Eccles ran another 6:01, still gaining ground on some who went out too fast for their physical or mental ability.</p>
                  <p align="left">In the third mile, Quinn opened up on the major hill with a 5:47 and put away the competition. Unfortunately, this may have had a negative affect on her teammates as they began to fall back with the hard move. Wicker managed a 6:04 mile, McVay a 6:09 and Gong a 6:10. Zeinstra's quad would not respond on the hill, causing her to slow to a 6:21. Benavides ran 6:16 and Eccles a 6:27 on the difficult hilly mile.</p>
                  <p align="left">Quinn finished in first, setting the course record in 21:39, a nine second victory. Previously, only seven runners had ever broken 22:00 on the Williams course. Today there were six more and MIT had three of them. Wicker came back strong to finish fifth in 21:57 and McVay was sixth in 21:59. Gong regained focus to finished hard in 22:06 for eighth place. Zeinstra held on for 20th place in 22:32, Benavides was 26th in 22:42 and Eccles was 42nd in 23:03. This gave the women's team six All Region honors as well.</p>
                  <p align="left">The amazing finish totaled 40 points, giving the Engineers their third regional title in the last six years, and this in the best region in the country.</p>
                  <p align="left">Both the men's and women's teams will be traveling to Mason, Ohio next week where Wilmington College will host this year's NCAA Division III Championship.</p>
