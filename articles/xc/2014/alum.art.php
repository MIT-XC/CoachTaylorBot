                  <p><span class="headline">Varsity Runners Exhibit Tight Deltas in Sweeps over Alums</span></p>
                  <p align="left">August 30, 2014</p>
                  <p align="left">In the first race of the season, MIT raced their Alums in <a href="alum.res.php">short course races</a> at Franklin Park, MIT's home course. The women had a seven second delta for runners one through five on the 1.94 mile course to sweep their older counterparts 15-42. The men raced a 5K, taking the top seven places with a 15 second delta to win 15-50.</p>
                  <p align="left">With 80 degree temperature and a bright sunny day, the one thing that made it an acceptable cross country race day was the relatively low humidity. The women raced first. The pace across the field was fast, guaranteed by the sprint across the field by guest runner Abby Klein '14. At the mile mark, Maryann Gong '17, Christina Wicker '17, Anna Frederich '18, Elaine McVay '15 and Nicole Zeinstra '16 led the way at 5:42 with Alexandra Taylor '14, Kaitlin Allen '14, Martina de Geus '14 and Jennifer Doyle '09 trailing for the Alumnae.</p>
                  <p align="left">Wicker pushed the pace in the second mile, opening up a large lead as they came off of Bear Cage Hill and onto the field. However, Gong had a furious kick to take the victory in 11:13 to Wicker's 11:15. Frederich was right behind in 11:16, McVay in 11:19 and Zeinstra in 11:20 to complete the sweep.</p>
                  <p align="left">In most years, the collective performance by the Alumnae would seriously contend for the victory, but not this year. Taylor placed 6th in 11:30, Allen 7th in 11:35, de Geus in 8th at 11:38 and Doyle 9th in 11:40. Alina Gatowski '11 completed the scoring the alums, finishing in 18th in 12:17. Also competing for the alums were Louise van den Heuvel '14, Maggie Lloyd '12 and Alisha Schor '07.</p>
                  <p align="left">Benji Xie '15 did not hold back, taking the lead from the gun. At the mile mark Xie held a five second lead thanks to pushing the second half of that mile in an effort to steal the race. At the two mile mark, Spencer Wenck '15 led the charge to close the gap on Xie, which was now four seconds 10:06 to 10:10. Cooper Sloan '18 was at 10:12. Then there was a gap before Rory Beyer '17, Colin Godwin '17, Nicholas Waltman '18 and Matt Deyo '16 gave chase at 10:18-10:21.</p>
                  <p align="left">Logan Trimble '13 and Justin Bullock '14 gave chase and were in the thick of it with Bullock just behind Sloan and Trimble riding on Godwin's tail. The remaining alums, Eric Khatchadourian '06, Eric Safai '14 and Gihan Amarasiriwardena '11 had settled near the back.</p>
                  <p align="left">As they hit the finish, Xie took first place honors, expanding his lead to six seconds over his teammates with a 15:58 on the slightly long 5K course. Deyo moved up to second in 16:04 with Wenck holding onto third in 16:05. Sloan outkicked fellow freshman Waltman, both being timed in 16:07 to complete the sweep. Close behind was Beyer in 16:10 and Godwin in 16:13. Trimble was right behind in 16:17, but Bullock faded to 16:28. Khatchadourian was 23rd, Safai in 24th and Amarasiriwardena was 25th to close out the scoring for the Alumni.</p>
                  <p align="left">Also in attendance for the alums were Stephanie Birkhimer '14 and Brooke Johnson '13, Claire O'Connell, Patrick Barragan '08, Dan Harper '13 and Don Shobrys '75.</p>
                  <p align="left">The race ended MIT's pre-season. Classes start on Wednesday as the regular season training gets underway. Tech's first race will be the Purple Valley Classic on 9/27 hosted by Williams College.</p>
