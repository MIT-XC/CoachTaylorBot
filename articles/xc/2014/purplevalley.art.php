                  <p><span class="headline">Women Win, Men Third at Purple Valley Classic</span></p>
                  <p align="left">September 27, 2014</p>
                  <p align="left">In their first intercollegiate race of the season, the Engineers traveled to Williamstown to take on 20+ teams from New England, New York and New Jersey in the annual <a href="purplevalley.res.php">Purple Valley Classic</a>. The 8K for men and 6K for women were the longest cross country races ever for the many freshmen on the team and the 80 degree temperatures promised to take a toll on nearly everyone who entertained pushing the pace early.</p>
                  <p align="left">The men raced first. Going into the meet there were six top 10 teams representing New England Division III with all earning a national ranking among the top 30 teams in the country. Williams was ranked sixth, MIT eighth, Colby 17th, Amherst 21st, Middlebury 29th and Tufts 30th. There was no shortage of competition. </p>
                  <p align="left">After a rather quick sprint across the field the pace settled nicely to a reasonably conservative pace, showing a respect for the course and the heat. The leaders came though the mile at 5:20 with Colby dominating the lead pack with six among the top ten and David Chelimo, a sophomore from Colby dictating the pace. Tech was poised nicely just off of Colby. Williams College runners were back a bit, displaying their experience with their home course.</p>
                  <p align="left">At mile two, Colby runners fell off slightly while MIT moved up slightly, still yielding a Colby lead. Williams made a significant move with their top two runners, but the rest of their team moved up marginally in the pack. Mile three was more of the same with the exception of the Engineers now leading the race by a slight margin over Colby and the ever closing Ephs from Williams.</p>
                  <p align="left">In the final two miles, which includes the second time up the &quot;big&quot; hill, Colby proved the tougher team as they improved slightly on their places from the three mile mark, led by individual winner Chelimo. Tech showed they were not mentally ready for the challenge, falling off from 58 points at mile three to 72 points at the finish. Williams three, four, five runners made a big push in the final two miles to overtake MIT for second place.</p>
                  <p align="left">Spencer Wenck '15 led the way for MIT, placing 5th overall in 26:27. Colin Godwin '17 placed 10th, just six second behind his teammate. Matt Deyo '16 did a great job coming off a sprained ankle to place 11th, four seconds behind Godwin. Benji Xie '15 fell off the pace in the final mile, placing 18th and Michael Picchini '18 made a great comeback in mile five after losing focus in the third and fourth mile to finish 28th overall.</p>
                  <p align="left">The women were up next and the temperature continued to rise. Also like the men, the women's field boasted many of the top teams in New England and even stronger representation with regard to national ranking. Williams, MIT and Middlebury, typically among the top five teams in the country at the NCAA Division III Championships each year, were once again ranked high. Williams was holding onto the number two position with MIT ranked fourth and Middlebury eighth. Also in attendance were 14th ranked Tufts and the 32nd ranked team from Amherst.</p>
                  <p align="left">Just like the men, there was a strong sprint across the field, with the Engineers expending too much energy when it was not necessary. Unlike the men, the pace continued to be brisk as they hit the mile mark in 5:56. Although MIT was not leading the race, Sarah Quinn '16, Maryann Gong '17, Elaine McVay '15 and Christina Wicker '17 were immediately behind the leader.</p>
                  <p align="left">During the second mile, the leader made a slight wrong turn despite a cyclist leading the way and McVay, Gong and Wicker followed for a couple of steps before turning back. This put Quinn at the lead, but as they hit mile two in 12:03 they were once again all together with teammate Nicole Zeinstra '16 right with them. Also in this pack were two runners from Tufts, two from Williams, one from Middlebury and one from Colby.</p>
                  <p align="left">In the third mile, the women hit the hill for the one and only time right at the three mile mark. The ambitious first mile took its tolls on some of the Tech runners, but not enough to make a difference in the race. Quinn pulled away after the hill, running a respectable 4:06 for the final three quarters of a mile to finish the 6K in first place in 22:23. Gong let Quinn get away too much, putting on a strong kick too late to catch her, but closing the gap to only six seconds at the finish for second place. Zeinstra's more conservative first mile served her well as she finished third for the team in sixth place overall. McVay lost focus a bit after the hill, but stayed in contention to finish ninth overall. Wicker let the hill get to her, but made dramatic improvement in her focus prior to that. Losing 44 seconds to Quinn in the final 3/4 mile left her in 18th place overall. MIT won with 36 points to 65 for Middlebury, 95 for Tufts and 115 for Williams.</p>
                  <p align="left">MIT will be off next week, but returns to action at the NEICAAA Championship on October 11 at Franklin Park, a race that is appropriately nicknamed the All New Englands as it pits all Division I, II and III teams against each other in  varsity and sub-varsity races.</p>
