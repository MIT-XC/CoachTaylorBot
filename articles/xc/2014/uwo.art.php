                  <p><span class="headline">Quinn, Wenck Lead Engineers at UWO-AAE Invitational</span></p>
                  <p align="left">October 18, 2014</p>
                  <p align="left">MIT traveled to Oshkosh, WI this weekend to race many nationally ranked NCAA Division II and III teams in the <a href="uwo.res.php">University of Wisconsin Oshkosh / AAE Cross Country Invitational</a> at the Lake Breeze Golf Course, the site of the 2015 NCAA Division III Cross Country Championship. In the women's field, the nationally ranked #1, #2, and #7 Division II teams, along with the #1, #4 (MIT), #6, #9, #12, #14, #19, #21, and #30 ranked teams for Division III joined the 42 total teams on the 45 degree, windy day on a very wet course. On the men's side, representing Division II were the #2, #8, #23, and #25 teams and for Division III, #1, #3, #11, #13, #14 (MIT), #16, #17, #21, #24, and #26 were among the 45 teams.</p>
                  <p align="left">MIT's women ran an even paced race on the 6K course, with the A group of Sarah Quinn '16, Elaine McVay '15, Maryann Gong '17 and Christina Wicker '17 going out in a conservative 5:47 first mile, well behind #1 ranked DIII team John's Hopkins top four, who were about seven to eight seconds faster. By the two mile mark, Quinn and McVay were together at 11:40, but Wicker and Gong had fallen back, not having their best day. Nicole Zeinstra '16, who had gone out with Cindy Huang '15, the heroine from last week for Tech, was moving forward nicely, joining Gong after miles of 5:55 and 5:52. Huang was not as prepared on this day and went out the back end of this race. Group C was represented by Kali Benavides '15, Laura Eccles '18 Liz Cox '18 and Stephanie Marzen '15 and Benavides, Eccles and Cox were running well together, coming through two miles at 12:08.</p>
                  <p align="left">Johns Hopkins' runners were falling back, but MIT runners were not closing the ground quickly enough. At three miles Quinn was 17:27, separating herself from McVay by two seconds. Zeinstra had moved up to third for MIT at 17:39 with Gong and Wicker not too far behind. At the finish MIT was fourth overall and second among Division III teams with Quinn finishing 13th in 21:35, the fifth Division III runner. McVay was 19th in 21:44, Zeinstra 29th in 21:59, Gong 34th in 22:03 and Wicker 35th in 22:11. The 36 second delta was solid, but the gap between two and three was too large and not having anyone among the top five overall finishers is something the Engineers will have to correct. Johns Hopkins was third overall and first among DIII teams, 10 points ahead of MIT when just scoring the DIII teams. </p>
                  <p align="left">The men from MIT got out very well among the 600+ runners, establishing themselves in good position going into the first turn. Between there and the mile mark, Tech's B group did not move well enough to establish a strong enough position to be real competitive in the meet while the A group was exactly where they wanted to be. Spencer Wenck '15 joined the leaders by the mile mark, coming through in a perfect 4:55. Close behind in 4:59 were Colin Godwin '17, Matt Deyo '16, Rory Beyer '17, and Benji Xie '15. Group C was led by Cooper Sloan '18, and Matt Jordan '15, at 5:06 and Allen Leung '15 who had fallen at the start. Trailing the group was Ben Freed '18 and Michael Picchini '18, at 5:07.</p>
                  <p align="left">Throughout the next two miles Wenck stayed with the leaders albeit toward the back of that elite group. Godwin and Deyo lost a little ground but were still within 15 seconds and looking strong. Xie, who had been close to Wenck at the two mile was back with Godwin and Deyo at three and going in the wrong direction. Leung had caught up to Cooper and Jordan by two miles and pulled slightly ahead at three. </p>
                  <p align="left">The fourth mile hurt the Engineers. Wenck continued to hit 5:00 miles, but Godwin and Beyer ran 5:13 and 5:15 respectively, while Beyer and Xie ran 5:23 and 5:24 miles. Leung continued to move up, now only a second behind Beyer. In the final mile, Wenck stayed strong with a 4:49 to finish in 15th in 24:50. Godwin finished 43rd in 25:20, Deyo 54th in 25:27, Beyer 89th in 25:47 and Xie holding on for 91st in 25:48 to close out the scoring. Leung was right behind at 25:49, Sloan 100th in 25:52 and Jordan 110th in 25:56. </p>
                  <p align="left">MIT placed seventh overall and third among Division III teams, only finishing behind #1 ranked North Central College and #3 ranked UW Eau Claire. They represented their ranking well, defeating some very good teams. The delta of 58 second 1-5 is too large, particularly the 30 second gap between one and two. The team could be a solid top eight team in Division III if they get a little more comfortable with running in discomfort by the time NCAA Division III Regional Qualifiers come around in four weeks.</p>
                  <p align="left">Tech is off next weekend, competing again on Nov. 1 to defend their NEWMAC Conference Championship hosted at the Smith College cross country course.</p>
