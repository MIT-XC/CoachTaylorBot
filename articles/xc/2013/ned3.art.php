              <p><span class="headline">Men and Women Both Finish Third at NCAA New England Regional - Move on to Nationals</span></p>
              <p align="left">MIT's men's and women's cross country teams rode amazing individual performances to third place team finishes in the <a href="ned3.res.php">NCAA Division III New England Region Cross Country Championship</a> today. The women finished with 127 points, claiming third out of 53 full teams, while the men totaled 113 points to take third place honors out of 50 complete teams. Both teams advance to Nationals next weekend at Hanover College in Hanover, Indiana.</p>
              <p align="left">The weather was near perfect for the women with 40-45 degree temperatures and overcast skies. The course was very flat with the exception of one hill, and only a bit muddy in the second and third miles. There were six nationally ranked teams vying for an opportunity to toe the line next weekend. Everyone expected a fast pace and Elaine McVay '15 did not disappoint, taking the opening mile out at 5:34. MIT's women were up towards the front where they were supposed to be with the exception of Tech's number seven runner, Louise van den Heuvel '14, who was up in third place at 5:35, which she has never come close to in a cross country race, particularly a 6K race. Kali Benavides '15, Kaitlin Allen '14, Brooke Johnson 'G, Alexandra Taylor '14 and Maryann Gong '17 were all between 5:41-5:43 in very good position.</p>
              <p align="left">As the team progressed through the second mile, everyone expected van den Heuvel to fall off, but she held her ground, finishing the two mile at 11:34. McVay was pulling ahead along with two other runners, clocking 11:21 at two miles. Benavides, who has been bothered by a upper leg injury, did not let it affect her as she stepped up beyond expectations. Also stepping up, Allen, who herself has been dealing with a hip injury, lost some ground in the second mile but was still only 23 seconds behind the streaking McVay. Taylor was competing great, running between Benavides and Allen. Johnson, experiencing very bad stomach pains, was doing her best to fight through it. Gong, still trying to get past her fears of dying in a 6K race was not being competitive.</p>
              <p align="left">McVay threw down another 5:47 mile in the third mile despite the hill and mud as she tried to break her opponents, but by the end of that mile both of them had pulled ahead of her. Benavides remained solid as did van den Heuvel and Allen. Taylor fell back a bit, now even with Allen. Both Williams and Middlebury were now dominating the race, but MIT was in a solid third. In the final .73, McVay really put on the speed, running 4:10, but still finishing third in 21:18, a 5:42 pace effort. Benavides finished in 21st with a 21:55. van den Heuvel stayed the course, finishing in 26th place with a time of 22:04. Allen ran the third fastest last .73 to finish 34th in 22:15, earning All Region honors. Johnson managed to speed up in the final minutes of the race to move past Taylor, placing 43rd in 22:24. Her surge in the final mile allowed MIT to stay ahead of Tufts University in the standings.</p>
              <p align="left">By the time the men started their race at 12:30, the sun was out and the temperatures had moved into the mid-50's. The men's side of the competition had seven nationally ranked teams fighting it out for a spot at the big show and the top five teams were very evenly matched depending on the day. </p>
              <p align="left">While the women's step up moment belonged to Louise van den Heuvel, Matt Deyo '16 took notice and figured he could go one better. Deyo sprinted across the field as if it were an 800 meter race instead of the 8,000 meter race he actually had to complete. Fortunately, once across the field, Deyo slowed to a normal pace, but no one was going to pass someone so willing to do the work. Deyo hit the mile mark in front at 4:47 with Roy Wedge '14 a few places behind. The rest of the team was there as well with Allen Leung '15, Rory Beyer '17 and Benji Xie '15 at 4:50 and Ian Tolan '16 and Matt Jordan '15 playing it safe at 4:53.</p>
              <p align="left">Over the next two miles very little changed. At the front, Wedge was where he expected to be in fifth place, hitting the three mile mark in 14:55. Deyo fell back a few places, but was solid at 14:58. Leung and Beyer were together in great position at 15:08. Xie was with them at two miles, but fell in the third mile, getting injured and laboring 12 seconds behind by the three mile mark. This meant someone else had to step up. The conservative Tolan was only three seconds behind Xie, but needed to move if the Engineers were going to be competitive. Jordan, who had been on antibiotics due to an infection from a cold and sinus illness was still too weak to contribute significantly. However, he was doing his best, splitting 15:33 for three miles.</p>
              <p align="left">In mile four, Deyo actually closed the gap a bit, moving right up to Wedge. Leung and Beyer fell back some more, but stayed tough. It became clear Tolan was going to have to step up as Xie had given up and Jordan was just not up to it.</p>
              <p align="left">In the end, Wedge hammered a 4:45 final mile to stay in fifth place. Deyo ran 4:50 to place 7th. With Xie running what he was capable of, MIT would have been in it for the team title, but as it was Williams and Middlebury finished 1-2. Leung pulled away from Beyer in the final half mile to claim 25th and 29th places respectively in 25:18 and 25:21. Tolan closed well in 5:10 to finish in 47th place with a 25:52. MIT finished 36 points out of first, which was a total that could have been achieved with a perfect race. They will get another opportunity to show what they are capable of next week in Indiana.</p>
