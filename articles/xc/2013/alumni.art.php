              <p><span class="headline">VARSITY MANAGE PERFECT SCORES AGAINST ALUMS</span></p>
              <p align="left">August 31, 2013</p>
              <p align="left">Back on their home course at Franklin Park after a year of renovations and repairs to the fabled cross country course, the annual <a href="alumni.res.pdf">MIT Alumni Meet</a> brought the current varsity teams against the &quot;old timers&quot; under very humid conditions, but overcast conditions.</p>
              <p align="left">Both the men's and women's varsity teams had perfect scores of 15 points, but in the women's race, it was only because the alumnae did not have five runners to score. Jacqui Wentz '10 schooled the youngsters as she put in a surge in the middle of the first mile to come through the mile in 5:40, a seven second lead, and cruise to a first place finish on the 3K course in 11:20.</p>
              <p align="left">The women's varsity was led by Maryann Gong '17 who finished six seconds behind Wentz in 11:26. The women had a delta of 11 seconds through five runners, an outstanding spread under the conditions. The grass was very high, slowing the times significantly, and combined with the heat and humidity, took its toll on the runners. The varsity runners were instructed to push the early pace, so maintaining contact with each other and not falling off the pace very much was impressive. Second for MIT was Elaine McVay '15 in 11:30. Kali Benavides '15 put in a kick to finish with McVay, also in 11:30. Alexandra Taylor '14 finished fourth for Tech in 11:32 and Kaitlin Allen '14 closed out the scoring with a 11:37. Also running for the Alumnae was Maggie Lloyd '12, finishing in 13:08. Kirsten Aarsvold '11 was in attendance to cheer on her teammates.</p>
              <p align="left">The Alumni were well represented with 10 competitors, and they competed well, but were not quite ready to challenge the full varisty squad. Benji Xie '15, led an MIT sweep with a tough 16:00 on the 5K course. Close behind was Roy Wedge '14 in 16:05. Matt Jordan '15 was third in 16:09. Allen Leung '15 was fourth in 16:10 and Rory Beyer '17 was fifth in 16:13. The men pushed the pace early, which affected some of the perfomances, particularly the freshmen, who tend to not have as great an ability to maintain competitiveness when they get fatigued. Nevertheless, the 13 second delta was pretty amazing.</p>
              <p align="left">Stephen Serene '12 was the top finisher for the &quot;Has-Beens&quot;, finishing in ninth overall in 17:04. Chris Wong '08 was next in 11th place in 17:17. Logan Trimble '13 was third for the alums in 14th place with a time of 17:45. Richard Prevost '11 finished in 17th in 18:06 and finishing as the final scorer for the alums was Fivos Constantinou '06 in 18:19. Other finishers were Eric Khatchadourian '06, Josh Feldman '97, Gihan Amarasiriwardena '11, Bill Phipps '10 and Joe Roy-Mayhew '08.</p>
              <p align="left">The Engineers will not compete again until 9/21 at the Pre-Regional race in Maine. There is a lot of work to do if the MIT squads hope to contend for regional honors, but the talent is certainly there.</p>
