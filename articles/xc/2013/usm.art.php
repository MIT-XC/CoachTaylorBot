              <p><span class="headline">MIT Wins Men's and Women's Pre-Regional Race</span></p>
              <p align="left">September 21, 2013</p>
              <p align="left">The first intercollegiate race of the year for the 2013 MIT Cross Country teams was as good a start as could be expected. Under 65 degree temperatures and overcast skies, the University of Southern Maine hosted the <a href="usm.res.php">pre-regional meet</a> so New England teams could have the opportunity to see the course prior to the all important NCAA Division III Regional meet on November 16. Entering the meet, the men's field of 19 teams included nationally ranked number five Bowdoin College, number seven Bates College and number 11 Tufts University. MIT was back a bit at number 26. In the women's 5K field, also containing 19 teams, MIT was entering ranked number three in the country for Division III while number 19 Bates and number 29 Tufts were also in the meet along with a talented Colby College squad.</p>
              <p align="left">The women were up first. The Engineers were missing Sarah Quinn '16, who has compartment syndrome in her calves and will have to have surgery; Brooke Johnson 'G who is out with plantar fasciitis; Nicole Zeinstra '16, who is almost back and Martina de Geus '14 coming back from ACL surgery due to a landing coming off the waterjump in the 3000 Steeplechase at the NCAA Championship last May. Entering the meet a little under the weather, Elaine McVay '16 was recovering from a severe cold and freshman star, Maryann Gong was also coming back from a cold. Despite these losses, Tech would be pretty strong though not as deep as in previous years. </p>
              <p align="left">The race went out at a solid pace on the relatively flat course as the lead runners hit the mile mark in 5:50. MIT runners led by Kaitlin Allen '14, Kali Benavides '15, McVay, Stephanie Marzen '15, Alexandra Taylor '14 and Gong came through just off the leader at 5:52. During the second mile, Allen took the lead and pulled away with a 5:53 mile as the field entered the hilly portion of the course. The rest of the MIT squad looked flat, but competitive. In the third mile, Allen continued to race well, putting down another 5:52 mile, but she failed to put the field away as she was capable of doing. As a result, she was caught in the final 100 meters by Hannah Zydanowicz from Roger Williams College. Allen finished first for MIT, second in the race overall in 18:11. Coming through to help MIT was Benavides in sixth, McVay in 12th, Marzen in 15th, and Taylor in 17th. The team delta was a respectable 30 seconds even though the times were a bit slow.</p>
              <p align="left">The end result had MIT's women taking first place with 52 points, just edging out a strong Tufts squad with 54 points and Bates College with 86 points.</p>
              <p align="left">The MIT men's harriers have plenty of talent, but still needed to prove it to themselves, and based on their ranking, the rest of the country. MIT was pretty healthy, only missing Matt Jordon '15 out of their top five as he recovers from a patella tendon injury suffered from something totally unrelated to running.</p>
              <p align="left">At the start, MIT's B group, led by Matt Deyo '16 and Ken Leidal '17 (still learning the concept of group running) abandoned all plans and decided to get out in front of the All America level lead group. MIT's A group, comprised of Roy Wedge '14, Benji Xie '15, Rory Beyer '17 and Allen Leung '15 were back a couple of seconds in 5:03. The lead group for MIT maintained consistency and integrity as they worked together, coming through in 10:15 through the hilly part of the course. Mile three was pretty fast at 5:08 and Tech was still with the leaders, and Deyo was only one second behind despite his foolhardy early pace. The fourth mile hit Deyo pretty hard, but he showed his toughness as he only dropped 20 seconds to the A group, who hit 5:00 as the race accelerated. Leung lost focus for a bit and slipped back with a 5:08 fourth mile. In the final mile, Wedge realized he had a lot more left than he thought. Wedge finished in third place in 25:20, nine seconds out of first place. Xie was fifth in 25:23, while Beyer, the top freshman in 25:37 was sixth. Leung hung tough to finish 10th in 25:47 and Deyo closed out the scoring in 13th in 25:58. The 38 second delta was quite impressive for the early season in such a competitive race.</p>
              <p align="left">When all was said and done MIT had come away with a tremendous upset victory with 37 points to 85 for Bates, 86 for Bowdoin and 95 for Tufts. This meet is extremely early in the season and does not mean anything other than identifying the teams to watch for two months from now in the championships.</p>
              <p align="left">Next week, MIT will travel to Williams College to test their abilities against the Ephs and many other teams entering the invitational. Williams is ranked number four on the women's side and eighth on the men's side.</p>
