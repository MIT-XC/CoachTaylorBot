              <p><span class="headline">Men's Cross Country - Remains Undefeated, Women Place Second</span></p>
              <p align="left">September 28, 2013</p>
              <p align="left">Competing for the second week in a row, MIT's nationally ranked Cross Country teams traveled to Williams College to compete in the <a href="williams.res.php">Purple Valley Classic</a>. The men, having moved up from 28th to 5th in the country for Division III after last week's impressive showing at the New England Pre-Regional meet, were facing 9th ranked Williams and 23rd ranked Middlebury College among the 13 team field. The women, ranked 3rd in the country despite missing many of their top runners at least for the next three weeks, were going up against 4th ranked Williams, 8th ranked Middlebury and 28th ranked Amherst College among their field of 13 teams.</p>
              <p align="left">The very hilly and soft course, combined with 75 degree temperatures under a bright sun did nothing to help with a fast pace. MIT, typically comfortable with running their own race, stayed back and allowed other teams to set the pace. This resulted in a pedestrian 5:20 first mile despite the flat part of the course. The hilly second mile netted a 5:31 for the leaders. Up to this point the pace was so slow that there was a glut of runners in the front. In the third mile the leaders picked up the pace to 5:11, which spread the field significantly. Two Williams runners, three MIT runners (Roy Wedge '14, Benji Xie '15, Rory Beyer '17) and a smattering of runners from other teams. Matt Deyo '16 and Allen Leung '15, who both had to stop and put a shoe back on early in the race were 14 and 22 seconds back respectively, far ahead of their competitors fourth and fifth men. The hilly fourth mile destroyed most of the field, but Wedge ran 5:08, Xie 5:11 and Beyer 5:14. Howeve, they were still behind the two Williams runners and Xie and Beyer were behind two Middlebury runners as well. The final mile still has part of the final uphill and a very steep downhill that usually finishes off any quadricep strength the runners may have still had. The Tech runners showed their strength, finishing well with Wedge running 5:04 in the final mile to claim third in 26:14, Xie fourth in 26:18 and Beyer, the top freshman in the field, fifth in 26:20. Deyo and Leung hung tough to finish 13th and 16th respectively, finishing ahead of Williams' and Middlebury's fourth runners. The end result left MIT with 41 points. Williams was second with 59 points and Middlebury third with 60 points.</p>
              <p align="left">The women were running 6K for the first time and the heat made this a concern. Similar to the men, the MIT women had no intention of leading. Middlebury did not mind taking the lead and pulled the group through a 6:00 lead mile. The second mile was a bit slower as they had to deal with the smaller hills on the course. Elaine McVay '15 led her MIT teammates at two miles in 12:10. Right with her were Maryann Gong '17, Kali Benavides '15 and Stephanie Marzen '15. Alexandra Taylor '14 and Kaitlin Allen '14 were two seconds back. Christina Wicker '17 was three more seconds back but then there was another 15 seconds before the next Tech runner. The third mile included the big hill the men had to do twice. McVay smoked the hill to take the lead but was caught on the downhill portion and then passed going to the finish, ending up in second with a fast 22:29. Benavides ran a superb race, finishing fourth in 22:43. Williams had finished 1-3 and MIT 2-4 and now came Middlebury with the next four places. Gong dropped out of the race from heat exhaustion, but Marzen and Taylor took up the slack, running their best 6K's ever to finish 9-10 in 22:58 and 22:59 respectively. Through four runners MIT had 25 points and Middlebury 26 and Williams only had two runners across the line. Middlebury's fifth runner was 12th and then Williams packed in four runners and there was another from Middlebury before Allen finished for MIT in 18th place. The final score was Middlebury 38, MIT 43 and Williams 46.</p>
              <p align="left">MIT has next weekend off, returning to competition at the NEICAAA Championship at Franklin Park, a championship that pits all of Division I, II and III teams against each other, as well as a rematch with Middlebury for the women.</p>
