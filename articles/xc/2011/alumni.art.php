                  <p><strong><span class="headline">Varsity Men Win Close One Over Alumni, No Score in Women's Meet</span></strong></p>
                  <p align="center">September 3, 2011</p>
                  <p align="left">The course certainly ran slow due to the high grass and soft ground, and the high heat and humidity further slowed the runners at the annual MIT <a href="alumni.res.php">Alumni/ae Meet</a> at Franklin Park, MIT's home course. The slow times did not take away from the competition, which is really what cross country is all about. The Varsity men outlasted the former MIT runners by a score of 24 to 31. In the women's meet, only one alum toed the line and the Varsity, while starting five only finished four--negating any score.</p>
                  <p align="left">There was tremendous representation from the Alumni on the men's side as 13 individuals representing four decades of MIT Cross Country ran the 8K race. At the start, the two teams were even as they took off across the open field. By the mile mark, the Varsity five man team was slightly ahead of the trailing &quot;Has Beens&quot;.</p>
                  <p align="left">At the two mile mark, Andrew Erickson '12, took a strong lead over freshman Matt Jordan, Chris Wong '08, and teammate Dawit Zewdie '13. The third mile was not a strong one for the varsity as Erickson let Wong back into the race and Jordan faded behind Zewdie. After a bit of a gap, Ethan Crain '96 led Ben Schmeckpeper '05 and varsity member Jan McKenna '13. While the varsity had a strong lead, the race could still go either way.</p>
                  <p align="left">The fourth mile was a good one for Zewdie as he took a slight lead over Wong and Erickson. Jordan had a solid lead over Crain, and McKenna started to pull away from Schmeckpeper and Bill Phipps '10. At the finish, Erickson put the finishing touches on a 5:11 last mile to bury the competition. Wong was second overall and first for the Alums. After Zewdie and Jordan claimed third and fourth, the Alumni had four of the next five as Crain took fifth and Phipps, Schmeckpeper and Gihan Amarasiriwardena '11, claimed 7-8-9, just behind a sprinting McKenna, putting five ahead of the varsity's fifth, Jacob Young '13.</p>
                  <p align="left">Other Alums racing were Jack Bourbonnais '10, Bob Collins '82, Josh Feldman '97, Terry McNatt '87, Tony Pelosi '01, Richard Prevost '11, Bill Singhose '90, and Bob Walmsley '84.</p>
                  <p align="left">Rumor has it two Alumni weddings prevented many of the more recent Alumnae from attending the race. As a result, the lone entry for the Alumnae was Andrea Bradshaw '09. Although there was no score in the race, Bradshaw disposed of the Varsity team without much of a contest.</p>
                  <p align="left">By the one mile mark, Bradshaw had a five second lead and the race never got any closer. At one point, Bradshaw had around a 15 second lead, but Maggie Lloyd '12, managed a strong third mile to close the lead back down to five seconds at the finish. Kali Benavides '14, finished four seconds behind Lloyd in 20:04. Sarah Sprague '12, was third for the Varsity and Margaret Coad '14, was fourth. Olivia Papa '13, dropped out after the first mile with breathing difficulty.</p>
                  <p align="left">Next year it is hoped the attendance will be much greater. To enhance this possibility, the race distances will be shortened to 3K for the women and 5K for the men. In addition, all MIT runners will participate in the races.</p>
                  <p align="left">Next week, MIT will again enter a partial team, not including any of their top seven runners as they host the Engineers Cup, a meet with RPI and WPI on Saturday at 1:00 for the women and 1:40 for the men.</p>
