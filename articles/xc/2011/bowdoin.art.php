                  <p><strong><span class="headline">Women Win Bowdoin Invite Without Top Five</span></strong></p>
                  <p><span style="font-weight: bold">Men Claim Second, Hold Out Top Twelve</span></p>
                  <p align="center">October 1, 2011</p>
                  <p align="left">&nbsp;</p>
                  <p align="left">MIT traveled to <a href="bowdoin.res.php">Bowdoin College</a> in Brunswick, ME to test out the NCAA Division III New England Region Championship course on Saturday. Most of MIT's runners, the top 12 men and all but two of the top 10 women experienced the course prior to the races, doing a fartlek for a hard workout. Those who did race represented MIT well as the women squeaked out a close two point victory over host Bowdoin in the 10 team field, 67 to 69. The men did not fair as well as Bowdoin swept the top six places, but the Engineers managed second place, also with 67 points.</p>
                  <p align="left">The course was extremely wet and quite muddy as it had rained  all night and morning, and continued to drizzle during the race. Despite the perfectly flat course, it did not run fast as it was like running on a sponge and the many turns were quite treacherous in the late going as the terrain became muddier.</p>
                  <p align="left">Stephanie Marzen '15, racing for the first time this year due to a bout with a cold that did not seem to want to go away, led the way for the Engineers with a seventh place overall finish in 23:37 for the 6K race. Milly Helmick '15, also racing for the first time due to an IT band issue, was close behind in 10th in 23:47. Cindy Huang '15, raced very well for her first 6K, placing 13th in 24:16. Chandler Burfield '13, also raced well, but cautiously, placing 18th in 24:28. Janice O'Brien '12, finished in 19th place, making a significant improvement over her previous two races, running 24:30, virtually tying with teammate Sarah Sprague '13, in 20th, to close out the scoring.</p>
                  <p align="left">The leaders went out in what seemed a slow 6:10 first mile, but the course was not allowing much more than that. By the second mile, Marzen was still close to the leaders at 12:21, but Helmick and the others had fallen back by at least nine seconds. The third mile did in Marzen, who was still battling her cold earlier this week, as she ran a 6:34, still the fastest mile three for MIT. MIT closed well as many had lost focus during the third mile and realized this as they approached the finish, having more left than they should have had.</p>
                  <p align="left">Andrew Erickson '12, led the way for MIT, placing 11th overall in 27:04, despite running with one untied shoe for nearly the entire race. Jay McKenna '14, was right behind in 27:06 in 12th place. Matt Weaver '12, had a great race, placing 14th in 27:11. Kris Frey '15, was 17th in 27:14, giving MIT a 10 second delta through four runners. Eric Safai '14, running his first race of the season, seemed to be getting used to racing again as he faded in the middle of the race to place 23rd in 27:46.</p>
                  <p align="left">Erickson went out just behind the leaders and was the only Tech runner who did. He came through the mile in 5:12, a bit behind the leaders. McKenna was in the second group at 5:16 along with the others. By the second mile, Erickson fell back due to his shoe, but remained competitive at 5:14 while the rest of the team fell back to around 5:24 pace. The pace continued to degrade for the Engineers as the third mile slowed to the 5:30's and the fourth mile the 5:40's, although the mile marks may have been off. The fifth mile was timed between 5:01 and 5:20 for Tech runners and they did not seem to accelerate that much.</p>
                  <p align="left">Next week, MIT goes to the its home course, Franklin Park, for the biggest race so far this season, the NEICAAA Championship, a race for all the colleges in New England despite their NCAA Division affiliation.</p>
