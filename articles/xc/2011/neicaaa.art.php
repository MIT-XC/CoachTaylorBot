                  <p><strong><span class="headline">Men Earn Highest Place in Modern Era In All New England Championship</span></strong></p>
                  <p align="center">October 8, 2011</p>
                  <p align="left">In their second race of the season, the Men's Varsity competed in the New England Intercollegiate Amateur Athletic Association <a href="neicaaa.res.php">(NEICAAA) Championship</a>. This meet pits Division I, II and III colleges in New England against each other as it has since 1877. This meet tends to compare favorably with the NCAA Division III Championship regarding place for both team and individual. So, when the NCAA Division III 7th ranked Engineers toed the line it was with the intent to compete at a level relative to their ranking. Despite racing without top freshmen Karl Baranov and Spencer Wenck due to injuries, MIT was confident that those racing would step up and get the job done. This they did even though another freshman, Benji Xie, collapsed at the 2 3/4 mile mark, possibly due to the heat (83 degrees and very sunny). Finishing 6th overall and second among Division III teams with 244 points shows that if they can get their entire top seven healthy they will be a contender to do very well at the NCAA Division III Championship on November 19.</p>
                  <p align="left">The women, ranked 3rd in the country, but also running in the absolute strongest region in the country for Division III, also had the intent to race well, but did not seem to be as hungry as the men to get the job done. Where the men ran with pure determination and without doubt, the women kept wondering if they could meet their goals. While they placed 8th overall and third among Division III teams with 276 points, it was obvious they could have done significantly better.<a href="../xcsched.php"></a></p>
                  <p align="left">The women competed first on the soft course (due to heavy rains earlier in the week). 39 teams and 269 individuals finished the race as the extreme heat took its toll on some of the runners. MIT got off the line well and positioned themselves well at the mile mark. The plan was to come through the first mile at 5:40-5:45 pace, which would put them in a favorable position given how fast the women competitors usually take the race out and the resulting decay of pace. MIT was out too fast however, particularly in the high temperatures, with Elaine McVay '15, and Brooke Johnson '13, running together at 5:34. The remainder of the varsity came through in the mid 5:40's. At mile two, McVay and Johnson continued to move up, coming through in 11:46, about 15-20 seconds slower than they should have been, but obviously feeling the effects of the first mile. Tania Morimoto '12, was close at 11:55, along with freshman Dacie Manion. Martina deGeus '15, not running to her potential was back at 12:06, Claire O'Connell '15, running varsity for the first time this year, came through in 12:13 with Maggie Lloyd '12, back at 12:19. In the third mile, McVay poured it on with a 5:59 effort to move into the top 20, however her finishing kick, which due to poor mechanics was not there, got passed by a few to end up in 21st place in 18:27, a superb performance for the freshman. Johnson stopped racing with a mile to go, just running through the motions, but due to her incredible talent still managed to place 37th in 18:42. deGeus finished in 66th in 19:05 even though she admittedly did not focus well the entire race. Morimoto ran 19:07 to place 70th and O'Connell passed a struggling Manion to finish in 86th position in 19:20 to close out the scoring for MIT. Manion, suffering from heat problems for her second race, was only three seconds behind Morimoto with 200 meters to go, but ended up 20 seconds back, barely making it across the line.</p>
                  <p align="left">In the sub-varsity race for the women, Stephanie Marzen '15, racing for the second time this season, showed her real abilities as she finished in sixth place in 19:05. Also performing well for MIT was Alexandra Taylor '14, placing 22nd in 19:29.</p>
                  <p align="left">The men's varsity were racing at the hottest part of the day. While the relative humidity was moderate, there was no breeze and very little shade. Also getting off the line well, the top six settled in to a very good pace, particularly given the conditions. Originally planning to go out at 4:50 pace, they exercised good sense, and focused more on position, coming through the mile in 4:54 for Dan Harper '12, 4:58 for Allen Leung '15, and Benji Xie '15, 5:00 for Roy Wedge '14, and 5:02 for Stephen Serene '12, and Ben Mattocks '12. By the second mile Harper had moved into the top 30 with a 10:05 effort. The other Tech runners grouped up between 10:20-10:25. Even though Xie dropped out, the remaining varsity runners stepped up, running a very strong fourth mile. Harper was flirting with cracking the top 20, coming through at 20:35, while the rest of the varsity had spread out. Wedge, Serene and Leung were making a strong charge, running 20:55, 21:01 and 21:04 respectively. Mattocks, knowing he was the fifth man regardless of his finish, was hurting, but still fairly close at 21:17. In the fifth mile all MIT runners put everything they had into the race, with Mattocks closing in on the varsity by nine seconds to move up to 71st place overall in 26:12. Harper finished in 22nd place in 25:22. Wedge placed 39th in 25:47, Serene 48th in 25:57 and Leung 66th in 26:04. The sixth place finish among the 43 teams was the highest place for the men since the World War II era.</p>
                  <p align="left">In the sub-varsity race, Andrew Erickson '12, ran a great race, placing 11th overall in 26:34.</p>
                  <p align="left">Next week, MIT will fly to Oshkosh, WI to race in the Division III Pre-National meet. The course is extremely flat and hopefully the temperature will be more seasonable.</p>
