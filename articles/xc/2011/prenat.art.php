                  <p><span style="font-weight: bold; font-size: 16px">Johnson Leads Women to Second Place Finish at Pre-Nationals</span></p>
                  <p style="font-weight: bold; font-size: 14px">Men Do Not Race Aggressively, Place Seventh</p>
                  <p align="center">October 15, 2011</p>
                  <p align="left">Forty seven teams competed in both the men's and women's races this weekend at the Brook's <a href="prenat.res.pdf">Pre-National Meet</a> hosted by UW Oshkosh. Numerous nationally ranked teams (9 for the women and 12 for the men) were competing in both  races, including top ranked North Central College, third ranked Calvin and fifth ranked Washington University of St. Louis in the men's race and third ranked Washington University of St. Louis in the women's race. MIT's was ranked fifth for the women and seventh for the men entering the race.</p>
                  <p align="left">The women toed the starting line first. Each team was permitted up to 10 runners in the 6K varsity race, making the field nearly 400 strong. At the start the right side of the field pushed hard to the left, shutting down many teams on the left that did not get out. Fortunately, MIT got out well. While the course is quite flat with no significant hills, the start is slightly uphill to the first turn, so getting out well, but not too hard was imperative. </p>
                  <p align="left">Although it had rained for three consecutive days, the course seemed to handle the water well, only seeming a little soft. The other factor affecting the race was the 45 mph winds that were constant throughout the day.</p>
                  <p align="left">Coming into the mile mark, Elaine McVay '15, and Brooke Johnson '13, found themselves inadvertently in the lead. Not meaning to take the lead they were passing runners and moving up and suddenly there was no one left to pass, a mistake they should not repeat. With the strong winds no one was going to prevent them from being the buffer. The mile split was pretty quick with both coming through in 5:49. Tania Morimoto '12, and Martina deGeus '14, were fairly close at 5:52. Dacie Manion '15, was not far behind in 5:54 and Alexandra Taylor '14, and Maggie Lloyd '12 were back at 5:58. </p>
                  <p align="left">By the two mile mark, both Johnson and McVay had fallen off the lead, but were still with the lead group, 11:44 and 11:46 respectively. Morimoto moved up past deGeus, but both fell back another 10 seconds from the leaders. At this point it as clear Wash. U. was the superior team, at least on this day as they had eight of their runners ahead of MIT's fifth.</p>
                  <p align="left">In the third mile the lead group spread out a lot. Johnson was holding onto fourth and McVay eighth, but both were paying for the mistake in the first mile. deGeus and Manion closed well to move up to 26th and 35th places respectively but Morimoto fell off to 50th place to close out MIT's scoring, giving them a hard fought second place.</p>
                  <p align="left">The men were without three of their top freshmen, all members of the top seven, for the second week in succession. Nevertheless they knew they needed to race well. Knowing and doing are often two different things, and this day fell into that category. Dan Harper '12, got out well, intending to run with the leaders and doing so right from the start. Stephen Serene '12, was only three seconds back at the mile mark, but Roy Wedge '14, was much too conservative, particularly on such a fast course, ending up 10 seconds off of Harper at the mile mark. Allen Leung '15, Ben Mattocks '12, and Andrew Erickson '12, were in very close proximity to Wedge, but back in about 100th place, much too far out to be competitive.</p>
                  <p align="left">During the second mile Harper continued to stay with the leaders, clocking 10:09 for two miles. Serene slowed down significantly, choosing to not bridge the gap to the leaders, but rather fall off to the next group, hitting the two mile mark in 10:25. Wedge moved up well, as he tends to do, to catch Serene, with Leung staying with them as well. Mattocks was only three seconds back, but they were still back in 70-80th place. Erickson fell off a lot, losing eight seconds to his teammates.</p>
                  <p align="left"> Mile three saw Harper continue to stay in 13th although the lead group began to spread out a bit. Serene, Leung and Wedge moved up some more, but lost seven or eight more seconds to Harper and the other leaders. Mattocks continued to lose ground as did Erickson.</p>
                  <p align="left">With two miles to go the Engineers needed to rally. Even though Harper maintained his 13th position and Serene and Wedge continued to move up to the top 35, Leung fell back during the final two miles, losing 25 seconds to Serene and Wedge. Mattocks lost 30 seconds in the final two miles and no one behind was able to offer the support necessary to help MIT place higher than seventh overall. The 1:04 delta for the top five is just not getting the job done at the level it takes to be a top ranked team.</p>
                  <p align="left">MIT is back on the course on Sunday, Oct 30 at Franklin Park for the NEWMAC Championship where the Engineers try to win their 14th consecutive conference title on the men's side and the fifth for the women.</p>
