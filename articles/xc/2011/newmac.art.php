                  <p><strong><span class="headline">Men and Women Continue The Streak of NEWMAC Championship Victories</span></strong></p>
                  <p align="center">October 31, 2011</p>
                  <p align="left">The men and women harriers from MIT entered the 14th annual <a href="newmac.res.php">NEWMAC Cross Country Championship</a> as favorites, but Springfield College on the men's side and Wellesley College among the women's teams had the talent to pose individual and team threats to MIT's yearly dominance at this event. Tech's men would be going for their 14th consecutive title having never lost the championship. The women were going for their seventh overall and fifth consecutive title.</p>
                  <p align="left">The championship, originally scheduled for Saturday, was moved to Sunday early in the season when it was determined there were too many races already scheduled for Saturday at Boston's premier cross country course, Franklin Park.  On Friday of this week, with the predicted snow and rain, combined with the onslaught of races, the Boston Parks Department shut down Franklin Park from Saturday afternoon at 1:00 until Monday, moving the NEWMAC Championship to Monday at 3:00. This caused academic issues among all the colleges as student-athletes arranged to miss classes or exams.</p>
                  <p align="left">The conditions were very muddy and wet with the temperatures in the upper 40's to low 50's. While great cross country conditions, no one was expecting fast times. While MIT runners were aware of this, the great performances begs the question how fast can they run on a dry course?</p>
                  <p align="left">The men started things off, guaranteed to dig up the course for the women. Springfield College flew across the field at the gun to take the initial lead while MIT was content to save their energy. The first half mile was a conservative 2:32, so MIT's top runners moved to the front. After the mile split of 5:04, Dan Harper '12, took control along with teammates Roy Wedge '14, and Stephen Serene '12. The trio ran a 5:14 second mile, opening up a decisive gap on the first Springfield runner. Continuing to speed up, Harper and Wedge threw down a 5:12 third mile. Serene fell back some as he lost a little focus. While MIT's top three were busy opening up a larger gap, Ben Mattocks '12, and Alan Leung '15, were busy moving up in the field, picking off opposing runners. At the four mile mark, Harper and Wedge came through in 20:38 after a blistering 5:07 mile. Serene had to stop briefly to pull a pine cone off of his spikes, but still came through in 20:47. Mattocks moved up to fifth place overall and Leung was now in the top 10. In the final mile, Harper proved too much for Wedge, the defending NEWMAC champion and 2010 Rookie of the Year, by finishing in 4:50 for a 25:28 victory. Wedge, not too shabby in his own performance, dropped a 4:56 to finish in 25:34. Serene completed the 1-2-3 sweep with his 25:45. Mattocks could not quite get fourth place, finishing three seconds behind Springfield's Ryan O'Connell with a 26:10 effort. Leung finished in eighth, just five seconds out of sixth, running 26:26, earning the 2011 Rookie of the Year. Running as MIT's bumpers, Logan Trimble '13, had a nice breakthrough race, placing 13th in 26:56 and Andrew Erickson '12, finished 16th in 27:00. MIT's 19 points easily outdistanced Springfield College's 44 points. Third was Coast Guard with 75, followed by Babson with 113, WPI with 122, Wheaton with 190 and Clark with 200 points.</p>
                  <p align="left">As the women took the course, it was obviously in much worse shape after the men's four loops. Similar to the men, the women took the pace out pretty casually, running 6:04 for the first mile. Brooke Johnson '13, and Elaine McVay '15, should have pushed the pace hard after the half mile mark, but were content to keep a steady pace. In the second mile, Claire O'Connell '14, and Tania Morimoto '12, made a strong move to join Johnson and McVay, coming through two miles in 12:04. Moving up in the pack were MIT's next group, led by Martina deGeus '14, and Dacie Manion '15, both of whom hit two miles in 12:13. Stephanie Marzen '15, should have been with them but was slightly back at 12:17. In the final mile, Morimoto made a slight move, but the group covered it and Randelle Boots '13, of Wellesley, the NCAA Division III Indoor Mile Champion, made a strong move that neither Johnson or McVay could match. Nevertheless, Johnson ran a 6:05 final mile for second place overall in 18:50 for the 5K race despite having a bad head cold. McVay finished third in 18:52, earning NEWMAC Rookie of the Year, and O'Connell finished fourth in 19:01. deGeus and Manion closed out Tech's top five, running 19:08 for sixth and 19:09 for seventh place respectively. Marzen led five more All NEWMAC performers for MIT as she placed ninth in 19:21, followed by Morimoto in 19:22 for 10th, Sarah Sprague '13, in 19:30 for 11th, Shivani Kaushal '15, in 19:31 for 12th and Maggie Lloyd '12, in 19:32 for 13th. The Engineers totaled 22 points, while Wellesley finished in second with 50, followed by Coast Guard with 102, Smith College with 142, Springfield with 163, Babson with 168, Mt. Holyoke with 192, WPI with 196, Clark University with 215 and Wheaton with 243.</p>
                  <p align="left">The next meet for MIT is the NCAA Division III Regional Qualifier, hosted by Bowdoin College in Brunswick, ME on Saturday, November 12. Going into that meet, MIT's men are seeded fifth in the region while the women are seeded third.</p>
