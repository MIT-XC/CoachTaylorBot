                  <p><strong><span class="headline">WOMEN ADVANCE TO NATIONALS, MEN'S POOR START, FAILURE TO RACE ENDS SEASON</span></strong></p>
                  <p align="left">November 12, 2011</p>
                  <p align="left">At the NCAA Division III New England Region Championship, hosted by Bowdoin College in Brunswick, ME, MIT's women harriers, ranked fifth in the country, advanced to the National Championship next Saturday in Oshkosh, WI. The men, defending New England Champions, finished sixth, failing to advance by one team, ending their season on a sour note.</p>
                  <p align="left">The women took to the soggy, but flat 6K course with the intent of challenging for the win. Elaine McVay '15, got out a bit too fast at the start, wasting a lot of unnecessary energy. The rest of the team got out well and were in good position to move up during the race.</p>
                  <p align="left">At the end of the first mile, Brooke Johnson '13, and McVay were with the leaders at 5:35. Tania Morimoto '12, was too far back (13 seconds) and the rest of the team, Martina deGeus '14, Stephanie Marzen '15, Sarah Sprague '13 and Dacie Manion '15, were back at 5:52. This was too big a gap for the first mile, particularly when the goal was for a delta of less than 45 seconds through five runners. By contrast, both Middlebury and Williams, the top two teams, had all seven of their runners among the top 35, which were all ahead of MIT's third runner. If MIT, who had gone out more conservatively had raced well and with determination, they should have been able to pass many of the front runners who had gone out too fast.</p>
                  <p align="left">Mile two saw Johnson hold onto the leaders as they pushed the pace, running another 5:35 mile. McVay fell off a couple seconds back of Johnson, showing some of the fatigue resulting from her fast start, but still holding onto eighth place. Morimoto was now 33 seconds back of Johnson, and deGeus seven more seconds back, was pulling ahead of the rest of the MIT team.</p>
                  <p align="left">In the final mile and three quarter, Morimoto lost another 46 seconds. A very reliable Manion late in the season, faded big in the final .72, losing a minute and 25 seconds. Fortunately, Johnson and McVay held onto sixth and seventh place respectively. McVay was the top freshman in the race of 353 finishers. deGeus closed well to finish in 32nd place. Marzen moved up a few places to finish in 43rd place, three places ahead of Morimoto.</p>
                  <p align="left">MIT's 134 points were good enough for third place out of 51 teams in the country's strongest region for Division III. Top ranked Williams College finished in second with 60 points, five points behind third ranked Middlebury College.</p>
                  <p align="left">The men did not get out well. Being in the middle with no real quality teams around them, they allowed the teams on the far right and left to get out ahead and squeeze them back. Even by the 3/4 mile mark, Dan Harper '12, MIT's top runner, was still back in about 50th place with his teammates at least 30 places behind him.</p>
                  <p align="left">By the second mile Harper had moved up to 20th place with Stephen Serene '12, right behind him. Roy Wedge '14, was in about 30th place, only five seconds behind Harper. Freshman Allen Leung was leading the way of the second group of Tech runners, but they were back about 20 places from Wedge. Considering where they started the group of Engineers had done well.</p>
                  <p align="left">However, the third mile hurt MIT. Harper stopped passing people. Serene and Wedge continued to move up, but the rest started falling back. Leung fell back 10 seconds during the mile, Ben Mattocks '12, 13 seconds, and Andrew Erickson '12, 20 seconds.</p>
                  <p align="left">The final two miles was more of the same, except Harper started passing people again and Wedge fell back a bit, losing nearly 20 seconds to Harper and Serene. Harper moved up to 12th place in 25:07, possibly qualifying for individual honors to go to NCAA's if the committee decides to take five teams. Serene was only two seconds back, running an excellent race, particularly given how far back he started. Wedge finished in 32nd place in 25:27 to earn All New England honors, but this was not a good race for the sophomore. Mattocks moved past Leung, but fell back to 66th position in 26:10. Leung was close behind in 26:14. </p>
                  <p align="left">Tech's score of 195 points was 20 points out of fifth place. The disappointment was palpable as expectations were so high due to the ability and readiness of the team. </p>
                  <p align="left">The women will move on to the NCAA Championship for the fourth consecutive year, hoping to have similar success as last year when after finishing fourth in the region they turned around and placed third at Nationals.</p>
