                  <p><strong><span class="headline">Alexandra Taylor Leads Engineer's to Title</span></strong></p>
                  <p><span style="font-weight: bold; font-size: 12px">Men Run to RPI's Strength and Lose Meet</span></p>
                  <p align="center">September 10, 2011</p>
                  <p align="left">Alexandra Taylor '14, won her first race as an MIT athlete and did it in extraordinary fashion, running nearly a minute PR, finishing the 5K course in 19:36 to win the <a href="eng.res.php">Engineer's Cup</a> by 15 seconds. Taylor's effort led Tech to the team title despite not running any of their top seven runners. MIT led the way with 23 points to 41 for RPI and 61 for WPI.</p>
                  <p align="left">Ben Mattocks '12, led the way for the MIT men, but the effort was not enough to see the team to victory. RPI claimed 1st, 3rd, 4th, 7th and 8th to score 23 points to 37 for MIT and 66 for WPI.</p>
                  <p align="left">The course was like a sponge after three consecutive days of rain, which certainly slowed the times by a minute or so. However the temperatures were pleasant with a dry 75 degrees with a slight breeze, so for this time of fall it was a very nice day for racing.</p>
                  <p align="left">The pack of women started a little too fast and it showed at the first mile as the leaders came through in 6:03. It was even more obvious the initial pace was too fast as only Taylor was able to come close to her first mile pace, hitting the two mile mark in 12:28, giving her a :06 lead over teammate Shivani Kaushal '15.</p>
                  <p align="left">By the end of the third mile Taylor had built an insurmountable lead at 18:56. Kaushal had fallen back to fourth as two from RPI moved ahead. MIT packed in four runners with Kaushal at 19:59, Claire O'Connell '14, in 20:06, Louise van den Heuvel '14, in 20:30 and Cindy Huang '15, in 20:33 to put the meet away.</p>
                  <p align="left">Despite not racing any of their top seven runners, the men from MIT could very well have won this race had they played to their strength, but instead they played to RPI's strength, allowing themselves to get passed in the latter stages of the race. Primarily middle-distance runners with exceptional mile speed, the MIT harriers were supposed to let RPI lead, and depending on the pace, take command of the race during the third or fourth mile. Showing no discipline, MIT was out front at the mile mark, coming through in 5:13, pretty fast for the course conditions with a big target on their back. By mile two, Justin Bullock '14, coming through in 10:46 along with teammates Mattocks and Logan Trimble '13 were just behind the lead RPI runner. RPI had about a dozen runners right behind, biding their time. Mixed in with the RPI runners were freshmen Chadd Kiggins and Kris Frey.</p>
                  <p align="left">By mile three the pace spread out the field as the RPI runners began to take control and walk down the fatiguing MIT runners, but many of them had also faded out the back. Bullock was still in the lead at 16:16, but was looking fatigued. Mattocks was two seconds back, but Trimble had fallen in arrears by seven seconds behind Mattocks. RPI was now in the lead with runners in 2, 4, 5, 7, 9 and 10 places, but Frey was catching back up to Kiggins to give MIT some hope. If MIT could have a strong final two miles they could get the job done.</p>
                  <p align="left">It was not to be as Bullock faded badly in the final two miles, ending up in 5th in 27:10. Mattocks had a very strong final two miles, running 10:26, but was outkicked at the finish by Jeff Corelli from RPI, both timed in 26:44. RPI packed in runners in 3rd, 4th, 7th and 8th for the 23 point total. Trimble had cramps in his calves, but still managed a strong final mile to catch up to Bullock for 6th. Kiggins finished in 11th while Frey ended up 13th.</p>
                  <p align="left">Next week MIT will race their top runners for the first time this season in the UMass Dartmouth Invitational. In 2010 MIT claimed second in both the men's and women's races and they hope to improve on that result in 2011.</p>
