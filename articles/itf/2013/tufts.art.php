              <p style="font-size: 12px"><span style="font-weight: bold">Men Dominate at Tufts Stampede, Women Claim Second But Dominate Distances</span></p>
              <p align="left">February 2, 2013</p>
              <p align="left">With the new NCAA indexing penalty for banked and oversized tracks (3+ for the mile, 9+ for the 5K, 8+ for the DMR), more and more teams are seeking the fastest of the flat tracks in order to achieve the best times. Due to the radius, Tufts is known to have one of the fastest flat tracks and 20+ teams gathered on Saturday to test it out.</p>
              <p align="left">MIT joined the fray at the <a href="tufts.res.php">Tufts Stampede</a>, hoping to both win the meet and qualify as many individuals as possible for the upcoming championships. The women started the meet with the 3000 and hoped to score big points while qualifying a few new individuals racing the 3K for the first time. The race plan was for Brooke Johnson '13 and Allison Hallock '16 to run 81 pace the entire way while Sarah Quinn '16 and Nicole Zeinstra '16 were to run 83 second quarters until only 1000 meters were remaining and then they could race if they felt strong enough to do so. Johnson and Hallock set the initial pace a little fast at 78 for the first 400. Quinn and Zeinstra were appropriately behind but too fast at a high 80. They then settled in on the correct pace. Johnson hit the 1600 in 5:19. Hallock had fallen off the brisk pace to 5:26. Quinn and Zeinstra had run the fourth quarter a little quick, coming through the 1600 in 5:27. At the prescribed 2000 mark, Quinn took off like she was running for her life, lowering her pace to 76 and then 73 as she passed Johnson, who was caught off guard and unable to respond to the momentum Quinn had going for her. Johnson smashed the previous MIT record of 10:05.97 by over four seconds with a 10:01.74. Quinn set both the varsity and freshman records with her 9:56.01 performance. Her final mile was a 5:11. MIT scored 36 points by sweeping 1-6 places.</p>
              <p align="left">The women collected four more victories during the meet. Lauren Kuntz '13 set a new indoor personal best in the pole vault with her victory, clearing 12-5 1/2, which currently has her ranked second in the country for Division III. This was Tech's second best event as they scored 25 points. Preethi Vaidyanathan '15 cleared 5-3 3/4 for the second week in succession to win the event, taking the victory over two higher ranked jumpers. Alexandra Taylor '14, coming back from a second place in the Mile Run with a 5:04.31, took the 1000 by taking the lead at the 600 mark and running a 73 final quarter to win in 3:00.98. The final victory was fittingly in the final event of the meet, the 4x800 Relay. Cindy Huang '15 (2:20), Hallock (2:24), Marie Burkland '13 (2:26) and Louise van den Heuvel '14 (2:24) opened up a big lead en route to 9:35.93 victory.</p>
              <p align="left">Isabella Stuopis '16 set yet another freshman record, this one an improvement on her Weight Throw record, moving the new mark to 45-1.</p>
              <p align="left">The Triple Jump was a successful event for the Engineers as all four jumpers qualified for the New England Division III Championship. Michelle Johnson '15 led the young jumpers with a third place 35-6, followed by Stephanie Birkhimer '14 in fourth at 35-1 1/4, Katharina Ross '16 in fifth at 34-10 1/4 and Veronica Szklarzewski '15 not scoring at 34-4.</p>
              <p align="left">The women finished a close second to the host Jumbos 166-155. The next closest team was Williams College with 95. This was a good preview of arguably the top three teams in New England Division III, the same three teams likely to do battle for the title in two weeks.</p>
              <p align="left">The men won their meet handidly, cruising to victory with 162 points. The closest team was Williams at 85.5 points. Tufts, a much stronger team than they showed did not compete many of their top runners. The top team in New England Division III, Bates College, was at another meet.</p>
              <p align="left">The men also won five events, dominating mostly with their long sprinters and middle distance runners, although two of their stars Justin Bullock '14 and Kyle Hannon '13 were inhibited by current or lingering illnesses.</p>
              <p align="left">Their two highest scoring events, the Triple Jump and 200 were not victories. In the Triple, Jackson Wirekoh '13 revisited seasons past, finding his rhythm to place second in 44-2 1/2. Carrington Motley '16, having difficultly being consistent, was far off his best of 46' as he finished third in 43-5. Angel Echevarria '16 claimed fourth at 42-8 3/4, fellow freshman Arinze Okeke '16 also jumped 42-8 3/4 for fifth and Adrian Samsel '16 landed at 41-11 1/2 to claim sixth.</p>
              <p align="left">In the 200, Tyler Singer-Clark 14, having difficulty finding his championship speed from last year, found some of it, taking second place in 23.02 out of lane three. Michael Kaba '16 claimed third in 23.16 and States Lee '16 took fifth in 23.26 even though he won his section.</p>
              <p align="left">MIT continued to show their strength in the running events with Lee taking first place honors in the 400 prior to his 200 effort. Lee's time of 50.96 was his best flat track indoor effort. Pat Marx '13 followed that up with a win in the 600, taking a close one in 1:22.27. Sam Parker '15, still nursing a tender knee, took fourth in 1:22.60.</p>
              <p align="left">MIT took both relay events, the 4x400 and the 4x800. In the 4x400, Tech put their four star freshmen together to go for the MIT freshman record. Kaba, Derrick Barnes '16, Samsel and Lee destroyed the record, running 3:24.28. Kaba started things off in 50.8. Barnes leg was a 51.7. Samsel ran a nice 51.3 and Lee brought it home in 50.4. In the 4x800, some of MIT's top runners were ailing, so a couple of replacements were called to step up and they got the job done. Jared Forman '13 doubled back with a 1:59.5 leadoff. Logan Trimble '13 did his job with a 2:01.1 as he gave MIT the lead. Harrison Hunter '15 faded a bit at the end, finishing in 2:03.1. Marx, getting accustomed to winning, outkicked the Tufts' anchor with a 1:57.5 to bring the team to victory in 8:01.29.</p>
              <p align="left">Matt Jordan '15 is really having a great season, demonstrating he knows how to not only run hard, but also to go for the win. In the 3000, he took the lead with 600 to go, pulling away to a 8:42.55 victory.</p>
              <p align="left">Even though he was sick, Bullock demonstrated the competitiveness a champion needs to have. While a few were asking not to compete, Bullock never complained, just stepped up and raced the mile. In a strategic race he claimed second in 4:24.04 as his legs were willing but the breathing was not there. In two weeks Bullock hopes to lead his team against the Bobcats from Bates and hopefully win their third title in four years. The women also hope to contend for the title despite the lack of sprinters.</p>
              <p align="left">Next week, MIT will be spreading themselves between a DMR invitation at Tufts, another major meet on the banked track at Boston University and their own meet at MIT, all three meets will be non-scoring.</p>
