              <p style="font-size: 12px"><strong><span style="">BULLOCK BREAKS VARSITY 3K RECORD</span></strong></p>
              <p align="left">December 1, 2012</p>
              <p align="left">Justin Bullock '14 was the lone entry for MIT in <a href="nu.res.php">Northeastern University's Jay Carisella Track and Field Invitational</a>, held at the Boston University Track and Tennis Center.</p>
              <p align="left">Not planning on entering anyone in the meet, MIT Coach Halston Taylor was talked into allowing Bullock to compete by Bullock himself. Bullock's last hard effort was the NCAA Division III New England Region Cross Country Championship three weeks ago. So with one week totally off, and no hard workouts the only thing Bullock had going for him was the confidence and desire to get a solid New England Division III qualifing time under his belt. With a relatively short regular season and the desire to qualify in all events from the 800 through the 5000, along with a national qualifying mark in the Distance Medley Relay, this meet made sense.</p>
              <p align="left">He did not waste the opportunity, running 66-67 second pace for most of the race, Bullock was in fifth place throughout the first mile, which he split in 4:27. With four laps to go Bullock moved into fourth place and then in the final 400 he threw down a 62 to move into third and close hard on the leaders. Bullocks 8:19.12 bests the MIT record set by Ben Schmeckpeper in 2005 of 8:20.69, run on the same track.</p>
              <p align="left">MIT has an unofficial Intrasquad Meet on 12/15, and an Alumni Meet on 1/12, but will not compete intercollegiately until 1/19 in the Harvard Open.</p>
