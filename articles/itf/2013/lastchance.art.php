              <p style="font-size: 12px"><span style="font-weight: bold">Konopka Improves Pentathlon Mark by Over 200 Points to Lead MIT Qualifiers to NCAA Division III</span></p>
              <p align="left">March 2, 2013</p>
              <p align="left">The final weekend to qualify for the NCAA Division III Championship on March 8-9, began with five MIT student-athletes making the trip to New York City to compete in the <a href="ecac.res.php">ECAC Division III Championship</a> at the New Balance Armory.</p>
              <p align="left">Jackie Konopka '14, seeded 16th in NCAA Division III Pentathlon field going into the meet, knew she needed to improve significantly to guarantee making the 15 member national field. She wasted no time in getting a strong start, running a personal best of 9.21 in the 60HH. In the second event, Konopka proceeded to add another PR, this time a 5-2 1/4 High Jump performance. In the Shot Put, she threw another personal best, 34-8 1/2. In the Long Jump she added a 1/4&quot; to her personal best, jumping 16-5 1/4. In the final event, the 800, Konopka surpassed even her coaches' expectations as she ran a 2:25.90. Her score, a new MIT record of 3453, the top score in the country for Division III. However, the penalty incurred by the banked track 800 reduced her NCAA qualifying score to 3432, three points behind the country's leader.</p>
              <p align="left">Her teammates were not able to make improvements, but had one more chance  at <a href="lastchance.art.php">Tufts University's Last Chance Qualifier</a>, which was held the next day.</p>
              <p align="left">Cimmy Virdi '16 took advantage of the second opportunity, clearing a season best 12' to move into a four way tie for 14th on the NCAA list. However, the NCAA only takes 15 women per event, 13 men and 10 teams per relay event. Ties are broken and with Virdi's second best vault only being 11-5 3/4, she will only make the NCAA field if two of the qualifiers ranked ahead of her do not enter the event.</p>
              <p align="left">The biggest event for MIT was the Men's Distance Medley Relay. Entering the competition they were sitting in 10th, but knew that place would not stand, so they went for an improvement, this time on a flat track. The anchor leg, Kyle Hannon '13, still affected by his walking pneumonia, realized he was not going to be able to get the job done and made the mature decision to give up his position to Justin Bullock '14. Logan Trimble '13 led things off in the stacked field, running 3:04.4 for the 1200. States Lee '16 made up a lot of ground with his 50.1 to move MIT into fourth place. Pat Marx '13 ran a superb 800 leg, closing up the rest of the ground on the leaders with his 1:52.3. Bullock went out too fast, running 29 for the first 200 and 60.9 for the 400. In the end this cost him dearly, but he still managed to hold onto a 4:13.2 to give MIT a 10:00.05, moving them up to number seven on the NCAA list and a lock to go to Nationals.</p>
              <p align="left">The women's 5000 was a much anticipated event as MIT was entering three very capable runners, all hoping to either break 17:00 or run very close to it. However, Brooke Johnson '13 was having plantar fasciitis issues and did not race. Elaine McVay '15, needed to improve on her 17:19 in order to make the Nationals field, but she had been sick all week. Although she was game her strength was not what it usually is. Sarah Quinn '16, attempting her first 5K had extremely tight calves but thought they would be okay for the race. Quinn set out to hammer 80 second 400's and did so very well through 2800 meters, coming through in 9:23. Her 3200 was 10:46 as her calves were not responding. She faded her final mile, running 5:41 even though she was not particularly tired. Quinn's final time was 17:09.15, a freshman record for MIT, putting her at number nine on the NCAA list.</p>
              <p align="left">Alexandra Taylor '14 won the 800 in 2:15.69 while Cindy Huang '15 ran a season best 2:17.80.</p>
              <p align="left">Marx won the 600 in 1:22.23, his second fastest time ever, less than two hours after the DMR. </p>
              <p align="left">States Lee, after running his DMR leg, came back to win the 400 in 50.32.</p>
              <p align="left">Many others came close to qualifying for the NCAA Division III Championship next weekend, but did not quite make it. Tech should be represented by the following individuals at Nationals. The women's DMR (Brooke Johnson, Cindy Huang, Alexandra Taylor, Sarah Quinn), the men's DMR (Logan Trimble, States Lee, Pat Marx, Justin Bullock), the men's 4x400 (States Lee, Tyler Singer-Clark, Derek Barnes, Pat Marx), Lauren Kuntz in the Pole Vault, possibly along with Cimmy Virdi, Quinn in the 5K and Jackie Konopka in the Pentathlon.</p>
