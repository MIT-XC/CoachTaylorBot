	  </td>          <td width="615" valign="top"> <div align="center">               <p><span class="headline">Jones Runs National Qualifying Time in 400 </span></p>              <p align="left">1/29/2010</p>              <p align="left">Portia Jones and Jamie Simmons were the only MIT athletes running at <a href="../itfsched.php">Boston University's Terrier Invitational</a>. Both of the athletes as well as the rest of their team have a four team invitational at Bowdoin College tomorrow. </p>              <p align="left">Jones ran 58.01 in the 400 to provisionally qualify for the NCAA Division III Championship. Jones finished eighth at the NCAA Championship last year and hopes to improve on that mark this year. Her race today went well, but if she had gotten the break it would likely have been better. Coming through the 200 in 26.9 was plenty fast enough, but the leader slowed dramatically, spiking Jones as she was beginning to try to pass her. Jones came around to take the lead off the final turn to win her section.</p>              <p align="left">Simmons was running her first ever 800, a frightening endeavor for a 400 runner. She got out well and put herself in perfect position, but the 31 second first 200 put a lot of doubt in her mind. By the time she came through 67 for the 400, she was out of the race and just hanging on. However, she never folded, holding on to run 2:22.26. This was a solid effort for her 800 debut.</p>              <p align="center"><a href="../itfsched.php">RETURN TO INDOOR</a>  </p>              <p align="left">&nbsp; </p>          </div>            </td>        </tr>        <tr>           <td>&nbsp;</td>          <td>&nbsp;</td>        </tr>      </table></td>  </tr></table><p>&nbsp;</p></body></html>