              <p style="font-size: 12px"><strong><span style="">MEN DEFEAT BOSTON UNIVERSITY, WOMEN TAKE DOWN NORTHEASTERN IN THE BATTLE OF BEANTOWN</span></strong></p>
              <p align="left">February 3, 2012</p>
              <p align="left">The last time MIT Track &amp; Field had a regular season scoring meet against Boston University, Harvard and Northeastern, It was 2002 and MIT was more dominant than all but Northeastern. Each indoor season during those early years last decade, MIT's men's program regularly defeated BU and Harvard. Since that time, both programs have new coaching staffs and their teams reflect those changes. The seeds going into the <a href="bob.res.php">Battle of Beantown</a> clearly had MIT finishing last for both men and women, substantially so. Knowing that many teams do not always perform to their seeds and understanding that outperforming seeds is the expectation at MIT, Tech went across the river to do battle at Harvard's Gordon Fieldhouse.</p>
              <p align="left">MIT demonstrated an exceptionally competitive spirit by most of their competitors, enough to set two varsity records, three rookie records, numerous personal bests and overcome their seeds to the degree the men were able to defeat Boston University and the women took down Northeastern University in a really close score. The final scores for the men were Harvard 191, Northeastern 156.5, MIT 93, and Boston University 66.5. Harvard also won the women's meet, scoring 196 to 176 for Boston University, 70 for MIT and 67 for Northeastern.</p>
              <p align="left">Two things were evident very early on in the meet; first, that Harvard had a very deep and strong team relative to the competition, and two, that many of the seeds submitted were not very accurate as MIT was outscoring their predicted score by a  large margin. </p>
              <p align="left">The star for MIT in the men's meet had to be Josh Duncavage '13. He had his best meet ever, setting personal bests in the 60 Meter Dash, the 60HH and the 200. Duncavage also anchored the 4x440 relay, closing with a relatively slow 51.0. In the 60 Meter Dash, Duncavage placed fourth in 6.99, easily an MIT record and the conversion would be well under the varsity record for 55 Meters. In the 60HH, Duncavage again placed fourth in his best hurdle race ever, finishing in 8.49, which would convert to a sub 8.00 in the 55HH. In the 200, he finished second, and would have won if he had an outside lane. His 22.35 was a personal best, missing out on first place by .01.</p>
              <p align="left">Tech had two winners in the meet; CK Umachi '12 took the Weight Throw at 61' 9 1/2&quot;, and Pat Marx '13 took the 500 Meter race in 1:05.36.</p>
              <p align="left">One of the most exciting races for the men was the 3K. Dan Harper '12, Allen Leung '15, Roy Wedge '14 and Benji Xie '15 ran together, working the race hard. Xie led the way for the Tech runners for most of the race, somewhat reluctant to make a big move to pull away from his teammates. Both Leung and Xie, mostly trying to score points for MIT, were also hoping to take down the rookie 3K record held by Bob Walmsley '84. Coming through the mile in 4:36, the foursome were on their way to a strong time. The pace slowed slightly to 70 second pace until Harper made a move in the final lap, closing in 32 seconds to claim fourth overall in 8:38.08. Leung took fifth and the rookie record in 8:38.39. Wedge closed out the scoring with a sixth place 8:38.65. Xie, doing most of the work, paid the price as he finished one place out of scoring with his seventh place 8:39.19.</p>
              <p align="left">There were  a few other great marks for the men. Nathan Peterson '12 set a season best in the Pole Vault, claiming second place at 15' 9&quot;. Justin Bullock '14 ran a personal best in the Mile Run, placing sixth in 4:14.64.</p>
              <p align="left">The women had the talent, but just not enough depth to compete with Harvard and Boston University. Portia Jones '12 earned two second places and anchored the 4x440 relay team to a season best 3:57.01. In the 400, Jones lost the break and that sealed the race, as she stayed in second the entire race, finishing in 57.13. In the 200, Jones had the inside lane, nearly guaranteeing she could not win. However, she did claim second, avenging her loss in the 400.</p>
              <p align="left">Jamie Simmons '12 crushed the MIT varsity record by three seconds in the 500 meter run, finishing in second place with a 1:15.79. Simmons added a 58.6 leadoff leg in the 4x440 relay. Also in the 500, Kristine Bunker '14, after placing fourth in the High Jump at 5' 2 1/2&quot;, ran her first 500, moving into MIT's top 10 list in that event with a 1:22.70. </p>
              <p align="left">Jackie Brew '14, had a big sprint day. First she ran to a personal best 7.96 in the 60 Meter Dash. She then ran 9.12 in the 60HH, currently tied for 14th on the NCAA Division III list. Brew also left the Triple Jump to run a 60.2 relay leg on the 4x440.</p>
              <p align="left">Cindy Huang '15, claimed fifth place in the 800 with a 2:17.84, good for a new MIT Rookie Record. Also running personal bests in the 800 were Alexandra Taylor '14, placing sixth in 2:19.79 and Margaret Coad '15, running 2:20.20.</p>
              <p align="left">Louise van den Heuvel '14, having hip pain after Tuesday's workout, and doubting her ability to race, put the worries aside and simply raced. In the Mile Run, she mixed it up with the leaders, coming through the first 440 in 72 seconds. Never backing off, van den Heuvel closed well to finish in a PR 5:00.07, good for fifth place and number five on the NCAA list. A little over an hour later, she returned to run a PR in the 1000 with a 2:58.91 for fourth place. Also in those races, Stephanie Marzen '15 ran a PR of 5:09.36 in the Mile Run, and Maggie Lloyd '12 dropped down in distance to claim sixth in the 1000 with a huge PR of 3:00.88.</p>
              <p align="left">Elaine McVay '15 and Dacie Manion '15 teamed up in the 3K to run a phenomenal race. McVay pushed the pace the entire race, coming through the mile in 5:20 and continued to push, getting outkicked in the final 400, but finishing in third with a new MIT Varsity and Freshman record of 10:05.97. Manion closed fast with a 38 final 200 to claim fourth and also run under the MIT Varsity and Freshman record with her 10:08.27.</p>
              <p align="left">Lauren Kuntz '13 moved up on the NCAA list to ninth place with her 11' 11 3/4&quot; second place finish in the Pole Vault. Also in the vault, Joanna Chen '15 had a PR of 10-11 3/4.</p>
              <p align="left">Michelle Johnson '15 added a point in the Triple Jump, punching a PR of 36'  on her final attempt. </p>
              <p align="left">Not placing, but throwing a huge, and significant personal best, Ashley Wheeler '15 continued to surprise with a 40' 2 3/4&quot; performance in the Weight Throw.</p>
              <p align="left">Next week, MIT again splits up the team, sending some top athletes to Boston University for the Valentine's Meet, while most field event athletes will be back at MIT for their final meet prior to the New England Division III Championship.</p>
