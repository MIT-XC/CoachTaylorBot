              <p style="font-size: 12px"><strong><span style="">SECOND FASTEST DMR IN NCAA DIVISION III HISTORY</span></strong></p>
              <p align="left">March 3, 2012</p>
              <p align="left">MIT sent seven individuals to the prestigious <a href="ic4a.res.php">IC4A Championship</a>, hosted by Boston University. CK Umachi '12 was entered in the Weight Throw, Sam Parker '15 and Chadd Kiggins '15 in the 800, and the DMR team of Justin Bullock '14, Tyler Singer-Clark '14, Pat Marx '13 and Kyle Hannon '13.</p>
              <p align="left">Kiggins and Parker were looking to run in the low 1:52 range in hopes of getting into the top 13 ranking for the NCAA Division III Championships next weekend. Kiggins was up first, and was fortunate to be in a fast heat. However, he never made a move when he needed to. He came through splits of 27.5, 56.0 and 1:24.8 before finishing in 1:55.93. Parker's heat was just as fast, coming through in very similar splits of 27.6, 56.1 and 1:25.2. Parker made a move but did so too late, having too much left when he crossed the line in 1:54.22.</p>
              <p align="left">Umachi threw well in the trials, making the finals easily. Having no fouls, Umachi placed third with a throw of 62-3.</p>
              <p align="left">The DMR could not afford to rest on their 9:55.71 performance, as it was ranked seventh on the NCAA Division III list and only 10 teams would be selected. Typically, a few teams run qualifying times on the final day. Loading up the team with their best possible foursome, the Engineers went for the best time possible in the trials on Saturday since Division III does not take qualifying times after this day. Bullock led off with a strong 1200 leg of 3:01.6, but was significantly behind New Hampshire and Penn. Singer-Clark ran an amazing 48.2 to close the gap and put MIT into second place. Marx not only caught the leader, but put 25 meters on the field with his 1:51.6. Hannon did was you are supposed to do in a relay when you have the lead--do not let them think they are catching you. He dropped a 59.5 and 2:02.5 to discourage anyone trying to close the gap. Not letting up, he finished in 4:07.5 to give MIT the heat win, fastest time in the trials and the second fastest time in NCAA Division III history with a 9:49.45.</p>
              <p align="left">MIT will take eight men to the NCAA Division III Championship being hosted by Grinnell College, March 9-10.</p>
