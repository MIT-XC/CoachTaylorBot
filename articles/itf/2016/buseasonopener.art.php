            <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">Men Qualify Four to New England Division III Championship in BU Season Opener</span></p>
            <p align="left">December 5, 2015</p>
            <p align="left">MIT took four of the men's distance squad over to the <a href="buseasonopener.res.php">Boston University Season Opener</a> with the purpose of achieving qualifying performances in either the 3K or 5K for the New England Division III Championships and all four were successful. &nbsp;Continuing their training from the recently completed cross country season and knowing they would need to qualify for both the 3K and 5K this season so that their team would have more options come championship time, the four were hoping to get one of these out of the way while they were race ready.</p>
            <p align="left">Rory Beyer '17 was the first to make the attempt and was doing so in the fast section of the 3,000. &nbsp;Beyer was hoping for a well-paced 8:30 or so, but the pace was too erratic. &nbsp;The first 400 was a slow 69 and then grew pedestrian to a 2:21 800. &nbsp;Finally the pace started to pick up with a couple 68's back to back and then the leader threw in a 65. &nbsp;Beyer went with the leaders but the 65 was a bit too much off of xc training. &nbsp;He was unable to stay with the leaders, dropping back to the middle of the pack as others kicked by him. &nbsp;He rallied for a 34 final 200 to finish in 8:37.12, a personal best for the junior.</p>
            <p align="left">The Engineers had three runners in the 5K, Brian Gilligan '17, Josh Rosenkranz '19 and Daniel Weiss '17. &nbsp;The qualifying time they needed to surpass was 15:30 for the New England Division III Championship and 15:24 for the ECAC Division III Championship. &nbsp;All three were in the slower of two sections, so there was no way to know if the opening pace would be fast enough. &nbsp;Fortunately the pace was crisp at 72 for the first quarter, and stayed that way through a 4:45 1600. At that point the pace slowed so that the second 1600 was 4:54, almost forcing Rosenkranz and Gilligan into the lead. &nbsp;Weiss fell off at the 2K mark and was six seconds back by the 3200. &nbsp;Gilligan did most of the work at this point with Rosenkranz on his heels for the next 800 meters before taking the lead for a short period. &nbsp;In the final 400 both were passed. &nbsp;However, Rosenkranz ran a 33 final 200 after having run a 5:02 final 1600 to finish second in 15:14.76. &nbsp;Gilligan ran a nice PR in 15:15.97. &nbsp;Weiss faded, but rallied enough to just eclipse the NED3 standard, finishing in 15:29.79.</p>
            <p align="left">The Engineers have an intrasquad meet next Saturday before entering a week of exams followed by a two week holiday break. &nbsp;They will pick up again on January 4 in preparation for their annual Alumni/ae Meet on January 9. &nbsp;Their first intercollegiate meet as a team will be on January 16 versus Bates and Colby at home.</p>
