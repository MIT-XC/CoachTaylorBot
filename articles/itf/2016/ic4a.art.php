            <h4><strong><center>
              <h3>Cameron, Wilson, 4x400 Set Freshman/Varsity Records - Eccles, Wilson and Zeinstra Add Names to NCCA DIII Qualifiers</h3></center></strong></h4>
            <p>&nbsp;</p>
            <p>March 4-6, 2016</p>
            <p>On Friday night of the final weekend to post a qualifying performance for the NCAA Division III Championship, MIT visited Tufts for their <a href="lastchance.res.php">Last Chance Qualifier</a>.&nbsp; MIT performed quite well, setting numerous personal bests in spite of MIT having a very heavy exam week.</p>
            <p>Probably the best performance of the evening was delivered by Mary Eccles '18.&nbsp; Still recovering from her 5K / 3K double two weeks ago and suffering from a cold, the sophomore refused to think about what could go wrong.&nbsp; Instead she jumped into the fast field, starting off running 82-83 400 pace, which she did through the first 1600 as she split 5:30.&nbsp; In the second 1600 the pace slowed a bit so she started going around people, moving up into&nbsp;the&nbsp;top six with a 5:36 1600.&nbsp; The final 1600 Eccles continued to pass people, moving all the way up to second with a 5:38 split and finished with a 41 final 200 to run a spectacular 17:25.09 to move her to #12 on the NCAA&nbsp;list.&nbsp; Eccles previous PR was a 17:48.</p>
            <p>The NCAA takes the top 15 men and 17 women for each event, as well as the top 12 relay teams.&nbsp; Ties are broken by second best performances for an absolute number per event.&nbsp; However, this means athletes do not know they are in until the qualifying period is over on Saturday.</p>
            <p>Scott Cameron '19 set a freshman record in the Pole Vault, clearing 15-5 1/2, finishing second in the event.&nbsp; In the women's vault, both Sherry Wan '16 and Jen Tylock '17 cleared personal bests as they both made 11-10 1/2.</p>
            <p>Nicole Zeinstra '16, running the final event of the day for MIT, the 3K, began a little fast in the first 200 but settled into the 78 second pace she planned on.&nbsp;At the 1600 she was 5:10, just a couple seconds fast.&nbsp; The second half of the race was more one of survival, splitting 82, 83 then 85 before rallying for a 38 final 200 to run a PR of 9:58.05, finishing third and moving up to 17th on the NCAA list.</p>
            <p>Other strong performances were delivered throughout the meet.&nbsp; Ariela Slutsky '18 in the Weight Throw threw a PR of 50-5 1/4.&nbsp; Carrington Motley '16 performed his season best of 46-10 as he won the Triple Jump.&nbsp; Motley's jump has him 17th on the NCAA list at the time of this article.&nbsp; If  two do not enter then he would be in.&nbsp; Tre Albritten '18 raced his first 400 ever.&nbsp; While making a couple strategic errors, Albritten raced with intensity, clocking 50.65.&nbsp; The sophomore is ranked 14th on the NCAA list in the 200.&nbsp; Should the&nbsp;Men's DMR make it in, and they are 13th at this time, MIT would need a substitute for the 400 leg as the DMR trials are immediately after the 4x400 Relay.&nbsp; Chris Sweeney '18 bounced back nicely from&nbsp;his hamstring injury as he finished second in the 60HH Final with a 8.29 PR.&nbsp; Haley Strouf '18 jumped a personal best 17-2 in the Women's Long Jump.&nbsp; Matt Deyo '16 continued his comeback from his stress fracture, showing the determination that earned him All America honors last year in both Cross Country and the 10K in Outdoor Track and Field.&nbsp; Deyo ran a reasonably consistent pace, running 1600 splits of 4:40, 4:45, 4:48 before closing in 33 to run a personal best of 14:46.36.</p>
            <p>At the <a href="ic4a.res.php">IC4A Championship</a> held at Boston University, primarily a Division I men's only championship, Jarod Wilson '19 lowered his own freshman record in the 800 trials.&nbsp; The freshman won his section in a fantastic 1:51.11 to move him to number eight on the NCAA list.&nbsp; The freshman will race the final on Sunday, but that time will not count towards Nationals.</p>
            <p>In the Mile, Aidan Gilson&nbsp;'19 raced to a personal best 4:14.38, but did not advance to finals.&nbsp; Kunal Tangri, another freshman, dropped off badly in the final 400, running 4:19.06.</p>
            <p>The Men's 4x400 were not only trying to improve their qualifying time, but also needed to qualify with Derek Barnes&nbsp;'16 on the relay, as the previous foursome included Jarod Wilson.&nbsp; Michael Kaba '16 led off, and surprisingly took the break very easily with a casual 22.9 opening 200.&nbsp; Not challenged and careful with his irritated hamstring&nbsp;he only managed a 49.3 split.&nbsp; The handoff to States Lee '16 was not the best as Lee has developed a habit of not looking the baton into his hand, but the baton did not appear to slow down through the exchange.&nbsp; Lee was passed, but unimpeded as he ran 22.2 and 47.9 to put MIT in a good spot time-wise.&nbsp; Derek Barnes '16 had an excellent exchange.&nbsp; His aggressive first 200 set up his overall race, which was the best of his indoor career, an&nbsp;excellent 48.4.&nbsp; The handoff to Danny Newman '17 hurt MIT as he left early, having to slow down significantly to receive the baton.&nbsp; Newman  was less than five meters behind the lead team, but did not gain or lose ground despite the slow first 200&nbsp;in 23.7.&nbsp; Newman moved a bit in the second lap but overall did not seem as aggressive as the past two weekends.&nbsp; Nevertheless his 48.9 gave Tech a new MIT record of 3:14.43.&nbsp; This performance moved the Engineers up to number three on the NCAA list.&nbsp; The quartet is excited to represent MIT another time at Nationals, especially knowing they left at least another second out there they can remove&nbsp;to lower their time.</p>
            <p>On Sunday, Wilson was back in action for the 800 final and he was certainly the only Division III runner in the field, if not the meet.&nbsp; He was in the outer alley, which allowed him the advantage of cutting in&nbsp;more safely, which he did in fourth place through a 26.1 200, he moved into third, making a slight mental error in the fourth turn, but came out unscathed with a 53.6 400.&nbsp; In the third 200 he moved well on the backstretch, moving into second place&nbsp;as he came through the 600 in 1:21.8 (faster than the MIT 600 record) and then moved well as he went for the victory.&nbsp; Coming off the final turn, although he was closing on the leader, another runner came up on him, nipping him at the line for second place with both timed in 1:49.70--a new MIT record&nbsp;and tying for the sixth fastest time in Division III history.</p>
            <p>Next Friday and Saturday the Engineers will compete at Grinnell College, in Grinnell, Iowa for the NCAA Division III&nbsp;Indoor Track &amp; Field Championship.&nbsp; At the time entries were made, the men will be taking Tre Albritten in the 200 (14th), States Lee in the 400 (8th), Jarod Wilson in the 800 (8th, as Sundays time does not count), Michael Kaba in the 60HH (13th), Lee, Kaba, Barnes, and Newman in the 4x400 Relay (3rd), and Arinze Okeke '17&nbsp;and Billy Ruschel in the Triple Jump (7th, 15th).&nbsp; Their is a possibility the DMR and Motley in the Triple Jump will get in.&nbsp; The women will bring Hannah Chen in the 400 (12th), Maryann Gong in the Mile (1st) and 3K (1st), Nicole Zeinstra in the 3K (17th), Mary Eccles in the 5K (12th), Chen, Gong, Bailey Tregoning '19 and Zeinstra in DMR (1st) and Cimmy Virdi in the Pole Vault (2nd).</p>
