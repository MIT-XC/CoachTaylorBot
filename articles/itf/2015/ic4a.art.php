              <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">Albritten Sets Freshman 200 Record</span></p>
              <p align="left">March 7, 2015</p>
              <p align="left">MIT competed in four events at the IC4A Championships, hosted by Boston University. The IC4A Championship is the oldest championship in the country, with the indoor championship starting in 1922.</p>
              <p align="left">First up for MIT was Colin Godwin '17 in the Mile Run. Colin had run 4:11.15 last week, but with the banked track conversion, was given credit for a 4:14.37. The time he would need to make the NCAA Division III Championship next week would be about a two second improvement. The Mile Run was set up as four trial heats with 10 advancing to tomorrow's final. Godwin was in the first heat, lane one. He was sort of asleep at the start, immediately finding himself in the back of the pack. With a quick burst he went around, positioning himself towards the front. Unfortunately the race was very strategic, in other words slow, as they came through the 800 in 2:14. He had been reacting to a lot of moves and kept getting stuck on the rail and ending up in the back again. So when the group moved he was not in position to immediately respond. He did run a 2:02 final 800 for a 4:16.75, but not what he needed to advance to Nationals.</p>
              <p align="left">Next for Tech was Tre Albritten '18 in the 200. Despite standing up at the start, and forcing it too much at the finish, he broke his own freshman record, posting a 21.99 time.</p>
              <p align="left">Benji Xie '15 represented the Engineers in the 5K. Having run a 14:30 earlier in the year, Xie needed to run about two seconds faster to guarantee a spot at Nationals. The early pace was fast, spreading the 23 runners out in a nice line, allowing Xie to tuck in without contact. His first 1600 was a 4:32 and he looked very strong despite the quick pace. The second 1600 slowed for the group in which he was running, giving him an 8:37 3K and 9:13 3200. As the fatigue set in, Xie was unable to prevent the pace from decaying to 71 quarters. As the kicks started in the final 800, Xie was only able to maintain the pace, finishing with a 35 final 200 after splitting 14:57 for 4800 meters. His final time of 14:33.12 means he will have to wait and see if some of those ahead of him on the list decide to run the DMR or 3K  if qualified in those events.</p>
              <p align="left">The final event for MIT was the 4x400 relay. MIT was sitting fifth on the DIII National list and wanted to move up. At the NCAA Championship the 4x400 is run as three sections of four. Having one of the fastest four times means they would get into the fastest and final section next Saturday. Running fresh, States Lee '16, Derek Barnes '16, Sam Parker '15 and Michael Kaba '16 were hoping to run in the mid 3:14's or better. Their best to date was 3:15.20. Lee started out with the intent of getting the break, but was not aggressive enough on the homestretch. He was a half step behind the inside runner as the outside runners crashed down on him, trying to edge him out. Lee stumbled, ending up behind everyone. On the backstretch he went to pass on the inside, and while successful, was bumped and slowed again. By the time he handed off the time was a dismal 49.4. Barnes went out hard, but relaxed. He took the baton in third place and handed off in third, splitting 48.6. Parker lost a place, but ran a solid 49.0 leg. Kaba took off in a tie for third, but could not get around the runner. About this time his achilles started bothering him. After a slow 23 first 200 that saw him lose significant ground to the other anchor legs, Kaba backed off, finishing in 50.5 for a cumulative time of 3:17.57. Given that Lee can run at least a second faster and Kaba two seconds faster, there is still hope the foursome will put it together at the NCAA's.</p>
