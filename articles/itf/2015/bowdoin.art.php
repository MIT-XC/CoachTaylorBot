              <p style="font-size: 12px"><span style="font-weight: bold">Engineers Remain Undefeated, Led by National and Regional Level Performances</span></p>
              <p align="left">January 17, 2015</p>
              <p align="left">Entering the week of the <a href="bowdoin.res.php">Bowdoin College Invite</a>, it was clear to the MIT coaches that the men's and women's indoor track &amp; field teams were suffering through an abnormal number of injuries and illnesses, everything from mono to foot injuries to hamstring strains to appendicitis. In total, six women and 10 men would be missing the weekend competition. The number of points Tech would give up left them concerned about their ability to win the meet. In addition, MIT would not be trying to maximize their points as there were some qualifying needs that needed to be taken care of regarding New England Division III and NCAA Division III Championships. So, when the performance lists came out Friday evening it was a big relief to realize the Engineers should not have to worry about the outcome of the meet and could focus on their performances, and that they did.</p>
              <p align="left">When the dust settled at the end of the day, and the hoarse voices from MIT athletes ceased their never ending cheering, the women won 14 of 19 events, scoring 283 points to 105 for WPI, 95 for host Bowdoin College, 56 for Springfield, 41 for RPI and 4 for Colby-Sawyer. The men cruised to a similar margin of victory with 11 victories in 18 events, scoring 204.33 to 102 for WPI, 95 for Bowdoin, 91.5 for RPI, 42.83 for Springfield and 19.33 for Colby-Sawyer. </p>
              <p align="left">Sarah Quinn '16 led things off with a 16:56.65 5K victory, setting the MIT record by nine seconds on a flat track and leads the country for DIII by over 25 seconds at this time. She hammered out 40 second laps through 3200 meters, hitting the 1600 in 5:20 and 10:42 for 3200 before slowing a bit in the final 1600. Quinn returned at the end of the meet to anchor the 4x800 to victory with a 2:20.5 split. Teammate Elaine McVay '15 added a very strong 17:45.49 for a second place finish.</p>
              <p align="left">Hannah Chen '18 was responsible for 28.5 points, winning the 60HH in a personal best of 9.37, the 200 in 26.59, placing third in the 60 meter dash in 8.12 and anchoring a come from behind effort in the 4x400 with a 59.2 leg to win in 4:12.27.</p>
              <p align="left">Not to be outdone, Maryann Gong '17, won the Mile with a national leading time of 4:57.10, took the victory in the 800 in 2:19.67 and ran a 63 split on the third leg of the 4x400 Relay. </p>
              <p align="left">Christina Wicker '17 placed second in the Mile in 4:58.48, the third best time in NCAA Division III, then came back to win the 1000 in 3:03.41 and led off the winning 4x800 Relay in 2:23.</p>
              <p align="left">Nicole Zeinstra '16 added to the middle-distance, distance domination by first placing third in the Mile with a conservatively run 5:09.00, as she saved a little something for her 10:09.65 victory in the 3K.</p>
              <p align="left">One  victory came as a surprise. Cindy Huang '15 in her first race back from injury, and fatigued from a red-eye flight, returning from a job interview, put all that aside to win the 600 in a personal best of 1:40.44. She then ran a 2:20.7 third leg on the 4x800 Relay.</p>
              <p align="left">In the field events, Cimmy Virdi '16 won the Pole Vault on misses at 12-3 1/2, the second best vault in the country for Division III. Ellen Liverpool '15 took first place honors in the 20 lb. Weight Throw at 51-10 1/2. Isabella Stuopis '16 made it a sweep of the throwing events by winning the Shot Put at 42-3 1/4. Natalie Alper '17, competing in the Long Jump for the first time as a collegian, led a 1-2-3 sweep with her 16-10 3/4 leap.</p>
              <p align="left">There were many excellent non-winning, but placing performances by the Engineers. Of particular note were two personal bests by Jackie Vahey '17. Vahey placed fourth in the Weight Throw at 46-2 1/2 and third in the Shot Put at 39-1.</p>
              <p align="left">Michael Kaba '17 led the way for the men, claiming three individual victories to score 30 points in the 10-8-6-4-2-1 scoring format. Kaba had to run trials in both the 60HH and the 60 Meter Dash to earn his way into the finals. His first final was the 60HH, which he won in 8.54. His next final was the 60 Meter Dash, which he won with a new personal best of 7.13. His final event was the 200, which he won in a narrow victory over teammate Luca Cacopardo '18, the winner of the 400 in 51.08, 22.62 to 22.67. Kaba's 200 time is not only a personal best, but faster than he had even run on a banked track in the past.</p>
              <p align="left">Matt Deyo '16 won the Mile in a personal best of 4:22.55. Although he did not win in his second event, he did earn second in the 1000 in 2:35.91 as the distance runner came down to work on some speed.</p>
              <p align="left">Also in the distance arena, Benji Xie '15 led wire to wire in the 5K, taking first place honors in 15:04.79. Xie ran this effort by himself as the closest competitor was 14 seconds back.</p>
              <p align="left">Three of MIT's victories came from the six field events. Marshall Wentworth '16 won the Pole Vault at 13-1 1/2, having very solid attempts at 13-7. Freshman Billy Ruschel jumped a personal best of 45-2 1/2 to take a one inch victory from teammate Angel Echevarria '17. Ken Cooper '15 earned first place in the Shot Put at 51-6 1/4. Cooper threw a season best of 57-3 3/4 in the Weight Throw but was only able to garner third place in the very talented field.</p>
              <p align="left">The Engineers won both the 4x400 and 4x800 Relay to put a nice ending to the meet. In the 4x400, Chris Sweeney '18, who had earlier run a personal best of 8.68 in the 60HH for second place, led off with a quality 52.0 leg. Danny Newman '17 ran a 52.8 second leg, handing off to Brandon Corts '17, who also ran a 52.8 to bring the baton in just about even for the lead as he handed off to Derek Barnes '17. Barnes, who had earlier thought he won the 400 in 50.54, but had been disqualified for running on the lane line, anchored the relay home with a 51.0 for a 3:28.84 cumulative time. Dan Kilcoyne '16, fresh off injury and a fourth place finish in the 800 earlier in the day, led off in 2:00.9. Rick Paez '15, who had placed second in that same 800 earlier in the day, ran the second leg in 1:59.6. Henry Tareque '17 faded a bit in the third leg with a 2:04.0, but anchor leg, Sam Parker '15, who ran a personal best of 1:22.04 in placing second in the 600 earlier, took the baton and cruised to victory in 1:57.3 for a fine cumulative time of 8:01.67.</p>
              <p align="left">Next weekend, MIT will host the Art Farnham Invitational. The goal will be to rest many of the runners and limit most of the others to one or two special efforts as the Engineers prepare for the stretch run leading to the Championships that start on February 20.</p>
