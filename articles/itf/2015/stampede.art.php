              <p style="font-size: 12px"><span style="font-weight: bold">Cooper and Okeke Lead MIT With Double Victories</span></p>
              <p align="left">January 31, 2015</p>
              <p align="left">In a split-squad weekend, MIT Track and Field took to both the John Thomas Terrier Invitational at Boston University and the <a href="stampede.res.php">Tufts Stampede</a>, hosted by Tufts University.  Following a Friday night full of stunning performances put together by the MIT Women’s team at BU, the heavily field event-dominated MIT team went to Tufts to improve their standings in both the New England Division III and National Championship rankings.</p>
              <p align="left">Overall, both the Men and Women’s teams dominated and competed well despite the fact that there was not much competition in many events.  The large meet posed a challenge for many to keep their competitive edge throughout the competition.  In the end, it was the support provided by the uncharacteristically small group of MIT athletes that helped to maintain the competitive focus of the team, as the Gantcher Center quickly became filled with chants of “MIT”.                </p>
              <p align="left">Luke Gray ’18 started everything off for MIT by performing in his first heptathlon, which began on Friday afternoon.  Gray put together solid performances in his first six events, with personal bests in the Pole Vault (12-3 ½) and High Jump (5-2 ¼).  With the 1000m his final event, Gray gutted out a strong performance of 2:59.83 to finish with a point total of 3965 and a 6th place finish.  Gray will now wait to see if this performance will be within the top 14 in New England, giving him the chance to compete at the New England Division III Championships in three weeks.                </p>
              <p align="left">The highlight of the day for the Men’s team was Ken Cooper’s dominating performance in both the Weight Throw and the Shot Put.   After spending much of the early part of the season starting slow with his Weight Throw technique, Cooper ‘15 showed that his work is paying off, throwing a season best 58-6 ¾ and beating his competition by over 8 feet.  Cooper then returned with an even more impressive performance in the Shot Put, recording a throw of 54-1 ¾.  With his performances this weekend, Cooper is now ranked 8th in the nation in Shot Put and 9th in the Weight Throw.                </p>
              <p align="left">In the jumps, Arinze Okeke ’17 stole the show with a fantastic double in the Long and Triple Jump.  Okeke started his day by jumping a lifetime best of 22-3 ¾ in the Long Jump, taking the win over teammate Carrington Motley ’16 who finished with a jump of 20-11 ¾ after having foul trouble with two jumps that would have been out over 22’.  Okeke then returned and, for the second week in a row, took the Triple Jump title with an indoor PR-tying 46-10 ¾.  With his performance in the Triple Jump, Okeke is now ranked 7th in the country.  MIT went on to take 6 of the top 7 spots in the Triple Jump, continuing their domination of opponents in the event.                </p>
              <p align="left">On the track, the MIT Men posted several season and personal bests to improve their standings for the New England Division III Championships.  In the 5K, Allen Leung ’15 went out with the intention of running under 15:10 in an effort to get seeded in the fast heat for New England Division III Championships.  Leung accomplished his mission, hanging in with a small pack behind a lead group that had a rabbit.  Leung fought hard, was gapped with 1K to go, but hung on for a strong performance of 15:03.59.  
                
                In the Mile, Nicholas O’Connell ’18 had his best race of the season, running a smart, tactical race, kicking from 7th to 4th place in the last 200m.  O’Connell’s time of 4:22.37 is a season best for the freshman.  Other notable performances were put forth by Michael Picchini ’18, establishing a personal best of 2:35.85 for fourth place in the 1000m and Henry Tareque ’17, who also established a new personal best mark in the 600m with a time of 1:25.15.</p>
              <p align="left"> On the Women’s side, the highlight of the day came in the High Jump with teammates Preethi Vaidyanathan ’15 and Natalie Alper ’17 battling it out with WPI’s nation-leading High Jumper, Laura Pumphrey.   Both Vaidyanathan and Alper cleared 5-3 ¾ with ease, offering them both the chance to attempt what would have been a new school record bar of 5-5 ¾.  Unfortunately, neither could clear the bar, with Vaidyanathan and Alper finishing 2nd and 3rd, respectively, to the All-American Pumphrey.                </p>
              <p align="left">In what has now become customary, the MIT Women’s Throwers took the top spots in both the Shot Put and the Weight Throw.  In the Shot Put, Jackie Vahey ’17 continued her upward climb with a personal best throw of 40-9 ½, dominating the rest of field and beating them by over two feet.  In the Weight Throw, Ashley Wheeler ’15 continued her consistently strong indoor campaign with a winning throw of 47-4 ¼, while Vahey threw 46-6 for 2nd place.                </p>
              <p align="left">In the horizontal jumps, MIT took 4 of the top 6 spots in a very unexciting Long Jump competition.  For the second time this season, Alper was the top Long Jump finisher for MIT, placing 2nd with a jump of 16-0 ¼, to finish off another strong double.  While not being challenged by any competitors in the field, Michelle Johnson ’15 stayed focused and continued her winning ways in the Triple Jump with an effort of 35-11 ¼.                </p>
              <p align="left">On the track, Kayla Harris ’15 continued her impressive improvement by dropping her 60m time by .08, finishing with a new personal best time of 8.02 and edging her teammate Veronica Szklarzewski ’15 for 3rd place overall.  In the 200m, Harris fought off a poor start and put together another strong performance of 27.02 for 3rd place.  In the Mile, Manolya Altan ’18 bolted to the front of her section and never was challenged for the lead.  Altan posted a 16-second improvement to her season best with a time of 5:36.59.  Last but certainly not least, Mary Eccles ’18 employed a consistent pace to wear down most of her competition, placing second in a new PR of 18:17.94.</p>
              <p align="left"> Next week, the MIT Men and Women once again will be split as some athletes will travel to Tufts University for the Cupid Challenge while others will head to Boston University for the Scarlet and White Invite.  The team will look for further improvement in what has been a fantastic, record-breaking weekend.</p>
