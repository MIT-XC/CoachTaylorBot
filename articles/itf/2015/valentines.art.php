              <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">MIT Prepares For Championships With Personal Bests</span></p>
              <p align="left">February 13-14, 2015</p>
              <p align="left">The only goal facing the Engineers at the <a href="valentines.res.php">Boston University David Hemery Valentine's Invitational</a> and <a href="GordonKelly.results.pdf">MIT's Gordon Kelly Invitational</a> were to better position themselves for the upcoming championships. At the conclusion of both meets the men's and women's teams were very successful in their goal.</p>
              <p align="left">Day one at BU was the women's show with the exception of the men's DMR, which was the first event of the day. The Engineers did not know if they had a foursome that had the mental and physical ability to run a low 9:50, which is approximately the banked track time (8+ second penalty) it will take to make the 10 teams selected for the NCAA Division III Championship, but they wanted to give it a try. They also needed to run a fast enough time to get into the fast section for the New England Division III Championship next weekend. Sam Parker '15 led Tech off with the 1200 leg. The pace was a little slow for the standout middle-distance runner and this caused him to feel dead in the legs. He was unable to correct the pace and slid back to an uncharacteristic 3:06.2 States Lee '16 took care of business with an outstanding 47.6 leg. Rick Paez '15 had 800 duty and he did his job well. Going out aggressively with a 55 first quarter, Paez continued to race his way towards the front, running 1:54.0. Colin Godwin '17, the least confident of the four, went out to get the job done, running 60 for the first quarter and 2:03 for the 800. At that point he slowed to a 66 as he gave into his fears. He snapped back to close in 62 and a 4:11 overall 1600 to lead MIT to a season best 9:59.86. One objective was certainly accomplished and now the Engineers know they have a strong shot at making the top 10 teams if they all run their best.</p>
              <p align="left">The women started the meet in fine fashion. Kayla Harris '15 ran her first 400 at BU, clocking a 59.23. Sam Fierro '18, running her first race of the year as she returns from injury, faired very well, running 61.90.</p>
              <p align="left">In the 800, Cindy Huang '15 continued her comeback from a foot injury, running a very well paced race. She came from dead last, and perhaps finished with too much left as she was passing runners all the way to the finish in 2:14.00. Maryann Gong '17 came down from her distance running events to try her hand at the 800. She went out a little quick, running a 32 first 200, but managed things well. She took the lead at 300 to go when she probably could have done so at the 400. Finishing with much too much left in 2:14.78, a huge personal best.</p>
              <p align="left">In the 200, Hannah Chen '18, despite running 9.37 in the 60HH earlier and drawing the disadvantage of lane three, ran a PR of 25.75. Kendra Knittel '17 found her groove and ran a personal best of her own, touring the oval in 25.82. Harris returned to the track and ran 26.39 for her best this season. Annie Dai '18 closed out the event for MIT, running a season best 27.15.</p>
              <p align="left">Christina Wicker '17 ended up in the lead in the 1000, although she certainly did not want that position. She went out too fast, running 32.5 for the first lap, so naturally no one wanted to take the lead at that point. Running 36-37 for the next three laps, others eventually moved up to challenge her. Wicker did not hold the lead but did fight back to finish in 2:56.28. Liz Cox '18 ran a near perfect race, moving up gradually throughout the race, eventually taking the lead, finishing with a 34 final 200 to win in 2:58.85. </p>
              <p align="left">The final event of day one for MIT was the 4x400 Relay. Both Harris and Huang felt running the relay would risk further injury so Knittel and Dai replaced them, joining Gong and Chen to see if they could get under 4:00 and move into the fast section in next week's championship. Knittel, continuing with her impressive day, opened up with a fine run of 60.3. Dai, not feeling her best, still ran 60.2 to put their team in good position. Gong competed very well, running 61.3, an excellent leg for a distance runner. Chen did her thing, catching people and running away with the victory as she anchored a 57.2, giving MIT a 3:59.05.</p>
              <p align="left">Day two was for the men and started with the 60HH. Michael Kaba '16, nursing an achilles problem, managed an 8.60, just off his best for the season.</p>
              <p align="left">Henry Tareque '17 was the first oval runner for MIT. He had been running the 600, but was moving up to the 800 to see if he would be a good relay runner in the upcoming championship. Tareque got out well and competed well throughout the race, dying off a little in the final lap to finish in 2:00.11</p>
              <p align="left">Derek Barnes '16 and Danny Newman '17 represented MIT in 400. Barnes hit the 200 in 22.9 and looked great. His final lap he tried to do more than his body wanted, which tightened him up a bit. Nevertheless he finished with a new indoor best of 49.10. Newman went out much slower, thinking instead of running, hitting the 200 in 24.00. He competed well in the final lap, finishing in a personal best 50.93.</p>
              <p align="left">MIT had four entries in the 1000. In the best to slowest format, Dan Kilcoyne '16 was up first. Kilcoyne got out fast, but when he went out in 28 and was not moving up to the two leaders he got a little discouraged and backed off, probably too much. He stayed competitive, moving up well in the final lap to finish in a personal best 2:28.74. In the next section, both Godwin and Paez returned from the DMR the day before. Both were towards the front and both split a 2:00.5 at the 800 as they were tied for first going into the final lap. Godwin closed well to win, but Paez tightened up and slowed. Godwin's time was 2:29.87 and Paez ran 2:31.86. Michael Picchini '18 went out a little fast in his section at 28 for the first 200 and pretty much slowed from there, finishing in 2:34.42.</p>
              <p align="left">The final individual event for the Engineers was the 200. States Lee produced the best time for the Engineers, running 22.09 out of lane five, a big indoor personal best for the junior. Kaba drew lane three in the same section, running 22.11. He had run 22.04 two weeks earlier, but had the advantage of lane six in that race. Luca Cacopardo '18 had an equally solid race, running one hundredth off his best at 22.14. Tre Albritten '18 ran a nice PR, clocking 22.38.</p>
              <p align="left">The last event, the 4x400, featured Lee leading off, Barnes running second, Parker in for Kaba, whose achilles was too tender to take a risk, and Cacopardo anchoring. Lee continued his outstanding weekend, leading off with a perfectly run 47.9 and a very well executed handoff to Barnes in first place. Barnes went out a bit slow in 23.1 and by the 300 mark was in fourth place. This pushed Parker to lane four for the exchange. Parker did not get out well at all causing the exchange to be too close to his body and he missed it twice before grabbing it 10 meters after the initial attempt. This gave Barnes a 49.8 split that was actually more like mid 48. The exchange by itself cost MIT around 15 meters. Parker ran a strong 49.7 leg, but the damage was done. Cacopardo had no one to race, but still ran 49.9 to bring the baton home in 3:17.32.</p>
              <p align="left">Next week the men will be at Springfield College to defend their New England Division III crown. The women will be at Smith College trying to break five consecutive years of finishing second in the New England Division III Championship.</p>
