              <p style="font-size: 12px"><span style="font-weight: bold">ENGINEERS SWEEP VICTORIES AT SPRINGFIELD COLLEGE INVITATIONAL</span></p>
              <p align="left">January 22, 2011 </p>
              <p align="left">MIT entered the nine team <a href="springfield.res.php">Springfield College Invitational</a> seeded first in both the men's and women's meets. Although the men were favored by a comfortable margin, that changed when two sprinters came up with minor injuries, forcing them to drop out of the meet, taking their points in the 55, 200 and 4x200. The women were only favored by three points over Tufts and had concerns about being able to defeat the much larger team from Tufts. Fortunately, the Engineers improved significantly over their seed performances while it appeared the seeds for the other teams were a bit optimistic. The men won the meet with 215 points to 99 for Tufts, 92 for host Springfield, 77 for Wheaton, 74 for UMass Dartmouth, 67 for Coast Guard and Ramapo and 39 for Westfield State. The women from MIT scored 206.5 to 139.5 for Tufts, 99.5 for Ramapo, 96 for Wheaton, 75 for Springfield, 36 for Westfield State, 31 for UMass Dartmouth, 26.5 for Coast Guard and 26 points for Smith.</p>
              <p align="left">The first running event was the 4x200. MIT's women, still without Martha Gross '12, due to a back injury were hoping to put together a sub 1:50 performance. Portia Jones '12, anchored the team with a superb 24.7 leg that brought the Engineers to within 1.37 seconds of the victory. An amount of time easily made up with the substitution of a healthy Gross. The men had to start the meet on a poor note as they had to scratch the event.</p>
              <p align="left">The second event set the tone for the rest of the meet for the men as Dan Harper '12, and Roy Wedge '14, went 1-2 in the 3000 with times of 8:47.18 and 8:47.53 respectively. At the same time, Ken Cooper '13, and CK Umachi '12, took first and second place honors in the 35 lb. Weight Throw with throws of 57-7 and 52-9 1/4.</p>
              <p align="left">The women came up with a second and third in the 3000 with excellent performances by Martina de Geus '14, and Maggie Lloyd '12 running 10:23.86 and 10:34.38 respectively. In the field events, the pole vault went first for the women and MIT dominated as they usually do in this event. Karin Fisher '11 (12-7 1/2) and Hazel Briner '11 (12-3 1/2) both hit the provisional mark for the NCAA Championships as they placed first and second. Fisher's performance is a new record for MIT. In addition MIT also picked up  fifth (Lauren Kuntz '13), sixth (Rebecca Stavely '11) and eighth Kate Wymbs '14) places in the event.</p>
              <p align="left">The men continued to dominate with both expected and surprise performances. Babajide Akinronbi '14, who has gone from 6.87 to 6.81 to 6.78 in his first two meets, continued the movement towards faster times by running 6.65 in the 55 meter dash trials and then 6.60 in the finals to claim 2nd place. Akinronbi also dropped a half second in the 200 with a 23.37. As expected, Pat Marx '13, busted out a 1:23.19 600 victory on a track with extremely tight turns, but everyone but Marx was surprised by his 49.8 anchor in the 4x400 that placed second. Winning his section of the 800, freshman Justin Bullock continued to surprise everyone as he dropped a 2:00.19 800, good for fifth place overall.</p>
              <p align="left">Other excellent performances by the men were led by Logan Trimble '13, who showed for the second week in succession that he is not a fluke. Trimble annihilated the field in the 1000 with an even split 2:32.18. He came back to anchor the 4x800 team to victory with a 1:59.2 leg. Cooper added a victory in the Shot Put with a personal best of 50-2 1/2. Matt Falk '12, continued his winning ways, claiming the high jump with his jump of 6-3 1/4. Kyle Hannon '13, came from the back to win the mile in 4:25.35 with a 62 second final 400.</p>
              <p align="left">For the women, Portia Jones had an excellent day on the tight turned track. After qualifying for the NCAA Championships in the 55HH in 8.36, a mark that was not satisfying given that she lost an individual race for the first time this year, she took out her frustration on the 200 field as she ran 25.93, easing up in the final 20 meters and still winning by .8. In her final event, the 4x400, Jones anchored a 59.2 to secure the victory with a 4:04.30.</p>
              <p align="left">In addition to her NCAA qualifying vault, Briner, set a personal best in the 55 dash twice as she ran 7.65 in the trials and then 7.64 in the finals to place seventh. She then popped a 17-6 1/4 leap in the long jump to place second and set a new MIT record. Briner capped her day with a superb 59.8 personal best leg in the 4x400 to set up Jones.</p>
              <p align="left">Jackie Brew '14, continued to improve for MIT. Her busy day included personal bests in the 55 dash, the 55HH, the 200 and the long jump. Brew's 7.54 in the 55 dash was a new freshman record. All three of her other marks (8.74, 16-2 1/4, 27.65) place her among the top ten all-time at MIT.</p>
              <p align="left">Louise van den Heuvel '14, took a double victory with two new personal bests. Her 5:15.96 won the mile going away, leading a 1-3-4-5 MIT near sweep (Olivia Papa '14, 5:20.54, Andrea Nickerson '14, 5:21.22, Alina Gatowski '11, 5:22.34). She came back to sprint away with the 1000 in 3:07.23, this time leading a 1-2-3 sweep (Nickerson 3:10.28, Gatowski 3:12.18).</p>
              <p align="left">Papa won her own event, running 2:27.80 in the 800. Jamie Simmons '12, claimed the only other individual victory, running a season best of 1:40.22 in the 600. Simmons also ran a 27.6 leg in the 4x200 and a 61.7 leadoff in the 4x400 relay.</p>
              <p align="left">Next weekend, MIT will split their squads. Some of the women will compete at BU on Friday in the Terrier Classic and others will compete at MIT on Saturday in the MIT Coed Invite I. Most of the runners on the men's team will compete at BU on Saturday while the field event specialists and remaining runners will be at MIT.</p>
