              <p style="font-size: 12px"><strong><span style="">MIT STARTS SEASON WITH VICTORIES OVER BATES AND COLBY TO GO 2-0</span></strong></p>
              <p align="left">January 15, 2011 </p>
              <p align="left">The MIT Engineers Track and Field teams traveled to Lewiston, Maine on Saturday to compete against host <a href="mittri.res.php">Bates College and Colby College</a>. Both opponents typically give Tech challenges, but this particular year, MIT just had too much quality and quantity for the Bobcats and White Mules. MIT pulled ahead early and kept the momentum going, winning easily. The men won seven of the 17 events, scoring 192 points to 116 for Bates and 45 for Colby. The women did even better, winning 13 of 18 events, scoring 216.5 to 85.5 for Bates and 76 for Colby.</p>
              <p align="left">Ken Cooper '13, led the way for the Engineers, although he placed second in both the 35 lb. Weight Throw and Shot Put. His 59- 2 1/4 toss in the weight earned him a solid NCAA provisional mark for the indoor championship. His 50-0 shot put made him only the seventh MIT track and field athlete to ever throw the indoor shot over 50'.</p>
              <p align="left">The meet scored six places per event, assigning 7 points to first, with the remaining places earning 5-4-3-2-1 points respectively, and no team could score more than four places in an event. MIT earned the maximum amount of scoring places in five events. While they did not win the pole vault, Nathan Peterson '12, led Tech to places 2-3-5-6 with his 14-1 1/4 vault. In the Triple Jump, Mattias Flander '11, led the way to a sweep of the top four places with his 44-0 3/4 leap. Brent Johnson '14, continued to shine as a first year hurdler with his 8.40 performance, leading a 3-4-5-6 performance in the 55HH. David Way '13, took control of the 600 and led wire to wire in 1:26.05 to lead Tech to a 1-2-3-4 sweep. In the Mile Run, Logan Trimble '13, had a major breakthrough, winning in 4:19.91 to lead MIT to a 1-3-4-5-6 domination of the event.</p>
              <p align="left">Additional victories were earned in the High Jump by Matt Falk '12, the 200 by Tyler Singer-Clarke '14, the 800 by Trimble and the 4x400 team of DJ Ronde '13, Nick Leonard '11, Mac Gager '14, and Pat Mark '13. In the High Jump, Falk cleared a personal best of 6-3 1/2 to take the victory away from teammate CK Umachi '12, who also cleared 6-3 1/2, a PR of his own. In the 200, Singer-Clarke used lane five to his advantage, taking victory away from teammate Josh Duncavage '13, 23.25 to 23.45. Trimble earned his second victory with a dominant 800, although the time was not fast (2:00.43). The 4x400 had modest splits of 53.1, 54.7, 52.5 and 51.2 to win in 3:31.74.</p>
              <p align="left">Karin Fisher '11, led the way in qualifying performances for the women, as she won the Pole Vault with an NCAA provisional qualifying mark of 12-2. Teammate, Hazel Briner '11, also qualified provisionally with her 11-7 3/4 vault. Portia Jones '12, did not qualify for the NCAA Championships, yet, but she did win three events in convincing fashion. She ran 7.52 in the 55 dash, 8.60 in the 55HH and 26.16 in the 200 to score 21 points.</p>
              <p align="left">The Engineers dominated the field events, only losing the throws events. Megan Bumgarner '14, was not only a surprise winner, but also tied the varsity record and set the freshman record with her leap of 5-01 3/4 in the High Jump. She also pulled out a nice victory in the 400 with her 63.76. Briner had a big day, winning the Long Jump in 16-6 1/2 in addition to a second place PR in the 55HH with her 8.72, a third place in the Shot Put with her toss of 34-0 3/4 and a 62.8 leadoff leg in the 4x400. Jackie Brew '14, won the Triple Jump with a jump of 34-5 1/2, in addition to setting a freshman record in the 55 Meter Dash with her 7.57 in the trials, her personal best of 8.83 in the trials of the 55HH and her third place finish in the 200 with a best of 27.80.</p>
              <p align="left">The performances were just as dominant in the running events. In addition to those mentioned by Jones and Bumgarner, victories were earned in the 600, Mile Run, 3000 and all three relays. Jamie Simmons '12, led wire to wire in the 600 to win in 1:42.64. Anna Holt-Gosselin '11, led a 1-2-3-4 sweep in the Mile Run with her 5:19.59 victory. Alina Gatowski '11, led the 3000 from start to finish to claim a victory in 10:29.67. In the 4x200 Relay, Simmons, Stephanie Ku '14, Bumgarner '14, and Kristine Bunker '14 ran 1:54.46 to win the event. The quartet of Briner, Bumgarner, Kirsten Aarsvold '11, and Simmons claimed the 4x400 victory in 4:16.74 and the Distance Medley Relay team of Olivia Papa '14, Jackie Konopka '14, Casey O'Brien '14, and Holt-Gosselin ran to first place in 13:10.08.</p>
              <p align="left">Next weekend, MIT travels to Springfield College to run in a nine team invitational featuring teams from host Springfield, Coast Guard Academy, Tufts, Wheaton, Ramapo, Smith, Westfield State and UMass Dartmouth. Tech will give some the weekend off to rest up for a chance to qualify for the NCAA Championships the week after in a large invitational at Boston University, but will still hope to win the meet.</p>
