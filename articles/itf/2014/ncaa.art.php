              <p style="font-size: 12px"><span style="font-weight: bold">Virdi Wins Pole Vault Title, MIT Earns 12 All America Awards, Women Place Seventh</span></p>
              <p align="left">March 14-15, 2014</p>
              <p align="left">MIT took 17 athletes to the <a href="ncaa.res.pdf">NCAA Division III Indoor Track &amp; Field Championship</a> at the University of Nebraska, seven men and 10 women. The women, taking advantage of their opportunities, performed extraordinarily well, placing seventh with 22 points.</p>
              <p align="left">On day one, Jackie Konopka '14 was MIT's first competitor, competing in the Pentathlon. Even though she placed sixth last year in this event, the field was much better this year and her achilles tendon issues have prevented her from training properly for weeks. Her rustiness showed in the first event, the 60HH. While she raced well, her start was slow. Nevertheless she ran 9.23. In the High Jump she cleared 5-3 after a series of ugly warmups, putting her in medal contention. However the Shot Put and Long Jump did not go in her favor, putting her back in 11th place at that point. The final sub-event, the 800, was difficult as her achilles was hurting her and she was out of contention. </p>
              <p align="left">While this was going on, Cimmy Virdi '16 was busy winning the national championship in the Pole Vault. Coming in as the fifth seed Virdi passed until 12-1 1/2. She was clean at that height as well as 12-5 1/2. At 12-7 1/2 she made it on her second attempt, but she was already in the lead on misses. At 12-9 1/2 and 12-11 1/2 she cleared the bar on her first attempt. Only Virdi and Kalsey of Westminster cleared 12-11 1/2. Virdi went to a bigger pole at 13-1 1/2 but was not able to clear the height. However, neither was Kalsey, giving the title to Virdi and a big 10 points for MIT.</p>
              <p align="left">Earlier, Louise van den Heuvel '14 cruised throughout the trials in the Mile Run. She ran towards the front of the pack, just going with the flow. Running 75 second quarters for the first half. The pace slowed in the third quarter and then picked up significantly in the final quarter to a 69. She finished third in her section with a 4:57.80 to advance to the finals.</p>
              <p align="left">Jackie Brew '14, not seeded to score in the 60HH, ran a PR and MIT record 8.80 in the trials to squeeze into finals with the eighth fastest time.</p>
              <p align="left">Nearly five hours later, van den Heuvel was back on the track leading off the Distance Medley Relay where the Engineers were seeded third. She picked up right where she left off, running 3:33.0 for the 1200 to put MIT in third at the handoff. Stephanie Guo '17 took the baton and went after it. Unfortunately her 26.9 first 200 was a little fast for her. She held on the best she could, handing off in a fine 59.5. Cindy Huang '15 did what she does best, getting Tech back into the mix. Her 65.1 first 400 did just that and her overall split of 2:14.1 kept her there. When Maryann Gong '17 grabbed the baton she had one thing in mind, how can she win. The initial 400 was 72.1, but still three teams came from way back to make it a six team race. The pace slowed to 2:29 and 3:45 before some teams fell off the back. In the final 400 Gong made her move, but that just pushed the pace of the front two teams. Gong held tough, moving into second place with 300 to go as she ran a 70 final 400 and a cumulative 4:55.6 to give MIT second place and another eight points.</p>
              <p align="left">The men competed well on day one, but only had one point to show for it. Ben Schreck '15 earned that point in the Pole Vault by garnering eighth place, clearing 15-7 on his second attempt.</p>
              <p align="left">States Lee '16 was seeded 12th going into the 400 competition. Lee started in the innermost lane, worked too hard in the first 200, coming through in 22.2 and had to break slightly as he got cut off going into the turn. This made the final lap painful as he finished in 49.20, finishing 11th overall.</p>
              <p align="left">Ken Cooper '15 was not seeded to score, but MIT was hoping the big guy could pop one. However, the competition was steep as it took a throw over 60' just to make it to finals. His throw of 56-3 1/4 was far from his best this season.</p>
              <p align="left">Day two began with  Michelle Johnson '15 in the Triple Jump and Isabella Stuopis '16 in the Shot Put. Neither were seeded to score as Johnson was seeded 14th and Stuopis was tied for 13th. Johnson finished in 10th at 37-0 1/2. Stuopis threw 43-6 3/4 to finish 13th.</p>
              <p align="left">Louise van den Heuvel was then up for her Mile Run final. She was in lane one so she had to be careful of getting boxed in and she did not want to take the lead so she eased to the middle of the pack. Here she remained for most of the race as it was pretty strategic through splits of 75, 2:32 and 3:46. The leaders took off and van den Heuvel just did not have the speed to match some of the leaders. She closed in 71 and fought hard to gain seventh place in 4:57.82.</p>
              <p align="left">Jackie Brew was next in the 60HH Final. She stumbled coming out of the blocks, nearly falling, but somehow regained her step and flew down the track. Her hurdling was excellent as she closed on the pack. She placed seventh in 8.87.</p>
              <p align="left">MIT was now in fourth place with 22 points with the 3K still to go. In the 3K MIT had a tired but game van den Heuvel and a relatively fresh Maryann Gong. Gong had done this double two weeks ago and won the 3K in 9:42.48 at All New Englands. This time she was unsure of the strategy and who to follow. She looked good in the front of the chase pack as they went out in 78 and then slowed to 81. The pace then picked up as someone broke away from the chase group with a 77. Gong went with her but then appeared unsure. Whenever she starts thinking she ends up giving up and that happened again tonight as she ended up last in 10:19.14. van den Heuvel raced as well as she could, but was unable to place better than 10th in 9:56.45. </p>
              <p align="left">Three teams passed MIT, although it only took 26 points to be one of the top four and on the podium. Ending up in seventh marked a successful end to a successful season. The Engineers should be proud of their performances.</p>
              <p align="left">The men only had Cooper coming back in the Shot Put, and the 4x400 Relay. Cooper did not appear competitive, and even though the field overall did not perform well, he could only manage a 51-0 throw to finish 10th.</p>
              <p align="left">The men were the fifth seeded team in the 4x400, racing in the second of the three section final. States Lee led off, probably should have taken the break at the 200, but did not. His lack of aggressiveness is not his style and this hurt his overall performance. Nevertheless the sophomore still led off with a 48.9 to handoff in third.  Michael Kaba '16, who had turned his ankle on Thursday, ran a very conservative first 200 in 23.5. He was able to close some ground on the second lap, but his 49.1 was a slower split than normal. Derek Barnes '16 ran his best leg of his career, leading off with a 22.5 and closing in 48.6 to put MIT in great position. Tyler Singer-Clark anchored the relay and did not disappoint. He maintained MIT's position, finishing in 48.5 for a cumulative time of 3:15.14. Although they were only third in their section, they finished fourth overall to give Tech six total points in the meet.</p>
              <p align="left">In total, the Engineers earned 12 All America awards. It is time for the outdoor season to start and MIT is wasting no time as the distance runners will take part in the Gerry Gravel Invitational at Westfield State College next Saturday, the day before they head to California for their Spring Training Trip.</p>
