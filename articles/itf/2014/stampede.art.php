              <p><strong><span class="style1" style="font-size: 18px">Engineers Remain Undefeated - Dominate Despite Looking Flat Early</span></strong></p>
              <p align="left">February 1, 2014</p>
              <p align="left">MIT traveled to Tufts University on Saturday for the opportunity to compete against some strong opponents, particularly host Tufts, while getting the opportunity to run on arguably the fastest flat track in New England.  The <a href="stampede.res.php">Tufts Stampede</a> would be the final scoring meet of the regular season for MIT, so winning the meet would allow both the men and women to finish the regular season undefeated.</p>
              <p align="left">Based on the seeds prior to the meet, it appeared Tech would easily win both meets. With the amazing performances the men and women achieved last week, combined with the idea they could not lose, perhaps the Engineers were not as mentally prepared to compete as they should have been. Based on the early performances that certainly seemed to be the case.</p>
              <p align="left">The start of the meet began with the Men's Weight Throw and High Jump and the Women's Shot Put and Pole Vault. While Ken Cooper '15 won the Weight with a sub-par 56-1 3/4 and Cimmy Virdi '16 won the Pole Vault with a PR of 12-6, there were very few strong performances by MIT in the four events. On the track, the trials in the 60 and 60HH failed to produce the performances the team expected although the Engineers were advancing to the finals. The 3000 meter race was the first running final, and even though MIT dominated the event, the performances were nothing short of poor. Benji Xie '15 won the men's event with an 8:46.36 and Ian Tolan '16 and Matt Deyo '16 placed third and sixth respectively with times of 8:49.20 and 8:50.78.  Xie should have been at least 10 seconds faster, but looked fatigued at 5K pace. Maryann Gong '17 and Elaine McVay '15 placed first and second with 10:16.30 and 10:19.02, but both times were pretty slow considering Gong ran 9:52 back in December. In the Miler Run, Louise van den Heuvel '14 continued to dominate, winning in 5:02.75, but Christina Wicker '17 and Nadia Lucas '17 fell off the pedestrian pace, struggling at the end. In the Men's Mile, one of three races set up with rabbits, MIT's men just caved right at the start, producing inferior times of 4:25-4:32 for the five MIT competitors. After the milers failed to deliver it was time to get the team together for a reevaluation of the matter at hand.</p>
              <p align="left">Fortunately, after that things began to turn around a bit. In the 60HH, Jackie Brew '14 ran a season best, winning in 9.07 while Jackie Konopka '14 placed second in 9.15. Stephanie Birkhimer '14, who had a superb day, ran a PR of 9.80 for sixth place. Although the 400 went well for both the men and women, there was no way the times would compare with those run on the banked track of Boston University last week. States Lee '16 remained undefeated against Division III competition with a 50.46. Mac Gager '14 won the 600 in a season best of 1:23.56.</p>
              <p align="left">In the 800, Wicker returned with a much better racing attitude, took the lead after 300 meters and won going away in 2:19.80. In the men's race, the other one with a rabbit, Sam Parker '15 got pulled out a little too fast, running 26 flat at the 200 split. His main competition used him until the final 150 and then moved ahead for a 1:51 to Parker's 1:53.15. A slightly smarter race would certainly have had him a second or more faster. The outstanding races continued as Veronica Szklarzewski '15 popped a very fast 60 Meter Dash for second place in 8.00. van den Heuvel took to the oval a second time with the same results, a win, this time in the 1000 in a season best 2:59.97. Andrew Mullen '17 earned his first victory of the season with a 2:36.31 performance in the 1000. A 28.2 first 200 guaranteed a slow performance, but Mullen competed well, managing a kick at the end to hold off his competitors. The men dominated the 200 as they have done every week, taking five of the top six places, with Tyler Singer-Clark '14 leading the way with a 22.68.</p>
              <p align="left">In the second round of field events, Ellen Liverpool '15, for the fifth week in succession, threw a personal best in the Weight Throw with a 51-0 3/4 performance, good for second place in the meet and second place on MIT's all-time list.  Isabel Stuopis '16 broke her streak of personal bests and MIT records, but still won the Shot Put, throwing 42-9 3/4. Cooper won his second event, putting the shot 52-7 1/4. Alex Noakes '16 had a huge personal best, adding over three feet for a new PR of 45-4 1/4. Preethi Vaidyanathan '15 saved her best for last, waiting till a jump off for first place in the High Jump to clear 5-4 1/4, tying her best mark. Birkhimer continued to compete well, winning the Triple Jump in 34-10 1/2.</p>
              <p align="left">Perhaps the best event of the meet for MIT, at least in terms of personal bests, was the 5K. Nicole Zeinstra '16 was the sole entry for Tech and she made the most of it, winning in a personal best of 18:09.94. Had she not allowed the pace to slow to 5:57 for the second 1600 she probably would have broken 18:00. She took the lead in the final 1600, speeding up to a 5:48 before closing in a 39 final 200. Roy Wedge '14 took advantage of a very fast pace, found a rhythm of 72 second quarters and ran a PR of 14:48.02 for third place. Allen Leung '15 also ran a personal best, placing fifth in 14:56.40. That makes four MIT runners under 15:00 already this season.</p>
              <p align="left">The men ended up winning the 12 team meet with a total of 257 points to 117 for second place Tufts. The women won their 17 team meet with 231 1/2 points to 165.5 for second place Tufts. Next weekend, MIT will again split its team up, sending the oval runners to the Boston University David Hemery Invitational where Galen Rupp will go for the Mile world record while the field event athletes along with the sprinters and hurdlers will be at MIT for the Gordon Kelly Invitational.</p>
