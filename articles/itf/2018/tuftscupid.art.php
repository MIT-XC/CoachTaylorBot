            <h1 class="bigtitle" style="font-size: 12px">Championship Qualifying Performances&nbsp;are the Theme for MIT </h1>
            <p style="font-size: 12px">&nbsp;</p>
            <p align="left">February 3, 2018</p>
            <p align="left">With just two weeks before the New England Division III Championships and five weeks before the NCAA Division III Championships there was a&nbsp;sense of urgency amongst the MIT Track &amp; Field athletes for the former and an eye on what the fields are looking like for the latter.</p>
            <p align="left">Once again MIT split their squads between two meets, sending some field event athletes to Reggie Lewis for the <a href="neicaaa.res.php">NEICAAA Championship</a> and the rest to the <a href="tuftscupid.res.php">Tufts University Cupid Challenge</a>.&nbsp; The NEICAAA Championship, termed the All New Englands because the championship is for all the college track and field teams in New England, regardless of division.&nbsp; Due to various conference championships, mostly among the Division I colleges, the date&nbsp;of the meet was moved to this early weekend in February.&nbsp; More significant was the fact the meet was moved from the Boston University track to the Reggie Lewis Center.&nbsp; While both tracks are banked, the BU track is significantly more banked, and faster than Reggie Lewis.&nbsp; The NCAA applies the same penalty to all banked or over-sized (&gt;200 meters) tracks, regardless of the degree of the bank.&nbsp; That penalty, ~3.2 seconds for the Mile and 9 seconds for the 5K, is arguably worth it at BU, but not at Reggie Lewis.&nbsp; Thus, many Division III teams who tend to have more national level athletes than their New England Division I counterparts, elected to use the flat tracks in the area, particularly since the Tufts University meet was using pacers in most of their races.</p>
            <p align="left">Things started off quite well for MIT on Friday evening at Reggie Lewis.&nbsp;Haley Strouf '18 jumped a season best 18-5 in the Long Jump, having an exceptional series, although she did not make the finals, finishing in 12th place.&nbsp; Strouf is currently seventh on the NCAA list.&nbsp; In the women's Pole Vault, Jacqueline Ahrens '21 set the MIT freshman record as she jumped a national qualifying 12-3 1/2, tying her for fourth on that list.&nbsp; Kari Stromhaug '19 also cleared 12-3 1/2.&nbsp; The pair placed fourth and fifth respectively.&nbsp; Makenzie Patarino '19 cleared a personal best 11-11 3/4, making the MIT top 10 pole vault list.</p>
            <p align="left">On Saturday, of significance was the men's High Jump.&nbsp; Both Alec Reduker '20 and Chris Washington '20 cleared 6-8 1/4, a season best for Reduker and a personal best for Washington, placing them sixth and seventh respectively.&nbsp; Both are tied for 13th on the NCAA list.</p>
            <p align="left">Over at Tufts the competition was fantastic&nbsp;and the pacers were doing a great job.&nbsp; The 5K's were early on the schedule and even though Mary Eccles '18 did not run her season best&nbsp;she did run a great race.&nbsp;The pacer set up a pace that was a little too fast at 81 pace. &nbsp;Eccles was trying to run her race, but the 1600 was still too fast at 5:28.&nbsp; After the pacer left the track things got on 83 pace, putting Eccles at 11:01 for 3200 meters.&nbsp; She certainly fatigued in the final 1600, running 5:39 and finishing in 17:20.31.&nbsp; She is currently 10th on the NCAA list.&nbsp; On the men's side, while Dennis Maloney '19 ran&nbsp;a solid 14:46.86, Alex Knoedler '18 and Nicholas Waltman '18 ran New England Division III qualifying times of 15:23.52 and 15:27.59.</p>
            <p align="left">The event of the day was definitely the Mile Run for MIT.&nbsp; In the men's race, a very high quality field was out to qualify for Nationals.&nbsp; Aidan Gilson '19 was representing MIT and looking to win the race as he makes the transition to a national level runner who is trying to win races, not just run in them.&nbsp; Gilson settled into the pace as the pace setter led the group through 61 and 2:04 quarters.&nbsp; Unfortunately the pace slowed dramatically to 3:09, but Gilson used this prepare for his kick.&nbsp; During the seventh lap it initially looked as though he had given up as the pace picked up.&nbsp; However, coming out of the final turn he took the lead and held it for the final 200, winning in 4:11.03, the third fastest time in the country, a banked track equivalent of 4:07.87, easily fast enough to get him into Nationals and the top time in New England.&nbsp; Also running a best for MIT was Brian Bates '20, winning his section in 4:22.94.</p>
            <p align="left">Marissa McPhillips '20 was in the women's Mile and figured she would do her part to maintain the winning ways.&nbsp; The pacer did her job of taking the pace out in 74 and 2:29, but then stepped off the track and there was no one within fifty meters of McPhillips.&nbsp; The sophomore had prepared for this and took care of business, running 74 point for each quarter to finish in a personal best 4:58.72.&nbsp; This converts to a banked track equivalent 4:55.80, the fourth best time in the country currently and third best in New England.</p>
            <p align="left">In the field events, Cody Bratten '18 threw his season best 45-1 1/2 in the Shot Put as he&nbsp;rounds into championship form.&nbsp; Other top performances came on the track as Benton Wilson '21 ran 22.43 in the 200 for a flat track PR and second ranked in New England while sitting on 18th place nationally.&nbsp; Chris Sweeney '18 ran an 8.32 to win the 60HH in a season best ranked second in New England and 19th nationally.&nbsp; In the 3K, Billy Woltz '20 had an outstanding race in the 3K, running 69-70 pace the entire way for a huge personal best of 8:39.73.&nbsp; Dylan Doblar '21 also ran a solid race, although he lost focus towards the end, he still ran 8:47.56.&nbsp; Josh Derrick '20 also qualified for the NED3 meet with an 8:50.64 as he had to lead the entire way in his section of the 3K.</p>
            <p align="left">The women had numerous season and personal bests on the day.&nbsp; Madeleine Waller 'G cleared 10-6 in the Pole Vault for a personal best and was close to making 11-0.&nbsp; Ariela Slutsky '18 had a season best of 55-5 3/4 in winning the 20 lb. Weight Throw.&nbsp; Slutsky is ranked second in New England and 11th nationally.&nbsp; Nneoma Okonkwo '18 returned from injury for her first meet of the year.&nbsp; She showed she is still better than most of the competition as she jumped 18-4 1/2 to claim second place&nbsp;in the Long Jump.&nbsp; Okonkwo is ranked eighth nationally.&nbsp; Hannah Chen '18 ran&nbsp;a season best of 9.09 to get second in the 60HH to move her to seventh on the New England list and 19th nationally.&nbsp; Michelle Menkiti '20 placed second in the 60 Meter Dash in a season best 8.02 as she rounds into championship shape, but is still only ranked 12th in New England.&nbsp; The sophomore also won the 200 in 26.19 out of lane three and is ranked third in New England.&nbsp; Kristin Frombach '19 ran a 400 PR of 62.59 to win her section.&nbsp; Katie Bacher '20 is back from injury and illness and gave notice in winning the 1000 in 2:59.05, one of three MIT runners ranked in the top four in New England.&nbsp; Megan McCandless '20 continues to find her way back from injury as she ran&nbsp;a season best 10:11.96 in the 3K to move her to eighth in New England rankings.</p>
            <p align="left">Next week MIT will once again split their squad.&nbsp; Similar to&nbsp;the previous weekend&nbsp;many of the top oval runners will compete at the Boston University David Hemery Valentines Invitational, while the field event athletes will compete at MIT's Gordon Kelly Invitational.            </p>
