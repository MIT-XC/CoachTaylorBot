            <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">Engineers Set Personal Records at BU Season Opener</span>            </p>
            <p align="left">December 2, 2017</p>
            <p align="left">Five MIT Track &amp; Field athletes started their Indoor Track &amp; Field season  at the <a href="buseasonopener.res.php">Boston University Season Opener</a>.&nbsp;&nbsp;Four of the five ran personal bests with one having a reasonable chance of qualifying for the NCAA Division III Indoor Track &amp; Field Championship.</p>
            <p align="left">All five athletes were those who had just completed the Cross Country season and wanted to have a shot at trying to qualifying for Nationals before taking a break and getting back to building mileage for the indoor season.</p>
            <p align="left">First up were Marissa McPhillips '20 and Katie Bacher '20&nbsp;in the 3,000 Meter race.&nbsp; In order to run at least a&nbsp;9:45, which is what it generally would take to make the&nbsp;top 17 in the country, accounting for the five second penalty incurred by the banked track for the 3K, they would have to run 78 second&nbsp;pace per 400 meters.&nbsp; They were both in the second of three sections.&nbsp; The first section was won in 8:55 with the slowest runner finishing in 9:32.&nbsp; The second section was not going to be fast enough unless McPhillips and Bacher did the work themselves.&nbsp; After running back to back 79's, McPhillips took the lead, moving the pace up so that they came through the 1600 in 5:13.&nbsp; McPhillips fell off the pace after&nbsp;2000 meters with no one to help her, winning her section in a personal best of 9:51.25, fourth on MIT's all-time list.&nbsp; Unfortunately the performance will not be fast enough to make the national field in March.&nbsp; Bacher faded after 1600 meters, still suffering from the asthma issues that affected her in the cross country season.</p>
            <p align="left">Mary Eccles '18 was next up in the 5K.&nbsp; Similar to the 3K the fast section was indeed fast, with the winner running 15:17.&nbsp; The second section was also quite fast.&nbsp; Eccles, hoping to run 82 seconds per 400, had to run 79 for the first 400 just to keep from getting dropped off the back.&nbsp; She was able to settle into 82 pace in the back of the pack, coming through 5:25 for the first 1600 and 5:31 for the second 1600, running PR's for both the 3K and 3200.&nbsp; The final 1600 was slower as she was by herself for a period and could not push the pace beyond 85 for 800 meters.&nbsp; She rallied for&nbsp;an 83 and then a final lap of 40 to finish&nbsp;in a new personal best of 17:13.95, a time that may make the national field&nbsp;at the end of the season despite the nine second penalty for the 5K.&nbsp; This time also moved Eccles up to sixth on the all-time MIT list.</p>
            <p align="left">Both Josh Rosenkranz '19 and David Walter '18 were entered in the men's 5K for MIT.&nbsp; Rosenkranz was in the second section and Walter the third section.&nbsp;The plan for Rosenkranz was to run 69 second pace with hopes of running sub 14:30&nbsp;in order to be one of the top 15 in the country at the end of the season.&nbsp; At the start there was&nbsp;a lot of contact, causing Rosenkranz to overreact instead of letting things sort themselves out.&nbsp; He wasted quite a lot of energy doing this that he would likely pay for dearly later in the race. Even with that the first 400 was 71.&nbsp; Rosenkranz moved up through the field to find the right pace.&nbsp; His first 1600 was 4:38 and his second 1600 a 9:18.&nbsp; He tried to move hard at the beginning of the third 1600, but soon fatigued and ended up running a 4:41 and could finish no better than a 35 final 200 for a final time of 14:44.45, a significant personal best and ninth on the all-time MIT list, but not close to what would be needed to make the national field.</p>
            <p align="left">Walter, in the last section was hoping to get a solid pace out of his opponents, but assumed he would be in it to try and get the win and hope for a pretty good time in the process.&nbsp; The initial pace was pretty quick at 70 seconds for the first 400 and Walter was sitting comfortably in third place. The pace settled at 71 second pace through the first 1600, then slowed to 74 pace.&nbsp; After 3200 Walter and one other runner jumped the pace to 72-73 until Walter took the lead with 300 meters to go, dropping a 32 final 200 to win in 15:02.46, a significant PR.</p>
            <p align="left">Although all five will now take a break and start to build mileage for the indoor season in February and March, the rest of the team will next host their annual Alumni/ae Meet on 12/16.</p>
