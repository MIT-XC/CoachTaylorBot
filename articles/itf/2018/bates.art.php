            <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">Engineers Compete Well in Victory, but Performances Lacking</span>            </p>
            <p align="left">January 13, 2017</p>
            <p align="left">The MIT Track &amp; Field team returned to practice on January 8 after three&nbsp;weeks absence due to exams and the holiday break.&nbsp; On Saturday they traveled to Lewiston, Maine to compete against host <a href="bates.res.php">Bates College and visiting Colby College in the team's first intercollegiate competition</a> of the indoor season.&nbsp; Entering this meet in the early season the Engineers were without many of their top athletes due to being cautious with minor injuries and a considerable number of non-injury related issues.&nbsp; The questions facing the team and coaches were: Could the athletes shake off the rust and compete at the expected level, and even if they did, would the competition be enough to bring out the performances required to be at the top of Division III teams in New England and the NCAA?&nbsp; The women are ranked #2 in the NCAA Division III pre-season poll while the men are ranked #8.</p>
            <p align="left">Having never lost to either Bates or Colby in this particular competition, the MIT team appeared pretty confident, perhaps a little over confident.&nbsp; The men dominated the competition in that they won 10 of the 17 events, winning with 100 points to 62 for Bates and 19 for Colby in the 5-3-2-1 scoring format.&nbsp; The women won 12 of the 18 events, winning with a score of 106 to 53.5 for Bates and 28.5 for Colby.&nbsp;Tech competed very well in most every event, generally doing what it would take to win.&nbsp; As feared, the competition was not at a high level in most events and the Engineers  did not do&nbsp;much to do anything more than compete for the win.</p>
            <p align="left">The men's strength is in the field events, the middle-distance and distance events and potentially the short sprints.&nbsp; Between the missing athletes and moving many of the runners to events that may not their best in order to qualify in secondary events for the New England Division III Championship, MIT needed for people to step up.&nbsp; The event that proved the most successful for Tech was the High Jump where they swept the event with all four jumpers clearing 6-4.&nbsp; Alec Reduker&nbsp;'20 and Chris Washington '20 tied for the win because the official did not know a jump-off was supposed to take place. Steven Speck '20 was third and Sope Eweje '19 jumped a four&nbsp;inch PR to claim fourth, showing that he stepped-up.</p>
            <p align="left">In four events the Engineers claimed three of the four scoring places.&nbsp; In the Pole Vault, Scott Cameron '19 led a 1-3-4 result, clearing 14-5 1/4.&nbsp;&nbsp;Tre Albritten '18 led a 1-2-3 result in the 200 with a 22.77 performance.&nbsp; Although he did not win either event, Aidan Gilson '19 led a 2-3-4 scoring effort in both the Mile Run and the 1000, running  slow paced strategic races with times of 4:25.06 (62 final 400) and 2:37.61 (30 final 200).</p>
            <p align="left">Billy Ruschel '18 was the only double winner for the men, taking both horizontal jumps.&nbsp; In the Long Jump he won with 21-4 3/4 and in the Triple Jump, he easily dominated with 46-2.</p>
            <p align="left">The Engineers had five other winning efforts.&nbsp; Chris Sweeney '18 dominated the 60HH in 8.78.&nbsp; Ken Acquah '20 took first place honors in the 400 with a gutsy finish in 54.04.&nbsp; Simon Alford '20, dueled with teammate Nathan Munet '19, coming out ahead in 2:00.26 to 2:00.59.&nbsp; Josh Rosenkranz '19 dominated the 3K in 8:49.13, throwing in a 65 400 split after a pedestrian 4:48 first mile, to break the race open.&nbsp; The quartet of Dylan Doblar '21, Acquah, Billy Woltz '20 and Jake Jorgensen '21 took the win in the Distance Medley Relay in 11:06.07.</p>
            <p align="left">The women swept the Pole Vault, which is their dominant event.&nbsp; Freshman Jacqueline Ahrens led Kari Stromhaug&nbsp;'19, Makenzie Patarino '19 and Kathryn Mohr '20 with her 11-11 3/4 victory.</p>
            <p align="left">In both the Triple Jump and the 3K, the women claimed the first three places.&nbsp; Jasmine Jin '20 led Lucy Lee '21 and Elizabeth Weeks '21 with her 35-2 1/2 triple jump.&nbsp; Mary Eccles '18 led a trio of outstanding performances with her 10:20.13 PR victory.&nbsp; Bailee Margolis '19 (10:38.45) and Emily Niu '21 (10:38.57) claimed second and third.</p>
            <p align="left">Also taking three of the scoring places, the Engineers dominated the Weight Throw.&nbsp; Ari Slutsky '18 won easily with a throw of 53-1 while Folusho Jebutu '20 and Elise McCormack-Kuhman '20 took second and fourth respectively.&nbsp; Jebutu's 45-9 1/4 throw put her  #9 on the all-time MIT list.</p>
            <p align="left">Other winners included Haley Strouf '18 in the Long Jump, winning with 17-6.&nbsp; Slutsky proved to be the only double winner on the women's team as she also won the Shot Put with a throw of 36-6 3/4.&nbsp; Hannah Chen '18 took her top event, the 60HH in 9.45.&nbsp; Skylar Brooks '20 won the 600 in 1:45.28.&nbsp; Katie Bacher '20 won the 800 in 2:22.77.&nbsp; Marissa McPhillips '20 won the Mile in 5:07.41.&nbsp; The quartet of Kristen Frombach '19, Jin, Bacher and McPhillips won the 4x400 Relay with an awesome come from behind effort in 4:18.54.</p>
            <p align="left">Next weekend Tech travels to Maine for the final time this year, taking on Bowdoin College, Division II Merrimack, The University of Southern Maine and Tufts University at Bowdoin.&nbsp; The Engineers are hoping the rust is off because the competition will be significantly better in the upcoming meet.</p>
