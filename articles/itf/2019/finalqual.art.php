            <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">Ackerman and DMR Qualify for Nationals</span></p>
            <p align="left">March 2, 2019</p>
            <p align="left">MIT sent a few individuals over to Tufts today for the <a href="finalqual.res.php">Tufts University Final Qualifier,</a> the last opportunity to achieve an NCAA Division III qualifying performance.&nbsp; These individuals were on the bubble&nbsp;of making the top 20 in their event or the top 12 in a relay.&nbsp; There are a handful of these meets&nbsp;around the country, which must be approved by the NCAA for this final weekend of competition.&nbsp; Colleges came from Ohio, Virginia, Georgia, New Jersey&nbsp;and New York, as well as many New England colleges&nbsp;to compete at Tufts.</p>
            <p align="left">The Pole Vault was one of the most successful events for the Engineers.&nbsp;Anais Marenco '22 cleared 12-2 1/2 to move up to #15 on the NCAA list.&nbsp;MIT already had Kari Stromhaug '19 at ninth on the list, and Jacqueline Ahrens '21 at 19th.&nbsp; Sending three individuals to Nationals in one event is quite impressive.&nbsp;</p>
            <p align="left">On the men's side, Liam Ackerman '21 added to&nbsp;last week's impressive results by winning the Pole Vault, clearing a personal best of 16-0 to move on to the National Championship.&nbsp; This performance moves Ackerman up to eighth on the NCAA list.</p>
            <p align="left">The men's DMR, after running a solid time last weekend&nbsp;without Aidan Gilson '19 or Benton Wilson '21, followed through with their plan to race at this meet, which typically qualifies nearly half the NCAA field each year.&nbsp;&nbsp;Simon Alford '20 led off with the 1200 leg.&nbsp; Alford positioned himself well, but started getting dropped by the top three teams after a 2:03.0 800, which should not happen&nbsp;to the 1:54 half-miler.&nbsp;He held onto fourth, finishing in a credible 3:04.4.&nbsp;Benton Wilson&nbsp;got the baton for the 400 leg&nbsp;and quickly put a dent in the lead.&nbsp; At the end of his 48.8 leg Wilson had closed the gap, handing off to Aiden Foucault-Etheridge just behind the front three teams.&nbsp; Foucault-Etheridge did not hesitate to move up and did so effectively with his 55.3 opening 400.&nbsp; He then took the lead handing off to Gilson with a slight lead and a 1:53.3 leg.&nbsp; Gilson took the lead when he should have allowed the Middlebury miler to lead.&nbsp;The pack was happy to let Gilson lead despite the slow splits of 63 and 2:07.&nbsp; Gilson fell back when the leaders moved, but rallied to finish the final 200 hard.&nbsp; His 4:16 split was less than stellar, but was enough for third place in 9:59.61, the sixth fastest time in the country.</p>
            <p align="left">Jon Fakkema '19 was unable to move up from his 22nd ranking in the Weight Throw with his 59-2 performance from earlier in the season, but he did throw a PR in the Shot Put with his 50-5 1/4 effort.&nbsp; Albert Menio '20 did not make it to finals in the Weight Throw, but he did throw a personal best of 52-2 1/2.</p>
            <p align="left">In the Triple Jump, Lucy Lee '21 jumped a personal best 36-9 3/4, just ahead of teammate Elizabeth Weeks '21 who jumped 36-4 3/4, although she narrowly fouled a 38+ jump.</p>
            <p align="left">Going into the NCAA Division III Championship at the Reggie Lewis Center in Boston next Friday and Saturday, the men will be represented by Yorai Shaoul '21 in the Triple Jump (#2), Alec Reduker '20 in the High Jump (#2), DMR team of Simon Alford, Benton Wilson, Aiden Foucault-Etheridge, and Aidan Gilson (#6), Liam Ackerman in the Pole Vault (#8), Gilson in the Mile (#9), Josh Rosenkranz '19 in the 5K (#11) and Wilson in the 200 (#16).&nbsp; Representing the women will be the #5 DMR team&nbsp;of Margaret Trautner '20, Sarah Ishamuddin '21, Bailey Tregoning '19 and Marissa McPhillips '20, Katie Collins '21 in the 5K (#6), McPhillips in the Mile (#8), Kari Stromhaug in the Pole Vault (#9), Trautner in the 800 (#11), Jenna Melanson '20 in the 3K (#13), Tregoning in the 800 (#15), Anais Marenco '22 in the Pole Vault (#15) and Jacqueline Ahrens '21 in the Pole Vault (#19).            </p>
