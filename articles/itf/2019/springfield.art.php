            <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">MIT Track &amp; Field Teams Accomplish Goals at Springfield Invite</span></p>
            <p align="left">February 16, 2019</p>
            <p align="left">When the NEICAAA Championship moved last year from this weekend to&nbsp;the first weekend in February, it left a gap week between the New England Division III Championship held on this weekend and the NCAA Final Qualifier on the first weekend in March.&nbsp; This year, the New England Division III Coaches Association voted to move the championship to the final weekend in February, creating the opportunity for a final qualifier of sorts on this current weekend.&nbsp; There were three such meets in New England at Bowdoin College, Middlebury College and the <a href="springfield.res.php">Springfield College Triangle Classic Invitational</a>.&nbsp; The teams attending were MIT, Conn. College,&nbsp; Springfield College, Trinity College, Tufts University, Williams College and WPI.</p>
            <p align="left">Most of the teams left a good portion their top athletes at home to rest for next weekend's New England Division III Championship, as most of the events were contested by those trying to get a&nbsp;qualifying performance, or a better one in some cases.&nbsp; Many of the top field event athletes competed making for some great competition in those events.</p>
            <p align="left">For the most part, MIT accomplished what they were hoping to, improving marks and providing flexibility with their potential entries for next weekend.&nbsp; For the men, one of primary areas of focus was to put up competitive times in the 4x400 Relay and DMR to get into the fast sections next weekend.&nbsp; For a variety of reasons MIT had not competed in a DMR and had only put together makeshift 4x400 units that had only produced a 3:31.32 performance.&nbsp; Benton Wilson '21, Nick Duchatellier '22, Aidan Gilson '19 and Aiden Foucault-Etheridge '22, with no competition, put together a 3:25.25 performance that moved them up to the number seven seed in New England.&nbsp; In the DMR, the quartet of Matthew Kearney '22, Joa Kennedy '21, Simon Alford '20 and Kent Slaney '21 put together respective splits of 3:10.5, 52.9, 1:59.0 and 4:23.6 in their second or third events of the day to run 10:26.16, a performance ranking them fourth in New England.</p>
            <p align="left">The most outstanding single event for the men was a battle between the Shot Putters and the High Jumpers, but the weight men pulled it out with four out of five personal bests.&nbsp; Will Woltmann '22 hit the 50' mark right on the button to take first place honors.&nbsp; Jon Fakkema&nbsp;'19 threw 49-7 1/4 for a personal best and second place.&nbsp; Ryan Nall '22 hit a PR of 47-8 1/2 for third place and Nathan Basinger '22 added his own personal best of 45-7 3/4 for fourth place.&nbsp; Three of the four are freshmen and will be a group to watch in the future.</p>
            <p align="left">While the vertical jumpers did not match the throwers, they did represent the Engineers quite well.&nbsp; Caleb Harris '22 matched his PR of 6-6 3/4 to win the event.&nbsp; Steven Speck '20 set&nbsp;a personal best of 6-4 3/4.&nbsp; Sope Eweje '20 did not set a PR, but jumped well, clearing 6-0 3/4.</p>
            <p align="left">The 800 runners got the job done&nbsp;to set themselves up as viable relay runners next week.&nbsp; Ellery Rajagopal '21 ran a 1:58.53 indoor PR to win his section in an excellent race.&nbsp; Matt Schofield '22 ran 2:00.12, a personal best.</p>
            <p align="left">In addition to his leg in the DMR, Joa Kennedy ran added personal bests of 21- 3 1/4 in the Long Jump and 8.76 in the 60HH.</p>
            <p align="left">For the women, the horizontal jumpers ruled the day.&nbsp; In the Long Jump, three of the four&nbsp;Tech jumpers entered the 17' range.&nbsp; Elena Andree '22 led the way with a 17-2 3/4 performance.&nbsp; Yilinn Yang '22 was close behind at 17-2 and Tema Nwana '21 right with them at 17-0 1/2.&nbsp; Nwana, added a 10' PR in the Weight Throw when she threw 40-11.</p>
            <p align="left">Yang joined Elizabeth Weeks '21 in the Triple Jump to place first and third.&nbsp; Weeks earned first with a season best of 36-5 1/2 and Yang placed third with a personal best of 35-6 3/4.&nbsp; Andree added a fourth place even though her 35-4 was not a season best. </p>
            <p align="left">On the track, the 4x400 unit of Sarah Ishamuddin '21, Margaret Trautner '20, Kristen Frombach '19 and Bailey Tregoning '19 put together a season best of 4:05.60 to put themselves in position to run in the second fastest section next weekend.</p>
            <p align="left">Trautner had earlier run her first 600 in 1:38.61 to earn second place behind teammate Tregoning who ran 1:38.51.</p>
            <p align="left">Next week the men will be at home where they will be favored to win the New England Division III Championship after not winning it the past two years.&nbsp; The men had won the previous four years in a row.&nbsp; The women will travel to Bowdoin College where they will be seeded&nbsp;a&nbsp;distant second place to Williams College, who won the championship last year.&nbsp; MIT had won the championship 2015-2017.</p>
