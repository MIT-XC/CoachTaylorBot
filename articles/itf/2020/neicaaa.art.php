            <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">Kenneth Wei Sets MIT Varsity and Freshman 60HH Record</span>            </p>
            <p align="left">February 1, 2020</p>
            <p align="left">Still managing a hamstring issue from the meet two weeks ago, Kenneth Wei '23 was only entered in the 60HH at the <a href="neicaaa.res.php">NEICAAA Championship</a>&nbsp;being held at the Reggie Lewis track in Boston.&nbsp; Wei made the best of the opportunity, setting the MIT Varsity and Rookie record of 8.12, placing third in the process.&nbsp; This mark puts the freshman at #7 on the NCAA Division III list.</p>
            <p align="left">MIT sent a small handful of athletes to this meet, which is a New England championship for&nbsp;all membership colleges, Division I, II and III.&nbsp;For many reasons, the meet is not attended by many Division III colleges, but nearly all of the DI and DII colleges in New England bring their&nbsp;best.</p>
            <p align="left">On the men's side, in addition to Wei, Bobby Upton '20 and Bryan Sperry '23 represented MIT in the Pole Vault.&nbsp; Upton tied for sixth, jumping a season best 14-11, and had some very solid jumps at 15-5.&nbsp; Sperry, still on less than a full approach, had difficulty bringing enough speed, and was unable to clear the opening height.</p>
            <p align="left">Wei ran an&nbsp;8.39 in the preliminary round, as he was uncomfortable with the blocks supplied by the venue.&nbsp; Nevertheless he qualified for the semi-finals.&nbsp; He ran 8.16 for the second fastest semi-final time, this time using his MIT&nbsp;blocks.&nbsp; In the final, Wei&nbsp;earned his third place with&nbsp;his record setting 8.12.</p>
            <p align="left">For the women, Margaret Redfield '21 cleared 5-3 1/4 in he High Jump, but did not place.&nbsp; In the Pole Vault, MIT competed Jacqueline Ahrens '21, Anais Marenco '22 and Lila Wine '21.&nbsp; Marenco cleared 11-7 3/4, Ahrens 11-1 3/4 and Wine was unable to clear a height.</p>
