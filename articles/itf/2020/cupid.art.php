            <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">MIT Dominates Tufts Cupid Invitational With National Marks</span>            </p>
            <p align="left">February 8, 2020</p>
            <p align="left">MIT Track &amp; Field traveled across town to compete at Tufts University for the second week&nbsp;in succession, this time in the <a href="cupid.res.php">Cupid Invitational</a>.&nbsp;MIT's primary objective was to qualify individuals in necessary events for the New England Division III championship and the NCAA Division III championship, while resting those who have already qualified.&nbsp; With three weeks before the former and five weeks before the latter, performances all through New England and around the country are improving rapidly.&nbsp; After a very successful Saturday, MIT should improve on their Men's and Women's national team ranking which had the men at number five and the&nbsp; women at number six going into the competition.</p>
            <p align="left">Kenneth Wei '23 again led the way for the Engineers.&nbsp; The super freshman jumped 23-8 1/4 in the Long Jump to move to number six in the country, and set the MIT Rookie Record while moving to number two all-time for MIT.&nbsp; This is the first time in MIT history that they have had three 23 foot jumpers at the same time.&nbsp; Wei came back&nbsp;in the Triple Jump to win with a leap of 46-5 1/4, which is 10th on the MIT all-time list and 23rd on&nbsp;the current national list.</p>
            <p align="left">Bobby Upton '20 moved to number nine on the NCAA list when he&nbsp;won the Pole&nbsp;Vault with a 15-9 performance.&nbsp; This also moved Upton to number 10 on the MIT All-time list.&nbsp; Upton narrowly missed 15-11.</p>
            <p align="left">On the track, Andrew Mah '22 took first place in the 3K with an 8:32.62 performance, which moves him to 24th on the NCAA list.&nbsp; Matthew Kearney '22 continues to improve rapidly as her returns to competition.&nbsp; The sophomore placed fourth in 8:44.74.</p>
            <p align="left">Chris Washington '20 took first place honors in the High Jump, clearing the bar at 6-8 1/4.&nbsp;Washington was over 6-10, but hit the bar on the way down to barely displace it.&nbsp; His mark is currently 17th on the NCAA list.</p>
            <p align="left">While the mark is not likely to qualify for nationals, the 4x400 moved to number nine on the national list with their 3:22.32 winning performance.&nbsp; Although Nisarg Dharia '23 and Andrew Shao '22 faced come competition, by the time Nick Duchatellier '22 and Benton Wilson '21 got the baton, the race was over.&nbsp; Dharia split a fine 51.5 on the flat track.&nbsp; An excellent hand-off to Shao kick-started a 50.6, by far the best indoor split ever for Shao.&nbsp; Duchatellier split 50.4 and Wilson a 49.7 as they brought the&nbsp;baton around in first.</p>
            <p align="left">Overall, the men won nine events. Ryan Nall '22&nbsp;stole the Weight Throw victory from teammate Albert Menio '20 on his final throw, throwing 55-7 3/4 to 55-5 1/2 for Menio.</p>
            <p align="left">On the track, prior to the 4x400, Wilson won the 200 in 22.32.&nbsp; Reed Foster '22 was forced to lead the 1000 at the start, and held it to the finish in 2:33.13.</p>
            <p align="left">Leading the way for the women were a pair of dominating performances from Katie Collins '21 and Jenna Melanson '20 in the 3K.&nbsp; Taking the lead from the start, Collins and Melanson gradually put all competitors in their rear view mirror.&nbsp; The pair came through the 1600 in&nbsp;5:14, which is close to Melanson's PR.&nbsp; The pair slowed slightly to 81 pace the&nbsp;rest of the way as Collins crossed the line first in 9:57.53 with Melanson close behind in 9:59.47.&nbsp; This moved the pair to seventh and ninth respectively on the national list.</p>
            <p align="left">Jacqueline Ahrens '21 joined teammate&nbsp;Anais Marenco '22 in clearing 11-7 3/4 in the Pole.&nbsp; Marenco won the event with Ahrens placing second.&nbsp; They are currently ranked 19th and 21st in the NCAA rankings.&nbsp; Both assume they will be over 12' soon.</p>
            <p align="left">Tema Nwana '21 continued her rapid improvement in the Weight Throw.&nbsp;&nbsp;Her second place performance of 50-8 moved her to number five on the MIT all-time list.</p>
            <p align="left">In the High Jump, Liana Reilly '21 cleared 5-4 1/2 to move to number 22 on the&nbsp;NCAA list and in a tie for fourth on the all-time MIT list with her new PR.</p>
            <p align="left">Back on the track, Margaret Trautner '20 and Marissa McPhillips '20 went 1-2 in the 1000.&nbsp; McPhillips took the lead when no one else wanted to.&nbsp; She and Trautner soon made it a two-person race.&nbsp; Trautner pulled even with one lap remaining and moved&nbsp;to the front, leading the way to&nbsp;the finish in 2:58.34 to 2:58.49.</p>
            <p align="left">Next week, MIT will split their squad with most oval track runners competing at the Boston University Valentine's Invitational, most field events athletes, sprinters and hurdlers at MIT's own Gordon&nbsp;Kelly Invitational.            </p>
