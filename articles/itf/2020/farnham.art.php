            <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">Field Event Performances Highlight Art Farnham Invitational</span>            </p>
            <p align="left">January 25, 2020</p>
            <p align="left">MIT sent a number of oval runners to the Boston University John Thomas Terrier Invitational, but most of the team competed at the home meet, the <a href="ArtFarnham.res.pdf">Art Farnham Invitational</a>. The competition was much better in the field events and MIT athletes took advantage of it.</p>
            <p align="left">In the Weight Throw, Ryan Nall '22 had another breakthrough to increase his personal best to 53-3 1/2, placing second in the event.&nbsp; Also throwing personal bests were Nathan Basinger '22 (49-0 3/4) and Henry Sobieszczyk '23 (39-5).&nbsp; Likewise, in the Women's Weight Throw, Savannah Inglin '20 threw a personal&nbsp;best 47-8&nbsp;1/2 to claim second&nbsp;place.</p>
            <p align="left">The Women's Pole Vault&nbsp; was a success for&nbsp;Jacqueline Ahrens '21 and Lila Wine '21 as both cleared 11-1 3/4 for season bests.</p>
            <p align="left">Laena Tieng '23 won the Triple Jump on her final attempt, jumping a personal best 36-2 1/4.&nbsp; Also in the jumps, Liana Reilly '21 jumped a season best 5-3 1/4 to win the High Jump.</p>
            <p align="left">On the track, Kimmy McPherson '23 ran a nice personal best of 9.55 in the 60HH to earn the&nbsp;victory.</p>
            <p align="left">Next week the Engineers will rest many of their athletes, but will send a few to the NEICAAA Championship&nbsp;at the Reggie Lewis Center.&nbsp; The rest of those competing will be at the Tufts Branwen Smith-King Invitational.</p>
