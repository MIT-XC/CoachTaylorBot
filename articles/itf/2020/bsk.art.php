            <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">MIT Men and Women Take Home Team Victories at Tufts Branwen Smith-King Invitational</span>            </p>
            <p align="left">February 1, 2020</p>
            <p align="left">The <a href="bsk.res.php">Branwen Smith-King Invitational</a> is scheduled as an invitational, so teams do not really go to this meet trying to win the team title, neither did MIT.&nbsp; However, this is a scored meet.&nbsp; Leaving home all male distance runners, and all but one female distance runner, competed freshman superstar Kenneth Wei and the top pole vaulters at another meet, as well as not doubling more than a small handful of individuals, the&nbsp;Engineers were focused on performing better than they had thus far this season.&nbsp;Nevertheless, the men won over host Tufts and the other 20 teams with 135.5 points, while the women won with 143 points.</p>
            <p align="left">To this point, MIT has fared well in competition, and displayed a competitive spirit, but the performances just were not where they should be at this&nbsp;time of the season.&nbsp; After a team meeting addressed this matter, Tech athletes came to right the ship, and they&nbsp;came through quite well.</p>
            <p align="left">Alec Reduker '20 led the way for MIT.&nbsp;&nbsp;Competing in the Long Jump for the first time this year, and the first time ever indoors, he won easily, jumping 23-02 1/2, moving him to #5 on the MIT all-time list,&nbsp;and #13 on the current NCAA Division III season ranking.&nbsp;The senior moved over to the High Jump, which had already been underway for some time, and promptly cleared 6-2 3/4.&nbsp; He then cleared 6-4 3/4, 6-6 3/4 and 6-9 to win the event over teammates Chris Washington '20 at 6-6 3/4 and Steven Marquez '23 at 6-4 3/4 as MIT swept the event.&nbsp; Already ranked #3 in the country with his 6-10 1/4, he&nbsp;moved the bar to 7-1.&nbsp; He had solid attempts, but was unable to clear the height on this day.&nbsp;</p>
            <p align="left">Reduker is also ranked at #4 in the country in the Triple Jump, which he just started this season.&nbsp; He did not compete in the Triple Jump at this meet, but Yorai Shaoul&nbsp;'21 did.&nbsp; The defending national champion only jumped in the trials, as once he reached 48-9,&nbsp;easily winning the event, the junior called it quits to further let his body recover as he gets closer to championship time.&nbsp; His 48-9 is ranked #5 in the country.</p>
            <p align="left">Ryan Nall '22 had a breakthrough day in the throwing events.&nbsp;For the second week in a row, the sophomore added ~ three feet to his personal best, throwing 57-4 3/4 to place second.&nbsp; In the Shot Put, Nall threw an indoor best of 45-7 1/4 to place fifth.&nbsp; This is still three feet south of where he threw outdoors last year, but he is closing the gap.&nbsp; Nathan Basinger '22 also had a big day in the throws, throwing two personal bests.&nbsp; In the Weight Throw he placed sixth, throwing 50-6.&nbsp; He came back in the Shot Put, earning third place with a 47-5 1/4 put.</p>
            <p align="left">Things were going well on the track as well.&nbsp; McKinley Polen '23 and Jeremiah Budiman '23 did battle in the 60 Meter Dash, teaming up to lay a solid beat down on the competition as they scored 15 points.&nbsp; In the trials, Polen got out well, running a PR of 7.07 while Budiman ran 7.13.&nbsp; Both advanced to the semis where Budiman had a strong start en route to a PR of 7.08.&nbsp; Polen had a very poor start, but still qualified for the final with his 7.15.&nbsp; In the finals, Polen again had a great start, which led to a victory in another 7.07.&nbsp; Budiman was close behind in fourth in 7.12.</p>
            <p align="left">Benton Wilson '21 had a casual day, only racing the 200 and the junior responded with the 10th best time in the country, winning with a 22.27.&nbsp;&nbsp;He was not challenged at anytime in the race and will certainly run faster when he is challenged.</p>
            <p align="left">In the 400, Nick Duchatellier '22 won with a 51.10 performance.&nbsp; This&nbsp;was the first 400 of the year for Duchatellier.&nbsp; As he gets accustomed to the event and gets challenged, his times will surely drop.&nbsp; Also performing well in the long sprints were Nisarg Dharia '23, who placed sixth in&nbsp;the 400 in 51.88, and Andrew Shao '22, who placed sixth in&nbsp;the 200 in 23.40 and eighth in the 400 in 52.22.</p>
            <p align="left">Reed Foster&nbsp;'22 returned to competition after dealing with an injury for a few weeks.&nbsp; He won the 800 off a casual first 600 followed by a 28 final 200 to pull away from the competition in 1:58.97.</p>
            <p align="left">Laena Tieng '23 led the way for the women, winning both horizontal jumps.&nbsp; The freshman won the Long Jump with a 16-11 1/4 jump, leading her&nbsp;teammates to a 1-2-4-6-8 domination of the event.&nbsp; She returned in the Triple Jump, jumping a personal best of 36-6 3/4.&nbsp; Lucy Lee '21 added a fourth place and Rikita Bansal '22 jumped an MIT best 35-1 for sixth.&nbsp; Overall, the Engineers scored 45 points in the horizontal jumps.</p>
            <p align="left">Tech also dominated in&nbsp;the Mile.&nbsp; While they lost first place, Marissa McPhillips '20, Katie Williams '21 and Margaret Trautner '20 teamed up to go 2-3-4 with very solid performances, running 5:04.89, 5:06.22 and 5:07.07 respectively.&nbsp; This moved them to 13th, 16th and 18th on the NCAA Division III ranking list. The trio, along with Skylar Brooks '20 got together to win the 4x800 Relay in 9:28.73.</p>
            <p align="left">Tema Nwana '21 had the most unlikely double.&nbsp; First she threw a personal best of 49-7 1/4 in the Weight Throw to place second and move up to #7 on the MIT all-time list.&nbsp;She then went over to the Long Jump to place sixth with a jump of 15-11 1/2.</p>
            <p align="left">Next week, the Engineers return to Tufts for the Cupid Invitational.</p>
