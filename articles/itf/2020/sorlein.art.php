            <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">MIT's Big Three Lead The Way at Sorlein Invitational</span>            </p>
            <p align="left">January 18, 2020</p>
            <p align="left">MIT Track &amp; Field dropped the long-standing Bowdoin Invite from their schedule this year, replacing it with the&nbsp;<a href="sorlein.res.php">University of Rhode Island Sorlein Invitational</a>.&nbsp; The Bowdoin&nbsp;meet was a competitive meet&nbsp;for most of the MIT competitors, but for those national level competitors on the team, an increase in the competitive level was desired.&nbsp; Competing teams were all Division I or II teams with the exception of MIT.&nbsp;In attendance were host URI, University of Connecticut, University of Massachusetts, Southern Connecticut State University, Assumption College, Quinnipiac University and Stonehill College.</p>
            <p align="left">Last spring, Yorai Shaoul '21 and Alec Reduker '20 were responsible for 30 points at the NCAA Division III Championship, earning second place as a team with the help of six points from Liam Ackerman '21 in the Pole Vault.&nbsp; Freshman Kenneth Wei '23 was added to the roster and is already doing some amazing things.&nbsp; They are thus far regarded as the Big Three.&nbsp; Regarding Reduker, Shaoul  and&nbsp;Wei, the bell was answered.&nbsp; Reduker started with a miss at 6-3 1/4, but cleared on his second attempt.&nbsp; He then made 6-5 on his first attempt.&nbsp; At 6-7 he again needed two attempts to clear, but then cleared 6-8 3/4 on his first attempt.&nbsp;He cleared 6-10 1/4 on his third attempt.&nbsp; Kevin Hack '20 of Assumption cleared on his second attempt.&nbsp; This was not a scored meet, so Reduker passed at 7-0, which Hack attempted and missed on all three attempts.&nbsp; Reduker attempted 7-1&nbsp;and was close on all three attempts, but had to settle with second place on misses and a national qualifying mark.&nbsp; He is currently ranked second in Division III.</p>
            <p align="left">Wei, undefeated in the high hurdles thus far this year, had run 8.32.&nbsp; The competition promised he would need to do better.&nbsp; Wei had the top time in the trials at 8.38.&nbsp; In the finals, Wei inched ahead through the first three hurdles.&nbsp; He then focused on sprinting instead of hurdling.&nbsp; The extra three inches of hurdle height are still a bit of an obstacle for the freshman, and he&nbsp;slightly hit the last two hurdles.&nbsp; However, his 8.22 took the victory and established yet another MIT freshman record for Wei.&nbsp; He is .02 away from the MIT varsity record for 60 Meter High Hurdles.&nbsp; The NCAA and MIT shifted from the 55 Dash and 55HH to the 60 Meter distance in 2012.&nbsp; Wei's performance is not a sure thing for NCAA qualifying although he is currently tied for eighth&nbsp;in the country for Division III.</p>
            <p align="left">Wei and Shaoul were in the Long Jump at the time of the 60HH Final.&nbsp; Wei had jumped 22-9 3/4 in the trials.&nbsp; After the hurdles, his hamstring, which had been injured earlier in the season, tightened up enough that he withdrew from the competition.&nbsp; Shaoul passed Wei, jumping 23-0 1/2 to finish second&nbsp;in the event, just ahead of Wei.&nbsp; Shaoul is ranked 10th nationally, and Wei 15th.</p>
            <p align="left">All three were entered in the Triple Jump, but with Wei's hamstring&nbsp;and Shaoul being cautious with some pain he was feeling, Reduker was the lone MIT competitor.&nbsp;As he continues to amaze his teammates and coaches, it seems there is nothing the senior cannot do.&nbsp; Last year, with no prior experience, Reduker wanted to compete in the Long Jump.&nbsp;With some reluctance, the coaches agreed and it ultimately paid off with a third place finish at the NCAA Division III Championship, finishing behind Shaoul with a jump of 24-8 1/4.&nbsp; Even before the long jump experiment, Reduker has been begging to try the Triple Jump.&nbsp; With his inexperience and&nbsp;back problems, the coaches were reluctant to acquiesce.&nbsp; However,&nbsp;when&nbsp;someone with&nbsp;the success of Reduker says they can do it, one tends to believe them.&nbsp; Having jumped 46-11 in his first effort at the MIT Intrasquad Meet back in December, it was no surprise when he jumped 48-10 1/4 to win the Triple Jump on Saturday.&nbsp;He is now ranked second in all of DIII, just ahead of Shaoul.</p>
            <p align="left">Benton Wilson '21 won the 400 earlier in the day, running a smart, well-paced race to finish in 50.93 for his first 400 of the season.&nbsp; He then came back in the 200, placing second in 22.35 on the 200 meter flat track.</p>
            <p align="left">In other strong performances for the men, Albert Menio '20 threw a personal best of 56-4 in the 35 lb. Weight Throw, placing third.&nbsp; Ellery Rajagopal '21 ran an indoor personal best of 1:58.42 in the 800.&nbsp; Lowell Hensgen '23 ran a solid 3K, placing second in 8:45.00.</p>
            <p align="left">The women had numerous strong performances.&nbsp; Marissa McPhillips '20 had the best double of the day.&nbsp;The Rhode Island native&nbsp;reacted well to competing in her home state.&nbsp; In the Mile, she moved up to the&nbsp;front pack, and moved well to counter a change of pace, coming through the half in 2:32.&nbsp; The pace slowed significantly in the third quarter, and McPhillips went for the win.&nbsp; Despite a 75 second final quarter,&nbsp;and a fine early season time of 5:07.97, she placed second, .12 out of first.&nbsp; The senior returned in the 1000.&nbsp; The first 200, led by teammate Margaret Trautner '20, was well paced at 35, but then slowed dramatically to 39 second pace.&nbsp; McPhillips allowed the pace to remain slow as they came through the 800 in 2:30.&nbsp; She kicked in with a 34 last 200 to finish in 3:04.80, but again it was just short of winning, this time by .75.</p>
            <p align="left">Anais Marenco '22 finished fourth in the Pole Vault with a season best vault of 11-7 3/4.&nbsp; Lily Papalia '22 threw a personal best in the Shot Put, placing second in her flight with a put of 37-11 1/2.&nbsp; Savannah Inglin '20 threw a personal best of 46-6 1/4 in the Weight Throw.&nbsp; Season bests were had by Yilinn Yang '22 (16-10 1/2) and Laena Tieng '23 (16-8) in the Long Jump. &nbsp;Lucy Lee had a season best in the Triple Jump, placing sixth, jumping 36-2 3/4.</p>
            <p align="left">The lone victory was earned by Katie Collins '21 in the 3K.&nbsp; Being the top seed, her and teammate Jenna Melanson '20 took the pace out pretty hard, clicking off 79 second 400's.&nbsp; The field dropped back immediately, conceding the victory.&nbsp; Collins came through the 1600 in 5:18.&nbsp; Melanson was a little gun shy by the pace and fell off just before the 1600, coming through in 5:24.&nbsp; Not challenged, Collins lost some focus and motivation, slowing a little to 82 second pace, finishing in 10:06.12.&nbsp; Melanson earned second in 10:27.36.</p>
            <p align="left">Next week, MIT will split their squad, sending some to the Boston University Terrier Classic on Friday and Saturday, and others to their own meet on Saturday, the Art Farnham Invitational.</p>
