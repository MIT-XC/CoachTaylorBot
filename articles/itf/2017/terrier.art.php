            <p style="font-size: 12px"><span style="font-size: 16px"><strong>Women Rock the NCAA Division III National Rankings</strong></span></p>
            <p align="left">January 27-28, 2017</p>
            <p align="left">While MIT's Women's Track &amp; Field team sent a select few over to the <a href="terrier.res.php">Boston University John Thomas Terrier Classic</a>, those individuals threw down some impressive performances that fired a warning shot to the rest of Division III. Although  six weeks remains until Nationals and everyone's best performances are still to come, this weekend's performances are an excellent starting point.</p>
            <p align="left">Bailey Tregoning '19 established the leading time in the 800 for NCAA Division III by running 2:12.08.&nbsp; There was obviously more in the tank as it was clear she was thinking she took the lead too soon after&nbsp;a&nbsp;1:36 600 split.&nbsp; Running sub 2:10 is certainly a possibility&nbsp;this season.&nbsp; Also in that event, but a different section was Marissa McPhillips&nbsp;'20.&nbsp; The freshman ran a superb race, clocking 2:15.31 as she nearly even split the quarters.</p>
            <p align="left">Although&nbsp;not an NCAA Division III Championship event, Katie Bacher '20 showed her talent as she ran&nbsp;a freshman and varsity record in  2:55.01 for the 1000 Meters.&nbsp; After coming through the 400 in 68.5, Bacher slowed a bit to hit the half in 2:19.9, but then finished in 35 for her record setting performance.</p>
            <p align="left">Maryann Gong '17 was in a very fast section of the Mile and it had her a bit nervous.&nbsp; She was reacting to too many moves and not owning the race, but still ran an impressive 4:53.67, the second fastest time in all of Division III thus far in the indoor season.&nbsp;The senior has much improvement ahead of her as she learns to relax during the race.</p>
            <p align="left">Hannah Chen '18 established season bests in both the 200 and 400, running 25.96 and 58.16.</p>
            <p align="left">Ariela Slutsky '18 threw a PR in the Weight Throw, establishing the 11th best throw in the country at 55-06 1/4</p>
            <p align="left">On the next morning, the quartet&nbsp;of McPhillips, Chen, Tregoning and Gong&nbsp;were aiming to run a solid national qualifying time and possibly break the MIT record of 11:34.20.&nbsp; McPhillips led off with the 1200 leg.&nbsp;Unfortunately, the Engineers were the top seed on the inside&nbsp;alley with the top team, Boston College on the outside alley.&nbsp; By the time it was clear the inside alley was going out too slow, it was too late.&nbsp; McPhillips split a reasonable, but slower than hoped for, 35.6.&nbsp; She stayed with&nbsp;the pack through another 200 for a 70.1 400.&nbsp; She took the lead on her pack, but was too far behind BC to set her sights on them, basically running by herself for the remainder of the leg.&nbsp; She split 3:36.8, which was solid, but at least two seconds slower than she wanted.&nbsp; Chen ran a very smooth 400, but also had no one to chase or run with.&nbsp; Her 57.5 kept Tech on pace to qualify but the record was slipping away. Tregoning went out great in 31.8 and 64.9.&nbsp; Although she was not real fatigued she simply did not know how hard she could push as she is still a relative novice to this level of racing.&nbsp; Her final 400 was&nbsp; a 69.6 for a 2:14.5 overall split.&nbsp; Gong took the baton and truly went after it.&nbsp; Her first 400 was a 71.7 and exactly what she wanted.&nbsp; However she slowed without&nbsp;really being aware of it, running 73.9 and 74.9.&nbsp; Fortunately she was coming up fast on BC at that point, but could still only manage another 74.9 to give MIT a second place 11:45.94.&nbsp; That time is now the top time in Division III and has always qualified for the National Championship.&nbsp; The Engineers will probably run it one more time prior to Nationals.</p>
            <p align="left">The men's meet, at least for MIT started with the 800.&nbsp; Both Nathan Munet '19 and Tyndale Hannan '20 were in the same section.&nbsp; Both resisted the urge to get pulled out with the leaders, who were running 53-54 pace.&nbsp; Hannan came through in 56.9, but had already disengaged from the race, running the third 200 in 30.2.&nbsp; Munet remained engaged, coming through in 56.0 and 1:25.2.&nbsp; Munet moved up well in the final 200 to finish in 1:54.60.&nbsp; Hannan actually was able to re-engage, finishing well to clock a 1:55.07.</p>
            <p align="left">Danny Newman '17 and Tom Frejowski '19 were both in the 400 and both ran season bests, with Frejowski running an indoor PR.&nbsp; Newman split 23.9 en route to his 50.65.&nbsp; Frejowski also split 23.9 and closed well after settling on the backstretch to finish in 51.11.</p>
            <p align="left">In the Mile Run, Tech was represented by Aidan Gilson '19.&nbsp; After four runners broke 4:00 in the fast section and the following sections all went out fast and finished under 4:10, there was hope the same would happen in Gilson's section.&nbsp; However, the 2:08 first 800 had people tripping over each other's feet and elbowing the entire time.&nbsp; All the contact took it out of Gilson, who nearly landed on his face after being tripped.&nbsp; His final time was 4:20.31</p>
            <p align="left">The final event for MIT was the 4x400 Relay.&nbsp;&nbsp;There was not much hope of qualifying for Nationals while missing two of the key legs, but qualifying for the fast section of the New England Division III Championship was a goal.&nbsp; Frejowski had a poor start and fell out of the race in the first turn.&nbsp; By the time he handed off at 52.3 he was 30 meters behind.&nbsp; Hannan ran an excellent leg without competition, splitting 49.9, but not making up any ground.&nbsp; Munet, despite still being sick, delivered a solid 50.6&nbsp;as he handed off to Newman.&nbsp; Newman just did not get out well, splitting 24.5 at the 200 and finishing with a 53.1.&nbsp; The overall time was 3:26.13.&nbsp; Job not accomplished.</p>
            <p align="left">Next week, MIT will compete most of their team at Tufts for their Cupid Challenge Invitational.            </p>
