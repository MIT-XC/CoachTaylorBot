            <p style="font-size: 12px"><span style="font-size: 16px; font-weight: bold">Vaulters Rule The Day at Tufts Final Qualifier</span></p>
            <p align="left">March 4, 2017</p>
            <p align="left">At the <a href="finalqualifier.php">Final Qualifier hosted by Tufts University</a>, a number of colleges from New England and as far away as Georgia and Ohio&nbsp;competed for their final opportunity to improve their chances of being among the top 15 men or top 17 women in all of Division III, the requirement for selection to the NCAA Division III Indoor Track &amp; Field Championship, held at North Central College in Naperville, Illinois on March 10-11.</p>
            <p align="left">MIT held out their athletes who were solid locks on making the field, but still competed a number of student-athletes.&nbsp; Among those competing for the Engineers were seven pole vaulters; five women&nbsp;and two men.&nbsp; Of the seven, three had personal bests and another a season best.&nbsp; Leading the&nbsp;way was Kari Stromhaug '19, a freshman eligibility wise as she was dealing with an injury from high school all through her freshman year.&nbsp; Stromhaug won the women's vault with the ninth best vault in the country for Division III, 12-5 1/4.&nbsp; Kaitlyn Sparks '19, broke her own freshman record by clearing 12-2 1/2, good for fourth place.&nbsp; Sparks currently sits at 17th on the NCAA list, which will get her into the championship field if no one passes her later tonight as this is the final day qualifying marks will be accepted.&nbsp; Setting a season best was Jen Tylock '17, clearing 11-8 1/2.&nbsp; Scott Cameron '19 cleared 15-9 1/4 to place second in setting his own personal best.&nbsp; Cameron is 21st on the NCAA list as it took 16-0 1/4 to make the field this year.</p>
            <p align="left">Ariela Slutsky '18 set a personal best in the Weight Throw, winning the event with a mark of 56-3 3/4.&nbsp; That mark puts the junior at 19th on the NCAA list, just missing the championship field unless at least two decide not to enter for some reason.</p>
            <p align="left">Haley Strouf '18 went from 10th the week before to 19th by the end of the day.&nbsp; She tried to improve her mark of 18-6 1/2, but was unable to do so.&nbsp; Like Slutsky, she will have to hope at least two individuals do not enter.</p>
            <p align="left">Nneoma Okonkwo '18, already in the Long Jump field as the number two seed at 19-4 1/4, tried to improve her chances in the 60 Meter Dash.&nbsp; She improved her second best performance to a 7.82, but remained in 20th position overall.</p>
            <p align="left">MIT has two injured athletes who will not be going to the National Championship, number three seed in the Triple Jump, Arinze Okeke '17 who suffered a broken fibula back in January, and Tre Livingston '18, the number four seed in the 200 who injured his hamstring two weeks before.&nbsp; The men will be represented by Dennis Maloney '19, the number three seed in both the 3K and 5K, Matt Deyo 'G the 12 seed in the 5K and 14 seed in the 3K, and Billy Ruschel '18, the 10 seed in the Triple Jump.</p>
            <p align="left">In addition to Okonkwo in the Long Jump and Stromhaug and Sparks in the Pole Vault, the women will be represented by Maryann Gong '17, the two seed in the Mile, the five seed in the 3K and part of the 4th seeded DMR.&nbsp; Bailey Tregoning '19 is the three seed in the 800 and part of the DMR.&nbsp; </p>
            <p align="left">The final event lists will be posted on Monday&nbsp;and the Engineers hope to get a couple&nbsp;of surprises in Strouf and Slutsky.</p>
