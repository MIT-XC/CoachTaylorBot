            <p style="font-size: 12px"><span style="font-size: 16px"><strong>Four Freshman Records Lead Engineers at Tufts Cupid Challenge</strong></span></p>
            <p align="left">February 4, 2017</p>
            <p align="left">While the premier event of the <a href="tuftscupid.res.php">Tufts Cupid Challenge</a> was undoubtedly the men's 5K, where the top two&nbsp;finishers, Tim Nichols '17 of Tufts and Dennis Maloney '19 of MIT took over the top two marks in the country for Division III with their 14:19.45 and 14:26.80 respectively, the overall leading theme of the day was the four freshman records produced by the rookie Engineers.&nbsp; Tyndale Hannan '20 won the 600 in 1:22.66 for the first record of the day.&nbsp; Immediately afterwards, Skylar Brooks '20 ran 1:40.70 to set the women's rookie 600 record.&nbsp; In the next event, Michelle Menkiti '20 broke the rookie 60 Meter Dash record by running an 8.04.&nbsp; At the end of the meet, the quartet of Jane Reed '20, Katie Bacher '20, Brooks and Marissa McPhillips '20, all doubling back, ran 4:07.90 to blow away the freshman record.</p>
            <p align="left">The non-scoring invitational allowed MIT to focus on marks rather than the victory.&nbsp; Two weeks from the start of the Championship season Tech knew there was urgency in getting top performances in order to either qualify or put themselves in position to be in the seeded section of their event.&nbsp; While Maloney almost certainly qualified for the NCAA Division III Championship in March, teammates David Walter '18 (15:18.66), Ben Freed '18 (15:21.66), and Daniel Weiss '19 (15:23.50) all qualified for the New England Division III Championship in the 5K.</p>
            <p align="left">Aidan Gilson '19 had his best day of the season as he improved his Mile time to 4:20.06 and then came back with a strong 1000, running 2:32.18.</p>
            <p align="left">Tom Frejowski '19 had a solid day, but still has more as his last 50 meters in the 400 proved.&nbsp; The sophomore came from behind to run 51.53 for an indoor PR, just beating teammate Danny Newman '17, who clocked 51.63.&nbsp; Ken Acquah '20 is also starting the come around&nbsp;as the walk-on sprinter ran 53.59 and then came back with a fine 23.69 in the 200.&nbsp; Newman ran a season best 23.36 in the 200 and Frejowski finished in 23.59.&nbsp; None are&nbsp;a threat to score in the NED3 Championship at this point, but the hope is that one or more will find it in the final week of competition leading into the championship.</p>
            <p align="left">Returning from a two month injury, Simon Alford '20 ran an inspired 1:58.52 for his first race back.&nbsp; Joe Faraguna '20 also made his debut, running 2:02.79.&nbsp; There is hope the duo, along with Ian McNally '20, can come back and make an impact in the Championships.</p>
            <p align="left">In the field events, Raja Rajcic '20 jumped a personal best in the Pole Vault, clearing 14-5 1/4 to finish just behind teammates Scott Cameron '19 (14-11) and Bobby Upton '20 (14-11).&nbsp; Cody Bratten '18 continues to improve from his early season elbow surgery, throwing 45-8 1/2 to win the Shot Put.&nbsp; Steven Speck '20 jumped a season best and collegiate PR of 6-4 in the High Jump, finishing behind event winner and teammate Alec Reduker '20 at 6-8.</p>
            <p align="left">The women's 5K was supposed to be equal to the men's as a premier event, but Megan McCandless '20 having to pull out after an 11:01 3200 split due to a plantar fasciitis flareup and Mary Eccles '18 falling off the 17:20 pace early on,  turned the race into a NED3 qualifying event for MIT.&nbsp; Eccles ran 17:59.46 and was followed by Leandra Zimmermann&nbsp;'19 in a PR of 18:08.21.&nbsp; An inspired victory in the slow&nbsp;section by Bailee Margolis '19 in 18:11.37 led her teammates to qualifying marks; Jenna Melanson '20 (18:43.67); Ciera Gordon '18 (18:44.36); and Charlotte Folinus '20 (18:45.54).</p>
            <p align="left">There were many excellent performances by the women on the track.&nbsp; Hannah Chen '18 is starting to find her groove in the 60HH as she ran 9.26 in the final to win the event.&nbsp; Menkiti returned from the 60 to blast an indoor PR of 26.48 in the 200.&nbsp; Jane Reed '20 ran an indoor PR of 61.43 as she continues to improve almost every week.&nbsp; Overshadowing Brooks's freshman record in the 600 was the overwhelming victory by Bailey Tregoning '19 as she ran 1:36.37.&nbsp; In the 1000, Marissa McPhillips '20 hammered out a vicious pace on the flat track, splitting 68 and 2:20, but could not hold it, slowing to a 2:58.82, still a PR.&nbsp; However, Maryann Gong '17 was right with McPhillips the entire way, but had a strong finish to win in a PR of 2:55.87.&nbsp;&nbsp;Prior to the 4x400, Bacher was in the Mile Run.&nbsp;&nbsp;Fresh off her 2:55 MIT record from last week, Bacher showed her confidence as she eased up past the leaders and pushed the final 600 in the lead to win in 5:00.76, currently the fifth fastest in Division III.&nbsp; Clemmie Mitchell '18 ran a well-paced, negative split 3K to win in 10:27.93.</p>
            <p align="left">In the field events, Ariela Slutsky '18 showed she is getting ready for the Championships.&nbsp; For the second week in succession,&nbsp;she threw a personal best in the Weight Throw. Her 56-00 victory is the 14th best throw in all of Division III.&nbsp; The junior then turned around to throw another PR, 39-10 1/2 in the Shot Put.&nbsp; Kathleen Brandes '19 won the Pole Vault in a PR of 11-5 3/4.&nbsp; Matching their season best, also clearing 11-5 3/4 were Kari Stromhaug '19 and Kaitlyn Sparks '20.</p>
            <p align="left">Next week, MIT will split their squad, sending the top oval runners and a few field athletes to the Boston University David Hemery Valentines Invitational and the rest of their squad to the MIT Gordon Kelly Invitational.</p>
